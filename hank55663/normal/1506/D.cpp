using namespace std;
void solve(){
    int n;
    scanf("%d",&n);
    map<int,int> m;
    for(int i = 0;i<n;i++){
        int x;
        scanf("%d",&x);
        m[x]++;
    }
    int ans=0;
    if(n&1)ans++;
    for(auto it:m){
        ans=max(ans,n-(n-it.y)*2);
    }
    printf("%d\n",ans);
}
int main(){
    int t=1;
    scanf("%d",&t);
    while(t--){
        solve();
    }
    //printf("%.12f\n",sum/tt);
    return 0;
}
