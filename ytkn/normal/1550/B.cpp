using namespace std;
typedef long long ll;

void solve(){
    int n, a, b; cin >> n >> a >> b;
    string s; cin >> s;
    if(b >= 0){
        cout << a*n+b*n << endl;
    }else{
        int m = 0;
        for(int i = 1; i < n; i++){
            if(s[i] != s[i-1]) m++;
        }
        cout << a*n+b*((m+3)/2) << endl;
    }
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}