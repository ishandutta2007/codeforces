int p, k;
int main(int argc, char const *argv[]) {
  std::ios_base::sync_with_stdio(false);
  std::cin.tie(nullptr), std::cout.tie(nullptr);
  std::cin >> p >> k;
  if (!k) {
    std::cout << Ksm(p, p - 1);
  } else if (k == 1) {
    std::cout << Ksm(p, p);
  } else {
    int now = k, cnt = 1;
    while (now != 1) cnt++, now = now * static_cast<long long>(k) % p;
    std::cout << Ksm(p, (p - 1) / cnt);
  }
  return 0;
}