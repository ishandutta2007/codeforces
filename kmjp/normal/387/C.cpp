int L;
string P;

void solve() {
    int f,i,j,k,l,x,y,r=0;
    
    cin>>P;
    
    y=L=P.size();
    r=1;
    while(y>=0) {
        x=y-1;
        while(P[x]=='0') x--;
        if(x<0) break;
        i=x-(y-x);
        if(i<0) break;
        if(i==0 && P.substr(0,x)<P.substr(x,x)) break;
        r++;
        y=x;
    }
    _P("%d\n",r);
    
}


