#include <bits/stdc++.h>

using namespace std;

int a[26];

int main (){
    ios_base::sync_with_stdio(false);
    int n;
    cin >> n;
    string s;
    cin >> s;
    for (int i = 0; i < s.size(); i++){
        if (s[i] - 'A' <= 26)
            s[i] = s[i] - 'A' + 'a';
    }
    for (int i = 0; i < s.size(); i++){
        int x = s[i] - 'a';
        a[x]++;
    }
    for (int i = 0; i < 26; i++){
        if (a[i] == 0)
            return cout << "NO", 0;
    }
    cout << "YES";
}