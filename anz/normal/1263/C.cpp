ll gcd(ll a, ll b) { for (; b; a %= b, swap(a, b)); return a; }

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0), cout.tie(0);

	int t; cin >> t;
	while (t--)
	{
		int n; cin >> n;
		set <int> ans;
		ans.insert(0);
		for (int i = 1; i*i <= n; i++)
		{
			ans.insert(i);
			ans.insert(n / i);
		}

		cout << ans.size() << '\n';
		for (auto it : ans)
			cout << it << ' ';
		cout << '\n';
	}
}