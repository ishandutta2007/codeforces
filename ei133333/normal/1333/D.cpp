template< typename T, typename V >
typename enable_if< is_class< T >::value == 0 >::type fill_v(T &t, const V &v) {
  t = v;
}

template< typename T, typename V >
typename enable_if< is_class< T >::value != 0 >::type fill_v(T &t, const V &v) {
  for(auto &e : t) fill_v(e, v);
}

template< typename F >
struct FixPoint : F {
  FixPoint(F &&f) : F(forward< F >(f)) {}

  template< typename... Args >
  decltype(auto) operator()(Args &&... args) const {
    return F::operator()(*this, forward< Args >(args)...);
  }
};

template< typename F >
inline decltype(auto) MFP(F &&f) {
  return FixPoint< F >{forward< F >(f)};
}


int main() {
  int N, K;
  cin >> N >> K;
  string S;
  cin >> S;
  bool update = true;
  vector< vector< int > > tap;
  int add = 0;
  while(update) {
    update = false;
    vector< int > nxt;
    for(int k = 1; k < N; k++) {
      if(S[k - 1] == 'R' && S[k] == 'L') {
        S[k - 1] = 'L';
        S[k] = 'R';
        nxt.emplace_back(k);
        ++k;
        ++add;
      }
    }
    if(nxt.size()) {
      update = true;
      tap.emplace_back(nxt);
    }
  }
  if(K < tap.size() || add < K) {
    cout << -1 << endl;
    return 0;
  }
  int more = K - (int) tap.size();
  for(auto &p : tap) {
    while(more > 0 && p.size() >= 2) {
      cout << 1 << " " << p.back() << "\n";
      p.pop_back();
      --more;
    }
    cout << p.size() << " " << p << "\n";
  }
}