#define pb push_back
#define fox(i,x,y) for(int i=(x);i<=(y);i++)
#define foxr(i,x,y) for(int i=(y);i>=(x);i--)

const int MN = 1e5+5;
int n, i, deg[MN], val[MN];
vector<pii> ans; queue<int> q;

int main(){
    for(scanf("%d",&n),i=0;i<n;i++){
        scanf("%d%d",&deg[i],&val[i]);
        if(deg[i]==1) q.push(i);
    }
    while(q.size()){
        int x=q.front(); q.pop();
        if(deg[x]!=1) continue;
        ans.pb({x,val[x]});
        val[val[x]]^=x;
        if((--deg[val[x]])==1) q.push(val[x]);
    }
    printf("%d\n",ans.size());
    for(auto v : ans)
        printf("%d %d\n",v.first,v.second);
    return 0;
}