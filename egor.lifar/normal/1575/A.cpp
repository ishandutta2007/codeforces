const int MAXN = 10002228;


string s[MAXN];
int order[MAXN];


bool comp(int a, int b) {
    return s[a] < s[b];
}


void solve() {
    int n, m;
    cin >> n >> m;
    for (int i = 0; i < n; i++) {
        cin >> s[i];
        for (int j = 1; j < sz(s[i]); j += 2) {
            s[i][j] = 25 - (s[i][j] - 'A') + 'A';
        }
        order[i] = i;
    }
    sort(order, order + n, comp);
    for (int i = 0; i < n; i++) {
        cout << order[i] + 1 << ' ';
    }
    cout << '\n';
}


