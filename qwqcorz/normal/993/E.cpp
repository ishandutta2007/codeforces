const int N=1<<20;
const double pi=acos(-1);
const double eps=0.1;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
struct Complex
{
	double x,y;
}f[N],g[N];
Complex operator +(Complex a,Complex b){return (Complex){a.x+b.x,a.y+b.y};}
Complex operator -(Complex a,Complex b){return (Complex){a.x-b.x,a.y-b.y};}
Complex operator *(Complex a,Complex b){return (Complex){a.x*b.x-a.y*b.y,a.x*b.y+a.y*b.x};}
int lim,l,rev[N];
void FFT(Complex *a,int type)
{
	for (int i=0;i<lim;i++) if (i<rev[i]) swap(a[i],a[rev[i]]);
	for (int mid=1;mid<lim;mid*=2)
	{
		Complex Wn=(Complex){cos(pi/mid),type*sin(pi/mid)};
		for (int k=0,len=mid*2;k<lim;k+=len)
		{
			Complex w=(Complex){1,0};
			for (int i=k;i<k+mid;i++,w=w*Wn)
			{
				Complex x=a[i],y=w*a[i+mid];
				a[i]=x+y;
				a[i+mid]=x-y;
			}
		}
	}
	if (type<0)
	for (int i=0;i<lim;i++)
	a[i].x/=lim,a[i].y/=lim;
}
void conv(Complex *a,Complex *b,int n)
{
	lim=1,l=0;
	while (lim<=n*2) lim*=2,l++;
	for (int i=0;i<lim;i++) rev[i]=(rev[i>>1]>>1)|((i&1)<<(l-1));
	FFT(a,1);
	FFT(b,1);
	for (int i=0;i<lim;i++) a[i]=a[i]*b[i];
	FFT(a,-1); 
}

