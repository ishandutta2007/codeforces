using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
mt19937 rnd(time(0));
 
 
 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	ll s = accumulate(all(a), 0LL);
	ll RES = -1;
	auto solve = [&](ll d){
		ll res = 0;
		pil next{-1, -1};
		vector<pil> t;
		t.reserve(n);
		for(int i = 0; i < n; ){
			int j = i, p = -1;
			ll sum = 0;
			if(next.first != -1){
				t.push_back(next);
				if(next.second >= (d + 1) / 2){
					p = next.first;
				}
				sum = next.second;
				next = {-1, -1};
			}
			while(sum < d && j < n){
				sum += a[j];
				if(sum > d){
					next = {j, sum % d};
					if(p == -1){
						p = j;
					}
					t.push_back({j, a[j] - (sum - d)});
				}
				else{
					if(p == -1 && sum >= (d + 1) / 2){
						p = j;
					}
					t.push_back({j, a[j]});
				}
				j ++;
			}
			for(int k = 0; k < t.size(); k ++){
				res += abs(t[k].first - p) * t[k].second;
			}
			i = j;
			while(!t.empty()){
				t.pop_back();
			}
		}
		if(RES == -1 || RES > res){
			RES = res;
		}
	};
	if(s > 1){
		solve(s);
	}
	for(ll p = 2; p * p <= s; p ++){
		if(s % p == 0){
			solve(p);
			while(s % p == 0){
				s /= p;
			}
		}
	}
	if(s > 1){
		solve(s);
	}
	cout << RES;
	return 0;
}
