const int INF = 1e9 + 5;

void run_case() {
    int N;
    cin >> N;
    vector<int> A(N + 2, INF);

    for (int i = 1; i <= N; i++)
        cin >> A[i];

    int maximum = *max_element(A.begin() + 1, A.begin() + N + 1);
    int dominant = -1;

    for (int i = 1; i <= N; i++)
        if (A[i] == maximum && min(A[i - 1], A[i + 1]) < A[i]) {
            dominant = i;
            break;
        }

    cout << dominant << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}