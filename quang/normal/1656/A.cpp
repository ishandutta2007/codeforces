void Solve(int test) {
  int n;
  cin >> n;
  vector<int> a(n);
  for (auto &u : a) {
    cin >> u;
  }  
  int min_val = min_element(a.begin(), a.end()) - a.begin();
  int max_val = max_element(a.begin(), a.end()) - a.begin();
  cout << min_val + 1 << ' ' << max_val + 1 << '\n';
}

int32_t main() {
  ios_base::sync_with_stdio(0); cin.tie(0);
  int tc;
  cin >> tc;
  for (int test = 1; test <= tc; ++test) Solve(test);
  return 0;
}