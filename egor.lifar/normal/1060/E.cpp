void dfs1(int u) {
    if (deep[u] & 1) {
        s++;
    }
    long long cursum = 0;
    for (auto x: g[u]) {
        deep[x] = deep[u] + 1;
        dfs1(x);
        sz[u] += sz[x];
    }
    sz[u]++;
    ans += 1LL * sz[u] * (n - sz[u]);
}

 
