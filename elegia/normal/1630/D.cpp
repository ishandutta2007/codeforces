template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

const int _ = 1000005;

int a[_];

int Gcd(int a, int b) { return b ? Gcd(b, a % b) : a; }

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int T; cin >> T;
  while (T--) {
    int N, M; cin >> N >> M;
    int g = 0;
    for (int i = 1; i <= N; ++i) cin >> a[i];
    while (M--) {
      int b; cin >> b;
      g = Gcd(g, b);
    }
    auto solve = [&]() {
      ll ret = 0;
      for (int i = 1; i <= g; ++i) {
        int par = 0, myn = 1e9;
        for (int j = i; j <= N; j += g) {
          if (a[j] >= 0) {
            myn = min(myn, a[j]);
            ret += a[j];
          } else {
            ret -= a[j];
            myn = min(myn, -a[j]);
            par ^= 1;
          }
        }
        if (par) ret -= myn * 2;
      }
      return ret;
    };
    ll ans = solve();
    for (int i = 1; i <= g; ++i) a[i] *= -1;
    ans = max(ans, solve());
    cout << ans << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}