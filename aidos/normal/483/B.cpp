ll c1, c2, x, y;

ll get(ll x, ll d) {
	//res - res/d >= x
	ll res = (x * d + 1)/d;
	while(res - res/d<x) res += x-(res-res/d);
	return res;
}
void solve(){
	cin >> c1 >> c2 >> x >> y;
	cout<<max(max(get(c1,x), get(c2, y)), get(c1+c2, x*y));
}
