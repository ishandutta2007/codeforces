#include <bits/stdc++.h>

struct edge {
	int u, v, w;
};

class DSU {
	std::vector<int> fa;

public:
	DSU(int n) : fa(n) {
		for (int i = 0; i < n; ++i) {
			fa[i] = i;
		}
	}

	int find(int x) {
		return fa[x] == x ? x : (fa[x] = find(fa[x]));
	}

	bool merge(int x, int y) {
		x = find(x), y = find(y);
		if (x == y) {
			return false;
		}
		fa[y] = x;
		return true;
	}
};

class tree {
	static int get_log(int n) {
		int res = 0;
		while ((1 << res) < n) {
			++res;
		}
		return res;
	}

	int n, LG;
	std::vector<std::vector<std::tuple<int, int, int>>> E;
	std::vector<int> dep, fid;
	std::vector<std::vector<int>> fa;
	std::vector<std::vector<int>> fw;
	DSU D;
	std::vector<int> ans;

	void dfs(int u) {
		for (int i = 1; i < LG && fa[u][i - 1] != -1; ++i) {
			fa[u][i] = fa[fa[u][i - 1]][i - 1];
			fw[u][i] = std::max(fw[u][i - 1], fw[fa[u][i - 1]][i - 1]);
		}
		for (auto [v, w, id] : E[u]) {
			if (v != fa[u][0]) {
				fid[v] = id;
				dep[v] = dep[u] + 1;
				fa[v][0] = u;
				fw[v][0] = w;
				dfs(v);
			}
		}
	}

public:
	tree(int _n, int m) : n(_n), LG(get_log(n)), E(n), dep(n), fid(n, -1),
	                      fa(n, std::vector<int>(LG, -1)), fw(n, std::vector<int>(LG)),
						  D(n), ans(m, -1) {}

	void add_edge(int u, int v, int w, int id) {
		E[u].emplace_back(v, w, id);
		E[v].emplace_back(u, w, id);
	}

	void build() {
		dfs(0);
	}

	int work(int u, int v, int w) {
		int x = u, y = v;
		if (dep[u] < dep[v]) {
			std::swap(u, v);
		}
		int mx = 0;
		for (int i = LG - 1; i >= 0; --i) {
			if (fa[u][i] != -1 && dep[fa[u][i]] >= dep[v]) {
				mx = std::max(mx, fw[u][i]);
				u = fa[u][i];
			}
		}
		if (u != v) {
			for (int i = LG - 1; i >= 0; --i) {
				if (fa[u][i] != fa[v][i]) {
					mx = std::max(mx, std::max(fw[u][i], fw[v][i]));
					u = fa[u][i];
					v = fa[v][i];
				}
			}
			mx = std::max(mx, std::max(fw[u][0], fw[v][0]));
			u = fa[u][0];
			v = fa[v][0];
		}
		auto cover = [&](int x) {
			while (dep[D.find(x)] > dep[u]) {
				x = D.find(x);
				ans[fid[x]] = w;
				D.merge(fa[x][0], x);
			}
		};
		cover(x);
		cover(y);
		return mx;
	}

	int get_ans(int id) {
		return ans[id];
	}
};

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int n, m;
	std::cin >> n >> m;
	std::vector<edge> E(m);
	for (int i = 0; i < m; ++i) {
		std::cin >> E[i].u >> E[i].v >> E[i].w;
		--E[i].u, --E[i].v;
	}
	std::vector<int> id(m);
	for (int i = 0; i < m; ++i) {
		id[i] = i;
	}
	std::sort(id.begin(), id.end(), [&](int i, int j) {
		return E[i].w < E[j].w;
	});

	DSU D(n);
	tree T(n, m);
	std::vector<bool> on_tree(m);
	for (int i : id) {
		if (D.merge(E[i].u, E[i].v)) {
			on_tree[i] = true;
			T.add_edge(E[i].u, E[i].v, E[i].w, i);
		} else {
			on_tree[i] = false;
		}
	}
	T.build();
	std::vector<int> ans(m, -1);
	for (int i : id) {
		if (!on_tree[i]) {
			ans[i] = T.work(E[i].u, E[i].v, E[i].w);
		}
	}
	for (int i : id) {
		if (ans[i] == -1) {
			ans[i] = T.get_ans(i);
		}
	}
	ans.resize(1);
	for (int x : ans) {
		std::cout << (x == -1 ? 1000000000 : x) << "\n";
	}
}