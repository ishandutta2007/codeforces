mt19937 rnd(chrono::steady_clock::now().time_since_epoch().count());



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, m;
	cin >> n >> m;
	vector<pii> a(n);
	cin >> a;
	for(auto &[l, r]: a){
		l --;
	}
	sort(all(a), [](pii x, pii y){return x.second > y.second;});
	if(a[0].second <= 0){
		cout << 0;
		return 0;
	}
	map<int, vi> t;
	for(auto [l, r]: a){
		t[l].push_back(r);
	}
	int L = 0;
	for(auto &[x, y]: t){
		L = max(L, (int)y.size());
	}
	vl sum(m);
	ll res = 0, cur = 0;
	for(int l = 0; l < L && !t.empty(); l ++){
		vi er;
		for(auto p: t){
			if(p.second.size() <= l){
				cur -= sum[p.first];
				er.push_back(p.first);
				continue;
			}
			sum[p.first] += p.second[l];
			cur += p.second[l];
			if(sum[p.first] <= 0){
				cur -= sum[p.first];
				er.push_back(p.first);
			}
		}
		for(auto k: er){
			t.erase(k);
		}
		res = max(res, cur);
	}
	cout << res;
	return 0;
}
