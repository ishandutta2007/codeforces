const int MN = 1e5 + 1;

struct Group{
    ll tot = 0, cnt = 0;
    void ins(ll x){ tot += x, cnt++; }
    void rem(ll x){ tot -= x, cnt--; }
    ll val(){ return (ll)(1.0 * tot / cnt + 1 - 1e-9); }
} group[MN];
vector<int> b[MN];

