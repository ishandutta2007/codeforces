ll a[300];
map<ll, ll> mp;
int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	ll n;
	cin >> n;
	ll mx = 1;
	for(int i = 0; i< n; i++){
		 cin>> a[i];
		 mp[a[i]] ++;
		 mx = max(mx, a[i]);
	}
	cout << mx << " ";
	for(int i = 1; i <= mx; i++){
		if(mx %i == 0) mp[i] --;
	}
	mx = 1;
	for(auto x : mp) if(x.S > 0) mx = max(mx, x.F);
	cout << mx;
	return 0;
}


