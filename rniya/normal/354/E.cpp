const int MAX_LOG = 19;

void solve() {
    ll n;
    cin >> n;

    vector<vector<pair<int, int>>> dp(MAX_LOG + 1, vector<pair<int, int>>(5, {-1, -1}));
    dp[0][0] = {0, 0};
    ll m = n;
    for (int i = 0; i < MAX_LOG; i++, m /= 10) {
        int d = m % 10;
        for (int j = 0; j < 5; j++) {
            if (dp[i][j] == make_pair(-1, -1)) continue;
            int target = (d + 10 - j) % 10;
            for (int& x : cand[target]) {
                dp[i + 1][(j + x) / 10] = {j, x};
            }
        }
    }
    if (dp[MAX_LOG][0] == make_pair(-1, -1)) {
        cout << -1 << '\n';
        return;
    }
    vector<ll> ans(6, 0);
    for (int i = MAX_LOG - 1, j = 0; i >= 0; i--) {
        auto p = dp[i + 1][j];
        int x = idx[p.second];
        // debug(x, digits[x]);
        for (int k = 0; k < 6; k++) ans[k] = ans[k] * 10 + digits[x][k];
        j = p.first;
        // debug(p);
    }

    cout << ans << '\n';
}

