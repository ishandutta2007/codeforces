    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        long inf = (long) 1e10;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }


        public void solve() {
            char[] data = new char[200000];
            int len = io.readString(data, 0);

            char[] words = new char[200000];

            ACAutomaton l2r = new ACAutomaton();
            ACAutomaton r2l = new ACAutomaton();

            int n = io.readInt();

            for (int i = 0; i < n; i++) {
                int cnt = io.readString(words, 0);
                l2r.beginBuilding();
                r2l.beginBuilding();
                for (int j = 0, k = cnt - 1; j < cnt; j++, k--) {
                    l2r.build(words[j]);
                    r2l.build(words[k]);
                }
                l2r.buildLast.cnt++;
                r2l.buildLast.cnt++;
            }

            l2r.endBuilding();
            r2l.endBuilding();

            r2l.beginMatching();
            int[] r2lData = new int[len];
            for (int i = len - 1; i >= 0; i--) {
                r2l.match(data[i]);
                r2lData[i] = r2l.matchLast.cnt;
            }

            long total = 0;
            l2r.beginMatching();
            for (int i = 0; i < len - 1; i++) {
                l2r.match(data[i]);
                total += (long)l2r.matchLast.cnt * r2lData[i + 1];
            }

            io.cache.append(total);
        }
    }


