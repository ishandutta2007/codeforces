struct S{
    ll len, sum, max;
};

ll inf = 1e18;

S e(){
    return S{0, 0, -inf};
}

S op(S x, S y){
    return S{x.len+y.len, x.sum+y.sum, max(x.max, y.max)};
}

ll id(){
    return -1;
}

S mapping(ll f, S x){
    if(f == id()) return x;
    return S{x.len, f*x.len, f};
}

ll composition(ll g, ll f){
    if(g == id()) return f;
    return g;
}

using RangeSetRangeSum = atcoder::lazy_segtree<S, op, e, ll, mapping, composition, id>;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, q; cin >> n >> q;
    vector<ll> a(n);
    vector<S> s(n);
    for(int i = 0; i < n; i++) {
        cin >> a[i];
        s[i] = S{1, a[i], a[i]};
    }
    RangeSetRangeSum seg(s);
    auto change_value = [&](int x, ll y){
        auto g = [&](S a){
            return a.max < y;
        };
        int l = seg.min_left(x+1, g);
        seg.apply(l, x+1, y);
    };
    auto buy = [&](int x, ll y) -> int{
        int cur = x;
        int ans = 0;
        while(cur < n){
            if(seg.get(n-1).max > y) break;
            int l = -1;
            if(seg.get(cur).max <= y){
                l = cur;
            }else{
                auto g = [&](S a){
                    return a.max <= y;
                };
                l = seg.min_left(n, g);
            }
            auto g = [&](S a){
                return a.sum <= y;
            };
            int r = seg.max_right(l, g);
            y -= seg.prod(l, r).sum;
            // cout << l << "->" << r << ' ' << seg_sum.prod(l, r).sum << endl;
            ans += r-l;
            cur = r;
        }
        return ans;
    };
    while(q--){
        int t, x, y; cin >> t >> x >> y; x--;
        if(t == 1){
            change_value(x, y);
        }else{
            // cout << x << ' ' << y << endl;
            // for(int i = 0; i < n; i++) cout << seg_max.get(i) << ' ';
            // cout << endl;
            cout << buy(x, y) << "\n";
        }
 
    }
}