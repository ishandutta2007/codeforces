#define all(x) x.begin(), x.end()
#define pb push_back
#define kill(x) return cout<<x<<'\n', 0;

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

int n, m, u, v, x, y, t, a, b, ans, node, cnt;
bool ok[MAXN];
bool mark[MAXN];
vector<int> G[MAXN];
vector<int> out;

void dfs(int node, int par){
	//cerr<<"dfs "<<node<<' '<<par<<endl;
	mark[node]=true;
	cnt-=ok[node];
	ok[node]^=1;
	for (int v:G[node]){
		if (mark[v]) continue ;
		out.pb(v);
		dfs(v, node);
		out.pb(node);
		ok[node]^=1;
	}
	if (ok[node] && par){
		out.pb(par);
		out.pb(node);
		ok[par]^=1;
	}
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	while (m--){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	for (int i=1; i<=n; i++){
		cin>>ok[i];
		if (ok[i]) cnt++;
		if (!node && ok[i]) node=i;
	}
	if (!node) kill(0);
	dfs(node, 0);
	if (cnt) kill(-1);
	if (!ok[node]) cout<<out.size()+1<<endl<<node<<' ';
	else cout<<out.size()<<endl;
	for (int i:out) cout<<i<<' ';cout<<endl;
	
	return 0;
}
