struct Info {
  int x, y, dir, tim;
  bool operator<(const Info& other) const {
    if (x != other.x) { return x < other.x; }
    if (y != other.y) { return y < other.y; }
    if (dir != other.dir) { return dir < other.dir; }
    return tim < other.tim;
  }
};
set<Info> vis;
set<PII> marked;
int dx[] = {0, 1, 1, 1, 0, -1, -1, -1};
int dy[] = {1, 1, 0, -1, -1, -1, 0, 1};

int sum = 0;
int pref[222];
PII Move(int x, int y, int dir) {
  return {x + dx[dir], y + dy[dir]};
}
void Go(int x, int y, int dir, int tim) {
  if (vis.count(Info{x, y, dir, tim})) { return; }
  vis.insert(Info{x, y, dir, tim});
  marked.insert({x, y});
  if (tim == sum) { return; }
  if (pref[tim]) {
    int ldir = (dir + 7) % 8;
    int rdir = (dir + 1) % 8;
    PII l = Move(x, y, ldir);
    PII r = Move(x, y, rdir);
    Go(l.st, l.nd, ldir, tim + 1);
    Go(r.st, r.nd, rdir, tim + 1);
  } else {
    PII nxt = Move(x, y, dir);
    Go(nxt.st, nxt.nd, dir, tim + 1);
  }
}
