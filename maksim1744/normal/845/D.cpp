int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    vector<int> speeds;
    speeds.pb(1e9);

    int n;
    cin >> n;
    int ans = 0;
    int cur = 0;
    int speed = 0;
    while (n--) {
        int tp;
        cin >> tp;
        if (tp == 1) {
            int s;
            cin >> s;
            speed = s;
            while (speeds.back() < s) {
                speeds.pop_back();
                ++ans;
            }
        } else if (tp == 2) {
            ans += cur;
            cur = 0;
        } else if (tp == 3) {
            int s;
            cin >> s;
            if (s < speed) {
                ++ans;
            } else {
                speeds.pb(s);
            }
        } else if (tp == 4) {
            cur = 0;
        } else if (tp == 5) {
            speeds.resize(1);
        } else if (tp == 6) {
            ++cur;
        } else {
            assert(false);
        }
    }
    cout << ans << '\n';

    return 0;
}