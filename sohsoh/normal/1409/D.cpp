ll dig_s(ll n) {
	ll ans = 0;
	while (n > 0) ans += n % 10, n /= 10;
	return ans;
}

ll solve() {
	ll x, s;
	cin >> x >> s;
	ll ds = dig_s(s);
	ll ans = 0;
	ll p = 1;
	while (dig_s(x) > s) {
		ll k = (10 - x % 10) % 10;
		ans += k * p;
		x += k;
		x /= 10;
		p *= 10;
	}

	cout << ans << endl;
	return 0;
}

