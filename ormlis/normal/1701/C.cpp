void solve() {
    int n, m; cin >> n >> m;
    vi cnt(n);
    rep(i, m) {
        int x; cin >> x;
        cnt[x-1]++;
    }
    auto check = [&] (int val) {
        ll have = 0;
        rep(i, n) {
            if (cnt[i] <= val) {
                have += (val - cnt[i]) / 2;
            } else {
                have += val - cnt[i];
            }
        }
        return have >= 0;
    };
    int L = 0;
    int R = m;
    while(R - L > 1) {
        int mid = (R + L) / 2;
        if (check(mid)) R = mid;
        else L = mid;
    }
    cout << R << "\n";
}

