template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int t; cin >> t;
  while (t--) {
    int n, l, r; cin >> n >> l >> r;
    vector<int> a(l), b(r); cin >> a >> b;
    vector<int> x(n), y(n);
    for (int u : a) ++x[u - 1];
    for (int u : b) ++y[u - 1];
    int ans = 0;
    for (int i = 0; i != n; ++i) {
      int m = min(x[i], y[i]);
      x[i] -= m; y[i] -= m;
    }
    int L = accumulate(x.begin(), x.end(), 0), R = accumulate(y.begin(), y.end(), 0);
    if (L < R) {
      swap(L, R); swap(x, y);
    }
    for (int i = 0; i != n; ++i) {
      while (L > R && x[i] >= 2) {
        x[i] -= 2; ++ans; L -= 2;
      }
    }
    ans += L; cout << ans << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}