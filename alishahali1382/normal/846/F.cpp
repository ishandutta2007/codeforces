#define pb push_back
#define kill(x) return cout<<x<<'\n', 0;
#define sqr(x) 1ll*(x)*(x)

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 1000010, LOG=20;

ll n, m, k, u, v, x, y, t, a, b, ans;
int A[MAXN];
vector<int> vec[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	for (int i=0; i<MAXN; i++) vec[i].pb(0);
	cin>>n;
	for (int i=1; i<=n; i++){
		cin>>A[i];
		vec[A[i]].pb(i);
		t+=(vec[A[i]].size()==2);
	}
	for (int i=0; i<MAXN; i++) vec[i].pb(n+1);
	for (int i=0; i<MAXN; i++) if (vec[i].size()>2){
		for (int j=0; j<vec[i].size()-1; j++) ans+=sqr(vec[i][j+1]-vec[i][j]-1);
	}
	//debug(ans)
	ld out=t-ans/((ld) sqr(n));
	cout<<setprecision(12)<<fixed<<out<<'\n';
	
	return 0;
}
