template< typename F >
struct FixPoint : F {
  FixPoint(F &&f) : F(forward< F >(f)) {}

  template< typename... Args >
  decltype(auto) operator()(Args &&... args) const {
    return F::operator()(*this, forward< Args >(args)...);
  }
};

template< typename F >
inline decltype(auto) MFP(F &&f) {
  return FixPoint< F >{forward< F >(f)};
}

template< typename T = int >
struct Edge {
  int from, to;
  T cost;
  int idx;

  Edge() = default;

  Edge(int from, int to, T cost = 1, int idx = -1) : from(from), to(to), cost(cost), idx(idx) {}

  operator int() const { return to; }
};

template< typename T = int >
struct Graph {
  vector< vector< Edge< T > > > g;
  int es;

  Graph() = default;

  explicit Graph(int n) : g(n), es(0) {}

  size_t size() const {
    return g.size();
  }

  void add_directed_edge(int from, int to, T cost = 1) {
    g[from].emplace_back(from, to, cost, es++);
  }

  void add_edge(int from, int to, T cost = 1) {
    g[from].emplace_back(from, to, cost, es);
    g[to].emplace_back(to, from, cost, es++);
  }

  void read(int M, int padding = -1, bool weighted = false, bool directed = false) {
    for(int i = 0; i < M; i++) {
      int a, b;
      cin >> a >> b;
      a += padding;
      b += padding;
      T c = T(1);
      if(weighted) cin >> c;
      if(directed) add_directed_edge(a, b, c);
      else add_edge(a, b, c);
    }
  }
};

template< typename T = int >
using Edges = vector< Edge< T > >;

template< typename T >
vector< T > bellman_ford(Edges< T > &edges, int V, int s) {
  const auto INF = numeric_limits< T >::max();
  vector< T > dist(V, INF);
  dist[s] = 0;
  for(int i = 0; i < V - 1; i++) {
    for(auto &e : edges) {
      if(dist[e.from] == INF) continue;
      dist[e.to] = min(dist[e.to], dist[e.from] + e.cost);
    }
  }
  for(auto &e : edges) {
    if(dist[e.from] == INF) continue;
    if(dist[e.from] + e.cost < dist[e.to]) return vector< T >();
  }
  return dist;
}

/**
 * @brief Union-Find
 * @docs docs/union-find.md
 */
struct UnionFind {
  vector< int > data;

  UnionFind() = default;

  explicit UnionFind(size_t sz) : data(sz, -1) {}

  bool unite(int x, int y) {
    x = find(x), y = find(y);
    if(x == y) return false;
    if(data[x] > data[y]) swap(x, y);
    data[x] += data[y];
    data[y] = x;
    return true;
  }

  int find(int k) {
    if(data[k] < 0) return (k);
    return data[k] = find(data[k]);
  }

  int size(int k) {
    return -data[find(k)];
  }

  bool same(int x, int y) {
    return find(x) == find(y);
  }
};


int main() {
  int N, M;
  cin >> N >> M;
  Edges< int > es;
  UnionFind uf(N + N);
  while(M--) {
    int a, b, c;
    cin >> a >> b >> c;
    --a, --b;
    if(c == 1) {
      es.emplace_back(a, b, -1);
      es.emplace_back(b, a, 1);
    } else {
      es.emplace_back(a, b, 1);
      es.emplace_back(b, a, 1);
    }
    uf.unite(a + N, b);
    uf.unite(a, b + N);
  }
  for(int i = 0; i < N; i++) {
    if(uf.find(i) == uf.find(i + N)) {
      cout << "NO\n";
      return 0;
    }
  }
  int best = -inf, id = 0;
  for(int i = 0; i < N; i++) {
    auto ret = bellman_ford(es, N, i);
    if(ret.empty()) {
      cout << "NO\n";
      return 0;
    }
    if(chmax(best, *max_element(begin(ret), end(ret)) - *min_element(begin(ret), end(ret)))) {
      id = i;
    }
  }
  cout << "YES\n";
  auto ret = bellman_ford(es, N, id);
  int v = *min_element(begin(ret), end(ret));
  cout << best << "\n";
  for(auto &p : ret) cout << 1000000 - (p - v) << " ";
  cout << "\n";
}