int main(int argc, const char **argv)
{
    redirect(argc, argv);
    int N, K;
    cin >> N >> K;

    int s = (N - 1) / K;
    int t = s + 1;
    int u = (N - 1) % K;
    if (u == 0)
    {
        u = K;
        s--;
        t--;
    }
    if (u == 1)
        cout << s + t << '\n';
    else
        cout << 2 * t << '\n';

    int p = 1;
    for (int i = 0; i < K; i++)
    {
        int m = (i < u) ? t : s;
        cout << N << ' '  << p << '\n';
        for (int j = 1; j < m; j++)
        {
            cout << p << ' ' << p + 1 << '\n';
            p++;
        }
        p++;
    }

    return 0;
}