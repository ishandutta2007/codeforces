const int N = 300'228;
vector<int> g[N], h[N];
int tin[N], tout[N];
int tim;

void dfs_t(int v) {
    tin[v] = tim++;

    for(int i : h[v]) {
        dfs_t(i);
    }

    tout[v] = tim++;
}

int ans;
set<pair<int, int>> cur;

pair<bool, pair<int, int>> test(int l, int r) {
    auto it = cur.lower_bound({l, r});
    if(it != cur.end() && it->x < r) {
        return {false, {-1, -1}};
    }
    if(it == cur.begin()) {
        return{true, {-1, -1}};
    }
    --it;
    if(it->y < l) {
        return{true, {-1, -1}};
    }
    assert(it->y > r);
    return{true, *it};
}

void dfs_ans(int v) {
    auto val = test(tin[v], tout[v]);
    if(val.x) {
        if(val.y.x != -1) {
            cur.erase(val.y);
        }
        cur.insert({tin[v], tout[v]});
    }
    ans = max(ans, sz(cur));

    for(int i : g[v]) {
        dfs_ans(i);
    }

    if(val.x) {
        cur.erase({tin[v], tout[v]});
        if(val.y.x != -1) {
            cur.insert(val.y);
        }
    }
}

void solve() {
    int n;
    cin >> n;

    tim = 0;
    ans = 1;
    for(int i = 1; i <= n; ++i) {
        g[i].clear();
        h[i].clear();
    }

    for(int i = 2; i <= n; ++i) {
        int x;
        cin >> x;

        g[x].push_back(i);
    }

    for(int i = 2; i <= n; ++i) {
        int x;
        cin >> x;

        h[x].push_back(i);
    }

    dfs_t(1);

    dfs_ans(1);
    cout << ans << endl;
}

void _main_function() {
    int t = 1;
    cin >> t;

    for(int i = 0; i < t; ++i) {
        solve();
    }
}


