	void solve()
	{
		char[] s = ns(200001);
		char[] t = ns(200001);
		int n = s.length;
		int m = t.length;
		
		int[][] fw = new int[n+1][26];
		Arrays.fill(fw[0], -1);
		int p = 0;
		for(int i = 1;i <= n;i++){
			for(int j = 0;j < 26;j++){
				fw[i][j] = fw[i-1][j];
			}
			if(p < m && s[i-1] == t[p]){
				fw[i][s[i-1]-'a'] = p;
				p++;
			}
		}
		
		int[][] bw = new int[n+1][26];
		Arrays.fill(bw[n], 999999);
		int q = m-1;
		for(int i = n-1;i >= 0;i--){
			for(int j = 0;j < 26;j++){
				bw[i][j] = bw[i+1][j];
			}
			if(q >= 0 && s[i] == t[q]){
				bw[i][s[i]-'a'] = q;
				q--;
			}
		}
		
		for(int i = 0;i < n;i++){
			if(fw[i+1][s[i]-'a'] < bw[i][s[i]-'a']){
				out.println("No");
				return;
			}
		}
		out.println("Yes");
	}
	
