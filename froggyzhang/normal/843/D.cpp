#include<bits/stdc++.h>
using namespace std;
#define N 100010
typedef long long ll;
const ll inf=0x3f3f3f3f3f3f3f3fll;
inline ll read(){
	ll x=0,f=1;
	char c=getchar();
	while(c<'0'||c>'9'){
		if(c=='-')f=-1;
		c=getchar();
	}
	while(c>='0'&&c<='9'){
		x=(x<<1)+(x<<3)+c-'0';
		c=getchar();
	}
	return x*f;
}
int n,m,Q,vis[N],now;
ll dis[N];
struct Edge{
	int to,val;
}E[N];
vector<int> G[N];
void Dijkstra(){
	priority_queue<pair<ll,int>,vector<pair<ll,int> >,greater<pair<ll,int> > > q;
	memset(dis,0x3f,sizeof(dis));
	memset(vis,0,sizeof(vis));
	dis[1]=0;
	q.push(make_pair(0LL,1));
	while(!q.empty()){
		int u=q.top().second;
		q.pop();
		if(vis[u])continue;
		vis[u]=1;
		for(auto id:G[u]){
			int v=E[id].to,w=E[id].val;
			if(dis[v]>dis[u]+w){
				dis[v]=dis[u]+w;
				q.push(make_pair(dis[v],v));
			}
		}
	}
}
vector<int> q[N*10];
void bfs(){
	static ll g[N];
	memset(g,0x3f,sizeof(g));
	memset(vis,0,sizeof(vis));
	g[1]=0;
	q[0].push_back(1);
	for(int i=0;i<=now;++i){
		for(int j=0;j<(int)q[i].size();++j){
			int u=q[i][j];
			if(vis[u])continue;
			vis[u]=1;
			for(auto id:G[u]){
				int v=E[id].to,w=E[id].val;
				if(g[v]>g[u]+w){
					g[v]=g[u]+w;
					if(g[v]-dis[v]<=now){
						q[g[v]-dis[v]].push_back(v);
					}
				}
			}
		}
		q[i].clear();
	}
	memcpy(dis,g,sizeof(dis));
}
int main(){
	n=read(),m=read(),Q=read();
	for(int i=1;i<=m;++i){
		int u=read(),v=read(),w=read();
		E[i]={v,w};
		G[u].push_back(i);
	}
	Dijkstra();
	while(Q--){
		int opt=read();
		if(opt==1){
			int u=read();
			bfs();
			printf("%lld\n",dis[u]==inf?-1:dis[u]);
			now=0;
		}
		else{
			int c=read();
			for(int i=1;i<=c;++i){
				int x=read();
				++E[x].val;	
			}	
			now+=c;
		}	
	}
	return 0;
}