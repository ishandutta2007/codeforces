const int mod = 1e9 + 7, N = 200001, logN = 20, K = 80000;

vector <int> adj[N];
vector <bool> leaf;
vector <int> res;

void dfs(int v, int pa) {
    int tmp = 0;
    bool ch = false;
    for (int u : adj[v]) if (u != pa) {
        dfs(u, v);
        ch = true;
        if (leaf[u]) leaf[v] = false, tmp++;
    }
    if (!leaf[v] && pa != -1) {
        res.pb(tmp - 1);
    }
}

void solve() {
    int n;
    cin >> n;
    for (int i = 0, u, v; i < n - 1; ++i) {
        cin >> u >> v, --u, --v;
        adj[u].pb(v), adj[v].pb(u);
    }
    int ans = 0;
    leaf.assign(n, true);
    res.clear();
    dfs(0, -1);
    for (int i : adj[0]) ans += leaf[i];
    bool is = false;
    test(res, ans);
    cout << max(ans, 1) + accumulate(all(res), 0) << endl;
    for (int i = 0; i < n; ++i) adj[i].clear();
}

