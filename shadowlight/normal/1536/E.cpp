const int mod = 1000000007;

void add(int& a, int b) {
  a += b;
  if (a >= mod) a -= mod;
  if (a < 0) a += mod;
}

int mult(int a, int b) {
  return a * (ll)b % mod;
}

int bp(int a, int b) {
  int res = 1;
  while (b > 0) {
    if (b & 1) res = mult(res, a);
    a = mult(a, a);
    b >>= 1;
  }
  return res;
}

void solve() {
  int n, m;
  cin >> n >> m;

  bool has_zero = false;
  int cnt = 0;
  int res = 1;

  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < m; ++j) {
      char c;
      cin >> c;
      if (c == '0') {
        has_zero = true;
      } else {
        ++cnt;
        res = mult(res, 2);
      }
    }
  }
  add(res, -(1 - has_zero));
  cout << res << "\n";
}

