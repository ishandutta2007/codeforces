ll cnt[MAXN];

int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	ll n, m;
	cin >> n >> m;
	ll x;
	for(int i = 0; i < m; i++){
		if(i == 0) x = n / m;
		else if(i > n) x = 0;
		else {
			x = 1 + ((n-i) / m);
		}
		cnt[(i * i) % m] += x;
	}
	debug(cnt[0]);
	ll ans = 0;
	for(int i = 0; i < m;i++) ans += (cnt[i] * cnt[(m-i) % m]);
	cout << ans;
	return 0;
}


