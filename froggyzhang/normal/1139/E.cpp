#include<bits/stdc++.h>
using namespace std;
#define N 23333
const int inf=0x3f3f3f3f;
typedef long long ll;
inline ll read(){
	ll x=0,f=1;
	char c=getchar();
	while(c<'0'||c>'9'){
		if(c=='-')f=-1;
		c=getchar();
	}
	while(c>='0'&&c<='9'){
		x=(x<<1)+(x<<3)+c-'0';
		c=getchar();
	}
	return x*f;
}
namespace Maxflow{
	int head[N],ecnt,S,T,dep[N];
	struct Edge{
		int nxt,to,val;
	}edge[N<<3];
	void add(int a,int b,int c){
		edge[++ecnt]={head[a],b,c};
		head[a]=ecnt;
	}
	void adde(int a,int b,int c){
		add(a,b,c),add(b,a,0);
	}
	void init(int _S,int _T){
		ecnt=1;
		memset(head,0,sizeof(head));
		S=_S,T=_T;
	}
	queue<int> q;
	bool bfs(){
		memset(dep,0,sizeof(dep));
		q.push(S);
		dep[S]=1;
		while(!q.empty()){
			int u=q.front();
			q.pop();
			for(int i=head[u];i;i=edge[i].nxt){
				int v=edge[i].to;
				if(edge[i].val&&!dep[v]){
					dep[v]=dep[u]+1;
					q.push(v);
				}
			}
		}
		return dep[T]>0;
	}
	int dfs(int u,int limit){
		if(u==T)return limit;
		int flow=0;
		for(int i=head[u];i&&limit;i=edge[i].nxt){
			int v=edge[i].to;
			if(dep[v]==dep[u]+1&&edge[i].val){
				int k=dfs(v,min(limit,edge[i].val));
				edge[i].val-=k;
				edge[i^1].val+=k;
				flow+=k;
				limit-=k;
			}
		}
		if(!flow)dep[u]=inf;
		return flow;
	}
	int Dinic(){
		int maxflow=0;
		while(bfs()){
			maxflow+=dfs(S,inf);
		}
		return maxflow;
	}
}
int n,m,Q,p[N],c[N],t[N],vis[N],ans[N];
vector<int> vec[N];
int main(){
	n=read(),m=read();
	for(int i=1;i<=n;++i){
		p[i]=read();
		vec[p[i]].push_back(i);
	}
	for(int i=1;i<=n;++i){
		c[i]=read();
	}
	Q=read();
	for(int i=1;i<=Q;++i){
		t[i]=read();
		vis[t[i]]=1;
	}
	Maxflow::init(2*n+m+2,2*n+m+3);
	for(int i=1;i<=m;++i){
		Maxflow::adde(Maxflow::S,i,1);
	}
	int now=0;
	for(int i=1;i<=n;++i){
		if(!vis[i]){
			Maxflow::adde(c[i],m+i,1);
			if(p[i]<=now)Maxflow::adde(m+i,m+n+p[i]+1,1);
		}
	}
	Maxflow::adde(n+m+1,Maxflow::T,1);
	int flow=0;
	for(int i=Q;i>=1;--i){
		flow+=Maxflow::Dinic();
		while(flow>now){
			++now;
			Maxflow::adde(n+m+now+1,Maxflow::T,1);
			for(auto u:vec[now]){
				if(!vis[u]){
					Maxflow::adde(m+u,m+n+now+1,1);
				}	
			}
			flow+=Maxflow::Dinic();
		}
		ans[i]=now;
		int u=t[i];
		vis[u]=0;
		Maxflow::adde(c[u],m+u,1);
		if(p[u]<=now){
			Maxflow::adde(m+u,n+m+p[u]+1,1);
		}
	}
	for(int i=1;i<=Q;++i){
		printf("%d\n",ans[i]);
	}
	return 0;
}