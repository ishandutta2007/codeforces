const int MAXN=5e5;
int n,k,a[MAXN+5],b[MAXN+5];ll ans=0;
bool check(ll mid){
	priority_queue<pii> q;ll sum=0;int cnt=0;
	for(int i=1;i<=n;i++){
		q.push(mp(-a[i],0));ll val=b[i]-mid-q.top().fi;
		if(val<0) sum+=val,q.pop(),q.push(mp(b[i]-mid,1));
	} while(!q.empty()) cnt+=q.top().se,q.pop();
	if(cnt>=k) return ans=sum+mid*k,1;
	else return 0;
}
int main(){
	scanf("%d%d",&n,&k);
	for(int i=1;i<=n;i++) scanf("%d",&a[i]);
	for(int i=1;i<=n;i++) scanf("%d",&b[i]);
	ll l=0,r=2e9,p=-1;
	while(l<=r){
		ll mid=0ll+l+r>>1;
		if(check(mid)) p=mid,r=mid-1;
		else l=mid+1;
	} assert(~p);check(p);printf("%lld\n",ans);
	return 0;
}