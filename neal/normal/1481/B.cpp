void run_case() {
    int N, K;
    cin >> N >> K;
    vector<int> H(N);

    for (auto &h : H)
        cin >> h;

    for (int k = 0; k < K; k++) {
        int stop = 0;

        while (stop < N - 1 && H[stop] >= H[stop + 1])
            stop++;

        if (stop == N - 1) {
            cout << -1 << '\n';
            return;
        }

        H[stop]++;

        if (k == K - 1) {
            cout << stop + 1 << '\n';
            break;
        }
    }
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}