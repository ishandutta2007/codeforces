#include<bits/stdc++.h>
#define all(x) begin(x), end(x)
using namespace std;
using ll = long long;

template<typename F>
void multitest(F func) {
	int t;
	cin >> t;
	while(t--) func();
}
void report(int ok) {
	cout << (ok?"YES":"NO") << '\n';
}

int main() {
	cin.tie(0)->sync_with_stdio(0);
	multitest([&](){
		int a, b;
		cin >> a >> b;
		if((a+b)%2) cout << "-1\n";
		else if(a == 0 && b == 0) cout << "0\n";
		else cout << 1+(a!=b) << '\n';
	});
	
}