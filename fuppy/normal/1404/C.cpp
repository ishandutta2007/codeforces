void solve() {
    int n, q;
    cin >> n >> q;
    vi a(n);
    rep (i, n) {
        cin >> a[i];
        a[i]--;
    }
    vector<vector<pii>> x2yi(n + 1);
    rep (i, q) {
        int x, y;
        cin >> x >> y;
        x2yi[x].push_back(pii(y, i));
    }

    vector<pll> temp(n);
    rep (i, n) {
        temp[i] = pii(i - a[i], -i);
        if (temp[i].first < 0) {
            temp[i].first = 2 * inf;
        }
    }
    // DEBUG_VEC(temp);
    LazySegmentTree<pll, int> seg1(temp, f1, g1, h1, pll(3 * inf, 3 * inf), 0);
    SegmentTree<int> seg2(vi(n), f, g, 0);

    vi ans(q);
    for (int x = n; x >= 0; x--) {
        // DEBUG(x);
        if (x == n) {
            for (pii yi: x2yi[x]) {
                ans[yi.second] = 0;
            }
            continue;
        }

        while (true) {
            pii mi = seg1.query(x, n, 0, 0, seg1.n);
            // DEBUG(mi);
            if (mi.first != 0) break;
            int idx = -mi.second;
            seg2.update(idx, 1, 0, 0, seg2.n);
            seg1.update(idx, idx + 1, 2 * inf, 0, 0, seg1.n);
            seg1.update(idx, n, -1, 0, 0, seg1.n);
        }

        for (pii yi: x2yi[x]) {
            int r = n - yi.first;
            ans[yi.second] = seg2.query(x, r, 0, 0, seg2.n);
        }
    }

    rep (i, q) {
        cout << ans[i] << '\n';
    }
}

