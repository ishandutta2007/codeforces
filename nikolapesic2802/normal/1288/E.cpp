	ordered_set<pair<int,int> > o;
	int n,m;
	scanf("%i %i",&n,&m);
	vector<int> mn(n),mx(n),pos(n);
	for(int i=0;i<n;i++)
        o.insert({i,i}),pos[i]=mn[i]=mx[i]=i;
    for(int i=0;i<m;i++){
        int a;
        scanf("%i",&a);
        a--;
        int p=o.order_of_key({pos[a],a});
        o.erase({pos[a],a});
        mn[a]=min(mn[a],0);
        mx[a]=max(mx[a],p);
        o.insert({tr,a});
        pos[a]=tr--;
    }
    auto it=o.begin();
    for(int i=0;i<n;i++,it++){
        int a=(*it).s;
        mn[a]=min(mn[a],i);
        mx[a]=max(mx[a],i);
    }
