int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n;
    cin >> n;
    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; ++i) {
        int a, b;
        cin >> a >> b;
        --a; --b;
        g[a].pb(b);
        g[b].pb(a);
    }
    vector<int> col(n, -1);
    function<void(int, int)> dfs = [&](int v, int c) {
        col[v] = c;
        for (auto k : g[v]) {
            if (col[k] == -1)
                dfs(k, 1 - c);
        }
    };
    
    dfs(0, 0);
    vector<int> c(2);
    for (int i = 0; i < n; ++i) {
        if (g[i].size() == 1)
            c[col[i]]++;
    }
    if (c[0] == 0 || c[1] == 0) cout << 1 << ' ';
    else cout << 3 << ' ';
    int cnt = n - 1;
    for (int i = 0; i < n; ++i) {
        int cl = 0;
        for (auto k : g[i]) {
            if (g[k].size() == 1) cl++;
        }
        cl = max(0, cl - 1);
        cnt -= cl;
    }
    cout << cnt << '\n';

    return 0;
}