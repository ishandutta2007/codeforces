void solv()
{
    int n;
    scanf("%d", &n);
    int ans = 0;
    while (n)
    {
        if (n == 1)
            break;
        if (n % 6 == 0)
            n /= 6;
        else
        {
            if (n % 3 != 0)
            {
                ans = -1;
                break;
            }
            n *= 2;
        }
        ++ans;
    }
    printf("%d\n", ans);
}

