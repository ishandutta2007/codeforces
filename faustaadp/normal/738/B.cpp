#include<bits/stdc++.h>
using namespace std;
int n,m,i,j,hs,l,r;
bool a[1010][1010],b;
int main()
{
	cin>>n>>m;
	for(i=1;i<=n;i++)
	{
		for(j=1;j<=m;j++)
		{
			scanf("%d",&a[i][j]);
			//cin>>a[i][j];
		}
	}
	for(i=1;i<=n;i++)
	{
		b=0;
		for(j=1;j<=m;j++)
		{
			if(a[i][j]==1)
			{
				b=1;
				l=j;
				break;
			}
		}
		for(j=m;j>=1;j--)
		{
			if(a[i][j]==1)
			{
				b=1;
				r=j;
				break;
			}
		}
		if(b==1)
		{
			for(j=1;j<=m;j++)
			{
				if(a[i][j]==0)
				{
					if(j<l||j>r)
						hs++;
					else
						hs=hs+2;
				}
			}
		}
	}
	//cout<<hs;
	for(j=1;j<=m;j++)
	{
		b=0;
		for(i=1;i<=n;i++)
		{
			if(a[i][j]==1)
			{
				b=1;
				l=i;
				break;
			}
		}
		for(i=n;i>=1;i--)
		{
			if(a[i][j]==1)
			{
				b=1;
				r=i;
				break;
			}
		}
		if(b==1)
		{
			for(i=1;i<=n;i++)
			{
				if(a[i][j]==0)
				{
					if(i<l||i>r)
						hs++;
					else
						hs=hs+2;
				}
			}
		}
	}
	cout<<hs<<endl;
}