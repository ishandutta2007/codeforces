void solve() {
    int xy = getans(1, 1);
    int l = 1;
    int r = 1000000000;
    while (l < r) {
        int mid = (l + r + 1)  >> 1;
        if (getans(mid, 1) != xy - (mid - 1)) {
            r = mid - 1;
        } else  {
            l = mid;
        }
    }
    int x = l;
    int y = xy - x + 2;
    int xq = getans(1, 1000000000);
    int q = 1000000000 - (xq - (x - 1));
    int py = getans(1000000000, 1);
    int p = 1000000000 - (py - (y - 1));
    cout << "! " << x << ' ' << y << ' ' << p << ' ' << q << endl;
}


