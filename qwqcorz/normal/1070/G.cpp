#include<bits/stdc++.h>
using namespace std;
const int N=105;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
bool vis[N];
vector<int>ans;
int x[N],h[N],id[N],a[N],b[N],sum[N],n,m;
bool check(int hp,int i,int j)
{
	int d=j-i;
	if (d>0) d=1;
	if (d<0) d=-1;
	while ((i+=d)!=j)
	{
		hp+=a[i];
		if (hp<0) return 0;
	}
	return 1;
}
bool check1(int x)
{
	sum[x+1]=0;
	for (int i=x;i>=1;i--) sum[i]=sum[i+1]+a[i];
	int last=0;
	for (int i=x;i>=1;i--) if (id[i]) last=i;
	if (!last) return 1;
	int i=x,j=x+1;
	for (;i>last;i--)
	if (!vis[id[i]]&&id[i]&&sum[i]-sum[j]<0&&check(h[id[i]],i,j))
	{
		vis[id[i]]=1;
		ans.push_back(id[i]);
		j=i;
	}
	if (check(h[id[last]],last,j))
	{
		a[x]=0;
		if (!vis[id[last]]) ans.push_back(id[last]);
		vis[id[last]]=1;
		return 1;
	}
	else return 0;
}
bool check2(int x)
{
	sum[x-1]=0;
	for (int i=x;i<=n;i++) sum[i]=sum[i-1]+a[i];
	int last=0;
	for (int i=x;i<=n;i++) if (id[i]) last=i;
	if (!last) return 1;
	int i=x,j=x-1;
	for (;i<last;i++)
	if (!vis[id[i]]&&id[i]&&sum[i]-sum[j]<0&&check(h[id[i]],i,j))
	{
		vis[id[i]]=1;
		ans.push_back(id[i]);
		j=i;
	}
	if (check(h[id[last]],last,j))
	{
		a[x]=0;
		if (!vis[id[last]]) ans.push_back(id[last]);
		vis[id[last]]=1;
		return 1;
	}
	else return 0;
}
void solve(int x,bool flag)
{
	ans.clear();
	memset(vis,0,sizeof(vis));
	for (int i=1;i<=n;i++) a[i]=b[i];
	if (flag)
	{
		if (check1(x)&&check2(x))
		{
			print(x);
			for (int i:ans) print(i,' ');
			for (int i=1;i<=m;i++) if (!vis[i]) print(i,' ');
			puts("");
			exit(0);
		}
	}
	else
	{
		if (check2(x)&&check1(x))
		{
			print(x);
			for (int i:ans) print(i,' ');
			for (int i=1;i<=m;i++) if (!vis[i]) print(i,' ');
			puts("");
			exit(0);
		}
	}
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	n=read(),m=read();
	for (int i=1;i<=m;i++) x[i]=read(),h[i]=read();
	for (int i=1;i<=m;i++) if (!id[x[i]]||h[id[x[i]]]<h[i]) id[x[i]]=i;
	for (int i=1;i<=n;i++) b[i]=read();
	for (int i=1;i<=n;i++) solve(i,0),solve(i,1);
	puts("-1");
	
	return 0;
}
