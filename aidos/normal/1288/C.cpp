int binpow(ll x, ll y) {
	if(y == 0) return 1;
	if(y&1) return binpow(x, y-1) * x % mod;
	return binpow(x * x % mod, y/2);
}
ll f[maxn];
void solve(){
	cin >> m >> n;
	n *= 2;
	f[0] = 1;
	for(int i = 1; i < maxn; i++) {
		f[i] = f[i-1] * i % mod;
	}
	cout << f[n + m - 1] * binpow(f[n] * f[m-1] % mod, mod-2) % mod << "\n";
}
