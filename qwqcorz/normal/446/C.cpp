#include<bits/stdc++.h>
using namespace std;
const int N=3e5+5;
const int p=1e9+9;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
inline void add(int &x,int y)
{
	x+=y;
	if (x>=p) x-=p;
}
int f[N],g[N],a[N];
#define ls (w*2)
#define rs (w*2+1)
#define mid ((l+r)/2)
int t[N*4],f1[N*4],f2[N*4];
void build(int w,int l,int r)
{
	if (l==r) return void(t[w]=a[l]);
	build(ls,l,mid);
	build(rs,mid+1,r);
	t[w]=(t[ls]+t[rs])%p;
}
void update(int w,int l,int r,int F1,int F2)
{
	add(t[w],(1LL*F1*f[r-l+1]+1LL*F2*g[r-l])%p);
	add(f1[w],F1);
	add(f2[w],F2);
}
void push_down(int w,int l,int r)
{
	if (l==r) return;
	if (!f1[w]&&!f2[w]) return;
	update(ls,l,mid,f1[w],f2[w]);
	update(rs,mid+1,r,(1LL*f1[w]*f[mid-l]+1LL*f2[w]*f[mid-l+1])%p,(1LL*f1[w]*f[mid-l+1]+1LL*f2[w]*f[mid-l+2])%p);
	f1[w]=f2[w]=0;
}
void add(int w,int l,int r,int ql,int qr)
{
	if (qr<l||r<ql) return;
	if (ql<=l&&r<=qr) return update(w,l,r,f[l-ql+1],f[l-ql+2]);
	push_down(w,l,r);
	add(ls,l,mid,ql,qr);
	add(rs,mid+1,r,ql,qr);
	t[w]=(t[ls]+t[rs])%p;
}
int query(int w,int l,int r,int ql,int qr)
{
	if (qr<l||r<ql) return 0;
	if (ql<=l&&r<=qr) return t[w];
	push_down(w,l,r);
	return (query(ls,l,mid,ql,qr)+query(rs,mid+1,r,ql,qr))%p;
}
#undef ls
#undef rs
#undef mid

signed main(signed Recall,char *_902_[])
{
	f[1]=f[2]=g[1]=1,g[2]=2;
	(void)Recall,(void)_902_;
	int n=read(),m=read();
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=3;i<=n+1;i++) f[i]=(f[i-1]+f[i-2])%p,g[i]=(g[i-1]+f[i])%p;
	build(1,1,n);
	for (int i=1;i<=m;i++)
	{
		int opt=read(),l=read(),r=read();
		if (opt==1) add(1,1,n,l,r);
		else print(query(1,1,n,l,r));
	}
	
	return 0;
}
