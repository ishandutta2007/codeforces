void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i], a[i]--;

    vector<int> b(n);
    vector<vector<int>> wish(n);
    for (int i = 0; i < n; i++) wish[a[i]].emplace_back(i);
    vector<int> check(n, 0);
    for (int i = 0; i < n; i++) {
        if (wish[i].empty()) continue;
        int j = wish[i].back();
        b[j] = i;
        check[j] |= 1;
        check[i] |= 2;
    }

    vector<int> A, B, C;
    for (int i = 0; i < n; i++) {
        if (check[i] == 1) A.emplace_back(i);
        if (check[i] == 2) B.emplace_back(i);
        if (check[i] == 0) C.emplace_back(i);
    }
    for (int i = 0; i < B.size(); i++) b[B[i]] = A[i];
    if (C.size() > 1) {
        int N = C.size();
        for (int i = 0; i < N; i++) b[C[i]] = C[(i + 1) % N];
    } else if (!C.empty()) {
        int x = C[0];
        if (!B.empty()) {
            b[x] = b[B[0]];
            b[B[0]] = x;
        } else {
            int y = wish[a[x]].back();
            b[x] = b[y];
            b[y] = x;
        }
    }

    int k = 0;
    for (int i = 0; i < n; i++) k += (a[i] == b[i]);

    cout << k << '\n';
    for (int i = 0; i < n; i++) cout << b[i] + 1 << (i + 1 == n ? '\n' : ' ');
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t;
    cin >> t;
    for (; t--;) solve();
    return 0;
}