const int inf = 2e9;
void test_case(int test) {
    int n, k;
    cin >> n >> k;
    vector<int> t(n, inf);
    vector<int> a1(k), a2(k);
    cin >> a1 >> a2;
    --a1;
    for (int i = 0; i < k; ++i) {
        t[a1[i]] = a2[i];
    }
    vector<int> ans(n, inf);
    int cur = inf;
    for (int i = 0; i < n; ++i) {
        cur = min(cur + 1, t[i]);
        ans[i] = min(ans[i], cur);
    }
    cur = inf;
    for (int i = n - 1; i >= 0; --i) {
        cur = min(cur + 1, t[i]);
        ans[i] = min(ans[i], cur);
    }
    cout << ans << '\n';
}

