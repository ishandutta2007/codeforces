vector<vector<int> > ans={{1,2,4},{5,3,8},{9,6,7}};
int main()
{
	int n;
	scanf("%i",&n);
	if(n<3){
		printf("-1\n");
		return 0;
	}
	int sol[n][n];
	int tr=1;
	for(int d=n;d>3;d--){
		int dist=d-3;
		if(dist&1){
			for(int i=n-1;i>=n-d;i--)
				sol[i][d-1]=tr++;
			for(int j=d-2;j>=0;j--)
				sol[n-d][j]=tr++;
		}
		else{
			for(int j=0;j<=d-2;j++)
				sol[n-d][j]=tr++;
			for(int i=n-d;i<n;i++)
				sol[i][d-1]=tr++;
		}
	}
	for(int i=n-3;i<n;i++)
		for(int j=0;j<3;j++)
			sol[i][j]=ans[i-n+3][j]+tr-1;
	for(int i=0;i<n;i++,printf("\n"))
		for(int j=0;j<n;j++)
			printf("%i ",sol[i][j]);
	return 0;
}