const int MAX_N = 200010;
int dp[MAX_N][3][2]
      [2];  // i  j  0  k, -  l  2 
tuple<int, int, int> pre[MAX_N][3][2][2];

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    cin >> a;

    for (int i = 0; i <= n + 1; i++) {
        for (int j = 0; j < 3; j++) {
            for (int k = 0; k < 2; k++) {
                for (int l = 0; l < 2; l++) {
                    dp[i][j][k][l] = -INF;
                    pre[i][j][k][l] = {-1, -1, -1};
                }
            }
        }
    }
    dp[0][0][0][0] = 0;

    for (int i = 0; i <= n; i++) {
        for (int j = 0; j < 3; j++) {
            for (int k = 0; k < 2; k++) {
                for (int l = 0; l < 2; l++) {
                    int val = dp[i][j][k][l];
                    if (val < 0) continue;
                    for (int nj = j; nj < 3; nj++) {
                        if (i == n and nj != 2) continue;
                        if (nj != 1) {
                            if (chmax(dp[i + 1][nj][k][l], val)) pre[i + 1][nj][k][l] = {j, k, l};
                        } else {
                            int nk = k | (a[i] == 0), nl = l ^ (a[i] < 0);
                            if (chmax(dp[i + 1][nj][nk][nl], val + (abs(a[i]) == 2)))
                                pre[i + 1][nj][nk][nl] = {j, k, l};
                        }
                    }
                }
            }
        }
    }

    // debug(dp[n + 1][2][0][0]);
    if (dp[n + 1][2][0][0] < 0) {
        cout << n << ' ' << 0 << '\n';
        return;
    }
    int x = 0, y = 0;
    for (int i = n + 1, j = 2, k = 0, l = 0; i > 0; i--) {
        if (i < n + 1) {
            x += (j == 0);
            y += (j == 2);
        }
        auto nxt = pre[i][j][k][l];
        int nj, nk, nl;
        tie(nj, nk, nl) = nxt;
        j = nj;
        k = nk;
        l = nl;
        // debug(j, k, l);
    }

    cout << x << ' ' << y << '\n';
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t;
    cin >> t;
    for (; t--;) solve();
    return 0;
}