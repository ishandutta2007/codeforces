// ============= Solution ============= //
// ans looks like ......11111, where the .'s are greedily taking only things that don't affect your IQ
//
// since the .'s are unaffected by the 1's, just maximize the length of the suffix of 1's
// do this by binary searching for the max valid suffix of 1's

int main() {
   int T;
   cin >> T;
   while (T--) {
      int n, q;
      cin >> n >> q;
      vector<int> v(n);
      for (int &i: v) cin >> i;

      auto can = [&](int x) -> bool {
         int cur = q;
         for (int i = n - 1 - x; i < n; ++i) {
            if (v[i] > cur) --cur;
         }
         return cur >= 0;
      };

      int lo = 0;
      int hi = n - 1;
      while (lo < hi) {
         int mid = (lo + hi + 1) / 2;
         if (can(mid)) {
            lo = mid;
         } else {
            hi = mid - 1;
         }
      }

      vector<int> ans(n);
      for (int i = 0; i < n - 1 - lo; ++i) {
         if (v[i] <= q) ans[i] = 1;
      }
      for (int i = n - 1 - lo; i < n; ++i) {
         ans[i] = 1;
      }
      for (int i = 0; i < n; ++i) {
         cout << ans[i];
      }
      cout << "\n";
   }
}

