void run() {
	vector<ll> a = readVec(), b = readVec();
	ll len = 0, i = 0, j = 0;

	if (a.back() != b.back()) {
		cout << -1 << endl;
		return;
	}

	while (i < sz(a) && j < sz(b)) {
		if (a[i] < b[j]) {
			i++;
		} else if (a[i] > b[j]) {
			j++;
		} else {
			len++; i++; j++;
		}
	}

	cout << len << endl;
}