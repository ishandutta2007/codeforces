template<typename T> vector<typename Z_p<T>::Type> Z_p<T>::MOD_INV;
template<typename T = integral_constant<decay<decltype(mod)>::type, mod>>
void precalc_inverse(int SZ){
	auto &inv = Z_p<T>::MOD_INV;
	if(inv.empty()) inv.assign(2, 1);
	for(; inv.size() <= SZ; ) inv.push_back((mod - 1LL * mod / (int)inv.size() * inv[mod % (int)inv.size()]) % mod);
}

template<typename T>
vector<T> precalc_power(T base, int SZ){
	vector<T> res(SZ + 1, 1);
	for(int i = 1; i <= SZ; ++ i) res[i] = res[i - 1] * base;
	return res;
}
using namespace chrono;
mt19937 rng(high_resolution_clock::now().time_since_epoch().count());
mt19937_64 rngll(high_resolution_clock::now().time_since_epoch().count());

// Requires Zp and random
template<class Zp>
struct polynomial_hash{
	int SZ;
	Zp base;
	vector<Zp> pow;
	polynomial_hash(int SZ): SZ(SZ), base(rng() % int(2e9) + int(1e8)), pow(precalc_power(base, SZ)){ }
	template<class Str>
	void find_matches(const Str &text, const Str &pattern, function<void(int)> act, int th = numeric_limits<int>::max()){
		int n = (int)text.size(), m = (int)pattern.size();
		if(n < m) return;
		Zp tval = 0, pval = 0;
		for(auto i = 0; i < m - 1; ++ i) tval = tval * base + text[i];
		for(auto i = 0; i < m; ++ i) pval = pval * base + pattern[i];
		for(auto i = 0; i <= n - m; ++ i){
			tval = tval * base + text[i + m - 1];
			if(tval == pval) act(i);
			tval -= pow[m - 1] * text[i];
		}
	}
};

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, m;
	string s, t;
	cin >> n >> m >> s >> t;
	polynomial_hash<Zp> hash(n);
	vector<array<int, 26>> ok(n - m + 1);
	array<vector<int>, 26> cpos;
	array<vector<int>, 26> dpos;
	for(auto c = 0; c < 26; ++ c){
		for(auto i = 0; i < n; ++ i){
			if(s[i] - 'a' == c){
				cpos[c].push_back(i);
			}
		}
		for(auto i = 0; i < m; ++ i){
			if(t[i] - 'a' == c){
				dpos[c].push_back(i);
			}
		}
	}
	for(auto c = 0; c < 26; ++ c){
		for(auto d = 0; d < 26; ++ d){
			static vector<int> a(n), b(m);
			for(auto i: cpos[c]){
				a[i] = true;
			}
			for(auto i: dpos[d]){
				b[i] = true;
			}
			hash.find_matches(a, b, [&](int i){ ok[i][c] |= 1 << d; });
			for(auto i: cpos[c]){
				a[i] = false;
			}
			for(auto i: dpos[d]){
				b[i] = false;
			}
		}
	}
	vector<int> res;
	for(auto i = 0; i <= n - m; ++ i){
		for(auto c = 0; c < 26; ++ c){
			for(auto d = 0; d < 26; ++ d){
				if(ok[i][c] & 1 << d && ok[i][d] & 1 << c){
					goto OK;
				}
			}
			goto FAIL;
			OK:;
		}
		res.push_back(i);
		FAIL:;
	}
	cout << (int)res.size() << "\n";
	for(auto i: res){
		cout << i + 1 << " ";
	}
	cout << "\n";
	return 0;
}

