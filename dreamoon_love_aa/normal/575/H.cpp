    for(int i=N;i<2*N;i++){
        one[i]=C(i,N)*2;
    }
    LL an=0,now=C(2*N,N);
    for(int i=2*N;i>N;i--){
        ADD(an,now);
        now=(now+one[i-1])*mypow(2,MOD-2)%MOD;
    }
