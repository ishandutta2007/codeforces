const int MAXN = 1e6 + 1;
const int p = 1e9 + 7;

long long gcd(long long a, long long b) {
    return a ? gcd(b % a, a) : b;
}

int n, g = 0;
vector<int> edge[MAXN];
int w[MAXN], c[MAXN];
long long s[MAXN];
int dp[MAXN];

void dfs(int i, int p = -1) {
    s[i] = w[i];
    for (int j : edge[i]) {
        if (j == p) continue;
        dfs(j, i);
        s[i] += s[j];
    }
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
    cin >> n;
    for (int i = 0; i < n; ++i) {
        cin >> w[i];
    }
    for (int i = 1; i < n; ++i) {
        int a;
        cin >> a;
        edge[a - 1].pb(i);
    }
    dfs(0);
    for (int i = 0; i < n; ++i) {
        long long g = gcd(s[0], s[i]);
        g = s[0] / g;
        if (g <= n) {
            c[g]++;
        }
    }
    for (int i = n; i >= 1; --i) {
        for (int j = i * 2; j <= n; j += i) {
            c[j] += c[i];
        }   
    }
    int ans = 0;
    dp[1] = 1;
    for (int i = 1; i <= n; ++i) {
        ans += dp[i];
        if (ans >= p) ans -= p;
        for (int j = i * 2; j <= n; j += i) {
            if (c[j] == j) {
                dp[j] += dp[i];
                if (dp[j] >= p) dp[j] -= p;
            }
        }
    }
    cout << ans << endl;
}