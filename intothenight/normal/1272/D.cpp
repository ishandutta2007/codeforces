int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	vector<vi> t;
	int res = 0;
	for(int i = 0; i < n; ){
		int j = i + 1;
		while(j < n && a[j - 1] < a[j]) j ++;
		t.push_back(vi());
		for(int k = i; k < j; k ++){
			t.back().push_back(a[k]);
		}
		res = max(res, j - i);
		i = j;
	}
	for(int i = 0; i < t.size(); i ++){
		if(t[i].size() == 1){
			if(i && i != t.size() - 1 && t[i - 1].back() < t[i + 1].front()){
				res = max<int>(res, t[i - 1].size() + t[i + 1].size());
			}
		}
		else{
			if(i && t[i - 1].back() < t[i][1]){
				res = max<int>(res, t[i - 1].size() + t[i].size() - 1);
			}
			if(i != t.size() - 1 && t[i][t[i].size() - 2] < t[i + 1].front()){
				res = max<int>(res, t[i].size() + t[i + 1].size() - 1);
			}
		}
	}
	cout << res;
	return 0;
}
 
