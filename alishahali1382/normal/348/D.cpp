#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<x<<endl;
#define debugp(x) cerr<<#x<<"= {"<<x.first<<", "<<x.second<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const ll inf=1e18+100;
const int mod = 1000000007;
const int MAXN = 3010;

int n, m, ans, x, y, tmp;
ll dp[MAXN][MAXN][2];
int A[MAXN][MAXN];
char ch;

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=1; i<=n; i++){
		for (int j=1; j<=m; j++){
			cin>>ch;
			A[i][j]=(ch=='.');
		}
	}
	dp[1][1][0]=dp[1][1][1]=1;
	for (int i=2; i<=n; i++) dp[i][1][0]=A[i][1]*dp[i-1][1][0];
	for (int i=2; i<=m; i++) dp[1][i][1]=A[1][i]*dp[1][i-1][1];
	for (int i=2; i<=n; i++){
		for (int j=2; j<=m; j++){
			if (!A[i][j]) continue ;
			dp[i][j][0]=(dp[i-1][j][0]+dp[i][j-1][0])%mod;
			dp[i][j][1]=(dp[i-1][j][1]+dp[i][j-1][1])%mod;
		}
	}
	cout<<((dp[n][m-1][0]*dp[n-1][m][1]-dp[n-1][m][0]*dp[n][m-1][1])%mod+mod)%mod<<endl;
	
	return 0;
}
