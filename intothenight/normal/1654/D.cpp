int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	number_theory nt(200000);
	auto __solve_tc = [&](auto __tc_num)->int{
		int n;
		cin >> n;
		graph<pair<int, int>> g(n);
		for(auto i = 0; i < n - 1; ++ i){
			int u, v, x, y;
			cin >> u >> v >> x >> y, -- u, -- v;
			g.link(u, v, {x, y});
		}
		vector<int> deg(n + 1);
		vector<int> mindeg(n + 1);
		vector<modular> res(n, 1);
		y_combinator([&](auto self, int u, int p)->void{
			for(auto id: g.adj[u] | ranges::views::filter([&](int id){ return !g.ignore || !g.ignore(id);})){
				auto &e = g.edge[id];
				int v = u ^ e.from ^ e.to;
				if(v == p){
					continue;
				}
				auto [x, y] = e.cost;
				if(e.from != u){
					swap(x, y);
				}
				map<int, int> mp;
				for(auto t = x; t > 1; t /= nt.lpf[t]){
					-- mp[nt.lpf[t]];
				}
				for(auto t = y; t > 1; t /= nt.lpf[t]){
					++ mp[nt.lpf[t]];
				}
				for(auto [prime, e]: mp){
					deg[prime] += e;
					ctmin(mindeg[prime], deg[prime]);
				}
				res[v] = res[u] * y / x;
				self(v, u);
				for(auto [prime, e]: mp){
					deg[prime] -= e;
				}
			}
		})(0, -1);
		debug(res);
		debug(mindeg);
		modular global = 1;
		for(auto prime = 2; prime <= n; ++ prime){
			if(mindeg[prime]){
				global *= modular(prime) ^ -mindeg[prime];
			}
		}
		for(auto &x: res){
			x *= global;
		}
		cout << accumulate(res.begin(), res.end(), modular(0)) << "\n";
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

