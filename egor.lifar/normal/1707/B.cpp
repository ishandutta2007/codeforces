const int MAXN = 500228;

int n;
int a[MAXN];
int b[MAXN];
int cnt[MAXN], cnt1[MAXN];


void solve() {
	cin >> n;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
	}
	for (int i = 0; i < n - 1; i++) {
		b[i] = a[i + 1] - a[i];
	}	
	n--;
	int td = 0;
	vector<int> st;
	for (int i = 0; i < n; i++) {
		cnt[b[i]]++;
		st.pb(b[i]);
		chkmax(td, b[i]);
	}
	sort(all(st));
	st.resize(distance(st.begin(), unique(all(st))));
	while (n > 1) {
		int cnt0 = 0;
		int last = -1;
		int td1 = 0;
		vector<int> fk;
		for (auto i: st) {
			cnt0 += max(cnt[i] - 1, 0);
			if (cnt[i]) {
				if (last != -1) {
					cnt1[i - last]++;
					fk.pb(i - last);
					chkmax(td1, i - last);
				}
				last = i;
			}
			cnt[i] = 0;
		}
		if (cnt0) {
			fk.pb(0);
		}
		sort(all(fk));
		fk.resize(distance(fk.begin(), unique(all(fk))));
		for (int i = 0; i < sz(fk); i++) {
			cnt[fk[i]] = cnt1[fk[i]];
			cnt1[fk[i]] = 0;
		}
		cnt[0] = cnt0;
		swap(st, fk);
		td = td1;
		n--;
	}
	cnt[td] = 0;
	cout << td << '\n';
}


