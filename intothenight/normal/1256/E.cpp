int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<pii> a(n);
	for(int i = 0; i < n; i ++){
		cin >> a[i].first, a[i].second = i;
	}
	sort(all(a));
	int res = a.back().first - a.front().first;
	vi d(n + 1), cut(n + 1);
	d[1] = d[2] = 1e9, d[3] = 0;
	int p = 0, cur = 0;
	for(int i = 4; i <= n; i ++){
		if(cur > d[i - 3] - a[i - 3].first + a[i - 4].first){
			p = i - 3;
			cur = d[i - 3] - a[i - 3].first + a[i - 4].first;
		}
		d[i] = cur, cut[i] = p;
	}
	cout << a.back().first - a.front().first + d[n] << " ";
	int cnt = 0;
	vi t(n);
	for(int i = n; i; i = cut[i]){
		cnt ++;
		for(int j = cut[i]; j < i; j ++){
			t[a[j].second] = cnt;
		}
	}
	cout << cnt << "\n" << t;
	return 0;
}
 
