using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<pii, int> piii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<x<<endl;
#define debugp(x) cerr<<#x<<"= {"<<x.first<<", "<<x.second<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back
#define kill(x) cout<<x<<'\n', exit(0);

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

int n, m, k, u, v, x, y, t=1, a, b, ans;
int ord[MAXN];
int A[MAXN];
vector<int> G[MAXN], out;

int dfs0(int node, int p){
	if (ord[node]<inf) return ord[node];
	for (int v:G[node]){
		if (v==p) continue ;
		ord[node]=min(ord[node], dfs0(v, node));
	}
	return ord[node];
}
void dfs(int node, int p){
	if (node!=1 && G[node].size()==1){
		if (A[t++]!=node) kill(-1);
	}
	for (int v:G[node]){
		if (v==p) continue ;
		out.pb(v);
		dfs(v, node);
		out.pb(node);
	}
}

bool cmp(int i, int j){ return ord[i]<ord[j];}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	memset(ord, 63, sizeof(ord));
	cin>>n;
	for (int i=0; i<n-1; i++){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	for (int i=2; i<=n; i++) if (G[i].size()==1) k++;
	for (int i=1; i<=k; i++){
		cin>>A[i];
		ord[A[i]]=i;
	}
	dfs0(1, 0);
	for (int i=1; i<=n; i++) sort(all(G[i]), cmp);
	dfs(1, 0);
	cout<<"1 ";
	for (int i:out) cout<<i<<' ';
	
	return 0;
}
