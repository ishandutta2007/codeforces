#include<bits/stdc++.h>
#define ll long long
using namespace std;
int f[111][111][2][2];
int main()
{
	int T,i,j;cin>>T;
	f[0][0][0][0]=f[0][0][1][0]=1;
	f[0][0][0][1]=f[0][0][1][1]=0;
	for (i=1;i<=100;i++)
	{
		f[i][0][0][0]=f[i][0][1][0]=1;
		f[i][0][0][1]=f[i][0][1][1]=0;
		f[i][i][1][0]=!((i/2)&1);f[i][i][0][0]=!((i-i/2)&1);
		f[i][i][1][1]=(i/2)&1;f[i][i][0][1]=(i-i/2)&1;
		for (j=1;j<i;j++)
		{
			f[i][j][0][0]=(f[i-1][j][1][0] || f[i-1][j-1][1][1]);
			f[i][j][1][0]=(f[i-1][j][0][0] && f[i-1][j-1][0][0]);
			f[i][j][0][1]=(f[i-1][j][1][1] || f[i-1][j-1][1][0]);
			f[i][j][1][1]=(f[i-1][j][0][1] && f[i-1][j-1][0][1]);
		}
	}
	while (T--)
	{
		int n,i,x,s=0;cin>>n;
		for (i=1;i<=n;i++){cin>>x;s+=(x&1);}
		if (f[n][s][0][0]) puts("Alice");
		else puts("Bob");
	}
}