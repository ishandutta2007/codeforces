const int N = 1e6 + 5;

int n[3];
vector<int> a[3];


long long f(long long a, long long b, long long c) {
    b -= a;
    c -= a;
    return b * b + c * c + (b - c) * (b - c);
}

void solve() {
    cin >> n[0] >> n[1] >> n[2];
    for (int i = 0; i < 3; ++i) {
        a[i].resize(n[i]);
        cin >> a[i];
        sort(a[i].begin(), a[i].end());
        a[i].resize(unique(a[i].begin(), a[i].end()) - a[i].begin());
        n[i] = a[i].size();
    }
    vector<int> p = {0, 1, 2};
    long long ans = 2e18;
    do {
        int it1 = 0, it2 = 0;
        for (int v : a[p[0]]) {
            while (it1 < n[p[1]] && a[p[1]][it1] < v) ++it1;
            if (it1 == n[p[1]]) --it1;
            int m = (v + a[p[1]][it1]) >> 1;
            while (it2 < n[p[2]] && a[p[2]][it2] < m) ++it2;
            for (int d = -2; d <= 2; ++d) {
                int _it2 = d + it2;
                if (_it2 < 0 || _it2 >= n[p[2]]) continue;
                chkmin(ans, f(v, a[p[1]][it1], a[p[2]][_it2]));
            }
        }
    } while (next_permutation(p.begin(), p.end()));
    cout << ans << '\n';
}
