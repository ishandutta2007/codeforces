{
	(void)Recall,(void)_902_;
	int n=read(),x=read(),m=0,ans0=0,tot=0;
	for (int i=1;i<=n;i++)
	{
		int k=read()<x;
		if (!k) tot++;
		else f[m++].x=tot+1,ans0+=tot*(tot+1)/2,tot=0;
	}
	f[m++].x=tot+1,ans0+=tot*(tot+1)/2,tot=0;
	memcpy(g,f,sizeof(g));
	reverse(g,g+m);
	conv(f,g,m);
	print(ans0);
	for (int i=1;i<m;i++) print(f[m-i-1].x+eps);
	for (int i=m;i<=n;i++) print(0);
	
	return 0;
}
