vector<int> z_function (string s) {
    int n = (int) s.length();
    vector<int> z (n);
    for (int i=1, l=0, r=0; i<n; ++i) {
        if (i <= r)
            z[i] = min (r-i+1, z[i-l]);
        while (i+z[i] < n && s[z[i]] == s[i+z[i]])
            ++z[i];
        if (i+z[i]-1 > r)
            l = i,  r = i+z[i]-1;
    }
    return z;
}
vector<int> prefix_function (string s) {
    int n = (int) s.length();
    vector<int> pi (n);
    for (int i=1; i<n; ++i) {
        int j = pi[i-1];
        while (j > 0 && s[i] != s[j])
            j = pi[j-1];
        if (s[i] == s[j])  ++j;
        pi[i] = j;
    }
    return pi;
}
const int M=1111*1111;
int n, k, h[M];
char s[M], rs[M];
int main()
{
    scanf("%d %d\n", &n, &k);
    scanf("%s", s);
    vector<int> pf=prefix_function(s), zf=z_function(s);
    for (int i=0, ba=0;i<n;++i)
    {
        int l=i+1, d=l-pf[i];
        if (l%d==0&&(l/d)%k==0)
        {
            ++h[i];
            int ABs=l/k, r=i;
            if (i+1<n) r=min(i+ABs, i+zf[i+1]);
            --h[r+1];
        }
        ba+=h[i];
        if (ba||(k==1)) rs[i]='1'; else rs[i]='0';
    }
    puts(rs);
    return 0;
}