fn main() {
	let (mut rin,mut rout) = rio();

	rp!{[rin.u()]
		let n = rin.u();
		let mut a = rin.vu(n);
		let f = |a: &[usize]| -> Vec<bool> {
			let mut res = v!([n+1] = false);
			let mut c = v!([n] = 0);
			l!(mut k,m = 0);
			for i in 0..n {
				c[a[i]] += 1;
				if c[a[i]] > 1 {
					break;
				} else {
					k += 1;
					max!(m, a[i]);
					if k == m {
						res[i+1] = true;
					}
				}
			}
			res
		};
		let da = f(&a);
		a.reverse();
		let db = f(&a);
		let mut ans = v!([]);
		for i in 1..n {
			if da[i] && db[n-i] {
				ans.push( (i,n-i) );
			}
		}
		writeln!(rout, "{}", ans.len()).unwrap();
		for (x,y) in ans {
			writeln!(rout, "{} {}", x, y).unwrap();
		}
	}
}