int main(){
	cin.tie(0)->sync_with_stdio(0);
	int tt;
	cin >> tt;
	while(tt --){
		int n;
		cin >> n;
		vi a(2 * n);
		cin >> a;
		vi l(1), r(1);
		vi lp(2 * n + 1, -1), rp(2 * n + 1, -1);
		lp[n] = rp[n] = 0;
		for(int i = 0; i < n; i ++){
			l.push_back(l.back() + (a[i] == 1 ? 1 : -1));
			r.push_back(r.back() + (a[2 * n - 1 - i] == 1 ? 1 : -1));
			lp[l.back() + n] = i + 1;
			rp[r.back() + n] = i + 1;
		}
		int res = 0;
		for(int i = 0; i <= 2 * n; i ++){
			if(lp[i] != -1 && rp[2 * n - i] != -1){
				res = max(res, lp[i] + rp[2 * n - i]);
			}
		}
		cout << 2 * n - res << "\n";
	}
	return 0;
}
 
