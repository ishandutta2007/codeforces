inline int readint() {
    int a = 0; char c = getchar(), p = 0;
    while(isspace(c)) c = getchar();
    if(c == '-') p = 1, c = getchar();
    while(isdigit(c)) a = a*10 + c - '0', c = getchar();
    return p ? -a : a;
}

const int maxN = 1000000 + 233;
int n, m, c[maxN];
int f[5][5], g[5][5];

int main() {
    n = readint(); m = readint();
    memset(c, 0, sizeof(c));
    for(int i = 0; i < n; ++i) c[readint()] ++;
    memset(f, 0x80, sizeof(f));
    f[0][0] = 0;
    for(int i = 1; i <= m; ++i) {
        memset(g, 0x80, sizeof(g));
        for(int x = 0; x < 5; ++x) for(int y = x; y < 5; ++y) {
            for(int z = x; z < x + 5 && z <= c[i]; ++z) {
                chmax(g[y - x][z - x], f[x][y] + x + (c[i] - z) / 3);
            }
        }
        for(int i = 4; i >= 0; --i) for(int j = 4; j >= 0; --j) {
            if(i) chmax(g[i - 1][j], g[i][j]);
            if(j) chmax(g[i][j - 1], g[i][j]);
        }
        memcpy(f, g, sizeof(g));
    }
    int ans = -maxN;
    for(int i = 0; i < 5; ++i) for(int j = 0; j < 5; ++j) chmax(ans, f[i][j]);
    printf("%d\n", ans);
    return 0;
}