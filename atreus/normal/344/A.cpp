#include <bits/stdc++.h>

using namespace std;

int main(){
    ios_base::sync_with_stdio(false);
    int n;
    cin >> n;
    string s[n];
    int ans = 0;
    for (int i = 0; i < n; i++){
        cin >> s[i];
        if (s[i] != s[i - 1])
            ans++;
    }
    cout << ans;
}