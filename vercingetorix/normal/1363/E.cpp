const int A = 200500;
vi g[A];
int a[A];
int dif[A];
ll ans = 0;

void dfs1(int v, int p) {
    int sa = abs(dif[v]);
    for(auto u : g[v]) {
        if(u == p) continue;
        a[u] = min(a[u], a[v]);
        dfs1(u,v);
        dif[v] += dif[u];
        sa += abs(dif[u]);
    }
    ans += ll(sa - abs(dif[v])) * a[v];
}

