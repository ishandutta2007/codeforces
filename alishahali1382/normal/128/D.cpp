bool check(vector<int> &B, int x){
	int n=B.size();
	sort(all(B));
	for (int i=0; 2*i<n; i++) A[i]=B[2*i];
	for (int i=1, j=n-1; i<n; i+=2, j--) A[j]=B[i];
	//for (int i=0; i<n; i++) cerr<<A[i]<<' ';cerr<<endl;
	A[n]=A[0];
	for (int i=0; i<n; i++) if (abs(A[i]-A[i+1])>x) return 0;
	return 1;
}

