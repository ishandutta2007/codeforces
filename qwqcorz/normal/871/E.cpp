#include<bits/stdc++.h>
using namespace std;
const int N=3e4+5;
const int M=205;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,nxt;
}e[N*2];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
vector<int>v[N];
int d[M][N],a[M],nd[N],fa[N],bel[N],dis[N];
void dfs(int now,int father)
{
	dis[now]=dis[father]+1;
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father) continue;
		dfs(to,now);
	}
}
bool check(int n,int k)
{
	dis[0]=-1;
	for (int i=1;i<=k;i++)
	{
		for (int j=1;j<=n;j++) dis[j]=0;
		dfs(a[i],0);
		for (int j=1;j<=n;j++) if (d[i][j]!=dis[j]) return 0;
	}
	return 1;
}
bool cmp(int x,int y){return d[1][x]<d[1][y];}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),k=read();
	for (int i=1;i<=k;i++)
	for (int j=1;j<=n;j++) if (!(d[i][j]=read())) a[i]=j;
	for (int i=1;i<=n;i++) bel[i]=a[1];
	for (int i=2;i<=k;i++)
	{
		int dist=d[1][a[i]];
		for (int j=1;j<=n;j++) if (d[1][j]+d[i][j]==dist) nd[d[1][j]]=j;
		for (int j=1;j<=dist;j++) fa[nd[j]]=nd[j-1];
		for (int j=1;j<=n;j++)
		if (d[1][j]-d[i][j]+dist>=0)
		if (!bel[j]||d[1][bel[j]]<(d[1][j]-d[i][j]+dist)/2)
		bel[j]=nd[(d[1][j]-d[i][j]+dist)/2];
	}
	for (int i=1;i<=n;i++) if (!fa[i]&&i!=a[1]) v[bel[i]].push_back(i);
	for (int i=1;i<=n;i++)
	if (fa[i]||i==a[1])
	{
		int last=i;
		sort(v[i].begin(),v[i].end(),cmp);
		for (int j=0;j<(int)v[i].size();j++)
		{
			fa[v[i][j]]=last;
			if (j+1==(int)v[i].size()||d[1][v[i][j]]!=d[1][v[i][j+1]]) last=v[i][j];
		}
	}
	int cnt=0;
	for (int i=1;i<=n;i++) if (fa[i]) cnt++,add_edge(i,fa[i]),add_edge(fa[i],i);
	if (cnt!=n-1||!check(n,k)) return puts("-1"),0;
	for (int i=1;i<=n;i++) if (fa[i]) print(i,' '),print(fa[i]);
	
	return 0;
}
