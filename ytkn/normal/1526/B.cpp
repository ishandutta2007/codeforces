using namespace std;
typedef long long ll;

void solve(){
    ll n; cin >> n;
    for(ll i = 0; i < 111; i++){
        if(i*11 > n) break;
        ll m = n-i*11;
        if(m%111 == 0){
            cout << "YES" << endl;
            return;
        }
    }
    cout << "NO" << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}