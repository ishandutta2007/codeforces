matrix fibinc(2,2),fibincof0(2,2),resof0(2,1);//Fibonacci Increase Matrix
void INITfibinc()
{
    fibinc[0][0]=1;
    fibinc[0][1]=1;
    fibinc[1][0]=1;
    fibinc[1][1]=0;
    fibincof0[0][0]=1;
    fibincof0[0][1]=0;
    fibincof0[1][0]=0;
    fibincof0[1][1]=1;
    resof0[0][0]=0;
    resof0[1][0]=0;
}
//Init Fibonacci Increase Matrix

//Segment Tree
matrix dt[MAXN],ic[MAXN];
void INITdtic()
{
    for(int i=0;i<MAXN;i++)
    {
        dt[i].resize(2,1);
        ic[i].resize(2,1);
        dt[i][0][0]=dt[i][1][0]=ic[i][0][0]=ic[i][1][0]=0;
    }
}

void assg(int now,int nl,int nr,int ai,int vi)//Assign value
{
    if(nl==nr&&nr==ai)
    {
        dt[now][0][0]=vi;
        return;
    }
    int m=(nl+nr)/2;
    if(m>=ai)assg(now*2,nl,m,ai,vi);
    else assg(now*2+1,m+1,nr,ai,vi);
    dt[now]=fibinc.pow(m-nl+1)*(dt[now*2+1]+ic[now*2+1])+(dt[now*2]+ic[now*2]);
}

void pls(int now,int nl,int nr,int l,int r,int vi)//plus value
{
    if(nl>=l&&nr<=r)
    {
        ic[now][0][0]+=vi;
        return;
    }
    if(nl>r||nr<l)return;
    int m=(nl+nr)/2;
    pls(now*2,nl,m,l,r,vi);
    pls(now*2+1,m+1,nr,l,r,vi);
    dt[now]=fibinc.pow(m-nl+1)*(dt[now*2+1]+ic[now*2+1])+(dt[now*2]+ic[now*2]);
}

matrix calc(int now,int nl,int nr,int l,int r)
{
    dt[now]+=ic[now];
    ic[now*2]+=ic[now];
    ic[now*2+1]+=ic[now];
    ic[now]=resof0;
    if(nl>=l&&nr<=r)return dt[now]+ic[now];
    int m=(nl+nr)/2;
    matrix res=resof0,fibb=fibincof0;
    if(m>=l)
    {
        res+=calc(now*2,nl,m,l,r);
        fibb=fibinc.pow(m-max(l,nl)+1);
    }
    if(m<r)
        res+=fibb*calc(now*2+1,m+1,nr,l,r);
    res+=ic[now];
    return res;
}

