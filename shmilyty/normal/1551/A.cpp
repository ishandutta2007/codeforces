const int INF=4e18;
inline int read()
{
	int x=0;
	bool flag=1;
	char c=getchar();
	while(c<'0'||c>'9')
	{
		if(c=='-')
			flag=0;
		c=getchar();
	}
	while(c>='0'&&c<='9')
	{
		x=(x<<1)+(x<<3)+c-'0';
		c=getchar();
	}
	return (flag?x:~(x-1));
}
int t,n;
void solve()
{
	n=read();
	int x=n/3,y=n/3;
	if(n%3==1)
		x++;
	if(n%3==2)
		y++;
	cout<<x<<" "<<y<<endl;
}
signed main()
{
	t=read();
	while(t--)
		solve();
	return 0;
}