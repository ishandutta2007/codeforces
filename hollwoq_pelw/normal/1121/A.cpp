#include <bits/stdc++.h>
using namespace std;
int main(){
    int n,m,k;
    cin >> n >> m >> k;
    int a[n], pw[n];
    int p[m];
    for (int i = 0; i < m; i++) p[i] = 0;
    for (int i = 0; i < n; i++) cin >> a[i];
    for (int i = 0; i < n; i++){
        cin >> pw[i];
        p[pw[i]-1] = max(p[pw[i]-1], a[i]);
    }
    int ans = 0, t;
    for (int i = 0; i < k; i++){
        cin >> t; //cout << a[t-1] << endl;
        ans += (a[t-1] != p[pw[t-1]-1]);
    }
    cout << ans;
}
