bool valid(int x, int y) {
  if (x==ax) return false;
  if (y==ay) return false;
  if (x+y==ax+ay) return false;
  if (x-y==ax-ay) return false;
  return 1<=x&&x<=n && 1<=y&&y<=n;
}
void dfs(int x, int y) {
  if (!valid(x,y)) return;
  if (seen[x][y]) return;
  seen[x][y]=true;
  for (int dx=-1;dx<=1;dx++) for (int dy=-1;dy<=1;dy++) {
    dfs(x+dx,y+dy);
  }
}

