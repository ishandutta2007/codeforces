template <typename T>
void read(T &x) {
    x = 0; bool f = 0;
    char c = getchar();
    for (;!isdigit(c);c=getchar()) if (c=='-') f=1;
    for (;isdigit(c);c=getchar()) x=x*10+(c^48);
    if (f) x=-x;
}

template<typename F>
inline void write(F x, char ed = '\n') {
	static short st[30];short tp=0;
	if(x<0) putchar('-'),x=-x;
	do st[++tp]=x%10,x/=10; while(x);
	while(tp) putchar('0'|st[tp--]);
	putchar(ed);
}

template <typename T>
inline void Mx(T &x, T y) { x < y && (x = y); }

template <typename T>
inline void Mn(T &x, T y) { x > y && (x = y); }

const int N = 500005;
int a[N], T, n;

int query(int x) {
	if (x == 0 || x > n) return 1e9;
	if (a[x]) return a[x];
	cout << "? " << x << endl;
	fflush(stdout);
	cin >> a[x];
	return a[x];
}



int main() {
	read(n);
	int l = 1, r = n;
	while (l <= r) {
		int mid = (l + r) >> 1;
		int tl = query(mid - 1), t = query(mid), tr = query(mid + 1);
		if (t < tl && t < tr) { cout << "! " << mid << endl; fflush(stdout); return 0; }
		if (tl < t) r = mid - 1;
		else l = mid + 1;
	}
	return 0;
}