inline int read(){
    int x=0,f=0;char ch=getchar();
    while(!isdigit(ch))f|=ch=='-',ch=getchar();
    while(isdigit(ch))x=10*x+ch-'0',ch=getchar();
    return f?-x:x;
}
template<typename T>void print(T x){
    if(x<0)putchar('-'),x=-x;
    if(x>=10)print(x/10);
    putchar(x%10+'0');
}
template<typename T>void print(T x,char ch){
    print(x),putchar(ch);
}

const int N=200005;

char a[N];int n;

int main(){
    int T=read();
    while(T--){
        scanf("%s",a+1);
        n=strlen(a+1);
        int cnt=0;
        if(a[n]=='A'){
            puts("NO");continue;
        }
        bool flag=0;
        for(int i=1;i<=n;i++){
            if(a[i]=='B')cnt++;
            else cnt--;
            if(cnt>0){
                puts("NO");
                flag=1;
                break;
            }
        }
        if(!flag)puts("YES");
    }
}