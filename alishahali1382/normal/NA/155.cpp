#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;

ll cw(ll x1, ll y1, ll x2, ll y2){
	if(x1>x2 || y1>y2) return 0;
	ll s=(abs(x2-x1)+1)*(abs(y2-y1)+1);
	s=max(0ll, s);
	if(s%2==0) return s/2;
	return s/2+((x1+y1)%2==0?1:0);
}

ll cb(ll x1, ll y1, ll x2, ll y2){
	if(x1>x2 || y1>y2) return 0;
	ll s=(x2-x1+1)*(y2-y1+1);
	s=max(0ll,s);
	if(s%2==0) return s/2;
	return s/2+((x1+y1)%2==0?0:1);
}

void MAIN(){
	ll n, m;
	cin>>n>>m;
	ll x1,x2,y1,y2,x3,y3,x4,y4,x5,y5,x6,y6;
	cin>>x1>>y1>>x2>>y2;
	cin>>x3>>y3>>x4>>y4;
	ll w=cw(1,1,n,m);
	ll b=cb(1,1,n,m);
	w+=cb(x1, y1, x2, y2);
	b-=cb(x1, y1, x2, y2);
	w-=cw(x3, y3, x4, y4);
	b+=cw(x3, y3, x4, y4);
	ll xx1=max(x3, x1), yy1=max(y1, y3);
	ll xx2=min(x2, x4), yy2=min(y2, y4);
	
	w-=cb(xx1,yy1,xx2,yy2);
	b+=cb(xx1,yy1,xx2,yy2);
	
	cout<<w<<' '<<b<<'\n';
	return ;
}

int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	ll t;
	cin>>t;
	while(t--) MAIN();
	
	return 0;
}
