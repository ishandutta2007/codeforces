  rep(i,m) {
    int a,b,c;
    scanf("%d%d%d",&a,&b,&c);
    --a; --b;
    d[a] += c;
    d[b] -= c;
  }
  vi l, r;
  rep(i,n) {
    if (d[i] < 0) r.pb(i), d[i] = -d[i];
    else if (d[i] > 0) l.pb(i);
  }
  vt ans;
  while (sz(l) && sz(r)) {
    int a = l.back(), b = r.back();
    ll x = min(d[a],d[b]);
    ans.eb(a+1,b+1,x);
    d[a] -= x; d[b] -= x;
    if (d[a] == 0) l.pop_back();
    if (d[b] == 0) r.pop_back();
  }
  printf("%d\n",sz(ans));
  rep(i,sz(ans)) {
    int a, b; ll x;
    tie(a,b,x) = ans[i];
    printf("%d %d %lld\n",a,b,x);
  }
  return 0;
