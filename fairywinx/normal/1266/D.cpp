void solve() {
	int n, m;
	cin >> n >> m;
	vector<int> sum(n);
	int sum_ans = 0;
	for(int i = 0; i < m; ++i) {
		int a, b, c;
		cin >> a >> b >> c;
		sum[a - 1] += c;
		sum[b - 1] -= c;
	}
	set<pair<int, int>> a;
	for(int i = 0; i < n; ++i) { 
		a.emplace(sum[i], i);
		sum_ans += abs(sum[i]);
	}
	vector<pair<pair<int, int>, int>> ans;
	while(a.begin()->first != 0) {
		auto z = *a.begin(), p = *a.rbegin();
		int c = min(abs(z.first), abs(p.first));
		a.erase(*a.begin());
		a.erase(*a.rbegin());
		sum[p.second] -= c;
		sum[z.second] += c;
		ans.push_back({{p.second + 1, z.second + 1}, c});
		a.emplace(sum[p.second], p.second);
		a.emplace(sum[z.second], z.second);
	}
	cout << ans.size() << '\n';
	for(auto i: ans)
		cout << i.first.first << ' ' << i.first.second << ' ' << i.second << '\n';
}

signed main() {
	setup();
	// SOLVE;
	solve();

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

