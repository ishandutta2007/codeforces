    ll ans = 0;
    vb seen(sz(xc));
    binary_indexed_tree<int> xbit(sz(xc));

    for (auto i = pts.begin(); i != pts.end(); ) {
        vi insx;
        auto j = i;
        while (j != pts.end() && j->s == i->s)
            insx.pb((j++)->f);
        dedup(insx);
        i = j;

        int pinx = -1;
        trav (x, insx) {
            ans -= ch(xbit.sum(pinx + 1, index(xc, x)));
            pinx = index(xc, x);
        }
        ans -= ch(xbit.sum(pinx + 1, xbit.S));

        trav (x, insx) {
            int xi = index(xc, x);
            if (!seen[xi]) {
                seen[xi] = true;
                xbit.add(xi, 1);
            }
        }

        int tot = xbit.sum(0, xbit.S);
        ans += ch(tot);
    }

    ps(ans);

    return 0;
