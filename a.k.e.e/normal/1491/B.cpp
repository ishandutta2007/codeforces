const int MAXN=105;

int n,u,v,a[MAXN];
void solve()
{
	readint(n),readint(u),readint(v);
	for(int i=1;i<=n;++i)readint(a[i]);
	int ans=2147483647;
	for(int i=1;i<n;++i)
	{
		if(abs(a[i]-a[i+1])>=2)ans=0;
		else if(abs(a[i]-a[i+1])==1)chkmin(ans,min(u,v));
		else chkmin(ans,v+min(u,v));
	}
	printf("%d\n",ans);
}

