using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(!isdigit(ch)){w |= ch == '-';ch = getchar();}
	while(isdigit(ch)){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int n, a[200005];
void solve()
{
	n = read();
	for (int i = 1; i <= n; i++) a[i] = read();
	int mn = 1, mx = 1;
	for (int i = 2; i <= n; i++)
	{
		if(a[i] < a[mn]) mn = i;
		if(a[i] > a[mx]) mx = i;
	 } 
	 printf("%d %d\n", mn, mx);
}
int main()
{
	int T = read();
	while(T--) solve(); 
	return 0;
}