template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

template <class T>
void chkMax(T& x, const T& y) {
  if (x < y) x = y;
}

template <class T>
void chkMin(T& x, const T& y) {
  if (x > y) x = y;
}


