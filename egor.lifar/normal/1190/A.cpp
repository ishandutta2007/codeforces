	cin >> n >> m >> k;
	for (int i = 0; i < m; i++) {
		cin >> p[i];
	}
	long long curcnt = 0;
	long long i = 0;
	long long res = 0;
	while (i < m) {
		res++;
		long long pos = p[i] - curcnt;
		long long kek = 0;
		while (i < m && (pos - 1) / k == (p[i] - curcnt - 1) / k) {
			kek++;
			i++;
		}
		curcnt += kek;
	}
