	void put(char[][] f, int x, int y) {
		for (int i = x - 1; i <= x + 1; i++) {
			for (int j = y - 1; j <= y + 1; j++) {
				if (i >= 0 && i < f.length && j >= 0 && j < f[i].length && f[i][j] == '1') {
					return;
				}
			}
		}
		f[x][y] = '1';
	}
	
	void solve(int h, int w) {
		char[][] f = new char[h][w];
		for (int i = 0; i < h; i++) {
			Arrays.fill(f[i], '0');
		}
		for (int i = 0; i < w; i++) {
			put(f, 0, i);
		}
		for (int i = 0; i < h; i++) {
			put(f, i, w - 1);
		}
		for (int i = w - 1; i >= 0; i--) {
			put(f, h - 1, i);
		}
		for (int i = h - 1; i >= 0; i--) {
			put(f, i, 0);
		}
		for (char[] row : f) {
			out.println(row);
		}
	}
	
	void submit() {
		int t = nextInt();
		while (t-- > 0) {
			int h = nextInt();
			int w = nextInt();
			solve(h, w);
		}
	}

