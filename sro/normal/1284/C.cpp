#include <bits/stdc++.h>
using namespace std;

int jc[250005];
int n;
int jt;

int main(){
	cin>>n>>jt;
	jc[0]=1;
	for(int i=1;i<=n;i++){
		jc[i]=1ll*jc[i-1]*i%jt;
	}
	int ans=0;
	for(int i=1;i<=n;i++){
		ans=(ans+1ll*jc[i]*jc[n-i]%jt*(n-i+1)%jt*(n-i+1)%jt)%jt;
	}
	cout<<ans<<endl;
	return 0;
}