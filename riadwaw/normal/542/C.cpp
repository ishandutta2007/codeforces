void solve() {
	int n;
	cin >> n;
	vector<int> vals(n);

	for (int i = 0; i < n; ++i) {
		cin >> vals[i];
		--vals[i];
	}

	

	li g = 1;

	int mn = 1;

	for (int i = 0; i < n; ++i) {
		vector<int> used(n);

		int pos = i;
		int num = 0;
		for (num = 1; !used[pos]; ++num) {
			used[pos] = num;

			pos = vals[pos];
		}

		int len = num - used[pos];


		g *= len / gcd(g, len);
		mn = max(mn, used[pos] - 1);
	}

	//cout << mn << ' ' << g << endl;
	cout << (mn + g - 1) / g * g;
}