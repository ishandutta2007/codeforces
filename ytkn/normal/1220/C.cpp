template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    string s; cin >> s;
    int n = s.size();
    string cum_min;
    for(int i = 0; i < n; i++){
        if(i == 0) {
            cum_min += s[i];
        }else{
            cum_min += min(s[i], cum_min.back());
        }
    }
    cout << "Mike" << endl;
    for(int i = 1; i < n; i++){
        if(cum_min[i-1] < s[i]){
            cout << "Ann" << endl;
        }else{
            cout << "Mike" << endl;
        }
    }
}