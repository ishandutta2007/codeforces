int a[105], l[105];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        for (int i=0; i<n; i++)
            cin >> a[i];
        vector<int> v;
        for (int i=0; i<n; i++) {
            cin >> l[i];
            if (!l[i]) v.push_back(a[i]);
        }
        sort(v.rbegin(), v.rend());
        for (int i=0, j=0; i<n; i++) {
            if (!l[i])
                a[i] = v[j++];
            cout << a[i] << " ";
        }
        cout << "\n";
    }

    return 0;
}