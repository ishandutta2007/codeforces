void run_case() {
    int N, K;
    cin >> N >> K;
    vector<int> X(N);

    for (auto &x : X)
        cin >> x;

    for (int i = 0; i < N; i++) {
        int y;
        cin >> y;
    }

    sort(X.begin(), X.end());
    vector<int> dp_suffix(N + 1, 0);
    int best = 0;

    for (int i = N - 1, j = N; i >= 0; i--) {
        while (j > i && X[j - 1] > X[i] + K)
            j--;

        best = max(best, j - i + dp_suffix[j]);
        dp_suffix[i] = max(j - i, dp_suffix[i + 1]);
    }

    cout << best << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}