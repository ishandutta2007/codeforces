const int MAXN = 1001;

bool used[MAXN][MAXN];
int dx[8] = {0, 0, 1, -1, 1, 1, -1, -1};
int dy[8] = {-1, 1, 0, 0, 1, -1, 1, -1};
int n;
int ax, ay;


void dfs(int i, int j) {
    used[i][j] = true;
    for (int k = 0; k < 8; k++) {
        int i1 = i + dx[k];
        int j1 = j + dy[k];
        if (i1 <= 0 || j1 <= 0 || i1 > n || j1 > n) {
            continue;
        }
        if (i1 == ax || j1 == ay) {
            continue;
        }
        if (i1 - ax == j1 - ay || i1 - ax == ay - j1) {
            continue;
        }
        if (used[i1][j1]) {
            continue;
        }
        dfs(i1, j1);
    }
}


