mt19937 rnd(time(0));


 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	auto area = [&](int i, int j, int k){
	    cout << "1 " << i << " " << j << " " << k << endl;
	    ll resp;
	    cin >> resp;
	    return resp;
	};
	auto sign = [&](int i, int j, int k){
	    cout << "2 " << i << " " << j << " " << k << endl;
	    int resp;
	    cin >> resp;
	    return resp > 0;
	};
	int n;
	cin >> n;
	vector<vi> t(2);
	vl ar(n + 1);
	for(int i = 3; i <= n; i ++){
		if(sign(1, 2, i)){
			t[0].push_back(i);
		}
		else{
			t[1].push_back(i);
		}
		ar[i] = area(1, 2, i);
	}
	vi res;
	for(int k = 0; k < 2; k ++){
		if(!t[k].empty()){
			int p = t[k][0];
			for(auto i: t[k]){
				if(ar[p] < ar[i]){
					p = i;
				}
			}
			vi left, right;
			for(auto i: t[k]){
				if(i != p){
					if(sign(1, p, i)){
						left.push_back(i);
					}
					else{
						right.push_back(i);
					}
				}
			}
			sort(all(left), [&](int i, int j){return ar[i] < ar[j];});
			sort(all(right), [&](int i, int j){return ar[i] > ar[j];});
			for(auto i: left){
				res.push_back(i);
			}
			res.push_back(p);
			for(auto j: right){
				res.push_back(j);
			}
		}
		if(k == 0){
			res.push_back(2);
		}
	}
	res.push_back(1);
	reverse(all(res));
	cout << "0 " << res;
	return 0;
}
