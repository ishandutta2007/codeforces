#include<bits/stdc++.h>
using namespace std;
#define N 100020
typedef long long ll;
inline ll read(){
	ll x=0,f=1;
	char c=getchar();
	while(c<'0'||c>'9'){
		if(c=='-')f=-1;
		c=getchar();
	}
	while(c>='0'&&c<='9'){
		x=(x<<1)+(x<<3)+c-'0';
		c=getchar();
	}
	return x*f;
}
int c[18][N],n,m,a[N],cnt[N],dp[1<<18],tot[1<<18];
int t[1<<18];
vector<int> p[N];
char s[N];
void calc(int x,int y){
	if(x==y){
		for(int i=1;i<(int)p[x].size();++i){
			int st=(1<<m)-1;
			int L=p[x][i-1],R=p[x][i];
			for(int k=0;k<m;++k){
				if(c[k][R-1]-c[k][L]>0)st^=1<<k;	
			}
			++t[st],--t[st^(1<<x)];
		}	
	}
	else{
		int i=0,j=0;
		while(i<(int)p[x].size()&&j<(int)p[y].size()){
			int L=p[x][i],R=p[y][j];
			if(L<R)++i;
			else swap(L,R),++j;
			int st=(1<<m)-1;
			for(int k=0;k<m;++k){
				if(k^x&&k^y&&c[k][R-1]-c[k][L]>0)st^=1<<k;	
			}
			
			++t[st],--t[st^(1<<x)],--t[st^(1<<y)],++t[st^(1<<x)^(1<<y)];
		}	
	}
}
int main(){
	n=read(),m=read();
	scanf("%s",s+1);
	for(int i=1;i<=n;++i)a[i]=s[i]-'a';
	for(int i=1;i<=n;++i){
		for(int j=0;j<m;++j){
			c[j][i]=c[j][i-1];	
		}
		++c[a[i]][i];
		p[a[i]].push_back(i);
		++cnt[a[i]];
	}
	for(int i=0;i<m;++i){
		for(int j=0;j<m;++j){
			int x=read();
			if(!x&&i<=j)calc(i,j);
		}
	}
	for(int j=0;j<m;++j){
		for(int i=0;i<(1<<m);++i){
			if(i>>j&1)t[i^(1<<j)]+=t[i];
		}
	}
	for(int i=0;i<(1<<m);++i){
		for(int j=0;j<m;++j){
			if(i>>j&1)tot[i]+=cnt[j];
		}
	}
	dp[(1<<m)-1]=1;
	int ans=n;
	for(int i=(1<<m)-1;i>=0;--i){
		if(!dp[i])continue;
		ans=min(ans,tot[i]);
		for(int j=0;j<m;++j){
			if(i>>j&1 && !t[i^(1<<j)])dp[i^(1<<j)]=1;
		}
	}
	printf("%d\n",ans);
	return 0;
}