    output fast() {
        vector<int> cnt(26);
        for (char c : s)
            cnt[c - 'a']++;
        int ones = 0;
        int pairs = 0;
        for (int i = 0; i < 26; i++) {
            if (cnt[i] % 2) {
                ones++;
                cnt[i]--;
            }
            pairs += cnt[i] / 2;
        }
        ones = min(ones, k);
        int res = 0;
        for (int i = 0; i <= pairs && ones + 2 * i <= k; i++) {
            int xones = ones + 2 * i;
            int xpairs = pairs - i;
            int tmp = 0;
            tmp += (xpairs / k) * 2;
            xpairs %= k;
            if (xones + xpairs >= k)
                tmp++;
            res = max(res, tmp);
        }
        return output{res};
    }

