int main() {
	int d,p; R(d,p);

	const int x = 1;
	const int y = 2;

	int acc = 100;
	auto add = [&](int a, int b) {
		W("+", a, b, ++acc);
		return acc;
	};

	auto powd = [&](int a) {
		W("^", a, ++acc);
		return acc;
	};
	
	const int MAXL = 30;
	auto mul = [&](int a, int m, bool raw = false) {
		if (!raw) {
			m--;
			m %= p;
			m += p;
			m %= p;
			m++;
		}

		vector<int> pw(MAXL + 1);
		pw[0] = a;
		for (int i = 1; i <= MAXL; i++)
			pw[i] = add(pw[i - 1], pw[i - 1]);

		vector<int> take;
		for (int i = MAXL; i >= 0; i--)
			if (m >> i & 1)
				take.emplace_back(i);

		int cur = pw[take.back()];
		take.pop_back();
		while (!take.empty()) {
			int nxt = pw[take.back()];
			take.pop_back();
			cur = add(cur, nxt);
		}

		return cur;
	};

	const int MAXC = 20;
	vector<vector<int>> con(8, vector<int>(MAXC + 1));
	con[0][0] = mul(x, p, true);
	con[0][1] = 3;
	for (int i = 2; i <= MAXC; i++)
		con[0][i] = add(con[0][i - 1], con[0][1]);

	for (int i = 0; i <= MAXC; i++)
		for (int r = 1; r <= 1; r++)
			con[r][i] = add(con[r ^ 1][i], x);

	for (int i = 0; i <= MAXC; i++)
		for (int r = 2; r <= 3; r++)
			con[r][i] = add(con[r ^ 2][i], y);

	for (int i = 0; i <= MAXC; i++)
		for (int r = 4; r <= 7; r++)
			con[r][i] = powd(con[r ^ 4][i]);

	vector<vector<int>> coeff(4, vector<int>(MAXC + 1, 0));
	for (int mask = 1; mask < (1 << d); mask++) {
		int m = (d - __builtin_popcount(mask)) % 2 == 0 ? +1 : -1;
		int curR = 0, curC = 0;
		for (int j = 0; j < d; j++) {
			if (mask >> j & 1) {
				if (j == 0) curR |= 1;
				if (j == 1) curR |= 2;
				if (j >= 2) curC++;
			}
		}

		coeff[curR][curC] += m;
	}

	int ans = con[0][0];
	for (int i = 0; i <= MAXC; i++)
		for (int r = 0; r <= 3; r++)
			ans = add(ans, mul(con[r | 4][i], coeff[r][i]));

	int fac = 1;
	for (int i = 2; i <= d; i++)
		fac *= i;

	int ifac = binpow(fac, p - 2, p);
	ans = mul(ans, ifac);

	W("f", ans);

	return 0;
}

