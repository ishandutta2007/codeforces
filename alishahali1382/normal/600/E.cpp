const int MAXN = 100010, LOG=20;

int n, m, k, u, v, x, y, t;
int mx[MAXN];
ll sum[MAXN];
map<int, int> cnt[MAXN];
ll ans[MAXN];
vector<int> G[MAXN];

void merge(int a, int b){
	if (cnt[a].size()<cnt[b].size()){
		cnt[a].swap(cnt[b]);
		swap(mx[a], mx[b]);
		swap(sum[a], sum[b]);
	}
	for (pii p:cnt[b]){
		cnt[a][p.first]+=p.second;
		if (cnt[a][p.first]==mx[a]) sum[a]+=p.first;
		if (cnt[a][p.first]>mx[a]){
			mx[a]=cnt[a][p.first];
			sum[a]=p.first;
		}
	}
}

void dfs(int node, int par){
	for (int v:G[node]) if (v!=par) dfs(v, node);
	ans[node]=sum[node];
	merge(par, node);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n; i++) cin>>x, cnt[i][x]++, sum[i]=x, mx[i]=1;
	for (int i=1; i<n; i++){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	dfs(1, 1);
	for (int i=1; i<=n; i++) cout<<ans[i]<<' ';
	
	return 0;
}
