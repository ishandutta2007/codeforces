signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    int n;
    cin >> n;
    string s;
    cin >> s;
    vector<vector<int>> poses(26);
    for (int i = 0; i < n; i++)
        poses[s[i] - 'a'].pb(i);
    int m;
    cin >> m;
    while (m--) {
        string cur;
        cin >> cur;
        vector<int> cnt(26, 0);
        for (auto el : cur)
            cnt[el - 'a']++;
        int ans = 0;
        for (int i = 0; i < 26; i++)
            if (cnt[i] != 0)
                ans = max(ans, poses[i][cnt[i] - 1]);
        cout << ans + 1 << '\n';
    }
}