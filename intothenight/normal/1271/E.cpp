int main(){
	cin.tie(0)->sync_with_stdio(0);
	ll n, k;
	cin >> n >> k;
	if(n == k){
		cout << 1;
		return 0;
	}
	auto subtr = [&](ll x){
		ll res = 0;
		ll t = 1;
		while(x * t <= n){
			if((x + 1) * t <= n){
				res += t;
			}
			else{
				res += n + 1 - x * t;
			}
			t *= 2;
		}
		return res;
	};
	ll l = 2, r = n / 2 * 2 + 2;
	while(r - l > 2){
		ll m = (l / 2 + r / 2 >> 1) * 2; // subtree(m) + subtree(m + 1) >= k
		if(subtr(m) + subtr(m + 1) >= k){
			l = m;
		}
		else{
			r = m;
		}
	}
	ll pl = l;
	l = 1, r = (n + 1) / 2 * 2 + 1;
	while(r - l > 2){
		ll m = (l / 2 + r / 2 >> 1) * 2 + 1; // subtree(m) >= k
		if(subtr(m) >= k){
			l = m;
		}
		else{
			r = m;
		}
	}
	cout << max(pl, l);
	return 0;
}
