const int Mod=1'000'000'007;
struct Matrix{
	int x,y;
	long long m[6][6];
	Matrix(){}
	Matrix(int x,int y):x(x),y(y){
		for(int i=0;i<x;i++){
			for(int j=0;j<y;j++){
				m[i][j]=0;
			}
		}
	}
	Matrix(int a,int b,int c){
		x=3;
		y=1;
		m[0][0]=a;
		m[1][0]=b;
		m[2][0]=c;
	}
	Matrix(int a,int b,int c,int d,int e){
		x=5;
		y=1;
		m[0][0]=a;
		m[1][0]=b;
		m[2][0]=c;
		m[3][0]=d;
		m[4][0]=e;
	}
	Matrix operator*(const Matrix &f){
		Matrix ans(x,f.y);
		for(int i=0;i<x;i++){
			for(int j=0;j<f.y;j++){
				for(int k=0;k<y;k++){
					ans.m[i][j]=(ans.m[i][j]+m[i][k]*f.m[k][j])%(Mod-1);
				}
			}
		}
		return ans;
	}
};

Matrix ksmii(const Matrix &a,long long b){
	if(b==1){
		return a;
	}
	Matrix x=ksmii(a,b/2);
	x=x*x;
	if(b%2){
		x=x*a;
	}
	return x;
}

int ksmii(int a,long long b){
	if(!b){
		return 1;
	}
	int x=ksmii(a,b/2);
	x=1LL*x*x%Mod;
	if(b%2){
		x=1LL*x*a%Mod;
	}
	return x;
}

