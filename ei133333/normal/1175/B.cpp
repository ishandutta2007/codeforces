const int64 LIM = 4294967295LL;

void fail() {
  cout << "OVERFLOW!!!" << endl;
  exit(0);
}

int main() {
  int T;
  cin >> T;

  stack< pair< int64, int64 > > st;
  st.emplace(1, 0);

  for(int i = 0; i < T; i++) {
    string S;
    cin >> S;
    if(S == "add") {
      st.top().second++;
    } else if(S == "end") {
      int64 p = 1LL * st.top().first * st.top().second;
      if(p > LIM) fail();
      st.pop();
      st.top().second += p;
      if(st.top().second > LIM) fail();
    } else {
      int x;
      cin >> x;
      st.emplace(x, 0);
    }
  }
  int64 p = 1LL * st.top().first * st.top().second;
  if(p > LIM) fail();
  cout << p << endl;
}