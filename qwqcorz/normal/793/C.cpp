#include<bits/stdc++.h>
#define double long double
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
double l=0,r=N;
void mice(int x,int dx,int up)
{
	if (x<up)
	{
		if (dx<=0) return;
		r=min(r,1.0L*(up-x)/dx);
	}
	else
	{
		if (dx>=0) return void(r=-1);
		l=max(l,1.0L*(up-x)/dx);
	}
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),x1=read(),y1=read(),x2=read(),y2=read();
	for (int i=1;i<=n;i++)
	{
		int x=read(),y=read(),dx=read(),dy=read();
		mice(x,dx,x2);
		mice(-x,-dx,-x1);
		mice(y,dy,y2);
		mice(-y,-dy,-y1);
	}
	if (r-l<1e-11) puts("-1");
			  else printf("%.20Lf",l);
	
	return 0;
}
