int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int t;
	cin >> t;
	while(t --){
		int n;
		cin >> n;
		string s;
		cin >> s;
		int pos = -1;
		for(int i = 1; i < n; i ++){
			if(s[i] < s[i - 1]){
				pos = i;
				break;
			}
		}
		if(pos == -1){
			cout << string(n, '1') << "\n";
			continue;
		}
		vi one, two;
		string res(n, '1');
		for(int i = 0; i < pos; i ++){
			if(s[i] <= s[pos]){
				one.push_back(i);
			}
			else{
				two.push_back(i);
				res[i] = '2';
			}
		}
		one.push_back(pos);
		bool ok = true;
		for(int i = pos + 1; i < n; i ++){
			if(s[i] >= s[two.back()]){
				two.push_back(i);
				res[i] = '2';
			}
			else if(s[i] <= s[two.front()] && s[i] >= s[one.back()]){
				one.push_back(i);
			}
			else{
				ok = false;
				break;
			}
		}
		if(ok){
			cout << res << "\n";
		}
		else{
			cout << "-\n";
		}
	}
	return 0;
}

