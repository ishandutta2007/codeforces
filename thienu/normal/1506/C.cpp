void solve(){
    string a, b;
    cin >> a >> b;
    int n = a.size(), m = b.size();
    // longest common substring
    vvi dp(n+1, vi(m+1));
    int maxi = 0;
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= m; j++){
            if(a[i-1] == b[j-1]){
                dp[i][j] = dp[i-1][j-1] + 1;
            }
            maxi = max(maxi, dp[i][j]);
        }
    }
    cout << n + m - 2 * maxi << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}