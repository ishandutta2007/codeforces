constexpr int md = (int) 1e9 + 7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;


const int nax = 1<<20;
Mint suma[nax*2], sumb[nax*2], dot[nax*2], pow2[nax], ipow2[nax];
int cnt[nax*2];

void add(int a, Mint v, int c = 1, int l = 0, int r = nax, int i = 1) {
  if (r-l == 1 && l == a) {
    cnt[i] = c;
    suma[i] = v * (1/(Mint)2);
    sumb[i] = v;
    dot[i] = 0;
  } else if (l > a || r <= a) return;
  else {
    int m = (l+r)>>1;
    add(a, v, c, l, m, i*2);
    add(a, v, c, m, r, i*2+1);

    cnt[i] = cnt[i*2]+cnt[i*2+1];
    suma[i] = suma[i*2]+ipow2[cnt[i*2]]*suma[i*2+1];
    sumb[i] = sumb[i*2]+pow2[cnt[i*2]]*sumb[i*2+1];
    dot[i] = dot[i*2]+dot[i*2+1]+ipow2[cnt[i*2]]*suma[i*2+1]*sumb[i*2];
  }
}

void remove(int a) {
  add(a, 0, 0);
}

int main() {
  ipow2[0] = pow2[0] = 1;
  for (int i = 1; i < nax; i++) {
    pow2[i] = pow2[i-1]*2;
    ipow2[i] = 1/pow2[i];
  }

  assert(pow2[3] == 8);
  assert(ipow2[1] == (md+1)/2);

  ios::sync_with_stdio(0); cin.tie(0);
  int n;
  cin >> n;

  vector<pair<int,int>> ind(n);
  int ids = 0;
  for (auto&[p,id] : ind) {
    cin >> p;
    id = ids++;
  }

  int queries;
  cin >> queries;
  vector<tuple<int,int,int>> q(queries);
  for (auto&[i,x,id] : q) {
    cin >> i >> x;
    i--;
    id = ids++;
    ind.emplace_back(x, id);
  }
  sort(ind.begin(), ind.end());
  vector<int> rev(ind.size());
  for (int i = 0; i < (int)ind.size(); i++)
    rev[ind[i].second] = i;

  vector<int> cur(n);
  for (int i = 0; i < n; i++) {
    add(rev[i], ind[rev[i]].first);
    cur[i] = rev[i];
  }

  cout << dot[1] << '\n';
  for (int qi = 0; qi < queries; qi++) {
    auto [i,x,id] = q[qi];
    remove(cur[i]);
    cur[i] = rev[id];
    add(cur[i], x);
    cout << dot[1] << '\n';
  }
}