int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	vector<vi> adj(n); // Edges coming in u
	vi res(n, -1);
	deque<int> q;
	for(int u = 0; u < n; u ++){
		int flag = false;
		if(a[u] <= u){
			adj[u - a[u]].push_back(u);
			if((a[u - a[u]] ^ a[u]) & 1){
				res[u] = 1;
				q.push_back(u);
				flag = true;
			}
		}
		if(u + a[u] < n){
			adj[u + a[u]].push_back(u);
			if(!flag && (a[u + a[u]] ^ a[u]) & 1){
				res[u] = 1;
				q.push_back(u);
			}
		}
	}
	while(!q.empty()){
		int u = q.front();
		q.pop_front();
		for(auto v: adj[u]){
			if(res[v] == -1){
				res[v] = res[u] + 1;
				q.push_back(v);
			}
		}
	}
	cout << res;
	return 0;
}
 
