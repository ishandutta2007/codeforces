#include <bits/stdc++.h>
using namespace std;

int n;
int sa[100005],ea[100005],sb[100005],eb[100005];
int cntt;

vector<pair<pair<int,int>,pair<int,int> > > se1,se2,se3,se4;
map<int,int> lss;

struct BIT{
	int dt[500005];
	void add(int a,int x){
		while(a<500005){
			dt[a]+=x;
			a+=a&-a;
		}
	}
	int sum(int a){
		int ans=0;
		while(a){
			ans+=dt[a];
			a-=a&-a;
		}
		return ans;
	}
}bit;

struct segtree{
	int mx[4000005],mn[4000005];
	void add(int to,int x,int now=1,int nl=1,int nr=cntt){
		if(nl==to&&nr==to){
			mx[now]+=x;
			mn[now]+=x;
			return;
		}
		int m=(nl+nr)>>1;
		if(m>=to){
			add(to,x,now<<1,nl,m);
		}else{
			add(to,x,now<<1|1,m+1,nr);
		}
		mx[now]=max(mx[now<<1],mx[now<<1|1]);
		mn[now]=min(mn[now<<1],mn[now<<1|1]);
	}
	bool get(int l,int r,int now=1,int nl=1,int nr=cntt){
		if(nr<l||nl>r){
			return 0;
		}
		if(nl>=l&&nr<=r){
			if(mx[now]||mn[now]){
				return 1;
			}else{
				return 0;
			}
		}
		int m=(nl+nr)>>1;
		return get(l,r,now<<1,nl,m)||get(l,r,now<<1|1,m+1,nr);
	}
}seg;

int main(){
	scanf("%d",&n);
	for(int i=1;i<=n;i++){
		int a,b,c,d;
		scanf("%d%d%d%d",&a,&b,&c,&d);
		lss[a];lss[b];lss[c];lss[d];
		se1.push_back(make_pair(make_pair(a,b),make_pair(c,d)));
		se2.push_back(make_pair(make_pair(b,a),make_pair(c,d)));
		se3.push_back(make_pair(make_pair(c,d),make_pair(a,b)));
		se4.push_back(make_pair(make_pair(d,c),make_pair(a,b)));
	}
	sort(se1.begin(),se1.end());
	sort(se2.begin(),se2.end());
	sort(se3.begin(),se3.end());
	sort(se4.begin(),se4.end());
	for(map<int,int>::iterator it=lss.begin();it!=lss.end();it++){
		it->second=++cntt;
	}
	int l=0;
	for(int i=0;i<n;i++){
		while(l<n&&se2[l].first.first<se1[i].first.first){
			bit.add(lss[se2[l].second.first],1);
			bit.add(lss[se2[l].second.second]+1,-1);
			seg.add(lss[se2[l].second.first],1);
			seg.add(lss[se2[l].second.second],-1);
			l++;
		}
		if(bit.sum(lss[se1[i].second.first])||seg.get(lss[se1[i].second.first],lss[se1[i].second.second])){
			puts("NO");
			return 0;
		}
	}
	memset(bit.dt,0,sizeof(bit.dt));
	memset(seg.mn,0,sizeof(seg.mn));
	memset(seg.mx,0,sizeof(seg.mx));
	l=0;
	for(int i=0;i<n;i++){
		while(l<n&&se4[l].first.first<se3[i].first.first){
			bit.add(lss[se4[l].second.first],1);
			bit.add(lss[se4[l].second.second]+1,-1);
			seg.add(lss[se4[l].second.first],1);
			seg.add(lss[se4[l].second.second],-1);
			l++;
		}
		if(bit.sum(lss[se3[i].second.first])||seg.get(lss[se3[i].second.first],lss[se3[i].second.second])){
			puts("NO");
			return 0;
		}
	}
	puts("YES");
	return 0;
}