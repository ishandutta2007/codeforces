void solve() {
    string S;
    cin >> S;

    int n = S.size();
    vector<vector<int>> pos(2);
    for (int i = 0; i < n; i++) {
        if (S[i] == '?') continue;
        int x = S[i] - '0';
        x ^= i & 1;
        pos[x].emplace_back(i);
    }
    for (int i = 0; i < 2; i++) pos[i].emplace_back(n);

    ll ans = 0;
    for (int i = 0, x = 0, y = 0; i < n; i++) {
        while (x < pos[0].size() && pos[0][x] < i) x++;
        while (y < pos[1].size() && pos[1][y] < i) y++;
        if (x == pos[0].size() || y == pos[1].size()) break;
        ans += max(pos[0][x], pos[1][y]) - i;
    }
    cout << ans << '\n';
}

