const int MOD = 1e9 + 7;

void solve() {
	int n;
	cin >> n;
	int ans = 1;
	int pow = 1;  
	for(int i = 1; i < n; ++i)
		ans = (ans * (i + 1)) % MOD, pow = (pow * 2) % MOD;
	cout << (ans - pow + MOD) % MOD << '\n';
}

