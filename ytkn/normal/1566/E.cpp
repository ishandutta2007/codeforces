template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void dfs(int v, vector<vector<int>> &g, vector<bool> &seen, vector<bool> &remaining, vector<int> &ans){
    seen[v] = true;
    int cnt = 0;
    for(int to: g[v]){
        if(seen[to]) continue;
        dfs(to, g, seen, remaining, ans);
        if(remaining[to]) cnt++;
    }
    if(cnt > 0){
        remaining[v] = false;
        ans.push_back(cnt);
    }
}

void solve(){
    int n; cin >> n;
    vector<bool> seen(n), remaining(n, true);
    vector<int> ans;
    vector<vector<int>> g(n);
    for(int i = 0; i < n-1; i++){
        int u, v; cin >> u >> v; u--; v--;
        g[u].push_back(v);
        g[v].push_back(u);
    }
    dfs(0, g, seen, remaining, ans);
    int a = 1;
    for(int x: ans) a += x-1;
    cout << a << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}