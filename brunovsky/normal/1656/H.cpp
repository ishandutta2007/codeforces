int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);
    int T;
    cin >> T;
    while (T--) {
        int N, M;
        cin >> N >> M;
        vector<int128> a(N), b(M);
        for (int i = 0; i < N; i++) {
            cin >> a[i];
        }
        for (int j = 0; j < M; j++) {
            cin >> b[j];
        }

        // Use a segtree to cancel the LCMs
        vector<segtree<Segnode>> aseg(N), bseg(M);
        for (int i = 0; i < N; i++) {
            vector<int128> base(M);
            for (int j = 0; j < M; j++) {
                base[j] = gcd(a[i], b[j]);
            }
            aseg[i].assign(M, base);
        }
        for (int j = 0; j < M; j++) {
            vector<int128> base(N);
            for (int i = 0; i < N; i++) {
                base[i] = gcd(a[i], b[j]);
            }
            bseg[j].assign(N, base);
        }

        // if aseg[i].all() is not a[i] we have to kill a[i]
        // If bseg[j].all() is not b[j] we have to kill b[j]

        vector<int8_t> alive(N, true), blive(M, true);

        vector<pair<int, int>> dead;
        for (int i = 0; i < N; i++) {
            if (a[i] != aseg[i].query_all()) {
                dead.emplace_back(0, i);
                alive[i] = false;
            }
        }
        for (int j = 0; j < M; j++) {
            if (b[j] != bseg[j].query_all()) {
                dead.emplace_back(1, j);
                blive[j] = false;
            }
        }
        debug(dead);

        int D = dead.size();
        for (int d = 0; d < D; d++) {
            if (auto [s, v] = dead[d]; s == 0) {
                for (int j = 0; j < M; j++) {
                    if (blive[j]) {
                        bseg[j].update_point(v, 1);
                        if (b[j] != bseg[j].query_all()) {
                            dead.emplace_back(1, j);
                            blive[j] = false, D++;
                        }
                    }
                }
            } else {
                for (int i = 0; i < N; i++) {
                    if (alive[i]) {
                        aseg[i].update_point(v, 1); // to 1, not to 0
                        if (a[i] != aseg[i].query_all()) {
                            dead.emplace_back(0, i);
                            alive[i] = false, D++;
                        }
                    }
                }
            }
        }

        int A = count(begin(alive), end(alive), true);
        int B = count(begin(blive), end(blive), true);
        if (A == 0 || B == 0) {
            cout << "NO\n";
            continue;
        }
        vector<int128> sa, sb;
        for (int i = 0; i < N; i++) {
            if (alive[i]) {
                sa.push_back(a[i]);
            }
        }
        for (int j = 0; j < M; j++) {
            if (blive[j]) {
                sb.push_back(b[j]);
            }
        }
        cout << "YES\n";
        cout << A << ' ' << B << '\n';
        for (int i = 0; i < A; i++) {
            cout << sa[i] << " \n"[i + 1 == A];
        }
        for (int i = 0; i < B; i++) {
            cout << sb[i] << " \n"[i + 1 == B];
        }
    }
    return 0;
}