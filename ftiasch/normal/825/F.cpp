#include <bits/stdc++.h>

const int N = 8'000;

int n, dec[N + 1], dp[N + 1], nxt[N + 1];
char s[N + 1];

int main() {
  scanf("%s", s);
  n = strlen(s);
  for (int i = 1; i <= n; ++i) {
    dec[i] = dec[i / 10] + 1;
  }
  dp[n] = 0;
  for (int i = n; i--;) {
    dp[i] = 2 + dp[i + 1];
    std::fill(nxt + i, nxt + n, i - 1);
    for (int j = i + 1; j < n; ++j) {
      int p = j - 1;
      while (i <= p) {
        p = nxt[p];
        if (s[j] == s[p + 1]) {
          nxt[j] = p + 1;
          break;
        }
      }
      int per = j - nxt[j];
      if ((j - i + 1) % per != 0) {
        per = j - i + 1;
      }
      dp[i] = std::min(dp[i], dec[(j - i + 1) / per] + per + dp[j + 1]);
    }
  }
  printf("%d\n", dp[0]);
}