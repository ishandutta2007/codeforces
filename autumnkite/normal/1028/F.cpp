#include <bits/stdc++.h>

struct Frac {
	int x, y;

	Frac() : x(0), y(1) {}
	Frac(int _x, int _y) : x(_x), y(_y) {}

	bool operator<(const Frac &rhs) const {
		return 1ll * x * rhs.y < 1ll * rhs.x * y;
	}
};

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int q;
	std::cin >> q;
	std::map<long long, std::vector<std::pair<int, int>>> S;
	std::map<Frac, int> cnt;
	int now = 0;
	while (q--) {
		int op, x, y;
		std::cin >> op >> x >> y;
		if (op == 1) {
			long long d = 1ll * x * x + 1ll * y * y;
			auto &T = S[d];
			++now;
			for (auto [tx, ty] : T) {
				cnt[Frac(x + tx, y + ty)] += 2;
			}
			++cnt[Frac(x, y)];
			T.emplace_back(x, y);
		} else if (op == 2) {
			long long d = 1ll * x * x + 1ll * y * y;
			auto &T = S[d];
			T.erase(std::find(T.begin(), T.end(), std::make_pair(x, y)));
			--cnt[Frac(x, y)];
			for (auto [tx, ty] : T) {
				cnt[Frac(x + tx, y + ty)] -= 2;
			}
			--now;
		} else {
			std::cout << now - cnt[Frac(x, y)] << "\n";
		}
	}
}