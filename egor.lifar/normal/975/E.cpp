const int MAXN = 200228;
typedef complex<double> point;
#define x real
#define y imag


double dot(point a, point b) {
    return (conj(a) * b).x();
}


double cross(point a, point b) {
    return (conj(a) * b).y();
}


int n, q;
point r[MAXN];
point c, nc;
int head = -1;

point get(int v) {
    if (head == -1) {
        return r[v]; 
    } else {
        point oy = r[head] - c;
        oy /= abs(oy);
        return nc + point(0, 1) * (r[v] - c) / oy;
    }
}



int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    //read(FILENAME);
    cin >> n >> q;
    for (int i = 0; i < n; i++) {
        double tx, ty;
        cin >> tx >> ty;
        r[i] = {tx, ty};
    }
    double total = 0;
    for (int i = 2; i < n; i++) {
        point cur_c = (r[i] + r[i - 1] + r[0]) / (point)3;
        double e = abs(cross(r[0] - r[i], r[i - 1] - r[i]));
        c += cur_c * e;
        total += e;
    }
    c /= total;
    int cur = 1;
    while (q--) {
        int t;
        cin >> t;
        if(t == 1) {
            int f, t;
            cin >> f >> t;
            f--, t--;
            cur ^= f;
            nc = get(cur) - point(0, abs(r[cur] - c));
            head = cur;
            cur ^= t;
        } else {
            int v;
            cin >> v;
            v--;
            point ans = get(v);
            cout.precision(12);
            cout << fixed << ans.x() << ' ' << ans.y() << endl;
        }
    }
    return 0;
}