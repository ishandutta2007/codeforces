int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n, m, s;
    cin >> n >> m >> s;
    vector<int> a(m), b(n), c(n);
    for (int& x : a) cin >> x;
    for (int& x : b) cin >> x;
    for (int& x : c) cin >> x;

    struct bug {
        int a, idx;
        bug(int a, int idx) : a(a), idx(idx){};
        bool operator<(const bug& rhs) const { return a > rhs.a; }
    };
    struct student {
        int b, c, idx;
        student(int b, int c, int idx) : b(b), c(c), idx(idx) {}
        bool operator<(const student& rhs) const { return b > rhs.b; };
    };
    vector<bug> A;
    vector<student> B;
    for (int i = 0; i < m; i++) A.emplace_back(a[i], i);
    for (int i = 0; i < n; i++) B.emplace_back(b[i], c[i], i);
    sort(ALL(A));
    sort(ALL(B));

    auto check = [&](int x) {  // x  s 
        priority_queue<int, vector<int>, greater<int>> pq;
        ll sum = 0;
        for (int i = 0, j = 0; i < m; i += x) {
            while (j < n && B[j].b >= A[i].a) pq.emplace(B[j++].c);
            if (pq.empty()) return false;
            sum += pq.top();
            pq.pop();
        }
        return sum <= s;
    };

    int lb = 0, ub = m + 1;
    while (ub - lb > 1) {
        int mid = (ub + lb) >> 1;
        (check(mid) ? ub : lb) = mid;
    }
    if (ub == m + 1) {
        cout << "NO" << '\n';
        return 0;
    }

    vector<int> ans(m);
    {
        int x = ub;
        priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>>> pq;
        for (int i = 0, j = 0; i < m; i += x) {
            while (j < n && B[j].b >= A[i].a) {
                pq.emplace(B[j].c, B[j].idx);
                j++;
            }
            assert(!pq.empty());
            int nxt = pq.top().second;
            pq.pop();
            for (int k = i; k < min(m, i + x); k++) ans[A[k].idx] = nxt + 1;
        }
    }

    cout << "YES" << '\n';
    cout << ans << '\n';
    return 0;
}