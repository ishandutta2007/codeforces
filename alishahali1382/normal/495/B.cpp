#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 1000000;

int n, m, ans;

int main(){
	ios_base::sync_with_stdio(false);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	if (n==m || (n==0 && m==0)){
		cout<<"infinity"<<endl;
		return 0;
	}
	if (n<m || n==0){
		cout<<0<<endl;
		return 0;
	}
	n-=m;
	int i=1;
	for (; i*i<n; i++){
		if (n%i) continue ;
		if (i>m) ans++;
		if ((n/i)>m) ans++;
	}
	if (i*i==n && i>m) ans++;
	cout<<ans<<endl;

	return 0;
}
