// min_i C_i(floor(HD/h_id_i) + 1)
// min_i C_i(floor(X / A_i) + 1)
//

fn run() {
    input! {
        n: usize,
        c: i64,
        p: [(i64, i64, i64); n],
        m: usize,
        ask: [(i64, i64); m],
    }
    let mut map = Map::new();
    for (c, d, h) in p {
        map.entry(c).or_insert(0).chmax(d * h);
    }
    let mut dp = vec![0; c as usize + 1];
    for (cost, val) in map {
        for i in (1..).take_while(|i| i * cost <= c) {
            dp[(i * cost) as usize].chmax(i * val - 1);
        }
    }
    for i in 2..dp.len() {
        dp[i] = dp[i].max(dp[i - 1]);
    }
    use std::fmt::*;
    let mut s = String::new();
    for (h, w) in ask {
        let x = dp.lower_bound(&(h * w));
        if x < dp.len() {
            write!(&mut s, "{} ", x).ok();
        } else {
            write!(&mut s, "-1 ").ok();
        }
    }
    s.pop();
    println!("{}", s);
}

fn main() {
    run();
}