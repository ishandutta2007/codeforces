int go(int k, int n, int m){
    int mul2 = k / 2, mul3 = k / 3, mul6 = k / 6;
    mul2 -= mul6; mul3 -= mul6;
    if(mul2 >= n) n = 0;
    else n -= mul2;
    if(mul3 >= m) m = 0;
    else m -= mul3;
    if(n+m <= mul6) return 1;
    return 0;
}
