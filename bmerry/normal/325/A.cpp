int main(int argc, const char **argv)
{
    redirect(argc, argv);
    int N;
    cin >> N;
    vi x1(N), x2(N), y1(N), y2(N);
    ll area = 0;

    for (int i = 0; i < N; i++)
    {
        cin >> x1[i] >> y1[i] >> x2[i] >> y2[i];
        area += (ll) (y2[i] - y1[i]) * (x2[i] - x1[i]);
    }
    ll minx = *min_element(RA(x1));
    ll maxx = *max_element(RA(x2));
    ll miny = *min_element(RA(y1));
    ll maxy = *max_element(RA(y2));
    if (maxy - miny == maxx - minx
        && area == (maxx - minx) * (maxy - miny))
        cout << "YES\n";
    else
        cout << "NO\n";

    return 0;
}