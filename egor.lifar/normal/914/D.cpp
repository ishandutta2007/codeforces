inline int gcd(int a, int b) {
    return (a == 0 ? b: gcd(b % a, a));
}


void change(int i, int y) {
    d[i] = y;
    while (i >> 1 > 0) {
        i >>= 1;
        d[i] = gcd(d[i * 2], d[i * 2 + 1]);
    }
}


bool rmq(int v, int vl, int vr, int l, int r, int x) {
    if (l > vr || vl > r) {
        return true;
    }
    if (l <= vl && vr <= r) {
        return (d[v] % x == 0);
    }
    bool t = rmq(v * 2, vl, (vl + vr) / 2, l, r, x);
    if (!t) {
        return false;
    }
    return rmq(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r, x);
}


bool check(int l, int r, int x) {
    if (l == r) {
        return true;
    }
    //cout << l << ' ' << r << endl;
    bool t = rmq(1, 1, ss, l, (l + r) / 2, x);
    if (t) {
        return check((l + r) / 2 + 1, r, x);
    }
    bool t1 = rmq(1, 1, ss, (l + r) / 2 + 1, r, x);
    if (!t1) {
        return false;
    }
    return check(l, (l + r) / 2, x);
}


