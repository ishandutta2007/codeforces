#define pb push_back
#define fox(i,x,y) for(int i=(x);i<=(y);i++)
#define foxr(i,x,y) for(int i=(y);i>=(x);i--)

ll q, i, x, y, w, ans;
map<pll,ll> cost;

int main(){
    for(scanf("%lld",&q);q;q--){
        scanf("%lld",&i);
        if(i==2){
            scanf("%lld%lld",&x,&y);
            ans = 0;
            while(x!=y){
                if(x<y) swap(x,y);
                ll par=(x>>1);
                if(cost.count({x,par}))
                    ans+=cost[{x,par}];
                x=par;
            }
            printf("%lld\n",ans);
        }
        else{
            scanf("%lld%lld%lld",&x,&y,&w);
            while(x!=y){
                if(x<y) swap(x,y);
                ll par=(x>>1);
                cost[{x,par}]+=w;
                x=par;
            }
        }
    }
    return 0;
}