const LL INF = 1e9 + 7;
const int N = 1e5 + 10;
LL cnt[2];
vector<int> v[N];
void dfs(int x, int o = 0, int fa = -1)
{
	cnt[o]++;
	for (auto &y : v[x])
	{
		if (y == fa) continue;
		dfs(y, o ^ 1, x);
	}
}
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	cin >> n;
	for (int i = 1; i < n; i++)
	{
		int x, y;
		cin >> x >> y;
		v[x].push_back(y);
		v[y].push_back(x);
	}
	dfs(1);
	cout << cnt[0] * cnt[1] - (n - 1) << endl;
	return 0;
}