long long a[500];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("input.txt", "r", stdin);
    //freopen("output.txt", "w", stdout);

    int n;
    cin >> n;
    for (int i=0; i<n; i++)
        cin >> a[i];

    long long ret = 0;
    for (int i=0; i<n; i++) {
        ret = max(ret, a[i]);
        for (int j=i+1; j<n; j++) {
            ret = max(ret, a[i] | a[j]);
            for (int k=j+1; k<n; k++)
                ret = max(ret, a[i] | a[j] | a[k]);
        }
    }

    cout << ret << "\n";

    return 0;
}