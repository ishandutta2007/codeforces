#include<bits/stdc++.h>
using namespace std;
const int N=5e5+5;
const int p=1e9+7;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
inline void Add(int &x,int y)
{
	x+=y;
	if (x>=p) x-=p;
}
int a[N],id[N];
bool cmp(int x,int y) {return a[x]<a[y];}
struct BIT
{
	int t[N],n;
	void add(int x,int y)
	{
		for (;x<=n;x+=x&-x) Add(t[x],y);
	}
	int query(int x)
	{
		int ret=0;
		for (;x;x=x&(x-1)) Add(ret,t[x]);
		return ret;
	}
}t1,t2;

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=t1.n=t2.n=read(),ans=0;
	for (int i=1;i<=n;i++) a[i]=read(),id[i]=i;
	sort(id+1,id+1+n,cmp);
	for (int i=1;i<=n;i++)
	{
		int now=id[i];
		Add(ans,1LL*now*(n-now+1)%p*a[now]%p);
		Add(ans,1LL*t1.query(now)*(n-now+1)%p*a[now]%p);
		Add(ans,1LL*(t2.query(n)-t2.query(now)+p)*now%p*a[now]%p);
		t1.add(now,now);
		t2.add(now,n-now+1);
	}
	print(ans);
	
	return 0;
}
