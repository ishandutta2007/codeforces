#include<bits/stdc++.h>
using namespace std;
const int N=105;
const int S=1<<15;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
int popcount[S],a[N];
map<vector<int>,int>mp;
vector<int>get(int n,int x,int k)
{
	vector<int>ret(0);
	int a1=popcount[((a[1]^x)>>k)&((1<<15)-1)];
	for (int i=2;i<=n;i++) ret.push_back(popcount[((a[i]^x)>>k)&((1<<15)-1)]-a1);
	return ret;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=1;i<(1<<15);i++) popcount[i]=popcount[i&(i-1)]+1;
	for (int i=0;i<(1<<15);i++)
	{
		vector<int>now=get(n,i<<15,15);
		if (!mp.count(now)) mp[now]=i<<15;
	}
	for (int i=0;i<(1<<15);i++)
	{
		vector<int>now=get(n,i,0);
		for (int j=0;j<(int)now.size();j++) now[j]=-now[j];
		if (mp.count(now)) return print(mp[now]|i),0;
	}
	print(-1);
	
	return 0;
}
