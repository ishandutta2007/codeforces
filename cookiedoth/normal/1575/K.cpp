const int Mod = 1000000007;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, ll b) {
    assert(b >= 0);
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}


ll n, m, k, r, c;


ll solver(ll a, ll b, ll r, ll c) {
    if (r <= 0 || c <= 0) {
        return 0;
    }
    if (r == 1 && c == 1) {
        return 1;
    }
    if (a == 0 && b == 0) {
        return (ll)r * c;
    }
    if (a >= r || b >= c) {
        return (ll)r * c;
    }
    ll lena = r - a;
    ll lenb = c - b;
    ll val = solver(r - lena + 1 - 1, c - lenb + 1 - 1, lena, lenb);
    val += (ll)r * c;
    val -= 2LL * lena * lenb;
    if (r - lena + 1 <= lena && c - lenb + 1 <= lenb) {
        val += (lena - (r - lena + 1) + 1) * (lenb - (c - lenb + 1) + 1);
    }
    return val;
}


