template <typename T, typename... Args>
void Mul(T &a, T b, Args... args) {
  Mul(a, mul(b, args...));
}
template <typename T1, typename T2>
T1 Ksm(T1 a, T2 b) {
  T1 s = 1;
  while (b) {
    if (b & 1) Mul(s, a);
    Mul(a, a);
    b >>= 1;
  }
  return s;
}
int n, x[200001], y[200001], s[200001], dp[200001], sum[200001];
int main(int argc, char const *argv[]) {
  Read(n);
  for (int i = 1; i <= n; i++) Read(x[i], y[i], s[i]);
  for (int i = 1; i <= n; i++) {
    int pos = std::lower_bound(x + 1, x + n + 1, y[i]) - x;
    dp[i] = add(x[i] - y[i], sub(sum[i - 1], sum[pos - 1]));
    sum[i] = add(sum[i - 1], dp[i]);
  }
  int ans = 0;
  for (int i = 1; i <= n; i++)
    if (s[i]) Add(ans, dp[i]);
  Add(ans, x[n] + 1);
  std::printf("%d", ans);
  return 0;
}