void solve() {
    int b, p, f;
    cin >> b >> p >> f;
    int h, c;
    cin >> h >> c;
    if (h < c) {
        swap(h, c);
        swap(p, f);
    }
    int t = min(b / 2, p);
    b -= 2 * t;
    //cout << b << " " << t << " ";
    cout << t * h + min(b / 2, f) * c << "\n";
}

int main() {
    ios_base::sync_with_stdio(0), cin.tie(0);
    int t;
    cin >> t;
    while (t --> 0)
        solve();
    return 0;
}