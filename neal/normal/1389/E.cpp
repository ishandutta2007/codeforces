int64_t choose2(int64_t n) {
    return n * (n - 1) / 2;
}

void run_case() {
    int64_t M, D, W;
    cin >> M >> D >> W;
    int64_t G = __gcd(D - 1, W);
    int64_t mod = W / G;
    int64_t N = min(M, D);
    int64_t answer = (N % mod) * choose2(N / mod + 1) + (mod - N % mod) * choose2(N / mod);
    cout << answer << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}