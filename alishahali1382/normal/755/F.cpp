const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 1000010;

int n, m, k, u, v, x, y, t, a, b, ans1, ans2;
int A[MAXN];
int C[MAXN];
bool mark[MAXN];
vector<int> vec;
bitset<MAXN> B;

int dfs(int node, int origin){
	mark[node]=1;
	if (A[node]==origin) return 1;
	return 1+dfs(A[node], origin);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>k;
	for (int i=1; i<=n; i++) cin>>A[i];
	for (int i=1; i<=n; i++) if (!mark[i]) vec.pb(dfs(i, i));
	//for (int i:vec) debug(i)
	
	for (int i:vec) C[i]++;
	for (int i=1; i<=n; i++){
		if (!C[i]) continue ;
		C[i]--;
		C[2*i]+=C[i]/2;
		C[i]%=2;
		C[i]++;
	}
	B[0]=1;
	for (int i=1; i<=n; i++) for (int j=0; j<C[i]; j++) B|=(B<<i);
	if (B[k]) ans1=k;
	else ans1=k+1;
	for (int i=0; i<vec.size(); i++){
		while (k && vec[i]>=2){
			ans2+=2;
			vec[i]-=2;
			k--;
		}
	}
	ans2+=k;
	ans2=min(ans2, n);
	cout<<ans1<<' '<<ans2<<endl;
	
	return 0;
}
