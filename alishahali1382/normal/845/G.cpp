const int mod = 1000000007;
const int MAXN = 100010, LOG=35;

int n, m, k, u, v, x, y, t, a, b, ans;
int A[MAXN];
int h[MAXN];
int hb[LOG];
bitset<MAXN> mark;
vector<pii> G[MAXN];

int query(int x){
	for (int i=0; i<LOG; i++) x=min(x, x^hb[i]);
	if (!x) return 0;
	hb[__builtin_clz(x)]=x;
	return x;
}

void dfs(int node){
	mark.set(node);
	for (pii p:G[node]){
		if (mark[p.first]) query(h[node]^h[p.first]^p.second);
		else{
			h[p.first]=h[node]^p.second;
			dfs(p.first);
		}
	}
}

