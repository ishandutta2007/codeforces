#define sz(x) (int)(x).size()


typedef string str;


#define ts to_string


str ts(const char* s) { return (str)s; }


template<class A> void pr(A x) { cout << ts(x); }

void ps() { pr("\n"); }
template<class H, class... T> void ps(const H& h, const T&... t) { pr(h); if (sizeof...(t)) pr(" "); ps(t...); }
 

void go() {
    string s;
    cin >> s;
    
    int tot = 0;
    int mn = 26;
    for (int i = 0; i < sz(s); i++) {
        mn = min(mn, s[i]-'a'+1);
        tot += s[i]-'a'+1;
    }
    
    int a = (sz(s) % 2 == 0 ? tot : tot - min(s[0]-'a'+1, s.back()-'a'+1));
    int b = tot-a;
    
    if (a > b) ps("Alice", a-b);
    else ps("Bob", b-a);
}

void solve()
{
    int t;
    for (cin >> t; t; t--) go();
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    solve();
    return 0;
}