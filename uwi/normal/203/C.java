	void solve()
	{
		int n = ni(), d = ni();
		int a = ni(), b = ni();
		int[][] c = new int[n][];
		for(int i = 0;i < n;i++){
			c[i] = new int[]{ni()*a+ni()*b, i};
		}
		
		Arrays.sort(c, new Comparator<int[]>() {
			public int compare(int[] a, int[] b) {
				return a[0] - b[0];
			}
		});
		
		long sum = 0;
		int j;
		for(j = 0;j < n && sum + c[j][0] <= d;j++){
			sum += c[j][0];
		}
		out.println(j);
		for(int k = 0;k < j;k++){
			if(k > 0)out.print(" ");
			out.print(c[k][1]+1);
		}
		out.println();
	}
	
