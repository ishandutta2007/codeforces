template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(int n){
    int c = 1;
    for(int a = 2; ;a++){
        int b = n-a-c;
        if(gcd(a, b) == c){
            cout << a << ' ' << b << ' ' << c << endl;
            assert(a+b+c == n);
            assert(a != b);
            assert(b != c);
            assert(c != a);
            return;
        }
    }
}

