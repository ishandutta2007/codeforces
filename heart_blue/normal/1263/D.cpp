const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
class UnionFind
{
public:
	int p[N];
	void init(int n = N)
	{
		memset(p, -1, sizeof(int) * n);
	}
	int treesize(int x)
	{
		return -p[Find(x)];
	}
	int Find(int x)
	{
		int s = x;
		while (p[s] >= 0) s = p[s];
		while (x != s)
		{
			int t = p[x];
			p[x] = s;
			x = t;
		}
		return s;
	}
	void Union(int x, int y)
	{
		int px = Find(x);
		int py = Find(y);
		if (p[px] > p[py]) swap(px, py);
		p[px] += p[py];
		p[py] = px;
	}
} uf;
vector<int> v[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	cin >> n;
	uf.init();
	for (int i = 1; i <= n; i++)
	{
		string str;
		cin >> str;
		for (auto& c : str)
		{
			v[c - 'a'].push_back(i);
		}
	}
	for (int i = 0; i < 26; i++)
	{
		if (v[i].empty()) continue;
		int x = v[i][0];
		for (auto& y : v[i])
		{
			if (uf.Find(x) != uf.Find(y))
				uf.Union(x, y);
		}
	}
	int ans = 0;
	for (int i = 1; i <= n; i++)
	{
		if (uf.Find(i) == i)
			ans++;
	}
	cout << ans << endl;
	return 0;
}