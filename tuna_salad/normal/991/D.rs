fn main() {
	let (mut rin,mut rout) = rio();

	let mut b = rin.board(2, None);
	let n = b[0].len();
	let mut ans = 0;
	for k in 1..n {
		let mut c = 0;
		let a = &[(0,k-1),(1,k-1),(0,k),(1,k)];
		for &(i,j) in a {
			if b[i][j] == b'0' {
				c += 1;
			}
		}
		if c >= 3 {
			ans += 1;
			let mut x = 3;
			for &(i,j) in a {
				if x > 0 && b[i][j] == b'0' {
					x -= 1;
					b[i][j] = b'#';
				}
			}
		}
	}
	writeln!(rout, "{}", ans).unwrap();
}