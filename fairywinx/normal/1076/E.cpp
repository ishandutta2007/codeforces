const int N = (1 << 20);
const int MAXD = 3e5 + 228;

long long t[2 * N + 228];

void update(int v, int l, int r, int L, int R, int val) {
	if (r <= L || R <= l)
		return;
	if (L <= l && r <= R) {
		t[v] += val;
		return;
	}

	int m = (l + r) / 2;
	update(v * 2 + 1, l, m, L, R, val);
	update(v * 2, m, r, L, R, val);
	t[v] = t[v * 2 + 1] + t[v * 2];
}

long long get(int v, int l, int r, int L, int R) {
	if (r <= L || R <= l)
		return 0;
	if (L <= l && r <= R)
		return t[v];

	int m = (l + r) / 2;
	return get(v * 2 + 1, l, m, L, R) + get(v * 2, m, r, L, R);
}

vector<int> G[N];
vector<pair<int, int>> q[N];
long long res[N];

void dfs(int v, int now_d, int p) {
	for(auto i: q[v])
		update(1, 0, N, i.first + now_d, i.first + 1 + now_d, i.second);
	res[v] = get(1, 0, N, now_d, N);
	for(auto i: G[v]) {
		if (i == p)
			continue;
		dfs(i, now_d + 1, v);
	}
	for(auto i: q[v]) 
		update(1, 0, N, i.first + now_d, i.first + 1 + now_d, -i.second);
}

signed main() {
	setup();
	
	int n;
	cin >> n;
	for(int i = 0; i < n - 1; ++i) {
		int a, b;
		cin >> a >> b;
		G[a - 1].push_back(b - 1);
		G[b - 1].push_back(a - 1);
	}

	int kol_q;
	cin >> kol_q;
	for(int i = 0; i < kol_q; ++i) {
		int v, d, x;
		cin >> v >> d >> x;
		d = min(MAXD, d);
		q[v - 1].emplace_back(d, x);
	}
	dfs(0, 0, 0);
	for(int i = 0; i < n; ++i)
		cout << res[i] << ' ';
	cout << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

