struct output {
    int res;

    void print() {
        cout << (res ? "YES" : "NO") << "\n";
    }

    bool operator == (const output& o) const {
        throw;
    }
};

struct input {
    vector<int> a;

    input() = default;

    void read() {
        a.resize(3);
        cin >> a[0] >> a[1] >> a[2];
    }

    void print() {
    }

    void gen() {
        // use static
    }

    output fast() {
        for (int i = 0; i < 3; i++) {
            int x = a[i];
            int y = a[(i + 1) % 3];
            int z = a[(i + 2) % 3];
            if (y == z && x % 2 == 0)
                return output{1};
            if (y + z == x)
                return output{1};
        }
        return output{0};
    }

    output slow() {
        return output();
    }
};

void test_case() {
    input in;
    in.read();
    output res = in.fast();
    res.print();
}

void work() {
    int t;
    cin >> t;
    while (t--)
        test_case();
}

