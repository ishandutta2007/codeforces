const ll MOD = 998244353;
ll add(ll x, ll y) {
	x += y;
	if (x >= MOD) return x - MOD;
	return x;
}
ll sub(ll x, ll y) {
	x -= y;
	if (x < 0) return x + MOD;
	return x;
}
ll mult(ll x, ll y) {
	return (x * y) % MOD;
}
ll bin_pow(ll x, ll p) {
	if (p == 0) return 1;
	if (p & 1) return mult(x, bin_pow(x, p - 1));
	return bin_pow(mult(x, x), p / 2);
}
ll rev(ll x) {
	return bin_pow(x, MOD - 2);
}


const int N = 200200;
ll f[N], rf[N];
char s[N];
int n, m;

ll getC(int n, int k) {
	if (k < 0 || k > n) return 0;
	return mult(f[n], mult(rf[k], rf[n - k]));
}

int main()
{
	startTime = clock();
//	freopen("input.txt", "r", stdin);
//	freopen("output.txt", "w", stdout);

	f[0] = 1;
	for (int i = 1; i < N; i++)
		f[i] = mult(f[i - 1], i);
	rf[N - 1] = rev(f[N - 1]);
	for (int i = N - 1; i > 0; i--)
		rf[i - 1] = mult(rf[i], i);

	scanf("%d %d %s", &n, &m, s);
	assert(m == 0);
	int bal = 0, q = 0;
	for (int i = 0; i < n; i++) {
		if (s[i] == '?') {
			q++;
			continue;
		}
		if ((s[i] == 'b') ^ (i & 1))
			bal++;
		else
			bal--;
	}
	ll ans = 0;
	for (int k = 0; k <= q; k++) {
		int x = bal + k - (q - k);
		x = abs(x);
		if (x % 4 != 0) continue;
		ans = add(ans, mult(x / 4, getC(q, k)));
	}
	ans = mult(ans, rev(bin_pow(2, q - 1)));
	printf("%lld\n", ans);

	return 0;
}