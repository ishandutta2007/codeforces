struct Segnode {
    static constexpr bool LAZY = false, RANGES = false;
    int128 g = 0;

    Segnode(int128 g = 0) : g(g) {}
    operator const int128&() const { return g; }

    void pushup(const Segnode& lhs, const Segnode& rhs) { g = lcm(lhs.g, rhs.g); }

    void apply(int128 v) { g = v; }
};

