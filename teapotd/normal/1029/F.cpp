int uplg(int n) { return 32-__builtin_clz(n); }
int uplg(ll n)  { return 64-__builtin_clzll(n); }

void run() {
	ll a, b; cin >> a >> b;
	ll sum = a+b;
	ll da = 1, db = 1;
	ll best = INT64_MAX;

	for (ll x = 1; x*x <= sum; x++) {
		if (a%x == 0) da = x;
		if (b%x == 0) db = x;
		if (sum%x == 0) {
			if (a/da <= sum/x || b/db <= sum/x) {
				best = min(best, x*2 + sum/x*2);
			}
		}
	}

	cout << best << endl;
}