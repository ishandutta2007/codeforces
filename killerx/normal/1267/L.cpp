#include <bits/stdc++.h>
using namespace std;

int n,m,k,nm;
char s[1000005];
int cnt[26];
char ss[1005][1005];
int len[1005];

int main(){
	scanf("%d%d%d",&n,&m,&k);
	nm=n*m;
	scanf("%s",s);
	sort(s,s+nm);
	for(int i=0;i<nm;i++){
		cnt[s[i]-'a']++;
	}
	int nds=k,bg=0;
	for(int ch=0;ch<26;ch++){
		int val=cnt[ch]/nds;
		int lt=cnt[ch]%nds;
		for(int i=1;i<=lt;i++){
			for(int j=0;j<=val;j++){
				ss[bg+i][len[bg+i]++]=ch+'a';
				cnt[ch]--;
				if(len[bg+i]==m){
					break;
				}
			}
		}
		for(int i=lt+1;bg+i<=k;i++){
			for(int j=0;j<val;j++){
				ss[bg+i][len[bg+i]++]=ch+'a';
				cnt[ch]--;
				if(len[bg+i]==m){
					break;
				}
			}
		}
		bg+=lt;
		nds=k-bg;
		if(len[k]==m){
			break;
		}
	}
	int nowch=0;
	for(int i=1;i<=n;i++){
		while(len[i]<m){
			while(cnt[nowch]==0){
				nowch++;
			}
			ss[i][len[i]++]=nowch+'a';
			cnt[nowch]--;
		}
	}
	for(int i=1;i<=n;i++){
		printf("%s\n",ss[i]);
	}
	return 0;
}