using int128 = __int128_t;

int128 gcd(int128 a, int128 b) {
    while (b) {
        a %= b, swap(a, b);
    }
    return a;
}
int128 lcm(int128 a, int128 b) { return a * (b / gcd(a, b)); }

istream& operator>>(istream& in, int128& x) {
    x = 0;
    string s;
    in >> s;
    assert('0' <= s[0] && s[0] <= '9');
    for (char c : s) {
        x = x * 10 + (c - '0');
    }
    return in;
}

string to_string(int128 x) {
    if (x == 0)
        return "0";
    __uint128_t k = x;
    if (k == (__uint128_t(1) << 127))
        return "-170141183460469231731687303715884105728";
    string result;
    if (x < 0) {
        result += "-";
        x *= -1;
    }
    string t;
    while (x) {
        t.push_back('0' + x % 10);
        x /= 10;
    }
    reverse(t.begin(), t.end());
    return result + t;
}

ostream& operator<<(ostream& o, int128 x) { return o << to_string(x); }

