struct custom_hash{
	static uint64_t splitmix64(uint64_t x){
		// http://xorshift.di.unimi.it/splitmix64.c
		x += 0x9e3779b97f4a7c15;
		x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
		x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
		return x ^ (x >> 31);
	}
	size_t operator()(uint64_t x) const {
		static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
		return splitmix64(x + FIXED_RANDOM);
	}
};
 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, m;
	cin >> n >> m;
	vi x(n);
	cin >> x;
	unordered_set<int, custom_hash> used;
	ll res = 0;
	deque<pii> q;
	vi pos;
	for(int i = 0; i < n; ++ i){
		q.push_back({x[i], 0});
		used.insert(x[i]);
	}
	while(1){
		auto [x, d] = q.front();
		q.pop_front();
		if(!used.count(x - 1)){
			used.insert(x - 1);
			pos.push_back(x - 1);
			q.push_back({x - 1, d + 1});
			res += d + 1;
			m --;
		}
		if(!m) break;
		if(!used.count(x + 1)){
			used.insert(x + 1);
			pos.push_back(x + 1);
			q.push_back({x + 1, d + 1});
			res += d + 1;
			m --;
		}
		if(!m) break;
	}
	cout << res << "\n" << pos;
	return 0;
}
 
