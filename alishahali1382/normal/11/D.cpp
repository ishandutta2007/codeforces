const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 20;

ll n, m, k, u, v, x, y, t, a, b, ans;
vector<int> G[MAXN];
bool adj[MAXN][MAXN];
ll dp[1<<MAXN][MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	while (m--){
		cin>>u>>v;
		u--;
		v--;
		if (u>v) swap(u, v);
		G[u].pb(v);
		G[v].pb(u);
		dp[(1<<u)|(1<<v)][v]=1;
		adj[u][v]=adj[v][u]=1;
	}
	for (int i=0; i<n; i++) dp[1<<i][i]=1;
	for (int mask=7; mask<(1<<n); mask++) if (__builtin_popcount(mask)>2) for (int i=0; i<n; i++) if (mask&(1<<i)){
		int mn=__builtin_ctz(mask);
		if (i==mn) continue ;
		for (int j:G[i]) if (mask&(1<<j)) dp[mask][i]+=dp[mask^(1<<i)][j];
		if (adj[mn][i]) ans+=dp[mask][i];
	}
	cout<<ans/2<<'\n';
	
	return 0;
}
