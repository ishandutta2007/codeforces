#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=1e6+5;
const int p=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
inline int power(int a,int b)
{
	int ret=1;
	while (b)
	{
		if (b&1) ret=ret*a%p;
		a=a*a%p;
		b/=2;
	}
	return ret;
}
inline void add(int &x,int y)
{
	x+=y;
	if (x>=p) x-=p;
}
struct node
{
	int x,y,v;
	bool operator <(const node &a)const
	{
		return v<a.v;
	}
}a[N];
int dp[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=read(),cnt=0;
	for (int i=1;i<=n;i++)
	for (int j=1;j<=m;j++)
	a[++cnt]=(node){i,j,read()};
	sort(a+1,a+1+cnt);
	a[cnt+1].v=-1;
	int x2=0,x=0,y2=0,y=0,sum=0;
	for (int i=1,j=0;i<=cnt;i++)
	if (a[i].v!=a[i+1].v)
	{
		for (int k=i;k>j;k--)
		{
			dp[k]=sum;
			add(dp[k],(a[k].x*a[k].x%p*j%p-a[k].x*x*2%p+x2+p)%p);
			add(dp[k],(a[k].y*a[k].y%p*j%p-a[k].y*y*2%p+y2+p)%p);
			dp[k]=dp[k]%p*power(j,p-2)%p;
		}
		for (int k=i;k>j;k--)
		{
			add(sum,dp[k]);
			add(x2,a[k].x*a[k].x%p);
			add(x,a[k].x);
			add(y2,a[k].y*a[k].y%p);
			add(y,a[k].y);
		}
		j=i;
	}
	int r=read(),c=read();
	for (int i=1;i<=cnt;i++) if (a[i].x==r&&a[i].y==c) print(dp[i]);
	
	return 0;
}
