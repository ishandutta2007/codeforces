#include<bits/stdc++.h>
using namespace std;
const int N=55;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
char a[N][N];
int dp[N][N][N][N];
inline void up(int &x,int y) {x=min(x,y);}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	for (int i=1;i<=n;i++)
	for (int j=1;j<=n;j++) cin>>a[i][j];
	for (int i=1;i<=n;i++)
	for (int j=1;j<=n;j++) dp[i][j][i][j]=a[i][j]=='#';
	for (int i=1;i<=n;i++)
	for (int j=1;j<=n;j++)
	for (int x=i;x>=1;x--)
	for (int y=j;y>=1;y--)
	if (x!=i||y!=j)
	{
		dp[x][y][i][j]=max(i-x+1,j-y+1);
		for (int k=x;k<i;k++) up(dp[x][y][i][j],dp[x][y][k][j]+dp[k+1][y][i][j]);
		for (int k=y;k<j;k++) up(dp[x][y][i][j],dp[x][y][i][k]+dp[x][k+1][i][j]);
	}
	print(dp[1][1][n][n]);
	
	return 0;
}
