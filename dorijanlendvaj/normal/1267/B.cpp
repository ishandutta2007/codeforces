int main(){
	cin.tie(0)->sync_with_stdio(0);
	string s;
	cin >> s;
	vector<pii> a;
	for(auto c: s){
		c -= 'A';
		if(a.empty() || a.back().first != c){
			a.push_back({c, 1});
		}
		else{
			a.back().second ++;
		}
	}
	auto kill = [](){
	    cout << 0;
	    exit(0);
	};
	int n = a.size();
	if(n % 2 == 0){
		kill();
	}
	for(int i = 0; i < n / 2; i ++){
		if(a[i].first != a[n - 1 - i].first || a[i].second + a[n - 1 - i].second < 3){
			kill();
		}
	}
	if(a[n / 2].second < 2){
		kill();
	}
	cout << a[n / 2].second + 1;
	return 0;
}
 
