const int N = 2e5 + 228;

vector<int> G[N];
int col[N];
int mx_cnt[N];
int ans[N];

int dfs1(int v, int p) {
	int res = 0;
	for(auto i: G[v]) {
		if (i == p)
			continue;
		int z = dfs1(i, v);
		res += z;
	}
	if (col[v] == 1)
		++res;
	else
		--res;
	mx_cnt[v] = max(res, 0);
	return max(0, res);
}

void dfs(int v, int p, int val) {
	vector<int> cur;
	for(auto i: G[v]) {
		if (i == p)
			continue;
		cur.push_back(mx_cnt[i]);
	}
	if (col[v] == 1)
		++val;
	else
		--val;
	if (cur.size() == 0) {
		ans[v] = val;
		return;
	}
	vector<int> pref(cur.size() + 1);
	for(int i = 0; i < cur.size(); ++i)
		pref[i + 1] = pref[i] + cur[i];
	vector<int> suf(cur.size() + 1);
	for(int i = cur.size() - 1; i >= 0; --i)
		suf[i] = suf[i + 1] + cur[i];
	ans[v] = val + pref.back();
	int j = 0;

	for(auto i: G[v]) {
		if (i == p)
			continue;
		dfs(i, v, max(pref[j] + suf[j + 1] + val, 0));
		++j;
	}
}

signed main() {
	setup();

	int n;
	cin >> n;
	for(int i = 0; i < n; ++i)
		cin >> col[i];
	for(int i = 0; i < n - 1; ++i) {
		int a, b;
		cin >> a >> b;
		G[a - 1].push_back(b - 1);
		G[b - 1].push_back(a - 1);
	}
	dfs1(0, -1);
	dfs(0, -1, 0);
	for(int i = 0; i < n; ++i)
		cout << ans[i] << ' ';
	cout << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

