#define debug(x) cerr<<#x<<'='<<(x)<<endl;
#define debugp(x) cerr<<#x<<"= {"<<(x.first)<<", "<<(x.second)<<"}"<<endl;
#define debug2(x, y) cerr<<"{"<<#x<<", "<<#y<<"} = {"<<(x)<<", "<<(y)<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back
#define kill(x) return cout<<x<<'\n', 0;

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 5010, LOG=20;

int n, m, k, u, v, x, y, t, root, ans;
vector<int> Gout[MAXN], Gin[MAXN];
vector<int> topol;
bitset<MAXN> mark;
int comp[MAXN];

void dfs(int node){
	mark[node]=1;
	for (int i:Gout[node]) if (!mark[i]) dfs(i);
	topol.pb(node);
}
void sfd(int node, int c){
	comp[node]=c;
	for (int i:Gin[node]) if (!comp[i]) sfd(i, c);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m>>root;
	while (m--){
		cin>>u>>v;
		Gout[u].pb(v);
		Gin[v].pb(u);
	}
	for (int i=1; i<=n; i++) if (!mark[i]) dfs(i);
	reverse(all(topol));
	for (int i:topol) if (!comp[i]) sfd(i, i);
	mark=0;
	mark[comp[root]]=1;
	for (int i=1; i<=n; i++) for (int j:Gout[i]) if (comp[i]!=comp[j]) mark[comp[j]]=1;
	for (int i=1; i<=n; i++) if (comp[i]==i && !mark[i]) ans++; 
	cout<<ans<<'\n';
	
	return 0;
}
