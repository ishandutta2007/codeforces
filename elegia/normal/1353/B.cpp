  while (t--) {
    int n, k;
    cin >> n >> k;
    vector<pair<int, int>> a;
    for (int rep = 0; rep < n * 2; ++rep) {
      int x;
      cin >> x;
      a.emplace_back(x, rep / n);
    }
    sort(a.begin(), a.end(), greater<pair<int, int>>());
    int cnt = 0, s = 0, ans = 0;
    for (int i = 0; i < n * 2; ++i) {
      if ((s + a[i].second) > k) continue;
      s += a[i].second;
      ans += a[i].first;
      if (++cnt == n) break;
    }
    cout << ans << '\n';
  }

