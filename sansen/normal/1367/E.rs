use std::io::Write;

fn gcd(a: usize, b: usize) -> usize {
    if b == 0 {
        a
    } else {
        gcd(b, a % b)
    }
}

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        ask: [(usize, usize, chars); t],
    }
    for (n, k, s) in ask {
        let mut map = std::collections::BTreeMap::new();
        for c in s {
            *map.entry(c).or_insert(0) += 1;
        }
        let mut ans = 1;
        for i in 1..=n {
            let g = gcd(i, k);
            let d = i / g;
            let mut sum = 0;
            for (_, &c) in map.iter() {
                sum += c / d * d;
            }
            if sum >= i {
                ans = i;
            }
        }
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}