	void solve()
	{
		char[] a = ns(100001);
		char[] b = ns(100001);
		String A = new String(a);
		String B = new String(b);
		int ca = a.length - kmpTable(a)[a.length];
		if(a.length % ca != 0)ca = a.length;
		int cb = b.length - kmpTable(b)[b.length];
		if(b.length % cb != 0)cb = b.length;
		int ct = 0;
		boolean f = false;
		for(int i = 100000;i >= 1;i--){
			if(i % ca == 0 && i % cb == 0 && a.length % i == 0 && b.length % i == 0){
				if(f || A.substring(0, i).equals(B.substring(0, i))){
					f = true;
					ct++;
				}
			}
		}
		out.println(ct);
	}
	
	public static int[] kmpTable(char[] str)
	{
		int n = str.length;
		int[] kmp = new int[n+1];
		kmp[0] = -1; kmp[1] = 0;
		for(int i = 2, j = 0;i <= n;i++){
			while(j > 0 && str[i-1] != str[j])j = kmp[j];
			kmp[i] = str[i-1] == str[j] ? ++j : 0;
		}
		return kmp;
	}
	
