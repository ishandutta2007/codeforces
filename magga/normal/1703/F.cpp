void solve(){
    int n;cin>>n;
    long long int arr[n+1];
    vector<long long int>ps(n+1);
    long long int ans = 0;
    for(int i=1;i<=n;i++){
        cin>>arr[i];
        if(arr[i]>=i){
            ps[i] = ps[i-1];
        }else{
            ps[i] = ps[i-1] + 1;
            if(arr[i]!=0)
            ans += ps[arr[i]-1];
        }
    }
    cout<<ans<<"\n";
}
