typedef long long ll;
typedef unsigned long long ull;
ll gcd(ll a,ll b) { return b==0?a:gcd(b,a%b); }

int n,p;

int solve() {
	for(int ret=1;;++ret) {
		ll x=n-(ll)ret*p;
		if(x<ret) return -1;
		int cnt=0; while(x>0) cnt+=x&1,x/=2;
		if(cnt<=ret) return ret;
	}
}

void run() {
	scanf("%d%d",&n,&p);
	printf("%d\n",solve());
}

int main() {
	run();
	return 0;
}