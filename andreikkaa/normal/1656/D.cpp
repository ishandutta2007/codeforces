long long mult(long long a, long long b, long long mod) {
    long long result = 0;
    while (b) {
        if (b & 1)
            result = (result + a) % mod;
        a = (a + a) % mod;
        b >>= 1;
    }
    return result;
}

long long f(long long x, long long c, long long mod) {
    return (mult(x, x, mod) + c) % mod;
}


long long brent(long long n, long long x0=2, long long c=1) {
    long long x = x0;
    long long g = 1;
    long long q = 1;
    long long xs, y;

    int m = 128;
    int l = 1;
    while (g == 1) {
        y = x;
        for (int i = 1; i < l; i++)
            x = f(x, c, n);
        int k = 0;
        while (k < l && g == 1) {
            xs = x;
            for (int i = 0; i < m && i < l - k; i++) {
                x = f(x, c, n);
                q = mult(q, abs(y - x), n);
            }
            g = gcd(q, n);
            k += m;
        }
        l *= 2;
    }
    if (g == n) {
        do {
            xs = f(xs, c, n);
            g = gcd(abs(xs - y), n);
        } while (g == 1);
    }
    return g;
}

unordered_map<ll, ll> mem;

inline void _solve() {
    ll n;
#ifndef LOCAL
    cin >> n;
#else
    static int now = 2;
    n = now;
    ++now;
#endif

    if(n & 1) {
        cout << 2 << endl;
        return;
    }

    if((n & (n - 1)) == 0) {
        cout << -1 << endl;
        return;
    }

    ll m = n;
    int cc = 0;
    while(m % 2 == 0) {
        m /= 2;
        ++cc;
    }

    ll tst = 2 * (1LL << cc);
    ld dd = tst;
    if(dd * (dd + 1) / 2 < 2e18 && tst * (tst + 1) / 2 <= n) {
        cout << tst << endl;
        return;
    }

    cout << m << endl;
    return;
    cout << n << ":( " << m << endl;
    while(m > 1) {
        auto it = mem.find(m);
        ll dv;
        if(it != mem.end()) {
            dv = it->y;
        } else {
            dv = brent(m);
            mem[m] = dv;
        }
        ld dd = dv;
        if(dd * (dd + 1) / 2 < 2e18 && dv * (dv + 1) / 2 <= n) {
            cout << dv << endl;
            return;
        }

        {
            ld ddd = m / dv;
            ll dvv = m / dv;
            if(ddd * (ddd + 1) / 2 < 2e18 && dvv * (dvv + 1) / 2 <= n) {
                cout << dvv << endl;
                return;
            }
        }

        m /= dv;
    }

    assert(false);
}

void _main_function() {
    mem.rehash(5'000'000);
    int t = 1;
    cin >> t;

    for(int i = 0; i < t; ++i) {
        _solve();
    }
}

