int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<vector<bool>> cnt(5, vector<bool>(5, false));
    auto idx = [](string S) {
        int x = -1, y = -1;
        if (S[0] == 'R')
            x = 0;
        else if (S[0] == 'G')
            x = 1;
        else if (S[0] == 'B')
            x = 2;
        else if (S[0] == 'Y')
            x = 3;
        else
            x = 4;
        y = S[1] - '1';
        return make_pair(x, y);
    };
    for (; n--;) {
        string S;
        cin >> S;
        auto p = idx(S);
        cnt[p.first][p.second] = true;
    }

    int ans = 10;
    for (int mask = 0; mask < (1 << 10); mask++) {
        vector<int> use;
        for (int i = 0; i < 10; i++) {
            if (mask >> i & 1) {
                use.emplace_back(i);
            }
        }

        int m = use.size();
        vector<int> ncnt(1 << m, 0);
        bool ok = true;
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                if (!cnt[i][j]) continue;
                int bit = 0;
                for (int k = 0; k < m; k++) {
                    if (use[k] < 5 && i == use[k]) bit |= 1 << k;
                    if (use[k] >= 5 && j == use[k] - 5) bit |= 1 << k;
                }
                if (ncnt[bit]++ == 1) ok = false;
            }
        }
        if (ok) chmin(ans, m);
    }

    cout << ans << '\n';
    return 0;
}