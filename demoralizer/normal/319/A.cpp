int pw(int a,int b,int p){
    if(b==0)return 1;
    int t=pw(a,b/2,p);
    if(b%2)return (((a*t)%p)*t)%p;
    else return ((t*t)%p);
}
void solve(){
    string x;
    cin>>x;
    int n=x.size();
    int ans=0;
    for(int i=0;i<n;i++){
    	ans+=(x[n-1-i]-'0')*pw(2,i,M);ans%=M;
    }
    ans*=pw(2,n-1,M);ans%=M;
    cout<<ans;
}
