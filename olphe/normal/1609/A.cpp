void Solve() {
	cin >> N;
	vector<long long>v(N);
	long long by = 1;
	for (auto& i : v) {
		cin >> i;
		while (i % 2 == 0) {
			i /= 2;
			by *= 2;
		}
	}
	sort(v.begin(), v.end());
	v.back() *= by;
	cout << accumulate(v.begin(), v.end(), 0LL) << endl;
	return;
}

