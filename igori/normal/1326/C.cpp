const int N = 510200;
const int MOD = 998244353;

int n, k;
int p[N];
int pos[N];
int a[N];
int c = 0;

signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >> n >> k;
    for (int i = 1; i <= n; i++) cin >> p[i];
    for (int i = 1; i <= n; i++)
    {
        if (p[i] > n - k)
        {
            a[i] = 1;
            c += p[i];
        }
    }
    long long ans = 1;
    int counter = k;
    for (int i = 1; i <= n; i++)
    {
        if (counter > 1 && a[i] == 1)
        {
            int j = i + 1;
            while (a[j] != 1)
            {
                j++;
            }
            ans = (ans * (j - i)) % MOD;
            i = j - 1;
            counter--;
        }
    }
    cout << c << " " << ans;
}