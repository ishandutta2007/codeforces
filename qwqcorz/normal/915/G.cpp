#include<bits/stdc++.h>
using namespace std;
const int N=2e6+5;
const int p=1e9+7;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int power(int a,int b)
{
	int ret=1;
	while (b)
	{
		if (b&1) ret=1LL*ret*a%p;
		a=1LL*a*a%p;
		b/=2;
	}
	return ret;
}
int pw[N];
bool vis[N];
int miu[N],pr[N/10],cntp=0;
struct edge
{
	int to,nxt;
}e[(int)(N*14.7)];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}

signed main(signed Recall,char *_902_[])
{
	miu[1]=1;
	(void)Recall,(void)_902_;
	int n=read(),k=read();
	for (int i=2;i<=k;i++)
	{
		if (!vis[i]) pr[++cntp]=i,miu[i]=-1;
		for (int j=1;j<=cntp;j++)
		{
			if (i*pr[j]>k) break;
			vis[i*pr[j]]=1;
			if (i%pr[j]==0)
			{
				miu[i*pr[j]]=0;
				break;
			}
			else miu[i*pr[j]]=miu[i]*-1;
		}
	}
	for (int i=1;i<=k;i++)
	for (int j=i;j<=k;j+=i) add_edge(j,i);
	for (int i=0;i<=k;i++) pw[i]=power(i,n);
	int ans=0;
	for (int i=1,tot=0;i<=k;i++)
	{
		for (int j=head[i];j;j=e[j].nxt)
		{
			int d=e[j].to;
			tot=(tot+1LL*miu[d]*(-pw[i/d-1]+pw[i/d])%p+p)%p;
		}
		ans=(ans+(tot^i))%p;
	}
	print(ans);
	
	return 0;
}
