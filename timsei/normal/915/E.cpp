#define getmid register int mid = (L + R) >> 1
#define lc ch[no][0]
#define rc ch[no][1]

void dn(int no , int L , int R) {
	if(!tag[no] || L == R) return;
	getmid; if(!lc) lc = ++ sz; if(!rc) rc = ++ sz;
	if(tag[no] == -1) {
		S[lc] = mid - L + 1; S[rc] = R - mid;
	}
	else S[lc] = S[rc] = 0;
	tag[lc] = tag[rc] = tag[no] , tag[no] = 0;
	S[no] = S[lc] + S[rc];
}

void push(int &no , int L , int R , int l , int r , int ty) {
	if(!no) no = ++ sz;
	if(L == l && R == r) {
		tag[no] = ty; if(ty == -1) S[no] = R - L + 1; else S[no] = 0;return;
	}
	dn(no , L , R);
	getmid;
	if(l > mid) {
		push(rc , mid + 1 , R , l , r , ty);
	}
	else if(r <= mid) push(lc , L , mid , l , r , ty);
	else {
		push(lc , L , mid , l , mid , ty) , push(rc , mid + 1 , R , mid + 1 , r , ty);
	}
	S[no] = S[lc] + S[rc];
}

