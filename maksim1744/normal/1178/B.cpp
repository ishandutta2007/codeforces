int main() {
    string s;
    cin >> s;
    int n = s.size();
    if (n < 5) {
        cout << 0 << '\n';
        return 0;
    }
    vector<ll> ls(n, 0), rs(n, 0);
    for (int i = 1; i < n - 1; ++i) {
        ls[i + 1] = ls[i];
        if (s[i] == 'v' && s[i - 1] == 'v') {
            ls[i + 1]++;
        }
    }
    for (int i = n - 2; i > 0; --i) {
        rs[i] = rs[i + 1];
        if (s[i] == 'v' && s[i + 1] == 'v') {
            ++rs[i];
        }
    }
    // cout << ls << endl;
    // cout << rs << endl;
    ll ans = 0;
    for (int i = 0; i < n; ++i) {
        if (s[i] == 'o')
            ans += ls[i] * rs[i];
    }
    cout << ans << '\n';
    return 0;
}