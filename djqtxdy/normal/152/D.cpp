int dc;
inline void Add(int x,int y,int v){
	if (!g[x][y]) dc++;
	g[x][y]+=v;
	if (!g[x][y]) dc--;
}
void Paint(rec x,int v=1){
	for (int i=x.X1;i<=x.X2;i++){
		Add(i,x.Y1,v);
		Add(i,x.Y2,v);
	}
	for (int i=x.Y1+1;i<x.Y2;i++){
		Add(x.X1,i,v);
		Add(x.X2,i,v);
	}
}
