struct fenwick {
    int n{};
    vector<ll> fenw{};

    void build(int k) {
        n = k;
        fenw.resize(n);
    }

    void upd(int i, ll x) {
        for (; i < n; i = i | (i + 1)) fenw[i] += x;
    }

    ll get(int i) {
        ll res = 0;
        for (; i >= 0; i = (i & (i + 1)) - 1) res += fenw[i];
        return res;
    }

    ll get(int l, int r) {
        if (l > r) return 0;
        return get(r) - get(l - 1);
    }
};


void solve() {
    vector<vector<int>> pos(30);
    int n; cin >> n;
    string s; cin >> s;
    fenwick f;
    f.build(n);
    for(int i = 1; i < n; ++i) f.upd(i, 1);
    for(int i = n - 1; i >= 0; --i) pos[s[i]-'a'].push_back(i);
    string s2 = s;
    reverse(all(s2));
    ll ans = 0;
    range(i, n) {
        int j = pos[s2[i]-'a'].back();
        pos[s2[i]-'a'].pop_back();
        int j2 = f.get(j);
        ans += j2;
        f.upd(j, -1);
    }
    cout << ans << "\n";
}

