void run_case() {
    int N, K;
    cin >> N >> K;
    vector<int> X(N), Y(N);

    for (int i = 0; i < N; i++)
        cin >> X[i] >> Y[i];

    bool possible = false;

    for (int base = 0; base < N; base++) {
        bool good = true;

        for (int i = 0; i < N; i++)
            if (abs(X[i] - X[base]) + abs(Y[i] - Y[base]) > K)
                good = false;

        possible = possible || good;
    }

    cout << (possible ? 1 : -1) << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}