constexpr int md = (int) 1e9 + 7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;
 
#define Hash uint64_t 

mt19937 rng((uint32_t) chrono::steady_clock::now().time_since_epoch().count());

int main () {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n, m;
  cin >> n >> m;
  set<pair<Hash, Mint>> st;
  Hash multi1 = 37;
  Mint multi2 = 37;
  auto Rand = [&] {
    return uniform_int_distribution<int>(1, multi1 - 1)(rng); 
  };
  
  vector<Hash> w1(256);
  vector<Hash> w2(256);
  w1['a'] = w2['c'] = Rand();
  w1['b'] = w2['a'] = Rand();
  w1['c'] = w2['b'] = Rand();
  auto Power = [&](Hash x, int y) {
    Hash res = 1;
    while (y > 0) {
      if (y & 1) {
        res *= x;
      }

      x *= x;
      y >>= 1;
    }

    return res;
  };
  
  auto Hash1 = [&](string s) {
    Hash res = 0;
    for (int i = 0; i < (int) s.size(); ++i) {
      res += Power(multi1, i) * w1[s[i]];
    }

    return res;
  };
  
  auto Hash2 = [&](string s) {
    Mint res = 0;
    for (int i = 0; i < (int) s.size(); ++i) {
      res += power(multi2, i) * w2[s[i]];
    }

    return res;    
  };


  auto change = [&](pair<Hash, Mint> x, char old, char nw, int ind) {
    {
      int add = w1[nw] - w1[old];
      x.first += add * Power(multi1, ind);  
    }

    {
      int add = w2[nw] - w2[old];
      x.second += add * power(multi2, ind);  
    }

    return x;
  };
  
  for (int i = 0; i < n; ++i) {
    string s;
    cin >> s;
    st.emplace(Hash1(s), Hash2(s));
  }

  while (m--) {
    string s;
    cin >> s;
    int size = (int) s.size();
    bool ok = false;
    auto cur = make_pair(Hash1(s), Hash2(s));
    for (int i = 0; i < size; ++i) {
      for (char c : "abc"s) {
        if (c == s[i]) {
          continue;
        }

        ok |= st.count(change(cur, s[i], c, i));
      }
    }

    cout << (ok ? "YES" : "NO") << '\n';
  }

}