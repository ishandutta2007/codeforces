void solve()
{
	string s;
	cin >> s;
	bool a = false, b = false;
	for (auto c : s)
	{
		if (c == '0')
		{
			cout << 3 - a * 2 << ' ' << 1 << '\n';
			a = !a;
		}
		else
		{
			cout << 1 << ' ' << 3 - b * 2 << '\n';
			b = !b;
		}
	}
}

