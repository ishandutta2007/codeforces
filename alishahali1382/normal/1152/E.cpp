const int MAXN = 100010, LOG=20;

int n, m, k, u, v, x, y, t, a, b, ans, root;
int B[MAXN], C[MAXN];
map<int, multiset<int>> G;
vector<int> tour;

void addedge(int u, int v){
	G[u].insert(v);
	G[v].insert(u);
}

void euler(int node){
	while (G[node].size()){
		int v=*G[node].begin();
		G[node].erase(G[node].begin());
		G[v].erase(G[v].find(node));
		euler(v);
		//debug2(node, v)
		//tour.pb(v);
	}
	tour.pb(node);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<n; i++) cin>>B[i];
	for (int i=1; i<n; i++) cin>>C[i];
	for (int i=1; i<n; i++){
		addedge(B[i], C[i]);
		if (C[i]<B[i]) kill(-1);
		root=B[i];
	}
	for (auto &it:G) if (it.second.size()&1) root=it.first;
	for (auto &it:G) if (it.second.size()&1) t++;
	
	euler(root);
	if (tour.size()<n-1 || t>2) kill(-1);
	for (int i:tour) cout<<i<<' ';
	
	return 0;
}
