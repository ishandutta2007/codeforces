#include <bits/stdc++.h>
using namespace std;

int n,m;
int r[100005],c[100005];
int dp[805];
int qzh[805];
vector<int> hv[100005];

int main(){
	scanf("%d%d",&n,&m);
	int wid=sqrt(m*6);
	for(int i=1;i<=m;i++){
		scanf("%d%d",r+i,c+i);
		int cc=r[i];
		r[i]=c[i]+n-r[i];
		c[i]=n+1-cc;
		hv[r[i]].push_back(c[i]);
	}
	for(int i=1;i<=n;i++){
		memset(qzh,0,sizeof(qzh));
		for(int j=0;j<hv[i].size();j++){
			qzh[min(wid+1,hv[i][j])]++;
		}
		for(int i=wid;i>=1;i--){
			qzh[i]+=qzh[i+1];
		}
		int sum=dp[0]+qzh[1]*3;
		for(int j=min(i,wid);j>=1;j--){
			dp[j]=min(dp[j-1]+qzh[j+1]*3,sum);
			sum=min(sum,dp[j-1]+qzh[j+1]*3+2+j*(j+1)/2);
		}
		dp[0]=sum;
	}
	printf("%d\n",dp[0]);
	return 0;
}