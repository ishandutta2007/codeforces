void solve() {
	int n, a, b;
	cin >> n >> a >> b;
	string s;
	cin >> s;
	s += '0';
	pair<int, int> ans = {0, 1e18};
	for(int i = 0; i <= n; ++i) {
		pair<int, int> tmp = {1e18, 1e18};
		if (s[i] == '0') {
			tmp.second = min(ans.second, ans.first + a) + b;
			tmp.first = min(ans.first, ans.second + a);
		}
		else 
			tmp.second = ans.second + b;
		ans = tmp;
	} 	
	cout << ans.first + n * (a + b) + b << '\n';
}

