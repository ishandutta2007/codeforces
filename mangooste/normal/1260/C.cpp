long long gcd(long long a, long long b) {
    while (b != 0) {
        a %= b;
        swap(a, b);
    }
    return a;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        long long r, b, k;
        cin >> r >> b >> k;
        if (r > b)
            swap(r, b);
        if (r * (k - 1) + gcd(r, b) < b)
            cout << "REBEL\n";
        else
            cout << "OBEY\n";
    }
}