    static class TaskA {
        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int n = in.nextInt();
            int[] p = in.readIntArray(n - 1);
            List<Integer>[] child = LUtils.genArrayList(n);
            for (int i = 0; i < n - 1; i++) {
                child[p[i] - 1].add(i + 1);
            }
            int[] depth = new int[n];
            for (int i = 1; i < n; i++) {
                depth[i] = depth[p[i - 1] - 1] + 1;
            }
            int ans = n;
            HashSet<Integer>[] dp = new HashSet[n];
            for (int i = 0; i < n; i++) dp[i] = new HashSet<>();
            for (int i = n - 1; i >= 0; i--) {
                int big = 0, bidx = -1;
                for (int nxt : child[i]) {
                    if (dp[nxt].size() > big) {
                        big = dp[nxt].size();
                        bidx = nxt;
                    }
                }
                if (bidx != -1) {
                    dp[i] = dp[bidx];
                }
                dp[i].add(depth[i]);
                for (int nxt : child[i]) {
                    if (nxt == bidx) continue;
                    for (int k : dp[nxt]) {
                        if (!dp[i].add(k)) dp[i].remove(k);
                    }
                }
            }

            out.println(dp[0].size());
        }

    }

    static class InputReader {
        private InputStream stream;
        private byte[] buf = new byte[1 << 16];
        private int curChar;
        private int numChars;

        public InputReader(InputStream stream) {
            this.stream = stream;
        }

        public int[] readIntArray(int tokens) {
            int[] ret = new int[tokens];
            for (int i = 0; i < tokens; i++) {
                ret[i] = nextInt();
            }
            return ret;
        }

        public int read() {
            if (this.numChars == -1) {
                throw new InputMismatchException();
            } else {
                if (this.curChar >= this.numChars) {
                    this.curChar = 0;

                    try {
                        this.numChars = this.stream.read(this.buf);
                    } catch (IOException var2) {
                        throw new InputMismatchException();
                    }

                    if (this.numChars <= 0) {
                        return -1;
                    }
                }

                return this.buf[this.curChar++];
            }
        }

        public int nextInt() {
            int c;
            for (c = this.read(); isSpaceChar(c); c = this.read()) {
                ;
            }

            byte sgn = 1;
            if (c == 45) {
                sgn = -1;
                c = this.read();
            }

            int res = 0;

            while (c >= 48 && c <= 57) {
                res *= 10;
                res += c - 48;
                c = this.read();
                if (isSpaceChar(c)) {
                    return res * sgn;
                }
            }

            throw new InputMismatchException();
        }

        public static boolean isSpaceChar(int c) {
            return c == 32 || c == 10 || c == 13 || c == 9 || c == -1;
        }

    }

    static class OutputWriter {
        private final PrintWriter writer;

        public OutputWriter(OutputStream outputStream) {
            writer = new PrintWriter(new BufferedWriter(new OutputStreamWriter(outputStream)));
        }

        public OutputWriter(Writer writer) {
            this.writer = new PrintWriter(writer);
        }

        public void close() {
            writer.close();
        }

        public void println(int i) {
            writer.println(i);
        }

    }

    static class LUtils {
        public static <E> List<E>[] genArrayList(int size) {
            return Stream.generate(ArrayList::new).limit(size).toArray(List[]::new);
        }

    }
