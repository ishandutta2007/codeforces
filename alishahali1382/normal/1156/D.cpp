struct DSU{
	int par[MAXN];
	int sz[MAXN];
	DSU(){ for (int i=1; i<MAXN; i++) par[i]=i, sz[i]=1;}
	int get(int x){
		if (par[x]==x) return x;
		return par[x]=get(par[x]);
	}
	void join(int x, int y){
		x=get(x);
		y=get(y);
		if (x==y) return ;
		par[x]=y;
		sz[y]+=sz[x];
	}
	ll getsz(int x){ return sz[get(x)];}
} dsu0, dsu1;

