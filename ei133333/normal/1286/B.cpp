template< typename F >
struct FixPoint : F {
  FixPoint(F &&f) : F(forward< F >(f)) {}

  template< typename... Args >
  decltype(auto) operator()(Args &&... args) const {
    return F::operator()(*this, forward< Args >(args)...);
  }
};

template< typename F >
inline decltype(auto) MFP(F &&f) {
  return FixPoint< F >{forward< F >(f)};
}

template< typename T >
struct edge {
  int src, to;
  T cost;

  edge(int to, T cost) : src(-1), to(to), cost(cost) {}

  edge(int src, int to, T cost) : src(src), to(to), cost(cost) {}

  edge &operator=(const int &x) {
    to = x;
    return *this;
  }

  operator int() const { return to; }
};

template< typename T >
using Edges = vector< edge< T > >;
template< typename T >
using WeightedGraph = vector< Edges< T > >;
using UnWeightedGraph = vector< vector< int > >;
template< typename T >
using Matrix = vector< vector< T > >;

int main() {
  int N;
  cin >> N;
  UnWeightedGraph g(N);
  vector< int > C(N);
  int r = -1;
  for(int i = 0; i < N; i++) {
    int p;
    cin >> p >> C[i];
    --p;
    if(p >= 0) g[p].emplace_back(i);
    else r = i;
  }

  vector< int > A(N);

  auto rec3 = MFP([&](auto rec3, int idx, int pad) -> int {
    A[idx] += pad;
    int sz = 1;
    for(auto &to : g[idx]) {
      sz += rec3(to, pad);
    }
    return sz;
  });


  auto rec4 = MFP([&](auto rec4, int idx, int pad) -> void {
    if(A[idx] >= pad) ++A[idx];
    for(auto &to : g[idx]) {
      rec4(to, pad);
    }
  });

  auto rec = MFP([&](auto rec, int idx) -> void {
    int sz = 0;
    for(auto &to : g[idx]) {
      rec(to);
      sz += rec3(to, sz);
    }
    if(sz < C[idx]) {
      cout << "NO" << endl;
      exit(0);
    }
    rec4(idx, C[idx]);
    A[idx] = C[idx];
  });
  rec(r);
  cout << "YES" << endl;
  for(auto &p : A) ++p;
  cout << A << endl;
}