void add(int x,int y){
	deg.flip(x);
	deg.flip(y);
	to[++e]=y;lst[e]=beg[x];beg[x]=e;
	to[++e]=x;lst[e]=beg[y];beg[y]=e;
}
void dfs(int x){
	for(int &i=beg[x];i;i=lst[i])if(!vis[i>>1]){
		vis[i>>1]=1;
		ans[i]=1;
		int y=to[i];
		i=lst[i];
		dfs(y);
	}
}
