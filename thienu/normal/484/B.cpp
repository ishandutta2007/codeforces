// harmonic series is log(n)!
void solve(){
    int n;
    cin >> n;
    vector<int> v(n);
    for(int i = 0; i < n; i++){
        cin >> v[i];
    }

    sort(v.begin(), v.end());
    vector<int> no_dup;
    for(int i = 0; i < n; i++){
        if(i == 0 || v[i] != v[i-1]){
            no_dup.push_back(v[i]);
        }
    }
    v = no_dup;
    n = v.size();
    if(n == 1) {
        cout << 0 << endl;
        return;
    }
    debug(v);
    int ans = 0;
    int maxi = v[n-1];
    for(int i = 0; i < n; i++){
        int j = v[i];
        for(int cand = 2*j; cand <= maxi+j; cand+=j){
            auto p = lower_bound(v.begin(), v.end(), cand) - v.begin();
            debug(j, cand, p-1);
            ans = max(ans, v[p-1] % j);
        }
    }
    cout << ans << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}