int n, d, e;

bool read() {
  if (scanf("%d%d%d", &n, &d, &e) < 3) {
    return false;
  }
  return true;
}

void solve() {
  e *= 5;
  int res = n;
  for (int i = 0; i * e <= n; i++) {
    int r = n - i * e;
    res = min(res, r % d);
  }
  printf("%d\n", res);
}

int main() {
  precalc();
#ifdef DEBUG
  assert(freopen(TASK ".in", "r", stdin));
  assert(freopen(TASK ".out", "w", stdout));
#endif
  while (read()) {
    solve();
#ifdef DEBUG
    eprintf("Time %.2f\n", (double) clock() / CLOCKS_PER_SEC);
#endif
  }
  return 0;
}