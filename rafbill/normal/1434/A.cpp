template<typename T>
T isqrt(T const&x){
  static_assert(is_integral<T>::value, "is_integral<T>::value");
  assert(x>=T(0));
  T ret = static_cast<T>(sqrtl(x));
  while(ret>0 && ret*ret>x) --ret;
  while(x-ret*ret>2*ret) ++ret;
  return ret;
}

template<typename T>
T icbrt(T const&x) {
  static_assert(is_integral<T>::value, "is_integral<T>::value");
  assert(x>=T(0));
  T ret = static_cast<T>(cbrt(x));
  while(ret>0 && ret*ret*ret>x) --ret;
  while(x-ret*ret*ret>3*ret*(ret+1)) ++ret;
  return ret;
}

const int K = 6;
li A[K];
const int N = 200'000;

li dp[N][K];

int main(){
  ios::sync_with_stdio(0); cin.tie(0);

  for(li i = 0; i < (li)(K); ++i) cin>>A[i];
  sort(begin(A), end(A));

  int n; cin>>n;
  vi B(n); for(li i = 0; i < (li)(n); ++i) cin>>B[i];
  sort(begin(B), end(B));

  map<li, vi> ST; for(li i = 0; i < (li)(n); ++i) for(li j = 0; j < (li)(K); ++j) ST[B[i]-A[j]].push_back(i);
  vector<pair<li,vi>> T(begin(ST), end(ST));

  vi E(n, 0);
  int cnt = 0;
  auto add = [&](int i) {
    for(auto x : T[i].second) {
      if(!E[x]) cnt++;
      E[x]++;
    }
  };
  auto rem = [&](int i) {
    for(auto x : T[i].second) {
      E[x]--;
      if(!E[x]) cnt--;
    }
  };

  li ans = 1ll<<60;
  int to = 0;
  while(cnt != n && to != (int)T.size()) { add(to); to += 1; }
  for(li from = 0; from < (li)(T.size()); ++from) {
    if(from) {
      rem(from-1);
      while(cnt != n && to != (int)T.size()) { add(to); to += 1; }
    }
    if(cnt == n) {
      ans = min(ans, T[to-1].first - T[from].first);
    }
  }

  cout << ans << endl;

  return 0;
}