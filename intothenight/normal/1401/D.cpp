constexpr int mod = 1e9 + 7;
//constexpr int mod = 998244353;
using Zp = Z_p<integral_constant<decay<decltype(mod)>::type, mod>>;

template<typename T> vector<typename Z_p<T>::Type> Z_p<T>::mod_inv;
template<typename T = integral_constant<decay<decltype(mod)>::type, mod>>
void precalc_inverse(size_t SZ){
	auto &inv = Z_p<T>::mod_inv;
	if(inv.empty()) inv.assign(2, 1);
	for(; inv.size() <= SZ; ) inv.push_back((mod - 1LL * mod / int(inv.size()) * inv[mod % int(inv.size())] % mod) % mod);
}

int main(){
	cin.tie(0)->sync_with_stdio(0);
	auto __solve_tc = [&](int __tc_num){
		int n;
		cin >> n;
		vector<vector<int>> adj(n);
		for(int i = 1; i < n; ++ i){
			int u, v;
			cin >> u >> v, -- u, -- v;
			adj[u].push_back(v), adj[v].push_back(u);
		}
		int m;
		cin >> m;
		vector<int> p(m);
		for(auto i = 0; i < m; ++ i){
			cin >> p[i];
		}
		sort(p.begin(), p.end());
		vector<Zp> val(n - 1, 1);
		if(m >= n - 1){
			copy(p.begin(), p.begin() + n - 1, val.begin());
			for(auto i = n - 1; i < m; ++ i){
				val.back() *= p[i];
			}
		}
		else{
			copy(p.begin(), p.end(), val.end() - m);
		}
		vector<int> sz(n, 1);
		vector<long long> t;
		function<void(int, int)> dfs = [&](int u, int p){
			for(auto v: adj[u]){
				if(v ^ p){
					dfs(v, u);
					sz[u] += sz[v];
					t.push_back(1LL * sz[v] * (n - sz[v]));
				}
			}
		};
		dfs(0, 0);
		sort(t.begin(), t.end());
		Zp res = 0;
		for(auto i = 0; i < n - 1; ++ i){
			res += t[i] * val[i];
		}
		cout << res << "\n";
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

