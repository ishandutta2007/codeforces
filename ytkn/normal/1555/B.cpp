template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int H, W;
    int x1, y1, x2, y2;
    int h, w;
    cin >> W >> H >> x1 >> y1 >> x2 >> y2 >> w >> h;
    if(x2-x1+w > W && y2-y1+h > H) {
        cout << -1 << endl;
        return;
    }
    int ans = 1e9;
    int x_max = max(W-x2, x1), y_max = max(H-y2, y1);
    if(x2-x1+w <= W) {
        chmin(ans, max(0, w-x_max));
    }
    if(y2-y1+h <= H) {
        chmin(ans, max(0, h-y_max));
    }
    // debug_value('H')
    cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}