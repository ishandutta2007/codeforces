void linearsieve(int n, vector<int> &lpf, vector<int> &prime){
	lpf.resize(n + 1);
	prime.reserve(n + 1);
	for(int i = 2; i <= n; i ++){
		if(!lpf[i]){
			lpf[i] = i;
			prime.push_back(i);
		}
		for(int j = 0; j < prime.size() && prime[j] <= lpf[i] && i * prime[j] <= n; j ++){
			lpf[i * prime[j]] = prime[j];
		}
	}
}

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	const ll mod = 1e9 + 7;
	vi lpf, prime;
	int mx = 1e5;
	linearsieve(mx, lpf, prime);
	vl p2(mx + 1);
	p2[0] = 1;
	for(int i = 1; i <= mx; i ++){
		p2[i] = p2[i - 1] * 2 % mod;
	}
	vi cnt(mx + 1);
	for(int i = 0; i < n; i ++){
		int x;
		cin >> x;
		for(int j = 1; j * j <= x; j ++){
			if(x % j == 0){
				cnt[j] ++;
				if(j * j != x){
					cnt[x / j] ++;
				}
			}
		}
	}
	vi mobius(mx + 1);
	mobius[1] = 1;
	for(int i = 2; i <= mx; i ++){
		int t = i;
		int p = lpf[t], cnt = 0;
		while(t % p == 0){
			cnt ++;
			t /= p;
		}
		if(cnt == 1){
			mobius[i] = -mobius[t];
		}
	}
	ll res = 0;
	for(int i = 1; i <= mx; i ++){
		res += mobius[i] * (p2[cnt[i]] - 1);
	}
	cout << (res % mod + mod) % mod;
	return 0;
}
