const int mod = 1<<30;
const int MAXN = 2000;

int n, m, u, v, x, y, t, a, b, c, ans;
bool sieve[MAXN];
int prime[MAXN], cnt;

unordered_map<ll, int> dp[305];

ll f(int a, int b, int c, int t){
	if (t==-1) return 1;
	ll A=(2001ll*a+b)*2001+c;
	if (dp[t].count(A)) return dp[t][A];
	ll p=prime[t--], res=0;
	for (int x=0, aa=1; aa<=a; x++, aa*=p){
		for (int y=0, bb=1; bb<=b; y++, bb*=p){
			for (int z=0, cc=1; cc<=c; z++, cc*=p){
				res=(res+(x+y+z+1)*f(a/aa, b/bb, c/cc, t))%mod;
			}
		}
	}
	return dp[t+1][A]=res;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	for (ll i=2; i<MAXN; i++){
		if (sieve[i]) continue ;
		prime[cnt++]=i;
		for (ll j=i*i; j<MAXN; j+=i) sieve[j]=true;
	}
	//debug(cnt);
	cin>>a>>b>>c;
	cout<<f(a, b, c, cnt-1)<<endl;
	
	return 0;
}
