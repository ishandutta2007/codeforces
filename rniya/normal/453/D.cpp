/**
 * @brief XOR
 * @docs docs/convolution/XORconvolution.md
 */
template <typename T> void fwt(vector<T>& f) {
    int n = f.size();
    for (int i = 1; i < n; i <<= 1) {
        for (int j = 0; j < n; j++) {
            if (!(j & i)) {
                T x = f[j], y = f[j | i];
                f[j] = (x + y) % p;
                f[j | i] = (x - y + p) % p;
            }
        }
    }
}

