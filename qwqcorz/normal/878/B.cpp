#include<bits/stdc++.h>
#define ll long long
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
ll ans=0;
int a[N],b[N],tot[N];

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),k=read(),m=read(),cnt=0;
	for (int i=1;i<=n;i++) b[i]=read();
	for (int i=1;i<=n;i++)
	{
		a[++cnt]=b[i];
		if (a[cnt]==a[cnt-1]) tot[cnt]=tot[cnt-1]+1;
						 else tot[cnt]=1;
		if (tot[cnt]==k) cnt-=k;
	}
	int l=1,r=n=cnt;
	while ("I cannot finish it.")
	{
		if (a[l]!=a[r]||l>r) break;
		int x=l;while (x<r&&a[x+1]==a[l]) x++;
		int y=r;while (y>l&&a[y-1]==a[r]&&x-l+1+r-y+1<k) y--;
		if (x==r)
		{
			if (1LL*m*(r-l+1)%k==0) return puts("0"),0;
			ans+=1LL*m*(r-l+1)/k*k;
			break;
		}
		if (x-l+1+r-y+1==k)
		{
			ans+=1LL*(m-1)*k;
			l=x+1,r=y-1;
		}
		else break;
	}
	if (l>r) puts("0");
		else print(1LL*n*m-ans);
	
	return 0;
}
