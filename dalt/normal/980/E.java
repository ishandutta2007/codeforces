    public static void solve() {
        int n = input.nextInteger();
        int k = input.nextInteger();
        int remain = n - k;

        LCTNode[] nodes = new LCTNode[n + 1];

        for (int i = 1; i <= n; i++) {
            nodes[i] = new LCTNode();
            nodes[i].id = i;
        }

        for (int i = 1; i < n; i++) {
            LCTNode a = nodes[input.nextInteger()];
            LCTNode b = nodes[input.nextInteger()];
            LCTNode.join(a, b);
        }

        int[] remove = new int[k];
        int removeLen = k;

        LCTNode root = nodes[n];
        LCTNode.makeRoot(root);
        for (int i = n; i >= 1; i--) {
            LCTNode.access(nodes[i]);
            LCTNode.splay(root);

            if (root.notIncludeNum > remain) {
                remove[--removeLen] = i;
                continue;
            }

            remain -= root.notIncludeNum;
            root.setInclude();
        }

        for (int i = 0; i < k; i++) {
            output.print(remove[i]);
            output.print(' ');
        }
    }

