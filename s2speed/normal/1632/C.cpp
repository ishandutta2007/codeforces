ll gcd(ll a , ll b){
	if(a < b) swap(a , b);
	if(b == 0) return a;
	return gcd(b , a % b);
}

inline ll tav(ll n , ll k){
	ll res = 1;
	while(k > 0){
		if(k & 1){
			res *= n; res %= md;
		}
		n *= n; n %= md;
		k >>= 1;
	}
	return res;
}

ll cal(ll a , ll b){
	ll res = 1;
	for(ll o = 1 << 19 ; o ; o >>= 1){
		if((a & o) && !(b & o)){
			ll h = o - (b & (o - 1));
			b += h;
			res += (a | b) - b + h;
			break;
		}
	}
	return res;
}

void solve(){
	ll a , b , ans;
	cin>>a>>b; ans = b - a;
	for(ll i = a ; i < b ; i++){
		ans = min(ans , i - a + cal(i , b));
	}
	cout<<ans<<'\n';
	return;
}

