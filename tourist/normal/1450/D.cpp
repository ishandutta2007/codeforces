template <typename T, class F = function<T(const T&, const T&)>>
class SparseTable {
 public:
  int n;
  vector<vector<T>> mat;
  F func;

  SparseTable(const vector<T>& a, const F& f) : func(f) {
    n = static_cast<int>(a.size());
    int max_log = 32 - __builtin_clz(n);
    mat.resize(max_log);
    mat[0] = a;
    for (int j = 1; j < max_log; j++) {
      mat[j].resize(n - (1 << j) + 1);
      for (int i = 0; i <= n - (1 << j); i++) {
        mat[j][i] = func(mat[j - 1][i], mat[j - 1][i + (1 << (j - 1))]);
      }
    }
  }

  T get(int from, int to) const {
    assert(0 <= from && from <= to && to <= n - 1);
    int lg = 32 - __builtin_clz(to - from + 1) - 1;
    return func(mat[lg][from], mat[lg][to - (1 << lg) + 1]);
  }
};

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
      cin >> a[i];
      --a[i];
    }
    string s(n, '0');
    {
      vector<int> b = a;
      sort(b.begin(), b.end());
      s[0] = '1';
      for (int i = 0; i < n; i++) {
        if (b[i] != i) {
          s[0] = '0';
          break;
        }
      }
    }
    SparseTable<int> st(a, [&](int i, int j) { return min(i, j); });
    int L = 0;
    int R = n - 1;
    auto Test = [&](int x, int y, int z) {
      if (st.get(x, y) == z) {
        s[n - 1 - z] = '1';
      }
    };
    Test(0, n - 1, 0);
    int num = 0;
    while (L < R) {
      if (a[L] == num) {
        Test(L + 1, R, num + 1);
      }
      if (a[R] == num) {
        Test(L, R - 1, num + 1);
      }
      if (a[L] == num) {
        ++L;
        ++num;
        continue;
      }
      if (a[R] == num) {
        --R;
        ++num;
        continue;
      }
      break;
    }
    cout << s << '\n';
  }
  return 0;
}