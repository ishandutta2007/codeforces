const int N = (1 << 18) + 228;

int t[2 * N];

void build(int v, int l, int r, string &s) {
	if (r - l == 1) {
		t[v] = s[l] - '0';
		return;
	}

	int m = (l + r) / 2;
	build(v * 2 + 1, l, m, s);
	build(v * 2, m, r, s);
	t[v] = t[v * 2 + 1] + t[v * 2];
}

int get(int v, int l, int r, int L, int R) {
	if (r <= L || R <= l)
		return -1;
	if (L <= l && r <= R && t[v] == 0)
		return -1;
	// cout << l << ' ' << r << ' ' << L << ' ' << R << ' ' << t[v] << '\n';
	if (r - l == 1)
		return l;

	int m = (l + r) / 2;
	int res = get(v * 2 + 1, l, m, L, R);
	if (res == -1)
		res = get(v * 2, m, r, L, R);
	return res;
}

int t1[2 * N];

void update1(int v, int l, int r, int L, int R, int val) {
	if (r <= L || R <= l)
		return;
	if (L <= l && r <= R) {
		t1[v] = val;
		return;
	}

	int m = (l + r) / 2;
	update1(v * 2 + 1, l, m, L, R, val);
	update1(v * 2, m, r, L, R, val);
	t1[v] = min(t1[v * 2 + 1], t1[v * 2]);
}

int get1(int v, int l, int r, int L, int R) {
	if (r <= L || R <= l)
		return 1e11 + 228;
	if (L <= l && r <= R) 
		return t1[v];

	int m = (l + r) / 2;
	return min(get1(v * 2 + 1, l, m, L, R), get1(v * 2, m, r, L, R));
}

signed main() {
	setup();
	
	int n, k;
	cin >> n >> k;
	string s;
	cin >> s;
	build(1, 0, n, s);
	vector<int> dp(n + 1);
	for(int i = 0; i <= n; ++i) 
		update1(1, 0, n + 1, i, i + 1, 1e11 + 228);
	update1(1, 0, n + 1, 0, 1, 0);
	for(int i = 1; i <= n; ++i) {
		dp[i] = dp[i - 1] + i;
		int res = get(1, 0, n, max((long long) 0, i - k - 1), i);
		if (res != -1)
			dp[i] = min(dp[i], res + 1 + get1(1, 0, n + 1, max((long long) 0, res - k), i));
		update1(1, 0, n + 1, i, i + 1, dp[i]);
	}
	cout << dp[n];

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


