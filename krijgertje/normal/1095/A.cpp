const int MAXN=55;

char s[MAXN+1]; int slen;

char ans[MAXN+1];

void solve() {
	int nans=0;
	for(int i=0;i<slen;i+=nans) ans[nans++]=s[i];
	ans[nans]='\0';
}

void run() {
	scanf("%d",&slen);
	scanf("%s",s);
	solve();
	printf("%s\n",ans);
}

