long long max(long long x, long long y){return (x > y ? x : y);}
long long min(long long x, long long y){return (x < y ? x : y);}
const long long maxn = 3000001, maxb = 23, maxm = 100100;

long long an[maxn];
long long num[maxn];
long long a[maxn];

int main()
{
    //ios_base::sync_with_stdio(0);
    //cin.tie(0);
    //cout.tie(0);
    long long n, m;
    cin >> n;
    for(int i = 0; i < n; i++){scanf("%I64d", &a[i]); num[a[i]]++;}
    for(int i = 1; i < maxn; i++)
    {
        if(!num[i]){continue;}
        for(int j = i; j < maxn; j += i)
        {
            if(j / i == i)
            {
                an[j] += num[i] * (num[i] - 1);
                continue;
            }
            an[j] += num[i] * num[j / i];
        }
    }
    for(int i = 1; i < maxn; i++){an[i] += an[i - 1];}
    cin >> m;
    for(int i = 0; i < m; i++)
    {
        long long p;
        scanf("%I64d", &p);
        printf("%I64d\n", (n * (n - 1)) - an[p - 1]);
    }
    return 0;
}