#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 2e2 + 10;
int cnt[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	string str;
	cin >> str;
	string alpha;
	cin >> alpha;
	MEM(cnt, 0);
	for (auto &c : alpha) cnt[c]++;
	for (auto &c : str)
	{
		for (int i = '9'; i > c; i--)
		{
			if (cnt[i])
			{
				c = i;
				cnt[i]--;
			}
		}
	}
	cout << str << endl;
	return 0;
}