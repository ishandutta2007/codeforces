	void solve()
	{
		int n = ni(), m = ni();
		int[] a = na(n);
		int h = n/2;
		long[] l = new long[1<<h];
		for(int i = 0;i < 1<<h;i++){
			for(int j = 0;j < h;j++){
				if(i<<~j<0){
					l[i] += a[j];
				}
			}
			l[i] %= m;
		}
		l = radixSort(l);
		
		long ans = 0;
		for(int i = 0;i < 1<<n-h;i++){
			long r = 0;
			for(int j = 0;j < n-h;j++){
				if(i<<~j<0){
					r += a[h+j];
				}
			}
			r %= m;
			int ind = Arrays.binarySearch(l, m-r-1);
			if(ind < 0){
				ind = -ind-2;
			}
			if(ind < 0){
				ind = l.length - 1;
			}
			long t = (l[ind] + r) % m;
			ans = Math.max(ans, t);
		}
		out.println(ans);
	}
	
	public static long[] radixSort(long[] f){ return radixSort(f, f.length); }
	public static long[] radixSort(long[] f, int n)
	{
		long[] to = new long[n];
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(int)(f[i]&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[(int)(f[i]&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(int)(f[i]>>>16&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[(int)(f[i]>>>16&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(int)(f[i]>>>32&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[(int)(f[i]>>>32&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(int)(f[i]>>>48&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[(int)(f[i]>>>48&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		return f;
	}

	
