void _solve() {
    int n, m;
    cin >> n >> m;

    vector<vector<int>> fin(n, vector<int>(m));
    cin >> fin;
    auto a = fin;

    auto rep = [&a](int x, int y) {
        int clr = -1;
        for(int i = x; i <= x + 1; ++i) {
            for(int j = y; j <= y + 1; ++j) {
                if(a[i][j] != -1) {
                    if(clr != -1 && clr != a[i][j]) {
                        return -1;
                    }
                    clr = a[i][j];
                }
            }
        }
        return clr;
    };

    auto ok = [&rep](int x, int y) {
        return rep(x, y) != -1;
    };

    queue<pair<int, int>> q;
    set<pair<int, int>> u;
    for(int i = 0; i + 1 < n; ++i) {
        for(int j = 0; j + 1 < m; ++j) {
            if(ok(i, j)) {
                q.push({i, j});
                u.insert({i, j});
            }
        }
    }

    vector<pair<pair<int, int>, int>> ops;
    while(!q.empty()) {
        int x = q.front().x;
        int y = q.front().y;
        q.pop();

        if(!ok(x, y)) {
            continue;
        }
        ops.push_back({{x, y}, rep(x, y)});
        //cout << x + 1 << " " << y + 1 << " " << rep(x, y) << endl;

        for(int i = x; i <= x + 1; ++i) {
            for(int j = y; j <= y + 1; ++j) {
                a[i][j] = -1;
            }
        }
        //for(auto &i : a) {
        //    cout << i << endl;
        //}
        //cout << endl;

        for(int i = x - 1; i <= x + 1; ++i) {
            for(int j = y - 1; j <= y + 1; ++j) {
                if(!u.count({i, j}) && 0 <= min(i, j) && i + 1 < n && j + 1 < m && ok(i, j)) {
                    u.insert({i, j});
                    q.push({i, j});
                }
            }
        }
    }

    for(auto &i : a) {
        for(auto &j : i) {
            if(j != -1) {
                cout << "-1" << endl;
                return;
            }
        }
    }

    reverse(all(ops));
    cout << sz(ops) << endl;
    for(auto &i : ops) {
        cout << i.x.x + 1 << " " << i.x.y + 1 << " " << i.y << endl;
    }
}

void _main_function() {
    int t = 1;
    //cin >> t;

    for(int i = 0; i < t; ++i) {
        _solve();
    }
}

