void solve(){
    string s;cin>>s;
    int a=0,b=0;
    for(auto i:s){
        if(i=='0'){
            a++;
        }else{
            b++;
        }
    }
    int c=0,d=0;
    int ans = s.size();
    for(int i=0;i<s.size();i++){
        ans=min(ans,c+b-d);
        ans=min(ans,d+a-c);
        if(s[i]=='0'){
            c++;
        }else{
            d++;
        }
        ans = min(ans, c + b - d);
        ans = min(ans, d + a - c);
    }
    cout<<ans<<"\n";
}
