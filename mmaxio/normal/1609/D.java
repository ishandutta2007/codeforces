	int get(int v, int[] p) {
		return p[v] == v ? v : (p[v] = get(p[v], p));
	}

	void submit() {
		int n = nextInt();
		int d = nextInt();
		int[] p = new int[n];
		int[] sz = new int[n];
		for (int i = 0; i < n; i++) {
			p[i] = i;
			sz[i] = 1;
		}
		
		int comps = n;
		int free = 1;
		
		while (d-- > 0) {
			int v = nextInt() - 1;
			int u = nextInt() - 1;
			
			v = get(v, p);
			u = get(u, p);
			
			if (p[v] != p[u]) {
				p[v] = u;
				sz[u] += sz[v];
				comps--;
			} else {
				free++;
			}
			
			int[] a = new int[n];
			for (int i = 0, j = 0; i < n; i++) {
				if (p[i] == i) {
					a[j++] = sz[i];
				}
			}
			Arrays.sort(a, 0, comps);
			int ret = 0;
			for (int i = comps - 1; i >= comps - free; i--) {
				ret += a[i];
			}
			out.println(ret - 1);
		}
		
	}

