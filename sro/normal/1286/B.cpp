void dfs1(int now){
	sz[now]=1;
	foreach(it,nei[now]){
		dfs1(*it);
		foreach(j,chi[*it]){
			chi[now].pub(*j);
		}
		chi[now].pub(*it);
		sz[now]+=sz[*it];
	}
}

void dfs2(int now,int l,int r){
	if(cnt[now]>=sz[now]){
		puts("NO");
		exit(0);
	}
	int cur=l;
	foreach(i,nei[now]){
		int cus=cur+sz[*i]-1;
		dfs2(*i,cur,cus);
		cur=cus+1;
	}
	int v=l+cnt[now];
	foreach(i,chi[now]){
		if(val[*i]>=v){
			val[*i]++;
		}
	}
	val[now]=v;
}

