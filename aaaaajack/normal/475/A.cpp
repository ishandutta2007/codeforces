#include<cstdio>
using namespace std;
int main(){
	int k,i;
	char s[20][3]={};
	scanf("%d",&k);
	char th=(k>=3)?'O':'#';
	if(k>=3) k--;
	for(i=0;i<33;i++){
		if(i<k) s[i/3][i%3]='O';
		else s[i/3][i%3]='#';
	}
	printf("+------------------------+\n");
	printf("|%c.%c.%c.%c.%c.%c.%c.%c.%c.%c.%c.|D|)\n",s[0][0],s[1][0],s[2][0],s[3][0],s[4][0],s[5][0],s[6][0],s[7][0],s[8][0],s[9][0],s[10][0]);
	printf("|%c.%c.%c.%c.%c.%c.%c.%c.%c.%c.%c.|.|\n",s[0][1],s[1][1],s[2][1],s[3][1],s[4][1],s[5][1],s[6][1],s[7][1],s[8][1],s[9][1],s[10][1]);
	printf("|%c.......................|\n",th);
	printf("|%c.%c.%c.%c.%c.%c.%c.%c.%c.%c.%c.|.|)\n",s[0][2],s[1][2],s[2][2],s[3][2],s[4][2],s[5][2],s[6][2],s[7][2],s[8][2],s[9][2],s[10][2]);
	printf("+------------------------+\n");
	return 0;
}