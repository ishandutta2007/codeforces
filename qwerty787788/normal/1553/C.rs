pub fn main() {
    let stdout = io::stdout();
    let mut out = std::io::BufWriter::new(stdout.lock());
    let mut sc = Scanner::new();

    let tc = sc.usize();
    for _ in 0..tc {
        let s = sc.string();
        let mut res = 10;
        for first_win in [false, true].iter() {
            let mut cur_first = 0;
            let mut cur_second = 0;
            let mut more_first = 5;
            let mut more_second = 5;
            for cnt in 0..10 {
                let go_first = cnt % 2 == 0;
                let score = if s[cnt] == b'?' {
                    if *first_win == go_first {
                        true
                    } else {
                        false
                    }
                } else {
                    s[cnt] == b'1'
                };
                let score = score as i32;
                if go_first {
                    cur_first += score;
                    more_first -= 1;
                } else {
                    cur_second += score;
                    more_second -= 1;
                }
                if cur_first > cur_second + more_second {
                    res = min(res, cnt + 1);
                }
                if cur_second > cur_first + more_first {
                    res = min(res, cnt + 1);
                }
            }
        }
        writeln!(out, "{}", res).unwrap();
    }
}