template <class A, class B>
bool smin(A &x, B &&y) {
  if (y < x) {
    x = y;
    return true;
  }
  return false;
}

template <class A, class B>
bool smax(A &x, B &&y) {
  if (x < y) {
    x = y;
    return true;
  }
  return false;
}

void solveTest() {
  int n, h, m;
  cin >> n >> h >> m;
  int x = h * 60 + m;
  int ans = 100000;
  while (n--) {
    int hh, mm;
    cin >> hh >> mm;
    int y = hh * 60 + mm;
    int dist = y - x;
    if (dist < 0) {
      dist += 24 * 60;
    }
    smin(ans, dist);
  }
  cout << ans / 60 << ' ' << ans % 60 << '\n';
}

