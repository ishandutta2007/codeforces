#include<cstdio>
#define LL long long
int n,t;
LL dp[22][12][2][4];
int main()
{
	scanf("%d%d",&n,&t),dp[2][0][0][1]=1,dp[2][0][0][2]=2,dp[2][0][0][3]=3;
	for(int i=2;i<n;++i)
		for(int j=0;j<t;++j)
		{
			dp[i+1][j][0][1]+=dp[i][j][0][0];
			dp[i+1][j][0][2]+=dp[i][j][0][0]+dp[i][j][0][1];
			dp[i+1][j][0][3]+=dp[i][j][0][0]+dp[i][j][0][1]+dp[i][j][0][2];
			dp[i+1][j][1][0]+=dp[i][j][0][1]+dp[i][j][0][2]+dp[i][j][0][3];
			dp[i+1][j][1][1]+=dp[i][j][0][2]+dp[i][j][0][3];
			dp[i+1][j][1][2]+=dp[i][j][0][3];
			dp[i+1][j+1][0][1]+=dp[i][j][1][0];
			dp[i+1][j+1][0][2]+=dp[i][j][1][0]+dp[i][j][1][1];
			dp[i+1][j+1][0][3]+=dp[i][j][1][0]+dp[i][j][1][1]+dp[i][j][1][2];
			dp[i+1][j][1][0]+=dp[i][j][1][1]+dp[i][j][1][2]+dp[i][j][1][3];
			dp[i+1][j][1][1]+=dp[i][j][1][2]+dp[i][j][1][3];
			dp[i+1][j][1][2]+=dp[i][j][1][3];
		}
	printf("%lld",dp[n][t-1][1][0]+dp[n][t-1][1][1]+dp[n][t-1][1][2]+dp[n][t-1][1][3]);
	return 0;
}