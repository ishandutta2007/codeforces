bool cmp(node a,node b)
{
	if (pos[a.l]^pos[b.l]) return a.l<b.l;
	if(pos[a.l]&1)return a.r<b.r;
	return a.r>b.r;
}
void del(int x)
{
	if (e[a[x]]==1) fre[P[a[x]]]--;
	if (e[a[x]]==2) fre[P[a[x]]]++;
	e[a[x]]--;
}
void ins(int x)
{
    if (e[a[x]]==0) fre[P[a[x]]]++;
    if (e[a[x]]==1) fre[P[a[x]]]--;
	e[a[x]]++;
 } 
