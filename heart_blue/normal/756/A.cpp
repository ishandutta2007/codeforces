#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 2e5 + 10;

class UnionSet
{
public:
	int fa[N];
	void init()
	{
		MEM(fa, -1);
	}
	int treesize(int x)
	{
		return -fa[Find(x)];
	}
	int Find(int x)
	{
		int root = x;
		while (fa[root] >= 0) root = fa[root];
		while (x != root)
		{
			int tmp = fa[x];
			fa[x] = root;
			x = tmp;
		}
		return root;
	}
	void Union(int x, int y)
	{
		int px = Find(x);
		int py = Find(y);
		if (fa[px] > fa[py]) swap(px, py);
		int sz = fa[px] + fa[py];
		fa[py] = px;
		fa[px] = sz;
	}
} uf;
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	uf.init();
	int n;
	cin >> n;
	int cnt = n;
	for (int i = 1; i <= n; i++)
	{
		int x;
		cin >> x;
		if (uf.Find(i) != uf.Find(x))
		{
			uf.Union(x, i);
			cnt--;
		}
	}
	int ans = cnt - 1;
	if (ans) ans++;
	int flag = 0;
	for (int i = 1; i <= n; i++)
	{
		int x;
		cin >> x;
		flag ^= x;
	}
	ans += flag ^ 1;
	cout << ans << endl;

	return 0;
}