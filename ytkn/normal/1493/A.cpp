using namespace std;
typedef long long ll;

void solve(){
    int n, k; cin >> n >> k;
    vector<int> ans;
    for(int i = 1; i <= n; i++){
        if(2*i >= k && i != k) ans.push_back(i);
    }
    cout << ans.size() << endl;
    for(int x : ans) cout << x << ' ';
    cout << endl;
}

