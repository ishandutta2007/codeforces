const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 300010;

ll ps[MAXN];

ll n, m, k, u, v, x, y, t, a, b;
ll ans[MAXN];
int h[MAXN];
vector<int> G[MAXN];
vector<pii> Q[MAXN];

void dfs(int node, int par){
	h[node]=h[par]+1;
	for (pii p:Q[node]){
		int d=p.first, x=p.second;
		ans[node]+=x;
		ps[min(h[node]+d+1, MAXN-1)]-=x;
	}
	ans[node]+=ans[par]+ps[h[node]];
	for (int i:G[node]){
		if (i==par) continue ;
		dfs(i, node);
	}
	for (pii p:Q[node]){
		int d=p.first, x=p.second;
		ps[min(h[node]+d+1, MAXN-1)]+=x;
	}
}

