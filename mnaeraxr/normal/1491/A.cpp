namespace asl
{
    template <typename T>
    using vec = std::vector<T>;

    template <typename T>
    std::istream &operator>>(std::istream &is, std::vector<T> &vec)
    {
        for (auto &value : vec)
            is >> value;
        return is;
    }

} 

#include <experimental/optional>

#include <utility>

#include <tuple>

#include <random>

#define endl '\n'

using namespace std;
using namespace asl;

void solve()
{
    int n, q;
    cin >> n >> q;
    vec<int> a(n);
    cin >> a;
    int one = 0;
    for (auto u : a)
        one += u;

    while (q--)
    {
        int m, p;
        cin >> m >> p;
        if (m == 1)
        {
            p--;
            if (a[p])
            {
                one--;
            }
            else
            {
                one++;
            }
            a[p] ^= 1;
        }
        else
        {
            cout << (one >= p) << endl;
        }
    }
}

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int t = 1;

    for (int i = 1; i <= t; ++i)
    {
        solve();
    }

    return 0;
}