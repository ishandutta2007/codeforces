#include<bits/stdc++.h>
using namespace std;
const int N=1e3+5;
const int M=2520;
const int V=N*M;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
bool vis[V],is[V],used[N];
int k[N],m[N],t[N][10],id[N][M],to[V],ans[V];
int st[V],top=0;

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),cntv=0;
	for (int i=1;i<=n;i++) k[i]=(read()%M+M)%M;
	for (int i=1;i<=n;i++)
	{
		m[i]=read();
		for (int j=0;j<m[i];j++) t[i][j]=read();
	}
	for (int i=1;i<=n;i++)
	for (int j=0;j<M;j++) id[i][j]=cntv++;
	#define out t[i][j%m[i]]
	for (int i=1;i<=n;i++)
	for (int j=0;j<M;j++)
	to[id[i][j]]=id[out][(j+k[out])%M];
	#undef out
	for (int i=0;i<cntv;i++)
	{
		int now=i;
		while (!vis[now])
		{
			vis[now]=1;
			st[++top]=now;
			now=to[now];
		}
		if (is[now]) while (top) is[st[top]]=1,ans[st[top--]]=ans[now];
		else
		{
			int tmp=top,answer=0;
			while (st[tmp]!=now)
			{
				answer+=!used[st[tmp]/M];
				used[st[tmp--]/M]=1;
			}
			answer+=!used[now/M];
			while (top) used[st[top]/M]=0,is[st[top]]=1,ans[st[top--]]=answer;
		}
	}
	int q=read();
	while (q--)
	{
		int x=read(),y=(read()%M+M+k[x])%M;
		print(ans[id[x][y]]);
	}
	
	return 0;
}
