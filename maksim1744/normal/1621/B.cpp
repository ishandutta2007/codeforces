void test_case(int test) {
    int n;
    cin >> n;
    map<int, int> left;
    map<int, int> right;
    map<int, int> len;
    for (int i = 0; i < n; ++i) {
        int l, r, c;
        cin >> l >> r >> c;

        if (left.count(l)) left[l] = min(left[l], c);
        else left[l] = c;
        if (right.count(r)) right[r] = min(right[r], c);
        else right[r] = c;
        if (len.count(r - l)) len[r - l] = min(len[r - l], c);
        else len[r - l] = c;

        int L = left.begin()->first;
        int R = (--right.end())->first;

        int maxlen = (--len.end())->first;
        int cost = left[L] + right[R];
        if (R - L == maxlen)
            cost = min(cost, len[maxlen]);
        cout << cost << '\n';
    }
}

