const int Mod = 1000000007;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}



struct node {
    int dp[32];
    //0 - a
    //1 - b
    //2 - c
    //3 - ab
    //4 - bc
    node(){
        for (int i = 0; i < 32; i++) {
            dp[i] = 1e9;
        }
    }
    node(char c) {
        for (int i = 0; i < 32; i++) {
            dp[i] = 1e9;
        }
        for (char a = 'a'; a <= 'c'; a++) {
            dp[1 << (a - 'a')] = a != c;
        }
    }
};

int comp[32][32];


node operator +(const node &a, const node &b) {
    node c;
    int st[33];
    int uk =0 ;
    for (int k1 = 0; k1 < 32; k1++) {
        if (b.dp[k1] < 1e8) {
            st[uk] = k1;
            uk++;
        }
    }
    for (int k = 0; k < 32; k++) {
        if (a.dp[k] < 1e8) {
            for (int i = 0; i < uk; i++) {
                int k1 = st[i];
                if (comp[k][k1] != -1) {
                    chkmin(c.dp[comp[k][k1]], a.dp[k] + b.dp[k1]);
                }
            }
        }
    }
    return c;
}



const int MAXN = 100228;

int n, q;
string f[6] = {"a", "b", "c", "ab", "bc", "abc"};
node d[MAXN * 4];
int ss = 1;


void solve() {
    for (int k = 0; k < 32; k++) {
        for (int k1 = 0; k1 < 32; k1++) {
            bool good[6];
            memset(good, 0, sizeof(good));
            for (int j = 0; j < 5; j++) {
                if (k & (1 << j)) {
                    good[j] = true;
                }
            }
            for (int j = 0; j < 5; j++) {
                if (k1 & (1 << j)) {
                    good[j] = true;
                }
            }
            for (int j = 0; j < 5; j++) {
                if (k & (1 << j)) {
                    for (int j1 = 0; j1 < 5; j1++) {
                        if (k1 & (1 << j1)) {
                            string g = f[j] + f[j1];
                            for (int t = 0; t < 6; t++) {
                                int uk = 0;
                                for (auto x: g) {
                                    if (uk < sz(f[t]) && f[t][uk] == x) {
                                        uk++;
                                    }
                                }  
                                if (uk == sz(f[t])) {
                                    good[t] = true;
                                }
                            }
                        }
                    }
                }
            }
            int res = 0;
            for (int i = 0; i < 5; i++) {
                if (good[i]) {
                    res += 1 << i;
                }
            }
            comp[k][k1] = res;
            if (good[5]) {
                comp[k][k1] = -1;
            }
        }
    }
    cin >> n >> q;
    string s;
    cin >> s;
    while (ss < n) {
        ss <<= 1;
    }
    for (int i = ss; i < ss + n; i++) {
        d[i] = node(s[i - ss]);
    }
    for (int i = ss + n; i < 2 * ss; i++) {
        d[i] = node();
        d[i].dp[0] = 0;
    }
    for (int i = ss - 1; i >= 1; i--) {
        d[i] = d[i * 2] + d[i * 2 + 1];
    }
    for (int it = 0; it < q; it++) {
        int pos;
        char c;
        cin >> pos >> c;
        pos--;
        d[ss + pos] = node(c);
        int i = ss + pos;
        while (i >> 1 > 0) {
            i >>= 1;
            d[i] = d[i * 2] + d[i * 2 + 1];
        }
        int ans = 1e9;
        for (int k = 0; k < 32; k++) {
            chkmin(ans, d[1].dp[k]);
        }
        cout << ans << '\n';
    }
}


