LL a;
string s;
void solve() {
    string res;
    while(a){
        if(SZ(s)==0){
            W(-1);
            return;
        }
        int x=a%10;
        a/=10;
        int y=s.back()-'0'-x;
        if(y<0)y+=10;
        res.PB(y+'0');
        s.pop_back();
        if(x+y>=10&&(SZ(s)==0||s.back()!='1')){
            W(-1);
            return;
        }
        if(x+y>=10)s.pop_back();
    }
    while(SZ(s)){
        res.PB(s.back());
        s.pop_back();
    }
    while(SZ(res)>1&&res.back()=='0')res.pop_back();
    reverse(ALL(res));
    W(res);
}
