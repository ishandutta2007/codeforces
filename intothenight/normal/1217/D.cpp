ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vector<pii> edge(m);
	vector<vi> adj(n);
	for(int i = 0; i < m; i ++){
		cin >> edge[i], edge[i].first --, edge[i].second --;
		adj[edge[i].first].push_back(edge[i].second);
	}
	vi visited(n), cur(n);
	function<bool(int)> iscycle = [&](int u){
		visited[u] = true;
		cur[u] = true;
		for(auto v: adj[u]){
			if(cur[v]){
				return true;
			}
			if(!visited[v] && iscycle(v)){
				return true;
			}
		}
		cur[u] = false;
		return false;
	};
	for(int u = 0; u < n; u ++){
		if(!visited[u] && iscycle(u)){
			cout << "2\n";
			for(auto e: edge){
				cout << (e.first > e.second) + 1 << " ";
			}
			return 0;
		}
	}
	cout << "1\n" << vi(m, 1);
	return 0;
}

