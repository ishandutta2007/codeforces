	for (ll i=1; i*i<=n; i++){
		if (n%i) continue ;
		vec.pb(i);
		if (i*i!=n) vec.pb(n/i);
	}
	sort(all(vec));
	for (int i=0; i<vec.size(); i++) for (int j=i; j<vec.size(); j++) if (vec[j]%vec[i]==0) D[vec[j]].pb(vec[i]);
	
	dfs(n, k);
	
	return 0;
