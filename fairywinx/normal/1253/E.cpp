const int M = 1e5 + 228;
const int N = 85;
const int INF = 1e9 + 228;

int dp[M][N];

int dist(int pos, pair<int, int> a) {
	if (pos >= a.first && pos <= a.second)
		return 0;
	return min(abs(pos - a.first), abs(pos - a.second));
}

signed main() {
	setup();
	for(int i = 1; i < M; ++i)
		fill(dp[i], dp[i] + N, INF);
	fill(dp[0], dp[0] + N, 0);
	
	int n, m;
	cin >> n >> m;
	vector<pair<int, int>> a(n);
	for(int i = 0; i < n; ++i) {
		int b, c;
		cin >> b >> c;
		a[i].first = max(0, b - c);
		a[i].second = min(m, b + c);
	}
	sort(a.begin(), a.end());
	vector<int> mx(m + 1);
	mx[0] = 0;
	// cout << a[0].first << '\n';
	for(int i = 1; i <= m; ++i) {
		for(int j = 0; j < n; ++j) {
			
			int c = dist(i, a[j]);
			dp[i][j] = mx[max(0, a[j].first - c - 1)] + c;
			dp[i][j] = min(dp[i][j], max(a[j].first - 1, dist(i, a[j])));

		}
		mx[i] = dp[i][0];
		for(int j = 1; j < n; ++j)
			mx[i] = min(mx[i], dp[i][j]);
	}
	cout << mx[m] << '\n';
	

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


