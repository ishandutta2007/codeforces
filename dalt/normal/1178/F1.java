    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        int n;
        int m;
        Segment root;
        int[] colors;
        int[] prev;
        int[] last;
        int[] first;

        Modular mod = new Modular(998244353);

        public void solve() {
            n = io.readInt();
            m = io.readInt();
            colors = new int[m + 1];

            for (int i = 1; i <= m; i++) {
                colors[i] = io.readInt();
            }

            prev = new int[m + 1];
            first = new int[n + 1];
            last = new int[n + 1];
            for (int i = 1; i <= m; i++) {
                prev[i] = last[colors[i]];
                last[colors[i]] = i;
                if (first[colors[i]] == 0) {
                    first[colors[i]] = i;
                }
            }

            root = new Segment(1, m, colors);
            Node tree = build(1, m);

            if (invalid) {
                io.cache.append(0);
                return;
            }

            dfs(tree);

            io.cache.append(way);
        }


        boolean invalid;

        public Node build(int l, int r) {
            if (l > r) {
                return Node.NIL;
            }

            int minColor = root.query(l, r, 1, m);
            if (first[minColor] < l || last[minColor] > r) {
                invalid = true;
                return Node.NIL;
            }

            Node node = new Node();
            node.l = build(l, first[minColor] - 1);
            node.r = build(last[minColor] + 1, r);

            int x = last[minColor];
            while (prev[x] != 0) {
                node.next.add(build(prev[x] + 1, x - 1));
                x = prev[x];
            }
            return node;
        }

        int way = 1;

        public void dfs(Node root) {
            dfs0(root);
            way = mod.mul(way, root.dp[0]);
        }


        public void dfs0(Node root) {
            if (root == Node.NIL) {
                return;
            }

            for (Node node : root.next) {
                dfs(node);
            }
            dfs0(root.l);
            dfs0(root.r);


            for (int i = 0; i <= 500; i++) {
                for (int j = 0; j + i + 1 <= 500; j++) {
                    root.dp[i + j + 1] = mod.plus(root.dp[i + j + 1], mod.mul(root.l.dp[i],
                            root.r.dp[j]));
                }
            }

            for (int i = 500 - 1; i >= 0; i--) {
                root.dp[i] = mod.plus(root.dp[i], root.dp[i + 1]);
            }
        }
    }

