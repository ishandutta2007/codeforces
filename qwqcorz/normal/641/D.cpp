#include<bits/stdc++.h>
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
double p1[N],p2[N],a[N],b[N];

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read();
	for (int i=1;i<=n;i++) scanf("%lf",&p1[i]);
	for (int i=1;i<=n;i++) scanf("%lf",&p2[i]);
	for (int i=1;i<=n;i++) p1[i]+=p1[i-1];
	for (int i=n;i>=1;i--) p2[i]+=p2[i+1];
	for (int i=1;i<=n;i++)
	{
		double sum=p1[i]-p2[i+1]+1,mul=p1[i],dec=sqrt(max(sum*sum-4*mul,0.0));
		a[i]=(sum-dec)/2,b[i]=(sum+dec)/2;
	}
	for (int i=1;i<=n;i++) printf("%.10lf ",a[i]-a[i-1]);putchar('\n');
	for (int i=1;i<=n;i++) printf("%.10lf ",b[i]-b[i-1]);putchar('\n');
	
	return 0;
}
