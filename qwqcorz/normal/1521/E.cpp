#include<bits/stdc++.h>
using namespace std;
const int N=505;
const int M=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct color
{
	int col,tim;
	bool operator <(const color &a)const
	{
		return tim>a.tim;
	}
}c[M];
int a[N][N];
vector<int>tmp;

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		tmp.clear();
		int m=read(),k=read();
		for (int i=1;i<=k;i++) c[i]=(color){i,read()};
		sort(c+1,c+1+k);
		for (int i=1;i<=k;i++)
		for (int j=1;j<=c[i].tim;j++) tmp.push_back(c[i].col);
		int l=1,r=500;
		while (l<=r)
		{
			int mid=(l+r)/2;
			if (mid*mid-(mid/2)*(mid/2)>=m&&(mid+1)/2*mid>=c[1].tim) r=mid-1;
			else l=mid+1;
		}
		int n=l,now=0;
		print(n);
		for (int i=1;i<=n;i++)
		for (int j=1;j<=n;j++) a[i][j]=0;
		for (int i=1;i<=n;i++)
		for (int j=1;j<=n;j++)
		if (i%2==0&&j%2==1&&now<(int)tmp.size()) a[i][j]=tmp[now++];
		for (int i=1;i<=n;i++)
		for (int j=1;j<=n;j++)
		if (i%2==1&&j%2==1&&now<(int)tmp.size()) a[i][j]=tmp[now++];
		for (int i=1;i<=n;i++)
		for (int j=1;j<=n;j++)
		if (i%2==1&&j%2==0&&now<(int)tmp.size()) a[i][j]=tmp[now++];
		for (int i=1;i<=n;i++,putchar('\n'))
		for (int j=1;j<=n;j++) print(a[i][j],' ');
	}
	
	return 0;
}
