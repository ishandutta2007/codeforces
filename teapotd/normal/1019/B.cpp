int n;

void answer(int x) {
	cout << "! " << x+1 << endl;
	exit(0);
}

int ask(int x) {
	cout << "? " << x+1 << endl;
	int y; cin >> y;
	return y;
}

int f(int x) {
	return ask(x) - ask((x+n/2) % n);
}

void run() {
	cin >> n;

	int begin = 0, end = n/2;

	int x = f(begin);
	if (x == 0) answer(x);
	bool sign = (x > 0);

	while (begin+1 < end) {
		int mid = (begin+end) / 2;
		int ans = f(mid);
		if (ans == 0) answer(mid);

		bool s = (ans > 0);

		if (s == sign) {
			begin = mid+1;
		} else {
			end = mid;
		}
	}

	if (f(begin) == 0) {
		answer(begin);
	} else {
		answer(-2);
	}
}