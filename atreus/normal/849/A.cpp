#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
        cin >> a[i];
    if (n % 2 == 0)
        return cout << "No", 0;
    else{
        if (a[0] % 2 == 0 || a[n - 1] % 2 == 0)
            return cout << "No", 0;
    }
    cout << "Yes";
}