const int maxn = 25;
int n;
int a[maxn];

int read() {
  if (scanf("%d", &n) < 1) {
    return false;
  }
  for (int i = 0; i < n; i++) {
    scanf("%d", &a[i]);
  }
  return true;
}

int ans[maxn];
pair<int, int> tosort[maxn];

void solve() {
  for (int i = 0; i < n; i++) {
    tosort[i] = make_pair(a[i], i);
  }
  sort(tosort, tosort + n);
  for (int i = 0; i < n; i++) {
    int x = tosort[i].second;
    ans[x] = tosort[(i + 1) % n].first;
  }
  for (int i = 0; i < n; i++) {
    printf("%d ", ans[i]);
  }
  printf("\n");
}

int main() {
  precalc();
#ifdef DEBUG
  assert(freopen(TASK ".in", "r", stdin));
  assert(freopen(TASK ".out", "w", stdout));
#endif
  while (read()) {
    solve();
#ifdef DEBUG
    eprintf("Time %.2f\n", (double) clock() / CLOCKS_PER_SEC);
#endif
  }
  return 0;
}