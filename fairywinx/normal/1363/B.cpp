void solve() {
	string s;
	cin >> s;
	int n = s.size();
	vector<pair<int, int>> kol1(n + 2, make_pair(0, 0));
	vector<pair<int, int>> kol2(n + 2, make_pair(0, 0));
	for(int i = 0; i < n; ++i) {
		if (s[i] == '0')
			++kol2[i + 1].first;
		else
			++kol1[i + 1].first;
		kol2[i + 1].first += kol2[i].first;
		kol1[i + 1].first += kol1[i].first;
	}
	for(int i = n - 1; i >= 0; --i) {
		if (s[i] == '0')
			++kol2[i].second;
		else
			++kol1[i].second;
		kol2[i].second += kol2[i + 1].second;
		kol1[i].second += kol1[i + 1].second;
	}

	int ans = 1e9 + 228;
	for(int i = 0; i < n; ++i) {
		ans = min(ans, kol1[i + 1].second + kol2[i].first);
		ans = min(ans, kol2[i + 1].second + kol1[i].first);
	}
	cout << ans << '\n';
}

