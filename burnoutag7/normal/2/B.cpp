#include<bits/stdc++.h>
using namespace std;

int f[1010][1010],pr[1010][1010];
int r[1010][1010][2];
int ci,cj,ans,kp,n,i,j,a,x,q;
int p[2010];

int main(){

    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin>>n;
    for(i=1;i<=n;i++){
        for(j=1;j<=n;j++){
            cin>>a;
            if(a==0){
                r[i][j][0]=1e6;
                r[i][j][1]=1e6;
                ci=i;
                cj=j;
            }else{
                x=a;
                while((x&1)==0){
                    r[i][j][0]++;
                    x>>=1;
                }
                x=a;
                while(x%5==0){
                    r[i][j][1]++;
                    x/=5;
                }
            }
        }
    }
    ans=1e9;kp=0;
    for(q=0;q<2;q++){
        memset(f,0,sizeof(f));
        memset(pr,0,sizeof(pr));
        f[1][1]=r[1][1][q];
        for(i=1;i<=n;i++){
            for(j=1;j<=n;j++){
                if(i+j==2)continue;else
                if(i==1){
                    f[i][j]=f[i][j-1]+r[i][j][q];
                    pr[i][j]=2;
                }else
                if(j==1){
                    f[i][j]=f[i-1][j]+r[i][j][q];
                    pr[i][j]=1;
                }else
                if(f[i-1][j]<f[i][j-1]){
                    f[i][j]=f[i-1][j]+r[i][j][q];
                    pr[i][j]=1;
                }else{
                    f[i][j]=f[i][j-1]+r[i][j][q];
                    pr[i][j]=2;
                }
            }
        }
        if(f[n][n]<ans){
            ans=f[n][n];
            kp=0;i=n;j=n;
            while(i+j>2){
                kp++;
                p[kp]=pr[i][j];
                if(p[kp]==1)i--;
                else j--;
            }
        }
    }
    if(ans>1 && ci>0){
        cout<<1<<endl;
        for(i=1;i<ci;i++)cout<<'D';
        for(j=1;j<cj;j++)cout<<'R';
        for(i=ci;i<n;i++)cout<<'D';
        for(j=cj;j<n;j++)cout<<'R';
        cout<<endl;
    }else{
        cout<<ans<<endl;
        for(i=kp;i>=1;i--){
            if(p[i]==1)cout<<'D';
            else cout<<'R';
        }
        cout<<endl;
    }

    return 0;
}