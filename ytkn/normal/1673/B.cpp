template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    string s; cin >> s;
    vector<bool> used(26);
    for(char c: s) {
        used[c-'a'] = true;
    }
    int cnt = 0;
    for(int i = 0; i < 26; i++){
        if(used[i]) cnt++;
    }
    int n = s.size();
    string t = s.substr(0, cnt);
    for(int i = 0; i < n; i+=cnt){
        if(i+cnt <= n){
            string u = s.substr(i, cnt);
            if(t != u){
                cout << "NO" << endl;
                return;
            }
        }else{
            int len = n-i;
            if(s.substr(0, len) != s.substr(i, len)){
                cout << "NO" << endl;
                return;
            }
        }
    }
    cout << "YES" << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}