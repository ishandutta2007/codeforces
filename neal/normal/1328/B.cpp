void run_case() {
    long long N, K;
    cin >> N >> K;

    for (int first = N - 2; first >= 0; first--) {
        int here = N - 1 - first;

        if (here >= K) {
            int second = N - K;
            string ans(N, 'a');
            ans[first] = ans[second] = 'b';
            cout << ans << '\n';
            return;
        }

        K -= here;
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}