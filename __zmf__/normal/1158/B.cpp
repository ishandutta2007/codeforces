#include <bits/stdc++.h>
using namespace std;
#define int long long
inline int read()
{
	int sum = 0, nega = 1;
	char ch = getchar();
	while (ch > '9'||ch < '0')
	{
	    if (ch == '-') nega = -1;
		ch = getchar();
	}
	while (ch <= '9'&&ch >= '0') sum = sum * 10 + ch - '0', ch = getchar ();
	return sum * nega;
}
int n, l, k;
signed main()
{
	n = read(), k = read();
 	l = (n - k) / 2 + 1;
 	for (int i = 1; i <= n; i++)
 		if(i % l > 0) putchar('1');
 		else putchar('0');
	return 0;
}