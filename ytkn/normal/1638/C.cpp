template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int n; cin >> n;
    vector<int> p(n);
    for(int i = 0; i < n; i++) {
        cin >> p[i]; p[i]--;
    }
    vector<int> mx(n+1, -1);
    vector<int> mn(n+1, n+1);
    int ans = 0;
    for(int i = 0; i < n; i++) mx[i+1] = max(mx[i], p[i]);
    for(int i = n-1; i >= 0; i--) mn[i] = min(mn[i+1], p[i]);
    for(int i = 0; i < n; i++){
        if(mx[i] > p[i]) continue;
        if(mx[i] < mn[i+1]) {
            // cout << mx[i] << ' ' << mn[i+1] << endl;
            ans++;
        }
    }
    cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}