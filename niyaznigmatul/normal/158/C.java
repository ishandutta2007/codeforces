class TaskC {
	public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int n = in.nextInt();
        Stack<String> stack = new Stack<String>();
        String current = "/";
        for (int i = 0; i < n; i++) {
            String command = in.next();
            if (command.equals("cd")) {
                String arg = in.next();
                if (arg.startsWith("/")) {
                    stack.clear();
                    arg = arg.substring(1);
                }
                String[] t = arg.split("/");
                for (String e : t) {
                    if (e.equals("..")) {
                        stack.pop();
                    } else {
                        stack.add(e);
                    }
                }
                StringBuilder sb = new StringBuilder("/");
                for (String e : stack) {
                    sb.append(e).append("/");
                }
                current = sb.toString();
            } else {
                out.println(current);
            }
        }
	}
}

class FastScanner extends BufferedReader {

    boolean isEOF;

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
            if (isEOF && ret < 0) {
                throw new InputMismatchException();
            }
            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    public String next() {
        StringBuilder sb = new StringBuilder();
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        while (!isWhiteSpace(c)) {
            sb.appendCodePoint(c);
            c = read();
        }
        return sb.toString();
    }

    static boolean isWhiteSpace(int c) {
        return c >= -1 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (!isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    }

