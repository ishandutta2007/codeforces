void solve() {
  int a, b;
  cin >> a >> b;
  if (a < b) swap(a, b);
  int d = a - b;
  cout << d << ' ';
  if (d == 0) {
    cout << "0\n";
  } else {
    cout << min({a % d, d - a % d, b % d, d - b % d}) << '\n';
  }
}

