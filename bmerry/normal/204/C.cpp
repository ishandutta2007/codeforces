int main(int argc, const char **argv)
{
    redirect(argc, argv);

    ll N;
    string a, b;
    cin >> N >> a >> b;
    double sums[2][26] = {{}};
    double scale = 0.0;
    for (ll i = 1; i <= N; i++)
    {
        scale += (N - i + 1) * (N - i + 1);
    }

    double score = 0.0;
    for (int i = 0; i < N; i++)
    {
        int ia = a[i] - 'A';
        int ib = b[i] - 'A';
        score += sums[1][ia] * (N - i);
        sums[0][ia] += i + 1;
        sums[1][ib] += i + 1;
        score += sums[0][ib] * (N - i);
    }

    fprintf(stdout, "%.9f\n", score / scale);

    return 0;
}