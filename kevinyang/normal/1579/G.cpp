signed main(){
	cin.tie(nullptr)->sync_with_stdio(false);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		vector<int>arr(n+1);
		for(int i = 1; i<=n; i++){
			cin >> arr[i];
		}
		int low = 0; int high = 2000; int mid = (low+high)/2;
		while(low+1<high){
			bitset<4000 >dp;
			bitset<4000 >rem;
			for(int i = 1000; i<=1000+mid; i++){
				rem[i] = 1;
				dp[i] = 1;
			}
			for(int i = 1; i<=n; i++){
				dp = ((dp<<arr[i])|(dp>>arr[i]));
				dp&=rem;
			}
			if(dp.count()>0)high = mid;
			else low = mid;
			mid = (low+high)/2;
		}
		cout << high << "\n";
	}
	return 0;
}