mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	pii king;
	cin >> king;
	int sz = 999;
	vector<pii> rook(sz * 2 / 3);
	vi r(sz + 1), c(sz + 1);
	cin >> rook;
	for(auto [x, y]: rook){
		r[x] ++, c[y] ++;
	}
	auto query = [&](){
		int k, x, y;
		cin >> k >> x >> y, k --;
		if(x <= 0){
			exit(0);
		}
		r[rook[k].first] --, c[rook[k].second] --;
		r[x] ++, c[y] ++;
		rook[k] = {x, y};
	};
	for(int x = king.first; x != sz + 1 >> 1; ){
		if(x < sz + 1 >> 1){
			x ++;
		}
		else{
			x --;
		}
		cout << x << " " << king.second << endl;
		query();
	}
	king.first = sz + 1 >> 1;
	for(int y = king.second; y != sz + 1 >> 1; ){
		if(y < sz + 1 >> 1){
			y ++;
		}
		else{
			y --;
		}
		cout << king.first << " " << y << endl;
		query();
	}
	king.second = sz + 1 >> 1;
	vi cnt(4);
	for(auto [x, y]: rook){
		cnt[2 * (x > sz + 1 >> 1) + (y > sz + 1 >> 1)] ++;
	}
	int p = 0;
	while(cnt[p] > sz * 2 / 3 / 4) p ++;
	vector<pii> dr{{1, 1}, {1, -1}, {-1, 1}, {-1, -1}};
	while(1){
		king = {king.first + dr[p].first, king.second + dr[p].second};
		if(r[king.first]){
			cout << king.first << " " << king.second - dr[p].second << endl;
			exit(0);
		}
		if(c[king.second]){
			cout << king.first - dr[p].first << " " << king.second << endl;
			exit(0);
		}
		cout << king.first << " " << king.second << endl;
		query();
	}
	return 0;
}
