#define mod 1000000007

long long power(long long a,long long b){
  long long x=1,y=a;
  while(b>0){
    if(b&1ll){
      x=(x*y)%mod;
    }
    y=(y*y)%mod;
    b>>=1;
  }
  return x%mod;
}

long long modular_inverse(long long n){
  return power(n,mod-2);
}

// range multiply(update) / range product(prod)
struct S_rmrp{
    long long value;
    int size;
};
using F_rmrp = long long;
S_rmrp op_rmrp(S_rmrp a, S_rmrp b){ return {(a.value * b.value)%mod, a.size+b.size}; }
S_rmrp e_rmrp(){ return {1, 0}; }
S_rmrp mapping_rmrp(F_rmrp f, S_rmrp x){ return {(x.value * power(f,x.size))%mod, x.size}; }
F_rmrp composition_rmrp(F_rmrp f, F_rmrp g){ return (f*g)%mod; }
F_rmrp id_rmrp(){ return 1; }

// range or(update) / range or(prod)

using S_roro = long long;
using F_roro = long long;

S_roro op_roro(S_roro a, S_roro b){ return (a|b); }
S_roro e_roro(){ return 0; }
S_roro mapping_roro(F_roro f, S_roro x){ return (x|f); }
F_roro composition_roro(F_roro f, F_roro g){ return (f|g); }
F_roro id_roro(){ return 0; }

int main(){
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  vector<int> pl;
  vector<int> fl(301,0);
  vector<long long> pf(301,0);
  for(int i=2;i<=300;i++){
    if(fl[i]==0){
      int tg=pl.size();
      pl.push_back(i);
      for(int j=i;j<=300;j+=i){
        fl[j]=1;
        pf[j]|=(1ll<<tg);
      }
    }
  }

  int n,q;
  cin >> n >> q;

  vector<int> a(n);
  for(auto &nx : a){cin >> nx;}

  vector<string> s(q);
  vector<int> l(q),r(q),x(q);
  for(int i=0;i<q;i++){
    cin >> s[i];
    if(s[i][0]=='T'){
      cin >> l[i] >> r[i];
      l[i]--;
      r[i]--;
    }
    else{
      cin >> l[i] >> r[i] >> x[i];
      l[i]--;
      r[i]--;
    }
  }

  vector<S_rmrp> v_rmrp(n);
  vector<S_roro> v_roro(n);
  for(int i=0;i<n;i++){
    v_rmrp[i]={a[i],1};
    v_roro[i]=pf[a[i]];
  }

  lazy_segtree<S_rmrp, op_rmrp, e_rmrp, F_rmrp, mapping_rmrp, composition_rmrp, id_rmrp> seg_rmrp(v_rmrp);
  lazy_segtree<S_roro, op_roro, e_roro, F_roro, mapping_roro, composition_roro, id_roro> seg_roro(v_roro);

  for(int i=0;i<q;i++){
    if(s[i][0]=='T'){
      long long res=seg_rmrp.prod(l[i],r[i]+1).value;
      long long curfl=seg_roro.prod(l[i],r[i]+1);
      long long frac_p=1,frac_q=1;
      for(int j=0;j<pl.size();j++){
        if(curfl&(1ll<<j)){
          frac_p*=(pl[j]-1);frac_p%=mod;
          frac_q*=(pl[j]);frac_q%=mod;
        }
      }
      res*=frac_p;res%=mod;
      res*=modular_inverse(frac_q);res%=mod;
      cout << res << "\n";
    }
    else{
      seg_rmrp.apply(l[i],r[i]+1,x[i]);
      seg_roro.apply(l[i],r[i]+1,pf[x[i]]);
    }
  }
  return 0;
}