void linearsieve(int n, vi &lpf, vi &prime){
	lpf.resize(n + 1);
	prime.reserve(n + 1);
	for(int i = 2; i <= n; i ++){
		if(!lpf[i]){
			lpf[i] = i;
			prime.push_back(i);
		}
		for(int j = 0; j < prime.size() && prime[j] <= lpf[i] && i * prime[j] <= n; j ++){
			lpf[i * prime[j]] = prime[j];
		}
	}
}

int main(){
	cin.tie(0)->sync_with_stdio(0);
	vi lpf, prime;
	linearsieve((int)1.5e7 + 10, lpf, prime);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	sort(all(a));
	if(a[0] == a[n - 1]){
		cout << -1;
		return 0;
	}
	int k = prime.size();
	vi cnt(k, n);
	vector<pii> q(k, {2e9, 2e9});
	for(auto x: a){
		for(int i = 0; prime[i] * prime[i] <= x; i ++){
			int p = prime[i];
			if(x % p == 0){
				cnt[i] --;
				int e = 0;
				while(x % p == 0){
					x /= p;
					e ++;
				}
				if(q[i].first > e){
					q[i] = {e, 1};
				}
				else if(q[i].first == e){
					q[i].second ++;
				}
			}
		}
		if(x > 1){
			int i = lower_bound(all(prime), x) - prime.begin();
			int p = x, e = 1;
			cnt[i] --;
			if(q[i].first > e){
				q[i] = {e, 1};
			}
			else if(q[i].first == e){
				q[i].second ++;
			}
		}
	}
	int res = 2e9;
	for(int i = 0; i < k; i ++){
		if(cnt[i]){
			res = min(res, cnt[i]);
		}
		else{
			res = min(res, q[i].second);
		}
	}
	cout << res;
	return 0;
}
