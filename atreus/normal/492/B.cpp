#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    int n, l;
    cin >> l >> n;
    double a[l + 10];
    cin >> a[0];
    for (int i = 1; i < l; i++)
        cin >> a[i];
    for (int i = 0; i < l - 1; i++){
        for (int j = i + 1; j < l; j++)
            if (a[i] < a[j])
                swap(a[i], a[j]);
    }
    double cnt = 0;
    for (int i = 0; i < l - 1; i++){
        if (cnt < a[i] - a[i + 1])
            cnt = a[i] - a[i + 1];
    }
    cnt = max(cnt / 2, n - a[0]);
    cnt = max(cnt, a[l - 1] - 0);
    double ans = cnt;
    cout << fixed << setprecision(9) << ans;
}