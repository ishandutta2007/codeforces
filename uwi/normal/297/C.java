	void solve()
	{
		int n = ni();
		int[][] s = new int[n][];
		for(int i = 0;i < n;i++){
			s[i] = new int[]{ni(), i};
		}
		Arrays.sort(s, new Comparator<int[]>() {
			public int compare(int[] a, int[] b) {
				return a[0] - b[0];
			}
		});
		
		int[] a = new int[n];
		int[] b = new int[n];
		int c = (n+2)/3;
		for(int i = 0;i < c;i++){
			a[s[i][1]] = i;
			b[s[i][1]] = s[i][0]-a[s[i][1]];
		}
		for(int i = c;i < 2*c && i < n;i++){
			b[s[i][1]] = i;
			a[s[i][1]] = s[i][0]-b[s[i][1]];
		}
		for(int i = 2*c;i < n;i++){
			b[s[i][1]] = 3*c-i-1;
			a[s[i][1]] = s[i][0]-b[s[i][1]];
		}
		out.println("YES");
		for(int i = 0;i < n;i++){
			if(i > 0)out.print(" ");
			out.print(a[i]);
		}
		out.println();
		for(int i = 0;i < n;i++){
			if(i > 0)out.print(" ");
			out.print(b[i]);
		}
		out.println();
	}
	
