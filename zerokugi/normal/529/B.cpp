main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	scanf("%d", &n);
	vector<pii> pp(n);	// <h, w>
	int maxh = 0;
	REP(i, n){
		scanf("%d%d", &pp[i].first, &pp[i].second);
		maxh = max(maxh, min(pp[i].second, pp[i].first));
	}
	ll ans = 1ll << 50;
	for(int i=maxh;i<1001;i++){
		ll sum = 0;
		int permit = n/2;
		vi gain;
		REP(j, n){
			if(pp[j].second > i){
				sum += pp[j].second;
				permit --;
			}else{
				sum += pp[j].first;
				if(pp[j].first <= i && pp[j].first > pp[j].second) gain.pb(pp[j].first - pp[j].second);
			}
		}
		sort(RALL(gain));
		for(int j=0;j<permit&&j<gain.size();j++) sum -= gain[j];
		if(permit >= 0) ans = min(ans, sum*i);
	}
	cout << ans << endl;
	return 0;
}