void solve(){
    ll x, y, a, b;
    cin >> x >> y >> a >> b;
    if(a > b) swap(a, b); // guarantee a < b
    if(x > y) swap(x, y); // guarantee x < y
    ll ans = 0;
    for(ll add = 1LL << 29; add; add >>= 1){
        ll mid = ans + add;
        ll x1 = x - a * mid;
        ll y1 = y - a * mid;
        if(min(x1, y1) < 0LL) continue;
        if(b-a == 0){
            ans += add;
            continue;
        }
        ll s = x1 / (b-a) + y1 / (b-a);
        if(s < mid) continue;
        ans += add;
    }
    cout << ans << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}