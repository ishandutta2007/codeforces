int n;
int a[5001];
int dp[5001][5001];
int best[5001][5001];



int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	//read(FILENAME);
	cin >> n;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
	}
	//12
	//12 23
	//1223
	//12 23 34 45
	//1223 2334 3445
	//12223334 23343445
	//1222233333344445
	//1 3 2 4 m - 2 m
	// 1 2 3 4  2 3 4 5 3 4 5 6 m m
	for (int i = n - 1; i >= 0; i--) {
		int cur = 0;
		for (int j = i; j < n; j++) {
			if ((j - i) % 2 == 0) {
				cur ^= a[j];
			}
			if (i == j) {
				dp[i][j] = a[i];
				best[i][j] = a[i];
			} else {
				if (i + 1 == j) {
					dp[i][j] = a[i] ^ a[i + 1];
				} else {
					if (i + 2 == j) {
						dp[i][j] = a[i] ^ a[j];
					} else {
						dp[i][j] = dp[i][j - 1] ^ dp[i + 1][j];
					}
				}
				best[i][j] = max(dp[i][j], max(best[i + 1][j], best[i][j - 1]));
			}
		}
	}
	int q;
	cin >> q;
	for (int i = 0; i < q; i++) {
		int l, r;
		cin >> l >> r;
		l--, r--;
		cout << best[l][r] << '\n';
	}
	return 0;
}