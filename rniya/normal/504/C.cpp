int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<int> a(n);
    for (int& x : a) cin >> x, x--;

    int l = 0, r = n - 1;
    while (l < r && a[l] == a[r]) l++, r--;
    if (l >= r) {
        cout << ll(n + 1) * n / 2 << '\n';
        return 0;
    }

    // is a[l,x) palindrome?
    auto in = [&](int l, int r, int x) { return l <= x && x < r; };
    auto check = [&](int x) {
        vector<int> cnt(n, 0);
        for (int i = l; i < x; i++) cnt[a[i]]++;
        int both = 0, odd = 0;
        for (int i = l, j = r; i <= j; i++, j--) {
            if (i == j) {
                odd += in(l, x, i);
            } else if (in(l, x, i) && in(l, x, j)) {
                both++;
            } else if (in(l, x, i)) {
                if (cnt[a[j]] <= 0) return false;
                cnt[a[j]]--;
            } else {
                if (a[i] != a[j]) {
                    return false;
                }
            }
        }
        for (int i = 0; i < n; i++) {
            both -= cnt[i] / 2;
            odd -= cnt[i] & 1;
        }
        return both == 0 && odd == 0;
    };

    if (!check(r + 1)) {
        cout << 0 << '\n';
        return 0;
    }

    ll ans = ll(l + 1) * (l + 1);
    for (int _ = 0; _ < 2; _++) {
        int lb = l, ub = r + 1;
        while (ub - lb > 1) {
            int mid = (ub + lb) >> 1;
            (check(mid) ? ub : lb) = mid;
        }
        ans += ll(r + 1 - ub) * (l + 1);
        reverse(a.begin(), a.end());
    }

    cout << ans << '\n';
    return 0;
}