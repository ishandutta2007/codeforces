const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
int a[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int ncase;
	scanf("%d", &ncase);
	while (ncase--)
	{
		int n;
		scanf("%d", &n);
		for (int i = 1; i <= n; i++)
			scanf("%d", &a[i]);
		sort(a + 1, a + n + 1);
		LL sum1 = a[1], sum2 = 0;
		int l = 2, r = n;
		int flag = 0;
		while (l < r)
		{
			sum1 += a[l++];
			sum2 += a[r--];
			if (sum1 < sum2)
				flag = 1;
		}
		if (flag) puts("YES");
		else puts("NO");
	}
	return 0;
}