template<typename T>
struct binary_indexed_tree {
    std::vector<T> bit;

    binary_indexed_tree(int n = 0, T value = 0) : bit(n + 1) {
        for (int i = 1; i <= n; i++)
            bit[i] = value * (i & -i);
    }

    void add(int pos, T delta) {
        for (pos++; pos < int(bit.size()); pos += pos & -pos)
            bit[pos] += delta;
    }

    T query(int pref) {
        T sum = 0;
        for (pref++; pref > 0; pref -= pref & -pref)
            sum += bit[pref];

        return sum;
    }

    T query(int l, int r) {
        if (r >= int(bit.size()))
            r = int(bit.size()) - 1;

        if (l < 0)
            l = 0;

        if (r <= l)
            return 0;

        return query(r - 1) - query(l - 1);
    }
};

int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);

    int n, q;
    cin >> n >> q;
    vector<int> a(n), b(n);
    for (auto &x : a) cin >> x;
    for (auto &x : b) cin >> x;
    vector<mint> c(n + 1);
    for (int i = 1; i < n; i++) {
        c[i] = a[i] - a[i - 1];
    }

    vector<ll> pref(n + 1);
    for (int i = 0; i < n; i++) {
        pref[i + 1] = pref[i] + b[i];
    }

    auto values = pref;
    sort(all(values));
    values.resize(unique(all(values)) - values.begin());

    auto coord = [&](ll x) {
        return lower_bound(all(values), x) - values.begin();
    };

    struct query {
        ll value;
        int id;
        mint coeff;
    };
    vector<vector<query>> queries(n + 1);
    vector<mint> ans(q);

    for (int i = 0; i < q; i++) {
        int l, r;
        cin >> l >> r;
        l--;
        assert(pref[r] == pref[l]);
        queries[l].push_back({pref[l], i, 1});
        queries[r - 1].push_back({pref[l], i, -1});
    }

    binary_indexed_tree<mint> bit_sum(len(values));
    binary_indexed_tree<mint> bit_coeffs(len(values));

    for (int i = n; i >= 0; i--) {
        for (auto &[value, id, coeff] : queries[i]) {
            int pos = coord(value);
            ans[id] += (mint(value) * bit_coeffs.query(pos) - bit_sum.query(pos)) * coeff;
            ans[id] += (-mint(value) * bit_coeffs.query(pos, len(values)) + bit_sum.query(pos, len(values))) * coeff;
        }

        int pos = coord(pref[i]);
        bit_sum.add(pos, mint(c[i]) * pref[i]);
        bit_coeffs.add(pos, mint(c[i]));
    }

    for (auto x : ans) cout << x << '\n';
}