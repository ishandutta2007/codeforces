	void solve()
	{
		int n = ni(), m = ni();
		int[] f = new int[m+1];
		for(int i = 0;i < n;i++)f[ni()]++;
		int[][] dp = new int[6][6];
		for(int i = 0;i < 6;i++)Arrays.fill(dp[i], -9999999);
		dp[0][0] = 0;
		for(int i = 0;i <= m;i++){
			int[][] ndp = new int[6][6];
			for(int j = 0;j < 6;j++)Arrays.fill(ndp[j], -9999999);
			for(int j = 0;j < 6;j++){
				for(int k = 0;k < 6;k++){
					if(dp[j][k] < 0)continue;
					for(int l = 0;l <= j && l <= k && l <= f[i];l++){
						int u = (f[i]-l) % 3;
						ndp[k-l][u] = Math.max(ndp[k-l][u], (f[i]-l)/3 + l + dp[j][k]);
						u += 3;
						if(u <= f[i]){
							ndp[k-l][u] = Math.max(ndp[k-l][u], (f[i]-l)/3-1 + l + dp[j][k]);
						}
					}
				}
			}
			dp = ndp;
		}
		long ans = 0;
		for(int i = 0;i < 6;i++){
			for(int j = 0;j < 6;j++){
				ans = Math.max(ans, dp[i][j]);
			}
		}
		out.println(ans);
	}
	
