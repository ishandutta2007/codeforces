void inc_int(int lb, int ub, vi& arr) {
    arr[lb] += 1;
    arr[ub+1] -= 1;
}

void solve() {
    int N, K;
    cin >> N >> K;

    vi A(N);
    for (int i=0;i<N;i++) cin >> A[i];

    vi arr(2*K+10, 0);
    for (int i=0;i<N/2;i++) {
        int a1 = A[i];
        int a2 = A[N-1-i];

        int xlb = min(a1+1, a2+1);
        int xub = max(a1+K, a2+K);

        inc_int(xlb, xub, arr);
        inc_int(a1+a2, a1+a2, arr);
    }
    
    vi cum(2*K+10, 0);
    for (int i=0;i+1<sz(cum);i++) {
        cum[i+1] = cum[i] + arr[i];
    }

    int savings = 0;
    for (int i=0;i<sz(cum);i++) savings = max(savings, cum[i]);

    cout << N - savings << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;
    cin >> T;

    for (int t=0;t<T;t++) solve();

    return 0;
}