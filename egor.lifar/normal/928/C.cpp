const string FILENAME = "input";
const int MAXN = 1001;


int n;
string s[MAXN];
int a[MAXN];
vector<pair<string, int> > g[MAXN];
map<pair<string, int>, int> who;
map<string, bool> used;


int main(){
	ios::sync_with_stdio(0);
    cin.tie(0);
    //read(FILENAME);
    cin >> n;
    for (int i = 0; i < n; i++) {
        cin >> s[i] >> a[i];
        int t;
        cin >> t;
        who[make_pair(s[i], a[i])] = i;
        for (int j = 0; j < t; j++) {
            string s1;
            int a1;
            cin >> s1 >> a1;
            g[i].push_back(make_pair(s1, a1));
        }
    }
    set<pair<int, pair<int, string> > > ss;
    ss.insert(make_pair(0, make_pair(-a[0], s[0])));
    vector<pair<string, int> > ans;
    while (!ss.empty()) {
        pair<int, pair<int, string> > f = *ss.begin();
        ss.erase(f);
        if (used[f.second.second]) {
            continue;
        }
        ans.push_back(make_pair(f.second.second, -f.second.first));
        used[f.second.second] = true;
        for (auto x: g[who[make_pair(f.second.second, -f.second.first)]]) {
            ss.insert(make_pair(f.first + 1, make_pair(-x.second, x.first)));
        }
    }
    sort(ans.begin() + 1, ans.end());
    cout << sz(ans) - 1 << '\n';
    for (int i = 1; i < sz(ans); i++) {
        cout << ans[i].first << ' ' << ans[i].second << endl;
    }
    return 0;
}