int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	ll pos = 0, neg = 0;
	ll lp = 0, ln = 0;
	for(int i = 0; i < n; i ++){
		if(a[i] > 0){
			lp ++;
		}
		else{
			ll tp = ln, tn = lp + 1;
			lp = tp, ln = tn;
		}
		pos += lp;
		neg += ln;
	}
	cout << neg << " " << pos;
	return 0;
}

