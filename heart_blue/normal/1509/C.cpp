#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 2e3 + 10;
LL dp[N][N];
int a[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	scanf("%d", &n);
	for (int i = 1; i <= n; i++)
	{
		scanf("%d", &a[i]);
	}
	sort(a + 1, a + n + 1);
	for (int len = 1; len <= n; len++)
	{
		for (int i = 1; i + len <= n; i++)
		{
			int j = i + len;
			dp[i][j] = a[j] - a[i] + min(dp[i + 1][j], dp[i][j - 1]);
		}
	}
	printf("%lld\n", dp[1][n]);

	return 0;
}