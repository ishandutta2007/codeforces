const int mod = 1000000007;

void add(int& a, int b) {
  a += b; if (a >= mod) a -= mod;
}

void dec(int &a, int b) {
  a -= b; if (a < 0) a += mod;
}

int mult(int a, int b) {
  return a * (ll)b % mod;
}

int bp(int a, int b) {
  int res = 1;
  while (b > 0) {
    if (b & 1) res = mult(res, a);
    a = mult(a, a);
    b >>= 1;
  }
  return res;
}

int n;
ll a[11];

int ok;

void solve() {
  cin >> n;
  readArr(a, a + n);

  for (int i = 0; i < n; ++i) a[i] = abs(a[i]);
  
  ok = 0;
  sort(a, a + n);

  int was = 0;
  for (int i = 0; i < n; ++i) if (a[i] == 0 || i + 1 < n && a[i] == a[i + 1]) was = 1;

  if (was) {
    cout << "YES\n"; return;
  }

  for (int v = 0; v < n && ok == 0; ++v) {
    vector<int> d; d.pb(0);
    for (int i = 0; i < n; ++i) if (i != v) {
      int sz = d.size();
      for (int x = 0; x < sz; ++x) d.pb(d[x] + a[i]), d.pb(d[x] - a[i]);
    }
    for (int x : d) if (x == a[v]) ok = 1;
  }

  if (ok) cout << "YES\n"; else 
  cout << "NO\n";
}


