int solve(){
	   int n;cin>>n;
	   vector<int>a(n+1),dp(n+1),last(n+1);
       Tree<int>t;
       for(int i=1;i<=n;i++){
            cin>>a[i];
            t.insert(a[i]);
       }
       for(int i=1;i<=n;i++){
            a[i]=t.ook(a[i])+1;
       }
       int mx=0;
       for(int i=1;i<=n;i++){
                dp[i]=1+dp[last[a[i]-1]];
                last[a[i]]=i;
                maxs(mx,dp[i]);
       }
       cout<<n-mx<<endl;
	return 0;
}
