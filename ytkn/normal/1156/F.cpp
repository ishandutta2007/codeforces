ModInt pow(ModInt a, ll n) {
    assert(n >= 0);
	ModInt ans = 1;
    while (n > 0) {
        if (n&1) ans = ans*a;
        a = a*a;
        n >>= 1;
    }
	return ans;
}

template<typename T>
class Cumsum{
    public:
    Cumsum(vector<T> v): v(v){
        n = v.size();
        v_cumsum = vector<T>(n+1, T(0));
        for(int i = 0; i < n; i++) v_cumsum[i+1] = v_cumsum[i]+v[i];
    }
    /**
     * v[l] + ... + v[r-1]
     */ 
    T sum(int l, int r){
        if(r <= l) return T(0);
        if(r > n) r = n;
        if(l < 0) l = 0;
        return v_cumsum[r]-v_cumsum[l];
    }
    private:
    int n;
    vector<T> v;
    vector<T> v_cumsum;
};

using mint = ModInt;
mint tri(int x){
    return mint(x)*mint(x-1)/2;
}

mint inv[10000];
mint tri_inv[10000];

void init(){
    for(int i = 1; i < 10000; i++){
        inv[i] = mint(i).inv();
        if(i >= 2) tri_inv[i] = tri(i).inv();
    }
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    init();
    int n; cin >> n;
    vector<int> a(n);
    vector<int> cnt(n+1);
    for(int i = 0; i < n; i++){
        cin >> a[i];
        cnt[a[i]]++;
    }
    auto cumsum = Cumsum<int>(cnt);
    auto dp = vec2d(n+1, n+1, mint(0));
    dp[0][0] = 1;
    mint ans = 0;
    for(int i = 0; i < n; i++){
        for(int j = 0; j <= n; j++){
            if(cnt[i+1] >= 2 && j+2 <= n){
                mint p = tri(cnt[i+1])*tri_inv[n-j];
                ans += dp[i][j]*p;
            }
            dp[i+1][j] += dp[i][j];
            if(j+1 <= n){
                dp[i+1][j+1] += dp[i][j]*cnt[i+1]*inv[n-j];
            }
        }
    }
    cout << ans << endl;
}