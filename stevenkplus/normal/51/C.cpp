int main() {
  int n;
  cin >> n;
  vector<int> v(n);
  for (auto &i: v) cin >> i;
  sort(v.begin(), v.end());

  vector<int> ans;
  auto ok = [&](int len) -> bool {
    ans.clear();
    ans.push_back(v[0]);
    for (int i: v) {
      if (i > ans.back() + len) {
        ans.push_back(i);
      }
    }
    pp(len, v, ans);
    return ans.size() <= 3;
  };

  int lo = 0;
  int hi = 1E9;
  while (lo < hi) {
    int mid = (lo + hi) / 2;
    if (ok(mid)) {
      hi = mid;
    } else {
      lo = mid + 1;
    }
  }
  pp(lo);

  ok(lo);
  cout << fixed << setprecision(6);
  cout << lo / 2. << "\n";
  while (sz(ans) < 3) ans.push_back(ans.back());
  for (int i: ans) {
    cout << i + lo / 2. << " "; 
  }
  cout << "\n";
}

