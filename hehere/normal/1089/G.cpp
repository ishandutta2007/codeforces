template<typename T1, typename T2>
inline bool chmin(T1 &a, const T2 &b) { return a > b ? a = b, true : false; }
template<typename T1, typename T2>
inline bool chmax(T1 &a, const T2 &b) { return a < b ? a = b, true : false; }
inline int readint() {
  int a = 0; char c = getchar(), p = 0;
  while(isspace(c)) c = getchar();
  if(c == '-') p = 1, c = getchar();
  while(isdigit(c)) a = a*10 + c - '0', c = getchar();
  return p ? -a : a;
}

int a[7];

int solve(int n) {
  int sum = 0;
  for(int i = 0; i < 7; ++i) sum += a[i];
  int ans = 0x3f3f3f3f;
  for(int i = 0; i < 7; ++i) {
    int t = n;
    int day = 0;
    int run;
    for(int j = i; j < 7; ++j) {
      t -= a[j]; ++day;
      if(t == 0) goto NEXT;
    }
		run = t / sum - 5;
    if(run > 0) t -= sum * run, day += 7 * run;
    for(int j = 0; ; j = (j + 1) % 7) {
      t -= a[j]; ++day;
      if(t == 0) goto NEXT;
    }
    NEXT:
      chmin(ans, day);
  }
  return ans;
}

int main() {
  int T = readint();
  while(T--) {
    int tot = readint();
    for(int i = 0; i < 7; ++i) a[i] = readint();
    printf("%d\n", solve(tot));
  }
  return 0;
}