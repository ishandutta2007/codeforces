#include<cstdio>
#include<cstring>
#include<algorithm>
using namespace std;
const int MAXN=1005;
int n,k,a[MAXN];
int f[MAXN*MAXN];
int main(){
	scanf("%d%d",&n,&k);
	for(int i=1; i<=n; i++)
		scanf("%d",a+i);
	sort(a+1,a+n+1);
	memset(f,0x3f,sizeof(f));
	f[0]=0;
	for(register int i=2; i<=n; i++){
		a[i]-=a[1];
		for(register int j=a[i]; j<=k*a[i]; j++)
			f[j]=min(f[j],f[j-a[i]]+1);
	}
	for(int i=0; i<=a[n]*k; i++)
		if(f[i]<=k) printf("%d ",a[1]*k+i);
	puts("");
	return 0;
}