int main() {
  int n, k; cin >> n >> k;
  string s; cin >> s;
  vector<bool> keys(26, false);
  while (k--) {
    char c; cin >> c;
    keys[c - 'a'] = true;
  }
  vector<long long> len;
  int now = 0;
  for (char c : s) {
    if (keys[c - 'a']) {
      ++now;
    } else {
      len.emplace_back(now);
      now = 0;
    }
  }
  len.emplace_back(now);
  long long ans = 0;
  for (long long e : len) ans += e * (e - 1) / 2 + e;
  cout << ans << '\n';
  return 0;
}