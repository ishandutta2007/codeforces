using namespace std;
typedef long long ll;

typedef vector<int> vi;
int n, L[55], R[55], cmp[55], cc[55];
double f(int i, int l, int r)
{
    l=max(l, L[i]);
    r=min(r, R[i]);
    int s=max(0, r-l+1);
    return s*1.0/(R[i]-L[i]+1);
}
double gt(int i, int v, int s)
{
    if (s==0) return f(i, v, v);
    if (s==-1) return f(i, 0, v-1);
    if (s==1) return f(i, v+1, 111111);
    throw;
}
int main()
{
    //freopen("in.txt", "r", stdin);
    //freopen("out.txt", "w", stdout);
    int n;
    scanf("%d", &n);
    for (int i=0;i<n;++i) scanf("%d %d", &L[i], &R[i]);
    double rs=0;
    int zzz=0;
    double tm=clock();
    for (int em=0;em<(1<<n);++em)
    {
        vi e, o;
        for (int i=0;i<n;++i) ((em&(1<<i))?e:o).push_back(i);
        for (int lm=0;lm<(1<<o.size());++lm)
        {
            vi l, g;
            for (int i=0;i<o.size();++i) ((lm&(1<<i))?l:g).push_back(o[i]);
            for (int i=0;i<e.size();++i) cmp[e[i]]=0, cc[e[i]]=0;
            for (int i=0;i<l.size();++i) cmp[l[i]]=1, cc[l[i]]=-1;
            for (int i=0;i<g.size();++i) cmp[g[i]]=-1, cc[g[i]]=1;
            sort(cmp, cmp+n);
            if (cmp[1]!=0) continue;
            ++zzz;
            //for (int i=0;i<n;++i) printf("%d ", cc[i]); puts("");
            for (int v=1;v<=10000;++v)
            {
                double pr=v;
                for (int i=0;i<n;++i) pr*=gt(i, v, cc[i]);
                rs+=pr;
            }
        }
    }
    //cerr<<(clock()-tm)/CLOCKS_PER_SEC<<endl;
    //cerr<<zzz<<endl;
    cout<<setprecision(13)<<fixed<<rs<<endl;
    return 0;
}