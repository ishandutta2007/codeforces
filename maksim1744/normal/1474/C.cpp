void test_case(int test) {
    int n;
    cin >> n;
    n *= 2;
    vector<int> a(n);
    cin >> a;
    sort(a.begin(), a.end());
    for (int ind = 0; ind + 1 < n; ++ind) {
        int x = a[ind] + a.back();
        multiset<int> left;
        for (int i = 0; i + 1 < n; ++i)
            if (i != ind)
                left.insert(a[i]);
        int cur = a.back();
        vector<pair<int, int>> ans;
        ans.eb(a[ind], a.back());
        while (!left.empty()) {
            int a = *--left.end();
            left.erase(--left.end());
            auto it = left.find(cur - a);
            if (it == left.end()) {
                break;
            }
            left.erase(it);
            ans.eb(a, cur - a);
            cur = a;
        }
        if (!left.empty()) continue;
        cout << "YES\n";
        cout << x << '\n';
        for (auto p : ans) {
            cout << p << '\n';
        }
        return;
    }
    cout << "NO\n";
}

