void solve() {
	int a, b, x, y, n;
	cin >> a >> b >> x >> y >> n;
	if (a - x + b - y <= n) 
		cout << x * y << '\n';
	else {
		int res = 1e18 + 228;
		int kol1 = a - x;
		int kol2 = b - y;
		if (a - n >= x)
			res = min(res, (a - n) * b);
		if (b - n >= y)
			res = min(res, (b - n) * a);
		if (kol1 <= n && b - (n - kol1) >= y)
			res = min(res, x * (b - (n - kol1)));
		if (kol2 <= n && a - (n - kol2) >= x)
			res = min(res, y * (a - (n - kol2)));
		cout << res << '\n';
	}
}

