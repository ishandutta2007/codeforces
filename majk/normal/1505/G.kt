fun main(args: Array<String>) {
    var N = readLine()!!.toInt()
    var z = ""
    repeat(N) {
        val X = readLine()!!
        z += when (X) {
            "1 0 0 1 0" -> 'a'
            "1 1 0 2 0" -> 'b'
            "2 0 0 1 1" -> 'c'
            "2 1 0 1 2" -> 'd'
            "1 1 0 1 1" -> 'e'
            "2 1 0 2 1" -> 'f'
            "2 2 0 2 2" -> 'g'
            "1 2 0 2 1" -> 'h'
            "1 1 0 1 1" -> 'i'
            "1 2 0 1 2" -> 'j'
            "1 0 1 2 0" -> 'k'
            "1 1 1 3 0" -> 'l'
            "2 0 1 2 1" -> 'm'
            "2 1 1 2 2" -> 'n'
            "1 1 1 2 1" -> 'o'
            "2 1 1 3 1" -> 'p'
            "2 2 1 3 2" -> 'q'
            "1 2 1 3 1" -> 'r'
            "1 1 1 2 1" -> 's'
            "1 2 1 2 2" -> 't'
            "1 0 2 2 1" -> 'u'
            "1 1 2 3 1" -> 'v'
            "2 0 2 2 2" -> 'x'
            "2 1 2 2 3" -> 'y'
            "1 1 2 2 2" -> 'z'
            "1 2 1 1 3" -> 'w'
            else -> error("foo $X")
        }
    }
    println(z)
}