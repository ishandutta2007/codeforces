void solve() {
	int n, x;
	cin >> n >> x;
	int kol1 = 0, kol2 = 0;
	for(int i = 0; i < n; ++i) {
		int a;
		cin >> a;
		if (a % 2 == 0)
			++kol2;
		else
			++kol1;
	} 
	if (x == n) {
		if (kol1 % 2 == 0)
			cout << "No\n";
		else
			cout << "Yes\n";
		return;
	}
	if (kol1 > 0 && (kol2 != 0 || kol2 == 0 && x % 2 == 1))
		cout << "Yes\n";
	else
		cout << "No\n"; 
}

