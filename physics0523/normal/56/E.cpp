// https://betrue12.hateblo.jp/entry/2020/09/23/005940#%E5%8C%BA%E9%96%93%E5%A4%89%E6%9B%B4%E5%8C%BA%E9%96%93%E5%92%8C%E5%8F%96%E5%BE%97
struct S{
    long long value;
    int size;
};
using F = long long;

const F ID = 8e18;

S op(S a, S b){ return {a.value+b.value, a.size+b.size}; }
S e(){ return {0, 0}; }
S mapping(F f, S x){
    if(f != ID) x.value = f*x.size;
    return x;
}
F composition(F f, F g){ return (f == ID ? g : f); }
F id(){ return ID; }

using pi=pair<int,int>;
using pip=pair<pi,int>;

int main(){
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  set<int> st;
  int n;
  cin >> n;
  vector<int> x(n),h(n);
  for(int i=0;i<n;i++){
    cin >> x[i] >> h[i];
    st.insert(x[i]);
    st.insert(x[i]+h[i]-1);
  }
  map<int,int> mp;
  int cid=0;
  for(auto &nx : st){
    mp[nx]=cid;
    cid++;
  }
  vector<pip> v;

  vector<S> init(262144, {0, 1});
  lazy_segtree<S, op, e, F, mapping, composition, id> seg(init);

  for(int i=0;i<n;i++){
    v.push_back({{mp[x[i]],mp[x[i]+h[i]-1]},i});
    seg.apply(mp[x[i]],seg.get(mp[x[i]]).value+1);
  }
  sort(v.begin(),v.end());
  reverse(v.begin(),v.end());

  vector<int> res(n);
  for(auto &nx : v){
    pi cs=nx.first;
    res[nx.second]=seg.prod(cs.first,cs.second+1).value;
    seg.apply(cs.first,cs.second+1,0);
    seg.apply(cs.first,res[nx.second]);
  }
  for(int i=0;i<n;i++){
    if(i){cout << ' ';}
    cout << res[i];
  }cout << '\n';
  return 0;
}