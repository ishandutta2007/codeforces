const int N = 1e6 + 5;

int n, m;
bool used[N];
vector<int> edge[N];
vector<int> rev_edge[N];

int calc_dist[N];
vector<int> dist[N];
int outer[N];
set<pair<int, int>> order;

void recount(int i) {
    order.erase({calc_dist[i], i});
    calc_dist[i] = min(calc_dist[i], dist[i].back() + outer[i] + 1);
    order.insert({calc_dist[i], i});
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n >> m;
    for (int i = 0; i < m; ++i) {
        int a, b;
        cin >> a >> b;
        edge[a - 1].push_back(b - 1);
        outer[a - 1]++;
        rev_edge[b - 1].push_back(a - 1);
    }
    for (int i = 0; i < n - 1; ++i) {
        calc_dist[i] = 1e9;
    }
    order.insert({0, n - 1});
    while (order.size()) {
        auto [d, i] = *order.begin();
        order.erase(order.begin());
        used[i] = true; 

        for (auto j : rev_edge[i]) {
            if (used[j]) {
                continue;
            }
            outer[j]--;
            dist[j].push_back(calc_dist[i]);
            recount(j);
        }
    }
    cout << calc_dist[0] << '\n';
}