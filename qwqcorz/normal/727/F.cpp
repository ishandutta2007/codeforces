#include<bits/stdc++.h>
#define ll long long
using namespace std;
const int N=755;

ll read()
{
	ll s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
ll low[N];
int n,m,a[N];
priority_queue<int>q;
bool check(ll sum,int k)
{
	while (!q.empty()) q.pop();
	for (int i=1;i<=n;i++)
	{
		q.push(-a[i]),sum+=a[i];
		while (sum<0) sum+=q.top(),q.pop(),k--;
		if (k<0) return 0;
	}
	return 1;
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	n=read(),m=read();
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=0;i<=n;i++)
	{
		ll l=0,r=i?low[i-1]:1e15;
		while (l<=r)
		{
			ll mid=(l+r)/2;
			if (check(mid,i)) r=mid-1;
						 else l=mid+1;
		}
		low[i]=l;
	}
	reverse(low,low+n+1);
	for (int i=1;i<=m;i++)
	{
		ll x=read();
		print(n-(upper_bound(low+1,low+1+n,x)-low)+1);
	}
	
	return 0;
}
