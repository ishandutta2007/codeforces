mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<vl> cost(3, vl(n));
	cin >> cost;
	vector<vi> adj(n);
	vi deg(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
		deg[u] ++, deg[v] ++;
	}
	if(any_of(all(deg), [](int x){return x == 3;})){
		cout << -1;
		return 0;
	}
	vi color(n);
	function<ll(int, int, int, int)> dfs = [&](int u, int p, int c, int pc){
		color[u] = c;
		ll res = cost[c - 1][u];
		for(int i = 1; i <= 3; i ++){
			if(i != c && i != pc){
				pc = c;
				c = i;
				break;
			}
		}
		for(auto v: adj[u]){
			if(v != p){
				res += dfs(v, u, c, pc);
			}
		}
		return res;
	};
	int u = 0, v = adj[u][0];
	ll res = 9e18;
	vi cres;
	for(int i = 1; i <= 3; i ++){
		for(int j = 1; j <= 3; j ++){
			if(i != j){
				ll temp = dfs(u, v, i, j) + dfs(v, u, j, i);
				if(res > temp){
					res = temp;
					cres = color;
				}
			}
		}
	}
	cout << res << "\n" << cres;
	return 0;
}

