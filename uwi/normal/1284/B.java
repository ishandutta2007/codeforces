	void solve()
	{
		int n = ni();
		boolean[] as = new boolean[n];
		int[] min = new int[n];
		int[] max = new int[n];
		for(int i = 0;i < n;i++) {
			int[] a = na(ni());
			int mi = Integer.MAX_VALUE;
			int ma = -1;
			for(int v : a) {
				if(mi < v) {
					as[i] = true;
				}
				mi = Math.min(mi, v);
				ma = Math.max(ma, v);
			}
			min[i] = mi;
			max[i] = ma;
		}
		
		int[] ivs = new int[n];
		int p = 0;
		for(int i = 0;i < n;i++) {
			if(!as[i]) {
				ivs[p++] = max[i];
			}
		}
		ivs = Arrays.copyOf(ivs, p);
		ivs = radixSort(ivs);
		long ans = (long)n*n;
		for(int i = 0;i < n;i++) {
			if(!as[i]) {
				int lb = lowerBound(ivs, min[i]+1);
				ans -= lb;
			}
		}
		out.println(ans);
	}
	
	public static int lowerBound(int[] a, int v){ return lowerBound(a, 0, a.length, v); }
	public static int lowerBound(int[] a, int l, int r, int v)
	{
		if(l > r || l < 0 || r > a.length)throw new IllegalArgumentException();
		int low = l-1, high = r;
		while(high-low > 1){
			int h = high+low>>>1;
			if(a[h] >= v){
				high = h;
			}else{
				low = h;
			}
		}
		return high;
	}
	

	
	public static int[] radixSort(int[] f){ return radixSort(f, f.length); }
	public static int[] radixSort(int[] f, int n)
	{
		int[] to = new int[n];
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(f[i]&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[f[i]&0xffff]++] = f[i];
			int[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(f[i]>>>16)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[f[i]>>>16]++] = f[i];
			int[] d = f; f = to;to = d;
		}
		return f;
	}

	
