const int MAXN = 100001;


int n, m;
vector<int> g[MAXN];
long long c[MAXN];
int used[MAXN];
int pr[MAXN];
int edge[MAXN][2];
long long ans[MAXN];
vector<int> cycle;


int inv(int id, int u) {
	return u ^ edge[id][0] ^ edge[id][1];
}


int dfs1(int v) {
	for (auto h: g[v]) {
		int u = inv(h, v);
		if (used[u] != -1) {
			if (used[v] == used[u]) {
				cycle.push_back(h);
				int w = v;
				while (w != u) {
					cycle.push_back(pr[w]);
					w = inv(pr[w], w);
				}
				return u;
			}
			continue;
		}
		used[u] = used[v] ^ 1;
		pr[u] = h;
		int w = dfs1(u);
		if (w != -1) {
			return w;
		}
	}
	return -1;
}


void dfs2(int v) {
	for (auto h: g[v]) {
		int u = inv(h, v);
		if (pr[u] != -1) {
			continue;
		}
		pr[u] = h;
		dfs2(u);
		ans[h] += c[u];
		c[v] -= c[u];
	}
}


