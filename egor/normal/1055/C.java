public class Main {
    public static void main(String[] args) {
        InputStream inputStream = System.in;
        OutputStream outputStream = System.out;
        InputReader in = new InputReader(inputStream);
        OutputWriter out = new OutputWriter(outputStream);
        CUdachnieDni solver = new CUdachnieDni();
        solver.solve(1, in, out);
        out.close();
    }

    static class CUdachnieDni {
        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int la = in.readInt();
            int ra = in.readInt();
            int ta = in.readInt();
            int lb = in.readInt();
            int rb = in.readInt();
            int tb = in.readInt();
            int g = IntegerUtils.gcd(ta, tb);
            int answer = 0;
            int qa = la / g;
            la -= qa * g;
            ra -= qa * g;
            int qb = lb / g;
            lb -= qb * g;
            rb -= qb * g;
            answer = Math.max(answer, getAnswer(la, ra, lb, rb));
            answer = Math.max(answer, getAnswer(la + g, ra + g, lb, rb));
            answer = Math.max(answer, getAnswer(la, ra, lb + g, rb + g));
            out.printLine(answer);
        }

        private int getAnswer(int la, int ra, int lb, int rb) {
            return Math.min(ra, rb) - Math.max(la, lb) + 1;
        }

    }

    static class InputReader {
        private InputStream stream;
        private byte[] buf = new byte[1024];
        private int curChar;
        private int numChars;
        private InputReader.SpaceCharFilter filter;

        public InputReader(InputStream stream) {
            this.stream = stream;
        }

        public int read() {
            if (numChars == -1) {
                throw new InputMismatchException();
            }
            if (curChar >= numChars) {
                curChar = 0;
                try {
                    numChars = stream.read(buf);
                } catch (IOException e) {
                    throw new InputMismatchException();
                }
                if (numChars <= 0) {
                    return -1;
                }
            }
            return buf[curChar++];
        }

        public int readInt() {
            int c = read();
            while (isSpaceChar(c)) {
                c = read();
            }
            int sgn = 1;
            if (c == '-') {
                sgn = -1;
                c = read();
            }
            int res = 0;
            do {
                if (c < '0' || c > '9') {
                    throw new InputMismatchException();
                }
                res *= 10;
                res += c - '0';
                c = read();
            } while (!isSpaceChar(c));
            return res * sgn;
        }

        public boolean isSpaceChar(int c) {
            if (filter != null) {
                return filter.isSpaceChar(c);
            }
            return isWhitespace(c);
        }

        public static boolean isWhitespace(int c) {
            return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
        }

        public interface SpaceCharFilter {
            public boolean isSpaceChar(int ch);

        }

    }

    static class IntegerUtils {
        public static int gcd(int a, int b) {
            a = Math.abs(a);
            b = Math.abs(b);
            while (b != 0) {
                int temp = a % b;
                a = b;
                b = temp;
            }
            return a;
        }

    }

    static class OutputWriter {
        private final PrintWriter writer;

        public OutputWriter(OutputStream outputStream) {
            writer = new PrintWriter(new BufferedWriter(new OutputStreamWriter(outputStream)));
        }

        public OutputWriter(Writer writer) {
            this.writer = new PrintWriter(writer);
        }

        public void close() {
            writer.close();
        }

        public void printLine(int i) {
            writer.println(i);
        }

    }
}