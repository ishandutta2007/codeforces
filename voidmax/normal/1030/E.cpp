typedef long long base;       
typedef pair<int, int> point;

const int MAXN = 3e5 + 1;

int n;
long long ans;
int w[MAXN];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
    cin >> n;
    long long pref0 = 1, pref1 = 0, cur = 0;
    for (int i = 0; i < n; ++i) {
        long long a;
        cin >> a;
        while (a) {
            w[i] += a & 1;
            a >>= 1;
        }
        cur = (cur + w[i]) & 1;
        if (cur) {
            ans += pref1;
            ++pref1;
        } else {
            ans += pref0;
            ++pref0;
        }
    }
    for (int i = 0; i < n; ++i) {
        vector<int> sum[2];
        int L = i - 1, cur = 0;
        while (cur < w[i]) {
            sum[0].pb(cur);
            if (L == -1) break;
            cur += w[L];
            --L;
        }
        int R = i + 1;
        cur = 0;
        while (cur < w[i]) {
            sum[1].pb(cur);
            if (R == n) break;
            cur += w[R];
            ++R;
        }
        for (auto a : sum[0]) {
            for (auto b : sum[1]) {
                if ((a + w[i] + b) & 1) continue;
                if (w[i] > a + b) {
                    --ans;
                }
            }
        }
    }
    cout << ans << '\n';
}