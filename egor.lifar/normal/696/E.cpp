void dfs(int u) {
    for (auto h: v[u]) {
        if (h != pr[u]) {
            pr[h] = u;
            parent[h][0] = u;
            dfs(h);
            g[u].push_back(h);
        }
    }
}


void dfs_sz(int u) {
    sz[u] = 1;
    for (auto &h: g[u]) {
        dfs_sz(h);
        sz[u] += sz[h];
        if (sz[h] > sz[g[u][0]]) {
            swap(h, g[u][0]);
        }
    }
}


void dfs_hld(int u) {
    in[u] = ++t;
    whin[in[u]] = u;
    for (auto &h: g[u]) {
        next[h] = (h == g[u][0] ? next[u]: h);
        dfs_hld(h);
    }
    out[u] = t;
}


