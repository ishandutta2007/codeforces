int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<int> val(n), flip(n, 0);
    for (int i = 0; i < n; i++) {
        int a, b;
        cin >> a >> b;
        a--, b--;
        if (a > b) {
            swap(a, b);
            flip[a]++;
        }
        if (b < n || n <= a) {
            cout << -1 << '\n';
            return 0;
        }
        val[a] = b;
    }

    vector<stack<int>> st(2);  // smaller number
    for (int i = 0; i < 2; i++) st[i].emplace(2 * n);
    vector<int> Max(n + 1, -1), sum(2, 0), cost(2, 0);
    for (int i = n - 1; i >= 0; i--) Max[i] = max(Max[i + 1], val[i]);
    int Min = 2 * n, ans = 0;

    for (int i = 0; i < n; i++) {
        if (st[0].top() > val[i]) {
            st[0].emplace(val[i]);
            sum[0]++;
            cost[0] += flip[i];
        } else if (st[1].top() > val[i]) {
            st[1].emplace(val[i]);
            sum[1]++;
            cost[1] += 1 - flip[i];
        } else {
            cout << -1 << '\n';
            return 0;
        }

        Min = min(Min, val[i]);
        if (Min > Max[i + 1]) {
            ans += min(cost[0] + cost[1], (sum[0] - cost[0]) + (sum[1] - cost[1]));
            for (int j = 0; j < 2; j++) {
                sum[j] = 0;
                cost[j] = 0;
                while (st[j].size() > 1) st[j].pop();
            }
        }
    }

    cout << ans << '\n';
    return 0;
}