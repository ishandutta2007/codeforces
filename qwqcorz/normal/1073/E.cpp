#include<bits/stdc++.h>
#define int long long
using namespace std;
const int p=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
inline void add(int &x,int y)
{
	x+=y;
	if (x>=p) x-=p;
}
int popcount[1024];
int f[20][1024][2],g[20][1024][2],a[20];
int calc(int n,int m)
{
	if (n==0) return 0;
	memset(f,0,sizeof(f));
	memset(g,0,sizeof(g));
	for (int i=1;i<=18;i++) a[i]=n%10,n/=10;
	n=18;
	while (a[n]==0) n--;
	g[n+1][0][1]=1;
	for (int i=2;i<=n;i++) g[i][0][0]=1;
	for (int i=n;i>=1;i--)
	{
		for (int j=0;j<1024;j++)
		for (int k=0;k<=1;k++)
		if (g[i+1][j][k])
		for (int t=0;t<=(k?a[i]:9);t++)
		if (popcount[j|(1<<t)]<=m)
		{
			add(g[i][j|(1<<t)][k&&t==a[i]],g[i+1][j][k]);
			add(f[i][j|(1<<t)][k&&t==a[i]],(f[i+1][j][k]*10+g[i+1][j][k]*t)%p);
		}
		f[i][1][0]=f[i][1][1]=g[i][1][0]=g[i][1][1]=0;
	}
	int ret=0;
	for (int j=0;j<1024;j++)
	for (int k=0;k<=1;k++) add(ret,f[1][j][k]);
	return ret;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	for (int i=1;i<1024;i++) popcount[i]=popcount[i&(i-1)]+1;
	int l=read(),r=read(),k=read();
	print((calc(r,k)-calc(l-1,k)+p)%p);
	
	return 0;
}
