void add(ll& a, ll b) {
    a += b;
    if (a >= MOD) {
        a -= MOD;
    }
}

void mul(ll& a, ll b) {
    a = (a * b) % MOD;
}

ll todeg(ll x, ll d) {
    if (d < 0) {
        d += MOD - 1;
    }
    if (d == 0) {
        return 1;
    }
    ll ans{ todeg(x, d / 2) };
    mul(ans, ans);
    if (d & 1) {
        mul(ans, x);
    }
    return ans;
}

void solve() {
    int n;
    cin >> n;
    if (n > 1 && (n & (n - 1)) == 0) {
        int isLose = 1;
        while (n > 2) {
            n /= 2;
            isLose ^= 1;
        }
        if (isLose) {
            cout << "Bob\n";
        }
        else {
            cout << "Alice\n";
        }
    }
    else if (n & 1) {
        cout << "Bob\n";
    }
    else {
        cout << "Alice\n";
    }
}

