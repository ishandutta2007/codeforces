int m=1000000007;
ll toPow(ll b)
{
	ll res=1;
	ll a=2;
	while (b)
	{
		if (b&1) res=(res*a)%m;
		a=(a*a)%m;
		b>>=1;
	}
	return res;
}

void run()
{

	ll n;
	cin>>n;
	n=toPow(n);
	n%=m;
	cout<<(n*(n+1)/2)%m<<endl;
}

