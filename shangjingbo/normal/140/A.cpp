const double pi =   acos(-1.0);
const double eps    =   1e-9;

int main()
{
    int n,R,r;
    scanf("%d%d%d",&n,&R,&r);
    
    if (n==1){
        if (r<=R){
            puts("YES");
        }else{
            puts("NO");
        }
    }else if (n==2){
        if (r+r<=R){
            puts("YES");
        }else{
            puts("NO");
        }
    }else{
        double ang=2*pi/n;
        ang/=2;
        double limit=sin(ang)*R/(1+sin(ang));
        if (r<=limit+eps){
            puts("YES");
        }else{
            puts("NO");
        }
    }
    
    return 0;
}