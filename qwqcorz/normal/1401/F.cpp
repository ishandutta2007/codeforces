#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=1<<18;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N],t[N*4+5],rev[20];
#define ls ((w*2)^re)
#define rs ((w*2+1)^re)
#define mid ((l+r)/2)
void build(int w,int l,int r)
{
	if (l==r) return void(t[w]=a[l]);
	int re=0;
	build(ls,l,mid);
	build(rs,mid+1,r);
	t[w]=t[ls]+t[rs];
}
void change(int w,int l,int r,int d,int re,int x,int k)
{
	re^=rev[d];
	if (l==r) return void(t[w]=k);
	if (x<=mid) change(ls,l,mid,d-1,re,x,k);
		   else change(rs,mid+1,r,d-1,re,x,k);
	t[w]=t[ls]+t[rs];
}
int query(int w,int l,int r,int d,int re,int ql,int qr)
{
	re^=rev[d];
	if (ql<=l&&r<=qr) return t[w];
	if (qr<=mid) return query(ls,l,mid,d-1,re,ql,qr);
	if (ql>mid) return query(rs,mid+1,r,d-1,re,ql,qr);
	return query(ls,l,mid,d-1,re,ql,qr)+query(rs,mid+1,r,d-1,re,ql,qr);
}
#undef ls
#undef rs
#undef mid

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=read(),siz=1<<n;
	for (int i=1;i<=siz;i++) a[i]=read();
	build(1,1,siz);
	for (int i=1;i<=m;i++)
	{
		int opt=read(),x=read(),y=opt==1||opt==4?read():0;
		if (opt==1) change(1,1,siz,n,0,x,y);
		if (opt==2) rev[x]^=1;
		if (opt==3) rev[x]^=1,rev[x+1]^=1;
		if (opt==4) print(query(1,1,siz,n,0,x,y));
	}
	
	return 0;
}
