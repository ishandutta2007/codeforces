vector<int> ret;
void solve(int n, int64 L, int64 R, int offset) {
  // trace(n, L, R, offset, ret);
  if (L >= R) return;
  if (n == 1) {
    ret.push_back(1);
    return;
  }
  if (L < 2 * (n - 1)) {
    for (int i = L; i < R && i < 2 * (n - 1); ++i) {
      if (i % 2 == 0) {
        ret.push_back(0 + offset);
      } else {
        ret.push_back((i + 1) / 2 + offset);
      }
    }
  }
  solve(n - 1, max(0LL, L - 2 * (n - 1)), R - 2 * (n - 1), offset + 1);
}

int main() {
  int cas;
  scanf("%d", &cas);
  while (cas--) {
    int n;
    int64 L, R;
    scanf("%d%lld%lld", &n, &L, &R);
    ret.clear();
    solve(n, L - 1, R, 1);
    for (int i = 0; i < ret.size(); ++i) {
      printf("%d%c", ret[i], " \n"[i + 1 == ret.size()]);
    }
  }
  return 0;
}