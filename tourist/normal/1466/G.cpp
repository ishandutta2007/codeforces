int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n, tt;
  cin >> n >> tt;
  string s0, t;
  cin >> s0 >> t;
  vector<Mint> p2(n + 2);
  p2[0] = 1;
  for (int i = 1; i <= n; i++) {
    p2[i] = p2[i - 1] * 2;
  }
  vector<vector<Mint>> aux(26, vector<Mint>(n + 1));
  for (int c = 0; c < 26; c++) {
    char d = (char) ('a' + c);
    for (int i = 0; i < n; i++) {
      aux[c][i + 1] = aux[c][i] * 2 + (t[i] == d ? 1 : 0);
    }
  }
  auto Get = [&](char d, int from, int to) {
    int c = (int) (d - 'a');
    return aux[c][to] - aux[c][from] * p2[to - from];
  };
  int len = (int) s0.size();
  while (tt--) {
    int k;
    string w;
    cin >> k >> w;
    int nw = (int) w.size();
    Mint ans = 0;
    for (int shift = 0; shift <= len; shift++) {
      int cur = shift;
      string s = "";
      bool ok = true;
      for (int i = 0; i < nw; i++) {
        if (cur == len) {
          s += w[i];
          cur = 0;
        } else {
          if (w[i] != s0[cur]) {
            ok = false;
            break;
          }
          cur += 1;
        }
      }
      if (!ok) {
        continue;
      }
      int pos = 0;
      while ((int) s.size() > 1) {
        if (pos == k) {
          ok = false;
          break;
        }
        bool even = true;
        bool odd = true;
        for (int i = 0; i < (int) s.size(); i++) {
          if (s[i] != t[pos]) {
            if (i % 2 == 0) {
              even = false;
            } else {
              odd = false;
            }
          }
        }
        if (!odd && !even) {
          ok = false;
          break;
        }
        if (odd != even) {
          string new_s = "";
          for (int i = (odd ? 0 : 1); i < (int) s.size(); i += 2) {
            new_s += s[i];
          }
          swap(s, new_s);
          pos += 1;
          continue;
        }
        break;
      }
      if (!ok) {
        continue;
      }
      int z = (int) s.size();
      if (z == 0) {
        ans += p2[k - pos];
        continue;
      }
      Mint c1 = 1;
      Mint c2 = 0;
      while (true) {
        if (z == 2) {
          ans += c2 * Get(s[0], pos, k);
          c2 = 0;
        }
        if (z == 1) {
          ans += c1 * Get(s[0], pos, k);
          ans += c2 * p2[k - pos];
          break;
        }
        if (pos == k || s[0] != t[pos]) {
          break;
        }
        int new_z = (z + 1) / 2;
        Mint new_c1 = c1;
        Mint new_c2 = c2;
        if (z % 2 == 0) {
          new_c1 += c1 + c2;
        } else {
          new_c2 += c1 + c2;
        }
        z = new_z;
        c1 = new_c1;
        c2 = new_c2;
        pos += 1;
      }
    }
    cout << ans << '\n';
  }
  return 0;
}