#include"bits/stdc++.h"
using namespace std;
int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        int n;
        cin>>n;
        int ans=0;
        for(int i=0;i<n;i++)
        {
            int c;
            scanf("%d",&c);
            if(c==2);
            else ans++;
        }
        cout<<ans<<endl;
    }
    return 0;
}