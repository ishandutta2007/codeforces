void test_case(int test) {
    string s;
    cin >> s;
    int n = s.size();
    int N = s.size() * 2 + 5;
    vector<int> edges(N, 0);
    int x = N / 2;
    for (auto c : s) {
        if (c == '0') {
            --x;
            edges[x]++;
        } else {
            edges[x]++;
            ++x;
        }
    }
    x = N / 2;
    string t;
    for (int i = 0; i < n; ++i) {
        if (edges[x - 1] >= 2 || (edges[x - 1] == 1 && edges[x] == 0)) {
            --x;
            --edges[x];
            t.pb('0');
        } else {
            --edges[x];
            ++x;
            t.pb('1');
        }
    }
    cout << t << '\n';
}

