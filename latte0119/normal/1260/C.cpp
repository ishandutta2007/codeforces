template<class T>
ostream& operator<<(ostream& ost,const vector<T>&v){
	ost<<"{";
	for(int i=0;i<v.size();i++){
		if(i)ost<<",";
		ost<<v[i];
	}
	ost<<"}";
	return ost;
}


void solve(){
	int r,b,k;
	cin>>r>>b>>k;
	if(r<b)swap(r,b);

	int g=__gcd(r,b);
	r/=g;b/=g;

	if(r!=1&&(r-2)/b+1>=k)cout<<"REBEL"<<endl;
	else cout<<"OBEY"<<endl;
}

signed main(){
	int T;cin>>T;
	while(T--)solve();
	return 0;
}