#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=605;
const int inf=1LL<<60;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
inline void up(int &x,int y) {x=min(x,y);}
bool vis[N];
int e[N][N],dis[N][N],n,m;
void dijkstra(int s,int *dis)
{
	memset(vis,0,sizeof(vis));
	for (int i=1;i<=n;i++) dis[i]=e[s][i]?e[s][i]:inf;
	for (int t=1;t<n;t++)
	{
		int now=0;
		for (int i=1;i<=n;i++) if (!vis[i]&&(!now||dis[now]>dis[i])) now=i;
		up(dis[now%n+1],dis[now]+1);
		for (int i=1;i<=n;i++)
		if (e[now][i])
		up(dis[(i+dis[now]-1)%n+1],dis[now]+e[now][i]);
		vis[now]=1;
	}
	dis[s]=0;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	n=read(),m=read();
	for (int i=1;i<=m;i++)
	{
		int u=read(),v=read();
		e[u+1][v+1]=read();
	}
	for (int i=1;i<=n;i++) dijkstra(i,dis[i]);
	for (int i=1;i<=n;i++,putchar('\n'))
	for (int j=1;j<=n;j++) print(dis[i][j],' ');
	
	return 0;
}
