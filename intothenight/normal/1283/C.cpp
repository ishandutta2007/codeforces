int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	for(auto &x: a){
		x --;
	}
	vi flag(n, 1), prev(n); // unused
	for(int i = 0, p = -1; i < n; i ++){
		if(a[i] >= 0){
			flag[a[i]] = false;
		}
		else{
			prev[i] = p;
			p = i;
		}
	}
	vi L;
	for(int i = 0; i < n; ++ i){
		if(flag[i]){
			L.push_back(i);
		}
	}
	for(int i = 0; i < n; ++ i){
		if(a[i] == -1){
			a[i] = L.back();
			L.pop_back();
			if(i == a[i]){
				int j = prev[i];
				if(j == -1){
					swap(a[i], L.back());
				}
				else{
					swap(a[i], a[j]);
				}
			}
		}
	}
	for(auto x: a){
		cout << x + 1 << " ";
	}
	return 0;
}
 
