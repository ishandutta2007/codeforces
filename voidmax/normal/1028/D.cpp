const int p = 1e9 + 7;

int ans = 1;

int n;
set<int> buy;
set<int> unknown;
set<int> sell;

int main() {
    ios_base::sync_with_stdio(false);
    cout.tie(0);
    //read("input");
    cin >> n;
    for (int i = 0; i < n; ++i) {
        string s;
        int v;
        cin >> s;
        cin >> v;
        int L = -1e9, R = 1e9;
        if (buy.size()) L = *buy.rbegin();
        if (sell.size()) R = *sell.begin();
        //cout << s << ' ' << v << endl;
        //cout << buy << ":" << unknown << ":" << sell << endl;
        if (s == "ACCEPT") {
            int mul = 2;
            if (buy.count(v)) {
                if (L == v) {
                    --mul;
                    buy.erase(v);
                } else {
                    cout << "0\n";
                    return 0;
                }
            } 
            if (sell.count(v)) {
                if (R == v) {
                    --mul;
                    sell.erase(v);
                } else {
                    cout << "0\n";
                    return 0;
                }
            }
            if (mul == 2) ans = (ans + ans);
            if (ans >= p) ans -= p; 
            for (auto j : unknown) {
                if (j == v) continue;
                if (j < v) {
                    buy.insert(j);
                } else {
                    sell.insert(j);
                }
            }
            unknown.clear();
        } else {
            if (L < v && v < R) {
                unknown.insert(v);
            } else if (v < L) {
                buy.insert(v);
            } else {
                sell.insert(v); 
            }
        }
    }
    ans = 1LL * ans * (unknown.size() + 1) % p;
    cout << ans << '\n';
}