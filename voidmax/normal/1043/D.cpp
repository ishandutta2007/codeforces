typedef long long base; 
typedef pair<int, int> point;      
typedef complex<double> comp;


int n, m;
int L, R;
vector<int> place[11];
vector<int> func[11];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
    cin >> n >> m;
    for (int i = 0; i < m; ++i) {
        func[i].resize(n);
        cin >> func[i];
        place[i].resize(n + 1);
        for (int j = 0; j < n; ++j) {
            place[i][func[i][j]] = j;
        }
    }
    long long ans = 0;
    for (int i = 0; i < n; ++i) {
        chkmax(R, i);
        L = i;
        while (R + 1 < n) {
            bool OK = true;
            for (int j = 1; j < m; ++j) {
                if (place[j][func[0][L]] - place[j][func[0][R + 1]] != L - R - 1) {
                    OK = false;
                    break;
                }
            }
            if (!OK) break;
            ++R;
        }
        ans += R - L + 1;
    }
    cout << ans << endl;
}