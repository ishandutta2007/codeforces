using namespace std;
int sol(int a,int b)
{
    if(b==1) return 666666666;
    int ans=0;
    while(a) a/=b,ans++;
    return ans;
}
int main()
{
    int t;sc(t);
    while(t--)
    {
        int a,b;
        sc(a,b);
        int ans=sol(a,b);
        for(int i=b+1;i<=b+15;i++) ans=min(ans,sol(a,i)+i-b);
        out(ans);
    }
}