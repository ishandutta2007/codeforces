void solve(){
    int n,m; cin >> n >> m;
    vector<vector<int>> a(n,vector<int>(n));
    for (int i=0;i<n;++i){
        for (int j=0;j<n;++j){
            cin >> a[i][j],--a[i][j];
        }
    }

    vector<int> dir(3),delta(3,0);
    iota(dir.begin(),dir.end(),0);

    string ope; cin >> ope;
    for (char c:ope){
        if (c=='R') ++delta[1];
        else if (c=='L') --delta[1];
        else if (c=='D') ++delta[0];
        else if (c=='U') --delta[0];
        else if (c=='I'){
            swap(dir[1],dir[2]);
            swap(delta[1],delta[2]);
        } else {
            swap(dir[0],dir[2]);
            swap(delta[0],delta[2]);
        }
    }
    for (int i=0;i<3;++i) delta[i]=(delta[i]%n+n)%n;

    vector<vector<int>> ans(n,vector<int>(n));
    for (int i=0;i<n;++i){
        for (int j=0;j<n;++j){
            vector<int> v={i,j,a[i][j]},u(3);
            for (int k=0;k<3;++k) u[k]=(v[dir[k]]+delta[k])%n;
            ans[u[0]][u[1]]=u[2];
        }
    }

    for (int i=0;i<n;++i){
        for (int j=0;j<n;++j){
            cout << ans[i][j]+1 << (j+1==n?'\n':' ');
        }
    }
}

int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t; cin >> t;
    for (;t--;){
        solve();
    }
}