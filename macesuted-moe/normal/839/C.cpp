#define maxn 100005

char str[maxn];

vector<vector<int> > graph;

double dfs(int p, int fa) {
    int siz = graph[p].size() - (p != 1);
    double ans = 0;
    for (vector<int>::iterator i = graph[p].begin(); i != graph[p].end(); i++)
        if (*i != fa) ans += dfs(*i, p) / siz;
    return ans + 1;
}

int main() {
    int n = read<int>();
    graph.resize(n + 1);
    for (register int i = 1, from, to; i < n; i++) {
        from = read<int>(), to = read<int>();
        graph[from].push_back(to), graph[to].push_back(from);
    }
    printf("%.15lf\n", dfs(1, -1) - 1);
    return 0;
}