template <typename T>
T input() {
    T res;
    cin >> res;
    LASSERT(cin);
    return res;
}

template <typename IT>
void input_seq(IT b, IT e) {
    std::generate(b, e, input<typename std::remove_reference<decltype(*b)>::type>);
}

#define SZ(vec)         int((vec).size())
#define ALL(data)       data.begin(),data.end()
#define RALL(data)      data.rbegin(),data.rend()
#define TYPEMAX(type)   std::numeric_limits<type>::max()
#define TYPEMIN(type)   std::numeric_limits<type>::min()

#define pb push_back
#define eb emplace_back


int main() {
    std::iostream::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    
    // code here.
    int n = input<int>();
    vector<int> seq(n);
    input_seq(ALL(seq));

    std::sort(ALL(seq));
    vector<bool> dp(SZ(seq));

    dp.back() = true;
    bool has_wins = false;
    for (int i = SZ(seq) - 2; i >= 0; --i) {
        if (seq[i] != seq[i + 1] and dp[i + 1])
            has_wins = true;
        
        if (has_wins or (seq[i] == seq[i + 1] and dp[i + 1]))
            dp[i] = false;
        else
            dp[i] = true;
    }

    bool can_win = false;
    for (int i = 0; i != SZ(seq); ++i)
        if (i == 0 or seq[i] != seq[i - 1])
            can_win = can_win or dp[i];
    
    cout << (can_win ? "Conan\n" : "Agasa\n");
    
    return 0;
}