int n,k;
bool flag;
int a[N];
int Max[N],Min[N];
signed main()
{
	cin>>T;
	while (T--)
	{
		cin>>n>>k;
		flag=0;
		for (int i=1;i<=n;i++) cin>>a[i];
		Max[1]=Min[1]=a[1];
		for (int i=2;i<=n;i++)
		{
			Max[i]=max(a[i],Max[i-1]-k+1);
			Min[i]=min(a[i]+k-1,Min[i-1]+k-1);
			if (Min[i]<a[i] || Max[i]>a[i]+k-1){flag=1;break;}
		}
		if (Max[n]!=a[n]) flag=1;
		if (flag) puts("No");else puts("Yes");
	}
}