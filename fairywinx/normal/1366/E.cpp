const int MOD = 998244353;

int res(int i, int j, vector<map<int, int>> &dp, vector<int> &prev, vector<int> &b, vector<int> &a) {
	if (dp[i + 1].count(j))
		return dp[i + 1][j];
	if (b[j - 1] == a[i]) {
		if (prev[i] == -1) {
			if (j == 1)
				dp[i + 1][j] = 1;
			else
				dp[i + 1][j] = 0;
		}
		else
			dp[i + 1][j] = (res(prev[i], j - 1, dp, prev, b, a) * (i - prev[i])) % MOD;
	}
	else {
		if (a[i] >= b[j - 1])
			dp[i + 1][j] = res(i - 1, j, dp, prev, b, a);
		else
			dp[i + 1][j] = 0;
	}
	return dp[i + 1][j];
}

void solve() {
	int n, m;
	cin >> n >> m;
	vector<int> a(n);
	vector<int> b(m + 1, 1e9 + 228);
	for(int i = 0; i < n; ++i)
		cin >> a[i];
	for(int i = 0; i < m; ++i) 
		cin >> b[i];
	vector<int> prev(n, -1);
	{
		vector<int> s;
		for(int i = n - 1; i >= 0; --i) {
			while(s.size()) {
				if (a[s.back()] > a[i]) {
					prev[s.back()] = i;
					s.pop_back();
				}
				else
					break;
			}
			s.push_back(i);
		}
	}
	vector<map<int, int>> dp(n + 1);
	dp[0][0] = 1;
	for(int i = 1; i <= m; ++i)
		dp[0][i] = 0;
	for(int i = 1; i <= n; ++i)
		dp[i][0] = 0;
	cout << res(n - 1, m, dp, prev, b, a) << '\n';
}

signed main() {
	setup();
	// SOLVE;
	solve();
	

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


