const int MM = 1e6+5;
int n, x, y, a, i, j; ll cnt[2*MM], ps[2*MM], len, ans=1LL<<60;

int main(){
    for(scanf("%d%d%d",&n,&x,&y),i=1;i<=n;i++){
        scanf("%d",&a);
        ps[a]+=a; cnt[a]++;
    }
    for(i=1;i<2*MM;i++){
        ps[i]+=ps[i-1];
        cnt[i]+=cnt[i-1];
    }
    len=x/y;
    for(i=2;i<MM;i++){
        ll tmp=0; ll cur, nxt;
        for(j=0;j*i<MM;j++){
            cur=j*i, nxt=(j+1)*i;
            int lo=max(cur+1,nxt-len);
            ll c=cnt[nxt]-cnt[lo-1];
            tmp+=y*max(0LL,c*nxt-ps[nxt]+ps[lo-1])+x*(cnt[lo-1]-cnt[cur]);
        }
        ans=min(ans,tmp);
    }
    printf("%lld\n",ans);
    return 0;
}