template< typename F >
struct FixPoint : F {
  FixPoint(F &&f) : F(forward< F >(f)) {}

  template< typename... Args >
  decltype(auto) operator()(Args &&... args) const {
    return F::operator()(*this, forward< Args >(args)...);
  }
};

template< typename F >
inline decltype(auto) MFP(F &&f) {
  return FixPoint< F >{forward< F >(f)};
}

template< int mod >
struct ModInt {
  int x;

  ModInt() : x(0) {}

  ModInt(int64_t y) : x(y >= 0 ? y % mod : (mod - (-y) % mod) % mod) {}

  ModInt &operator+=(const ModInt &p) {
    if((x += p.x) >= mod) x -= mod;
    return *this;
  }

  ModInt &operator-=(const ModInt &p) {
    if((x += mod - p.x) >= mod) x -= mod;
    return *this;
  }

  ModInt &operator*=(const ModInt &p) {
    x = (int) (1LL * x * p.x % mod);
    return *this;
  }

  ModInt &operator/=(const ModInt &p) {
    *this *= p.inverse();
    return *this;
  }

  ModInt operator-() const { return ModInt(-x); }

  ModInt operator+(const ModInt &p) const { return ModInt(*this) += p; }

  ModInt operator-(const ModInt &p) const { return ModInt(*this) -= p; }

  ModInt operator*(const ModInt &p) const { return ModInt(*this) *= p; }

  ModInt operator/(const ModInt &p) const { return ModInt(*this) /= p; }

  bool operator==(const ModInt &p) const { return x == p.x; }

  bool operator!=(const ModInt &p) const { return x != p.x; }

  ModInt inverse() const {
    int a = x, b = mod, u = 1, v = 0, t;
    while(b > 0) {
      t = a / b;
      swap(a -= t * b, b);
      swap(u -= t * v, v);
    }
    return ModInt(u);
  }

  ModInt pow(int64_t n) const {
    ModInt ret(1), mul(x);
    while(n > 0) {
      if(n & 1) ret *= mul;
      mul *= mul;
      n >>= 1;
    }
    return ret;
  }

  friend ostream &operator<<(ostream &os, const ModInt &p) {
    return os << p.x;
  }

  friend istream &operator>>(istream &is, ModInt &a) {
    int64_t t;
    is >> t;
    a = ModInt< mod >(t);
    return (is);
  }

  static int get_mod() { return mod; }
};

using modint = ModInt< mod >;


const int MAX_N = 2000000;

int main() {
  int N, K;
  cin >> N >> K;

  vector< vector< int > > U(MAX_N + 1);
  vector< modint > po(MAX_N + 1);
  for(int i = 1; i <= MAX_N; i++) {
    po[i] = modint(i).pow(N);
    if(i >= 2 && U[i].empty()) {
      for(int j = i; j <= MAX_N; j += i) {
        U[j].emplace_back(i);
      }
    }
  }

  modint ret = 0;
  vector< modint > dp(K + 1);
  for(int i = 1; i <= K; i++) {
    dp[i] = dp[i - 1] + po[i] - po[i - 1];
    for(int j = 1; j < (1 << U[i].size()); j++) {
      int w = 1;
      for(int k = 0; k < U[i].size(); k++) {
        if((j >> k) & 1) w *= U[i][k];
      }
      if(__builtin_parity(j)) dp[i] -= po[i / w] - po[i / w - 1];
      else dp[i] += po[i / w] - po[i / w - 1];
    }
    ret += i ^ dp[i].x;
  }
  cout << ret << "\n";
}