const int N = 1e5 + 5;

int n;

int dp[N][2][2];

void test() {
    int t;
    cin >> t;
    string s[2];
    cin >> s[0] >> s[1];
    for (int i = 0; i <= t + 1; ++i) {
        dp[i][0][0] = dp[i][1][0] = dp[i][0][1] = dp[i][1][1] = -1e9;
    }
    dp[0][0][0] = 0;
    for (int i = 0; i < t; ++i) {
        int have0 = s[0][i] == '0' || s[1][i] == '0';
        int have1 = s[0][i] == '1' || s[1][i] == '1';
        for (int v0 = 2; v0--;) {
            for (int v1 = 2; v1--;) {
                chkmax(dp[i + 1][v0 | have0][v1 | have1], dp[i][v0][v1]);
            }
        }
        for (int v0 = 2; v0--;) {
            for (int v1 = 2; v1--;) {
                int mex = 0;
                if (v0) {
                    if (v1) {
                        mex = 2;
                    } else {
                        mex = 1;
                    }
                }
                chkmax(dp[i + 1][0][0], dp[i + 1][v0][v1] + mex);
            }
        }
    }
    cout << dp[t][0][0] << '\n';
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n;
    while (n--) {
        test();
    }
}