using namespace std;
template<typename T>
ostream& operator+(ostream& out, const vector<T> &vec){
    for(const auto &x : vec){
        out<<x<<" ";
    }
    out<<"\n";
    return out;
}
template<typename T>
ostream& operator*(ostream& out, const vector<T> &vec){
    for(const auto &x : vec){
        out+x;
    }
    return out;
}
template<typename T>
istream& operator>>(istream& in, vector<T> &vec){
    for(auto &x : vec){
        in>>x;
    }
    return in;
}
void solve(){
    int n;
    cin>>n;
    vector<int> exp(n);
    cin>>exp;
    sort(exp.begin(), exp.end());
    int ans=0;
    for(int i=0, mxingroup=-1, size=0;i<n;i++){
        mxingroup=max(mxingroup, exp[i]);
        ++size;
        if(size>=mxingroup){
            mxingroup=-1;
            ++ans;
            size=0;
            continue;
        }
    }
    cout<<ans<<"\n";
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
}