const int N = 1e5 + 10;
int flag[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int a, b, m, r;
	cin >> a >> b >> m >> r;
	MEM(flag, 0);
	for (int i = 1; i < N; i++)
	{
		if (flag[r])
		{
			cout << i - flag[r] << endl;
			return 0;
		}
		flag[r] = i;
		r = (a*r + b) % m;
	}
	return 0;
}