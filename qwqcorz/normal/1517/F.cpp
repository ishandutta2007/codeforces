#include<bits/stdc++.h>
using namespace std;
const int N=305;
const int p=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
void add(int &x,int y){x+=y;if (x>=p) x-=p;}
struct edge
{
	int to,nxt;
}e[N*2];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
int r,h[N],f[N][N],g[N][N],tf[N],tg[N];
void dfs(int now,int father)
{
	h[now]=1;
	f[now][0]=g[now][0]=(p+1)/2;
	for (int t=head[now];t;t=e[t].nxt)
	{
		int to=e[t].to;
		if (to==father) continue;
		dfs(to,now);
		for (int i=0;i<=h[now];i++)
		for (int j=0;j<=h[to];j++)
		{
			add(tf[min(i,j+1)],1LL*f[now][i]*f[to][j]%p);
			add(tg[max(i,j+1)],1LL*g[now][i]*g[to][j]%p);
			if (i+j+1<=r)
			{
				add(tf[i],1LL*f[now][i]*g[to][j]%p);
				add(tf[j+1],1LL*g[now][i]*f[to][j]%p);
			}
			else
			{
				add(tg[i],1LL*g[now][i]*f[to][j]%p);
				add(tg[j+1],1LL*f[now][i]*g[to][j]%p);
			}
		}
		h[now]=max(h[now],h[to]+1);
		for (int i=0;i<=h[now];i++) f[now][i]=tf[i],g[now][i]=tg[i],tf[i]=tg[i]=0;
	}
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),ans=n;
	for (int i=1;i<n;i++)
	{
		int u=read(),v=read();
		add_edge(u,v);
		add_edge(v,u);
	}
	for (r=0;r<=n;r++)
	{
		memset(f,0,sizeof(f));
		memset(g,0,sizeof(g));
		dfs(1,0);
		for (int i=0;i<=r;i++) ans=(ans+p-f[1][i])%p;
	}
	print(ans);
	
	return 0;
}
