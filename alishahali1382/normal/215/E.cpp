const int MAXN = 110, LOG=61;

ll l, r, k, u, v, x, y, t;
ll M[MAXN];

ll f(ll n){
	if (!n) return 0;
	ll ans=0;
	for (ll k=1; k<LOG; k++) for (ll t=2; k*t<LOG; t++){
		ll mn=(1<<(k-1)), mx=inf;
		ll tmp=0;
		for (int i=0; i<t; i++) tmp|=1ll<<(i*k);
		//for (ll tmp=n, x=0; x<t; tmp>>=k, x++) mx=min(mx, ((1ll<<k)-1)&tmp);
		//if (n>=(1ll<<(k*t))) mx=(1ll<<k)-1;
		mx=min(n/tmp, (1ll<<k)-1);
		
		ans+=max(0ll, mx-mn+1)*M[t];
	}
	return ans;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	for (int i=2; i<LOG; i++){
		for (int j=2; j<i; j++) if (i%j==0){
			M[i]=-M[i/j];
			break ;
		}
		if (!M[i]) M[i]=1;
	}
	for (int i=2; i*i<LOG; i++) for (int j=i*i; j<LOG; j+=i*i) M[j]=0;
	
	cin>>l>>r;
	cout<<f(r)-f(l-1)<<'\n';
	
	return 0;
}
