void run_case() {
    string S;
    cin >> S;
    int N = int(S.size());
    bool success = false;

    for (int split = 0; split <= N; split++) {
        vector<int> remove;

        for (int i = 0; i < split; i++)
            if (S[i] == '1')
                remove.push_back(i);

        for (int i = split; i < N; i++)
            if (S[i] == '0')
                remove.push_back(i);

        bool good = true;

        for (int i = 1; i < int(remove.size()); i++)
            if (remove[i] == remove[i - 1] + 1)
                good = false;

        success = success || good;
    }

    cout << (success ? "YES" : "NO") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}