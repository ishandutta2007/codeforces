signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        int n, k;
        cin >> n >> k;
        vector<int> a(n);
        for (int &el : a)
            cin >> el;
        vector<int> pref(n, 0);
        for (int i = 1; i < n; i++) {
            pref[i] = pref[i - 1];
            if (a[i] > a[i - 1] && a[i] > a[i + 1])
                pref[i]++;
        }
        int ans = -1, L = -1;
        for (int i = 0; i + k - 1 < n; i++) {
            int cur_ans = pref[i + k - 2] - pref[i];
            if (cur_ans > ans)
                ans = cur_ans, L = i;
        }
        cout << ans + 1 << ' ' << L + 1 << '\n';
    }
}