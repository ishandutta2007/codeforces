typedef long long ll;
typedef unsigned long long ull;
ll gcd(ll a,ll b) { return b==0?a:gcd(b,a%b); }



void run() {
	int x; scanf("%d",&x);
	int ret=1;
	for(int i=1;;++i) {
		ll cur=((1LL<<i)-1)<<(i-1);
		if(cur>x) break; else if(x%cur==0&&cur>ret) ret=cur;
	}
	printf("%d\n",ret);

}

