#include <bits/stdc++.h>

using namespace std;

int a[200];

int main(){
    ios_base::sync_with_stdio(false);
    int n;
    cin >> n;
    int a[n + 2];
    for (int i = 1; i <= n; i++){
        cin >> a[i];
    }
    for (int i = 2; i < n; i++){
        if (a[i] % 2 != a[i + 1] % 2 && a[i] % 2 != a[i - 1] % 2)
            return cout << i << endl, 0;
    }
    if (a[1] % 2 != a[2] % 2 && a[1] % 2 != a[3] % 2)
        return cout << 1 << endl, 0;
    else
        cout << n << endl;
}