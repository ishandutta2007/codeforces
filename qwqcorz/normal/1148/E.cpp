#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=3e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct node {int u,v,w;};
vector<node>ans;
int st[N],top=0;
int a[N],s[N],t[N],p[N],d[N];
bool cmp(int x,int y) {return a[x]<a[y];}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),sum=0;
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=1;i<=n;i++) p[i]=i;
	sort(p+1,p+1+n,cmp);
	for (int i=1;i<=n;i++) s[i]=a[p[i]];
	for (int i=1;i<=n;i++) t[i]=read();
	sort(t+1,t+1+n);
	for (int i=1;i<=n;i++) d[i]=t[i]-s[i];
	for (int i=1;i<=n;i++) if ((sum+=d[i])<0) return puts("NO")*0;
	if (sum) return puts("NO")*0;
	for (int i=1;i<=n;i++)
	if (d[i]>0) st[++top]=i;
	else if (d[i]<0)
	{
		int now=-d[i];
		while (now)
		{
			int tmp=min(d[st[top]],now);
			ans.push_back((node){i,st[top],tmp});
			d[st[top]]-=tmp;
			now-=tmp;
			if (d[st[top]]==0) top--;
		}
	}
	puts("YES");
	print(ans.size());
	for (node i:ans) print(p[i.v],' '),print(p[i.u],' '),print(i.w);
	
	return 0;
}
