#include <bits/stdc++.h>
using namespace std;

int n;
int a[200005];

void solve(){
	scanf("%d",&n);
	for(int i=1;i<=n;i++){
		scanf("%d",a+i);
	}
	int mx=-0x7f7f7f7f,pos=-1,l=-1,r;
	for(int i=1;i<=n;i++){
		if(a[i]+mx>=0){
			l=pos;
			r=i;
			break;
		}
		if(-a[i]-1>mx-2){
			mx=-a[i]-2;
			pos=i;
		}
	}
	if(~l){
		printf("YES\n%d %d\n",l,r);
		return;
	}
	reverse(a+1,a+n+1);
	mx=-0x7f7f7f7f,pos=-1,l=-1;
	for(int i=1;i<=n;i++){
		if(a[i]+mx>=0){
			l=pos;
			r=i;
			break;
		}
		if(-a[i]-1>mx-2){
			mx=-a[i]-2;
			pos=i;
		}
	}
	if(~l){
		printf("YES\n%d %d\n",n-r+1,n-l+1);
		return;
	}
	printf("NO\n");
}

int main(){
	int t;
	scanf("%d",&t);
	while(t--){
		solve();
	}
	return 0;
}