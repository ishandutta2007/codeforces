int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=1; i<=n; i++){
		cin>>A[i];
		ans=0;
		int sum=m-A[i];
		for (int j=1; j<=100; j++) if (cnt[j]){
			if (sum>=cnt[j]*j){
				sum-=cnt[j]*j;
				ans+=cnt[j];
				continue ;
			}
			int tmp=max(0, sum/j-5);
			while ((tmp+1)*j<=sum) tmp++;
			ans+=tmp;
			sum-=tmp*j;
			break ;
		}
		cout<<i-1-ans<<' ';
		cnt[A[i]]++;
	}
	
	
	return 0;
}