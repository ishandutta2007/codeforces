int n, k;
long long a[52];
bool used[52][52];

bool check(long long mask) {
	for (int i = 0; i <= n; ++i) {
		for (int j = 0; j <= k; ++j) {
			used[i][j] = false;
		}
	}
	queue<pair<int, int>> que;
	que.push({0, 0});
	while (que.size()) {
		auto v = que.front();
		que.pop();
		if (v.x == n) continue;
		long long sum = a[v.x];
		for (int R = v.x + 1; R <= n; ++R) {
			if ((sum & mask) == mask) {
				pair<int, int> u = {R, v.y + 1};
				if (!used[u.x][u.y]) {
					que.push(u);
					used[u.x][u.y] = true;
				}
			}
			sum += a[R];
		}
	}
	return used[n][k];
}

int main() {
	ios::sync_with_stdio(0);
	cin >> n >> k;
	for (int i = 0; i < n; ++i) {
		cin >> a[i];
	}
	long long ans = 0;
	for (int i = 61; i--;) {
		ans += (1LL << i);
		if (!check(ans)) {
			ans -= (1LL << i);
		}
	}
	cout << ans << '\n';
}