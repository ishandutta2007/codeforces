struct point
{
	double x, y;
	point(){}
	point(double _x, double _y) {
		x = _x;
		y = _y;
	}	
};

double dist(point a, point b) {
	return sqrt((a.x - b.x) * (a.x - b.x) + (a.y - b.y) * (a.y - b.y));
}


int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	//freopen("input.in", "r", stdin);
	double fa, fb, fc;
	cin >> fa >> fb >> fc;
	point a, b;
	cin >> a.x >> a.y >> b.x >> b.y;
	double res = abs(a.x - b.x) + abs(a.y - b.y);
	cout.precision(15);
	if (abs(fa) < 1e-9 || abs(fb) < 1e-9) {
		cout << fixed << res << '\n';
		return 0;
	}
	//fa * a.x + fb * y + fc = 0
	double yy = (-fc - fa * a.x) / fb;
	vector<point> st, st1;
	st.pb(point(a.x, yy));
	double xx = (-fc - fb * a.y) / fa;
	st.pb(point(xx, a.y));
	yy = (-fc - fa * b.x) / fb;
	st1.pb(point(b.x, yy));
	xx = (-fc - fb * b.y) / fa;
	st1.pb(point(xx, b.y));
	for (auto x: st) {
		for (auto y: st1) {
			chkmin(res, dist(x, a) + dist(x, y) + dist(y, b));
		}
	}
	cout << fixed << res << endl;
	return 0;
}