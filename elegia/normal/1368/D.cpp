  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int n;
  cin >> n;
  vector<int> a(n);
  vector<int> cnt(20);
  for (int rep = 0; rep < n; ++rep) {
    int x;
    cin >> x;
    for (int i = 0; i < 20; ++i) {
      cnt[i] += x & 1;
      x >>= 1;
    }
  }
  for (int i = 0; i < 20; ++i) {
    for (int j = 0; j < cnt[i]; ++j)
      a[j] |= 1 << i;
  }
  ll ans = 0;
  for (int x : a)
    ans += x * (ll)x;
  cout << ans << '\n';

