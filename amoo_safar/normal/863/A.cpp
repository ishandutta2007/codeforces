vector<ll> a, b;

int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	ll n;
	cin >> n;
	while(n % 10 == 0) n /= 10;
	ll m = n;
	while(m > 0){
		a.pb(m % 10);
		m /= 10;
	}
	m = n;
	while(m > 0){
		b.pb(m % 10);
		m /= 10;
	}
	reverse(all(b));
	for(int i = 0;i<a.size();i++) if(a[i] != b[i]) return cout << "NO", 0;
	cout << "YES";
	return 0;
}


