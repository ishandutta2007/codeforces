const int N = 1.5e6 + 5;

int n, m;
int cnt[N];
int lst = 0;
vector<int> a[N];

int dp[2][7][7];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
    //read("input")
    // cin >> n;
   	cin >> n >> m;
   	for (int i = 0; i < n; ++i) {
   		int a;
   		cin >> a;
   		cnt[a]++;
   	}
   	int ans = 0;
   	for (int i = 0; i <= m; ++i) {
   		if (cnt[i] > 6) {
   			ans += (cnt[i] - 6) / 3;
   			cnt[i] = (cnt[i] % 3 + 6);
   		}
   	}
   	memset(dp[0], -1, sizeof(dp[1]));
   	dp[0][0][0] = ans;
   	for (int i = 0; i < N; ++i) {
   		memset(dp[1], -1, sizeof(dp[1]));
   		for (int j = cnt[i]; j >= 0; --j) {
   			int k = (cnt[i] - j) / 3;
   			for (int t = 0; t <= 6; ++t) {
   				for (int t1 = t; t1 <= j; ++t1) {
   					if (dp[0][t][t1] == -1) continue;
   					chkmax(dp[1][t1 - t][j - t], dp[0][t][t1] + t + k);
   				}
   			}
   		}
   		swap(dp[0], dp[1]);
   	}
   	for (int i = 0; i <= 6; ++i) {
   		for (int j = 0; j <= 6; ++j) {
   			chkmax(ans, dp[0][i][j]);
   		}
   	}
   	cout << ans << '\n';
}