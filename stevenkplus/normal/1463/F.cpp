const ll infl = 1LL << 60;
const int inf = 1000 << 20;
// ============= Solution ============= //
int main() {
   int n, x, y;
   cin >> n >> x >> y;
   int k = 1 << 23;
   vector<int> dp(k, -inf);
   int msk = (1 << x) | (1 << y);
   vector<int> ans(x + y);
   dp[0] = 0;
   vector<int> vals(x + y);
   for (int i = 0; i < x + y; ++i) {
      vals[i] = n / (x + y) + (i < (n % (x + y)));
   }
   pp(vals);
   for (int i = 0; i < x + y; ++i) {
      vector<int> ndp(k, -inf);
      int val = 0;
      for (int j = 0; j < k; ++j) {
         int nj = (j << 1) & (k - 1);
         ndp[nj] = max(ndp[nj], dp[j]);
         int add = (nj & msk) ? 0 : vals[i];
         ndp[nj ^ 1] = max(ndp[nj ^ 1], dp[j] + add);
         val = max(val, dp[j]);
      }
      ans[i] = val;
      dp = ndp;
   }
   pp(ans);
   cout << ans.back() << "\n";
}

