int64_t sum_up_to(int64_t n) {
    return n * (n + 1) / 2;
}

void run_case() {
    int64_t X1, Y1, X2, Y2;
    cin >> X1 >> Y1 >> X2 >> Y2;
    int64_t X = X2 - X1;
    int64_t Y = Y2 - Y1;

    if (X > Y)
        swap(X, Y);

    int64_t answer = sum_up_to(X) + (Y - X) * X + sum_up_to(X - 1) + 1;
    cout << answer << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}