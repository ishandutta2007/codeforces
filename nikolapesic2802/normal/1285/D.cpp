const int N=2e5+5;
int solve(vector<int> a,int j){
    if(j==-1)
        return 0;
    vector<int> zero,one;
    for(auto p:a)
        if(p&(1<<j))
            one.pb(p);
        else
            zero.pb(p);
    if(zero.empty())
        return solve(one,j-1);
    if(one.empty())
        return solve(zero,j-1);
    return min(solve(one,j-1),solve(zero,j-1))+(1<<j);
}
