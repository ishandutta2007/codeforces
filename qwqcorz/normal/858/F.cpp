#include<bits/stdc++.h>
#define mk make_pair
#define fi first
#define se second
using namespace std;
const int N=2e5+5; 

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,nxt;
}e[N*2];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
vector<pair<pair<int,int>,int>>ans;
bool vis[N];
int deep[N];
bool dfs(int now,int father)
{
	vis[now]=1;
	deep[now]=deep[father]+1;
	vector<int>a(0);
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father) continue;
		if (vis[to]) deep[to]<deep[now]?a.push_back(to):void();
		else if (dfs(to,now)) a.push_back(to);
	}
	bool ret=!(a.size()&1);
	if (father) a.push_back(father);
	for (int i=0;i+1<(int)a.size();i+=2) ans.push_back(mk(mk(a[i],now),a[i+1]));
	return father?ret:!ret;
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read();
	for (int i=1;i<=m;i++)
	{
		int u=read(),v=read();
		add_edge(u,v);
		add_edge(v,u);
	}
	for (int i=1;i<=n;i++) if (!vis[i]) dfs(i,0);
	print(ans.size());
	for (auto i:ans) print(i.fi.fi,' '),print(i.fi.se,' '),print(i.se);
	
	return 0;
}
