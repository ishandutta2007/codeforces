int grs[] = {0, 1, 2, 1, 4, 3, 2, 1, 5, 6, 2, 1, 8, 7, 5, 9, 8, 7, 3, 4, 7, 4, 2, 1, 10, 9, 3, 6, 11, 12, 14};

bool f[100000];


bool solve(){
    int n;
    cin >> n;
    int c = 1;
    int x = (int)sqrt(n + 0.1);
    int g = 1;

    for(int i = 2; i <= x; ++i){
        if(f[i]){ continue; }
        int y = 0;
        for(LL j = i; j <= n; j *= i){
            ++y;
            ++c;
            if(j <= x){
                f[j] = true;
            }
        }
        g ^= grs[y];
    }
    
    g ^= (n - c) & 1;
    
    return g;
}

