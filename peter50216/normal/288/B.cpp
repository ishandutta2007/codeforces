int c=0;
int vis[8];
inline bool d2(int np){
    if(vis[np]==1)return 1;
    if(vis[np]==2)return 0;
    vis[np]=2;
    if(d2(to[np]))return vis[np]=1;
    return 0;
}
inline void dfs(int np){
    if(np==k){
        MS0(vis);
        vis[0]=1;
        for(int i=0;i<k;i++){
            if(!d2(i))return;
        }
        c++;
        return;
    }
    for(int i=0;i<k;i++){
        to[np]=i;
        dfs(np+1);
    }
}
