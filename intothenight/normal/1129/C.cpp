vector<int> z_function(deque<char> s){
	int n = int(s.size());
	vector<int> z(n);
	for (int i = 1, l = 0, r = 0; i < n; i ++){
		if(i <= r) z[i] = min(r - i + 1, z[i - l]);
		while(i + z[i] < n && s[z[i]] == s[i + z[i]]) z[i] ++;
		if(i + z[i] - 1 > r) l = i, r = i + z[i] - 1;
	}
	return z;
}

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	deque<char> s;
	int m;
	cin >> m;
	vector<vl> d(m + 1, vl(m + 1));
	for(int i = 0; i <= m; i ++) d[i][i] = 1;
	ll res = 0;
	const ll mod = 1e9 + 7;
	for(int j = 1; j <= m; j ++){
		char c;
		cin >> c;
		s.push_front(c);
		auto t = z_function(s);
		int l = *max_element(all(t));
		for(int i = 0; i < j; i ++){
			d[i][j] = d[i][j - 1];
			if(j > 1) d[i][j] += d[i][j - 2];
			if(j > 2) d[i][j] += d[i][j - 3];
			if(j > 3){
				auto temp = string({s[3], s[2], s[1], s[0]});
				if(temp != "0011" && temp != "0101" && temp != "1110" && temp != "1111"){
					d[i][j] += d[i][j - 4];
				}
			}
			d[i][j] %= mod;
			if(j - i > l){
				res += d[i][j];
			}
		}
		res %= mod;
		cout << res << "\n";
	}
	return 0;
}

