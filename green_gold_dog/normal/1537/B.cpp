#include <bits/stdc++.h>
typedef long long ll;

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t;
	cin >> t;
	for (ll _ = 0; _ < t; _++) {
		ll n, m, i, j;
		cin >> n >> m >> i >> j;
		cout << 1 << ' ' << 1 << ' ' << n << ' ' << m << '\n';
	}
}