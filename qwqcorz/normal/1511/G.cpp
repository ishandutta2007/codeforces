#include<bits/stdc++.h>
using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int g[N][20],f[N][20];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=read();
	for (int i=1;i<=n;i++) g[read()][0]^=1;
	for (int j=1;j<=18;j++)
	for (int i=1;i+(1<<j)-1<=m;i++)
	{
		g[i][j]=g[i][j-1]^g[i+(1<<(j-1))][j-1];
		f[i][j]=f[i][j-1]^f[i+(1<<(j-1))][j-1]^(g[i+(1<<(j-1))][j-1]<<(j-1));
	}
	int q=read();
	while (q--)
	{
		int l=read(),r=read(),ans=0,now=0;
		for (int i=19;i>=0;i--)
		if (l+(1<<i)-1<=r)
		{
			ans^=f[l][i]^(g[l][i]*now);
			now+=1<<i;
			l+=1<<i;
		}
		putchar("AB"[ans==0]);
	}
	
	return 0;
}
