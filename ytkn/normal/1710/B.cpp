template<typename T>
class Compress{
    public:
    vector<T> data;
    int offset;
    Compress(vector<T> data_, int offset=0): offset(offset){
        data = data_;
        sort(begin(data), end(data));
        data.erase(unique(begin(data), end(data)), end(data));
    };
    int operator[](T x) {
        auto p = lower_bound(data.begin(), data.end(), x);
        assert(x == *p);
        return offset+(p-data.begin());
	}
    T inv(int x){
        return data[x-offset];
    }
    int size(){
        return data.size();
    }
};

const ll inf = 1e18;

ll e(){
    return -inf;
}

ll op(ll x, ll y){
    return max(x, y);
}

ll id(){
    return 0;
}

using SegMax = atcoder::segtree<ll, op, e>;

void solve(){
    int n; ll m; cin >> n >> m;
    vector<ll> x(n), p(n);
    for(int i = 0; i < n; i++){
        cin >> x[i] >> p[i];
    }
    vector<ll> v;
    for(int i = 0; i < n; i++){
        v.push_back(x[i]);
        v.push_back(x[i]+p[i]);
        v.push_back(x[i]-p[i]);
    }
    auto cp = Compress<ll>(v);
    int sz = cp.size();
    vector<ll> imos_a(sz+1);
    vector<ll> imos_b(sz+1);
    auto add_line = [&](int l, int r, ll a, ll b){
        imos_a[l] += a;
        imos_a[r] -= a;
        imos_b[l] += b;
        imos_b[r] -= b;
    };
    
    for(int i = 0; i < n; i++){
        int l = cp[x[i]-p[i]];
        int c = cp[x[i]];
        int r = cp[x[i]+p[i]];
        add_line(l, c, 1, -(x[i]-p[i]));
        add_line(c, r, -1, x[i]+p[i]);
    }
    for(int i = 1; i <= sz; i++){
        imos_a[i] += imos_a[i-1];
        imos_b[i] += imos_b[i-1];
    }
    vector<ll> rain(sz);
    for(int i = 0; i < sz; i++){
        ll a = imos_a[i], b = imos_b[i];
        ll p = cp.data[i];
        rain[i] = a*p+b;
    }
    SegMax seg_pos(sz);
    SegMax seg_neg(sz);
    SegMax seg_rain(sz);
    string ans;
    // print_vector(rain);
    for(int i = 0; i < sz; i++){
        ll p = cp.data[i];
        seg_pos.set(i, rain[i]+p);
        seg_neg.set(i, rain[i]-p);
        seg_rain.set(i, rain[i]);
    }
    for(int i = 0; i < n; i++){
        int l = cp[x[i]-p[i]];
        int c = cp[x[i]];
        int r = cp[x[i]+p[i]];
        // -X+(x[i]-p[i]) 
        ll max_l = seg_neg.prod(l, c)+(x[i]-p[i]);
        // X-(x[i]+p[i]) 
        ll max_r = seg_pos.prod(c, r)-(x[i]+p[i]);
        ll mx = max(seg_rain.prod(0, l), seg_rain.prod(r, sz));
        if(max({max_l, max_r, mx}) <= m) ans += '1';
        else ans += '0';
    }
    cout << ans << '\n';
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}