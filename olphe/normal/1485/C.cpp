void Solve() {
	cin >> N >> M;
	long long int ans = 0;
	for (int i = 3; i <= 40000; i++) {
		if (M + 1 < i)break;
		ans += min(i - 2LL, N / i);
	}
	for (int i = 40001; i <= M + 1; i++) {
		if (N < i)break;
		ans += (N / i)*min(M + 1, (N / (N / i)));
		ans -= (N / i)*(i - 1);
		i = N / (N / i);
	}
	cout << ans << endl;
}

