const int K = 228;

void solve() {
	int n, m;
	cin >> n >> m;
	vector<string> a(n);
	for(int i = 0; i < n; ++i)
		cin >> a[i];
	vector<pair<string, string>> ans;
	vector<int> used(n);

	for(int i = 0; i < n; ++i) {
		string tmp = a[i];
		reverse(all(tmp));
		for(int j = 0; j < i; ++j) {
			if (!used[j] && tmp == a[j]) {
				ans.emplace_back(a[i], a[j]);
				used[i] = 1, used[j] = 1;
				break;
			}
		}
	}
	string mx;
	for(int i = 0; i < n; ++i) {
		string tmp = a[i];
		reverse(all(tmp));
		if (tmp == a[i] && !used[i]) {
			if (mx.size() < tmp.size())
				mx = tmp;
		}
	}

	string res;
	for(int i = 0; i < ans.size(); ++i)
		res += ans[i].first;
	res += mx;
	if (ans.size() != 0) {
		for(int i = ans.size() - 1; i >= 0; --i) 
			res += ans[i].second;
	}
	cout << res.size() << '\n';
	cout << res;
}

signed main() {
	setup();
	// SOLVE;
	solve();
	

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


