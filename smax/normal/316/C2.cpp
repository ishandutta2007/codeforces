const int dr[] = {-1, 0, 1, 0};
const int dc[] = {0, 1, 0, -1};

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    cin >> n >> m;
    vector<vector<int>> a(n, vector<int>(m));
    for (int i=0; i<n; i++)
        for (int j=0; j<m; j++)
            cin >> a[i][j];

    int s = n * m, t = n * m + 1;
    mcf_graph<int, int> mcmf(n * m + 2);
    for (int r=0; r<n; r++)
        for (int c=0; c<m; c++) {
            if ((r + c) % 2 == 0) {
                mcmf.add_edge(s, r * m + c, 1, 0);
                for (int i=0; i<4; i++) {
                    int nr = r + dr[i], nc = c + dc[i];
                    if (0 <= nr && nr < n && 0 <= nc && nc < m)
                        mcmf.add_edge(r * m + c, nr * m + nc, 1, a[r][c] != a[nr][nc]);
                }
            } else {
                mcmf.add_edge(r * m + c, t, 1, 0);
            }
        }
    cout << mcmf.flow(s, t).second << "\n";

    return 0;
}