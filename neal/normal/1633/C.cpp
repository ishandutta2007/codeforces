int64_t ceil_div(int64_t a, int64_t b) {
    return a / b + ((a ^ b) > 0 && a % b != 0);
}


bool win(int64_t HC, int64_t DC, int64_t HM, int64_t DM) {
    int64_t our_attacks = ceil_div(HM, DC);
    int64_t their_attacks = ceil_div(HC, DM);
    return our_attacks <= their_attacks;
}

void run_case() {
    int64_t HC, DC, HM, DM, K, W, A;
    cin >> HC >> DC >> HM >> DM >> K >> W >> A;
    bool ans = false;

    for (int64_t w = 0; w <= K; w++) {
        int64_t a = K - w;
        ans = ans || win(HC + a * A, DC + w * W, HM, DM);
    }

    cout << (ans ? "YES" : "NO") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}