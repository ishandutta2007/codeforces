	void solve()
	{
		int n = ni();
		char[][] b = new char[n][];
		for(int i = 0;i < n;i++){
			b[i] = ns(n);
		}
		int[] rplus = new int[n];
		int[] cplus = new int[n];
		int z = 0;
		for(int i = n-1;i >= 1;i--){
			for(int j = 0;j <= n-1-i;j++){
				if((b[i+j][j]-'0' + rplus[i+j] + cplus[j]) % 2 == 1){
					rplus[i+j]++;
					cplus[j]++;
					z++;
				}
			}
		}
		
		int[] rplus2 = new int[n];
		int[] cplus2 = new int[n];
		for(int i = n-1;i >= 1;i--){
			for(int j = 0;j <= n-1-i;j++){
				if((b[j][i+j]-'0' + rplus2[j] + cplus2[i+j]) % 2 == 1){
					rplus2[j]++;
					cplus2[i+j]++;
					z++;
				}
			}
		}
		for(int i = 0;i < n;i++){
			if((b[i][i]-'0' + rplus[i] + cplus[i] + rplus2[i] + cplus2[i]) % 2 == 1){
				z++;
			}
		}
		out.println(z);
	}
	
