constexpr int mod = 1e9 + 7;
// constexpr int mod = (119 << 23) + 1;
using Zp = Z_p<integral_constant<decay<decltype(mod)>::type, mod>>;

template<typename T> vector<typename Z_p<T>::Type> Z_p<T>::mod_inv;
template<typename T = integral_constant<decay<decltype(mod)>::type, mod>>
void precalc_inverse(size_t SZ){
	auto &inv = Z_p<T>::mod_inv;
	if(inv.empty()) inv.assign(2, 1);
	for(; inv.size() <= SZ; ) inv.push_back((mod - 1LL * mod / (int)inv.size() * inv[mod % (int)inv.size()]) % mod);
}
template<typename T>
vector<T> precalc_power(T base, size_t SZ){
	vector<T> res(SZ + 1, 1);
	for(int i = 1; i <= SZ; ++ i) res[i] = res[i - 1] * base;
	return res;
}

struct disjoint_set{
	vector<int> p, loop;
	vector<vector<int>> edges;
	disjoint_set(int n): p(n, -1), loop(n, -1), edges(n){ }
	bool share(int a, int b){ return root(a) == root(b); }
	int sz(int u){ return -p[root(u)]; }
	int root(int u){ return p[u] < 0 ? u : p[u] = root(p[u]); } // O(alpha(n))
	bool merge(int u, int v, int i){
		u = root(u), v = root(v);
		if(u == v || ~loop[u] && ~loop[v]) return false;
		if(p[u] > p[v]) swap(u, v);
		p[u] += p[v], p[v] = u;
		edges[u].push_back(i);
		for(auto i: edges[v]){
			edges[u].push_back(i);
		}
		if(!~loop[u]){
			loop[u] = loop[v];
		}
		return true;
	}
	void setloop(int u, int i){
		u = root(u);
		if(!~loop[u]){
			loop[u] = i;
		}
	}
};

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, m;
	cin >> n >> m;
	disjoint_set dsu(m);
	for(auto i = 0; i < n; ++ i){
		int k;
		cin >> k;
		if(k == 1){
			int u;
			cin >> u, -- u;
			dsu.setloop(u, i);
		}
		else{
			int u, v;
			cin >> u >> v, -- u, -- v;
			dsu.merge(u, v, i);
		}
	}
	Zp res = 1;
	vector<int> S;
	for(auto u = 0; u < m; ++ u){
		if(dsu.root(u) == u){
			for(auto i: dsu.edges[u]){
				S.push_back(i);
			}
			if(~dsu.loop[u]){
				S.push_back(dsu.loop[u]);
				res *= Zp(2) ^ dsu.sz(u);
			}
			else{
				res *= Zp(2) ^ dsu.sz(u) - 1;
			}
		}
	}
	sort(S.begin(), S.end());
	cout << res << " " << (int)S.size() << "\n";
	for(auto i: S){
		cout << i + 1 << " ";
	}
	cout << "\n";
	return 0;
}

