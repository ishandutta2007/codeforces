void test_case(int test) {
    int n;
    cin >> n;
    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; ++i) {
        int p;
        cin >> p;
        g[p - 1].pb(i + 1);
    }
    vector<ll> l(n), r(n);
    for (int i = 0; i < n; ++i) {
        cin >> l[i] >> r[i];
    }

    int ans = 0;
    vector<ll> val(n);
    y_combinator([&](auto dfs, int v, int p) -> void {
        ll sm = 0;
        for (int k : g[v]) {
            if (k == p) continue;
            dfs(k, v);
            sm += val[k];
        }
        if (sm >= l[v]) {
            val[v] = min(sm, r[v]);
        } else {
            val[v] = r[v];
            ++ans;
        }
    })(0, -1);
    cout << ans << '\n';
}

