#include <bits/stdc++.h>
using namespace std;

char nm[][20]={"Gryffindor","Hufflepuff","Ravenclaw","Slytherin"};

struct jtakioi{
	int x[4];
	jtakioi(int a,int b,int c,int d){
		x[0]=a;
		x[1]=b;
		x[2]=c;
		x[3]=d;
	}
	bool operator<(const jtakioi& jt)const{
		for(int i=0;i<4;i++){
			if(x[i]!=jt.x[i]){
				return x[i]<jt.x[i];
			}
		}
		return 0;
	}
	int sml(){
		return min(min(x[0],x[1]),min(x[2],x[3]));
	}
	jtakioi upd(int f){
		jtakioi xx=*this;
		xx.x[f]++;
		return xx;
	}
};

int getnum(char ch){
	return ch=='G'?0:(ch=='H'?1:(ch=='R'?2:3));
}

int n;
char s[10005];
set<jtakioi> ss;
bool can[4];

int main(){
	scanf("%d",&n);
	scanf("%s",s);
	ss.insert(jtakioi(0,0,0,0));
	for(int i=0;i<n;i++){
		set<jtakioi> newss;
		if(s[i]=='?'){
			for(set<jtakioi>::iterator it=ss.begin();it!=ss.end();it++){
				jtakioi ff=*it;
				int sm=ff.sml();
				for(int j=0;j<4;j++){
					if(ff.x[j]==sm){
						newss.insert(ff.upd(j));
					}
				}
			}
		}else{
			for(set<jtakioi>::iterator it=ss.begin();it!=ss.end();it++){
				jtakioi ff=*it;
				newss.insert(ff.upd(getnum(s[i])));
			}
		}
		ss=newss;
	}
	for(set<jtakioi>::iterator it=ss.begin();it!=ss.end();it++){
		jtakioi ff=*it;
		int sm=ff.sml();
		for(int j=0;j<4;j++){
			if(ff.x[j]==sm){
				can[j]=1;
			}
		}
	}
	for(int i=0;i<4;i++){
		if(can[i]){
			puts(nm[i]);
		}
	}
	return 0;
}