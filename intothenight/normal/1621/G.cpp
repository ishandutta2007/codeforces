template<class T>
struct fenwick_tree{
	int n;
	vector<T> data;
	fenwick_tree(){ }
	// O(n)
	fenwick_tree(int n): n(n), data(n){ }
	// O(n)
	fenwick_tree(int n, T init): fenwick_tree(vector<T>(n, init)){ }
	// O(n)
	fenwick_tree(const vector<T> &v): n((int)v.size()), data(v){
		for(auto i = 1; i <= n; ++ i) if(i + (i & -i) <= n) data[i + (i & -i) - 1] += data[i - 1];
	}
	fenwick_tree(const fenwick_tree &otr): n(otr.n), data(otr.data){
	}
	// O(log n)
	void update(int p, T x){
		assert(0 <= p && p < n);
		for(++ p; p <= n; p += p & -p) data[p - 1] += x;
	}
	// O(log n)
	T query(int r) const{
		T s{};
		for(; r > 0; r -= r & -r) s += data[r - 1];
		return s;
	}
	// O(log n)
	T query(int l, int r) const{
		assert(0 <= l && l <= r && r <= n);
		return query(r) - query(l);
	}
	// pred(sum[0, r)) is T, T, ..., T, F, F, ..., F
	// Returns max r with T
	// O(log n)
	int max_pref(auto pred) const{
		assert(pred({}));
		int p = 0;
		T sum{};
		for(auto pw = 1 << __lg(n + 1); pw; pw >>= 1) if(p + pw <= n && f(sum + data[p + pw - 1])){
			sum += data[p + pw - 1];
			p += pw;
		}
		return p;
	}
	template<class output_stream>
	friend output_stream &operator<<(output_stream &out, const fenwick_tree<T> &fw){
		out << "[";
		for(auto i = 0; i < fw.n; ++ i){
			out << fw.query(i, i + 1);
			if(i != fw.n - 1) out << ", ";
		}
		return out << ']';
	}
};

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	auto __solve_tc = [&](auto __tc_num)->int{
		int n;
		cin >> n;
		vector<int> a(n);
		for(auto i = 0; i < n; ++ i){
			cin >> a[i];
		}
		{
			auto cmpr = a;
			ranges::sort(cmpr);
			cmpr.erase(unique(cmpr.begin(), cmpr.end()), cmpr.end());
			for(auto &x: a){
				x = ranges::lower_bound(cmpr, x) - cmpr.begin();
			}
		}
		vector<modular> dp_front(n), dp_back(n);
		{
			fenwick_tree<modular> fw(n);
			for(auto i = 0; i < n; ++ i){
				fw.update(a[i], dp_front[i] = fw.query(a[i]) + 1);
			}
			fw = {n};
			for(auto i = n - 1; i >= 0; -- i){
				fw.update(a[i], dp_back[i] = fw.query(a[i] + 1, n) + 1);
			}
		}
		vector<int> max_suff;
		for(auto i = n - 1; i >= 0; -- i){
			if(max_suff.empty() || a[max_suff.back()] < a[i]){
				max_suff.push_back(i);
			}
		}
		int m = (int)max_suff.size();
		vector<vector<int>> g(m);
		for(auto i = 0; i < n; ++ i){
			int x = a[i];
			int j = ranges::partition_point(max_suff, [&](int i){ return a[i] <= x;  }) - max_suff.begin();
			if(j < m && i < max_suff[j]){
				g[j].push_back(i);
			}
		}
		modular res = 0;
		for(auto j = 0; j < m; ++ j){
			vector<int> b;
			for(auto i: g[j]){
				b.push_back(a[i]);
			}
			b.push_back(a[max_suff[j]]);
			int k = (int)b.size();
			{
				auto cmpr = b;
				ranges::sort(cmpr);
				cmpr.erase(unique(cmpr.begin(), cmpr.end()), cmpr.end());
				for(auto &x: b){
					x = ranges::lower_bound(cmpr, x) - begin(cmpr);
				}
			}
			vector<modular> dp_bad(k);
			{
				fenwick_tree<modular> fw(k);
				fw.update(b[k - 1], dp_bad[k - 1] = 1);
				for(auto t = k - 2; t >= 0; -- t){
					fw.update(b[t], dp_bad[t] = fw.query(b[t] + 1, k));
				}
			}
			for(auto t = 0; t < k - 1; ++ t){
				int i = g[j][t];
				res += dp_front[i] * (dp_back[i] - dp_bad[t]);
			}
		}
		cout << res << "\n";
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

