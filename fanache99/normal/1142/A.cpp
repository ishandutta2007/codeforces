    small = 1LL * n * k;
    big = 0;
    doCase(n, k, a % k, b % k);
    doCase(n, k, (k - a) % k, b % k);
    doCase(n, k, a % k, (k - b) % k);
    doCase(n, k, (k - a) % k, (k - b) % k);
    cout << small << " " << big << "\n";
