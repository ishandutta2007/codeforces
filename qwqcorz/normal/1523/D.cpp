#include<bits/stdc++.h>
#define ull unsigned long long
#define ll long long
using namespace std;
const int N=2e5+5;

int get()
{
	char c=getchar();
	while (c<'0'||'9'<c) c=getchar();
	return c-'0';
}
int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
ull rnd()
{
	ull ret=rand();
	ret=(ret<<16)^rand();
	return ret;
}
ll ans=0,a[N],w[N];
int n,m,p,sum[N],popcount[N];
void solve(ll x)
{
	int cnt=0;
	for (ll tmp=x;tmp;tmp=(tmp-1)&tmp) w[cnt++]=tmp&-tmp;
	for (int i=1;i<=n;i++)
	{
		int now=0;
		for (int j=0;j<cnt;j++) if (a[i]&w[j]) now|=1<<j;
		sum[now]++;
	}
	for (int j=0;j<cnt;j++)
	for (int i=(1<<cnt)-1;i>=0;i--)
	if (!((i>>j)&1))
	sum[i]+=sum[i^(1<<j)];
	int now=0;
	for (int i=1;i<(1<<cnt);i++)
	if (sum[i]>=(n+1)/2&&popcount[now]<popcount[i])
	now=i;
	ll real=0;
	for (int i=0;i<cnt;i++) real|=w[i]*((now>>i)&1);
	if (__builtin_popcountll(ans)<__builtin_popcountll(real)) ans=real;
	for (int i=0;i<(1<<cnt);i++) sum[i]=0;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	std::mt19937 rnd(std::chrono::system_clock::now().time_since_epoch().count());
	n=read(),m=read(),p=read();
	for (int i=1;i<(1<<p);i++) popcount[i]=popcount[i&(i-1)]+1;
	for (int i=1;i<=n;i++)
	for (int j=1;j<=m;j++)
	a[i]=(a[i]<<1)|get();
	int time=50;
	while (time--) solve(a[rnd()%n+1]);
	for (int i=m-1;i>=0;i--) putchar('0'+((ans>>i)&1));
	
	return 0;
}
