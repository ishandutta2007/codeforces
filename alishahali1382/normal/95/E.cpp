using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<pii, int> piii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<(x)<<endl;
#define debugp(x) cerr<<#x<<"= {"<<(x.first)<<", "<<(x.second)<<"}"<<endl;
#define debug2(x, y) cerr<<"{"<<#x<<", "<<#y<<"} = {"<<(x)<<", "<<(y)<<"}"<<endl;
#define debugv(v) cerr<<#v<<" : ";for (auto x:v) cerr<<x<<' ';cerr<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back
#define kill(x) return cout<<x<<'\n', 0;

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

int n, m, k, u, v, x, y, t, a, b, ans=inf;
int dp[MAXN];
int C[MAXN];
bitset<MAXN> mark;
vector<int> G[MAXN];

void update(int x, int cost){
	for (int i=MAXN-1; i>=x; i--) dp[i]=min(dp[i], dp[i-x]+cost);
}

int dfs(int node){
	mark[node]=1;
	int res=1;
	for (int i:G[node]) if (!mark[i]) res+=dfs(i);
	return res;
}

bool lucky(int x){
	for (; x; x/=10) if (x%10!=4 && x%10!=7) return 0;
	return 1;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	while (m--){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	memset(dp, 63, sizeof(dp));
	dp[0]=-1;
	for (int i=1; i<=n; i++) if (!mark[i]) C[dfs(i)]++;
	for (int i=1; i<=n; i++) if (C[i]){
		for (int j=0; j<LOG; j++) if ((1<<j)<=C[i]){
			C[i]-=(1<<j);
			update((1<<j)*i, (1<<j));
		}
		update(C[i]*i, C[i]);
	}
	for (int i=1; i<=n; i++) if (lucky(i)) ans=min(ans, dp[i]);
	
	if (ans==inf) ans=-1;
	cout<<ans<<'\n';
	
	return 0;
}
