const int Mod = 1000000007;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}

const int MAXN = 1028;


int n, d;
int parent[MAXN];
int cnt[MAXN];


int findset(int a) {
    if (a == parent[a]) {
        return a;
    }
    return parent[a] = findset(parent[a]);
}


void solve() {
    cin >> n >> d;
    for (int i = 0; i < n; i++) {
        parent[i] = i;
    }
    for (int k = 1; k <= d; k++) {
        int x, y;
        cin >> x >> y;
        x--, y--;
        parent[findset(x)] = findset(y);
        for (int i = 0; i < n; i++) {
            cnt[i] = 0;
        }
        for (int i = 0; i < n; i++) {
            cnt[findset(i)]++;
        }
        int have = k;
        int t = 0;
        vector<int> kek;
        for (int i = 0; i < n; i++) {
            if (cnt[i]) {
                kek.pb(cnt[i]);
                chkmax(t, cnt[i] - 1);
                have -= cnt[i] - 1;
            }
        }
        sort(all(kek));
        reverse(all(kek));
        for (int i = 1; i < sz(kek); i++) {
            if (have) {
                t += kek[i];
                have--;
            }
        }
        cout << t << '\n';
    }
}


