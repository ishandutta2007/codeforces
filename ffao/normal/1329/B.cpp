void solve()
{
    for (re(t); t; t--) {
        re(d,m);

        int ans = 1 % m;
        for (int i = (1<<29); i > 0; i >>= 1) if (i <= d) {
            ans = (ans * 1ll * ((min(2*i,d+1) - i) + 1)) % m; 
        }

        ans--;
        if (ans < 0) ans += m;
        ps(ans);
    }
}

