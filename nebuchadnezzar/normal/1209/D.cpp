void solve() {
	int n, k;
	cin >> n >> k;
	vector<vector<int>> graph(n);
	for (int i = 0; i < k; ++i) {
		int a, b;
		cin >> a >> b;
		--a; --b;
		graph[a].push_back(b);
		graph[b].push_back(a);
	}

	vector<bool> used(n);

	function<int(int)> dfs = [&](int v) {
		used[v] = true;
		int ret = 1;
		for (int to : graph[v]) {
			if (!used[to]) {
				ret += dfs(to);
			}
		}
		return ret;
	};

	int cnt = 0;

	for (int i = 0; i < n; ++i) {
		if (!used[i]) {
			cnt += dfs(i) - 1;
		}
	}

	cout << k - cnt << "\n";
}


