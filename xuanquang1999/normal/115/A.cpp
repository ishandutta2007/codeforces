int n, depth[maxN];
vector<int> ke[maxN];

void DFS(int u) {
    forit(it, ke[u]) {
        int v = *it;
        if (!depth[v]) {
            depth[v] = depth[u]+1;
            DFS(v);
        }
    }
}

