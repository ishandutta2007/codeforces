int mod;
int add(int a,int b){
    a+=b;
    if(a>=mod)
        a-=mod;
    return a;
}
int sub(int a,int b){
    a-=b;
    if(a<0)
        a+=mod;
    return a;
}
int mul(int a,int b){
    return (long long)a*b%mod;
}
int pwrmod(int x,int k){
    int ans=1;
    for(;k;k>>=1,x=mul(x,x))
        if(k&1)
            ans=mul(ans,x);
    return ans;
}
const int N=401;
vector<int> fact,pw;
vector<int> ifact;
int dp[N][N];
int calc(int n,int koliko){
	if(koliko<0||n==0)
		return 0;
	if(n<=0)
		return koliko==0;
	if(dp[n][koliko]!=-1)
		return dp[n][koliko];
	dp[n][koliko]=0;
	for(int i=n;i>=1;i--){
		dp[n][koliko]=add(dp[n][koliko],mul(mul(pw[i],ifact[i]),calc(n-i-1,koliko-i)));
	}
	//printf("%i %i: %i\n",n,koliko,dp[n][koliko]);
	return dp[n][koliko];
}
