	void solve()
	{
		char[] s = ns().toCharArray();
		int n = s.length;
		long ret = 0;
		for(int i = 0;i < n;i++){
			if(s[i] == '@'){
				for(int j = i+1;j < n && (s[j] != '@' && s[j] != '_');j++){
					if(s[j] == '.'){
						if(j == i+1)break;
						int k;
						int letter1 = 0;
						for(k = i-1;k >= 0;k--){
							if(s[k] == '@' || s[k] == '.')break;
							if(s[k] != '_' && !(s[k] >= '0' && s[k] <= '9'))letter1++;
						}
						int letter2 = 0;
						for(k = j+1;k < n;k++){
							if(s[k] == '@' || s[k] == '.' || s[k] == '_' || (s[k] >= '0' && s[k] <= '9'))break;
							letter2++;
						}
						ret += (long)letter1 * letter2;
						i = j;
						break;
					}
				}
			}
		}
		out.println(ret);
	}
	
