const int SIZE = 1<<20;
char s[SIZE];
int sn;
int d[300002][26],id;
char nxt[300002];
int ending[300002];
int num[300002];
int add(){
    s[sn]=0;
    int now=0;
    bool done=0;
    int need=sn;
    REP(i,sn){
        if(i&&num[now]==1&&!done){
            done=1;
            int j=i;
            int tmp=now;
            while(nxt[tmp]!=-1&&nxt[tmp]+'a'==s[j]){
                tmp=d[tmp][(int)nxt[tmp]];
                j++;
            }
            if(j-i>1&&nxt[tmp]==-1)need=sn-(j-i)+1;
        }
        if(!d[now][s[i]-'a']){
            d[now][s[i]-'a']=++id;
            nxt[now]=s[i]-'a';
        }
        num[now]++;
        now=d[now][s[i]-'a'];
    }
    num[now]++;
    ending[now]++;
    if(ending[now]>1){
        now=0;
        REP(i,sn){
            num[now]--;
            now=d[now][s[i]-'a'];
        }
        num[now]--;
    }
    sn=0;
    return need;
}
