int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	auto cal = [&](ll x){
		if(x < 10){
			return x;
		}
		int cnt = 2;
		ll res = 9, k = 10;
		for(; k <= 1e17 + 1 && k * 10 <= x; k *= 10, cnt ++){
			res += 9 * (k / 10);
		}
		int lead = x / k;
		res += (lead - 1) * (k / 10) + (x - x / k * k) / 10 + 1;
		if(lead > x % 10) res --;
		return res;
	};
	ll l, r;
	cin >> l >> r;
	cout << cal(r) - cal(l - 1);
	return 0;
}

