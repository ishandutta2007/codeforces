use std::io::Write;

fn main() {
    use std::io::Read;
    let mut s = String::new();
    std::io::stdin().read_to_string(&mut s).unwrap();
    let mut sc = scanner::Scanner::new(&s);
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    run(&mut sc, &mut out);
}

fn run<W: Write>(sc: &mut scanner::Scanner, out: &mut std::io::BufWriter<W>) {
    let t: u32 = sc.next();
    for _ in 0..t {
        let n: usize = sc.next();
        let a: Vec<usize> = sc.next_vec(n);
        let mut cnt = vec![0; n + 1];
        for a in a.iter() {
            cnt[*a] += 1;
        }
        for i in 1..=n {
            cnt[i] += cnt[i - 1];
        }
        cnt.dedup();
        let up = n.next_power_of_two().trailing_zeros();
        let mut ans = std::u32::MAX;
        for i in 0..=up {
            for j in 0..=up {
                let mut add = 0;
                let v = 2u32.pow(i);
                let x = cnt.upper_bound(&v);
                add += v - cnt[x - 1];
                let v = 2u32.pow(j);
                let y = cnt.upper_bound(&(v + cnt[x - 1]));
                add += v - (cnt[y - 1] - cnt[x - 1]);
                let rem = n as u32 - cnt[y - 1];
                add += rem.next_power_of_two() - rem;
                ans = ans.min(add);
            }
        }
        writeln!(out, "{}", ans).ok();
    }
}