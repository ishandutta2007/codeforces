long long pw(long long a, long long n, long long m) {
    ll res = 1;
    while (n) {
        if (n & 1ll) {
            res = res * a % m;
        }
        a = a * a % m;
        n >>= 1;
    }
    return res;
}

long long inv(long long a, long long p) {
    long long res = 1;
    while (a > 1) {
        res = res * (p - p / a) % p;
        a = p % a;
    }
    return res;
}

const ll is_query = -(1LL<<62);
struct Line {
    ll m, b;
    mutable function<const Line*()> succ;
    bool operator<(const Line& rhs) const {
        if (rhs.b != is_query) return m < rhs.m;
        const Line* s = succ();
        if (!s) return 0;
        ll x = rhs.m;
        return b - s->b < (s->m - m) * x;
    }
};
struct HullDynamic : public multiset<Line> { // will maintain upper hull for maximum
    bool bad(iterator y) {
        auto z = next(y);
        if (y == begin()) {
            if (z == end()) return 0;
            return y->m == z->m && y->b <= z->b;
        }
        auto x = prev(y);
        if (z == end()) return y->m == x->m && y->b <= x->b;
        long long delta = (x->b - y->b)*(z->m - y->m) - (y->b - z->b)*(y->m - x->m);

        long double ddelta = (x->b - y->b) * 1.0L * (z->m - y->m) - (y->b - z->b) * 1.0L * (y->m - x->m);
        if (ddelta > 1e18) {
            return true;
        }
        if (ddelta < -1e18) {
            return false;
        }
        return delta >= 0;
    }

    void insert_line(ll m, ll b) {
        auto y = insert({ m, b });
        y->succ = [=] { return next(y) == end() ? 0 : &*next(y); };
        if (bad(y)) { erase(y); return; }
        while (next(y) != end() && bad(next(y))) erase(next(y));
        while (y != begin() && bad(prev(y))) erase(prev(y));
    }
    ll eval(ll x) {
        auto l = *lower_bound((Line) { x, is_query });
        return l.m * x + l.b;
    }
};

void solve(int test) {
    int n = nxt();

    long long a[n];
    for (int i = 0; i < n; ++i) {
        a[i] = nxt();
    }
    long long s1[n + 1];
    s1[0] = 0;

    long long s2[n + 1];
    s2[0] = 0;

    for (int i = 0; i < n; ++i) {
        s1[i + 1] = s1[i] + i * a[i];
        s2[i + 1] = s2[i] + a[i];
    }

    HullDynamic hullDynamic;

    hullDynamic.insert_line(1, 0);

    long long ans = 0;

    for (int i = 1; i <= n; ++i) {
        hullDynamic.insert_line(-(i - 1), -s1[i] + s2[i] * (i - 1));
        //cerr << s1[i] + hullDynamic.eval(s2[i]) << " ";
        ans = max(ans, s1[i] + hullDynamic.eval(s2[i]));
    }

    cout << ans << "\n";

}


