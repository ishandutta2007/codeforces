int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);

    int n;
    cin >> n;
    vector<ll> a(n);
    cin >> a;
    for (int i = 1; i < n; ++i)
        a[i] += a[i - 1];
    a.insert(a.begin(), 0);
    ++n;
    if (a.back() != maxe(a) || a[0] != mine(a)) {
        cout << -1 << '\n';
        return 0;
    }
    ordered_set<pair<ll, int>> s;
    int id = 0;
    ll ans = 0;
    for (ll k : a) {
        auto it = s.lower_bound(mp(k + 1, -1));
        if (it != s.end()) {
            ans += s.size() - s.order_of_key(*it);
        }
        s.insert({k, id++});
    }
    cout << ans << '\n';

    return 0;
}