const int MOD = 1000000007;
int n, x[300005], ans;
int power(int A, int B)
{
    int res = 1;
    while (B)
    {
        if (B & 1)
            res = res * A % MOD;
        B >>= 1;
        A = A * A % MOD;
    }
    return res;
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin >> n;
    for (int i = 1; i <= n; ++i)
        std::cin >> x[i];
    std::sort(x + 1, x + 1 + n);
    for (int i = 1; i < n; ++i)
        ans = (ans + (x[i + 1] - x[i]) * (power(2, i) - 1) % MOD * (power(2, n - i) - 1)) % MOD;
    std::cout << ans << std::endl;
    return 0;
}