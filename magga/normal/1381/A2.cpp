void solve(){
    ll n;cin>>n;
    string a,b;cin>>a>>b;
    vector<int>ans;
    for(int i=0;i<n;i++){
        if(i%2==0){
            if(a[i/2]==b[n-1-i]){
                ans.push_back(1);
                ans.push_back(n-i);
            }else{
                ans.push_back(n-i);
            }
        }else{
            if(a[n-1-i/2]!=b[n-1-i]){
                ans.push_back(1);
                ans.push_back(n-i);
            }else{
                ans.push_back(n-i);
            }
        }
    }
    cout<<ans.size()<<" ";
    for(auto i:ans){
        cout<<i<<" ";
    }
    cout<<"\n";
}
