vi p;
vi t;
vi ans;
void fillans(vi &a,int c)
{
    assert(a.size()<=4);
    vi id(a.size());
    iota(id.begin(),id.end(),0);
    sort(id.begin(),id.end(),[&](int x,int y){
        return t[a[x]]<t[a[y]];
    });
    for(auto u:id)
    {
        ans[a[u]]=c;
        c++;
    }
    c--;
    if(a.size()==4)
    {
        for(int i=c-3;i<c;i++)
        {
            bool suc=1;
            if(id[3]!=0&&ans[a[id[3]-1]]==i) suc=0;
            if(id[3]!=3&&ans[a[id[3]+1]]==i) suc=0;
            if(suc)
                ans[a[id[3]]]=i;
        }
        assert(ans[a[id[3]]]!=c);
    }
}
const int maxn=1e4+7;
void solve(vi a,int c=1)
{
    if(a.size()<=4)
    {
        fillans(a,c);
        return;
    }
    vi vis(maxn);
    vi nxt;
    set<int> pos;
    for(int i=0;i<a.size();i+=3)
    {
        vi tmp;
        vi idd;
        for(int j=i;j<min((int)a.size(),i+3);j++)
        {
            tmp.push_back(a[j]);
            idd.push_back(j);
        }
        vi id(tmp.size());
        iota(id.begin(),id.end(),0);
        sort(id.begin(),id.end(),[&](int x,int y){
            return t[tmp[x]]<t[tmp[y]];
        });
        nxt.push_back(tmp[id[0]]);
        vis[tmp[id[0]]]=1;
    }
    sort(a.begin(),a.end(),[&](int x,int y){
        return t[x]<t[y];
    });
    dbg(a);
    for(auto u:a)
    {
        if(vis[u])
        {
            pos.insert(u);
            continue;
        }
        int l=-1,r=-1;
        auto it=pos.lower_bound(u);
        map<int,int> cnt;
        if(it!=pos.end())
        {
            if(!vis[*it])
            {
                dbg(*it);
                r=ans[*it];
                auto itr=it;
                while(itr!=pos.end()&&!vis[*itr])
                {
                    cnt[ans[*itr]]++;
                    itr++;
                }
            }
        }
        if(it!=pos.begin())
        {
            it--;
            if(!vis[*it])
            {
                l=ans[*it];
                do{
                    if(vis[*it]) break;
                    cnt[ans[*it]]++;
                    if(it!=pos.begin())
                        it--;
                }while(it!=pos.begin());
            }
        }
        dbg(u,l,r);
        if(cnt[c]==0) ans[u]=c;
        else if(cnt[c+1]==0) ans[u]=c+1;
        else if(cnt[c+2]==0) ans[u]=c+2;
        else{
            for(int j=c;j<c+3;j++)
            {
                if(j!=l&&j!=r)
                {
                    ans[u]=j;
                    break;
                }
            }
        }
        assert(ans[u]);
        pos.insert(u);
    }
    dbg(nxt);
    solve(nxt,c+3);
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int T;
    cin>>T;
    while(T--)
    {
        int n;
        cin>>n;
        p=vi(n);
        t=vi(n);
        ans=vi(n);
        for(int i=0;i<n;i++)
        {
            cin>>p[i];
            p[i]--;
            t[p[i]]=i;
        }
        vi all(n);
        iota(all.begin(),all.end(),0);
        solve(all);
        print(ans);
    }
}