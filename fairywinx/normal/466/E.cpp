const int N = 1e5 + 228;
const int K = 17;
const int INF = 2e9 + 228;

pair<int, int> up[N][K];
vector<pair<int, int>> G[N];
int d[N];

void dfs(int v, pair<int, int> p, int nowd) {
	d[v] = nowd;
	up[v][0] = p;
	for(auto i: G[v]) 
		dfs(i.first, {v, i.second}, nowd + 1);
}

pair<int, int> la(int a, int level) {
	int res = 0;
	for(int k = K - 1; k >= 0; --k) {
		if (level >= (1 << k)) {
			res = max(res, up[a][k].second);
			a = up[a][k].first;
			level -= (1 << k);
		}
	}
	return {a, res};
}

signed main() {
	setup();

	int n, m;
	cin >> n >> m;
	vector<pair<int, int>> q2;
	vector<pair<int, int>> q3;
	vector<int> used(n + 1);

	for(int i = 0; i < m; ++i) {
		int type;
		cin >> type;
		if (type == 3) {
			int a, b;
			cin >> a >> b;
			q3.emplace_back(a, b - 1);
		}
		if (type == 2) {
			int a;
			cin >> a;
			q2.emplace_back(a, i);
		}
		if (type == 1) {
			int v, p;
			cin >> v >> p;
			G[p].emplace_back(v, i);
			used[v] = 1;
		}
	}
	for(int i = 1; i <= n; ++i) {
		if (!used[i])
			G[0].emplace_back(i, INF);
	}
	dfs(0, {0, INF}, 0);
	for(int k = 1; k < K; ++k) {
		for(int i = 0; i <= n; ++i) {
			up[i][k].first = up[up[i][k - 1].first][k - 1].first;
			up[i][k].second = max(up[up[i][k - 1].first][k - 1].second, up[i][k - 1].second);
		}
	}
	for(auto i: q3) {
		int id = i.second;
		int v = i.first;
		auto z = la(q2[id].first, d[q2[id].first] - d[v]);
		if (z.first != v || z.second > q2[id].second) 
			cout << "NO\n";
		else
			cout << "YES\n";
	}

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

