#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=5e3+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct node
{
	int x,y;
	bool operator <(const node &a)const
	{
		return x==a.x?y<a.y:x<a.x;
	}
	node operator +(const node &a)
	{
		return (node){x+a.x,y+a.y};
	}
	node operator -(const node &a)
	{
		return (node){x-a.x,y-a.y};
	}
	void output()
	{
		print(x,' ');
		print(y);
	}
}a[N*2],st[N],b[N];
int cross(node a,node b) {return a.x*b.y-a.y*b.x;}
int get(int n)
{
	sort(a+1,a+1+n);
	int top=0,ret=0;
	for (int i=1;i<=n;i++)
	{
		while (top>1&&cross(st[top-1]-a[i],st[top]-a[i])>=0) top--;
		st[++top]=a[i];
	}
	for (int i=1;i<=top;i++) b[++ret]=st[i];
	top=0;
	for (int i=1;i<=n;i++)
	{
		while (top>1&&cross(st[top-1]-a[i],st[top]-a[i])<=0) top--;
		st[++top]=a[i];
	}
	for (int i=top-1;i>1;i--) b[++ret]=st[i];
	for (int i=1;i<=ret;i++) a[i]=b[i];
	return ret;
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),S=read()*0;
	for (int i=1;i<=n;i++) a[i].x=read(),a[i].y=read();
	sort(a+1,a+1+n);
	n=get(n);
	for (int i=1;i<=n;i++) a[i+n]=a[i];
	int x=0,y=0,z=0;
	for (int k=1;k<=n-2;k++)
	for (int i=1,j=i+k+1;i<=n;i++)
	{
		while (cross(a[i+k]-a[j],a[i]-a[j])<=cross(a[i+k]-a[j+1],a[i]-a[j+1])) j++;
		if (cross(a[i+k]-a[j],a[i]-a[j])>S)
		{
			S=cross(a[i+k]-a[j],a[i]-a[j]);
			x=i,y=j,z=i+k;
		}
	}
	(a[x]+a[y]-a[z]).output();
	(a[x]+a[z]-a[y]).output();
	(a[y]+a[z]-a[x]).output();
	
	return 0;
}
