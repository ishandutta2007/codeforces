#define lson o<<1
#define rson o<<1|1
#define Lson L,mid,lson
#define Rson mid+1,R,rson
struct D{
    ll lz,mx;
    D(ll v=0x3f3f3f3f3f3f3f3f):lz(0),mx(v){}
    inline void add(ll v)
    {
        lz+=v;
        mx+=v;
    }
    inline void push(D &x,D &y)
    {
        x.add(lz);
        y.add(lz);
        lz=0;
    }
    static D merge(D &x,D &y)
    {
        return D(min(x.mx,y.mx));
    }
};
int sz;
template<class D>
struct sgt{
    vc<D> tree;
    sgt(){}
    template<class T>
    sgt(const vc<T>& a)
    {
        int n=a.size();
        sz=1;
        while(sz<n) sz*=2;
        tree.resize(sz*2);
        for(int i=0;i<n;i++) tree[sz+i]=D(a[i]);
        for(int i=sz-1;i>=1;i--) upd(i);
    }
    inline void push(int o)
    {
        tree[o].push(tree[lson],tree[rson]);
    }
    inline void upd(int o)
    {
        tree[o]=D::merge(tree[lson],tree[rson]);
    }
    void modify(int p,ll val,int L=0,int R=sz-1,int o=1)
    {
        if(L==R)
        {
            tree[o]=D(val);
            return;
        }
        push(o);
        int mid=L+R>>1;
        if(p<=mid) modify(p,val,Lson);
        else modify(p,val,Rson);
        upd(o);
    }
    void add(int l,int r,ll val,int L=0,int R=sz-1,int o=1)
    {
        if(l<=L&&r>=R)
        {
            tree[o].add(val);
            return;
        }
        push(o);
        int mid=L+R>>1;
        if(l<=mid) add(l,r,val,Lson);
        if(r>mid) add(l,r,val,Rson);
        upd(o);
    }
    ll query(int l,int r,int L=0,int R=sz-1,int o=1)
    {
        if(l<=L&&r>=R)
            return tree[o].mx;
        push(o);
        int mid=L+R>>1;
        ll ret=0x3f3f3f3f3f3f3f3f;
        if(l<=mid) ret=min(ret,query(l,r,Lson));
        if(r>mid) ret=min(ret,query(l,r,Rson));
        return ret;
    }
};
struct Bit{
    vc<ll> a;
    int nn;
    Bit(int n)
    {
        nn=n+1;
        a=vc<ll>(nn,0);
    }
    void add(int x,ll v)
    {
        while(x)
        {
            a[x]+=v;
            x-=x&-x;
        }
    }
    ll query(int x)
    {
        ll ret=0;
        while(x<nn)
        {
            ret+=a[x];
            x+=x&-x;
        }
        return ret;
    }
};
