ModInt pow(ModInt a, ll n) {
    assert(n >= 0);
	ModInt ans = 1;
    while (n > 0) {
        if (n&1) ans = ans*a;
        a = a*a;
        n >>= 1;
    }
	return ans;
}

using mint = ModInt;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    string s, t; cin >> s >> t;
    int n = s.size(), m = t.size();
    auto dp = vec2d(n+1, n+1, mint(0));
    for(int i = 0; i <= n; i++){
        dp[i][i] = 1;
    }
    auto ok = [&](int i, char c){
        if(i >= m) return true;
        return t[i] == c;
    };
    for(int i = 0; i < n; i++){
        for(int l = 0; l <= n; l++){
            int r = l+i;
            if(r > n) continue;
            // [l-1, r)
            if(l != 0 && ok(l-1, s[i])){
                dp[l-1][r] += dp[l][r];
            }
            // [l, r+1)
            if(r+1 <= n && ok(r, s[i])){
                dp[l][r+1] += dp[l][r];
            }
        }
    }
    mint ans = 0;
    for(int r = m; r <= n; r++){
        ans += dp[0][r];
    }
    // for(int l = 0; l < n; l++) print_vector(dp[l]);
    cout << ans << endl;
}