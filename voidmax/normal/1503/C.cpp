typedef long long base; 
typedef pair<int, int> point;      
typedef complex<double> comp;

const int N = 1e6 + 5;

int n;
int a[N], c[N];
vector<int> order;
long long res[N];
set<pair<int, int>> small;
set<pair<int, int>> big[2];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n;
    long long sum = 0;
    for (int i = 0; i < n; ++i) {
        cin >> a[i] >> c[i];
        sum += c[i];
        order.push_back(i);
    }
    sort(order.begin(), order.end(), [&](int i, int j) {
        return a[i] < a[j];
    });
    big[0].insert({res[order[0]], order[0]});
    big[1].insert({a[order[0]] + c[order[0]], order[0]});
    for (int i = 1; i < n; ++i) {
        while (big[1].size() && big[1].begin()->first < a[order[i]]) {
            int j = big[1].begin()->second;
            // cout << j << "?" << endl;
            big[0].erase({res[j], j});
            big[1].erase({a[j] + c[j], j});
            small.insert({res[j] - a[j] - c[j], j});
        }
        res[order[i]] = 1e18;
        if (big[0].size()) {
            chkmin(res[order[i]], big[0].begin()->first);
        }
        if (small.size()) {
            chkmin(res[order[i]], small.begin()->first + a[order[i]]);
        }
        big[0].insert({res[order[i]], order[i]});
        big[1].insert({a[order[i]] + c[order[i]], order[i]});
    }
    cout << sum + res[order[n - 1]] << '\n';
}