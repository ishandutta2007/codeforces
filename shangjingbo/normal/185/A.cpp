const int base	=	1000000007;

inline LL calc(LL a,LL t,LL base)
{
	if (t==0) return 1%base;
	LL y=calc(a,t/2,base);
	y=y*y%base;
	if (t%2==1) y=y*a%base;
	return y;
}

