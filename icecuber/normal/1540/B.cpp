constexpr int md = (int) 1e9 + 7;


using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;
const int nax = 201;
vector<int> node[nax];

Mint dp[nax][nax];

int dfsPath(vector<int>&path, int p, int target, int par = -1) {
  path.push_back(p);
  if (p == target) return 1;
  for (int i : node[p]) {
    if (i == par) continue;
    if (dfsPath(path, i, target, p)) return 1;
  }
  path.pop_back();
  return 0;
}

int szDfs(int p, int par) {
  int r = 1;
  for (int i : node[p])
    if (i != par) r += szDfs(i, p);
  return r;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n;
  cin >> n;
  for (int i = 0; i < n-1; i++) {
    int a, b;
    cin >> a >> b;
    a--, b--;
    node[a].push_back(b);
    node[b].push_back(a);
  }
  for (int i = 0; i < nax; i++) {
    for (int j = 0; j < nax; j++) {
      if (i == 0) dp[i][j] = 1;
      else if (j == 0) dp[i][j] = 0;
      else dp[i][j] = (dp[i-1][j]+dp[i][j-1])/2;
    }
  }
  Mint ans = 0;
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < i; j++) { 
      vector<int> path;
      dfsPath(path, i, j);
      assert(path.size() >= 2);
      set<int> inpath;
      for (int x : path) inpath.insert(x);
      for (int d = 0; d < (int)path.size(); d++){ 
        int sz = 1;
        int p = path[d];
        for (int k : node[p]) 
          if (!inpath.count(k))
            sz += szDfs(k, p);
        ans += dp[d][path.size()-1-d] * sz;
      }
    }
  }
  ans /= n;
  cout << ans << endl;
}