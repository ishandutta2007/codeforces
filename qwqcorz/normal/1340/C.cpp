#include<bits/stdc++.h>
using namespace std;
const int N=1e4+5;
const int G=1e3+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
deque<int>q;
bool vis[N*G];
int id[N][G],dis[N*G],d[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int m=read(),n=read(),cnt=0;(void)m;
	for (int i=1;i<=n;i++) d[i]=read();
	sort(d+1,d+1+n);
	int g=read(),r=read();
	for (int i=1;i<=n;i++)
	for (int j=0;j<g;j++) id[i][j]=cnt++;
	memset(dis,0x3f,sizeof(dis));
	int inf=dis[0];
	dis[0]=0;
	q.push_back(0);
	while (!q.empty())
	{
		int now=q.front();q.pop_front();
		if (vis[now]) continue;
		vis[now]=1;
		int x=now/g+1,y=now%g;
		if (x>1&&y+d[x]-d[x-1]<=g)
		{
			int nx=x-1,ny=y+d[x]-d[x-1],val=0;
			if (ny==g) ny=0,val=1;
			if (dis[id[nx][ny]]>dis[id[x][y]]+val)
			{
				dis[id[nx][ny]]=dis[id[x][y]]+val;
				if (val==0) q.push_front(id[nx][ny]);
					   else q.push_back(id[nx][ny]);
			}
		}
		if (x<n&&y+d[x+1]-d[x]<=g)
		{
			int nx=x+1,ny=y+d[x+1]-d[x],val=0;
			if (ny==g) ny=0,val=1;
			if (dis[id[nx][ny]]>dis[id[x][y]]+val)
			{
				dis[id[nx][ny]]=dis[id[x][y]]+val;
				if (val==0) q.push_front(id[nx][ny]);
					   else q.push_back(id[nx][ny]);
			}
		}
	}
	int ans=dis[id[n][0]]<inf?dis[id[n][0]]*(g+r)-r*!!dis[id[n][0]]:inf;
	for (int i=1;i<g;i++) ans=min(ans,dis[id[n][i]]<inf?dis[id[n][i]]*(g+r)+i:inf);
	if (ans<inf) print(ans);
			else puts("-1");
	
	return 0;
}
