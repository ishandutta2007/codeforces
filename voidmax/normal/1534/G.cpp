const int N = 1e6 + 5;

int n;
map<int, vector<int>> coors;
int moveR = 0;
map<int, int> slopeL, slopeR;

void change(map<int, int>& slope, int x, int delta) {
    slope[x] += delta;
    if (slope[x] == 0) {
        slope.erase(x);
    }
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n;
    for (int i = 0; i < n; ++i) {
        int x, y;
        cin >> x >> y;
        coors[x + y].push_back(x);
    }

    slopeL = {{0, 0}};
    slopeR = {{0, 0}};
    int lastxy = 0;
    long long ans = 0;
    for (auto [xy, xcoor] : coors) {
        moveR += xy - lastxy;
        lastxy = xy;
        for (auto x : xcoor) {
            int minL = slopeL.rbegin()->first;
            int minR = slopeR.begin()->first + moveR;
            if (x <= minL) {
                ans += abs(minL - x);
                change(slopeL, x, 2);
                change(slopeL, minL, -1);
                change(slopeR, minL - moveR, 1);
            } else if (minR <= x) {
                ans += abs(minR - x);
                change(slopeR, x - moveR, 2);
                change(slopeR, minR - moveR, -1);
                change(slopeL, minR, 1);
            } else {
                change(slopeL, x, 1);
                change(slopeR, x - moveR, 1);
            }
            minL = slopeL.rbegin()->first;
        }
    }
    cout << ans << '\n';
}