void solve() {
    INT(n);
    STR(s); 
    vector dp(n + 1, vc<mint>(n + 1, 0)), sum(n + 1, vc<mint>(n + 2, 0));
    auto calc = [&](int i) -> void {
        for(int j = 0; j <= n; ++ j) {
            sum[i][j + 1] = sum[i][j] + dp[i][j];
        }
    };
    vvc<int> zs(n);
    rep(i, n) {
        zs[i] = atcoder::z_algorithm(s.substr(i, n - i));
    }
    // dp[0][0] = 1;
    // calc(0);
    rep(i, n) {
        for(int j = 1; j <= i; ++ j) {
            if(s[i + 1 - j] == '0') continue;
            dp[i + 1][j] = sum[i + 1 - j][j]; // [0, j)
            if(i + 1 - 2 * j >= 0) {
                int len = zs[i + 1 - 2 * j][j];
                if(len < j and s[i + 1 - 2 * j + len] < s[i + 1 - j + len]) {
                    dp[i + 1][j] += dp[i + 1 - j][j];
                }
            }
        }
        dp[i + 1][i + 1] = 1;
        calc(i + 1);
    }
    mint ans = 0;
    for(int j = 1; j <= n; ++ j) {
        ans += dp[n][j];
    }
    out(ans);
}

