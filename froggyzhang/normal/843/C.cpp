#include<bits/stdc++.h>
using namespace std;
#define N 200010
typedef long long ll;
inline ll read(){
	ll x=0,f=1;
	char c=getchar();
	while(c<'0'||c>'9'){
		if(c=='-')f=-1;
		c=getchar();
	}
	while(c>='0'&&c<='9'){
		x=(x<<1)+(x<<3)+c-'0';
		c=getchar();
	}
	return x*f;
}
int n,rt1,rt2;
int siz[N],maxp[N];
vector<int> G[N];
vector<tuple<int,int,int> > ans;
void get_root(int u,int fa){
	siz[u]=1;
	for(auto v:G[u]){
		if(v==fa)continue;
		get_root(v,u);
		siz[u]+=siz[v];
		maxp[u]=max(maxp[u],siz[v]);
	}	
	maxp[u]=max(maxp[u],n-siz[u]);
	if(!rt1)rt1=u;
	else if(maxp[u]<maxp[rt1]){
		rt1=u,rt2=0;	
	}
	else if(maxp[u]==maxp[rt1]){
		rt2=u;
	}
}
queue<int> q;
vector<int> A,B;
int d[N],f[N];
void dfs(int u,int fa){
	f[u]=fa;
	d[u]=G[u].size();
	A.push_back(u);
	for(auto v:G[u]){
		if(v==fa)continue;
		dfs(v,u);
	}	
}
set<int> g[N];
void Solve(int s,int F){
	int X=F,Y=s;
	A.clear();
	dfs(s,F);
	B.clear();
	--d[s];
	for(auto u:A){
		if(d[u]==1)q.push(u);
	}
	g[s].insert(F);
	B.push_back(F);
	while(!q.empty()){
		int u=q.front();
		q.pop();
		ans.emplace_back(X,Y,u);
		X=u;
		for(auto v:G[u]){
			if(g[u].count(v))continue;
			Y=v;break;	
		}
		B.push_back(u);
		for(auto v:G[u]){
			if(v==F)continue;
			--d[v];
			g[v].insert(u);
			if(d[v]==1)q.push(v);
		}
	}
	for(int i=(int)B.size()-4;i>=0;--i){
		ans.emplace_back(B[i],B[i+1],B[(int)B.size()-2]);
	}
}
int main(){
	n=read();
	for(int i=1;i<n;++i){
		int u=read(),v=read();
		G[u].push_back(v);
		G[v].push_back(u);	
	}
	get_root(1,0);
	if(rt2){
		for(auto v:G[rt1]){
			if(v==rt2)continue;
			Solve(v,rt1);	
		}
		for(auto v:G[rt2]){
			if(v==rt1)continue;
			Solve(v,rt2);	
		}
	}
	else{
		for(auto v:G[rt1]){
			Solve(v,rt1);
		}
	}
	printf("%d\n",(int)ans.size());
	for(auto [x,y,z]:ans){
		printf("%d %d %d\n",x,y,z);
	}
	return 0;
}