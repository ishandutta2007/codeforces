const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

ll n, m, k, u, v, x, y, t, a, b, ans1=inf, ans2;

void update(ll a, ll b){
	ll lmin=inf, lmax=1;
	for (ll i=1; i<MAXN; i++){
		ll l=abs(i*k+b-a);
		ll tmp=__gcd(n*k, l);
		lmin=min(lmin, tmp);
		lmax=max(lmax, tmp);
	}
	ans1=min(ans1, n*k/lmax);
	ans2=max(ans2, n*k/lmin);
	if (a==b) ans1=1;
}


int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>k>>a>>b;
	update(a, b);
	update(a, k-b);
	update(k-a, b);
	update(k-a, k-b);
	cout<<ans1<<' '<<ans2<<'\n';
	
	return 0;
}
