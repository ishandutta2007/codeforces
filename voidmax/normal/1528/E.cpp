const int N = 1e6 + 5;
const int p = 998244353;

int sum(int a, int b) {
    a += b;
    return a >= p ? a - p : a;
}

int mul(long long a, int b) {
    return a * b % p;
}

int rev(int a) {
    long long v = 1;
    while (v % a != 0) {
        v += p;
    }
    return v / a;
}

int n;
int dp[N];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n;
    ++n;
    dp[1] = 1;
    int pref = 0;
    for (int i = 2; i <= n; ++i) {
        dp[i] = sum(dp[i - 1], sum(mul(dp[i - 1], pref), mul(mul(dp[i - 1] + 1, dp[i - 1]), rev(2))));
        pref = sum(pref, dp[i - 1]);
    }
    pref = sum(pref, p - dp[n - 1]);
    int ans = 0;
    ans = sum(ans, dp[n]);
    ans = sum(ans, mul(rev(6), mul(dp[n - 1] + 2, mul(dp[n - 1] + 1, dp[n - 1]))));
    ans = sum(ans, mul(rev(2), mul(dp[n - 1], mul(pref + 1, pref))));
    ans = sum(ans, mul(rev(2), mul(pref, mul(dp[n - 1] + 1, dp[n - 1]))));
    ans = sum(mul(ans, 2), p - 1);
    pref = 0;
    for (int i = 2; i < n; ++i) {
        int rest = n - i + 1;
        ans = sum(ans, mul(pref, sum(dp[rest], p - dp[rest - 1])));
        pref = sum(pref, sum(dp[i], p - dp[i - 1]));
    }
    cout << ans << '\n';
}