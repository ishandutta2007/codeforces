void make_set(int i) {
    parent[i] = i;
    rank[i] = 1;
}


void make_set1(int i) {
    parent1[i] = i;
    rank1[i] = 1;
}


int find_set(int i) {
    while (parent[i] != i) {
        i = parent[i];
    }
    return i;
}


int find_set1(int i) {
    while (parent1[i] != i) {
        i = parent1[i];
    }
    return i;
}


bool set_union(int aa, int ba) {
    aa = find_set(aa);
    ba = find_set(ba);
    if (aa == ba) {
        return false;
    }
    if (rank[aa] > rank[ba]) {
        parent[ba] = aa;
        chkmax(rank[aa], rank[ba] + 1);
    } else {
        parent[aa] = ba;
        chkmax(rank[ba], rank[aa] + 1);
    }
    return true;
}


bool set_union1(int aa, int ba) {
    aa = find_set1(aa);
    ba = find_set1(ba);
    if (aa == ba) {
        return false;
    }
    if (rank1[aa] > rank1[ba]) {
        parent1[ba] = aa;
        chkmax(rank1[aa], rank1[ba] + 1);
    } else {
        parent1[aa] = ba;
        chkmax(rank1[ba], rank1[aa] + 1);
    }
    return true;
}


