class FACT{
    companion object {
        var store = IntArray(0)
        var invStore = IntArray(0)

        var slowStore:IntArray = IntArray(0)

        fun preCal(upto:Int){
            store = IntArray(upto+1)
            invStore = IntArray(upto + 1 )
            store[0] = 1
            invStore[0] = 1

            for(i in 1..upto) {
                store[i] = store[i-1] modM i
                invStore[i] = invStore[i-1] modM (i.inverse())
            }
        }
        fun choose(n:Int,r:Int):Int{
            if(r < 0 || r > n) return 0
            val a = store[n]
            val b = invStore[n-r]
            val c = invStore[r]
            return (a modM b) modM c
        }

        fun bigChoose(n:Int,r:Int):Int{
            var ret = 1
            for(i in 0 until r){
                ret = ret modM (n - i)
            }
            ret = ret modM (invStore[r])
            return ret
        }

    }
}

data class polynomial(val terms:IntArray){
    val degree = (terms.indices.lastOrNull{it != 0 }) ?: -1

    companion object{
        fun zeroOfDegree(d:Int): polynomial {
            return polynomial(IntArray(d+1))
        }

        fun interpolate(X:IntArray, Y:IntArray, newpoint:Int):Int{
            // N^2 version
            kotlin.assert(X.size == Y.size)
//            val find = X.indexOf(newpoint)
//            if(find != -1){
//                return Y[find]
//            }
            var ans = 0
            val n = X.size

            for (i in 0 until n) {
                var num = Y[i]
                var den = 1
                for (j in 0 until n) {
                    if (i != j) {
                        num = num modM (newpoint modMinus X[j])
                        den = den modM (X[i] modMinus X[j])
                    }
                }
                ans = (ans modPlus (num modM den.inverse()))
            }
            return ans
        }
    }

    operator fun get(index:Int): Int {
        return terms[index]
    }
    operator fun set(index:Int, new:Int) {
        terms[index] = new
    }
    operator fun plus(other:polynomial): polynomial {
        val d = maxOf(this.degree, other.degree)
        val new = terms.copyOf(d + 1 )
        for(i in 0 until other.degree){
            new[i] = new[i] modPlus other.terms[i]
        }
        return polynomial(new)
    }
    operator fun times(other:polynomial):polynomial{
        val ret = zeroOfDegree(other.degree + this.degree)
        for(l in 0..degree){
            for(r in 0..other.degree){
                val new = l + r
                ret[new] = ret[new] modPlus (this[l] modM other[r])
            }
        }
        return ret
    }

    operator fun unaryMinus():polynomial{
        val new = terms.copyOf()
        for(i in new.indices){
            new[i] = new[i].additiveInverse()
        }
        return polynomial(new)
    }
