void solve() {
    int n, k;
    cin >> n >> k;
    vector<pair<int, int> > d;
    for (int i = 2; i <= n; i++) {
        d.pb(mp(getsum(1, i), i));
    }
    int b = (d[0].first + d[1].first - getsum(2, 3)) / 2;
    vector<pair<int, int> > st;
    st.pb(mp(b, 1));
    for (int i = 2; i <= n; i++) {
        st.pb(mp(d[i - 2].first - b, i));
    }
    sort(all(st));
    cout << "finish" << ' ' << st[k - 1].first << endl;
}


