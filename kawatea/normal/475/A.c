#include <stdio.h>

char s[6][30] = {"+------------------------+", "|#.#.#.#.#.#.#.#.#.#.#.|D|)", "|#.#.#.#.#.#.#.#.#.#.#.|.|", "|#.......................|", "|#.#.#.#.#.#.#.#.#.#.#.|.|)", "+------------------------+"};

int main()
{
    int k, i, j;
    
    scanf("%d", &k);
    
    while (k) {
        for (i = 0; i < 30; i++) {
            for (j = 0; j < 6; j++) {
                if (s[j][i] == '#') {
                    s[j][i] = 'O';
                    
                    break;
                }
            }
            
            if (j < 6) break;
        }
        
        k--;
    }
    
    for (i = 0; i < 6; i++) printf("%s\n", s[i]);
    
    return 0;
}