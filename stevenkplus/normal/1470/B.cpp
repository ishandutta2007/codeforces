int main() { // subscribe to me on youtube: https://www.youtube.com/channel/UCl9IahGhVii0YrjdJvM1XNg
   int T;
   cin >> T;
   while (T--) {
      int n;
      cin >> n;
      vector<int> v(n);
      for (int &i: v) cin >> i;
      for (int &i: v) {
         for (int j = 2; j * j <= i; ++j) {
            while (i % (j * j) == 0) {
               i /= j * j;
            }
         }
      }

      map<int, int> counts;
      for (int i: v) ++counts[i];
      int ans0 = 0;
      int ans1 = 0;
      for (pii p: counts) {
         ans0 = max(ans0, p.se);
         if (p.fi == 1 || p.se % 2 == 0) {
            ans1 += p.se;
         }
      }
      ans1 = max(ans1, ans0);

      int q;
      cin >> q;
      vector<ll> queries(q);
      for (ll &i: queries) cin >> i;
      vector<int> ans(q);
      for (int i = 0; i < q; ++i) {
         ans[i] = queries[i] == 0 ? ans0 : ans1;
      }
      for (int i: ans) cout << i << " ";
      cout << "\n";
   }
}

