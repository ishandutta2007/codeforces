    char last;
    int cnt0 = 0;
    for (int i = 0; i < sz(s); i++) {
        if (len == 0) {
            len++;
            last = s[i];
        } else {
            if (last == s[i]) {
                len++;
            } else {
                if (len) {
                    if (last == '0') {
                        cnt0++;
                    }
                }
                len = 1;
                last = s[i];
            }
        }
    }
    if (last == '0') {
        cnt0++;
    }
    if (cnt0 == 0) {
        cout << 0 << endl;
        return 0;
    }
    long long res = 1e18;
    for (int i = 1; i <= cnt0; i++) {
        chkmin(res, 1LL * i * y + 1LL * (cnt0 - i) * x);
    }
