int sub_to_errichto() {
   int T;
   cin >> T;
   while (T--) {
      ll d, k;
      cin >> d >> k;
      // let x be largest int s.t. (x,x) in circle
      // consider whether (x, x + 1) in circle
      // if YES:
      //   - then player 1 wins; move in first turn, then make every move result in (+1, +1) -- will guarantee end at (x, x + 1) after x moves
      // if NO:
      //   - then player 2 wins; make every move result in (+1, +1) -- will guarantee end at (x, x)
      ll x = 0;
      while (2 * (x + 1) * (x + 1) * k * k <= d * d) {
         ++x;
      }
      pp(x, d);
      if ((x * x + (x + 1) * (x + 1)) * k * k <= d * d) {
         cout << "Ashish\n";
      } else {
         cout << "Utkarsh\n";
      }
   }
}

