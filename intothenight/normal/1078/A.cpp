int main(){
	cin.tie(0)->sync_with_stdio(0);
	double a, b, c;
	pair<double, double> p, q;
	cin >> a >> b >> c >> p >> q;
	c = -c;
	cout << fixed << setprecision(15);
	p.first -= q.first, p.second -= q.second;
	c -= a * q.first + b * q.second;
	if(p.first < 0) p.first = -p.first, a = -a;
	if(p.second < 0) p.second = -p.second, b = -b;
	if(a < 0) a = -a, b = -b, c = -c;
	if(a * b >= 0 || c < 0 && -c >= -b * p.second || c > 0 && c >= a * p.first){
		cout << p.first + p.second;
		return 0;
	}
	pair<double, double> A, B;
	if(c >= 0){
		A = {1.0 * c / a, 0};
	}
	else{
		A = {0, 1.0 * c / b};
	}
	if(c <= p.first * a + p.second * b){
		B = {(c - p.second * b) / 1.0 / a , p.second};
	}
	else{
		B = {p.first, (c - p.first * a) / 1.0 / b};
	}
	auto dis = [](pair<double, double> X, pair<double, double> Y){
		return hypot(X.first - Y.first, X.second - Y.second);
	};
	cout << dis({0, 0}, A) + dis(A, B) + dis(B, p);
	return 0;
}

