const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
vector<int> v[N];
int ans = 0;
int dfs(int x, int fa = -1)
{
	int son = 1;
	for (auto &y : v[x])
	{
		if (y == fa) continue;
		int ret = dfs(y, x);
		if (ret % 2 == 0) ans++;
		son += ret;
	}
	return son;
}
