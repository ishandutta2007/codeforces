const string FILENAME = "input";
const int MAXN = 100228;
 

int n, m;
int x[MAXN];

 
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);    
	//read(FILENAME);
    cin >> n >> m;
    for (int i = 0; i < n; i++) {
        cin >> x[i];
    }
    vector<pair<int, int> > st;
    for (int i = 0; i < m; i++) {
        int x1, x2, y;
        cin >> x1 >> x2 >> y;
        if (x1 != 1) {
            continue;
        }
        st.pb({y, x2});
    }
    sort(all(st));
    vector<pair<int, int> > g;
    for (auto xx: st) {
        if (g.empty() || xx.first != g.back().first) {
            g.pb(xx);
        }
    }
    vector<int> s;
    for (auto x: g) {
        s.pb(x.second);
       // cout << x.second << '\n';
    }
    sort(all(s));
    sort(x, x + n);
    int l = 0;
    int ans = sz(s);
    while (l < sz(s) && s[l] < (0 == n ? 1000000000: x[0])) {
        l++;
    }
    ans = sz(s) - l;
    for (int i = 0; i < n; i++) {
        while (l < sz(s) && s[l] < (i == n - 1 ? 1000000000: x[i + 1])) {
            l++;
        }
        chkmin(ans, i + 1 + sz(s) - l);
    }
    cout << ans << '\n';
    return 0;
}