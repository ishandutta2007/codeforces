const int K = 1e5 + 10;
int b, k, a[K];

void solve() {
    scanf("%d%d", &b, &k);
    for(int i = 0; i < k; ++i)
        scanf("%d", &a[i]);
    int res = 0;
    int pw = 1;
    for(int i = k - 1; i >= 0; --i) {
        res ^= (pw * a[i]) % 2;
        dbg(res);
        pw = (pw * b) % 2;
    }
    puts(res % 2 == 1 ? "odd" : "even");
}

