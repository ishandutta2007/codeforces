class TaskB {
    public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int n = in.nextInt();
        int[] a = in.readIntArray(n);
        int p = in.nextInt();
        int sum = 0;
        for (int i : a) {
            sum += i;
        }
        if (sum <= p) {
            out.println(n);
            return;
        }
        double[] fact = new double[n + 1];
        fact[0] = 1;
        for (int i = 1; i <= n; i++) {
            fact[i] = fact[i - 1] * i;
        }
        double ans = 0;
        for (int last = 0; last < n; last++) {
            double[][] dp = new double[n][p + 1];
            dp[0][0] = 1;
            for (int i = 0; i < n; i++) {
                if (i == last) {
                    continue;
                }
                int w = a[i];
                for (int j = n - 2; j >= 0; j--) {
                    for (int k = 0; k + w <= p; k++) {
                        dp[j + 1][k + w] += dp[j][k];
                    }
                }
            }
            for (int w = 0; w <= p; w++) {
                if (w + a[last] > p) {
                    for (int cnt = 0; cnt < n; cnt++) {
                        ans += fact[cnt] * fact[n - cnt - 1] * dp[cnt][w] * cnt;
                    }
                }
            }
        }
        out.println(ans / fact[n]);
    }
}

class FastScanner extends BufferedReader {

    boolean isEOF;

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
            if (isEOF && ret < 0) {
                throw new InputMismatchException();
            }
            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    static boolean isWhiteSpace(int c) {
        return c >= -1 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (!isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    public int[] readIntArray(int n) {
        int[] ret = new int[n];
        for (int i = 0; i < n; i++) {
            ret[i] = nextInt();
        }
        return ret;
    }

    }

