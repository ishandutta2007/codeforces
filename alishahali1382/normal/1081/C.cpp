#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 998244353;

ll n, m, l, x, s, k, ans=1;
ll F[1000001];
ll I[1000001];

ll powmod(ll a, ll b){
	if (!b) return 1;
	if (b%2) return (powmod(a*a%mod, b>>1)*a)%mod;
	return powmod(a*a%mod, b>>1);
}

ll C(ll n, ll r){
	if (n<0 || r<0 || r>n) return 0;
	return (F[n]*I[r]%mod*I[n-r])%mod;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	F[0]=1;
	for (int i=1; i<=1000000; i++) F[i]=(F[i-1]*i)%mod;
	I[0]=1;
	for (int i=1; i<=1000000; i++) I[i]=(I[i-1]*powmod(i, mod-2))%mod;
	
	cin>>n>>m>>k;
	ans=C(n-1, k)*m%mod;
	for (int i=0; i<k; i++) ans=(ans*(m-1))%mod;
	cout<<ans<<endl;
	
	return 0;
}
