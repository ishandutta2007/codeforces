using namespace std;
typedef pair<llint, llint> P;

llint T;
llint m, d, w;

llint gcd(llint a, llint b){
	if(b == 0) return a;
	return gcd(b, a%b);
}

int main(void)
{
	ios::sync_with_stdio(0);
	cin.tie(0);
	
	cin >> T;
	for(int t = 0; t < T; t++){
		cin >> m >> d >> w;
		
		if(d == 1){
			cout << 0 << endl;
			continue;
		}
		
		llint x = w / gcd(w, d-1), L = min(d, m);
		llint ans = L * (L / x);
		ans -= (L/x) * (L/x+1) / 2 * x;
		cout << ans << endl;
	}
	
	return 0;
}