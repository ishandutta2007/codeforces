int A, B;

int main() {
	scanf("%d%d", &A, &B);
	if(1 == A && !B) { puts("0 0"); return 0; }
	if(B < 1 || 9*A < B) { puts("-1 -1"); return 0; }

	{
		vector<int> V;
		for(int s = B; s;) {
			V.pb(min(9, s));
			s -= min(9, s);
		}
		revv(V);
		if(A == sz(V)) {
			for(int v : V) putchar('0' + v);
			revv(V); putchar(' ');
			for(int v : V) putchar('0' + v);
			return 0;
		}
		putchar('1');
		for(int t = A-sz(V)-1; t--;) putchar('0');
		V[0]--;
		for(int v : V) putchar('0' + v);
		V[0]++;
		putchar(' ');
		revv(V);
		for(int v : V) putchar('0' + v);
		for(int t = A-sz(V); t--;) putchar('0');
	}
	return 0;
}