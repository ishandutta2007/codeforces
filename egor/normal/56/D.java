	void solve() {
		char[] first = in.readString().toCharArray();
		char[] second = in.readString().toCharArray();
		int firstLength = first.length;
		int secondLength = second.length;
		int[][] operationsRequired = new int[firstLength + 1][secondLength + 1];
		for (int i = firstLength; i >= 0; i--)
			operationsRequired[i][secondLength] = firstLength - i;
		for (int i = secondLength; i >= 0; i--)
			operationsRequired[firstLength][i] = secondLength - i;
		for (int i = firstLength - 1; i >= 0; i--) {
			for (int j = secondLength - 1; j >= 0; j--) {
				operationsRequired[i][j] = Math.min(operationsRequired[i + 1][j], operationsRequired[i][j + 1]) + 1;
				if (first[i] == second[j])
					operationsRequired[i][j] = Math.min(operationsRequired[i][j], operationsRequired[i + 1][j + 1]);
				else
					operationsRequired[i][j] = Math.min(operationsRequired[i][j], operationsRequired[i + 1][j + 1] +
						1);
			}
		}
		out.println(operationsRequired[0][0]);
		int firstIndex = 0;
		int secondIndex = 0;
		while (firstIndex != firstLength || secondIndex != secondLength) {
			if (secondIndex != secondLength && operationsRequired[firstIndex][secondIndex] ==
				operationsRequired[firstIndex][secondIndex	+ 1] + 1)
			{
				out.println("INSERT " + (secondIndex + 1) + " " + second[secondIndex]);
				secondIndex++;
			} else if (operationsRequired[firstIndex][secondIndex] == operationsRequired[firstIndex + 1][secondIndex] +
				1)
			{
				out.println("DELETE " + (secondIndex + 1));
				firstIndex++;
			} else {
				if (first[firstIndex] != second[secondIndex])
					out.println("REPLACE " + (secondIndex + 1) + " " + second[secondIndex]);
				firstIndex++;
				secondIndex++;
			}
		}
	}

