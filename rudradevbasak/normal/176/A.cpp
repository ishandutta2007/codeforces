int get(VII obj)
{
	sort(all(obj));
	int left=k;
	int sum=0;
	for(int i=m-1;i>=0;i--)
	{
		if(obj[i].FF<0) break;
		int num = min(left, obj[i].SS);
		sum += num * obj[i].FF;
		left -= num;
	}
	return sum;
}

void solve()
{
	int best = 0;
	for(int i=0;i<n;i++)
		for(int j=0;j<n;j++)
		{
			if(i==j) continue;
			VII objs;
			for(int k=0;k<m;k++)
				objs.pb(mp(b[j][k]-a[i][k], c[i][k]));
			best = max(best, get(objs));
		}
	printf("%d\n",best);
}

