void solve() {
    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }
    int res = 0;
    for (int i = 0; i < n - 1; ++i) {
        res += min(abs(a[i+1] - a[i]) + abs(b[i+ 1] - b[i]), abs(a[i+1] - b[i]) + abs(b[i+ 1] - a[i]));
    }
    cout << res << '\n';
}
