#define pb push_back
#define fox(i,x,y) for(int i=(x);i<=(y);i++)
#define foxr(i,x,y) for(int i=(y);i>=(x);i--)
#define F first
#define S second

const int MN = 5e5+5;
int N, M, i, x, y, a[MN], val[MN], f;
vi ans, adj[MN];

int main(){
    scanf("%d%d",&N,&M);
    for(i=1;i<=M;i++){
        scanf("%d%d",&x,&y);
        adj[x].pb(y);
        adj[y].pb(x);
    }
    for(i=1;i<=N;i++){
        scanf("%d",&a[i]);
        ans.pb(i);
    }
    sort(ans.begin(),ans.end(),[](int i,int j){return a[i]<a[j];});
    for(auto v : ans){
        set<int> use;
        for(auto u : adj[v])
            use.insert(val[u]);
        for(i=1;i<=N;i++){
            if(!use.count(i)) break;
        }
        val[v] = i;
        if(val[v]!=a[v]){
            f=1;
            break;
        }
    }
    if(f) printf("-1\n");
    else{
        for(auto v : ans)
            printf("%d ",v);
        printf("\n");
    }
    return 0;
}