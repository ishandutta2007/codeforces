// Returns the xor of the integers 1, ..., n.
int64_t xor_of_first_n(int64_t n) {
    assert(n >= 0);

    switch (n % 4) {
        case 0: return n;
        case 1: return 1;
        case 2: return n + 1;
        case 3: return 0;
    }

    assert(false);
}


void run_case() {
    int64_t A, B;
    cin >> A >> B;
    int64_t xor_goal = xor_of_first_n(A - 1) ^ B;
    cout << A + (xor_goal == 0 ? 0 : (xor_goal != A ? 1 : 2)) << '\n';
}

