ll solve(ll x) {
	ll z = x / 3;
	x -= z;
	ll y = x / 2;
	x -= y;
	return 2 * (x * y + x * z + y * z);
}

const ll INF = (ll)1e6;
void solve() {
	ll x;
	scanf("%lld", &x);
	ll l = 0, r = INF;
	while(r - l > 1) {
		ll y = (l + r) / 2;
		if (solve(y) >= x)
			r = y;
		else
			l = y;
	}
	printf("%lld\n", r);
}

