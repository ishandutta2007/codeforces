using Zp = Z_p<integral_constant<decay<decltype(mod)>::type, mod>>;

template<typename T> vector<typename Z_p<T>::Type> Z_p<T>::MOD_INV;
template<typename T = integral_constant<decay<decltype(mod)>::type, mod>>
void precalc_inverse(int SZ){
	auto &inv = Z_p<T>::MOD_INV;
	if(inv.empty()) inv.assign(2, 1);
	for(; inv.size() <= SZ; ) inv.push_back((mod - 1LL * mod / (int)inv.size() * inv[mod % (int)inv.size()]) % mod);
}

template<typename T>
vector<T> precalc_power(T base, int SZ){
	vector<T> res(SZ + 1, 1);
	for(auto i = 1; i <= SZ; ++ i) res[i] = res[i - 1] * base;
	return res;
}
template<class T, class U> T &ctmax(T &x, const U &y){ return x = max<T>(x, y); }
template<class T, class U> T &ctmin(T &x, const U &y){ return x = min<T>(x, y); }

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n;
	cin >> n;
	vector<int> c(n);
	for(auto i = 0; i < n; ++ i){
		cin >> c[i];
	}
	vector<int> b(n - 1);
	for(auto i = 0; i < n - 1; ++ i){
		cin >> b[i];
	}
	vector<int> suff(n);
	for(auto i = n - 2; i >= 0; -- i){
		suff[i] = suff[i + 1] + b[i];
	}
	vector<int> pref(n + 1);
	for(auto i = 0; i < n; ++ i){
		pref[i + 1] = pref[i] + suff[i];
	}
	int lower = numeric_limits<int>::max();
	int upper = numeric_limits<int>::max();
	for(auto i = 0; i < n; ++ i){
		ctmin(lower, pref[i + 1] / (i + 1));
		static int cpref = 0;
		cpref += c[i];
		ctmin(upper, (cpref + pref[i + 1]) / (i + 1));
	}
	++ upper;
	int qn;
	cin >> qn;
	map<int, vector<int>> q;
	for(auto qi = 0; qi < qn; ++ qi){
		int low;
		cin >> low, low += suff[0];
		q[clamp(low, lower, upper)].push_back(qi);
	}
	vector<Zp> res(qn);
	for(auto [low, queries]: q){
		vector<Zp> dp{1};
		for(auto i = 0; i < n; ++ i){
			vector<Zp> dp_next((int)dp.size() + c[i]);
			vector<Zp> pref_dp((int)dp.size() + 1);
			for(auto x = 0; x < (int)dp.size(); ++ x){
				pref_dp[x + 1] = pref_dp[x] + dp[x];
			}
			// x + y = s
			for(auto x = 0; x < (int)dp_next.size(); ++ x){
				if(x + pref[i + 1] >= low * (i + 1)){
					dp_next[x] = pref_dp[clamp(x + 1, 0, (int)dp.size())] - pref_dp[clamp(x - c[i], 0, (int)dp.size())];
				}
			}
			swap(dp, dp_next);
		}
		Zp sum = accumulate(dp.begin(), dp.end(), Zp(0));
		for(auto qi: queries){
			res[qi] = sum;
		}
	}
	for(auto x: res){
		cout << x << "\n";
	}
	return 0;
}

