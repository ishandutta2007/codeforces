const int N = 100005;

int main() {
    int n;
    scanf("%d", &n);
    long long ans = 0;
    for (int i = 2; i <= n; i++) {
        int cnt = 2;
        for (int j = 2 * i; j <= n; j += i) {
            ans += 4 * cnt;
            cnt++;
        }
    }
    printf("%lld\n", ans);
    return 0;
}