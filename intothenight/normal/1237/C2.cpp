mt19937 rnd(time(0));



struct tpl{
	int x, y, z, id;
	bool operator<(const tpl &otr) const{
		typedef tuple<int, int, int> T;
		T a(x, y, z), b(otr.x, otr.y, otr.z);
		return a < b;
	}
};
int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<tpl> a(n);
	for(int i = 0; i < n; i ++){
		cin >> a[i].x >> a[i].y >> a[i].z, a[i].id = i + 1;
	}
	sort(all(a));
	vector<tpl> t;
	for(int i = 0; i < n; i ++){
		if(i < n - 1 && a[i].x == a[i + 1].x && a[i].y == a[i + 1].y){
			cout << a[i].id << " " << a[i + 1].id << "\n";
			i ++;
		}
		else{
			t.push_back(a[i]);
		}
	}
	a = t;
	n = a.size();
	t.clear();
	for(int i = 0; i < n; i ++){
		if(i < n - 1 && a[i].x == a[i + 1].x){
			cout << a[i].id << " " << a[i + 1].id << "\n";
			i ++;
		}
		else{
			t.push_back(a[i]);
		}
	}
	a = t;
	n = a.size();
	t.clear();
	for(int i = 0; i < n - 1; i ++){
		cout << a[i].id << " " << a[i + 1].id << "\n";
		i ++;
	}
	return 0;
}

