void init() {
	fill(isp + 2, isp + mn, true);
	for (int i = 2; i < mn; i++) {
		if (!isp[i])continue;
		ps.push_back(i);
		chp[i].push_back(i);
		for (int j = 2 * i; j < mn; j += i) {
			isp[j] = false;
			chp[j].push_back(i);
		}
	}
}
