int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=2; i<=n; i++){
		cin>>ps[i];
		ps[i]+=ps[i-1];
	}
	bool flag=1;
	for (int i=2; i<=n; i++) if (ps[i]<0) flag=0;
	if (flag) pos=1;
	else{
		pos=2;
		for (int i=3; i<=n; i++) if (ps[i]<ps[pos]) pos=i;
	}
	A[pos]=1;
	A[1]=1-ps[pos];
	for (int i=2; i<=n; i++) A[i]=ps[i]+A[1];
	for (int i=1; i<=n; i++) if (A[i]>n) kill(-1);
	for (int i=1; i<=n; i++) mark[A[i]]=1;
	for (int i=1; i<=n; i++) if (!mark[i]) kill(-1);
	for (int i=1; i<=n; i++) cout<<A[i]<<' ';
	
	
	return 0;
}
