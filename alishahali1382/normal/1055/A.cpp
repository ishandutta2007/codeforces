const int mod = 1000000007;
const int MAXN = 3010;

ll n, s, ans, x, y, tmp, mx;
int A[MAXN];
int B[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>s;
	for (int i=1; i<=n; i++) cin>>A[i];
	for (int i=1; i<=n; i++) cin>>B[i];
	
	if (s==1){
		cout<<"YES"<<endl;
		return 0;
	}
	if (!A[1] || !(A[s]+B[s])){
		cout<<"NO"<<endl;
		return 0;
	}
	for (int i=s+1; i<=n; i++){
		 if (B[i]){
		 	mx=i;
		 	break;
		 }
	}
	if (A[s] || A[mx] && B[s]){
		cout<<"YES"<<endl;
		return 0;
	}
	
	for (int i=s; i<=n; i++){
		if (A[i] && B[i]){
			cout<<"YES"<<endl;
			return 0;
		}
	}
	cout<<"NO"<<endl;
	
	
	return 0;
}
