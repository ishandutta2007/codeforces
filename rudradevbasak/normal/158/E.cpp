void solve()
{
	if(n==0)
	{
		printf("86400\n");
		return;
	}
	

	dp[0][0]=1;
	int ans = t[0]-1;
	
	for(int i=0;i<n;i++)
		for(int j=0;j<=k && j<=i+1;j++)
		{
			int &ret = dp[i+1][j];
			ret = max(dp[i][j],t[i])+d[i];
			if(j>0) ret = min(ret,dp[i][j-1]);
			
			if(j==k)
			{
				int beg = ret;
				int end = (i==n-1) ? 86401 : t[i+1];
				ans = max(ans,end-beg);
			}
		}
			
	printf("%d\n",ans);	
}



