int main(){
	cin.tie(0)->sync_with_stdio(0);
	int tt;
	cin >> tt;
	while(tt --){
		int n, p, k;
		cin >> n >> p >> k;
		vl a(n);
		cin >> a;
		sort(all(a));
		vl sum(n + 1), ksum(n + k);
		partial_sum(all(a), sum.begin() + 1);
		for(int i = k; i < n + k; ++ i){
			ksum[i] = ksum[i - k] + a[i - k];
		}
		//ksum[i] = a[i - k] + a[i - 2k] + .. + a[i % k]
		int res = 0;
		for(int r = 0; r < k; ++ r){
			if(sum[r] > p) continue;
			int left = 0, right = (n - r) / k + 1;
			while(right - left > 1){
				int mid = left + right >> 1;
				ll s = sum[r] + ksum[r + mid * k - 1 + k] - ksum[r - 1 + k];
				s <= p ? left = mid : right = mid;
			}
			res = max(res, r + k * left);
		}
		cout << res << "\n";
	}
	return 0;
}
