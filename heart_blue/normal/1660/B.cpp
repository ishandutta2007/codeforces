const int N = 2e5 + 10;
int a[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int ncase;
	scanf("%d", &ncase);
	while (ncase--)
	{
		int n;
		scanf("%d", &n);
		LL sum = 0;
		for (int i = 1; i <= n; i++)
		{
			scanf("%d", &a[i]);
			sum += a[i];
		}
		sort(a + 1, a + n + 1);
		sum -= a[n];
		if (n > 1 && a[n] > a[n - 1] + 1) puts("NO");
		else if (a[n] > sum + 1) puts("NO");
		else puts("YES");
	}
	return 0;
}