#include <bits/stdc++.h>
using namespace std;

const int M = 200005;
int a[M][2];

int main(){
    int n;
    cin >> n;
    for (int i = 0; i < 2*n; i ++){
        int x; cin >> x;
        if (a[x][0]) a[x][1] = i;
        else a[x][0] = i;
    }
    long long ans = 0;
    for (int i = 0; i < n; i++){
        //cout << a[i][0] << ' ' << a[i][1] << endl;
        ans += min(abs(a[i][0]-a[i+1][0])+abs(a[i][1]-a[i+1][1]), 
                    abs(a[i][1]-a[i+1][0])+abs(a[i][0]-a[i+1][1]));
    }
    cout << ans;
}
