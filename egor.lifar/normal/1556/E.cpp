ll pref[MAXN];



struct rmqmin{
    int ss = 1;
    vector<ll> d;
    void init(int n) {
        while (ss < n) {
            ss <<= 1;
        }
        d.resize(2 * ss);
        for (int i = 0; i < sz(d); i++) {
            d[i] = 1e18;
        }
    }
    void change(int i, ll val) {
        i += ss;
        d[i] = val;
        while (i >> 1 > 0) {
            i >>= 1;
            d[i] = min(d[i * 2], d[i * 2 + 1]);
        }
    }
    ll get(int l, int r) {
        l += ss;
        r += ss;
        ll ans = 1e18;
        while (l <= r){
            if (l & 1) {
                chkmin(ans, d[l]);
                l++;
            }
            if (!(r & 1)) {
                chkmin(ans, d[r]);
                r--;
            }
            l >>= 1;
            r >>= 1;
        }
        return ans;
    }
};


struct rmqmax{
    int ss = 1;
    vector<ll> d;
    void init(int n) {
        while (ss < n) {
            ss <<= 1;
        }
        d.resize(2 * ss);
        for (int i = 0; i < sz(d); i++) {
            d[i] = -1e18;
        }
    }
    void change(int i, ll val) {
        i += ss;
        d[i] = val;
        while (i >> 1 > 0) {
            i >>= 1;
            d[i] = max(d[i * 2], d[i * 2 + 1]);
        }
    }
    ll get(int l, int r) {
        l += ss;
        r += ss;
        ll ans = -1e18;
        while (l <= r){
            if (l & 1) {
                chkmax(ans, d[l]);
                l++;
            }
            if (!(r & 1)) {
                chkmax(ans, d[r]);
                r--;
            }
            l >>= 1;
            r >>= 1;
        }
        return ans;
    }
};
void solve() {
    cin >> n >> q;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
        a[i] = b[i] - a[i];
    }
    for (int i = 0; i < n; i++) {
        pref[i] = a[i] + (i ? pref[i - 1]: 0);
    }
    rmqmin k, ls;
    rmqmax k1, l1;
    k.init(n);
    ls.init(n);
    k1.init(n);
    l1.init(n);
    for (int i = 0; i < n; i++) {
        k.change(i, a[i]);
         k1.change(i, a[i]);
          ls.change(i, pref[i]);
            l1.change(i, pref[i]);
    }
    for (int it = 0; it < q; it++) {
        int l, r;
        cin >> l >> r;
        l--, r--;
        if (pref[r] - (l ? pref[l - 1]: 0) != 0) {
            cout << -1 << '\n';
            continue;
        }
        ll ans = 0;
        ll val = ls.get(l, r) - (l ? pref[l - 1]: 0);
        if (val < 0) {
            cout << -1 << '\n';
            continue;
        }
        chkmax(ans, abs(k.get(l, r)));
        chkmax(ans, abs(k1.get(l, r)));
        chkmax(ans, abs(l1.get(l, r) - (l ? pref[l - 1]: 0)));
        cout << ans << '\n';
    }
}


