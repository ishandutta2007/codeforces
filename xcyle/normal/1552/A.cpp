#define maxn 1000005
using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(!isdigit(ch)){w |= ch == '-';ch = getchar();}
	while(isdigit(ch)){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int n, t[maxn];
char s[maxn];
void solve()
{
	n = read();
	scanf("%s", s + 1);
	for (int i = 1; i <= n; i++) t[i] = s[i] - 'a';
	sort(t + 1, t + n + 1);
	int cnt = 0;
	for (int i = 1; i <= n; i++) if(s[i] - 'a' != t[i]) cnt++;
	printf("%d\n", cnt);
}
int main()
{
	int T = read();
	while(T--) solve(); 
	return 0;
}