void solve() {
	int n, h, l, r;
	cin >> n >> h >> l >> r;
	vector<int> a(n);
	for(int i = 0; i < n; ++i) 
		cin >> a[i];
	vector<vector<int>> dp(n + 1, vector<int> (h + 1, -1));
	dp[0][0] = 0;
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < h; ++j) {
			if (dp[i][j] == -1)
				continue;
			if ((j + a[i]) % h >= l && (j + a[i]) % h <= r)
				dp[i + 1][(j + a[i]) % h] = max(dp[i + 1][(j + a[i]) % h], dp[i][j] + 1);
			else
				dp[i + 1][(j + a[i]) % h] = max(dp[i + 1][(j + a[i]) % h], dp[i][j]);
			if ((j + a[i] - 1) % h >= l && (j + a[i] - 1) % h <= r)
				dp[i + 1][(j + a[i] - 1) % h] = max(dp[i + 1][(j + a[i] - 1) % h], dp[i][j] + 1);
			else
				dp[i + 1][(j + a[i] - 1) % h] = max(dp[i + 1][(j + a[i] - 1) % h], dp[i][j]);
		}
	}
	int ans = 0;
	for(int i = 0; i <= n; ++i) {
		for(int j = 0; j <= h; ++j)
			ans = max(ans, dp[i][j]);
	}
	cout << ans << '\n';
}

signed main() {
	setup();
	// SOLVE;
	solve();

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

