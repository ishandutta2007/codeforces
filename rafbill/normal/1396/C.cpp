template<typename T>
T isqrt(T const&x){
  static_assert(is_integral<T>::value, "is_integral<T>::value");
  assert(x>=T(0));
  T ret = static_cast<T>(sqrtl(x));
  while(ret>0 && ret*ret>x) --ret;
  while(x-ret*ret>2*ret) ++ret;
  return ret;
}

template<typename T>
T icbrt(T const&x) {
  static_assert(is_integral<T>::value, "is_integral<T>::value");
  assert(x>=T(0));
  T ret = static_cast<T>(cbrt(x));
  while(ret>0 && ret*ret*ret>x) --ret;
  while(x-ret*ret*ret>3*ret*(ret+1)) ++ret;
  return ret;
}

const int N = 2'000'000;
li cost0[N];
li cost1[N];

li dpL[N], dpR[N];

int main(){
  ios::sync_with_stdio(0); cin.tie(0);

  li n, r1, r2, r3, d; cin>>n>>r1>>r2>>r3>>d;
  r1 = min(r1, r3);
  vi A(n);
  for(li i = 0; i < (li)(n); ++i) cin>> A[i];

  for(li i = 0; i < (li)(n); ++i) {
    cost1[i] = 1ll<<60;
    smin(cost1[i], A[i] * r1 + r3);
    smin(cost1[i], A[i] * r1 + (r1+r1) + 2*d);
    smin(cost1[i], r2 + r1 + 2*d);

  }

  for(li i = 0; i < (li)(n); ++i) {
    cost0[i] = 1ll<<60;
    smin(cost0[i], A[i] * r1 + r3);
    smin(cost0[i], A[i] * r1 + (r1+r1));
    smin(cost0[i], r2 + r1);

  }

  li ans = 1ll<<60;

  dpL[0] = 0;
  for(li i = 0; i < (li)(n); ++i) {
    dpL[i+1] = dpL[i] + cost1[i];
    if(i) smin(dpL[i+1], dpL[i-1] + cost0[i] + cost0[i-1] + 2*d);
  }
  smin(ans, dpL[n]);

  li rr = cost1[n-1];
  for(li i = (n-2); i >= (li)(0); --i) {
    rr += d + cost0[i];
    smin(ans, rr + dpL[i]);
  }

  cout << (n-1)*d+ans << endl;

  return 0;
}