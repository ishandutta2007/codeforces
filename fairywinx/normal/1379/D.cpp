const int INF = 1e9 + 228;

int m;
int k;

pair<int, int> _need(int M) {
	return {(M + 1) % m, (M + k) % m};
}

pair<int, int> need(int M) {
	auto z = _need(M);
	if (z.first >= m / 2)
		z.first -= m / 2;
	if (z.second >= m / 2)
		z.second -= m / 2;
	return z;
}

void solve() {
	int n, h;
	cin >> n >> h >> m >> k;
	vector<pair<int, int>> a(n);
	for(int i = 0; i < n; ++i) 
		cin >> a[i].first >> a[i].second;
	pair<int, int> ans = {n, 0};
	vector<pair<pair<int, int>, int>> z;
	vector<pair<int, int>> who(n + 1);
	for(int i = 0; i < n; ++i) {
		auto p = need(a[i].second);
		// cout << a[i].second << ' ' << p.first << ' ' << p.second << '\n';
		who[i].first = p.first;
		who[i].second = p.second;
		z.emplace_back(make_pair(p.first, 1), i);
		z.emplace_back(make_pair(p.second, 2), i);
	}
	sort(z.begin(), z.end());
	z.emplace_back(make_pair(INF, 0), -1);	
	vector<int> bad(n);
	int kol = 0;
	for(int i = 0; i < n; ++i) {
		if (who[i].second < who[i].first)
			bad[i] = true, ++kol;
	}
	// cout << kol << '\n';
	for(int j = 0; j < z.size() - 1; ++j) {
		auto i = z[j].first;
		// cout << kol << ' ' << z[j].first.first << ' ' << z[j].first.second << ' ' << z[j].second << '\n';
		if (i.second == 1) {
			if (i.first != 0)
				ans = min(ans, {kol, i.first - 1});
			++kol;
		}
		else {
			--kol;
			ans = min(ans, {kol, i.first});
		}
	}
	vector<int> used(n);
	for(auto i: z) {
		if (i.first.first > ans.second)
			break;
		if (!bad[i.second]) {
			if (i.first.second == 1)
				used[i.second] = 1;
			if (i.first.second == 2)
				used[i.second] = 0;
		}
	}
	for(int i = 0; i < n; ++i) {
		if (bad[i]) {
			// cout << who[i].first << ' ' << who[i].second << ' ' << -ans.second << '\n';
			if (who[i].first <= ans.second || ans.second < who[i].second)
				used[i] = 1;
		}
	}
	cout << ans.first << ' ' << ans.second << '\n';
	for(int i = 0; i < n; ++i) {
		if (used[i])
			cout << i + 1 << ' ';
	}
	cout << '\n';
}

signed main() {
	setup();
	// SOLVE;
	solve();


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

