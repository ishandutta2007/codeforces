int main() {
  ios_base::sync_with_stdio(false); cin.tie(0);
#ifdef LOCAL
  freopen("input.txt", "r", stdin);
#endif
  int n;
  cin >> n;
  vector <int> a(n), b(n);
  vector <bool> sw(n);
  vector <int> who_l(2 * n), who_r(2 * n);
  for (int i = 0; i < n; ++i) {
    cin >> a[i] >> b[i];
    --a[i], --b[i];
    if (a[i] > b[i]) {
      swap(a[i], b[i]);
      sw[i] = 1;
    }
    who_l[a[i]] = i, who_r[b[i]] = i;
  }
  {
    vector <int> id(n);
    iota(id.begin(), id.end(), 0);
    sort(id.begin(), id.end(), [&] (int i, int j) {
      return a[i] < a[j];
    });
    int mn = Inf;
    for (int i : id) {
      if (mn < a[i]) {
        cout << "-1\n";
        return 0;
      }
      mn = b[i];
    }   
  }      
  segtree_max mx(2 * n);
  segtree_min mn(2 * n);
  for (int i = 0; i < n; ++i) {
    mx.modify(a[i], a[i], b[i]);
    mn.modify(b[i], b[i], a[i]);
  }
  vector <int> need(n); 
  vector <int> used(n);
  vector <int> c;
  function <void(int)> dfs = [&] (int u) {
    used[u] = 1;
    c.push_back(u);
    mx.modify(a[u], a[u], -1);
    mn.modify(b[u], b[u], Inf);
    while (true) {
      auto cur = mx.get(a[u], b[u]);
      if (cur.max_r > b[u]) {
        int v = who_r[cur.max_r];
        need[v] = need[u] ^ 1;
        dfs(v);
      } else {
        break;
      }
    }
    while (true) {
      auto cur = mn.get(a[u], b[u]);
      if (cur.min_l < a[u]) {
        int v = who_l[cur.min_l];
        need[v] = need[u] ^ 1;
        dfs(v);
      } else {
        break;
      } 
    }
  };
  int ans = 0;
  for (int i = 0; i < n; ++i) {
    if (!used[i]) {
      c.clear();
      dfs(i);
      vector <int> cnt(2);
      for (int u : c) {
        ++cnt[need[u] ^ sw[u]];
      }
      ans += min(cnt[0], cnt[1]);
    }
  }
  for (int i = 0; i < n; ++i) {
    if (need[i]) swap(a[i], b[i]);
  }   
  {
    vector <int> id(n);
    iota(id.begin(), id.end(), 0);
    sort(id.begin(), id.end(), [&] (int i, int j) {
      return a[i] < a[j];
    });
    for (int i = 1; i < n; ++i) {
      if (b[id[i]] > b[id[i - 1]]) {
        cout << "-1\n";
        return 0;
      }
    }
  }      
  cout << ans << '\n';
}