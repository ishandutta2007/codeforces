#include<bits/stdc++.h>
#define mk make_pair
#define fi first
#define se second
#define ll long long
using namespace std;
const int N=1e5+5;
const int M=1e6+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct BIT
{
	const int n=1e6;
	int t[M];
	void add(int x,int y)
	{
		if (x<=0) return;
		for (;x;x=x&(x-1)) t[x]+=y;
	}
	int query(int x)
	{
		if (x<=0) return 0;
		int ret=0;
		for (;x<=n;x+=x&-x) ret+=t[x];
		return ret;
	}
}t;
struct node
{
	int x,l,r;
	bool operator <(const node &a)const
	{
		return x<a.x;
	}
}c[N];
pair<int,int>a[N],b[N];
ll ans=1;

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=read(),n1=0,n2=0;
	for (int i=1;i<=n;i++)
	{
		int x=read(),l=read(),r=read();
		if (l==0&&r==(int)1e6) ans++;
		if (l==0) a[++n1]=mk(r,x);
			 else b[++n2]=mk(l,x);
	}
	for (int i=1;i<=m;i++)
	{
		int x=read(),l=read(),r=read();
		if (l==0&&r==(int)1e6) ans++;
		c[i]=(node){x,l,r};
	}
	sort(a+1,a+1+n1,less<pair<int,int>>());
	sort(b+1,b+1+n2,greater<pair<int,int>>());
	sort(c+1,c+1+m);
	memset(t.t,0,sizeof(t.t));
	for (int i=1,j=1;i<=n1;i++)
	{
		while (j<=m&&c[j].x<=a[i].fi) t.add(c[j].r,1),t.add(c[j].l-1,-1),j++;
		ans+=t.query(a[i].se);
	}
	memset(t.t,0,sizeof(t.t));
	for (int i=1,j=m;i<=n2;i++)
	{
		while (j&&c[j].x>=b[i].fi) t.add(c[j].r,1),t.add(c[j].l-1,-1),j--;
		ans+=t.query(b[i].se);
	}
	print(ans);
	
	return 0;
}
