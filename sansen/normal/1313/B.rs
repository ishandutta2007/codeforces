use std::io::Write;
use std::cmp;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        p: [(i64, i64, i64); t],
    }
    for (n, x, y) in p {
        let (x, y) = (cmp::min(x, y), cmp::max(x, y));
        let min = if x + y < n + 1 {
            1
        } else {
            x + y - n + 1
        };
        let max = if x + y <= n + 1 {
            x + y - 1
        } else {
            n
        };
        writeln!(out, "{} {}", cmp::min(min, n), max).ok();
    }
}

fn main() {
    run();
}