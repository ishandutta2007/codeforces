const int N = 100000;

int n, m, a[N + 2], suffix[N + 3];

int main() {
  scanf("%d%d", &n, &m);
  a[n + 1] = m;
  for (int i = 1; i <= n; ++i) {
    scanf("%d", a + i);
  }
  suffix[n + 1] = suffix[n + 2] = 0;
  for (int i = n; i >= 0; --i) {
    suffix[i] = (m - a[i]) - suffix[i + 1];
  }
  int result = suffix[0];
  for (int i = 0, prefix = 0; i <= n; ++i) {
    int len = a[i + 1] - a[i];
    if (len > 1) {
      result = std::max(result, prefix + (len - 1) + suffix[i + 1 + (i & 1)]);
    }
    if (~i & 1) {
      prefix += a[i + 1] - a[i];
    }
  }
  printf("%d\n", result);
}