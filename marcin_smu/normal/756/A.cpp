const int MAX = 2e5 + 100;
int f[MAX];
int find(int a){
  return f[a] == a ? a : f[a] = find(f[a]);
}
int n,res;
void uni(int a,int b){
  a = find(a);
  b = find(b);
  if(a == b)return;
  f[a] = b;
  res--;
}
