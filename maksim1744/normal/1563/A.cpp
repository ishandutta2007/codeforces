void test_case(int test) {
    int a, b;
    cin >> a >> b;
    vector<int> ans;
    for (int d = -1; d <= 1; ++d) {
        // ca1 + ca0 = cb1 + cb0 + d = s
        // ca1 + cb0 = a
        // cb1 + ca0 = b
        int s2 = a + b + d;
        if (s2 % 2 != 0) continue;
        int s = s2 / 2;
        for (int ca1 = 0; ca1 <= a; ++ca1) {
            int cb0 = a - ca1;
            int cb1 = s - d - cb0;
            int ca0 = s - ca1;
            if (cb1 < 0 || ca0 < 0) continue;
            assert(ca0 + cb1 == b);
            ans.pb(ca0 + cb0);
        }
    }
    sort(ans.begin(), ans.end());
    ans.erase(unique(ans.begin(), ans.end()), ans.end());
    cout << ans.size() << '\n';
    cout << ans << '\n';
}

