constexpr int md = (int) 1e9 + 7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n, m;
  cin >> n >> m;
  vector<vector<pair<int, int>>> g(n);
  for (int i = 0; i < m; i++) {
    int x, y;
    cin >> x >> y;
    --x; --y;
    g[x].emplace_back(y, i + 1);
    g[y].emplace_back(x, i + 1);
  }
  const int ALPHA = 10;
  const int LOG = 22;
  vector<vector<int>> trie(1, vector<int>(ALPHA, -1));
  vector<vector<int>> pr(1, vector<int>(LOG, -1));
  vector<Mint> res(1, 0);
  vector<int> depth(1, 0);
  vector<int> pc(1, -1);
  auto Go = [&](int v, int num) {
    static vector<int> digits;
    digits.clear();
    while (num > 0) {
      digits.push_back(num % 10);
      num /= 10;
    }
    reverse(digits.begin(), digits.end());
    for (int d : digits) { 
      if (trie[v][d] == -1) {
        int t = (int) trie.size();
        trie[v][d] = t;
        trie.emplace_back(ALPHA, -1);
        pr.emplace_back(LOG, -1);
        pc.push_back(d);
        depth.push_back(depth[v] + 1);
        res.push_back(res[v] * 10 + d);
        pr[t][0] = v;
        for (int j = 1; j < LOG; j++) {
          pr[t][j] = pr[pr[t][j - 1]][j - 1];
          if (pr[t][j] == -1) {
            break;
          }
        }
      }       
      v = trie[v][d];
    }
    return v;
  };
  auto Compare = [&](int x, int y) {
    if (y == -1) {
      return true;
    }
    if (depth[x] != depth[y]) {
      return (depth[x] < depth[y]);
    }
    for (int j = LOG - 1; j >= 0; j--) {
      if (pr[x][j] != pr[y][j]) {
        x = pr[x][j];
        y = pr[y][j];
      }
    }
    return (pc[x] < pc[y]);
  };
  const int inf = (int) 1e9;
  vector<int> dist(n, inf);
  vector<int> ver(n, -1);
  set<pair<int, int>> s;
  s.emplace(0, 0);
  dist[0] = 0;
  ver[0] = 0;
  while (!s.empty()) {
    int i = s.begin()->second;
    s.erase(s.begin());
    for (auto& p : g[i]) {
      int to = p.first;
      int num = p.second;
      int new_ver = Go(ver[i], num);
      if (Compare(new_ver, ver[to])) {
        int new_dist = depth[new_ver];
        if (new_dist < dist[to]) {
          if (dist[to] != inf) {
            s.erase(make_pair(dist[to], to));
          }
          dist[to] = new_dist;
          s.emplace(dist[to], to);
        }
        ver[to] = new_ver;
      }
    }
  }
  for (int i = 1; i < n; i++) {
    cout << res[ver[i]] << '\n';
  }
  return 0;
}