int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	string s, t;
	cin >> s >> t;
	vi cnt(2);
	for(auto &c: s) cnt[c - 'a'] ++;
	for(auto &c: t) cnt[c - 'a'] ++;
	if(cnt[0] % 2 || cnt[1] % 2){
		cout << -1;
		return 0;
	}
	vector<vi> pos(2);
	for(int i = 0; i < n; i ++){
		if(s[i] != t[i]){
			pos[s[i] - 'a'].push_back(i + 1);
		}
	}
	if(pos[0].size() % 2){
		cout << pos[0].size() / 2 + pos[1].size() / 2 + 2 << "\n";
		for(int j = 0; j < 2; j ++){
			for(int i = 1; i < pos[j].size(); i += 2){
				cout << pos[j][i - 1] << " " << pos[j][i] << "\n";
			}
		}
		cout << pos[0].back() << " " << pos[0].back() << "\n" << pos[0].back() << " " << pos[1].back();
	}
	else{
		cout << pos[0].size() / 2 + pos[1].size() / 2 << "\n";
		for(int j = 0; j < 2; j ++){
			for(int i = 1; i < pos[j].size(); i += 2){
				cout << pos[j][i - 1] << " " << pos[j][i] << "\n";
			}
		}
	}
	return 0;
}

