template<typename _T>
struct point{
	_T x,y;
	point(){}
	point(_T x,_T y):x(x),y(y){}
	inline point<_T> operator+(const point<_T> &pt){return point(x+pt.x,y+pt.y);}
	inline point<_T> operator-(const point<_T> &pt){return point(x-pt.x,y-pt.y);}
	inline point<_T> operator*(const _T &a){return point(x*a,y*a);}
	inline point<_T> operator/(const _T &a){return point(x/a,y/a);}
	inline _T operator*(const point<_T> &pt){return x*pt.x+y*pt.y;}//dot
	inline _T operator%(const point<_T> &pt){return x*pt.y-y*pt.x;}//det
	inline _T length(){return x*x+y*y;}
	inline long double operator^(const point<_T> &pt){return acos(*this*pt)/length()/pt.length();}
};
