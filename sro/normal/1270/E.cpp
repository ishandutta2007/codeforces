#include <bits/stdc++.h>
using namespace std;

int n;
int x[1005],y[1005];
vector<int> ans;

bool spli(int a,int b){
	ans.clear();
	for(int i=1;i<=n;i++){
		if(((x[i]+y[i])%a+a)%a==b){
			ans.push_back(i);
		}
	}
	return ans.size()>0&&ans.size()<n;
}

bool splr(int a,int b){
	ans.clear();
	for(int i=1;i<=n;i++){
		if((x[i]%a+a)%a==b){
			ans.push_back(i);
		}
	}
	return ans.size()>0&&ans.size()<n;
}

int main(){
	scanf("%d",&n);
	for(int i=1;i<=n;i++){
		scanf("%d%d",x+i,y+i);
	}
	bool fnd=0;
	for(int len=2;len<=1e8;len<<=1){
		for(int i=0;i<len;i++){
			if(spli(len,i)){
				fnd=1;
				break;
			}
		}
		if(fnd){
			break;
		}
		for(int i=0;i<len;i++){
			if(splr(len,i)){
				fnd=1;
				break;
			}
		}
		if(fnd){
			break;
		}
	}
	printf("%d\n",(int)ans.size());
	for(int i=0;i<ans.size();i++){
		printf("%d ",ans[i]);
	}
	puts("");
	return 0;
}