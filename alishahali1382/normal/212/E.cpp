const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 5010, LOG=20;

int n, m, k, u, v, x, y, t, a, b;
int sz[MAXN];
bitset<MAXN> dp, ans;
vector<int> G[MAXN];

int dfs(int node, int p){
	sz[node]=1;
	for (int i:G[node]) if (i!=p) sz[node]+=dfs(i, node);
	return sz[node];
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=0; i<n-1; i++){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	dfs(1, 0);
	for (int i=1; i<=n; i++){
		dp=1;
		for (int j:G[i]) if (sz[j]<sz[i]) dp|=(dp<<sz[j]);
		dp|=dp<<(n-sz[i]);
		ans|=dp;
	}
	ans[0]=ans[n-1]=0;
	cout<<ans.count()<<'\n';
	for (int i=1; i<=n; i++) if (ans[i]) cout<<i<<' '<<n-i-1<<'\n';
	
	return 0;
}
