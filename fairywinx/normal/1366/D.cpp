const int N = 1e7 + 228;

int mn[N];

signed main() {
	setup();
	// SOLVE;
	fill(mn, mn + N, 0);

	int n;
	cin >> n;
	for(int i = 2; i < N; ++i) {
		if (mn[i] == 0) {
			if (((long long) i * i) >= N)
				continue;
			for(int j = i * i; j < N; j += i) {
				if (mn[j] == 0)
					mn[j] = i;
			}
		}
	}
	vector<pair<int, int>> ans(n);
	for(int i = 0; i < n; ++i) {
		int a;
		cin >> a;
		if (mn[a] == 0) {
			ans[i] = {-1, -1};
			continue;
		}
		int a1 = mn[a];
		while(a % a1 == 0) 
			a = a / a1;
		if (a == 1) {
			ans[i] = {-1, -1};
			continue;
		}
		int c = 1;
		while(a != 1) {
			if (mn[a] == 0) {
				c *= a;
				break;
			}
			int z = mn[a];
			c *= z;
			while(a % z == 0) 
				a /= z;
		}
		ans[i] = {a1, c};
	}
	for(int i = 0; i < n; ++i) 
		cout << ans[i].first << ' ';
	cout << '\n';
	for(int i = 0; i < n; ++i)
		cout << ans[i].second << ' ';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


