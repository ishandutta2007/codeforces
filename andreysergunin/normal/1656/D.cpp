void solve() {
    ll n;
    cin >> n;
    ll k = 0; 
    while (n % (1ll << k) == 0) {
        ++k;
    }
    if (2 * n / (1ll << k) >= (1ll << k) + 1) {
        cout << (1ll << k) << "\n";
        return;
    }
    if (2 * n == (1ll << k)) {
        cout << -1 << "\n";
        return;
    }
    cout << 2 * n / (1ll << k) << endl;
}