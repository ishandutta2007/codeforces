const double pi = acos(-1.);
const int maxn = 100100;



int solution() {

	int n, m;
	scanf("%d%d", &n, &m);
	int lx = mod, rx = 0;
	int ly = mod, ry = 0;
	int s = 0;
	for (int i = 0; i < n; i++) {
		scanf("\n");
		for (int j = 0; j < m; j++) {
			char c = getchar();
			if (c == 'X') {
				s++;
				lx = min(lx, i);
				rx = max(rx, i);
				ly = min(ly, j);
				ry = max(ry, j);
			}
		}
	}

	if (s == (rx - lx + 1) * (ry - ly + 1)) {
		cout << "YES\n";
	} else {
		cout << "NO\n";
	}

	return (0);
}

