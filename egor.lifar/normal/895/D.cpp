static const long long buf_size = 4096;
       
inline long long getChar() {
    static char buf[buf_size];
    static long long len = 0, pos = 0;
    if (pos == len) {
        pos = 0, len = fread(buf, 1, buf_size, stdin);
    }
    if (pos == len) {
        return -1;
    }
    return buf[pos++];
}
       
