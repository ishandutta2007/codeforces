const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 1000010, LOG=20;

ll n, m, k, u, v, x, y, t, a, b, ans;
ll A[MAXN];
ll B[MAXN];
vector<pii> vec[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n; i++){
		cin>>A[i];
		A[i]+=A[i-1];
	}
	for (int i=1; i<=n; i++){
		cin>>B[i];
		B[i]+=B[i-1];
	}
	
	int flag=0;
	if (A[n]>B[n]) swap(A, B), flag=1;
	
	int i=0, j=0;
	for (; i<=n; i++){
		while (B[j+1]<=A[i] && j<n) j++;
		vec[A[i]-B[j]].pb({i, j});
	}
	for (int i=0; i<=n; i++) if (vec[i].size()>=2){
		pii p1=vec[i][0], p2=vec[i][1];
		
		if (flag){
			swap(p1.first, p1.second);
			swap(p2.first, p2.second);
		}
		
		cout<<p2.first-p1.first<<'\n';
		for (int x=p1.first+1; x<=p2.first; x++) cout<<x<<' ';cout<<'\n';
		
		cout<<p2.second-p1.second<<'\n';
		for (int x=p1.second+1; x<=p2.second; x++) cout<<x<<' ';cout<<'\n';
		
		return 0;
	}
	
	return 0;
}
