#include <bits/stdc++.h>
using namespace std;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	cout << fixed << setprecision(15);
	int n, h;
	cin >> n >> h;
	for(auto i = 1; i < n; ++ i){
		cout << h / sqrt(n) * sqrt(i) << " ";
	}
	cout << "\n";
	return 0;
}

