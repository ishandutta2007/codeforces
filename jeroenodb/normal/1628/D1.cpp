const int mxN = 2e3+1, oo = 1e9;
const long long MD = 1e9+7;
template<long long MOD=MD> struct mdint {
    int d=0;
    mdint () {d=0;}
    mdint (long long _d) : d(_d%MOD){};
    friend mdint& operator+=(mdint& a, const mdint& o) {
        a.d+=o.d; if(a.d>=MOD) a.d-=MOD;
        return a;
    }
    friend mdint& operator-=(mdint& a, const mdint& o) {
        a.d-=o.d; if(a.d<0) a.d+=MOD;
        return a;
    }
    friend mdint& operator*=(mdint& a, const mdint& o) {
        return a = mdint((ll)a.d*o.d);
    }
    mdint operator*(const mdint& o) const {
        mdint res = *this;
        res*=o;
        return res;
    }
    mdint operator+(const mdint& o) const {
        mdint res = *this;
        res+=o;
        return res;
    }
    mdint operator-(const mdint& o) const {
        mdint res = *this;
        res-=o;
        return res;
    }
    mdint operator^(long long b) const {
        mdint tmp = 1;
        mdint power = *this;
        while(b) {
            if(b&1) {
                tmp = tmp*power;
            }
            power = power*power;
            b/=2;
        }
        return tmp;
    }
    friend mdint operator/=(mdint& a, const mdint& o) {
        a *= (o^(MOD-2));
        return a;
    }
    mdint operator/(const mdint& o) {
        mdint res = *this;
        res/=o;
        return res;
    }
    bool operator==(const mdint& o) { return d==o.d;}
    bool operator!=(const mdint& o) { return d!=o.d;}
    friend istream& operator>>(istream& c, mdint& a) {return c >> a.d;}
    friend ostream& operator<<(ostream& c, const mdint& a) {return c << a.d;}
};
using  mint = mdint<MD>;
mint dp[mxN][mxN];
bool vis[mxN][mxN];
const mint halve = mint(1)/2;
mint k;
mint solve(int n, int m) {
    if(n==0) return 0;
    if(m==n) return k*n;
    if(m==0) return 0;
    if(dp[n][m]!=-1) return dp[n][m];
    auto h = solve(n-1,m-1), h2 = solve(n-1,m);
    auto use = (h-h2) * halve;
    return dp[n][m] = use+h2;
}
int main() {
    int t; cin >> t;
    while(t--) {
        int n,m; cin >> n >> m >> k;
        // okay
        for(int i=0;i<=n;++i) for(int j=0;j<=m;++j) {
            dp[i][j].d=-1;
        }
        cout << solve(n,m) << '\n';
    }
}