const int p=1e9+7;
const int P=1e9+6;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
struct Matrix {int a[5][5];}t;
struct Vector {int a[5];}a;
Matrix operator *(const Matrix &a,const Matrix &b)
{
	Matrix ret;
	memset(ret.a,0,sizeof(ret.a));
	for (int i=0;i<5;i++)
	for (int k=0;k<5;k++)
	for (int j=0;j<5;j++)
	ret.a[i][j]=(ret.a[i][j]+a.a[i][k]*b.a[k][j])%P;
	return ret;
}
Vector operator *(const Vector &a,const Matrix &b)
{
	Vector ret;
	memset(ret.a,0,sizeof(ret.a));
	for (int i=0;i<5;i++)
	for (int j=0;j<5;j++)
	ret.a[j]=(ret.a[j]+a.a[i]*b.a[i][j])%P;
	return ret;
}
void Power(int b)
{
	while (b)
	{
		if (b&1) a=a*t;
		t=t*t;
		b/=2;
	}
}
int power(int a,int b)
{
	int ret=1;
	while (b)
	{
		if (b&1) ret=ret*a%p;
		a=a*a%p;
		b/=2;
	}
	return ret;
}

