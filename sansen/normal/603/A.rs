fn run() {
    input! {
        _n: usize,
        s: chars,
    }
    let mut s = s.into_iter().map(|s| (s, 1)).collect::<Vec<_>>();
    s.dedup_by(|a, b| {
        a.0 == b.0 && {
            b.1 += a.1;
            true
        }
    });
    let mut ans = s.len();
    if s.iter().any(|p| p.1 > 2) {
        ans += 2;
    } else {
        ans += std::cmp::min(2, s.iter().filter(|p| p.1 > 1).count());
    }
    println!("{}", ans);
}

fn main() {
    run();
}