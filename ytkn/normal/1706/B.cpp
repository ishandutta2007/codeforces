template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int n; cin >> n;
    vector<int> c(n);
    vector<vector<int>> indices(n);
    for(int i = 0; i < n; i++) {
        cin >> c[i];
        c[i]--;
        indices[c[i]].push_back(i);
    }
    auto sub_solve = [&](vector<int> &v) -> int{
        vector<int> dp(2, 0);
        for(int i: v){
            int p = i%2;
            int q = p^1;
            chmax(dp[p], dp[q]+1);
        }
        return max(dp[0], dp[1]);
    };
    for(int r = 0; r < n; r++){
        cout << sub_solve(indices[r]) << ' ';
    }
    cout << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}