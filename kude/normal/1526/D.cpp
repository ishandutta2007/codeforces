using ll = long long;
using P = pair<int,int>;
using VI = vector<int>;
using VVI = vector<VI>;
using VL = vector<ll>;
using VVL = vector<VL>;

const char z[] = "TANO";

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int tt;
    cin >> tt;
    while(tt--) {
        string s;
        cin >> s;
        int n = s.size();
        int hist[4] = {};
        for(char c: s) {
            hist[c % 4]++;
        }
        VI ord(4);
        iota(all(ord), 0);
        ll mx = -1;
        string ans;
        do {
            fenwick_tree<int> ft(n);
            VVI idx(4);
            rrep(i, n) {
                idx[s[i] % 4].push_back(i);
            }
            ll r = 0;
            for(int i: ord) {
                rep(_, hist[i]) {
                    int p = idx[i].back(); idx[i].pop_back();
                    r += ft.sum(p, n);
                    ft.add(p, 1);
                }
            }
            if (r > mx) {
                mx = r;
                ans.clear();
                for(int i: ord) {
                    char c = z[i];
                    rep(_, hist[i]) {
                        ans.push_back(c);
                    }
                }
            }
        } while(next_permutation(all(ord)));
        cout << ans << '\n';
    }
}