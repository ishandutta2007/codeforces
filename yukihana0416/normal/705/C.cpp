int vis[maxn];
int Q[maxn];
vector<int> v[maxn];

void work()
{
    int n,m;
    while(~scanf("%d%d",&n,&m))
    {
        int cnt=1;
        int ans=0;
        int L=1,R=1;
        memset(vis,0,sizeof(vis));
        for(int i=1;i<=n;i++) v[i].clear();
        for(int i=1;i<=m;i++)
        {
            int x,y; read(x), read(y);
            if(x==1)
            {
                v[y].push_back(cnt);
                Q[R++]=cnt;
                cnt++;
                ans++;
            }
            else if(x==2)
            {
                for(int i=0;i<v[y].size();i++)
                    if(!vis[v[y][i]])
                        ans--, vis[v[y][i]]=1;
                v[y].clear();
            }
            else
            {
                while(L<R)
                {
                    int id=Q[L];
                    if(id>y) break;
                    if(!vis[id]) ans--, vis[id]=1;
                    L++;
                }
            }
            printf("%d\n",ans);
        }
    }
}

