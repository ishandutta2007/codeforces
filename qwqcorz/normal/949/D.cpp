#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N],b[N];

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),d=read(),m=read(),sum=0,le=(n+1)/2*m;
	for (int i=1;i<=n;i++)
	{
		int x=read();
		if (sum+x>=le) x-=a[i]=le-sum,sum=le;
				  else a[i]=x,sum+=x,x=0;
		b[i]=x;
	}
	for (int i=1;i<=n;i++) a[i]+=a[i-1];
	for (int i=n;i>=1;i--) b[i]+=b[i+1];
	int ans1=0,ans2=0;
	for (int dec=0,i=1;i<=(n+1)/2;i++) if (a[min(i+i*d,n)]-dec>=m) dec+=m,ans1++;
	for (int dec=0,i=n;i>(n+1)/2;i--) if (b[max(i-(n-i+1)*d,1LL)]-dec>=m) dec+=m,ans2++;
	print(max((n+1)/2-ans1,n/2-ans2));
	
	return 0;
}
