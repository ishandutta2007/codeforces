void test_case(int test) {
    int n;
    ll k;
    cin >> n >> k;
    vector<vector<int>> g(n);
    for (int i = 1; i < n; ++i) {
        int p;
        cin >> p;
        --p;
        g[p].pb(i);
    }
    vector<ll> s(n);
    cin >> s;

    ll ans = y_combinator([&](auto dfs, int v, ll k) -> pair<ll, ll> {
        if (g[v].empty()) {
            return {s[v] * k, s[v]};
        } else if (g[v].size() == 1) {
            auto [x, y] = dfs(g[v][0], k);
            return {k * s[v] + x, y + s[v]};
        } else {
            vector<ll> here;
            ll ans = k * s[v];
            for (int u : g[v]) {
                auto [tot, nx] = dfs(u, k / g[v].size());
                ans += tot;
                here.pb(nx);
            }
            sort(here.rbegin(), here.rend());
            ll left = k % g[v].size();
            for (int i = 0; i < left; ++i)
                ans += here[i];
            return {ans, here[left] + s[v]};
        }
    })(0, k).first;

    cout << ans << '\n';
}

