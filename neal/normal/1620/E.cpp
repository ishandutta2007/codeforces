template<typename T_vector>
void output_vector(const T_vector &v, bool add_one = false, int start = -1, int end = -1) {
    if (start < 0) start = 0;
    if (end < 0) end = int(v.size());

    for (int i = start; i < end; i++)
        cout << v[i] + (add_one ? 1 : 0) << (i < end - 1 ? ' ' : '\n');
}


const int X_MAX = int(5e5) + 5;

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int Q;
    cin >> Q;
    vector<int> type(Q), X(Q), Y(Q);

    for (int q = 0; q < Q; q++) {
        cin >> type[q] >> X[q];

        if (type[q] == 2)
            cin >> Y[q];
    }

    vector<int> A;
    vector<int> final(X_MAX);
    iota(final.begin(), final.end(), 0);

    for (int q = Q - 1; q >= 0; q--)
        if (type[q] == 1)
            A.push_back(final[X[q]]);
        else
            final[X[q]] = final[Y[q]];

    reverse(A.begin(), A.end());
    output_vector(A);
}