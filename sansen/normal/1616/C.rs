fn main() {
    use std::io::Read;
    let mut s = String::new();
    std::io::stdin().read_to_string(&mut s).unwrap();
    let mut sc = scanner::Scanner::new(&s);
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    run(&mut sc, &mut out);
}

fn run<W: Write>(sc: &mut scanner::Scanner, out: &mut std::io::BufWriter<W>) {
    let t: u32 = sc.next();
    for _ in 0..t {
        let n: usize = sc.next();
        let a: Vec<i64> = sc.next_vec(n);
        let mut ans = n - 1;
        for s in 0..n {
            for t in (s + 1)..n {
                let mut cnt = 0;
                for i in 0..n {
                    let d = (t - s) as i64;
                    let nu = (a[t] - a[s]) * (i as i64 - s as i64) + d * a[s];
                    if nu % d == 0 && nu / d == a[i] {
                    } else {
                        cnt += 1;
                    }
                }
                ans = ans.min(cnt);
            }
        }
        writeln!(out, "{}", ans).ok();
    }
}