#include <bits/stdc++.h>
#if defined(__GNUC__)
#pragma GCC optimize ("Ofast")
#endif
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<pii, int> piii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<x<<endl;
#define debugp(x) cerr<<#x<<"= {"<<x.first<<", "<<x.second<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back
#define kill(x) return cout<<x<<'\n', 0;

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

ll n, m, k, x, y, u, v, w, ans;
int G[26][26];
string s, t, out;
char a, b;

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	memset(G, 63, sizeof(G));
	cin>>s>>t>>n;
	if (s.size()!=t.size()) kill(-1);
	while (n--){
		cin>>a>>b>>w;
		u=a-'a';
		v=b-'a';
		G[u][v]=min(G[u][v], (int)w);
	}
	for (int i=0; i<26; i++) G[i][i]=0;
	for (int k=0; k<26; k++) for (int i=0; i<26; i++) for (int j=0; j<26; j++) G[i][j]=min(G[i][j], G[i][k]+G[k][j]);
	for (int i=0; i<s.size(); i++){
		int a=s[i]-'a', b=t[i]-'a', x=0;
		for (int j=1; j<26; j++) if (G[a][j]+G[b][j]<G[a][x]+G[b][x]) x=j;
		ans+=G[a][x]+G[b][x];
		out+=('a'+x);
	}
	if (ans>inf) kill(-1);
	cout<<ans<<endl<<out;
	
	return 0;
}
