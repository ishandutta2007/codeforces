using namespace std;
const int N=3e5+5;
const int M=2005;
const int mod=998244353;
inline int read(){int x=0,f=1;char ch=getchar();while(ch>'9'||ch<'0'){if(ch=='-')f=-1;ch=getchar();}while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}return x*f;}
int ksm(int x,int y){int ret=1;while (y){if (y&1) ret=(ret*x)%mod;x=(x*x)%mod;y>>=1;}return ret;}
int a[N];
int fac[N],inv[N];
int n,ans;
signed main()
{
    cin>>n;
    for (int i=1;i<=2*n;i++) cin>>a[i];
    sort(a+1,a+2*n+1);
    for (int i=1;i<=n;i++) ans-=a[i];
    for (int i=n+1;i<=2*n;i++) ans+=a[i];
    ans%=mod;
    fac[0]=inv[0]=1;
    for (int i=1;i<=2*n;i++) fac[i]=fac[i-1]*i%mod;
    inv[2*n]=ksm(fac[2*n],mod-2);
    for (int i=2*n-1;i>=0;i--) inv[i]=inv[i+1]*i%mod;
    cout<<ans*fac[2*n]%mod*ksm(fac[n],2*mod-4)%mod<<endl;
}