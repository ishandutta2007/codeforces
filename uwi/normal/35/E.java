	void solve()
	{
		int n = ni();
		int[][] es = new int[2*n][];
		for(int i = 0;i < n;i++){
			int h = ni(), s = ni(), t = ni();
			es[2*i] = new int[]{h, s, 1, i};
			es[2*i+1] = new int[]{h, t, -1, i};
		}
		Arrays.sort(es, new Comparator<int[]>() {
			public int compare(int[] a, int[] b) {
				return a[1] - b[1];
			}
		});
		
		int ch = 0;
		int[][] hist = new int[4*n+1][];
		int p = 0;
//		int I = -1000000007;
		MaxHeap heap = new MaxHeap(n);
		for(int i = 0;i < 2*n;i++){
			if(es[i][2] == 1){
				heap.add(es[i][3], es[i][0]);
			}else{
				heap.remove(es[i][3]);
			}
			if(i == 2*n-1 || es[i][1] < es[i+1][1]){
				int nh = heap.max();
				if(ch != nh){
					hist[p++] = new int[]{es[i][1], ch};
					hist[p++] = new int[]{es[i][1], nh};
					ch = nh;
				}
			}
		}
		out.println(p);
		for(int i = 0;i < p;i++){
			out.println(hist[i][0] + " " + hist[i][1]);
		}
	}
	
	public static class MaxHeap {
		public int[] a;
		public int[] map; // 
		public int[] imap; // 
		public int n;
		public int pos;
		public static int INF = 0;
		
		public MaxHeap(int m)
		{
			n = m+2;
			a = new int[n];
			map = new int[n];
			imap = new int[n];
			Arrays.fill(a, INF);
			Arrays.fill(map, -1);
			Arrays.fill(imap, -1);
			pos = 1;
		}
		
		public int add(int ind, int x)
		{
			int ret = imap[ind];
			if(imap[ind] < 0){
				a[pos] = x; map[pos] = ind; imap[ind] = pos;
				pos++;
				up(pos-1);
			}
			return ret != -1 ? a[ret] : x;
		}
		
		public int remove(int ind)
		{
			if(pos == 1)return INF;
			if(imap[ind] == -1)return INF;
			
			pos--;
			int rem = imap[ind];
			int ret = a[rem];
			map[rem] = map[pos];
			imap[map[pos]] = rem;
			imap[ind] = -1;
			a[rem] = a[pos];
			a[pos] = INF;
			map[pos] = -1;
			
			up(rem);
			down(rem);
			return ret;
		}
		
		public int max() { return a[1]; }
		public int argmax() { return map[1]; }
		public int size() {	return pos-1; }
		
		private void up(int cur)
		{
			for(int c = cur, p = c>>>1;p >= 1 && a[p] < a[c];c>>>=1, p>>>=1){
				int d = a[p]; a[p] = a[c]; a[c] = d;
				int e = imap[map[p]]; imap[map[p]] = imap[map[c]]; imap[map[c]] = e;
				e = map[p]; map[p] = map[c]; map[c] = e;
			}
		}
		
		private void down(int cur)
		{
			for(int c = cur;2*c < pos;){
				int b = a[2*c] > a[2*c+1] ? 2*c : 2*c+1;
				if(a[b] > a[c]){
					int d = a[c]; a[c] = a[b]; a[b] = d;
					int e = imap[map[c]]; imap[map[c]] = imap[map[b]]; imap[map[b]] = e;
					e = map[c]; map[c] = map[b]; map[b] = e;
					c = b;
				}else{
					break;
				}
			}
		}
	}
	
