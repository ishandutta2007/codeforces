typedef long long LL;
typedef unsigned long long ULL;
#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 1e6 + 10;
int a[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n, m;
	scanf("%d%d", &n, &m);
	for (int i = 1; i <= n; i++) scanf("%d", &a[i]);
	LL ans = 0;
	LL l = 1, r = n;
	while (l <= r)
	{
		ans += (a[r] - a[l]) * 2LL;
		r -= m;
		l += m;
	}
	printf("%lld\n", ans);
	return 0;
}