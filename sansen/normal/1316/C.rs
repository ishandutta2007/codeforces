fn run() {
    input! {
        n: usize,
        m: usize,
        p: u64,
        a: [u64; n],
        b: [u64; m],
    }
    let mut a = a;
    let mut b = b;
    for x in a.iter_mut().chain(b.iter_mut()) {
        *x %= p;
    }
    let mut x = 0;
    let mut y = 0;
    while x < n && y < m {
        if a[x] == 0 {
            x += 1;
        } else if b[y] == 0 {
            y += 1;
        } else {
            println!("{}", x + y);
            return;
        }
    }

}

fn main() {
    run();
}