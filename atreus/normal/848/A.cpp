#include <bits/stdc++.h>

using namespace std;

const int maxn = 1e5;
int a[maxn];
int pl[maxn];
int main (){
    ios_base::sync_with_stdio(false);
    int n, b = 0;
    cin >> n;
    a[1] = 1;
    for (int i = 2; i <= n; i++){
        a[i] = a[i - 1] + i;
    }
    int p = 0;
    while (n > 0){
        for (int i = 1; i <= n; i++){
            if (a[i] <= n){
                b = a[i];
                pl[p] = i;
            }
            else
                break;
        }
        p++;
        n -= b;
    }
    string s;
    for (int i = 0; i < p; i++){
        while (pl[i] >= 0){
            s += 'a' + i;
            pl[i]--;
        }
    }
    if (s.size() == 0)
        s += "reza";
    cout << s << endl;
}