constexpr int mod = 1'000'000'007;
using Mint = Montgomery<mod>;

void solve() {
	int n = nxt(), k = nxt();
	if (k >= n) {
		cout << Mint(2).pow(n) << "\n";
		return;
	}
	InvfactStuff<mod> stuff(n);
	--n, --k;
	n -= k;
	Mint ans = stuff.C(n + k + 1, k + 1);
	for (int i = 0; i < k; ++i) {
		ans += stuff.C(n + k, i) * 2;
	}
	ans += stuff.C(n + k, k);
	cout << ans << "\n";
}

