void solve() {
    int n;
    sc.read(n);

    VV<int> mp(2 * n, V<int>(n));
    for (int i = 0; i < 2 * n; i++) {
        for (int j = 0; j < n; j++) {
            sc.read(mp[i][j]);
        }
    }

    VV<int> g(2 * n, V<int>(2 * n));
    for (int i = 0; i < 2 * n; i++) {
        for (int j = i + 1; j < 2 * n; j++) {
            for (int k = 0; k < n; k++) {
                if (mp[i][k] == mp[j][k]) {
                    g[i][j] = g[j][i] = 1;
                    break;
                }
            }
        }
    }
    
    struct E {
        int to;
    };

    VV<E> g0(2 * n);
    for (int i = 0; i < 2 * n; i++) {
        for (int j = i + 1; j < 2 * n; j++) {
            if (g[i][j]) {
                g0[i].push_back({j});
                g0[j].push_back({i});
            }
        }
    }
    MaxMatching<E> mt(g0);

    using P = array<int, 2>;
    V<P> pairs;
    for (int i = 0; i < 2 * n; i++) {
        if (i < mt.mt[i]) {
            pairs.push_back({i, mt.mt[i]});
        }
    }
    atcoder::two_sat ts(n);
    atcoder::dsu d(2 * n);

    for (int i = 0; i < n; i++) {
        for (int j = i + 1; j < n; j++) {
            auto a = pairs[i], b = pairs[j];

            for (int x = 0; x < 2; x++) {
                for (int y = 0; y < 2; y++) {
                    if (g[a[x]][b[y]]) {
                        ts.add_clause(i, x == 0, j, y == 0);
                        d.merge(2 * i + x, 2 * j + (1 - y));
                        d.merge(2 * i + (1 - x), 2 * j + y);
                    }
                }
            }
        }
    }

    ts.satisfiable();

    auto ans = ts.answer();

    V<int> sol;
    for (int i = 0; i < n; i++) {
        if (ans[i]) swap(pairs[i][0], pairs[i][1]);
        sol.push_back(pairs[i][0]);
    }

    atcoder::scc_graph scc_g(n);
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            if (i == j) continue;
            if (g[pairs[i][1]][pairs[j][0]]) {
                scc_g.add_edge(i, j);
            }
        }
    }
    auto u = scc_g.scc();
    dbg(u);
    int cnt = 0;    
    for (auto v : scc_g.scc()) {
        V<int> our(n);
        for (auto x : v) our[x] = true;

        bool ok = true;

        for (int i = 0; i < n; i++) {
            if (!our[i]) continue;
            for (int j = 0; j < n; j++) {
                if (i == j) continue;
                if (our[j] && g[pairs[i][1]][pairs[j][1]]) ok = false;
                if (!our[j] && g[pairs[i][1]][pairs[j][0]]) ok = false;
            }
        }
        if (ok) cnt++;
    }

    dbg(sol, cnt);

    pr.writeln(modint998244353(2).pow(cnt).val());
    for (int x : sol) {
        pr.write(x + 1);
        pr.write(' ');
    }
    pr.writeln();
}

