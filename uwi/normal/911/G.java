	public static class SegmentTreeArray {
		public int M, H, N;
		public int[][] node;
		public int K = 101;
		
		public SegmentTreeArray(int n)
		{
			N = n;
			M = Integer.highestOneBit(Math.max(N-1, 1))<<2;
			H = M>>>1;
			
			node = new int[M][];
			for(int i = 0;i < N;i++){
				int[] item = new int[K];
				for(int j = 0;j < K;j++)item[j] = j;
				
				node[H+i] = item;
			}
			for(int i = H-1;i >= 1;i--)propagate(i);
		}
		
		private void propagate(int cur)
		{
			node[cur] = prop2(node[2*cur], node[2*cur+1], node[cur]);
		}
		
		private int[] prop2(int[] L, int[] R, int[] C)
		{
			if(L != null && R != null){
				if(C == null)C = new int[K];
				for(int i = 0;i < K;i++){
					C[i] = R[L[i]];
				}
				return C;
			}else if(L != null){
				return prop1(L, C);
			}else if(R != null){
				return prop1(R, C);
			}else{
				return null;
			}
		}
		
		private int[] prop1(int[] L, int[] C)
		{
			if(C == null)C = new int[K];
			System.arraycopy(L, 0, C, 0, K);
			return C;
		}
		
		public void update(int x, int f, int t) {
			node[H+x][f] = t;
			for(int i = H+x>>>1;i >= 1;i>>>=1)propagate(i);
		}
	}

	
