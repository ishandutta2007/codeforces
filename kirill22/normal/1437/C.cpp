void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    sort(all(a));
    int dp[n][500];
    for (int i = 0; i < n; i++) for (int j = 0; j < 500; j++) dp[i][j] = -1;
    for (int i = 1; i <= a[0]; i++) dp[0][i] = a[0] - i;
    for (int i = 1; i < n; i++) {
        int x = a[i], tmp = -1;
        for (int j = 1; j < 500; j++) {
            if (tmp == -1) tmp = dp[i - 1][j - 1];
            else if (dp[i - 1][j - 1] != -1) tmp = min(tmp, dp[i - 1][j - 1]);
            if (tmp != -1) {
                dp[i][j] = tmp + abs(x - j);
            }
        }
    }
    int ans = 1e18;
    for (int i = 1; i < 500; i++) if (dp[n - 1][i] != -1) ans = min(ans, dp[n - 1][i]);
    cout << ans << '\n';
}

signed main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int q;
    cin >> q;
    while (q--) {
        solve();
    }
}