	void solve()
	{
		int n = ni();
		int[] a = na(n);
		SegmentTreeRMQ st = new SegmentTreeRMQ(a);
		long[] cc = new long[3000000];
		int p = 0;
		for(int i = 0;i < n;i++){
			int pos = i;
			int cg = a[i];
			while(cg > 1 && pos < n){
				int low = st.firstle(pos, cg);
				if(low == -1)low = n;
				cc[p++] = (long)cg<<32|low-pos;
				pos = low;
				cg = st.min(i, low+1);
			}
			if(pos < n){
				cc[p++] = 1L<<32|n-pos;
			}
		}
		cc = Arrays.copyOf(cc, p);
		cc = radixSort(cc);
		
		int[] num = new int[p];
		long[] freq = new long[p];
		int z = 0;
		for(int i = 0;i < p;i++){
			if(i == 0 || cc[i]>>>32 != cc[i-1]>>>32){
				num[z++] = (int)(cc[i]>>>32);
			}
			freq[z-1] += (int)cc[i];
		}
		int Q = ni();
		for(int i = 0;i < Q;i++){
			int m = ni();
			int ind = Arrays.binarySearch(num, 0, z, m);
			if(ind < 0){
				out.println(0);
			}else{
				out.println(freq[ind]);
			}
		}
	}
	
	public static long[] radixSort(long[] f)
	{
		long[] to = new long[f.length];
		{
			int[] b = new int[65537];
			for(int i = 0;i < f.length;i++)b[1+(int)(f[i]&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < f.length;i++)to[b[(int)(f[i]&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < f.length;i++)b[1+(int)(f[i]>>>16&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < f.length;i++)to[b[(int)(f[i]>>>16&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < f.length;i++)b[1+(int)(f[i]>>>32&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < f.length;i++)to[b[(int)(f[i]>>>32&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < f.length;i++)b[1+(int)(f[i]>>>48&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < f.length;i++)to[b[(int)(f[i]>>>48&0xffff)]++] = f[i];
			long[] d = f; f = to;to = d;
		}
		return f;
	}
	
	
	public static int gcd(int a, int b) {
		while (b > 0) {
			int c = a;
			a = b;
			b = c % b;
		}
		return a;
	}
	
	public static class SegmentTreeRMQ {
		public int M, H, N;
		public int[] st;
		
		public SegmentTreeRMQ(int n)
		{
			N = n;
			M = Integer.highestOneBit(Math.max(N-1, 1))<<2;
			H = M>>>1;
			st = new int[M];
			Arrays.fill(st, 0, M, Integer.MAX_VALUE);
		}
		
		public SegmentTreeRMQ(int[] a)
		{
			N = a.length;
			M = Integer.highestOneBit(Math.max(N-1, 1))<<2;
			H = M>>>1;
			st = new int[M];
			for(int i = 0;i < N;i++){
				st[H+i] = a[i];
			}
			Arrays.fill(st, H+N, M, Integer.MAX_VALUE);
			for(int i = H-1;i >= 1;i--)propagate(i);
		}
		
		private void propagate(int i)
		{
			st[i] = gcd(st[2*i], st[2*i+1]);
		}
		
		
		public int min(int l, int r){ return l >= r ? 0 : min(l, r, 0, H, 1);}
		
		private int min(int l, int r, int cl, int cr, int cur)
		{
			if(l <= cl && cr <= r){
				return st[cur];
			}else{
				int mid = cl+cr>>>1;
				int ret = -1;
				if(cl < r && l < mid){
					int res = min(l, r, cl, mid, 2*cur);
					if(ret == -1){
						ret = res;
					}else{
						ret = gcd(ret, res);
					}
				}
				if(mid < r && l < cr){
					int res = min(l, r, mid, cr, 2*cur+1);
					if(ret == -1){
						ret = res;
					}else{
						ret = gcd(ret, res);
					}
				}
				return ret;
			}
		}
		
		public int firstle(int l, int v) {
			int cur = H+l;
			while(true){
				if(st[cur] % v != 0){
					if(cur < H){
						cur = 2*cur;
					}else{
						return cur-H;
					}
				}else{
					cur++;
					if((cur&cur-1) == 0)return -1;
					if((cur&1)==0)cur>>>=1;
				}
			}
		}
		
		public int lastle(int l, int v) {
			int cur = H+l;
			while(true){
				if(st[cur] <= v){
					if(cur < H){
						cur = 2*cur+1;
					}else{
						return cur-H;
					}
				}else{
					if((cur&cur-1) == 0)return -1;
					cur--;
					if((cur&1)==1)cur>>>=1;
				}
			}
		}
	}

	
