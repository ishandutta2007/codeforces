void solve() {
    ll n;
    cin >> n;
    ll answer = LLONG_MAX;
    for (int k1 = 0; k1 <= 100; k1++)
        for (int k2 = 0; k2 <= 100; k2++)
            answer = min(answer, k1 * 15 + k2 * 20 + (max(0ll, n - k1 * 6 - k2 * 8) + 9) / 10 * 25);

    cout << answer << '\n';
}

int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);

    int tests;
    cin >> tests;
    while (tests--)
        solve();
}