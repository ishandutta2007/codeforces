use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        ask: [(usize, bytes, bytes); t],
    }
    for (_n, a, b) in ask {
        if a.iter().zip(b.iter()).any(|p| *p.0 > *p.1) {
            writeln!(out, "-1").ok();
            continue;
        }
        let w = 20;
        let mut u = union_find::UF::new(w);
        for (a, b) in a.iter().zip(b.iter()) {
            let a = (*a - b'a') as usize;
            let b = (*b - b'a') as usize;
            u.unite(a, b);
        }
        let mut ans = 0;
        for i in 0..w {
            if i == u.root(i) {
                ans += u.get_size(i) - 1;
            }
        }
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}