void setup() {
	#ifdef LOCAL
		freopen("input.txt", "r", stdin);
		freopen("output.txt", "w", stdout);
	#else
		ios_base::sync_with_stdio(0);
		cin.tie(0);
		cout.tie(0);
	#endif 
	srand(time(0));
}

const int C = 27;

int cnt[C][C];

signed main() {
	setup();
	for(int i = 0; i < C; ++i)
		fill(cnt[i], cnt[i] + C, 0);
	int n;
	cin >> n;
	string s;
	cin >> s;
	vector<int> ans(n);
	int mx = 0;
	for(int i = 0; i < n; ++i) {
		int c = s[i] - 'a';
		vector<int> now(C);
		for(int j = c + 1; j < C; ++j) {
			for(int k = 0; k < C; ++k)
				now[k] += cnt[j][k];
		}
		// for(int j = 0; j < C; ++j) {
		// 	cout << now[j] << ' ';
		// }
		// cout << '\n';
		for(int j = 0; j < C; ++j) {
			if (!now[j]) {
				ans[i] = j + 1;
				mx = max(mx, ans[i]);
				cnt[c][ans[i] - 1]++;
				break;
			}
		}
	}
	cout << mx << '\n';
	for(auto i: ans)
		cout << i << ' ';
	cout << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

