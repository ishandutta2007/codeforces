const int Mod = 1000000007;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}



const int MAXN = 305;


int n, m;
int a[MAXN * MAXN];
int b[MAXN * MAXN];
int c[MAXN * MAXN];
int d[MAXN][MAXN];


bool comp(int i, int j) {
    return a[i] < a[j] || (a[i] == a[j] && i > j);
}


void solve() {
    cin >> n >> m;
    for (int i = 0; i < n * m; i++) {
        cin >> a[i];
        b[i] = i;
    }    
    sort(b, b + n * m, comp);
    for (int i = 0; i < n * m; i++) {
        c[b[i]] = i;
    }
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            d[i][j] = 0;
        }
    }
    int ans = 0;
    for (int i = 0; i < n * m; i++) {
        int pos = c[i] / m;
        int pos1 = c[i] % m;
        //cout << pos << ' ' << pos1 << endl;
        for (int k = 0; k < pos1; k++) {
            ans += d[pos][k];
        }
        d[pos][pos1] = true;
    }
    cout << ans << '\n';
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    //read(FILENAME);
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}