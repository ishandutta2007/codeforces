string s;
int n, ans, cnts[MAXN][30], cntt[MAXN][30];
vector<int> ls[30], lt[30];

void Solve () {
    cin >> n >> s;
    string t = s;
    reverse(all(t));
    for (int i = 0; i < n; i++){
        ls[s[i] - 'a'].pb(i); lt[t[i] - 'a'].pb(i);
        for (int j = 0; j < 26; j++){
            cnts[i][j] = (i ? cnts[i - 1][j] : 0) + (s[i] - 'a' == j);
            cntt[i][j] = (i ? cntt[i - 1][j] : 0) + (t[i] - 'a' == j);
        }
    }
    for (int i = 0; i < 26; i++){
        for (int j = 0; j < ls[i].size(); j++){
            int sp = ls[i][j], tp = lt[i][j];
            for (int k = i + 1; k < 26; k++)
                ans += abs(cnts[sp][k] - cntt[tp][k]);
        }
    }
    cout << ans << endl;
}

