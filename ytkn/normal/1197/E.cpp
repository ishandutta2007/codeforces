ModInt pow(ModInt a, ll n) {
    assert(n >= 0);
	ModInt ans = 1;
    while (n > 0) {
        if (n&1) ans = ans*a;
        a = a*a;
        n >>= 1;
    }
	return ans;
}

template<typename T>
class Compress{
    public:
    vector<T> data;
    int offset;
    Compress(vector<T> data_, int offset=0): offset(offset){
        set<T> st;
        for(T x: data_) st.insert(x);
        for(T x: st) data.push_back(x);
    };
    int operator[](T x) {
        auto p = lower_bound(data.begin(), data.end(), x);
        assert(x == *p);
        return offset+(p-data.begin());
	}
    T inv(int x){
        return data[x-offset];
    }
    int size(){
        return data.size();
    }
};

using mint = ModInt;
const ll inf = 1e15;

struct edge{
    int to;
    ll len;
};

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n; cin >> n;
    vector<ll> in(n), out(n);
    vector<ll> v;
    for(int i = 0; i < n; i++){
        cin >> out[i] >> in[i];
        v.push_back(in[i]);
        v.push_back(out[i]);
    }
    ll max_in = *max_element(in.begin(), in.end());
    auto cp = Compress<ll>(v);
    int m = cp.size();
    vector<vector<edge>> nx(m);
    for(int i = 0; i < n; i++){
        int out_idx = cp[out[i]];
        int in_idx = cp[in[i]];
        nx[out_idx].push_back(edge{in_idx, out[i]-in[i]});
    }
    for(int i = 1; i < m; i++){
        nx[i].push_back(edge{i-1, 0});
    }
    vector<ll> mn(m, inf);
    auto dp = vec2d(m, 2, mint(0));
    for(int i = 0; i < n; i++){
        if(out[i] <= max_in) continue;
        int out_idx = cp[out[i]];
        int in_idx = cp[in[i]];
        dp[in_idx][1] += 1;
        mn[in_idx] = in[i];
    }
    for(int i = m-1; i >= 0; i--){
        if(mn[i] == inf) continue;
        for(edge e: nx[i]){
            int to = e.to;
            if(chmin(mn[to], mn[i]-e.len)){
                dp[to][0] = 0;
                dp[to][1] = 0;
            }
            if(mn[to] == mn[i]-e.len){
                int j = e.len == 0 ? 0 : 1;
                dp[to][j] += dp[i][0]+dp[i][1];
            }
        }
    }
    ll x = *min_element(mn.begin(), mn.end());
    mint ans = 0;
    for(int i = 0; i < m; i++){
        if(x == mn[i]) ans += dp[i][1];
    }
    assert(x > 0);
    cout << ans << endl;
}