const int N = 5e4 + 228;

map<int, vector<pair<pair<int, int>, int>>> pt;
vector<int> a[N];
int used[N];

void f(vector<pair<pair<int, int>, int>> b, int _) {
	map<int, vector<pair<int, int>>> z;
	for(auto i: b) 
		z[i.first.first].emplace_back(i.first.second, i.second - 1);
	for(auto i: z) {
		sort(all(i.second));
		for(int j = 0; j + 1 < i.second.size(); j += 2) {
			cout << i.second[j].second + 1 << ' ' << i.second[j + 1].second + 1 << '\n';
			used[i.second[j].second] = 1, used[i.second[j + 1].second] = 1;
		}
	}
	vector<pair<pair<int, int>, int>> c;
	for(auto i: b) {
		if (!used[i.second - 1])
			c.push_back(i);
	}
	sort(c.begin(), c.end());
	for(int i = 0; i + 1 < c.size(); i += 2) {
		cout << c[i].second << ' ' << c[i + 1].second << '\n';
		used[c[i].second - 1] = 1, used[c[i + 1].second - 1] = 1;
	}
}

signed main() {
	setup();
	// SOLVE;

	fill(used, used + N, 0);

	int n;
	cin >> n;
	for(int i = 0; i < n; ++i) {
		int x, y, z;
		cin >> x >> y >> z;
		a[i] = {x, y, z};
		pt[x].push_back({{y, z}, i + 1});
	}
	int _ = 0;
	for(auto i: pt)
		f(i.second, _++);
	vector<pair<pair<int, int>, pair<int, int>>> c;
	for(int i = 0; i < n; ++i) {
		if (!used[i])
			c.push_back({{a[i][0], a[i][1]}, {a[i][2], i}});
	}
	sort(c.begin(), c.end());
	for(int i = 0; i + 1 < c.size(); i += 2) 
		cout << c[i].second.second + 1 << ' ' << c[i + 1].second.second + 1 << '\n';


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


