fn main() {
    use std::io::Read;
    let mut s = String::new();
    std::io::stdin().read_to_string(&mut s).unwrap();
    let mut sc = scanner::Scanner::new(&s);
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    run(&mut sc, &mut out);
}

// 
// 0
// a[n] + k
// 
// a[1]
// 0 + N-1-k
// 
// 
// 
// k
// 0, 1
// a[n] + k

struct R;
impl TE for R {
    type T = (i64, usize);
    type E = i64;
    fn fold(l: &Self::T, r: &Self::T) -> Self::T {
        std::cmp::min(*l, *r)
    }
    fn eval(x: &Self::T, f: &Self::E) -> Self::T {
        (x.0 + *f, x.1)
    }
    fn merge(g: &Self::E, h: &Self::E) -> Self::E {
        *g + *h
    }
    fn e() -> Self::T {
        (std::i64::MAX / 2, std::usize::MAX / 2)
    }
    fn id() -> Self::E {
        0
    }
}

fn run<W: Write>(sc: &mut scanner::Scanner, out: &mut std::io::BufWriter<W>) {
    let t: u32 = sc.next();
    for _ in 0..t {
        let n: usize = sc.next();
        let k: usize = sc.next();
        let a: Vec<i64> = sc.next_vec(n);
        let ini = a.iter().enumerate().map(|(i, a)| (-*a + (n - 1 - i) as i64, i)).collect::<Vec<_>>();
        let mut seg = LazySegmentTree::<R>::build_by(&ini);
        let mut sum = a.iter().sum::<i64>();
        for _ in 0..k {
            let (v, k) = seg.find(0, n);
            sum += v;
            seg.update(0, n, -1);
            seg.update(k, k + 1, std::i64::MAX / 2);
        }
        writeln!(out, "{}", sum).ok();
    }
}