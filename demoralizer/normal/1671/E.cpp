int n;
string s;
int ans = 1;

string get(int v){
	if(v >= (1 << n)){
		return "";
	}
	int l = 2 * v;
	int r = 2 * v + 1;
	
	string a = get(l);
	string b = get(r);
	string c = "";
	c += s[v];
	if(a == b){
		return c + a + b;
	}
	if(b < a) swap(a,b);
	ans = ans * 2 % M;
	return c + a + b;
}

void solve(){
	cin >> n >> s;
	
	reverse(all(s));
	s.pb('0');
	reverse(all(s));
	
	get(1);
	
	cout << ans;
}
