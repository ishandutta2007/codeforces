static constexpr usize sigma = 26;

u64 solve(const usize n, std::vector<std::vector<int>> s) {
  const usize m = s.size();
  u64 ans = 2 * u64(m) * (m - 1) / 2;
  std::vector<int> cat;
  for (const auto &str : s) {
    cat.insert(cat.end(), str.begin(), str.end());
    cat.push_back(sigma);
    cat.insert(cat.end(), str.begin(), str.end());
  }
  const auto sa = atcoder::suffix_array(cat, sigma);
  auto lcp = atcoder::lcp_array(cat, sa);
  lcp.insert(lcp.begin(), 0);
  lcp.push_back(0);

  const usize T = sa.size();

  std::vector<int> inv(T);
  for (const usize i : rep(0, T)) {
    inv[sa[i]] = i;
  }

  std::vector<int> query(T, -1);
  for (const usize i : rep(0, m)) {
    usize prev = 0;
    for (const usize j : rep(1, n + 1)) {
      if (j == n || s[i][j - 1] > s[i][j]) {
        query[inv[(2 * n + 1) * i + j]] = 1 + n - (j - prev);
        prev = j;
      }
    }
  }

  for (const usize loop : rep(0, 2)) {
    std::reverse(query.begin(), query.end());
    std::reverse(lcp.begin(), lcp.end());

    std::vector<usize> stack;
    usize pos = 0;
    for (const usize i : rep(0, T)) {
      while (!stack.empty() && lcp[stack.back()] >= lcp[i]) {
        stack.pop_back();
      }
      stack.push_back(i);
      pos = std::min(pos, stack.size() - 1);
      while (pos != stack.size() - 1 && lcp[stack[pos + 1]] < n) {
        pos += 1;
      }

      if (query[i] == -1) {
        continue;
      }
      usize t = pos;
      while (lcp[stack[t]] >= query[i]) {
        t -= 1;
      }
      ans -= i - stack[t];
    }
  }

  return ans;
}

