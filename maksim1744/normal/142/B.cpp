int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n, m;
    cin >> n >> m;
    int ans = 0;
    for (int i = 0; i < 2; ++i) {
        ans = max({ans, (n + 2) / 3 * m, (m + 2) / 3 * n});
        int cntn = n / 4 * 2 + min(n % 4, 2);
        int cntm = m / 4 * 2 + min(m % 4, 2);
        ans = max(ans, cntn * cntm);
        if (n % 2 == 0 || m % 2 == 0) ans = max(ans, n * m / 2);
        else {
            int cnt = n * (m - 1) / 2;
            cnt += (n + 1) / 2;
            ans = max(ans, cnt);
        }
        swap(n, m);
    }
    cout << ans << endl;

    return 0;
}