typedef complex<double> comp;

const int N = 7e2;

int n, m;
int ans[N];
bool used[N];
int edge[N][N];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n >> m;
    for (int i = 0; i < m; ++i) {
        int a, b;
        cin >> a >> b;
        cin >> edge[a][b];
    }
    for (int i = 0; i < n; ++i) {
        memset(used, 0, sizeof(used));
        memset(ans, -1, sizeof(ans));
        ans[i] = 0;
        for (int t = 0; t < n + 1; ++t) {
            int j = -1;
            for (int q = 0; q < n; ++q) {
                if (used[q] || ans[q] == -1) continue;
                if (j == -1 || ans[q] < ans[j]) {
                    j = q;
                }
            }
            // cout << "cur: " << j << '\n';
            int dist = ans[j];
            if (t) {
                used[j] = true;
            } else {
                ans[i] = -1;
            }
            int need = (-dist % n + n) % n;
            for (int q = 0; q < n; ++q) {
                if (edge[j][need]) {
                    if (ans[q] == -1) ans[q] = 2e9;
                    chkmin(ans[q], dist + edge[j][need]);
                }   
                ++need;
                if (need == n) {
                    need = 0;
                }
            }
            if (t) {
                if (ans[(j + 1) % n] == -1) ans[(j + 1) % n] = 2e9;
                chkmin(ans[(j + 1) % n], dist + 1);
            }
        }
        ans[i] = 0;
        for (int j = 0; j < n; ++j) {
            cout << ans[j] << ' ';
        }
        cout << '\n';
    }
}