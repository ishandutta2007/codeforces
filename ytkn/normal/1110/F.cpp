struct edge{
    int to;
    ll cost;
    edge(int to, ll cost): to(to), cost(cost) {}
};

/**
 * [maspy](https://maspypy.com/euler-tour-%E3%81%AE%E3%81%8A%E5%8B%89%E5%BC%B7#toc1)()
 */ 
class EulerTour{
    public:
    vector<vector<edge>> g;
    int root;
    int n;
    vector<ll> depth;
    vector<int> ord;
    vector<int> in, out;
    EulerTour(vector<vector<edge>> g, int root): g(g), root(root){
        n = g.size();
        depth.resize(n);
        in.resize(n);
        out.resize(n);
        build();
    }
    private:
    void build(){
        dfs(root);
        for(int i = 0; i < n; i++) in[i] = 1e9;
        for(int i = 0; i < ord.size(); i++){
            chmin(in[ord[i]], i);
            chmax(out[ord[i]], i);
        }
        assert(ord.size() == 2*n);
    }
    void dfs(int v){
        ord.push_back(v);
        int cnt = 0;
        for(edge e: g[v]){
            depth[e.to] = depth[v]+e.cost;
            dfs(e.to);
            cnt++;
        }
        ord.push_back(v);
    }
};

struct query{
    int idx, l, r;
    query(int idx, int l, int r): idx(idx), l(l), r(r) {}
};

const ll inf = 1e18;

ll op(ll x, ll y){
    return min(x, y);
}

ll e(){
    return inf;
}

ll add(ll x, ll y){
    return x+y;
}

ll id(){
    return 0;
}

using Seg = atcoder::lazy_segtree<ll, op, e, ll, add, add, id>;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, q; cin >> n >> q;
    vector<vector<edge>> g(n);
    for(int i = 1; i < n; i++){
        int p; ll w; cin >> p >> w; p--;
        g[p].push_back(edge(i, w));
    }
    EulerTour et(g, 0);
    vector<int> in_ord;
    for(int i = 0; i < n; i++) in_ord.push_back(et.in[i]);
    sort(in_ord.begin(), in_ord.end());
    vector<vector<query>> queries(n);
    for(int i = 0; i < q; i++){
        int v, l, r; cin >> v >> l >> r; v--; l--; r--;
        l = in_ord[l];
        r = in_ord[r];
        queries[v].push_back(query(i, l, r));
    }
    vector<ll> ans(q);
    vector<ll> dists(2*n, inf);
    for(int i = 0; i < 2*n; i++){
        int vv = et.ord[i];
        if(g[vv].empty()){
            dists[i] = et.depth[vv];
        }
    }
    Seg seg(dists);
    function<void(int)> dfs = [&](int v){
        for(query q: queries[v]){
            ans[q.idx] = seg.prod(q.l, q.r+1);
        }
        for(edge e: g[v]){
            seg.apply(0, 2*n, e.cost);
            seg.apply(et.in[e.to], et.out[e.to]+1, -2*e.cost);
            dfs(e.to);
            seg.apply(et.in[e.to], et.out[e.to]+1, 2*e.cost);
            seg.apply(0, 2*n, -e.cost);
        }
    };
    dfs(0);
    print_vector(ans, '\n');
}