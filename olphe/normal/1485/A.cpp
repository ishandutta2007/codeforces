//int N, M, K, T, H, W, L, R;
long long int N, M, K, T, H, W, L, R;

void Solve() {
	cin >> N >> M;
	long long int ans = MOD;
	for (int i = M; i <= M + 100; i++) {
		if (i == 1)continue;
		long long int add = 0;
		K = N;
		while (K) {
			K /= i;
			add++;
		}
		ans = min(ans, i - M + add);
	}
	cout << ans << endl;
}

int main() {
	ios::sync_with_stdio(false);
	cin.tie(0);

	cin >> T;
	while (T--) {
		Solve();
	}
}