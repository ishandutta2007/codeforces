const int N = 5e3 + 5;
const int M = 1e9;

int n, m;
int max_ans[N];

int cnt[N * 3];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
//    freopen("input", "r", stdin);
#endif
    cin >> n >> m;
    for (int i = 1; i < N; ++i) {
        max_ans[i] = max_ans[i - 1] + ((i - 1) >> 1);
    }
    if (max_ans[n] < m) {
        cout << "-1\n";
        return 0;
    }
    int i = 0;
    while (i + 1 < n && max_ans[i + 1] <= m) {
        ++i;
    }
    m -= max_ans[i];
    vector<int> ans;
    for (int x = 1; x <= i; ++x) {
        for (int y = 1; y < x; ++y) {
            cnt[x + y]++;
        }
        ans.push_back(x * 5003);
    }
    for (int x = i + 1;; ++x) {
        if (cnt[x] == m) {
            ans.push_back(x * 5003);
            break;
        }
    }
    int cur = M;
    while (ans.size() != n) {
        ans.push_back(cur);
        --cur;
    }
    sort(ans.begin(), ans.end());
    cout << ans << endl;
}