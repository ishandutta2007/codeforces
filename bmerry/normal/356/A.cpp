int main(int argc, const char **argv)
{
    redirect(argc, argv);

    int N, M;
    cin >> N >> M;
    vector<int> killer(N + 1, 0);
    set<int> knights;
    for (int i = 0; i < N; i++)
        knights.insert(i + 1);
    for (int i = 0; i < M; i++)
    {
        int l, r, x;
        cin >> l >> r >> x;
        auto p = knights.lower_bound(l);
        auto q = knights.upper_bound(r);
        for (auto j = p; j != q; j++)
            if (*j != x)
                killer[*j] = x;
        knights.erase(p, q);
        knights.insert(x);
    }
    for (int i = 1; i < N; i++)
        cout << killer[i] << ' ';
    cout << killer[N] << '\n';

    return 0;
}