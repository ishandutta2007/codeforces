vi v;
vi depth;

void build(int left, int right, int d){
    debug(left, right, d);
    if(left > right) return;
    if(left == right){
        depth[left] = d;
        return;
    }
    int m = 0;
    int p = 0;
    for(int i = left; i <= right; i++){
        if(v[i] > m){
            m = v[i];
            p = i;
        }
    }
    depth[p] = d;
    build(left, p-1, d+1);
    build(p+1, right, d+1);
}

void solve(){
    int n;
    cin >> n;
    v.resize(n);
    for(int i = 0; i < n; i++){
        cin >> v[i];
        v[i]--;
    }
    depth.resize(n);
    build(0, n-1, 0);
    for(int i = 0; i < n; i++) cout << depth[i] << " ";
    cout << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}