mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vector<vi> adj(n);
	vi deg(n);
	for(int i = 0; i < m; i ++){	
		int u, v;
		cin >> u >> v, u --, v --;
		adj[min(u, v)].push_back(max(u, v));
		deg[u] ++, deg[v] ++;
	}
	ll sum = 0;
	auto solve = [&](int u){
		return 1LL * adj[u].size() * (deg[u] - adj[u].size());
	};
	for(int u = 0; u < n; u ++) sum += solve(u);
	int qq;
	cin >> qq;
	cout << sum << "\n";
	while(qq --){
		int u;
		cin >> u, u --;
		sum -= solve(u);
		for(auto v: adj[u]){
			sum -= solve(v);
			adj[v].push_back(u);
			sum += solve(v);
		}
		adj[u].clear();
		cout << sum << "\n";
	}
	return 0;
}

