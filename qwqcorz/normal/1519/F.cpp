#include<bits/stdc++.h>
#define mk make_pair
#define fi first
#define se second
using namespace std;
const int N=7;
const int S=15625;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
void ckmn(int &x,int y){x=min(x,y);}
int a[N],b[N],c[N][N],dp[N][S],pw[N],state[N],n,m,up,ch[N];
void get_state(int s)
{
	for (int i=0;i<m;i++)
	state[i]=s%5,s/=5;
}
vector<pair<int,int>>turn;
void dfs(int now,int ad,int co,int i)
{
	if (now==a[i]) return void(turn.push_back(mk(ad,co)));
	for (int j=now?ch[now-1]:0;j<m;j++)
	if (state[j]<b[j])
	{
		state[j]++;
		ch[now]=j;
		dfs(now+1,ad+pw[j],co+(now==0||ch[now-1]!=j?c[i][j]:0),i);
		state[j]--;
	}
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	n=read(),m=read(),up=round(pow(5,m));
	for (int i=0;i<n;i++) a[i]=read();
	for (int i=0;i<m;i++) b[i]=read();
	for (int i=0;i<n;i++)
	for (int j=0;j<m;j++) c[i][j]=read();
	memset(dp,0x3f,sizeof(dp));
	int inf=dp[0][0];
	dp[0][0]=0;
	for (int i=pw[0]=1;i<m;i++) pw[i]=pw[i-1]*5;
	for (int i=0;i<n;i++)
	for (int j=0;j<up;j++)
	if (dp[i][j]<inf)
	{
		get_state(j);
		turn.clear();
		dfs(0,0,0,i);
		for (auto k:turn) ckmn(dp[i+1][j+k.fi],dp[i][j]+k.se);
	}
	int ans=*min_element(dp[n],dp[n]+up);
	if (ans<inf) print(ans);
			else puts("-1");
	
	return 0;
}
