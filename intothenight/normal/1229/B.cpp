mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vl val(n);
	cin >> val;
	vector<vi> adj(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vector<vector<pli>> b(n);
	b[0].push_back({val[0], 1});
	const ll mod = 1e9 + 7;
	ll res = val[0] % mod;
	function<void(int, int)> dfs = [&](int u, int p){
		for(auto &v: adj[u]){
			if(v != p){
				vector<pli> temp(b[u]), t;
				temp.push_back({val[v], 1});
				for(auto &p: temp){
					p.first = gcd(p.first, val[v]);
				}
				sort(all(temp));
				for(auto &p: temp){
					if(t.empty() || p.first != t.back().first){
						t.push_back(p);
					}
					else{
						t.back().second += p.second;
					}
				}
				b[v] = t;
				for(auto &p: b[v]){
					res += p.first * p.second % mod;
				}
				res %= mod;
				dfs(v, u);
			}
		}
	};
	dfs(0, -1);
	cout << res;
	return 0;
}

