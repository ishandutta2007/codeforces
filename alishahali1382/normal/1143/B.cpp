ll zarb(ll x){
	ll res=1;
	for (; x; x/=10) res*=x%10;
	return res;
}

ll f(ll n){
	//debug(n)
	if (n<10) return max(n, 1ll);
	ll x=n, tmp, tav=1;
	while (x>=10) x/=10;
	tmp=x;
	while (tav*10<=n) tav*=10;
	tmp*=tav;
	//debug2(tav, tmp)
	
	ll res=x*f(n-tmp);
	if (x>1) res=max(res, (x-1)*zarb(tav-1));
	else res=max(res, zarb(tav-1));
	return res;
	
}

