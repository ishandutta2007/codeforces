const int mod=998244353;
map<ll,int> dp[37][37];
template<typename T,int D>
struct Base{
	T a[D];
    int m;
	Base(){m = 0, memset(a, 0, sizeof(a));}
	void clear(){m = 0, memset(a, 0, sizeof(a));}
	bool ins(T x)
	{
		for(int i = D - 1; ~i; --i)
			if(x >> i & 1)
			{
				if(a[i]) x ^= a[i];
				else{
					m++;
					a[i] = x;return 1;
				}
			}
		return 0;
	}
};
int main()
{
    int n,m;
    cin>>n>>m;
    vc<ll> a(n);
    Base<ll,60> base;
    for(int i=0;i<n;i++)
    {
        cin>>a[i];
        base.ins(a[i]);
    }
    dp[m+1][0][0]=1;
    for(int i=m;~i;--i)
    {
        for(int j=0;j<=m;j++)
        {
            for(auto u:dp[i+1][j])
            {
                ll nxt=u.first^base.a[i];
                {
                    int cc=0;
                    ll tmp=u.first;
                    if(tmp>>i&1) tmp-=(1LL<<i),cc++;
                    dp[i][j+cc][tmp]=(dp[i][j+cc][tmp]+u.second)%mod;

                }
                if(base.a[i])
                {
                    int cc=0;
                    if(nxt>>i&1) nxt-=(1LL<<i),cc++;
                    dp[i][j+cc][nxt]=(dp[i][j+cc][nxt]+u.second)%mod;
                }
            }
        }
    }
    int cc=n-base.m;
    ll val=1;
    for(int i=0;i<cc;i++) val=val*2%mod;
    vc<ll> ans(m+1);
    for(int i=0;i<=m;i++)
    {
        for(auto u:dp[0][i])
            ans[i]=(ans[i]+1LL*u.second*val%mod)%mod;
    }
    print(ans);
}