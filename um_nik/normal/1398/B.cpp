const int N = 5555;
int n;
char s[N];

void solve() {

	scanf("%s", s + 1);
	s[0] = '0';
	n = strlen(s);
	s[n++] = '0';
	vector<int> a;
	int len = 0;
	for (int i = 0; i < n; i++) {
		if (s[i] == '0') {
			if (len > 0) a.push_back(len);
			len = 0;
		} else {
			len++;
		}
	}
	sort(all(a));
	reverse(all(a));
	int ans = 0;
	for (int i = 0; i < (int)a.size(); i += 2)
		ans += a[i];
	printf("%d\n", ans);
}

