const int maxn = 112345;

int n, m;
int par[maxn];

int Find(int x) {
  return par[x] == x ? x : par[x] = Find(par[x]);
}

void solve() {
  cin >> n >> m;
  iota(par + 1, par + n + 1, 1);
  int ans = m;
  while (m--) {
    int u, v;
    cin >> u >> v;
    if (u == v) {
      ans--;
      continue;
    }
    u = Find(u), v = Find(v);
    if (u == v) ans++;
    else par[u] = v;
  }
  cout << ans << '\n';
}

