int prob[MAXN];
int main()
{
    scanf("%d%d",&n,&k);
    fact[0]=invf[0]=1;
    for(int i=1;i<=n;i++) fact[i]=1LL*fact[i-1]*i%MOD;
    invf[n]=pow_mod(fact[n],MOD-2);
    for(int i=n-1;i>=1;i--) invf[i]=1LL*invf[i+1]*(i+1)%MOD;
    for(int i=1;i<=n;i++) inv[i]=1LL*invf[i]*fact[i-1]%MOD;
    f[0]=k-1;
    for(int i=1;i<n;i++) f[i]=1LL*(k-1)*(n+1LL*i*f[i-1]%MOD)%MOD*inv[n-i]%MOD;
    for(int i=n-1;i>=0;i--) add(f[i],f[i+1]);
    for(int i=1;i<=n;i++)
    {
        int x;
        scanf("%d",&x);
        cnt[x]++;
    }
    int num=cnt[-1],invk=pow_mod(k,MOD-2),pr=1LL*invk*(k-1)%MOD;
    for(int i=0;i<=num;i++)
        prob[i]=1LL*comb(num,i)*pow_mod(invk,i)%MOD*pow_mod(pr,num-i)%MOD;
    vector<int> v1,v2;
    for(int i=0;i<=n;i++) v1.push_back(prob[i]);
    for(int i=n;i>=0;i--) v2.push_back(f[i]);
    vector<int> g=fft::multiply_mod(v1,v2,MOD);
    int ans=0;
    int c=0;
    for(auto p:cnt)
    {
        if(p.F==-1) continue;
        c++;
        add(ans,g[n-p.S]);
    }
    add(ans,1LL*(k-c)*g[n]%MOD);
    dec(ans,1LL*(k-1)*f[0]%MOD);
    ans=1LL*ans*pow_mod(k-1,MOD-2)%MOD;
    printf("%d\n",ans);
    return 0;
}