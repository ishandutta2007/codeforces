void solve() {
  int n;
  cin >> n;
  vector<int> cnt(202);
  for (int i = 0; i < n; i++) {
    int x;
    cin >> x;
    cnt[abs(x)]++;
  }
  int ans = 0;
  for (auto x : cnt) {
    ans += min(x, 2LL);
  }
  if (cnt[0] >= 2) ans--;
  cout << ans;

  cout << '\n';
}

