using namespace std;

inline int nxt() {
	int x;
	// scanf("%d", &x);
	cin >> x;
	return x;
}

void solve() {
	int n = nxt(), m = nxt();
	if (n > m) {
		swap(n, m);
	}
	if (n == 1) {
		cout << m - (m == 1) << "\n";
		return;
	}
	cout << 2 * (n - 1) + (m - 1) + 1 << "\n";
}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	int t = nxt();
	for (int i = 1; i <= t; ++i) {
		solve();
	}

	return 0;
}