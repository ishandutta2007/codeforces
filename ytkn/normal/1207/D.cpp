ModInt pow(ModInt a, ll n) {
    assert(n >= 0);
	ModInt ans = 1;
    while (n > 0) {
        if (n&1) ans = ans*a;
        a = a*a;
        n >>= 1;
    }
	return ans;
}

#define N_MAX 300002
ModInt fac[N_MAX];

void init(){
    fac[0]=1;fac[1]=1;
    for(int i=2;i<N_MAX;i++){
        fac[i]=fac[i-1]*(ll) i;
    }
}

using mint = ModInt;
using P = pair<int, int>;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    init();
    int n; cin >> n;
    vector<int> cnt_a(n), cnt_b(n);
    map<P, int> cnt;
    for(int i = 0; i < n; i++){
        int a, b; cin >> a >> b; a--; b--;
        cnt_a[a]++;
        cnt_b[b]++;
        P p(a, b);
        if(cnt.count(p) == 0){
            cnt[p] = 1;            
        }else{
            cnt[p]++;
        }
    }
    mint ans = fac[n];
    mint ng_a = 1;
    mint ng_b = 1;
    bool both_ng = [&](){
        int pre = -1;
        for(auto [p, _]: cnt){
            if(pre > p.second) return false;
            pre = p.second;
        }
        return true;
    }();
    mint ng_ab = both_ng ? 1 : 0;
    for(int i = 0; i < n; i++){
        ng_a *= fac[cnt_a[i]];
        ng_b *= fac[cnt_b[i]];
    }
    for(auto [p, c]: cnt) ng_ab *= fac[c];
    ans -= ng_a;
    ans -= ng_b;
    ans += ng_ab;
    cout << ans << endl;
}