const int INF = 1e9;
const long long IINF = 1e18;
const int dx[4] = {1, 0, -1, 0}, dy[4] = {0, 1, 0, -1};
const char dir[4] = {'D', 'R', 'U', 'L'};
const long long MOD = 1000000007;
// const long long MOD = 998244353;

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<string> S(n);
    cin >> S;

    vector<string> T(2 * n - 1, "");
    vector<vector<pair<int, int>>> to(n, vector<pair<int, int>>(n));
    vector<vector<pair<int, int>>> inv(2 * n - 1, vector<pair<int, int>>(n));
    vector<int> sz(2 * n - 1, 0);
    for (int i = n - 1; i >= 0; i--) {
        for (int j = 0; j < n; j++) {
            to[i][j] = make_pair(i + j, T[i + j].size());
            inv[i + j][T[i + j].size()] = make_pair(i, j);
            T[i + j] += S[i][j];
            sz[i + j]++;
        }
    }

    vector<vector<int>> dp(2 * n - 1, vector<int>(1 << n));
    vector<int> nmask(26, 0);
    for (int i = 2 * n - 2; i >= 0; i--) {
        for (int mask = 1; mask < (1 << sz[i]); mask++) {
            int& val = dp[i][mask];
            if (i == 2 * n - 2) {
                val = 0;
                val += (T[i][0] == 'a');
                val -= (T[i][0] == 'b');
                continue;
            }
            int pre = -1;
            bool ok = true;
            for (int j = 0; j < sz[i]; j++) {
                if (!(mask >> j & 1)) continue;
                int cur = T[i][j] - 'a';
                if (pre != -1 and pre != cur) {
                    ok = false;
                    break;
                }
                pre = cur;
            }
            if (!ok) continue;
            for (int j = 0; j < sz[i]; j++) {
                if (!(mask >> j & 1)) continue;
                auto p = inv[i][j];
                int x = p.first, y = p.second;
                for (int k = 0; k < 2; k++) {
                    int nx = x + dx[k], ny = y + dy[k];
                    if (n <= nx or n <= ny) continue;
                    nmask[S[nx][ny] - 'a'] |= 1 << to[nx][ny].second;
                }
            }
            int Min = INF, Max = -INF;
            for (int j = 0; j < 26; j++) {
                if (!nmask[j]) continue;
                chmin(Min, dp[i + 1][nmask[j]]);
                chmax(Max, dp[i + 1][nmask[j]]);
                nmask[j] = 0;
            }
            val = (i & 1 ? Max : Min);
            val += (pre == 0);
            val -= (pre == 1);
        }
    }

    int ans = dp[0][1];
    if (ans > 0)
        cout << "FIRST";
    else if (ans < 0)
        cout << "SECOND";
    else
        cout << "DRAW";
    cout << '\n';
    return 0;
}