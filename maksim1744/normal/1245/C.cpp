const ll mod = 1e9 + 7;

int main() {
    string s;
    cin >> s;
    int n = s.size();
    vector<ll> dp(n);
    dp[0] = 1;
    bool ok = true;
    for (auto c : s) {
        if (c == 'm' || c == 'w') {
            cout << 0 << '\n';
            return 0;
        }
    }
    for (int i = 1; i < n; ++i) {
        if (s[i] != 'n' && s[i] != 'u') {
            dp[i] = dp[i - 1];
            continue;
        }
        if (s[i] == s[i - 1]) {
            dp[i] = (dp[i - 1] + (i - 2 < 0 ? 1 : dp[i - 2])) % mod;
        } else {
            dp[i] = dp[i - 1];
        }
    }
    cout << dp.back() << '\n';
    return 0;
}