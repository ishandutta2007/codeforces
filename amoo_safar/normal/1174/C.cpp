const ll LOG = (ll) 30;


ll p[MAXN];
map<ll, ll> mp;
int main(){
	ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	for(int i = 2; i < MAXN; i++){
		if(p[i] == 0) for(int j = i; j<MAXN; j+=i) p[j] = i;
	}
	ll n;
	cin >> n;
	ll cnt = 1;
	for(int i = 2; i<= n;i ++){
		if(mp.count(p[i])) cout << mp[p[i]] << " ";
		else{
			cout << cnt << " " ;
			mp[p[i]] = cnt;
			cnt ++;
		}
	}
	
	return 0;
}

