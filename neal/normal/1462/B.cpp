void run_case() {
    int N;
    string S;
    cin >> N >> S;
    bool possible = false;

    for (int first = 0; first <= 4; first++) {
        int second = 4 - first;

        if (S.substr(0, first) + S.substr(N - second) == "2020")
            possible = true;
    }

    cout << (possible ? "YES" : "NO") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}