mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	vi l(n), r(n, n - 1);
	if(a[0]) l[0] = -1e9;
	if(a[n - 1]) r[n - 1] = 1e9;
	for(int i = 1; i < n; i ++){
		if(a[i]){
			l[i] = l[i - 1];
		}
		else{
			l[i] = i;
		}
	}
	for(int i = n - 2; i >= 0; i --){
		if(a[i]){
			r[i] = r[i + 1];
		}
		else{
			r[i] = i;
		}
	}
	for(int i = 0; i < n; i ++){
		cout << min(i - l[i], r[i] - i) << " ";
	}
	return 0;
}
