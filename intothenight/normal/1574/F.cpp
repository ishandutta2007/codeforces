struct disjoint_set{
	vector<int> p;
	disjoint_set(int n): p(n, -1){ }
	bool share(int a, int b){ return root(a) == root(b); }
	int size(int u){ return -p[root(u)]; }
	int root(int u){ return p[u] < 0 ? u : p[u] = root(p[u]); } // O(alpha(n))
	bool merge(int u, int v){
		u = root(u), v = root(v);
		if(u == v) return false;
		if(p[u] > p[v]) swap(u, v);
		p[u] += p[v], p[v] = u;
		return true;
	}
	vector<vector<int>> group_up(){
		vector<vector<int>> g((int)p.size());
		for(auto i = 0; i < (int)p.size(); ++ i) g[root(i)].push_back(i);
		g.erase(remove_if(g.begin(), g.end(), [&](auto &s){ return s.empty(); }), g.end());
		return g;
	}
};

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, m, k;
	cin >> n >> m >> k;
	disjoint_set dsu(k);
	vector<int> next(k, -1), prev(k, -1), fail(k);
	for(auto i = 0; i < n; ++ i){
		int c;
		cin >> c;
		vector<int> a(c);
		for(auto j = 0; j < c; ++ j){
			cin >> a[j], -- a[j];
		}
		{
			auto b = a;
			sort(b.begin(), b.end());
			b.erase(unique(b.begin(), b.end()), b.end());
			if((int)a.size() != (int)b.size()){
				fail[dsu.root(a[0])] = true;
			}
		}
		for(auto j = 0; j < c - 1; ++ j){
			int x = a[j], y = a[j + 1];
			if(~next[x] || ~prev[y]){
				if(~next[x] && next[x] != y){
					fail[dsu.root(x)] = true;
				}
				if(~prev[y] && x != prev[y]){
					fail[dsu.root(y)] = true;
				}
				next[x] = y, prev[y] = x;
				bool nfail = fail[dsu.root(x)] || fail[dsu.root(y)];
				dsu.merge(x, y);
				fail[dsu.root(x)] = nfail;
			}
			else{
				if(dsu.share(x, y)){
					fail[dsu.root(x)] = true;
				}
				bool nfail = fail[dsu.root(x)] || fail[dsu.root(y)];
				dsu.merge(x, y);
				fail[dsu.root(x)] = nfail;
				next[x] = y, prev[y] = x;
			}
		}
	}
	map<int, int> cnt;
	for(auto g: dsu.group_up()){
		if(fail[dsu.root(g[0])]){
			continue;
		}
		if((int)g.size() <= m){
			++ cnt[(int)g.size()];
		}
	}
	vector<modular> dp(m + 1);
	dp[0] = 1;
	for(auto x = 0; x <= m; ++ x){
		for(auto [y, c]: cnt){
			if(x + y <= m){
				dp[x + y] += dp[x] * c;
			}
		}
	}
	cout << dp[m] << "\n";
	return 0;
}

