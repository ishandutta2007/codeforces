const ll MAXN = 1e6 + 10;
const ll INF = 8e18;
const ll MOD = 1e9 + 7; // 998244353; // 1e9 + 9;

ll n, q, ps[MAXN];

int main() {
	ios_base::sync_with_stdio(false); cin.tie(0); cout.tie(0);
	cin >> n >> q;
	for (int i = 1; i <= n; i++) {
		char c;
		cin >> c;
		ps[i] = ps[i - 1] + int(c - 'a') + 1;
	}

	while (q--) {
		int L, R;
		cin >> L >> R;
		cout << ps[R] - ps[L - 1] << endl;
	}
	return 0;
}