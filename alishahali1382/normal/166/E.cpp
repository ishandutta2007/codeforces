struct MAT{
	ll a, b, c, d;
} mat={0, 1, 3, 2};

MAT operator *(MAT m1, MAT m2){
	MAT out;
	out.a=(m1.a*m2.a+m1.b*m2.c)%mod;
	out.b=(m1.a*m2.b+m1.b*m2.d)%mod;
	out.c=(m1.c*m2.a+m1.d*m2.c)%mod;
	out.d=(m1.c*m2.b+m1.d*m2.d)%mod;
	return out;
}

MAT operator ^(MAT m, int x){
	if (x==1) return m;
	if (x&1) return (m^(x-1))*m;
	return (m*m)^(x>>1);
}

