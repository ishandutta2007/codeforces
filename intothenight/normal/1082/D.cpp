mt19937 rnd(chrono::steady_clock::now().time_since_epoch().count());



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	if(accumulate(all(a), 0) < 2 * n - 2){
		cout << "NO";
		return 0;
	}
	vi cnt1, cnt2;
	for(int u = 0; u < n; u ++){
		a[u] > 1 ? cnt2.push_back(u) : cnt1.push_back(u);
	}
	cout << "YES " << cnt2.size() + min(2, (int)cnt1.size()) - 1 << "\n" << n - 1 << "\n";
	auto print = [&](int u, int v){
		cout << u + 1 << " " << v + 1 << "\n";
	};
	for(int i = 1; i < cnt2.size(); i ++){
		print(cnt2[i - 1], cnt2[i]);
		a[cnt2[i - 1]] --, a[cnt2[i]] --;
	}
	if(cnt1.size() >= 2){
		print(cnt1[0], cnt2.back());
		a[cnt2.back()] --;
		for(int i = 1, j = 0; i < cnt1.size(); i ++){
			while(a[cnt2[j]] == 0) j ++;
			print(cnt1[i], cnt2[j]);
			a[cnt2[j]] --;
		}
	}
	else{
		int j = 0;
		for(auto u: cnt1){
			while(a[cnt2[j]] == 0) j ++;
			print(u, cnt2[j]);
			a[cnt2[j]] --;
		}
	}
	return 0;
}
