const int MAXN = 305010;

ll n, m, k, u, v, x, y, t, a, b, ans;
ll A[MAXN];
ll dp[5010][5010];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>k;
	for (int i=1; i<=n; i++) cin>>A[i];
	sort(A+1, A+n+1);
	m=n/k;
	memset(dp, 63, sizeof(dp));
	dp[0][0]=0;
	for (int i=1; i<=k && i*m<=n; i++) dp[i][0]=dp[i-1][0]+A[i*m]-A[(i-1)*m+1];
	for (int i=1; i<=k && i*m+i<=n; i++) dp[0][i]=dp[0][i-1]+A[i*m+i]-A[(i-1)*m+i];
	for (int i=1; i<=k; i++) for (int j=1; j<=k; j++){
		int s=(i+j)*m+j;
		if (s>n) break ;
		dp[i][j]=min(dp[i-1][j]+A[s]-A[s-m+1], dp[i][j-1]+A[s]-A[s-m]);
	}
	cout<<dp[k-n%k][n%k]<<endl;
	
	return 0;
}
