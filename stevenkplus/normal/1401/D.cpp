// ============= Solution ============= //
int main() {
   int T;
   cin >> T;
   for (int t = 1; t <= T; ++t) {
      int n;
      cin >> n;
      vector<vector<int>> ed(n);
      for (int i = 0; i < n - 1; ++i) {
         int u, v;
         cin >> u >> v;
         --u, --v;
         ed[u].push_back(v);
         ed[v].push_back(u);
      }

      vector<int> subsz(n);
      auto dfs = yc([&](auto dfs, int cur, int par = -1) -> void {
         subsz[cur] = 1;
         for (int nxt: ed[cur]) {
            if (nxt == par) continue;
            dfs(nxt, cur);
            subsz[cur] += subsz[nxt];
         }
      });
      dfs(0);

      int m;
      cin >> m;
      vector<int> p(m);
      for (int &i: p) cin >> i;
      sort(p.begin(), p.end());

      vector<ll> prods(n - 1);
      for (int i = 1; i < n; ++i) {
         prods[i - 1] = 1LL * subsz[i] * (n - subsz[i]);
      }
      sort(prods.begin(), prods.end());
      vector<mint> vals(n - 1, 1);
      for (int i = 0; i < m; ++i) {
         vals[max(n - 1 - m, 0) + min(i, n - 2)] *= p[i];
      }

      mint sum = 0;
      for (int i = 0; i < n - 1; ++i) {
         sum += vals[i] * prods[i];
      }
      cout << sum << "\n";
   }
}

