ll a, b, c, p;

void solve () {
	cin >> p >> a >> b >> c;
	
	ll res = max({a, b, c});
	res = min(res, (a - p % a) % a);
	res = min(res, (b - p % b) % b);
	res = min(res, (c - p % c) % c);
	
	cout << res << endl;
}

