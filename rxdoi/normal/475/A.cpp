string s[10];
int n;

int main()
{
	s[1]="+------------------------+";
	s[2]="|#.#.#.#.#.#.#.#.#.#.#.|D|)";
	s[3]="|#.#.#.#.#.#.#.#.#.#.#.|.|";
	s[4]="|#.......................|";
	s[5]="|#.#.#.#.#.#.#.#.#.#.#.|.|)";
	s[6]="+------------------------+";
	scanf("%d",&n);
	for (int i=1;i<=n;i++)
	{
		if (i<=4) {s[i+1][1]='O';continue;}
		s[i%3==2?2:(i%3==0?3:5)][(i-5)/3*2+3]='O';
	}
	for (int i=1;i<=6;i++) printf("%s\n",s[i].c_str());
}