int vis[10004][1005];
struct D{
    int u,d;
};
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,m;
    cin>>n>>m;
    vi d(m+1);
    for(int i=1;i<=m;i++) cin>>d[i];
    sort(d.begin(),d.end());
    int g,r;
    cin>>g>>r;
    deque<D> q;
    memset(vis,0x3f,sizeof(vis));
    q.push_back(D{1,0});
    vis[1][0]=0;
    while(!q.empty())
    {
        auto u=q.front();q.pop_front();
        if(u.u<m)
        {
            int nt=u.d+d[u.u+1]-d[u.u];
            if(nt==g) nt=0;
            //dbg(nt);
            if(nt>g) ;
            else{
                if(nt==0)
                {
                    if(vis[u.u+1][0]>vis[u.u][u.d]+1)
                    {
                        vis[u.u+1][0]=vis[u.u][u.d]+1;
                        q.push_back(D{u.u+1,0});
                    }
                }
                else{
                    if(vis[u.u+1][nt]>vis[u.u][u.d])
                    {
                        vis[u.u+1][nt]=vis[u.u][u.d];
                        q.push_front(D{u.u+1,nt});
                    }
                }

            }
        }
        if(u.u>1)
        {
            int nt=u.d+d[u.u]-d[u.u-1];
            if(nt==g) nt=0;
            if(nt>g) ;
            else{
                if(nt==0)
                {
                    if(vis[u.u-1][0]>vis[u.u][u.d]+1)
                    {
                        vis[u.u-1][0]=vis[u.u][u.d]+1;
                        q.push_back(D{u.u-1,0});
                    }
                }
                else{
                    if(vis[u.u-1][nt]>vis[u.u][u.d])
                    {
                        vis[u.u-1][nt]=vis[u.u][u.d];
                        q.push_front(D{u.u-1,nt});
                    }
                }

            }
        }
    }
    ll ans=1e18;
    for(int i=0;i<g;i++)
    {
        if(vis[m][i]>10000000) continue;
        if(i==0&&vis[m][i]>=1)
            ans=min(ans,1LL*vis[m][i]*(r+g)-r);
        else ans=min(ans,1LL*vis[m][i]*(r+g)+i);
    }
    if(ans>1e17) ans=-1;
    print(ans);
}