fn dfs(u: usize, e: &[Vec<usize>], c: &[i32]) -> (Z,Z) {
	if e[u].len() == 0 {
		if c[u] == 0 {
			(Z1,Z0)
		} else {
			(Z0,Z1)
		}
	} else if c[u] == 1 {
		let mut res = Z1;
		for &v in e[u].iter() {
			let (z0,z1) = dfs(v, e, c);
			res *= z0 + z1;
		}
		(Z0,res)
	} else {
		let m = e[u].len();
		let mut res_sub = v!([m]);
		for &v in e[u].iter() {
			res_sub.push(dfs(v, e, c));
		}
		l!(mut pre,suf = v!([m+1]));
		let mut z = Z1;
		pre.push(z);
		for &(z0,z1) in res_sub.iter() {
			z *= z0 + z1;
			pre.push(z);
		}
		z = Z1;
		suf.push(z);
		for &(z0,z1) in res_sub.iter().rev() {
			z *= z0 + z1;
			suf.push(z);
		}
		let res0 = pre[m];
		let mut res1 = Z0;
		for i in 0..m {
			res1 += pre[i] * suf[m-1-i] * res_sub[i].1;
		}
		(res0, res1)
	}
}

