use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        s: [u64; t],
    }
    for s in s {
        let mut s = s;
        let mut ans = 0;
        while s >= 10 {
            let v = s / 10 * 10;
            ans += v;
            s -= v;
            s += v / 10;
        }
        ans += s;
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}