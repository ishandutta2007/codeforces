typedef pair<int, int> point;

int ri() {
	int v = 0;
	char c = _getchar_nolock();
	while ('0' <= c && c <= '9') {
		v = (v << 3) + (v << 1) + c - '0';
		c = _getchar_nolock();
	}
	return v;
}

const int MAXN = 1e5 + 1;

set<point> used;
set<int> coorx[2], coory[2];

int main() {
	ios::sync_with_stdio(0);
	for (int t = 0; t < 2; ++t) {
		for (int i = 0; i < 4; ++i) {
			int a, b;
			cin >> a >> b;
			coorx[t].insert(a);
			coory[t].insert(b);
		}
	}
	for (int i = *coorx[0].begin(); i <= *coorx[0].rbegin(); ++i) {
		for (int j = *coory[0].begin(); j <= *coory[0].rbegin(); ++j) {
			used.insert({i, j});
		}
	}		
	int M = *coorx[1].rbegin() + *coorx[1].begin();
	M >>= 1;
	for (int i = *coorx[1].begin(); i <= *coorx[1].rbegin(); ++i) {
		int L = *coory[1].begin(), R = *coory[1].rbegin();
		L += abs(i - M);
		R -= abs(i - M);
		for (int j = L; j <= R; ++j) {
			if (used.count({i, j})) {
				cout << "YES" << endl;
				return 0;
			}
		}
	}
	cout << "NO" << endl;	
}