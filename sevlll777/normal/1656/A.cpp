void solve() {
  int n;
  cin >> n;
  vector<int> A(n);
  for (int i = 0; i < n; i++) {
    cin >> A[i];
  }
  int mn = (int) 2e9, mx = (int) -2e9, im = 0, imx = 0;
  for (int i = 0; i < n; i++) {
    if (A[i] < mn) {
      mn = A[i];
      im = i;
    }
    if (A[i] > mx) {
      mx = A[i];
      imx = i;
    }
  }
  cout << im + 1 << ' ' << imx + 1;
  cout << '\n';
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t;
  cin >> t;
  while (t--) {
    solve();
  }
}