	Fast;
	int t;
	cin >> t;
	while (t--){
		ll p, f, cs, cw, s, w;
		cin >> p >> f >> cs >> cw >> s >> w;
		if (s > w){
			s ^= w ^= s ^= w;
			cs ^= cw ^= cs ^= cw;
		}
		ll ans = 0;
		for (int i = 0; i <= cs; i++){
			ll s1 = i;
			if (s1*s > p) continue;
			ll s2 = min(f/s, cs-i);
			ll w1 = (p - s1*s) / w, w2 = (f - s2*s) / w;
			ll S = min(s1+s2, cs), W = min(w1+w2, cw);
			//t4(s1, s2, w1, w2); t2(S, W);
			ans = max(ans, S+W);
		}
		cout << ans << endl;
	}
