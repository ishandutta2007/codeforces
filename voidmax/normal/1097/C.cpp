typedef complex<double> comp;

const int N = 5e5 + 5;

int n;
int ans, cnt;
int a[N], b[N];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
    //read("input")
    cin >> n;
    for (int i = 0; i < n; ++i) {
        string s;
        cin >> s;
        int c = 0, minc = 0, maxc = 0;
        for (int v : s) {
            if (v == '(') ++c;
            else          --c;
            chkmin(minc, c);
            chkmax(maxc, c);
        }
        if (c < 0) {
            if (minc == c) ++b[-c];
        } else {
            if (minc == 0) {
                ++a[c];
            }
        }
    }
    for (int i = 0; i < N; ++i) {
        ans += min(a[i], b[i]);
        if (i == 0) ans += max(0, a[i] - b[i]) >> 1;
    }
    cout << ans << endl;

}