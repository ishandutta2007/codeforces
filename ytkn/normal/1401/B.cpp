template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int x1, y1, z1; cin >> x1 >> y1 >> z1;
    int x2, y2, z2; cin >> x2 >> y2 >> z2;
    int cnt21 = min(z1, y2);
    int cnt12 = 0;
    if(z1-cnt21+x1 < z2){
        cnt12 = z2-(z1-cnt21+x1);
    }
    cout << cnt21*2-cnt12*2 << endl;
}

