#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=3e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,nxt;
}e[N*2];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
int ri[N],sum[N],fa[N],deep[N];
int sigma(int x){return x*(x+1)/2;}
void dfs(int now,int father)
{
	fa[now]=father;
	deep[now]=deep[father]+1;
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father) continue;
		if (fa[to])
		{
			if (deep[to]>deep[now]) continue;
			int l=to,r=to;
			for (int k=now;k!=to;k=fa[k]) l=min(l,k),r=max(r,k);
			ri[l]=r-1;
		}
		else dfs(to,now);
	}
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read();
	for (int i=1;i<=m;i++)
	{
		int u=read(),v=read();
		add_edge(u,v);
		add_edge(v,u);
	}
	for (int i=1;i<=n+1;i++) ri[i]=n;
	for (int i=1;i<=n;i++) if (!fa[i]) dfs(i,-1);
	for (int i=n;i>=1;i--) ri[i]=min(ri[i],ri[i+1]);
	for (int i=n;i>=1;i--) sum[i]=sum[i+1]+ri[i]-i+1;
	int q=read();
	while (q--)
	{
		int l=read(),r=read();
		int pos=upper_bound(ri+l,ri+r+1,r)-ri;
		print(sum[l]-sum[pos]+sigma(r-pos+1));
	}
	
	return 0;
}
