bool prime[1000001]; //10^6

//f
void init_prime() {
	memset(prime, 1, sizeof(prime));
	prime[0] = prime[1] = false;
	for (int i = 2; i < sizeof(prime); i++) if (prime[i])
		for (int j = i * 2; j < sizeof(prime); j += i) prime[j] = false;
}


int main() {
	init_prime();
	int n;
	cin >> n;
	for (int i = 3; i < n; i++) {
		if (prime[i] || prime[n - i]) continue;
		printf("%d %d\n",i,n-i);
		return 0;
	}
	return 0;
}