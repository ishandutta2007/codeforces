template<class T> T gcd(T a, T b) { return b ? gcd(b, a % b) : a; }

int cnt[ten(6)+10][26];
int sm[ten(6)+10];

int main(){
	ll n,m; cin>>n>>m;
	string x,y; cin>>x>>y;

	int z = gcd(sz(x),sz(y));

	FOR(i,sz(y)){
		cnt[i%z][y[i]-'a']++;
		sm[i%z]++;
	}

	ll ans = 0;
	FOR(i,sz(x)){
		int tmp = sm[i%z] - cnt[i%z][x[i]-'a'];
		ans += tmp;
	}

	ans *= n * z / sz(y);

	cout << ans << endl;

	return 0;
}