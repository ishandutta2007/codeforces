int getsg(int x) {return (x <= 1000) ? sg[x] : sg[(x - 1) % 34 + 69];}
void solve() {
	cin >> n >> s + 1;
	int R = 0, B = 0;
	for (int i = 1; i <= n; i++) R += (s[i] == 'R'), B += (s[i] == 'B');
	if (R > B) return puts("Alice"), void();
	if (B > R) return puts("Bob"), void();
	int res = 0;
	for (int l = 1, r; l <= n; l = r + 1) {
		r = l;
		while (r < n && s[r] != s[r + 1]) ++r;
		res ^= getsg(r - l + 1);
	}
	printf("%s\n", (!res) ? "Bob" : "Alice");
}
