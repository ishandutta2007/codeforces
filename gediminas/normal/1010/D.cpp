struct node {
	int type;
	int out = -2;
	int tola = -1;
	int tolb = -1;
	int pak;
	int oper(int a = 0, int b = 0) {
		if (type == 0) {
			return out;
		}
		else if (type == 1) {
			return a & b;
		}
		else if (type == 2) {
			return a | b;
		}
		else if (type == 3) {
			return a ^ b;
		}
		else if (type == 4) {
			return !a;
		}

		return -1;
	}
};
vector<node> a;

int output(int d = 0) {
	if (d < 0)
		return -1;

	if (a[d].out == -2)
		return a[d].out = a[d].oper(output(a[d].tola), output(a[d].tolb));

	return a[d].out;
}

int change(int d = 0) {
	if (d < 0)
		return -1;

	if (a[d].out != a[d].oper(!output(a[d].tola), output(a[d].tolb))) {
		a[a[d].tola].pak = a[d].pak;
	}

	if (a[d].out != a[d].oper(output(a[d].tola), !output(a[d].tolb))) {
		a[a[d].tolb].pak = a[d].pak;
	}

	change(a[d].tola);
	change(a[d].tolb);
}

