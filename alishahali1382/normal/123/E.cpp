const int MAXN = 100010, LOG=20;

ll n, m, k, u, v, x, y, t, a, b, ans, sx, sy;
int X[MAXN], Y[MAXN];
ll sumx[MAXN];
ll sz[MAXN];
vector<int> G[MAXN];

void dfs(int node, int par){
	for (int v:G[node]) if (v!=par){
		dfs(v, node);
		sumx[node]+=sumx[v];
		sz[node]+=sz[v];
	}
	sumx[node]+=X[node];
	sz[node]++;
	if (node!=1) ans+=sz[node]*sumx[node]*Y[par];
	ans+=(n-sz[node])*(sx-sumx[node])*Y[node];
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<n; i++){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	for (int i=1; i<=n; i++) cin>>X[i]>>Y[i], sx+=X[i], sy+=Y[i];
	dfs(1, 1);
	ld out=1.*ans/(sx*sy);
	cout<<setprecision(15)<<fixed<<out<<'\n';
	
	return 0;
}
