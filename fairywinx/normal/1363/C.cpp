const int N = 1e3 + 228;

vector<int> G[N];

pair<int, int> z;

int dfs(int v, int p) {
	int kol = 0;
	int kol1 = 0, kol2 = 0;
	for(auto i: G[v]) {
		if (i == p)
			continue;
		if (dfs(i, v) % 2 == 0)
			++kol2;
		else
			++kol1;
	}
	if (p == -1) 
		z = {kol1, kol2};
	if (kol1 % 2 == 1)
		return 0;
	else
		return 1;
}

void solve() {
	int n, x;
	cin >> n >> x;
	for(int i = 0; i < n; ++i)
		G[i].clear();
	for(int i = 0; i < n - 1; ++i) {
		int a, b;
		cin >> a >> b;
		G[a - 1].push_back(b - 1);
		G[b - 1].push_back(a - 1);
	}
	if (G[x - 1].size() == 1 || n == 1) {
		cout << "Ayush\n";
		return;
	}
	dfs(x - 1, -1);

	if ((n % 2 == 1))
		cout << "Ashish\n";
	else 
		cout << "Ayush\n";
}

