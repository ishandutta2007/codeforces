int main()
{
	//freopen("in.txt","r",stdin);
	//freopen("out.txt","w",stdout);
	int n;
	multiset<int> trVals;
	set<int> times;
	scanf("%i",&n);
	vector<int> a(n),t(n);
	for(int i=0;i<n;i++)
		scanf("%i",&a[i]);
	map<int,vector<int> > ins;
	for(int i=0;i<n;i++)
		scanf("%i",&t[i]);
	for(int i=0;i<n;i++)
		ins[a[i]].pb(t[i]),times.insert(a[i]);
	ll sol=0;
	ll sum=0;
	while(times.size()){
		int t=*times.begin();
		times.erase(times.begin());
		for(auto p:ins[t])
			trVals.insert(p),sum+=p;
		sum-=*trVals.rbegin();
		trVals.erase(trVals.find(*trVals.rbegin()));
		sol+=sum;
		if(trVals.size()){
			times.insert(t+1);
		}
	}
	printf("%lld\n",sol);
	return 0;
}