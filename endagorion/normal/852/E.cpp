const int maxn = 110000;
const i64 P = 1000000000 + 7;
int d[maxn];

i64 deg(i64 x, i64 d) {
    i64 y = 1;
    while (d) {
        if (d & 1) (y *= x) %= P;
        (x *= x) %= P;
        d /= 2;
    }
    return y;
}

