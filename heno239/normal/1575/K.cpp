const int max_n = 1 << 2;
modint fact[max_n], factinv[max_n];
void init_f() {
	fact[0] = modint(1);
	for (int i = 0; i < max_n - 1; i++) {
		fact[i + 1] = fact[i] * modint(i + 1);
	}
	factinv[max_n - 1] = modint(1) / fact[max_n - 1];
	for (int i = max_n - 2; i >= 0; i--) {
		factinv[i] = factinv[i + 1] * modint(i + 1);
	}
}
modint comb(int a, int b) {
	if (a < 0 || b < 0 || a < b)return 0;
	return fact[a] * factinv[b] * factinv[a - b];
}
modint combP(int a, int b) {
	if (a < 0 || b < 0 || a < b)return 0;
	return fact[a] * factinv[a - b];
}





void solve() {
	int n, m, k, r, c; cin >> n >> m >> k >> r >> c;
	int sx, sy, tx, ty;
	cin >> sx >> sy >> tx >> ty;
	if (sx == tx && sy == ty) {
		modint ans = mod_pow(k, (ll)n * m);
		cout << ans << "\n";
		return;
	}
	int dx = tx - sx;
	int dy = ty - sy;
	ll num = 0;

	int cx = 0, cy = 0;
	if (dx >= 0) {
		cx = max(r - dx, 0);
		cx = max(0, r - cx);
	}
	else {
		cx = min(r, -dx);
		cx = max(cx, 0);
	}
	if (dy >= 0) {
		cy = max(c - dy, 0);
		cy = max(0, c - cy);
	}
	else {
		cy = min(c, -dy);
		cy = max(cy, 0);
	}
	num += (ll)cx * c;
	num += (ll)cy * r;
	num -= (ll)cx * cy;

	modint ans = 1;
	ll al = (ll)n * m;
	al -= 2 * (ll)r * c;
	int lx = max(tx, sx);
	int rx = min(tx + r, sx + r);
	int ly = max(ty, sy);
	int ry = min(ty + c, sy + c);
	ll lenx = max(0, rx - lx);
	ll leny = max(0, ry - ly);
	al += lenx * leny;
	ans *= mod_pow(k, al+num);
	cout << ans << "\n";
}


