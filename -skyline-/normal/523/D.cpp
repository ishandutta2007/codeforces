using namespace std;
long long n,k;
priority_queue<long long> s;
int main()
{
    scanf("%I64d%I64d",&n,&k);
    for(int i=1;i<=k;i++)s.push(0);
    for(int i=1;i<=n;i++){
            long long x,y,k;
            scanf("%I64d%I64d",&x,&y);
            k=max(x,-s.top());
            s.pop();
            s.push(-k-y);        
            printf("%I64d\n",k+y);
    }
   // system("pause");
    return 0;
}