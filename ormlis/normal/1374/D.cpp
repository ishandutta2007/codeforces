void solve() {
    int n, k;
    cin >> n >> k;
    ll ans = 0;
    map<int, int> cnt;
    range(i, n) {
        int a;
        cin >> a;
        cnt[k - a % k]++;
    }
    for (auto &p: cnt) {
        if (p.first == k) continue;
        ans = max(ans, p.first + 1ll * k * (p.second - 1) + 1);
    }
    cout << ans << "\n";
}

