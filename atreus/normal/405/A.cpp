#include <bits/stdc++.h>

using namespace std;

int main ()
{
    ios_base::sync_with_stdio(false);
    int n;
    cin >> n;
    int m = n;
    int a[n];
    for (int i = 0; i < n; i++)
        cin >> a[i];
    while (n >= 1){
        for (int i = n - 2; i >= 0; i--){
            if (a[i] > a[n - 1]){
                int m = a[i];
                a[i] = a[n - 1];
                a[n - 1] = m;
            }
        }
        n--;
    }
    for (int i = 0; i < m; i++)
        cout << a[i] << " ";

}