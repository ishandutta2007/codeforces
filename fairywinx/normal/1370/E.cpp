signed main() {
	setup();
	// SOLVE;

	int n;
	cin >> n;
	string s, t;
	cin >> s >> t;
	int ans = 0;
	set<int> a[2];
	for(int i = 0; i < n; ++i) {
		if (s[i] != t[i]) {
			a[s[i] - '0'].insert(i);
		}
	}
	if (a[0].size() != a[1].size()) {
		cout << -1;
		return 0;
	}
	while(a[0].size() && a[1].size()) {
		++ans;
		int type = 0;
		if (*a[0].begin() < *a[1].begin())
			type = 0;
		else
			type = 1;
		int s = type;
		int pos = *a[type].begin();
		while(true) {
			if (a[type].lower_bound(pos) == a[type].end())
				break;
			pos = *a[type].lower_bound(pos);
			if (type != s || a[type ^ 1].lower_bound(pos) != a[type ^ 1].end())
				a[type].erase(pos);
			else
				break;
			type ^= 1;
		}
	}
	cout << ans << '\n';


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

