struct segtree
{
    ll val[MAXV*4],lazy[MAXV*4];
    void Lazy(int k,int l,int r)
    {
        if(!lazy[k]) return;
        int mid=(l+r)/2;
        lazy[k*2]+=lazy[k];lazy[k*2+1]+=lazy[k];
        val[k*2]+=1LL*(mid-l+1)*lazy[k];val[k*2+1]+=1LL*(r-mid)*lazy[k];
        lazy[k]=0;
    }
    void build(int k,int l,int r)
    {
        if(l==r){val[k]=a[pos[l]]; return;}
        int mid=(l+r)/2;
        build(k*2,l,mid);build(k*2+1,mid+1,r);
        val[k]=val[k*2]+val[k*2+1];
    }
    void update(int k,int l,int r,int x,int y,int v)
    {
        if(l>y||x>r) return;
        if(l>=x&&r<=y)
        {
            val[k]+=1LL*(r-l+1)*v;
            lazy[k]+=v;
            return;
        }
        Lazy(k,l,r);
        int mid=(l+r)/2;
        update(k*2,l,mid,x,y,v); update(k*2+1,mid+1,r,x,y,v);
        val[k]=val[k*2]+val[k*2+1];
    }
    ll query(int k,int l,int r,int x,int y)
    {
        if(l>y||x>r) return 0;
        if(l>=x&&r<=y) return val[k];
        Lazy(k,l,r);
        int mid=(l+r)/2;
        return query(k*2,l,mid,x,y)+query(k*2+1,mid+1,r,x,y);
    }
}seg;
void dfs(int v,int p,int dd)
{
    pa[0][v]=p;d[v]=++t;pos[t]=v;
    depth[v]=dd;
    for(int i=0;i<(int)G[v].size();i++)
        if(G[v][i]!=p) dfs(G[v][i],v,dd+1);
    f[v]=t;
}
void init(int V)
{
    dfs(1,0,0);
    for(int k=0;k+1<MAXLOGV;k++)
    {
        for(int v=1;v<=V;v++)
        {
            if(!pa[k][v]) pa[k+1][v]=0;
            else pa[k+1][v]=pa[k][pa[k][v]];
        }
    }
}
int get(int v,int x)
{
    for(int k=0;k<MAXLOGV;k++)
        if((x>>k)&1)
            v=pa[k][v];
    return v;
}
int lca(int u,int v)
{
    if(depth[u]>depth[v]) swap(u,v);
    int dd=(depth[v]-depth[u]);
    v=get(v,dd);
    if(u==v) return u;
    for(int k=MAXLOGV-1;k>=0;k--)
    {
        if(pa[k][u]!=pa[k][v])
        {
            u=pa[k][u];
            v=pa[k][v];
        }
    }
    return pa[0][u];
}
void update(int v,int x)
{
    if(v==root)
    {
        seg.update(1,1,n,1,n,x);
        return;
    }
    if(d[v]<=d[root]&&f[v]>=f[root])
    {
        int tot=get(root,depth[root]-depth[v]-1);
        seg.update(1,1,n,1,n,x);
        seg.update(1,1,n,d[tot],f[tot],-x);
    }
    else seg.update(1,1,n,d[v],f[v],x);
}
ll query(int v)
{
    if(v==root) return seg.query(1,1,n,1,n);
    if(d[v]<=d[root]&&f[v]>=f[root])
    {
        int tot=get(root,depth[root]-depth[v]-1);
        ll ans=seg.query(1,1,n,1,n);
        ans-=seg.query(1,1,n,d[tot],f[tot]);
        return ans;
    }
    else return seg.query(1,1,n,d[v],f[v]);
}
