void Solve(){
	cin>>d>>m;
	int hb=31-__builtin_clz(d);
	//debug(hb)
	for (int i=0; i<hb; i++) A[i]=(1<<i);
	A[hb]=d-(1<<hb)+1;
	ans=1;
	for (int i=0; i<=hb; i++) ans=ans*(A[i]+1)%m;
	ans--;
	ans%=m;
	if (ans<0) ans+=m;
	cout<<ans<<'\n';
}

