ll gcd(ll a,ll b) { return b==0?a:gcd(b,a%b); }


pair<int,int> solve(int n) {
	if(n%2==1) return MP(n/2,(n+1)/2);
	if(n%4==0) return MP(n/2-1,n/2+1);
	return MP(n/2-2,n/2+2);
}

int n;

void run() {
	scanf("%d",&n);
	pair<int,int> res=solve(n);
	printf("%d %d\n",res.first,res.second);
}

