const int INF = 1e9 + 7;

void solve(){
    int n;
    cin >> n;
    vi v(n);
    for(int i = 0; i < n; i++) cin >> v[i];
    if(n <= 2){
        cout << "YES" << endl;
        return;
    }
    set<int> s;
    s.insert(v[0]);
    s.insert(v[1]);
    for(int i = 2; i < n; i++){
        debug(s);
        int l = s.lower_bound(v[i-1]) == s.begin() ? -INF : *prev(s.lower_bound(v[i-1]));
        int r = next(s.lower_bound(v[i-1])) == s.end() ? INF : *next(s.lower_bound(v[i-1]));
        debug(l, r);
        if(v[i] < l || v[i] > r){
            cout << "NO" << endl;
            return;
        }
        s.insert(v[i]);
    }
    cout << "YES" << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}