const string FILENAME = "input";
const int MAXN = 200228;


int n, m, k;
int parent[MAXN];


int findset(int a) {
    if (parent[a] == a) {
        return a;
    }
    return parent[a] = findset(parent[a]);
}



void solve() {
    cin >> n >> m >> k;
    vector<pair<int, pair<int, int> > > st;
    for (int i = 0; i < m; i++) {
        int a, b, c;
        cin >> a >> b >> c;
        a--, b--;
        st.pb(mp(c, mp(a, b)));
    }   
    sort(all(st));
    for (int i = 0; i < n; i++) {
        parent[i] = i;
    }
    bool was = false;
    ll keker = 0;
    vector<ll> fd;
    for (auto xx: st) {
        int a = xx.second.first;
        int b = xx.second.second;
        if (findset(a) != findset(b)) {
            parent[findset(a)] = findset(b);
            if (xx.first >= k) {
                was = true;
                keker += xx.first - k;
            }
            fd.pb(xx.first);
        } else {
            fd.pb(xx.first);
        }
    }  
    if (was) {
        cout << keker << '\n';
    } else {
        keker = 1e18;
        for (auto x: fd) {
            chkmin(keker, abs(x - k));
        }
        cout << keker << '\n';
    }
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    //read(FILENAME);
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}