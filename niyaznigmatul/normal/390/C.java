class TaskC {
    public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int n = in.nextInt();
        int k = in.nextInt();
        int w = in.nextInt();
        char[] c = in.next().toCharArray();
        int[][] s = new int[k][c.length];
        for (int i = 0; i < c.length; i++) {
            int have = c[i] - '0';
            for (int j = 0; j < k; j++) {
                int dif = (i - j + k) % k;
                int need = 0;
                if (dif == k - 1) {
                    need = 1;
                }
                s[j][i] = have ^ need;
                if (i > 0) s[j][i] += s[j][i - 1];
            }
        }
        for (int i = 0; i < w; i++) {
            int l = in.nextInt() - 1;
            int r = in.nextInt() - 1;
            int ans = s[l % k][r];
            if (l > 0) ans -= s[l % k][l - 1];
            out.println(ans);
        }
    }
}

class FastScanner extends BufferedReader {

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
//            if (isEOF && ret < 0) {
//                throw new InputMismatchException();
//            }
//            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    public String next() {
        StringBuilder sb = new StringBuilder();
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        if (c < 0) {
            return null;
        }
        while (c >= 0 && !isWhiteSpace(c)) {
            sb.appendCodePoint(c);
            c = read();
        }
        return sb.toString();
    }

    static boolean isWhiteSpace(int c) {
        return c >= 0 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (c >= 0 && !isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    public String readLine() {
        try {
            return super.readLine();
        } catch (IOException e) {
            return null;
        }
    }

}

