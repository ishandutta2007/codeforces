    int n;
    bool vis[N];
    int prim[N],cnt;
    void init()
    {
    	for (int i=2;i<N;i++)
    	{
    		if (!vis[i]) prim[++cnt]=i;
    		for (int j=1;j<=cnt;j++)
    		{
    			if (i*prim[j]>=N) break;
    			vis[i*prim[j]]=1;
    			if (i%prim[j]==0) break;
    		}	
    	}	
    }
