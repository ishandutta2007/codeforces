const int MAXN = 5005;



string s;
int dx[4] = {0, 0, 1, -1};
int dy[4] = {1, -1, 0, 0};
int c[MAXN];


void solve() {
    cin >> s;  
    int xa = 0, ya = 0;
    int n = sz(s); 
    int it = 0;
    for (auto x: s) {
        if (x == 'L') {
            c[it] = 3;
        } else if (x == 'R') {
            c[it] = 2;
        } else if (x == 'D') {
            c[it] = 1;
        } else {
            c[it] = 0;
        }
        it++;
    }
    vector<pair<int, int> > st;
    int curx = 0;
    int cury = 0;
    for (int i = 0; i < n; i++) {
        curx += dx[c[i]];
        cury += dy[c[i]];
        if (curx == 0 && cury == 0) {
            continue;
        }
        st.pb(mp(curx, cury));
    }
    for (auto x: st) {
        int curx = 0;
        int cury = 0;
        for (int i = 0; i < n; i++) {
            curx += dx[c[i]];
            cury += dy[c[i]];
            if (curx == x.first && cury == x.second) {
                curx -= dx[c[i]];
                cury -= dy[c[i]];
                continue;
            }
        }
        if (curx == 0 && cury == 0) {
            xa = x.first;
            ya = x.second;
            break;
        }
    }
    cout << xa << ' ' << ya << '\n';
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    //read(FILENAME);
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}