void solve() {
    int n;
    cin >> n;
    vector<vector<ll>> pref(2, vector<ll>(n + 1));
    for (auto i : {0, 1})
        for (int j = 0; j < n; j++) {
            int x;
            cin >> x;
            pref[i][j + 1] = pref[i][j] + x;
        }

    ll answer = LLONG_MAX;
    for (int pos = 0; pos < n; pos++)
        answer = min(answer, max(pref[1][pos], pref[0].back() - pref[0][pos + 1]));

    cout << answer << '\n';
}

int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);

    int tests;
    cin >> tests;
    while (tests--)
        solve();
}