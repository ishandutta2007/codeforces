void solve() {
	int n; cin >> n;
    vector<int> t(n);
    rep(i, n) {
        cin >> t[i];
    }
    sort(all(t));
    vector<vector<int>> dp(3 * n + 2, vector<int>(n + 1, INF));
    dp[0][0] = 0;
    int ans = INF;
    for(int i = 0; i <= 3 * n; ++ i) {
        for(int j = 0; j <= n; ++ j) {
            chmin(dp[i + 1][j], dp[i][j]);
            if(j - 1 >= 0) {
                chmin(dp[i + 1][j], dp[i][j - 1] + abs(i + 1 - t[j - 1]));
            }
        }
        chmin(ans, dp[i + 1][n]);
    }
    cout << ans << endl;
}

