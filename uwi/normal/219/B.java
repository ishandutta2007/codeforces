	void solve()
	{
		long p = nl();
		long d = nl();
		long prev = p;
		for(long j = 1, k = 10;j <= 17;j++,k*=10){
			long y = p / k;
			if(p % k == k - 1){
			}else{
				y--;
				if(y < 0){
					out.println(prev);
					return;
				}
			}
			long x = y * k + (k-1);
			if(p-x > d){
				out.println(prev);
				return;
			}
			prev = x;
		}
		out.println(prev);
	}
	
