template<typename T>
void output_vector(const vector<T> &v, bool add_one = false, int start = -1, int end = -1) {
    if (start < 0) start = 0;
    if (end < 0) end = v.size();

    for (int i = start; i < end; i++)
        cout << v[i] + (add_one ? 1 : 0) << (i < end - 1 ? ' ' : '\n');
}


int main() {
    int64_t P, K;
    cin >> P >> K;
    vector<int64_t> A;

    do {
        int64_t remainder = (P % K + K) % K;
        A.push_back(remainder);
        P -= remainder;
        P /= -K;
    } while (P != 0);

    cout << A.size() << '\n';
    output_vector(A);
}