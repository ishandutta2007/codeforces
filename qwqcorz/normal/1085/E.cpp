#include<bits/stdc++.h>
using namespace std;
const int N=1e6+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
int n;
char a[N],b[N],s[N];
bool used[26];
char p[26];
bool check1(int m,int x)
{
	memset(used,0,sizeof(used));
	memset(p,0,sizeof(p));
	for (int i=1;i<=x;i++)
	{
		p[s[i]-'a']=a[i];
		used[a[i]-'a']=1;
	}
	for (int i=x+1,k=m-1;i<=n;i++)
	{
		if (!p[s[i]-'a'])
		{
			while (used[k]) k--;
			used[k]=1;
			p[s[i]-'a']=k+'a';
		}
		char c=p[s[i]-'a'];
		if (c<a[i]) return 0;
		if (c>a[i]) return 1;
	}
	return 1;
}
bool check2(int m,int x)
{
	memset(used,0,sizeof(used));
	memset(p,0,sizeof(p));
	for (int i=1;i<=x;i++)
	{
		p[s[i]-'a']=b[i];
		used[b[i]-'a']=1;
	}
	for (int i=x+1,k=0;i<=n;i++)
	{
		if (!p[s[i]-'a'])
		{
			while (used[k]) k++;
			used[k]=1;
			p[s[i]-'a']=k+'a';
		}
		char c=p[s[i]-'a'];
		if (c<b[i]) return 1;
		if (c>b[i]) return 0;
	}
	return 1;
}
bool check(int m,int x)
{
	memset(used,0,sizeof(used));
	memset(p,0,sizeof(p));
	for (int i=1;i<x;i++)
	{
		if (p[s[i]-'a']&&p[s[i]-'a']!=a[i]) return 0;
		if (!p[s[i]-'a']&&used[a[i]-'a']) return 0;
		p[s[i]-'a']=a[i];
		used[a[i]-'a']=1;
	}
	if (x>n) return 1;
	if (p[s[x]-'a'])
	{
		if (p[s[x]-'a']>b[x]) return 0;
		if (p[s[x]-'a']<a[x]) return 0;
		if (a[x]<p[s[x]-'a']&&p[s[x]-'a']<b[x]) return 1;
		if (a[x]==p[s[x]-'a']) return check1(m,x);
		if (b[x]==p[s[x]-'a']) return check2(m,x);
	}
	for (int i=a[x]+1;i<b[x];i++) if (!used[i-'a'])
	{
		p[s[x]-'a']=i;
		used[i-'a']=1;
		return 1;
	}
	bool f1=used[a[x]-'a'],f2=used[b[x]-'a'];
	if (!f1&&check1(m,x)) return 1;
	if (!f2&&check2(m,x)) return 1;
	return 0;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		int m=read(),x=1;
		scanf("%s%s%s",s+1,a+1,b+1);
		n=strlen(s+1);
		if (strcmp(a,b)>0)
		{
			puts("NO");
			continue;
		}
		while (x<=n&&a[x]==b[x]) x++;
		if (check(m,x))
		{
			for (int j=0,k=0;j<m;j++)
			if (!p[j])
			{
				while (used[k]) k++;
				p[j]=(k++)+'a';
			}
			puts("YES");
			for (int i=0;i<m;i++) putchar(p[i]);
			puts("");
		}
		else puts("NO");
	} 
	
	return 0;
}
