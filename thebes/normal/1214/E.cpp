#define pb push_back
#define fox(i,x,y) for(int i=(x);i<=(y);i++)
#define foxr(i,x,y) for(int i=(y);i>=(x);i--)

const int MN = 2e5+5;
int N, i, d[MN], x, ord[MN], dis[2*MN];
vector<pii> ans;

int main(){
    for(scanf("%d",&N),i=1;i<=N;i++){
        scanf("%d",&d[i]);
        ord[i]=i;
    }
    sort(ord+1,ord+N+1,[](int i,int j){return d[i]>d[j];});
    for(i=1;i<N;i++) ans.pb({2*ord[i],2*ord[i+1]}),dis[i]=2*ord[i];
    dis[N]=2*ord[N];
    for(i=1;i<=N;i++){
        int t = i+d[ord[i]]-1;
        ans.pb({dis[t],2*ord[i]-1});
        if(!dis[t+1]) dis[t+1]=2*ord[i]-1;
    }
    for(auto v : ans) printf("%d %d\n",v.first,v.second);
    return 0;
}