#include <bits/stdc++.h>

using namespace std;

const int maxn = 1000;
int a[maxn + 10];

int main (){
    ios_base::sync_with_stdio(false);
    int n;
    long long sum = 0;
    cin >> n;
    for (int i = 0; i < n; i++){
        cin >> a[i];
        sum += a[i];
    }
    double x = (1.0 * sum) / n;
    cout << fixed << setprecision(5) << x;
}