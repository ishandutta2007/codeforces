int dy[100005];

void ass(){
	int n;
	scanf("%d",&n);
	int m=n+n;
	vector<int> sm,bg;
	//1. 
	for(int i=1;i<=n;i++){
		int a=2*i-1,b=2*i;
		if(sml(a,b)){
			swap(a,b);
		}
		sm.push_back(b);
		bg.push_back(a);
		dy[a]=b;
		dy[b]=a;
	}
	//2. 
	for(int i=0;i<n;i++){
		for(int j=i+1;j<n;j++){
			if(sml(sm[i],sm[j])){
				swap(sm[i],sm[j]);
			}
		}
	}
	//3. 
	int pos;
	for(int i=0;i<n;i++){
		if(bg[i]==dy[sm[0]]){
			pos=i;
			break;
		}
	}
	bg.erase(bg.begin()+pos);
	bg.insert(bg.begin(),dy[sm[0]]);
	//4. 
	for(int i=1;i<n;i++){
		for(int j=i+1;j<n;j++){
			if(sml(bg[i],bg[j])){
				swap(bg[i],bg[j]);
			}
		}
	}
	//5. nn
	for(int i=1;i<n;i++){
		sml(bg[i],sm[n-i-1]);
	}
	//6. 
	ahh_shit();
}

