using namespace std;

inline int read()
{
	int ans=0,f=1;
	char c=getchar();
	while(c>'9'||c<'0'){if(c=='-')f=-1;c=getchar();}
	while(c>='0'&&c<='9'){ans=(ans<<1)+(ans<<3)+c-'0';c=getchar();}
	return ans*f;
}

inline void write(int x)
{
	if(x<0) putchar('-'),x=-x;
	if(x/10) write(x/10);
	putchar((char)(x%10)+'0');
}

template<typename T>inline T Abs(T a){return a>0?a:-a;};
template<typename T,typename TT>inline T Min(T a,TT b){return a<b?a:b;}
template<typename T,typename TT> inline T Max(T a,TT b){return a<b?b:a;}

const int N=1e6+5;
int n,m,sm[22],ans;
bool vis[N*22],vis2[N];

signed main()
{
	n=read();m=read();
	int now=0;
	for(int i=1;i<=20;++i)
	{
		for(int j=1;j<=m;++j)
			if(!vis[i*j])
				now++,vis[i*j]=1;
		sm[i]=now;
	}
	for(int i=2;i<=n;++i)
		if(!vis2[i])
		{
			int num=0;
			for(int j=i;j<=n;j*=i)
				vis2[j]=1,++num;
			ans+=sm[num];
		}
	write(ans+1);
	return 0;
}