    for (int cntv = 0; cntv <= min(n, 8); cntv++) {
        for (int cntx = 0; cntx <= min(n - cntv, 48); cntx++) {
            bool bad = false;
            for (int t = 0; t <= cntv; t++) {
                if (t && (t * 4) % 9 == 0) {
                    bad = true;
                }
                for (int t1 = 0; t1 <= cntx; t1++) {
                    if ((t * 4 + t1 * 9) % 49 == 0 && t * 4 + t1 * 9 >= 49) {
                        bad = true;
                    } 
                }
            }
            if (bad) {
                continue;
            }
            int sum = cntv * 4 + cntx * 9;
            g[sum % 49].pb({sum, n - cntv - cntx});
        }
    }
    for (int i = 0; i < 49; i++) {
        sort(all(g[i]));
        long long prev = -1;
        for (auto x: g[i]) {
            if (prev < x.first) {
                prev = x.first - 49;
            }
            long long nxt = x.first + 1LL * x.second * 49;
            if (nxt <= prev) {
                continue;
            }
            ans += (nxt - prev) / 49;
            prev = nxt;
        }
    }
    cout << ans << '\n';
