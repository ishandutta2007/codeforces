int n,T,fa[101000],d[101000],sz[101000],ans[101000];
char yes[]="YES",no[]="NO";

vector<pii>vec[404000];
int clo[404000],top,bo[404000],st[404000];

int GF(int x,int &dx){
	if (x==fa[x]) return x;
	dx+=d[x]; return GF(fa[x],dx);
}

void gao(int x,int y){
	int dx=0,fx,dy=0,fy,dis;
	fx=GF(x,dx); fy=GF(y,dy);
	if (fx==fy){
		st[top+1]=0;
		bo[top+1]=bo[top]&&((dx+dy)&1);
		top++;
	}else{
		dis=!((dx+dy)&1);
		if (sz[fx]>sz[fy]){
			fa[fy]=fx;
			sz[fx]+=sz[fy];
			d[fy]=dis;
			st[top+1]=fy;
		}else{
			fa[fx]=fy;
			sz[fy]+=sz[fx];
			d[fx]=dis;
			st[top+1]=fx;
		}
		bo[top+1]=bo[top];
		top++;
	}
}

void RE(int pos){
	for (;top>pos;top--){
		sz[fa[st[top]]]-=sz[st[top]];
		fa[st[top]]=st[top];
		d[st[top]]=0;
	}
}

void A(int x,int l,int r,int st,int en,int u,int v){
	//if (x==1) printf(" %d-%d  %d %d\n",st,en,u,v);
	if (st>en) return;
	if (l==st&&r==en){
		vec[x].push_back((pii){u,v});
		return;
	}
	int mid=l+r>>1;
	A(x<<1,l,mid,max(st,l),min(en,mid),u,v);
	A(x<<1|1,mid+1,r,max(st,mid+1),min(en,r),u,v);
}

void D(int x,int l,int r){
	//printf("%d-%d:: ",l,r);
	for (int i=0;i<(int)vec[x].size();i++){
		gao(vec[x][i].fi,vec[x][i].se);
	}
	//printf("  %d %d\n",top,bo[top]);
	if (l==r){
		ans[l]=bo[top];
		return;
	}
	clo[x]=top;
	
	int mid=l+r>>1;
	D(x<<1,l,mid); RE(clo[x]);
	D(x<<1|1,mid+1,r); RE(clo[x]);
}

int main(){
	read(n); read(T);
	int x,y;
	for (int i=1;i<=T;i++){
		read(x); read(y);
		p=(pii){x,y};
		if (mp.count(p)){
			A(1,1,T,mp[p],i-1,x,y);
			mp.erase(p);
		}else{
			mp[p]=i;
		}
	}
	for (;!mp.empty();mp.erase(mb)) 
		A(1,1,T,mb->se,T,mb->fi.fi,mb->fi.se);
	for (int i=1;i<=n;i++) {
		fa[i]=i; sz[i]=1;
	}
	bo[top=1]=1;
	D(1,1,T);
	for (int i=1;i<=T;i++) println(ans[i]?yes:no);
}