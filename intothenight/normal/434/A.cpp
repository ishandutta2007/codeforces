template<class T1, class T2>
istream &operator>>(istream &in, pair<T1, T2> &P){
	in >> P.first >> P.second;
	return in;
}
template<class T1, class T2>
ostream &operator<<(ostream &out, const pair<T1, T2> &P){
	out << "(" << P.first << ", " << P.second << ")";
	return out;
}
template<class T>
istream &operator>>(istream &in, vector<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, const vector<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}
template<class T>
istream &operator>>(istream &in, deque<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vi a(m);
	cin >> a;
	vector<vi> near(n + 1);
	ll val = 0;
	for(int i = 1; i < m; i ++){
		if(a[i - 1] != a[i]){
			val += abs(a[i - 1] - a[i]);
			near[a[i - 1]].push_back(a[i]), near[a[i]].push_back(a[i - 1]);
		}
	}
	ll dec = 0;
	for(int i = 1; i <= n; i ++) if(!near[i].empty()){
		sort(all(near[i]));
		ll temp = 0;
		for(auto x: near[i]) temp += abs(i - x);
		for(auto x: near[i]) temp -= abs(near[i][sz(near[i]) / 2] - x);
		dec = max(dec, temp);
	}
	cout << val - dec;
	return 0;
}

