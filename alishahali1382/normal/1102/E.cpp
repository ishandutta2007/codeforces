const int mod = 998244353;
const int MAXN = 200010;

int n, m, k, u, v, x, y, t, a, b, ans=1, cnt;
int A[MAXN];
map<int, vector<int>> mp;
set<int> st;

void rem(int a, int b){
	for (; a<b; a=*st.lower_bound(a)) st.erase(a);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n; i++) st.insert(i);
	for (int i=1; i<=n; i++){
		cin>>A[i];
		mp[A[i]].pb(i);
	}
	for (auto p:mp){
		int a=p.second[0], b=p.second[p.second.size()-1];
		//debug(a);
		//debug(b)
		rem(a, b);
	}
	cnt=st.size()-1;
	//if (!st.count(1)) cnt--;
	debug(cnt);
	while (cnt--) ans=(ans*2)%mod; 
	cout<<ans<<endl;
	
	return 0;
}
