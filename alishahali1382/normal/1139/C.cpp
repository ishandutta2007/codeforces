const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

ll powmod(ll a, ll b){
	if (!b) return 1;
	if (b&1) return a*powmod(a*a%mod, b>>1)%mod;
	return powmod(a*a%mod, b>>1);
}

ll n, m, k, u, v, x, y, t, a, b, ans;
vector<int> G[MAXN];
vector<ll> sz;
bitset<MAXN> mark;

ll dfs(int node){
	mark[node]=1;
	int res=1;
	for (int i:G[node]) if (!mark[i]) res+=dfs(i);
	return res;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>k;
	for (int i=0; i<n-1; i++){
		cin>>u>>v>>x;
		if (x) continue ;
		G[u].pb(v);
		G[v].pb(u);
	}
	for (int i=1; i<=n; i++) if (!mark[i]) sz.pb(dfs(i));
	for (ll x:sz) ans=(ans+powmod(x, k))%mod;
	ans=((powmod(n, k)-ans)%mod+mod)%mod;
	cout<<ans<<'\n';
	
	return 0;
}
