inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(ch < '0' || ch > '9'){w |= ch == '-';ch = getchar();}
	while(ch >= '0' && ch <= '9'){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int T, x, y;
void solve()
{
	scanf("%d%d", &x, &y);
	if(x > y) printf("%d\n", x + y);
	else printf("%d\n", x * (y / x) + (y % x) / 2);
}
int main()
{
	scanf("%d", &T);
	while(T--) solve();
	return 0;
}