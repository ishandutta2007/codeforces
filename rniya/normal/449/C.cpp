int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    auto table = prime_factor_table(n + 1);
    vector<vector<int>> fac(n + 1);
    vector<int> kind(n + 1, 0);
    for (int i = 2; i <= n; i++) {
        int j = i;
        while (j > 1) {
            int prime = table[j];
            kind[i]++;
            fac[prime].emplace_back(i);
            while (j % prime == 0) j /= prime;
        }
    }

    vector<pair<int, int>> ans;
    vector<bool> used(n + 1, false);
    for (int i = n; i >= 2; i--) {
        vector<int> cand;
        for (int& x : fac[i]) {
            if (!used[x]) {
                cand.emplace_back(x);
            }
        }
        int idx = cand.size() - 1;
        for (int j = 0; j + 1 < (int)cand.size(); j++) {
            if (kind[cand[j]] > kind[cand[idx]]) {
                idx = j;
            }
        }
        if (idx != cand.size() - 1) swap(cand[idx], cand.back());
        for (int j = 0; j + 1 < (int)cand.size(); j += 2) {
            ans.emplace_back(cand[j], cand[j + 1]);
            used[cand[j]] = used[cand[j + 1]] = true;
        }
    }

    cout << ans.size() << '\n';
    for (auto& p : ans) cout << p.first << ' ' << p.second << '\n';
    return 0;
}