ll gcd(ll a, ll b) { for (; b; a %= b, swap(a, b)); return a; }

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0), cout.tie(0);

	int t; cin >> t;
	while (t--)
	{
		int n, x, a, b; cin >> n >> x >> a >> b;
		if (a > b) swap(a, b);
		while (x && a > 1) x--, a--;
		while (x && b < n) x--, b++;
		cout << b - a << '\n';
	}
}