void dfs(int u) {
    f.push_back(make_pair(posss[u], u));
    used[u] = true;
    for (auto h: sd[u]) {
        if (!used[h.first]) {
            posss[h.first] = posss[u] + h.second;
            dfs(h.first);
        }
    }
}


