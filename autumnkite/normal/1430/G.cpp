#include <bits/stdc++.h>

const long long INF = 1ll << 60;

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int n, m;
	std::cin >> n >> m;
	std::vector<std::tuple<int, int, int>> E;
	for (int i = 0; i < m; ++i) {
		int u, v, w;
		std::cin >> u >> v >> w;
		--u, --v;
		E.emplace_back(u, v, w);
	}
	std::vector<bool> can(1 << n);
	for (int S = 0; S < (1 << n); ++S) {
		can[S] = true;
		for (auto [u, v, w] : E) {
			if ((S >> u & 1) && (S >> v & 1)) {
				can[S] = false;
				break;
			}
		}
	}
	std::vector<long long> f(1 << n, INF);
	std::vector<int> g(1 << n);
	auto upd = [&](long long &a, long long b) {
		if (b < a) {
			a = b;
			return true;
		} else {
			return false;
		}
	};
	f[0] = 0;
	for (int S = 1; S < (1 << n); ++S) {
		bool flag = true;
		long long sum = 0;
		for (auto [u, v, w] : E) {
			if ((S >> u & 1) ^ (S >> v & 1)) {
				if (S >> u & 1) {
					flag = false;
					break;
				} else {
					sum += w;
				}
			}
		}
		if (!flag) {
			continue;
		}
		for (int T = S; T; T = (T - 1) & S) {
			if (can[T] && upd(f[S], f[S ^ T])) {
				g[S] = T;
			}
		}
		f[S] += sum;
	}

	int now = (1 << n) - 1;
	int id = n;
	std::vector<int> a(n);
	while (now) {
		--id;
		for (int i = 0; i < n; ++i) {
			if (g[now] >> i & 1) {
				a[i] = id;
			}
		}
		now ^= g[now];
	}
	for (int x : a) {
		std::cout << x << " ";
	}
	std::cout << "\n";
}