#include<bits/stdc++.h>
#define ll long long
using namespace std;
const ll M=20;
const ll N=(1<<18)+5;

ll read()
{
	ll s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
void up(ll &x,ll y){x=min(x,y);}
ll dp[M][N],a[N],tot[N];

signed main(signed Recall,char *_902_[])
{
	memset(dp,0x3f,sizeof(dp));
	(void)Recall,(void)_902_;
	ll n=read(),m=log2(n),x=0;
	for (ll i=1;i<=n;i++) if ((a[i]=read())<0) x=i;
	dp[0][n]=0;
	tot[0]=0;
	for (ll i=1;i<=m;i++) tot[i]=tot[i-1]+(1<<(m-i));
	for (ll i=0;i<=m;i++)
	for (ll j=n;j>x;j--)
	{
		if (n-j<tot[i]) up(dp[i][j-1],dp[i][j]);
		up(dp[i+1][j-1],dp[i][j]+a[j]);
	}
	ll ans=dp[0][0];
	for (ll i=0;i<=m;i++) up(ans,dp[i][x]);
	print(ans);
	
	return 0;
}
