pii AB[MAXN];
pii tmp[MAXN];

bool check(ll x, ll y){
	for (int i=0; i<n; i++) tmp[i]={x-X[i], y-Y[i]};
	sort(tmp, tmp+n);
	for (int i=0; i<n; i++) if (tmp[i]!=AB[i]) return 0;
	cout<<x<<' '<<y<<endl;
	return 1;
}

