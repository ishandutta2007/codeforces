using S = int;
S op(S x, S y) { return std::min(x, y); }
S e() { return int(1e9); }
using F = int;
S map(F f, S x) { return x + f; }
F comp(F g, F f) { return f + g; }
F id() { return 0; }
using type = atcoder::lazy_segtree<S, op, e, F, map, comp, id>;

