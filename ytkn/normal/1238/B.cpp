void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

// calc ceil(a/b) 
template<typename T>
T ceil_div(T a, T b){
    return (a+b-1)/b;
}

void solve(){
    int n, r; cin >> n >> r;
    set<int> st;
    for(int i = 0; i < n; i++) {
        int x; cin >> x; st.insert(x);
    }
    n = st.size();
    vector<int> x;
    for(int a: st) x.push_back(a);
    int ans = n;
    for(int i = 0; i < n; i++){
        int c = ceil_div(x[i], r);
        int m = c + max(0, (n-i-1)-c);
        chmin(ans, m);
    }
    cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}