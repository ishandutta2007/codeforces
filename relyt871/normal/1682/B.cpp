const int MAXN = 200005;

template <typename T> inline void read(T &WOW) {
	T x = 0, flag = 1;
	char ch = getchar();
	while (!isdigit(ch)) {
		if (ch == '-') flag = -1;
		ch = getchar();
	}
	while (isdigit(ch)) {
		x = x * 10 + ch - '0';
		ch = getchar();
	}
	WOW = flag * x;
}

int n, p[MAXN];

void solve() {
    read(n);
    int ans = (1 << 30) - 1;
    for (int i = 0; i < n; ++i) {
        read(p[i]);
        if (i == p[i]) continue;
        ans &= (i & p[i]);
    }
    printf("%d\n", ans);
}

int main() {
    int T; read(T);
    while (T--) {
        solve();
    }
    return 0;
}