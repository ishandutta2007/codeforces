#define int long long

void Hollwo_Pelw(){
	int n, f = (1 << 15) - 1; cin >> n;
 
	int res = 1e9;
 
	for (int i = 0; i < 15; i++) {
		int x = (n + i) & f, c = i;
		while (x)
			x = x << 1 & f, c++;
		res = min(res, c);
	}
 
	cout << res << ' ';
}