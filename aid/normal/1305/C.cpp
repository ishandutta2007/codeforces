const int maxn = (int) 2e5 + 5;
int n, m;
int a[maxn];

bool read() {
  if (scanf("%d%d", &n, &m) < 2) {
    return false;
  }
  for (int i = 0; i < n; i++) {
    scanf("%d", &a[i]);
  }
  return true;
}

void solve() {
  if (n > m) {
    printf("0\n");
    return;
  }
  int res = 1 % m;
  for (int i = 0; i < n; i++) {
    for (int j = i + 1; j < n; j++) {
      int cur = abs(a[j] - a[i]) % m;
      res = res * cur % m;
    }
  }
  printf("%d\n", res);
}

int main() {
  precalc();
#ifdef DEBUG
  assert(freopen(TASK ".in", "r", stdin));
  assert(freopen(TASK ".out", "w", stdout));
#endif
  while (read()) {
    solve();
#ifdef DEBUG
    eprintf("Time %.2f\n", (double) clock() / CLOCKS_PER_SEC);
#endif
  }
  return 0;
}