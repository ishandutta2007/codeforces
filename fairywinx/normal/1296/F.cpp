const int N = 5e5 + 228;

vector<pair<int, int>> G[N];
bool used[N];

bool flag = false;

bool dfs(int v, int p, int f, int val, vector<int> &ans) {
	if (v == f)
		return true;
	for(auto i: G[v]) {
		int u = i.first;
		int id = i.second;
		if (u == p)
			continue;
		if (dfs(u, v, f, val, ans)) {
			if (used[id] && ans[id] == val)
				flag = true;
			if (!used[id]) {
				ans[id] = val;
				flag = true;
				used[id] = true;
			}
			return true;
		}
	}
	return false;
}

signed main() {
	setup();
	fill(used, used + N, 0);

	int n;
	cin >> n;
	vector<int> ans(n - 1, 1000000);
	for(int i = 0; i < n - 1; ++i) {
		int a, b;
		cin >> a >> b;
		G[a - 1].emplace_back(b - 1, i);
		G[b - 1].emplace_back(a - 1, i);
	}	
	int m;
	cin >> m;
	vector<pair<int, pair<int, int>>> c;
	for(int i = 0; i < m; ++i) {
		int val, a, b;
		cin >> a >> b >> val;
		c.emplace_back(make_pair(val, make_pair(a - 1, b - 1)));
	}
	sort(c.rbegin(), c.rend());
	for(auto i: c) {
		flag = false;
		dfs(i.second.first, -1, i.second.second, i.first, ans);
		if (!flag) {
			cout << -1 << '\n';
			return 0;
		}
	}
	for(auto i: ans)
		cout << i << ' ';
	cout << '\n';


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

