int solve()
{
    int n;
    if (!(cin >> n))
        return 1;
    ll m, k;
    cin >> m >> k;
    vector<ll> a(n);
    for (ll &i : a)
        cin >> i;
    sort(a.begin(), a.end());
    ll x = a.back();
    ll y = a[a.size() - 2];
    ll t = m / (k + 1);
    m %= (k + 1);
    cout << (x * k + y) * t + m * x << endl;

    return 0;
}

