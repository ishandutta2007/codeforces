const long long MOD = 998244353;

using mint = modint<MOD>;
const int MAX = 26, MAX_N = 403;
mint dp1[2][MAX_N][2][2];         // 0:     1:
mint dp2[2][MAX_N][MAX_N][3][3];  // 0:1    1:2     2:
mint sum1[MAX_N], sum2[MAX_N][MAX_N];

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<int> c(26);
    cin >> c;

    // beautiful
    mint ans = MAX * MAX * mint(MAX - 1).pow(n - 2);

    // 1
    dp1[0][0][1][1] = 1;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            for (int k = 0; k < 2; k++) {
                for (int l = 0; l < 2; l++) {
                    mint x = dp1[i & 1][j][k][l];
                    if (l != 0) dp1[(i + 1) & 1][j + 1][0][k] += x;
                    dp1[(i + 1) & 1][j][1][k] += x * (MAX - 1 - (i >= 2 && l == 1));
                    dp1[i & 1][j][k][l] = 0;
                }
            }
        }
    }
    for (int j = 0; j < MAX_N; j++) {
        for (int k = 0; k < 2; k++) {
            for (int l = 0; l < 2; l++) {
                sum1[j] += dp1[n & 1][j][k][l];
            }
        }
    }

    for (int i = 0; i < MAX; i++) {
        for (int j = c[i] + 1; j < MAX_N; j++) {
            ans -= sum1[j];
        }
    }

    // 2
    dp2[0][0][0][2][2] = 1;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            for (int k = 0; k < n; k++) {
                for (int l = 0; l < 3; l++) {
                    for (int m = 0; m < 3; m++) {
                        mint x = dp2[i & 1][j][k][l][m];
                        if (m != 0) dp2[(i + 1) & 1][j + 1][k][0][l] += x;
                        if (m != 1) dp2[(i + 1) & 1][j][k + 1][1][l] += x;
                        dp2[(i + 1) & 1][j][k][2][l] += x * (MAX - 2 - (i >= 2 && m == 2));
                        dp2[i & 1][j][k][l][m] = 0;
                    }
                }
            }
        }
    }
    for (int j = 0; j < MAX_N; j++) {
        for (int k = 0; k < MAX_N; k++) {
            for (int l = 0; l < 3; l++) {
                for (int m = 0; m < 3; m++) {
                    sum2[j][k] += dp2[n & 1][j][k][l][m];
                }
            }
        }
    }

    for (int i = 0; i < MAX; i++) {
        for (int j = i + 1; j < MAX; j++) {
            for (int k = c[i] + 1; k < MAX_N; k++) {
                for (int l = c[j] + 1; l < MAX_N; l++) {
                    ans += sum2[k][l];
                }
            }
        }
    }

    cout << ans << '\n';
    return 0;
}