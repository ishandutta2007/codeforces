int check(int k, int s1, int s2, int n) {
	if (k <= s1)
		return 0;
	if (k < s1 + s2)
		return k - s1;
	if (s1 >= s2)
		return (k - s1 - s2) / 2 + s2;
	int c = k / (s1 + s2);
	if (c >= n)
		return s2 * n + (k - (s1 + s2) * n) / 2;
	k = k % (s1 + s2);
	return c * s2 + max(k / 2, k - s1);
}

void solve() {
	int n, k;
	cin >> n >> k;
	int l1, r1;
	cin >> l1 >> r1;
	int l2, r2;
	cin >> l2 >> r2;
	if (l2 < l1) {
		swap(l1, l2);
		swap(r1, r2);
	}
	int s1 = 0, s2;
	if (l2 >= r1) {
		s1 = l2 - r1;
		l2 = r1;
	}
	else 
		k -= (min(r1, r2) - l2) * n;
	s2 = abs(l2 - l1) + abs(r1 - r2);
	int l = -1, r = 2e9 + 5;
	// cout << s1 << ' ' << s2 << '\n';
	while(r - l > 1) {
		int m = (l + r) / 2;
		if (check(m, s1, s2, n) >= k)
			r = m;
		else
			l = m;
	}
	cout << r << '\n';
}

