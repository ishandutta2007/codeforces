int main()
{
	//freopen("in.txt","r",stdin);
	//freopen("out.txt","w",stdout);
	int n;
	scanf("%i",&n);
	string s;
	cin >> s;
	const int L=26;
	int cnt[L]={},mx=0;
	set<int> nope[L];
	vector<int> color(n);
	for(int i=n-1;i>=0;i--){
		int tr=s[i]-'a';
		while(nope[tr].count(cnt[tr]))
			cnt[tr]++;
		color[i]=cnt[tr];
		mx=max(mx,cnt[tr]+1);
		for(int j=tr+1;j<L;j++)
			nope[j].insert(cnt[tr]);
	}
	if(mx>2){
		printf("NO\n");
		return 0;
	}
	printf("YES\n");
	for(int i=0;i<n;i++)
		printf("%i",color[i]);
	return 0;
}