    static class TaskF1 {
        long[][] result;
        long[][] split;
        int[] c;

        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int n = in.readInt();
            int m = in.readInt();
            int[] c = in.readIntArray(m);
            MiscUtils.decreaseByOne(c);
            int[] min = new int[n];
            int[] max = new int[n];
            Arrays.fill(min, m - 1);
            for (int i = 0; i < m; i++) {
                min[c[i]] = Math.min(min[c[i]], i);
                max[c[i]] = Math.max(max[c[i]], i);
            }
            result = new long[n][n];
            split = new long[n][n];
            long answer = 1;
            for (int i = n - 1; i >= 0; i--) {
                for (int j = min[i]; j <= max[i]; j++) {
                    if (c[j] != i) {
                        IntList colors = new IntArrayList();
                        while (c[j] != i) {
                            if (c[j] < i) {
                                out.printLine(0);
                                return;
                            }
                            colors.add(c[j]);
                            j = max[c[j]] + 1;
                        }
                        answer *= solve(colors.toArray());
                        answer %= MiscUtils.MODF;
                    }
                }
            }
            IntList colors = new IntArrayList();
            int j = 0;
            while (j < n) {
                colors.add(c[j]);
                j = max[c[j]] + 1;
            }
            answer *= solve(colors.toArray());
            answer %= MiscUtils.MODF;
            out.printLine(answer);
        }

        private long solve(int[] c) {
            this.c = c;
            int nn = c.length;
            for (int i = 0; i < nn; i++) {
                Arrays.fill(result[i], 0, nn, -1);
                Arrays.fill(split[i], 0, nn, -1);
            }
            return go(0, nn - 1);
        }

        private long go(int f, int t) {
            if (f >= t) {
                return 1;
            }
            if (result[f][t] != -1) {
                return result[f][t];
            }
            int at = f;
            for (int j = f + 1; j <= t; j++) {
                if (c[j] < c[at]) {
                    at = j;
                }
            }
            return result[f][t] = goSplit(f, at - 1) * goSplit(at + 1, t) % MiscUtils.MODF;
        }

        private long goSplit(int f, int t) {
            if (f > t) {
                return 1;
            }
            if (split[f][t] != -1) {
                return split[f][t];
            }
            split[f][t] = 0;
            for (int i = f - 1; i <= t; i++) {
                split[f][t] += go(f, i) * go(i + 1, t) % MiscUtils.MODF;
            }
            return split[f][t] %= MiscUtils.MODF;
        }

    }

    static class MiscUtils {
        public static final int MODF = 998244353;

        public static void decreaseByOne(int[]... arrays) {
            for (int[] array : arrays) {
                for (int i = 0; i < array.length; i++) {
                    array[i]--;
                }
            }
        }

    }

    static abstract class IntAbstractStream implements IntStream {
        public String toString() {
            StringBuilder builder = new StringBuilder();
            boolean first = true;
            for (IntIterator it = intIterator(); it.isValid(); it.advance()) {
                if (first) {
                    first = false;
                } else {
                    builder.append(' ');
                }
                builder.append(it.value());
            }
            return builder.toString();
        }

        public boolean equals(Object o) {
            if (!(o instanceof IntStream)) {
                return false;
            }
            IntStream c = (IntStream) o;
            IntIterator it = intIterator();
            IntIterator jt = c.intIterator();
            while (it.isValid() && jt.isValid()) {
                if (it.value() != jt.value()) {
                    return false;
                }
                it.advance();
                jt.advance();
            }
            return !it.isValid() && !jt.isValid();
        }

        public int hashCode() {
            int result = 0;
            for (IntIterator it = intIterator(); it.isValid(); it.advance()) {
                result *= 31;
                result += it.value();
            }
            return result;
        }

    }

    static interface IntList extends IntReversableCollection {
        public abstract int get(int index);

        public abstract void addAt(int index, int value);

        public abstract void removeAt(int index);

        default public IntIterator intIterator() {
            return new IntIterator() {
                private int at;
                private boolean removed;

                public int value() {
                    if (removed) {
                        throw new IllegalStateException();
                    }
                    return get(at);
                }

                public boolean advance() {
                    at++;
                    removed = false;
                    return isValid();
                }

                public boolean isValid() {
                    return !removed && at < size();
                }

                public void remove() {
                    removeAt(at);
                    at--;
                    removed = true;
                }
            };
        }

        default public void add(int value) {
            addAt(size(), value);
        }

    }

    static interface IntIterator {
        public int value() throws NoSuchElementException;

        public boolean advance();

        public boolean isValid();

    }

    static interface IntCollection extends IntStream {
        public int size();

        default public void add(int value) {
            throw new UnsupportedOperationException();
        }

        default public int[] toArray() {
            int size = size();
            int[] array = new int[size];
            int i = 0;
            for (IntIterator it = intIterator(); it.isValid(); it.advance()) {
                array[i++] = it.value();
            }
            return array;
        }

        default public IntCollection addAll(IntStream values) {
            for (IntIterator it = values.intIterator(); it.isValid(); it.advance()) {
                add(it.value());
            }
            return this;
        }

    }

    static class IntArrayList extends IntAbstractStream implements IntList {
        private int size;
        private int[] data;

        public IntArrayList() {
            this(3);
        }

        public IntArrayList(int capacity) {
            data = new int[capacity];
        }

        public IntArrayList(IntCollection c) {
            this(c.size());
            addAll(c);
        }

        public IntArrayList(IntStream c) {
            this();
            if (c instanceof IntCollection) {
                ensureCapacity(((IntCollection) c).size());
            }
            addAll(c);
        }

        public IntArrayList(IntArrayList c) {
            size = c.size();
            data = c.data.clone();
        }

        public IntArrayList(int[] arr) {
            size = arr.length;
            data = arr.clone();
        }

        public int size() {
            return size;
        }

        public int get(int at) {
            if (at >= size) {
                throw new IndexOutOfBoundsException("at = " + at + ", size = " + size);
            }
            return data[at];
        }

        private void ensureCapacity(int capacity) {
            if (data.length >= capacity) {
                return;
            }
            capacity = Math.max(2 * data.length, capacity);
            data = Arrays.copyOf(data, capacity);
        }

        public void addAt(int index, int value) {
            ensureCapacity(size + 1);
            if (index > size || index < 0) {
                throw new IndexOutOfBoundsException("at = " + index + ", size = " + size);
            }
            if (index != size) {
                System.arraycopy(data, index, data, index + 1, size - index);
            }
            data[index] = value;
            size++;
        }

        public void removeAt(int index) {
            if (index >= size || index < 0) {
                throw new IndexOutOfBoundsException("at = " + index + ", size = " + size);
            }
            if (index != size - 1) {
                System.arraycopy(data, index + 1, data, index, size - index - 1);
            }
            size--;
        }

        public int[] toArray() {
            return Arrays.copyOf(data, size);
        }

    }

