	long inv(char[] a, char[] b)
	{
		int n = a.length;
		int[] aa = new int[n];
		int[] bb = new int[n];
		{
			int[] f = new int[26];
			for (int i = 0; i < n; i++) {
				aa[i] = a[i] << 20 | ++f[a[i] - 'A'];
			}
		}
		{
			int[] f = new int[26];
			for (int i = 0; i < n; i++) {
				bb[i] = b[i] << 20 | ++f[b[i] - 'A'];
			}
		}
		Map<Integer, Integer> map = new HashMap<>();
		for(int i = 0;i < n;i++){
			map.put(bb[i], i);
		}
		for(int i = 0;i < n;i++){
			aa[i] = map.get(aa[i]);
		}
		return inversion(aa);
	}

	public static long inversion(int[] a)
	{
		int n = a.length;
		int[] b = a;
		int[] ft = new int[n+1];
		long inv = 0;
		for(int i = n-1;i >= 0;i--){
			inv += sumFenwick(ft, b[i]-1);
			addFenwick(ft, b[i], 1);
		}
		return inv;
	}

	public static int sumFenwick(int[] ft, int i)
	{
		int sum = 0;
		for(i++;i > 0;i -= i&-i)sum += ft[i];
		return sum;
	}

	public static void addFenwick(int[] ft, int l, int r, int v)
	{
		addFenwick(ft, l, v);
		addFenwick(ft, r, -v);
	}

	public static void addFenwick(int[] ft, int i, int v)
	{
		if(v == 0 || i < 0)return;
		int n = ft.length;
		for(i++;i < n;i += i&-i)ft[i] += v;
	}



	public static int[] shrink(int[] a) {
		int n = a.length;
		long[] b = new long[n];
		for (int i = 0; i < n; i++) b[i] = (long) a[i] << 32 | i;
		Arrays.sort(b);
		int[] ret = new int[n];
		int p = 0;
		for (int i = 0; i < n; i++) {
			if (i > 0 && (b[i] ^ b[i - 1]) >> 32 != 0) p++;
			ret[(int) b[i]] = p;
		}
		return ret;
	}


	public static class IterablePermutation implements Iterable<int[]>, Iterator<int[]>
			{
				int[] a;
				boolean first = true;

				public IterablePermutation(int n) {
					assert n >= 1;
					a = new int[n];
					for(int i = 0;i < n;i++)a[i] = i;
				}

				public IterablePermutation(int... a) {
					this.a = Arrays.copyOf(a, a.length);
				}

				@Override
				public boolean hasNext() {
					if(first)return true;
					int n = a.length;
					int i;
					for(i = n - 2;i >= 0 && a[i] >= a[i+1];i--);
					return i != -1;
				}

				@Override
				public int[] next() {
					if(first) {
						first = false;
						return a;
					}
					int n = a.length;
					int i;
					for(i = n - 2;i >= 0 && a[i] >= a[i+1];i--);
					assert i != -1;
					int j;
					for(j = i + 1;j < n && a[i] < a[j];j++);
					int d = a[i]; a[i] = a[j - 1]; a[j - 1] = d;
					for(int p = i + 1, q = n - 1;p < q;p++,q--){
						d = a[p]; a[p] = a[q]; a[q] = d;
					}
					return a;
				}

				@Override
				public Iterator<int[]> iterator() {
					return this;
				}
			}
	
