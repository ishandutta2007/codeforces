typedef vector<pii> vpii;
template<class T>
using Ftn = function<T(T, T)>;
template<class T>
using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
typedef tuple<int, int, int> tpl;
typedef vector<tpl> vtpl;
 
 
 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int tt;
	cin >> tt;
	while(tt --){
		int n, T, a, b;
		cin >> n >> T >> a >> b;
		vpii prob(n);
		for(int i = 0; i < n; ++ i){
			int x;
			cin >> x;
			prob[i].second = x ? b : a;
		}
		vl event;
		for(int i = 0; i < n; ++ i){
			int t;
			cin >> t;
			prob[i].first = t;
			event.push_back(t);
		}
		event.push_back(T + 1);
		sort(all(prob));
		vl sum(n + 1);
		for(int i = 0; i < n; ++ i){
			sum[i + 1] = sum[i] + prob[i].second;
		}
		sort(all(event));
		event.resize(unique(all(event)) - event.begin());
		reverse(all(event));
		vl cnt(2);
		ll res = 0;
		for(auto t: event){
			while(!prob.empty() && prob.back().first >= t){
				prob.back().second == a ? cnt[0] ++ : cnt[1] ++;
				prob.pop_back();
			}
			ll s = sum[sz(prob)];
			t --;
			if(s <= t){
				t -= s;
				if(t >= cnt[0] * a + cnt[1] * b){
					res = max(res, cnt[0] + cnt[1] + sz(prob));
				}
				else if(t >= cnt[0] * a){
					res = max(res, cnt[0] + (t - 1LL * cnt[0] * a) / b + sz(prob));
				}
				else{
					res = max(res, t / a + sz(prob));
				}
			}
		}
		cout << res << "\n";
	}
	return 0;
}
