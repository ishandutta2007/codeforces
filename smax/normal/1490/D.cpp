int a[105], d[105];

void solve(int l, int r, int cur = 0) {
    if (l > r)
        return;
    int pos = int(max_element(a + l, a + r + 1) - a);
    d[pos] = cur;
    solve(l, pos - 1, cur + 1);
    solve(pos + 1, r, cur + 1);
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        for (int i=0; i<n; i++)
            cin >> a[i];

        solve(0, n - 1);
        for (int i=0; i<n; i++)
            cout << d[i] << " ";
        cout << "\n";
    }

    return 0;
}