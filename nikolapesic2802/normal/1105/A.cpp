int main()
{
	int n;
	scanf("%i",&n);
	vector<int> sticks(n);
	for(int i=0;i<n;i++)
        scanf("%i",&sticks[i]);
    int minn=INT_MAX,sol;
    for(int t=1;t<101;t++)
    {
        int cost=0;
        for(auto p:sticks)
        {
            int delta=0;
            if(p<t)
                delta=t-1-p;
            if(p>t)
                delta=p-t-1;
            assert(delta>=0);
            cost+=delta;
        }
        if(cost<minn)
        {
            minn=cost;
            sol=t;
        }
    }
    printf("%i %i\n",sol,minn);
    return 0;
}