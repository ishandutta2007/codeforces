vector<int> bfs(vector<vector<int>> &g, int s){
    int n = g.size();
    queue<int> que;
    vector<int> dist(n, -1);
    auto push = [&](int v, int d){
        if(dist[v] == -1){
            dist[v] = d;
            que.push(v);
        }
    };
    push(s, 0);
    while(!que.empty()){
        int v = que.front(); que.pop();
        for(int to: g[v]){
            push(to, dist[v]+1);
        }
    }
    return dist;
}

int op(int x, int y){
    return max(x, y);
}

int e(){
    return -1;
}

using Seg = atcoder::segtree<int, op, e>;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, m, k; cin >> n >> m >> k;
    vector<int> a(k);
    for(int i = 0; i < k; i++) {
        cin >> a[i]; a[i]--;
    }
    vector<vector<int>> g(n);
    for(int i = 0; i < m; i++) {
        int x, y; cin >> x >> y; x--; y--;
        g[x].push_back(y);
        g[y].push_back(x);
    }
    auto ds = bfs(g, 0);
    auto dt = bfs(g, n-1);
    int d = ds[n-1];
    auto ok = [&](int d){
        Seg seg(n+1);
        for(int i = 0; i < k; i++){
            int v = a[i];
            int l = max(0, d-ds[v]-1);
            int x = seg.prod(l, n);
            if(x != -1){
                int tmp = dt[v]+x+1;
                if(tmp >= d) return true;
            }
            if(seg.get(dt[v]) < ds[v]) seg.set(dt[v], ds[v]);
        }
        return false;
    };
    if(ok(d)){
        cout << d << endl;
        return 0;
    }
    int r = d, l = 0;
    while(r-l > 1){
        int x = (l+r)/2;
        if(ok(x)) l = x;
        else r = x;
    }
    cout << l << endl;
    
}