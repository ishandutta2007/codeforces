vector<Mint> fac(1, 1), inv_fac(1, 1);

Mint C(int x, int y) {
  if (x < 0 || y > x || y < 0) {
    return 0;
  }
  while ((int) fac.size() <= x) {
    fac.push_back(fac.back() * (int) fac.size());
    inv_fac.push_back(1 / fac.back());  
  }
  return fac[x] * inv_fac[y] * inv_fac[x - y];
}

Mint P(int x, int y) {
  if (x < 0 || y > x || y < 0) {
    return 0;
  }
  while ((int) fac.size() <= x) {
    fac.push_back(fac.back() * (int) fac.size());
    inv_fac.push_back(1 / fac.back());  
  }
  return fac[x] * inv_fac[y];
}


 
