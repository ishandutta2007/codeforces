const string FILENAME = "input";

const int MAXN = 2e5 + 1;

int n;
vector<int> a;
bool need[MAXN];
int lst[MAXN];
int lstv = -1, cnt = 0;

int get_lst(int i) {
	if (!need[i]) return i;
	return lst[i] = get_lst(lst[i]);
}

int main() {
	srand(time(0));
	ios::sync_with_stdio(false);
	cin >> n;
	a.resize(n);
	for (int i = 0; i < n; ++i) {
		cin >> a[i];
		while (cnt < a[i]) {
			++cnt;
			need[get_lst(i - 1)] = true;
		}
		lst[i] = i - 1;
		if (lstv < a[i]) {
			lstv = a[i];
			need[i] = true;
			++cnt;
		}
	}
	long long d = 0, cur = 0;
	for (int i = 0; i < n; ++i) {
		cur += need[i];
		d += cur - a[i] - 1;
	}
	cout << d << endl;
}