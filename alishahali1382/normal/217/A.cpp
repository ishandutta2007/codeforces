#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 110;

int n, ans;
int X[MAXN];
int Y[MAXN];
bool mark[MAXN];
vector<int> G[MAXN];

void dfs(int node){
	mark[node]=true;
	for (int v:G[node]){
		if (mark[v]) continue ;
		dfs(v);
	}
}

int main(){
	ios_base::sync_with_stdio(false);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=0; i<n; i++){
		cin>>X[i]>>Y[i];
		for (int j=0; j<i; j++){
			if (X[i]==X[j] || Y[i]==Y[j]) {
				G[i].pb(j);
				G[j].pb(i);
			}
		}
	}
	for (int i=0; i<n; i++) {
		if (mark[i]) continue ;
		ans++;
		dfs(i);
	}
	cout<<ans-1<<endl;
	return 0;
}
