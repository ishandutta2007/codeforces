#include <bits/stdc++.h>

const int INF = 0x3f3f3f3f;

class segment_tree_inside {
	int n;

	struct Node {
		Node *ls, *rs;
		int mn;

		Node() : ls(NULL), rs(NULL), mn(INF) {}

		void up() {
			mn = INF;
			if (ls != NULL) {
				mn = std::min(mn, ls->mn);
			}
			if (rs != NULL) {
				mn = std::min(mn, rs->mn);
			}
		}
	};

	Node *rt;

	void modify(Node *&u, int l, int r, int x, int v) {
		if (u == NULL) {
			u = new Node();
		}
		if (l + 1 == r) {
			u->mn = std::min(u->mn, v);
			return;
		}
		int mid = (l + r + 1) >> 1;
		if (x < mid) {
			modify(u->ls, l, mid, x, v);
		} else {
			modify(u->rs, mid, r, x, v);
		}
		u->up();
	}

	int query(Node *u, int l, int r, int L, int R) {
		if (u == NULL) {
			return INF;
		}
		if (L <= l && r <= R) {
			return u->mn;
		}
		int mid = (l + r + 1) >> 1;
		if (R <= mid) {
			return query(u->ls, l, mid, L, R);
		} else if (L >= mid) {
			return query(u->rs, mid, r, L, R);
		} else {
			return std::min(query(u->ls, l, mid, L, R), query(u->rs, mid, r, L, R));
		}
	}

public:
	segment_tree_inside(int _n) : n(_n), rt(NULL) {}

	void modify(int x, int v) {
		modify(rt, 0, n, x, v);
	}

	int query(int l, int r) {
		return query(rt, 0, n, l, r);
	}
};

int enlarge(int n) {
	int res = 1;
	while (res < n) {
		res <<= 1;
	}
	return res;
}

class segment_tree {
	int n;
	std::vector<segment_tree_inside> a;

	void modify(int u, int l, int r, int x, int y, int v) {
		a[u].modify(y, v);
		if (l + 1 == r) {
			return;
		}
		int mid = (l + r + 1) >> 1;
		if (x < mid) {
			modify(u << 1, l, mid, x, y, v);
		} else {
			modify(u << 1 | 1, mid, r, x, y, v);
		}
	}

	int query(int u, int l, int r, int L, int R) {
		if (L <= l && r <= R) {
			return a[u].query(L, R);
		}
		int mid = (l + r + 1) >> 1;
		if (R <= mid) {
			return query(u << 1, l, mid, L, R);
		} else if (L >= mid) {
			return query(u << 1 | 1, mid, r, L, R);
		} else {
			return std::min(query(u << 1, l, mid, L, R), query(u << 1 | 1, mid, r, L, R));
		}
	}

public:
	segment_tree(int _n) : n(_n), a(enlarge(n) << 1, n) {}

	void insert(int l, int r, int v) {
		modify(1, 0, n, l, r - 1, v);
	}

	int query(int l, int r) {
		if (l >= r) {
			return INF;
		}
		return query(1, 0, n, l, r);
	}
};

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int n, m;
	std::cin >> n >> m;
	std::vector<int> l(m), r(m);
	std::vector<std::vector<int>> pos(n);
	for (int i = 0; i < m; ++i) {
		std::cin >> l[i] >> r[i];
		--l[i];
		pos[r[i] - l[i] - 1].push_back(i);
	}
	
	segment_tree T(n);
	std::vector<int> ans(n);
	for (int i = n - 1; i >= 0; --i) {
		for (int id : pos[i]) {
			T.insert(l[id], r[id], id);
		}
		std::priority_queue<std::tuple<int, int, int>> Q;
		auto add = [&](int L, int R) {
			int t = T.query(L, R);
			if (t != INF) {
				Q.emplace(-t, L, R);
			}
		};
		add(0, n);
		while (!Q.empty()) {
			auto [id, L, R] = Q.top();
			Q.pop();
			id = -id;
			ans[i] += r[id] - l[id];
			add(L, l[id]);
			add(r[id], R);
		}
	}
	for (int x : ans) {
		std::cout << x << "\n";
	}
}