  for (int i = 0; i != L; ++i) if ((d >> i) & 1) {
    r -= 1 << i;
    e.emplace_back(1, n - 1 - i, r);
  }
  e.emplace_back(1, n, l);

  cout << n << ' ' << e.size() << '\n';
