static void solve()
{
    string s;
    cin >> s;
    if (!count(RA(s), '.'))
        s += '.';
    int dot = s.find('.');
    string prefix = s.substr(0, dot);
    string suffix = s.substr(dot + 1);
    string comb = prefix + suffix;
    int lz = 0;
    while (comb[lz] == '0')
        lz++;
    comb = comb.substr(lz);
    int exp = SZ(comb) - 1 - SZ(suffix);
    while (comb.back() == '0')
        comb.pop_back();
    if (SZ(comb) > 1)
        comb = comb.substr(0, 1) + "."s + comb.substr(1);
    if (exp != 0)
    {
        comb += 'E';
        comb += to_string(exp);
    }
    cout << comb << '\n';
}

