struct disjoint: vi{
	int n;
	disjoint(int n): n(n){
		this->resize(n);
		iota(all((*this)), 0);
	}
	int insert(){
		this->push_back(this->size());
		return this->size() - 1;
	}
	int root(int u){
		return (*this)[u] == u ? u : ((*this)[u] = root((*this)[u]));
	}
	void merge(int u, int v){
		(*this)[root(v)] = root(u);
	}
};

struct edge{
	int a, b;
	ll w;
	bool operator<(const edge &otr) const{
		return w > otr.w;
	}
};

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<pll> pos(n);
	cin >> pos;
	vl c(n), k(n);
	cin >> c >> k;
	priority_queue<edge> q;
	for(int u = 1; u <= n; u ++){
		q.push({0, u, c[u - 1]});
		for(int v = u + 1; v <= n; v ++){
			q.push({u, v, (k[u - 1] + k[v - 1]) * (abs(pos[u - 1].first - pos[v - 1].first) + abs(pos[u - 1].second - pos[v - 1].second))});
		}
	}
	int cnt = n + 1;
	disjoint dsu(n + 1);
	ll cost = 0;
	vi power;
	vector<pii> connect;
	while(cnt > 1){
		auto [u, v, w] = q.top();
		q.pop();
		if(dsu.root(u) != dsu.root(v)){
			cnt --;
			dsu.merge(u, v);
			cost += w;
			if(u){
				connect.push_back({u, v});
			}
			else{
				power.push_back(v);
			}
		}
	}
	cout << cost << "\n" << power.size() << "\n" << power << connect.size() << "\n";
	for(auto p: connect){
		cout << p.first << " " << p.second << "\n";
	}
	return 0;
}

