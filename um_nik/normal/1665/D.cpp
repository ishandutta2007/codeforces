int X = 999;

int query(int y, int z) {
	assert(y != z);
	printf("? %d %d\n", y, z);
	fflush(stdout);
	int x;
#ifdef LOCAL
	x = gcd(X + y, X + z);
#else
	scanf("%d", &x);
#endif
	return x;
}

void solve() {
	int ans = 0;
	for (int k = 0; k < 30; k++) {
		int x = query((1 << k) - ans, (3 << k) - ans);
		if (x == ((x >> (k + 1)) << (k + 1))) ans += 1 << k;
	}
	printf("! %d\n", ans);
	fflush(stdout);
}

