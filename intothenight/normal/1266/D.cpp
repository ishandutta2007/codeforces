template<class T>
using Ftn = function<T(T, T)>;
template<class T>
using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
typedef tuple<int, int, ll> tpl;
typedef vector<tpl> vtpl;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, m;
	cin >> n >> m;
	vector<pll> a(n); // in, out
	for(int i = 0; i < m; i ++){
		int u, v, w;
		cin >> u >> v >> w, u --, v --;
		a[u].second += w, a[v].first += w;
	}
	vector<pli> in, out; // w, u
	for(int u = 0; u < n; u ++){
		if(a[u].first < a[u].second){
			out.push_back({a[u].second - a[u].first, u});
		}
		else if(a[u].first > a[u].second){
			in.push_back({a[u].first - a[u].second, u});
		}
	}
	sort(all(in)), sort(all(out));
	vector<tpl> res;
	while(!in.empty()){
		if(in.back().first < out.back().first){
			res.push_back({out.back().second, in.back().second, in.back().first});
			out.back().first -= in.back().first;
			in.pop_back();
		}
		else if(in.back().first > out.back().first){
			res.push_back({out.back().second, in.back().second, out.back().first});
			in.back().first -= out.back().first;
			out.pop_back();
		}
		else{
			res.push_back({out.back().second, in.back().second, in.back().first});
			in.pop_back();
			out.pop_back();
		}
	}
	cout << res.size() << "\n";
	for(auto [u, v, w]: res){
		cout << vl{u + 1, v + 1, w};
	}
	return 0;
}
