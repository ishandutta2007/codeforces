const int N = 500500;
char s[N];
int n;

void solve() {
	scanf("%d %s", &n, s);
	int ans = 0;
	int p = 0;
	while(p + 2 <= n) {
		if (s[p] == '(') {
			ans++;
			p += 2;
		} else {
			int q = p;
			p++;
			while(p < n && s[p] == '(') p++;
			if (p == n) {
				p = q;
				break;
			}
			p++;
			ans++;
		}
	}
	printf("%d %d\n", ans, n - p);
}

