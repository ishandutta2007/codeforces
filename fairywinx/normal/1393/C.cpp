void solve() {
	int n;
	cin >> n;
	vector<int> cnt(n + 1);
	for(int i = 0; i < n; ++i) {
		int a;
		cin >> a;
		++cnt[a];
	}	
	int l = 0, r = n;
	while(r - l > 1) {
		int m = (l + r) / 2;
		set<pair<int, int>> z;
		for(int i = 0; i < n + 1; ++i) 
			z.emplace(cnt[i], i);
		vector<int> c = cnt;
		vector<int> ban(3 * n + 5, -1);
		bool can = true;
		// cout << m << ':';
		for(int i = 0; i < n; ++i) {
			if (ban[i] != -1)
				z.emplace(c[ban[i]], ban[i]);
			if (!z.size() || z.rbegin()->first == 0) {
				can = false;
				break;
			}
			auto a = *z.rbegin();
			z.erase(a);
			ban[i + m + 1] = a.second;
			// cout << a.first << ' ' << a.second << ' ' << ban[i] << '\n';
			c[a.second]--;
		}
		// cout << "MEOW " << can << '\n';
		if (can)
			l = m;
		else
			r = m;
	}
	cout << l << '\n';
}

