#include <bits/stdc++.h>
using namespace std;
const int MAXN=12;
bool canu[MAXN][MAXN][MAXN];
int n,m,k;
int tx,ty,ans=0;
char c;
void dfs(int z,int x,int y){
	if(!canu[z][x][y])	return;
	canu[z][x][y]=false;
	ans++;
	dfs(z,x,y+1);
	dfs(z,x,y-1);
	dfs(z,x+1,y);
	dfs(z,x-1,y);
	dfs(z+1,x,y);
	dfs(z-1,x,y);
}
int main(){
	cin>>k>>n>>m;
	for(int i=1;i<=k;i++)
		for(int j=1;j<=n;j++)
			for(int ii=1;ii<=m;ii++){
				cin>>c;
				if(c=='.')	canu[i][j][ii]=true;
				else canu[i][j][ii]=false;
			}
	/*for(int i=1;i<=k;i++){ 
		for(int j=1;j<=n;j++){ 
			for(int ii=1;ii<=m;ii++){
				cout<<canu[i][j][ii];
			}
			cout<<endl;
		}
		cout<<endl;
	} */
	cin>>tx>>ty;
	dfs(1,tx,ty);
	cout<<ans<<endl;
}
