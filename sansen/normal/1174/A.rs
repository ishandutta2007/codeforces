use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        n: usize,
        a: [u32; 2 * n],
    }
    let mut a = a;
    a.sort();
    if a[0] == a[2 * n - 1] {
        writeln!(out, "-1").unwrap();
    } else {
        for (i, a) in a.iter().enumerate() {
            if i > 0 {
                write!(out, " ").unwrap();
            }
            write!(out, "{}", *a).unwrap();
        }
        write!(out, "\n").unwrap();
    }
}

fn main() {
    run();
}