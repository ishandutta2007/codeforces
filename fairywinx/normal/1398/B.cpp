void solve() {
	string s;
	cin >> s;
	int ans = 0;
	vector<int> z;
	for(int i = 0; i < s.size(); ++i) {
		int c = 0;
		while(i < s.size() && s[i] == '1')
			++c, ++i;
		z.push_back(c);
	}
	sort(z.rbegin(), z.rend());
	for(int i = 0; i < z.size(); i += 2)
		ans += z[i];
	cout << ans << '\n';
}

