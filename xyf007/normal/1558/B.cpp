template <typename T>
T add(T a, T b) {
  return (a + b) % m;
}
template <typename T, typename... Args>
T add(T a, T b, Args... args) {
  return add(add(a, b), args...);
}
template <typename T>
T sub(T a, T b) {
  return (a + m - b) % m;
}
template <typename T>
T mul(T a, T b) {
  return a * static_cast<long long>(b) % m;
}
template <typename T, typename... Args>
T mul(T a, T b, Args... args) {
  return mul(mul(a, b), args...);
}
