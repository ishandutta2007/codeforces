template<typename T>
vector<vector<T>> vec2d(int n, int m, T v){
    return vector<vector<T>>(n, vector<T>(m, v));
}

template<typename T>
vector<vector<vector<T>>> vec3d(int n, int m, int k, T v){
    return vector<vector<vector<T>>>(n, vector<vector<T>>(m, vector<T>(k, v)));
}

template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int n, m; cin >> n >> m;
    auto a = vec2d(n, m, 0);
    vector<bool> ok_row(n, true), ok_col(m, true);
    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            cin >> a[i][j];
            if(a[i][j] > 0){
                ok_row[i] = false;
                ok_col[j] = false;
            }
        }
    }
    int nn = 0, mm = 0;
    for(int i = 0; i < n; i++){
        if(ok_row[i]) nn++;
    }
    for(int j = 0; j < m; j++){
        if(ok_col[j]) mm++;
    }
    int mn = min(nn, mm);
    if(mn%2 == 0){
        cout << "Vivek" << endl;
    }else{
        cout << "Ashish" << endl;
    }
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}