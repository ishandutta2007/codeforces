class InputReader {
public:
    InputReader() {
        input_file = stdin;
        cursor = 0;
        fread(buffer, SIZE, 1, input_file);
    }

    InputReader(const char *file_name) {
        input_file = fopen(file_name, "r");
        cursor = 0;
        fread(buffer, SIZE, 1, input_file);
    }

    template<typename T>
    inline InputReader &operator>>(T &n) {
        while ((buffer[cursor] < '0' || buffer[cursor] > '9') && buffer[cursor] != '-') {
            advance();
        }
        int sign = 1;
        if (buffer[cursor] == '-') {
            sign = -1;
            advance();
        }
        n = 0;
        while ('0' <= buffer[cursor] && buffer[cursor] <= '9') {
            n = n * 10 + buffer[cursor] - '0';
            advance();
        }
        n *= sign;
        return *this;
    }

private:
    FILE *input_file;
    static const int SIZE = 1 << 17;
    int cursor;
    char buffer[SIZE];

    inline void advance() {
        ++cursor;
        if (cursor == SIZE) {
            cursor = 0;
            fread(buffer, SIZE, 1, input_file);
        }
    }
};

const int MAXN = 100000;

char a[1 + MAXN], b[1 + MAXN];

int main() {
    timeBegin();
    //  ifstream cin("input.txt");
//    ofstream cout("answer.txt");
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    srand(time(0));
    int tests;
    cin >> tests;
    for (int test = 1; test <= tests; test++) {
        int n;
        cin >> n >> (a + 1) >> (b + 1);
        int bad = 0;
        int cntBad[2] = {0, 0}, cntGood[2] = {0, 0};
        for (int i = 1; i <= n; i++) {
            bad += (a[i] != b[i]);
            if (a[i] != b[i]) {
                cntBad[a[i] - '0']++;
            } else {
                cntGood[a[i] - '0']++;
            }
        }
        int ans = 2 * n;
        if (bad % 2 == 0 && cntBad[0] == cntBad[1]) {
            ans = min(ans, bad);
        }
        if ((n - bad) % 2 == 1 && cntGood[1] == cntGood[0] + 1) {
            ans = min(ans, n - bad);
        }
        if (ans == 2 * n) {
            ans = -1;
        }
        cout << ans << "\n";
    }
    return 0;
}