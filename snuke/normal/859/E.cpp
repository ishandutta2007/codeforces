// Union find
struct uf {
  vi d, e;
  uf(){}
  uf(int mx):d(mx,-1),e(mx,0){}
  int root(int x) {
    if(d[x] < 0) return x;
    return d[x] = root(d[x]);
  }
  bool unite(int x, int y) {
    x = root(x); y = root(y);
    e[x]++;
    if(x == y) return false;
    if(d[x] > d[y]) swap(x,y);
    e[x] += e[y];
    d[x] += d[y]; d[y] = x;
    return true;
  }
  int size(int x) { return -d[root(x)];}
};
