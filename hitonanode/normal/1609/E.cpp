struct S {
    lint n00, n01, n02, n11, n12, n22;
    lint get_min() {
        return min({n00, n01, n02, n11, n12, n22});
    }
};
S e() { return S{0, 0, 0, 0, 0, 0}; }
S op(S l, S r) {
    lint n00 = l.n00 + r.n00;
    lint n11 = l.n11 + r.n11;
    lint n22 = l.n22 + r.n22;
    lint n01 = min({l.n00 + r.n01, l.n01 + r.n11, l.n00 + r.n11});
    lint n12 = min({l.n12 + r.n22, l.n11 + r.n12, l.n11 + r.n22});
    lint n02 = min({
        l.n01 + r.n12,
        l.n00 + r.n02,
        l.n02 + r.n22,
    });
    chmin(n01, n00);
    chmin(n01, n11);
    chmin(n12, n11);
    chmin(n12, n22);
    chmin(n02, n01);
    chmin(n02, n12);
    return {n00, n01, n02, n11, n12, n22};
}

constexpr lint inf = 1 << 20;

S A() { return {1, 0, 0, 0, 0, 0}; }
S B() { return {0, 0, 0, 1, 0, 0}; }
S C() { return {0, 0, 0, 0, 0, 1}; }

S char2s(char c) {
    if (c == 'a') return A();
    if (c == 'b') return B();
    if (c == 'c') return C();
    throw;
}

int main() {
    int N, Q;
    string str;
    cin >> N >> Q >> str;
    vector<S> init;
    for (auto c : str) init.push_back(char2s(c));
    atcoder::segtree<S, op, e> tree(init);
    while (Q--) {
        int pos;
        char c;
        cin >> pos >> c;
        pos--;
        tree.set(pos, char2s(c));
        auto p = tree.all_prod();
        cout << p.get_min() << '\n';
    }
}