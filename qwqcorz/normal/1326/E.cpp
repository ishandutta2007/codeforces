#include<bits/stdc++.h>
using namespace std;
const int N=3e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
#define ls (w*2)
#define rs (w*2+1)
#define mid ((l+r)/2)
int t[N*4],tag[N*4],p[N],np[N],q[N];
void update(int w,int x)
{
	t[w]+=x;
	tag[w]+=x;
}
void push_down(int w)
{
	if (!tag[w]) return;
	update(ls,tag[w]);
	update(rs,tag[w]);
	tag[w]=0;
}
void add(int w,int l,int r,int ql,int qr,int x)
{
	if (qr<l||r<ql) return;
	if (ql<=l&&r<=qr) return update(w,x);
	push_down(w);
	add(ls,l,mid,ql,qr,x);
	add(rs,mid+1,r,ql,qr,x);
	t[w]=max(t[ls],t[rs]);
}
#undef ls
#undef rs
#undef mid

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	for (int i=1;i<=n;i++) np[p[i]=read()]=i;
	for (int i=1;i<=n;i++) q[i]=read();
	print(n,' ');
	add(1,1,n,1,np[n],1);
	for (int i=1,ans=n;i<n;i++)
	{
		add(1,1,n,1,q[i],-1);
		while (t[1]<=0) add(1,1,n,1,np[--ans],1);
		print(ans,' ');
	}
	
	return 0;
}
