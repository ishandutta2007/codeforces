void solve() {
    int n; cin >> n;
    vector<int> a(n), b(n);
    rep(i, n) {
        cin >> a[i];
    }
    rep(i, n) {
        cin >> b[i];
    }
    mf_graph<int> g(n + 2);
    int s = n, t = n + 1;
    int offset = 0;
    rep(i, n) {
        if(b[i] > 0) {
            offset += b[i];
            g.add_edge(s, i, b[i]);
            g.add_edge(i, t, 0);
        } else {
            g.add_edge(i, t, -b[i]);
            g.add_edge(s, i, 0);
        }
    }
    vector<bool> check(101, 0);
    rep(i, n) {
        check.assign(101, 0);
        for(int j = i - 1; j >= 0; -- j) {
            if(!check[a[j]] and a[i] % a[j] == 0) {
                g.add_edge(i, j, inf);
                check[a[j]] = 1;
            }
        }
    }
    cout << offset - g.flow(s, t) << "\n";
}

