mt19937 rnd(time(0));



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, t;
	cin >> n >> t, t;
	string a, b;
	cin >> a >> b;
	int cnt = 0;
	for(int i = 0; i < n; i ++){
		if(a[i] != b[i]){
			cnt ++;
		}
	}
	if(t < cnt - cnt / 2){
		cout << -1;
		return 0;
	}
	t = n - t, cnt = n - cnt;
	int eq = 0, eqa = 0, eqb = 0; // t: number of dif
	if(t > cnt){                    // eq: number of dif at eq digits
		eq = cnt;               // dif digit: eqa: equal to a, eqb: equal to b; 
		eqa = eqb = t - cnt;
	}
	else{
		eq = t;
	}
	string res(n, 'a');
	for(int i = 0; i < n; i ++){
		if(a[i] == b[i]){
			if(eq){
				eq --;
				res[i] = a[i];
			}
			else{
				while(res[i] == a[i] || res[i] == b[i]){
					res[i] ++;
				}
			}
		}
		else{
			if(eqa){
				eqa --;
				res[i] = a[i];
			}
			else if(eqb){
				eqb --;
				res[i] = b[i];
			}
			else{
				while(res[i] == a[i] || res[i] == b[i]){
					res[i] ++;
				}
			}
		}
	}
	cout << res;
	return 0;
}

