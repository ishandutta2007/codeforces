ll gcd(ll a, ll b){
    if(min(a, b) == 0) return max(a, b);
    return __gcd(a, b);
}

// kactl
struct Tree {
	typedef ll T;
	static constexpr T unit = 0;
	T f(T a, T b) { return gcd(a, b); } // (any associative fn)
	vector<T> s; int n;
	Tree(int n = 0, T def = unit) : s(2*n, def), n(n) {}
	void update(int pos, T val) {
		for (s[pos += n] = val; pos /= 2;)
			s[pos] = f(s[pos * 2], s[pos * 2 + 1]);
	}
	T query(int b, int e) { // query [b, e)
		T ra = unit, rb = unit;
		for (b += n, e += n; b < e; b /= 2, e /= 2) {
			if (b % 2) ra = f(ra, s[b++]);
			if (e % 2) rb = f(s[--e], rb);
		}
		return f(ra, rb);
	}
};

void solve(){
    int n;
    cin >> n;
    vll v(n);
    for(int i = 0; i < n; i++) cin >> v[i];
    vll d(n-1);
    for(int i = 0; i+1 < n; i++){
        d[i] = abs(v[i+1] - v[i]);
    }
    debug(d);
    Tree st(n-1);
    for(int i = 0; i < n-1; i++){
        st.update(i, d[i]);
    }
    int p = 0, ans = 0;
    for(int i = 1; i <= n-1; i++){
        debug(i, p, st.query(p, i));
        while(st.query(p, i) == 1){
            p++;
        }
        debug(i, p, st.query(p, i));
        ans = max(ans, i-p);
    }
    cout << ans+1 << endl;
}

