template <class A, class B>
bool smin(A &x, B &&y) {
  if (y < x) {
    x = y;
    return true;
  }
  return false;
}

template <class A, class B>
bool smax(A &x, B &&y) {
  if (x < y) {
    x = y;
    return true;
  }
  return false;
}

void solveTest() {
  int n, m;
  cin >> n >> m;
  string s, t;
  cin >> s >> t;
  if (s.substr(n - m + 1, m - 1) != t.substr(1)) {
    cout << "NO\n";
    return;
  }
  int h[2]{};
  for (int i = 0; i < n - m + 1; ++i) {
    h[s[i] - '0'] = 1;
  }
  cout << (h[t[0] - '0'] ? "YES" : "NO") << '\n';
}

