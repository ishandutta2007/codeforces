const int MAXN = 100010;

ll n, m, k, x, y, z, a, b, c, t1, t2, cnt, p;
ll X[MAXN];
ll Y[MAXN];
bool X2[MAXN];
bool Y2[MAXN];

ll powmod(ll a, ll b){
	if (!b) return 1;
	if (b&1) return a*powmod(a, b-1)%p;
	return powmod(a*a%p, b>>1);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m>>k;
	if ((m^n)&1) kill(0);
	for (int i=0; i<k; i++){
		cin>>a>>b>>c;
		X[a]++;
		Y[b]++;
		if (c==-1){
			X2[a]^=1;
			Y2[b]^=1;
		}
	}
	for (int i=1; i<=n; i++) if (X[i]==m){
		if (!X2[i]) kill(0);
		t1++;
	}
	for (int i=1; i<=m; i++) if (Y[i]==n){
		if (!Y2[i]) kill(0);
		t2++;
	}
	cnt=(n-t1-1)*(m-t2-1)-(k-t1*m-t2*n+t1*t2);
	cin>>p;
	cout<<powmod(2, cnt)<<endl;
	
	return 0;
}
