constexpr int mod = (int)1e9 + 7;
//constexpr int mod = 998244353;
using Zp = Z_p<integral_constant<decay<decltype(mod)>::type, mod>>;

int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, m;
	cin >> n >> m;
	vector<int> sweet(n);
	for(auto i = 0; i < n; ++ i){
		cin >> sweet[i], -- sweet[i];
	}
	vector<vector<array<int, 2>>> h(n);
	for(auto i = 0; i < m; ++ i){
		int x, y;
		cin >> x >> y, -- x;
		int l = 0, r = n - 1, t = y;
		while(l < n){
			if(sweet[l] == x){
				-- t;
			}
			if(!t){
				break;
			}
			++ l;
		}
		if(l < n){
			t = y;
			while(r >= 0){
				if(sweet[r] == x){
					-- t;
				}
				if(!t){
					break;
				}
				-- r;
			}
			h[x].push_back({l, r});
		}
	}
	for(auto s = 0; s < n; ++ s){
		sort(h[s].begin(), h[s].end());
	}
	pair<int, Zp> res{0, 1};
	for(auto s = 0; s < n; ++ s){
		for(auto i = 0; i < int(h[s].size()); ++ i){ // h[s][i] is furthest cow from left
			pair<int, Zp> cur{1, 1};
			for(auto t = 0; t < n; ++ t){
				if(s == t){
					int cnt = 0;
					for(auto j = 0; j < int(h[t].size()); ++ j){
						if(i ^ j && h[s][i][0] < h[t][j][1]){
							++ cnt;
						}
					}
					if(cnt){
						++ cur.first;
						cur.second *= cnt;
					}
				}
				else{
					int cnt_s = 0, cnt_l = 0;
					for(auto j = 0; j < int(h[t].size()); ++ j){
						if(h[t][j][0] < h[s][i][0] && h[s][i][0] < h[t][j][1]){
							++ cnt_s;
						}
						if(h[t][j][0] < h[s][i][0] || h[s][i][0] < h[t][j][1]){
							++ cnt_l;
						}
					}
					if(cnt_l >= 1){
						if(cnt_l >= 2 && cnt_s >= 1){
							cur.first += 2;
							cur.second *= cnt_s * (cnt_l - 1);
						}
						else{
							++ cur.first;
							cur.second *= cnt_s + cnt_l;
						}
					}
				}
			}
			if(cur.first == res.first){
				res.second += cur.second;
			}
			else if(cur.first > res.first){
				res = cur;
			}
			cur = {1, 1};
			for(auto t = 0; t < n; ++ t){
				if(s ^ t){
					int cnt = 0;
					for(auto j = 0; j < int(h[t].size()); ++ j){
						if(h[s][i][1] < h[t][j][1]){
							++ cnt;
						}
					}
					if(cnt){
						++ cur.first;
						cur.second *= cnt;
					}
				}
			}
			if(cur.first == res.first){
				res.second += cur.second;
			}
			else if(cur.first > res.first){
				res = cur;
			}
		}
	}
	cout << res.first << " " << res.second << "\n";
	return 0;
}

