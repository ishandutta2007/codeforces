using namespace std;
template<typename T>
using minpq = priority_queue<T, vector<T>, greater<T>>;

void solve() {
    ll a, b;
    cin >> a >> b;
    if(a == b) cout << 0 << ' ' << 0 << '\n';
    else
    cout << abs(a - b) << ' ' << min(a % abs(a - b), abs(a - b) - (a % abs(a - b))) << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int te;
    cin >> te;
    while(te--) solve();
}