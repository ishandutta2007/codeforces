const int N=2e5+5;
vector<vector<int> > graf(N),rev(N);
vector<vector<pair<int,int> > > bi(N);
vector<int> done(N);
void dfs(int tr){
	done[tr]=1;
	for(auto p:graf[tr]){
		if(done[p]==1){
			printf("-1\n");
			exit(0);
		}
		if(!done[p]){
			dfs(p);
		}
	}
	done[tr]=2;
}
vec<2,int> visited(N,2);
void dfs2(int a,int lst){
	visited[a][lst]=1;
	for(auto p:bi[a])
		if(!visited[p.f][lst]&&p.s==lst)
			dfs2(p.f,lst);
}
int main()
{
	int n,m;
	scanf("%i %i",&n,&m);
	for(int i=0;i<m;i++){
		int a,b;
		scanf("%i %i",&a,&b);
		a--;b--;
		graf[a].pb(b);
		bi[a].pb({b,0});
		bi[b].pb({a,1});
	}
	for(int i=0;i<n;i++)
		if(!done[i])
			dfs(i);
	string sol(n,'E');
	int cnt=0;
	for(int i=0;i<n;i++){
		if(!visited[i][0]&&!visited[i][1]){
			cnt++;
			sol[i]='A';
		}
		if(!visited[i][0])
			dfs2(i,0);
		if(!visited[i][1])
			dfs2(i,1);
	}
	cout << cnt << endl << sol << endl;
	return 0;
}