const LL INF = 1e9 + 7;
const int N = 3e5 + 10;
LL a[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	cin >> n;
	for (int i = 1; i <= n; i++) cin >> a[i];
	sort(a + 1, a + n + 1);
	int l = 1, r = n;
	LL ans = 0;
	while (l < r)
	{
		LL x = a[l++] + a[r--];
		ans += x * x;
	}
	cout << ans << endl;
	return 0;
}