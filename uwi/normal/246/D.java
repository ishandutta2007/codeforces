	void solve()
	{
		int n = ni(), m = ni();
		int[] c = new int[n];
		boolean[] alive = new boolean[100001];
		for(int i = 0;i < n;i++){
			c[i] = ni();
			alive[c[i]] = true;
		}
		
		int[] from = new int[m];
		int[] to = new int[m];
		for(int i = 0;i < m;i++){
			from[i] = ni()-1;
			to[i] = ni()-1;
		}
		int[][] g = packU(n, from, to);
		long[] q = new long[2*m];
		int p = 0;
		for(int i = 0;i < n;i++){
			for(int j = 0;j < g[i].length;j++){
				q[p++] = (long)c[i]<<32|c[g[i][j]];
			}
		}
		Arrays.sort(q);
		
		int[] cost = new int[100001];
		for(int i = 0;i < 2*m;i++){
			if(i > 0 && q[i] == q[i-1])continue;
			int co = (int)(q[i]>>>32);
			int t = (int)q[i];
			if(t != co){
				cost[co]++;
			}
		}
		int max = -1;
		int maxi = -1;
		for(int i = 0;i <= 100000;i++){
			if(alive[i] && cost[i] > max){
				max = cost[i];
				maxi = i;
			}
		}
		out.println(maxi);
	}
	
	static int[][] packU(int n, int[] from, int[] to) {
		int[][] g = new int[n][];
		int[] p = new int[n];
		for(int f : from)
			p[f]++;
		for(int t : to)
			p[t]++;
		for(int i = 0;i < n;i++)
			g[i] = new int[p[i]];
		for(int i = 0;i < from.length;i++){
			g[from[i]][--p[from[i]]] = to[i];
			g[to[i]][--p[to[i]]] = from[i];
		}
		return g;
	}
	
