#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 100010;

int n, ans=1, x, tmp;
int A[MAXN];
int dp[MAXN];
int sieve[MAXN];

vector<int> vec;

int main(){
	ios_base::sync_with_stdio(false);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	for (int i=1; i<MAXN; i++) sieve[i]=i;
	for (int i=2; i*i<MAXN; i++){
		if (sieve[i]!=i) continue ;
		for (int j=i*i; j<MAXN; j+=i) sieve[j]=min(sieve[j], i);
	}
	cin>>n;
	for (int i=0; i<n; i++) cin>>A[i];
	sort(A, A+n);
	
	for (int i=0; i<n; i++){
		//if (!A[i]) continue ;
		tmp=A[i];
		vec.clear();
		while (tmp!=1){
			x=sieve[tmp];
			while (tmp%x==0) tmp/=x;
			vec.pb(x);
		}
		x=0;
		for (int i:vec) x=max(x, dp[i]+1);
		for (int i:vec) dp[i]=max(dp[i], x);
	}
	for (int i=0; i<MAXN; i++) ans=max(ans, dp[i]);
	cout<<ans<<endl;
	return 0;
}
