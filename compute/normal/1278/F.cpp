const int INF=0x3f3f3f3f;
const int mod=998244353;
const int maxn=5e3+7;
ll dp[maxn][maxn];
ll quick(ll a,ll b)
{
	ll ret=1;
	while(b)
	{
		if(b&1) ret=ret*a%mod;
		a=a*a%mod;
		b>>=1;
	}
	return ret;
}
ll inv(ll a){return quick(a,mod-2);}
int main()
{
	ll n,m,k;
	cin>>n>>m>>k;
	dp[0][0]=1;
	for(int i=1;i<=k;i++)
		for(int j=1;j<=i;j++)
			dp[i][j]=(dp[i-1][j-1]*(n-j+1)%mod+dp[i-1][j]*j%mod)%mod;
	ll ans=0;
	ll mm=inv(m);
	ll cur=mm;
	for(int i=1;i<=k;i++)
	{
		ans=(ans+cur*dp[k][i])%mod;
		cur=cur*mm%mod;
	}
	cout<<ans<<endl;
}