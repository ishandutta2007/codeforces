use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        ask: [(usize, usize); t],
    }
    for (n, k) in ask {
        let mut ans = vec![vec![0; n]; n];
        let mut x = k;
        for i in 0..n {
            let mut r = std::cmp::min(n, x);
            x -= r;
            for j in 0..n {
                if r > 0 {
                    ans[j][(i + j) % n] = 1;
                    r -= 1;
                }
            }
        }
        let mut s = String::new();
        s.push_str(&format!("{}\n", if k % n == 0 {0} else {2}));
        for a in ans {
            for a in a {
                s.push_str(&format!("{}", a));
            }
            s.push('\n');
        }
        write!(out, "{}", s).ok();
    }
}

fn main() {
    run();
}