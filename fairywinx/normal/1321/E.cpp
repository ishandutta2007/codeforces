const int N = (1 << 18);
const int INF = -2e9 - 228;

int t[2 * N];
int mod[2 * N];

void push(int v) {
	t[v * 2 + 1] += mod[v];
	t[v * 2] += mod[v];
	mod[v * 2] += mod[v];
	mod[v * 2 + 1] += mod[v];
	mod[v] = 0;
}

void update(int v, int l, int r, int L, int R, int val) {
	if (r <= L || R <= l)
		return;
	if (L <= l && r <= R) {
		t[v] += val;
		mod[v] += val;
		return;
	}

	push(v);
	int m = (l + r) / 2;
	update(v * 2 + 1, l, m, L, R, val);
	update(v * 2, m, r, L, R, val);
	t[v] = max(t[v * 2 + 1], t[v * 2]);
}

void build(int v, int l, int r, vector<pair<int, int>> &b) {
	if (r - l == 1) {
		t[v] = -b[l].second;
		mod[v] = 0;
		return;
	}

	int m = (l + r) / 2;
	build(v * 2 + 1, l, m, b);
	build(v * 2, m, r, b);
	t[v] = max(t[v * 2], t[v * 2 + 1]);
}

signed main() {
	setup();
	
	int n, m, p;
	cin >> n >> m >> p;
	vector<pair<int, int>> a(n);
	vector<pair<int, int>> b(m);
	for(int i = 0; i < n; ++i) 
		cin >> a[i].first >> a[i].second;
	for(int i = 0; i < m; ++i)
		cin >> b[i].first >> b[i].second;
	vector<pair<pair<int, int>, int>> c(p);
	for(int i = 0; i < p; ++i)
		cin >> c[i].first.first >> c[i].first.second >> c[i].second;
	sort(a.begin(), a.end());
	sort(b.begin(), b.end());
	sort(c.begin(), c.end());
	build(1, 0, m, b);
	int l = 0;
	int ans = INF;
	for(int i = 0; i < n; ++i) {
		// cout << "MEOW " << a[i].first << ' ' << a[i].second
		while(l < p && c[l].first.first < a[i].first) {
			int ind = upper_bound(b.begin(), b.end(), make_pair(c[l].first.second, -INF)) - b.begin();
			if (ind != m)
				update(1, 0, m, ind, m, c[l].second);
			++l;
		}
		ans = max(ans, -a[i].second + t[1]);
	}
	cout << ans << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

