const int maxn = 1e5 + 100, inf = 1e9 + 100, sq = 300, mod = 1e9 + 7, LG = 17;

int rem[maxn];

int n, m;

vector<int> e[maxn];

int a[maxn];

queue<int> g;

int ans;

bool can[maxn];

bool now;

vector<int> b;

void go(int v){
    if (a[v])
        now = 1;
    for (auto i : e[v]){
        rem[i]--;
        if (a[v] && !a[i])
            can[i] = 1, b.push_back(i);
        if (rem[i] == 0){
            if (can[i])
                g.push(i);
            else
                go(i);
        }
    }
}

