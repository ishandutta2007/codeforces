template<class T>
T kadane(const vector<T> &arr){
	int n = arr.size();
	T lm = 0, gm = 0;
	for(int i = 0; i < n; i ++){
		lm = max(arr[i], arr[i] + lm);
		gm = max(gm, lm);
	}
	return gm;
}

int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, c;
	cin >> n >> c;
	vi a(n);
	cin >> a;
	vi cnt(n + 1);
	for(int i = 1; i <= n; i ++){
		cnt[i] = cnt[i - 1] + (a[i - 1] == c);
	}
	int MX = 5e5 + 1;
	vector<vi> freq(MX);
	for(int i = 0; i < n; i ++){
		if(a[i] != c){
			freq[a[i]].push_back(i);
		}
	}
	int res = cnt[n];
	for(int ii = 0; ii < MX; ii ++) if(freq[ii].size()){
		vi t{1};
		for(int i = 1; i < freq[ii].size(); i ++){
			t.push_back(-cnt[freq[ii][i]] + cnt[freq[ii][i - 1]]);
			t.push_back(1);
		}
		res = max(res, cnt[n] + kadane(t));
	}
	cout << res;
	return 0;
}
