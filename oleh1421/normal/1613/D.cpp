#include <bits/stdc++.h>
#define endl '\n'
typedef long long ll;
using namespace std;
mt19937 rnd(time(nullptr));
const int N=1000100;
const ll mod=998244353;
ll dp[N][2];
void solve(){
    int n;cin>>n;
    for (int i=1;i<=n+1;i++){
        for (int j=0;j<2;j++){
            dp[i][j]=0;
        }
    }
    dp[1][0]=1;
    for (int i=1;i<=n;i++){
        int x;cin>>x;
        x++;
        dp[x+1][1]+=dp[x+1][1];
        dp[x+1][1]%=mod;
        dp[x+1][0]+=dp[x+1][0]+dp[x][0];
        dp[x+1][0]%=mod;
        dp[x-1][1]+=dp[x-1][0]+dp[x-1][1];
        dp[x-1][1]%=mod;
    }
    ll res=0ll;
    for (int i=1;i<=n+1;i++) res=(res+dp[i][0]+dp[i][1])%mod;
    cout<<(res+mod-1)%mod<<endl;
}
int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int tt;cin>>tt;
    while (tt--){
        solve();
    }
    return 0;
}

