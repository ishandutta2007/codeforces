	int mod = 1000000007;
	int[][] fif = enumFIF(200005, mod);
	
	
	void solve()
	{
		int f = ni(), w = ni(), h = ni();
		if(f == 0){
			if(w <= h){
				out.println(0);
			}else{
				out.println(1);
			}
			return;
		}
		if(w == 0){
			out.println(1);
			return;
		}
		
		long[] fs = new long[100005];
		long[] ws = new long[100005];
		long[] whs = new long[100005];
		for(int i = 1;i <= 100004;i++){
			fs[i] = C(f-1, i-1, mod, fif);
			ws[i] = C(w-1, i-1, mod, fif);
			whs[i] = C(w-1-(long)i*h, i-1, mod, fif);
		}
		long Q = 0, P = 0;
		for(int i = 0;i <= 100000;i++){
			Q += fs[i]*ws[i]*2+fs[i+1]*ws[i]+fs[i]*ws[i+1];
			Q %= mod;
			P += fs[i]*whs[i]*2+fs[i+1]*whs[i]+fs[i]*whs[i+1];
			P %= mod;
		}
		long ret = P*invl(Q, mod) % mod;
		if(ret < 0)ret += mod;
		out.println(ret);
	}
	
	long X(int num, int place, int h)
	{
		if(num < 0)return 0;
		if(h < 0)return 0;
		if(num > (long)place*h)return 0;
		
		long ret = 0;
		int sgn = 1;
		for(int i = 0;num-(h+1)*i >= 0;i++, sgn = -sgn){
			ret += C(place, i, mod, fif) * C(num-(h+1)*i+place-1, place-1, mod, fif) * sgn;
			ret %= mod;
		}
		if(ret < 0)ret += mod;
		return ret;
	}
	
	public static long invl(long a, long mod) {
		long b = mod;
		long p = 1, q = 0;
		while (b > 0) {
			long c = a / b;
			long d;
			d = a;
			a = b;
			b = d % b;
			d = p;
			p = q;
			q = d - c * q;
		}
		return p < 0 ? p + mod : p;
	}

	
	public static long C(long n, int r, int mod, int[][] fif) {
		if (n < 0 || r < 0 || r > n)
			return 0;
		return (long) fif[0][(int)n] * fif[1][r] % mod * fif[1][(int)n - r] % mod;
	}

	
	public static int[][] enumFIF(int n, int mod) {
		int[] f = new int[n + 1];
		int[] invf = new int[n + 1];
		f[0] = 1;
		for (int i = 1; i <= n; i++) {
			f[i] = (int) ((long) f[i - 1] * i % mod);
		}
		long a = f[n];
		long b = mod;
		long p = 1, q = 0;
		while (b > 0) {
			long c = a / b;
			long d;
			d = a;
			a = b;
			b = d % b;
			d = p;
			p = q;
			q = d - c * q;
		}
		invf[n] = (int) (p < 0 ? p + mod : p);
		for (int i = n - 1; i >= 0; i--) {
			invf[i] = (int) ((long) invf[i + 1] * (i + 1) % mod);
		}
		return new int[][] { f, invf };
	}

	
	
	void run() throws Exception
	{
		is = oj ? System.in : new ByteArrayInputStream(INPUT.getBytes());
		out = new PrintWriter(System.out);
		
		long s = System.currentTimeMillis();
		solve();
		out.flush();
		tr(System.currentTimeMillis()-s+"ms");
	}
	
