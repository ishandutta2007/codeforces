void solve() {
	int n;
	cin >> n;	
	vector<int> a(n), b(n);
	for(int i = 0; i < n; ++i) {
		cin >> a[i];
	}
	for(int i = 0; i < n; ++i) {
		cin >> b[i];
	}
	if (n % 2 == 1 && b[n / 2] != a[n / 2]) {
		cout << "No\n";
		return;
	}
	vector<pair<int, int>> lv;
	for(int i = 0; i < n / 2; ++i) {
		lv.emplace_back(i, a[i]);
	}
	for(int i = (n + 1) / 2; i < n; ++i) {
		lv.emplace_back(n - 1 - i, a[i]);
	}
	multiset<pair<int, int>> z;
	for(int i = 0; i < n / 2; ++i)
		z.emplace(b[i], b[n - 1 - i]);
	bool ans = true;
	sort(lv.begin(), lv.end());
	for(int i = 0; i < lv.size(); i += 2) {
		if (z.find({lv[i].second, lv[i + 1].second}) != z.end()) 
			z.erase(z.find({lv[i].second, lv[i + 1].second}));
		else if (z.count({lv[i + 1].second, lv[i].second}))
			z.erase(z.find({lv[i + 1].second, lv[i].second}));
		else {
			// cout << lv[i].first << ' ' << lv[i].second << ' ' << lv[i + 1].second << '\n';
			ans = false;
		}
	}
	if (ans)
		cout << "Yes\n";
	else
		cout << "No\n";
}

