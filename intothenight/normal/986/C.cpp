mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	const int mx = 1 << n;
	bitset<1 << 22> visited, used;
	used.set();
	for(int i = 0; i < m; i ++){
		int u;
		cin >> u;
		used.reset(u);
	}
	int res = 0;
	for(int u = 0; u < mx; u ++){
		if(!used[u]){
			res ++;
			visited[u] = used[u] = true;
			vi q{u};
			while(!q.empty()){
				int u = q.back();
				q.pop_back();
				if(!used[mx - 1 - u]){
					visited[mx - 1 - u] = used[mx - 1 - u] = true;
					q.push_back(mx - 1 - u);
				}
				for(int bit = 0; bit < n; bit ++){
					if(!(u & 1 << bit)){
						int v = u ^ 1 << bit;
						if(!visited[v]){
							visited[v] = true;
							q.push_back(v);
						}
					}
				}
			}
		}
	}
	cout << res;
	return 0;
}
