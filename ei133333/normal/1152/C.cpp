int64 lcm(int64 x, int64 y) {
  return x / __gcd(x, y) * y;
}

vector< int64_t > divisor(int64_t n) {
  vector< int64_t > ret;
  for(int64_t i = 1; i * i <= n; i++) {
    if(n % i == 0) {
      ret.push_back(i);
      if(i * i != n) ret.push_back(n / i);
    }
  }
  sort(begin(ret), end(ret));
  return (ret);
}

int64 get(int64 A, int64 B) {
  if(A == B) return 0;
  if(A < B) swap(A, B);
  auto div = divisor(A - B);
  int64 ret = infll;
  int64 ans;
  for(auto &p : div) {
    int64 latte = (A + p - 1) / p * p, malta = (B + p - 1) / p * p;
    int64 D = max(latte - A, malta - B);
    latte = A + D, malta = B + D;
    if(chmin(ret, lcm(latte, malta))) {
      ans = D;
    } else if(ret == lcm(latte, malta)) {
      chmin(ans, D);
    }
  }
  return ans;
}

