template< typename T >
vector< T > dijkstra(WeightedGraph< T > &g, int s) {
  const auto INF = numeric_limits< T >::max();
  vector< T > dist(g.size(), INF);

  using Pi = pair< T, int >;
  priority_queue< Pi, vector< Pi >, greater< Pi > > que;
  dist[s] = 0;
  que.emplace(dist[s], s);
  while(!que.empty()) {
    T cost;
    int idx;
    tie(cost, idx) = que.top();
    que.pop();
    if(dist[idx] < cost) continue;
    for(auto &e : g[idx]) {
      auto next_cost = cost + e.cost;
      if(dist[e.to] <= next_cost) continue;
      dist[e.to] = next_cost;
      que.emplace(dist[e.to], e.to);
    }
  }
  return dist;
}


int main() {
  int N, M;
  cin >> N >> M;
  WeightedGraph< int > g(N), h(N);
  for(int i = 0; i < M; i++) {
    int a, b;
    cin >> a >> b;
    --a, --b;
    g[a].emplace_back(b, 1);
    h[b].emplace_back(a, 1);
  }
  int V;
  cin >> V;
  vector< int > K(V);
  cin >> K;
  for(auto &p : K) --p;

  auto latte = dijkstra(g, K[0]);
  auto malta = dijkstra(h, K[V - 1]);
  vector< set< int > > st(N);
  for(int i = 0; i < N; i++) {
    for(auto &to : g[i]) {
      if(malta[to] + 1 == malta[i]) {
        st[i].emplace(to);
      }
    }
  }


  int x = 0, y = 0;
  for(int k = 0; k + 1 < V; k++) {
    int s = K[k];
    int t = K[k + 1];
    if(malta[t] + 1 != malta[s]) ++x;

    if(st[s].count(t)) {
      if(st[s].size() >= 2) ++y;
    } else {
      ++y;
    }
  }
  cout << x << " " << y << endl;
}