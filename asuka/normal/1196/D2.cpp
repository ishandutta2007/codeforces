#include<bits/stdc++.h>
#define ll long long
#define N 200015
#define inf 0x3f3f3f3f
#define pb push_back
#define mp make_pair
#define lowbit(i) ((i)&(-i))
using namespace std;
int t,n,dp[N][3],k,ans;
string s,a="RGB",b="GBR",c="BRG";
int f(char s){
	if(s=='R') return 0;	
	if(s=='G') return 1;	
	if(s=='B') return 2;	
}

int main(){
	//freopen(".in","r",stdin);
	//freopen(".out","w",stdout);
	ios::sync_with_stdio(0);
	cin.tie(0);
	cin>>t;
	while(t--){
		cin>>n>>k;
		cin>>s;
		int cur = 0;
		for(int i = 0;i < n;++i) 
		for(int j = 0;j <= 2;++j)
		dp[i][j] = 0;
		for(int i = 0;i < n;++i,cur = (cur+1)%3){
			if(s[i]!=a[cur]) dp[i][0] = 1;
			if(s[i]!=b[cur]) dp[i][1] = 1;
			if(s[i]!=c[cur]) dp[i][2] = 1;
		}
		for(int i = 1;i < n;++i) 
		for(int j = 0;j <= 2;++j)
		dp[i][j] += dp[i-1][j];
		ans = inf;
		for(int i = 0;i+k-1 < n;++i){
			for(int j = 0;j <= 2;++j){
				ans = min(ans,dp[i+k-1][j]-dp[i-1][j]);
			}
		}
		cout << ans << endl;
	}
	return 0;
}