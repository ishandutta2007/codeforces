void solve() {
	int x1, y1, z1;
	cin >> x1 >> y1 >> z1;
	int x2, y2, z2;
	cin >> x2 >> y2 >> z2;
	cout << 2 * (min(z1, y2) - min(y1, max(z2 - x1 - (z1 - min(z1, y2)), 1ll * 0))) << '\n';
}

