#include <bits/stdc++.h>

using namespace std;

const int MAXN = 100010;

int n, m, q;
int A[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=0; i<n; i++) cin>>A[i];
	sort(A, A+n);
	cin>>m;
	while (m--){
		cin>>q;
		cout<<upper_bound(A, A+n, q)-A<<'\n';
	}
	return 0;
}
