void test_case(int test) {
    int n;
    cin >> n;
    ll h;
    cin >> h;
    vector<ll> a(n);
    cin >> a;
    ll L = 0, R = h + 1;
    while (R - L > 1) {
        ll C = (L + R) / 2;
        ll total = 0;
        ll last = -1e18;
        if (a[0] + C >= a.back()) {
            total = a.back() + C - a[0];
        } else {
            for (ll l : a) {
                ll r = l + C - 1;
                l = max(l, last + 1);
                total += r - l + 1;
                if (total >= h) break;
                last = r;
            }
        }
        if (total >= h)
            R = C;
        else
            L = C;
    }
    cout << R << '\n';
}

