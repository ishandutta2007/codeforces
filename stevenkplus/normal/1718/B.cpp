int main() {
   int T;
   cin >> T;
   while (T--) {
      int n;
      cin >> n;
      vector<int> v(n);
      for (int &i: v) cin >> i;
      ll sum = 0;
      for (int i: v) sum += i;
      vector<ll> fibs = {0, 1};
      sum -= 1;
      while (sum > 0) {
         fibs.push_back(fibs.end()[-1] + fibs.end()[-2]);
         sum -= fibs.back();
      }
      if (sum != 0) {
         cout << "NO\n";
         continue;
      }

      reverse(fibs.begin(), fibs.end());
      fibs.pop_back();
      int prv = -1;
      bool ok = true;
      for (ll f: fibs) {
         int cur = -1;
         for (int i = 0; i < n; ++i) {
            if (i == prv) continue;
            if (v[i] >= f) {
               if (cur == -1 || v[i] > v[cur]) {
                  cur = i;
               }
            }
         }
         if (cur == -1) {
            cout << "NO\n";
            ok = false;
            break;
         }
         v[cur] -= f;
         prv = cur;
      }
      if (!ok) continue;
      cout << "YES\n";
   }
}

