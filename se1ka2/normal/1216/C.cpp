#include <iostream>
using namespace std;

int main()
{
    int x[6], y[6];
    for(int i = 0; i < 6; i++) cin >> x[i] >> y[i];
    if((x[2] <= x[0] && x[3] >= x[1] && y[2] <= y[0] && y[3] >= y[1]) ||
       (x[4] <= x[0] && x[5] >= x[1] && y[4] <= y[0] && y[5] >= y[1])){
       cout << "NO" << endl;
       return 0;
    }
    if((x[2] <= x[0] && x[3] >= x[4] && x[5] >= x[1] && y[2] <= y[0] && y[3] >= y[1] && y[4] <= y[0] && y[5] >= y[1]) ||
       (x[4] <= x[0] && x[5] >= x[2] && x[3] >= x[1] && y[4] <= y[0] && y[5] >= y[1] && y[2] <= y[0] && y[3] >= y[1]) ||
       (y[2] <= y[0] && y[3] >= y[4] && y[5] >= y[1] && x[2] <= x[0] && x[3] >= x[1] && x[4] <= x[0] && x[5] >= x[1]) ||
       (y[4] <= y[0] && y[5] >= y[2] && y[3] >= y[1] && x[4] <= x[0] && x[5] >= x[1] && x[2] <= x[0] && x[3] >= x[1])){
        cout << "NO" << endl;
        return 0;
    }
    cout << "YES" << endl;
}