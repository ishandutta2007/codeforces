bitset<mn> b[2];

void easy(int n) {
  int cur=0,nxt=1;
  b[cur][0]=true;
  for (int d=0;d<n;d++) {
    b[nxt].reset();
    for (int x=0;x<4;x++) {
      b[nxt]|=b[cur]<<tab[x];
    }
    swap(cur,nxt);
  }
  int ans=b[cur].count();
  printf("%d\n",ans);
  exit(0);
}

