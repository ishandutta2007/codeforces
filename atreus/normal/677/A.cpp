#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    int n, l, cnt = 0;
    cin >> n >> l;
    int a[n + 10];
    for (int i = 0; i < n; i++){
        cin >> a[i];
        if (a[i] > l)
            cnt += 2;
        else
            cnt ++;
    }
    cout << cnt;
}