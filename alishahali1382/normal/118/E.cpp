int h[MAXN];
vector<int> G[MAXN];
vector<pii> out;

int dfs(int node, int par){
	if (par) out.pb({par, node});
	int res=h[node]=h[par]+1;
	for (int v:G[node]){
		if (v==par) continue ;
		if (h[v]){
			res=min(res, h[v]);
			if (h[v]<h[node]) out.pb({node, v});
		}
		else res=min(res, dfs(v, node));
		
	}
	if (par && res>=h[node]){
		cout<<"0\n";
		exit(0);
	}
	return res;
}

