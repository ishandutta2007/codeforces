	void solve()
	{
		int n = ni();
		char[] s = ns(n);

		int[] lens = new int[n];
		int[] fs = new int[n];
		int sp = 0;
		int rep = 0;
		long S = 0;
		long ans = 0;
		for(char c : s){
			if(c == '0'){
				while(rep > 0){
					lens[sp] = rep;
					fs[sp] = 1;
					sp++;
					S += rep;
					rep--;
				}
				assert rep == 0;

				ans += S;
				if(sp-1 >= 0 && lens[sp-1] == 0){
					fs[sp-1]++;
				}else{
					lens[sp] = 0;
					fs[sp] = 1;
					sp++;
				}
			}else{
				rep++;
				int f = 0;
				while(sp-1 >= 0 && lens[sp-1] <= rep){
					f += fs[sp-1];
					S -= (long)fs[sp-1] * lens[sp-1];
					sp--;
				}
				lens[sp] = rep;
				fs[sp] = f;
				sp++;
				S += (long)fs[sp-1] * lens[sp-1];
				ans += S + (long)rep*(rep+1)/2;
			}
		}
		out.println(ans);
	}

