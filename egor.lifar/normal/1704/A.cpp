int n, m;
string a, b;
bool can[100][2];


void solve() {
	cin >> n >> m;
	cin >> a >> b;
	memset(can, 0, sizeof(can));
	can[0][a[0] - '0'] = true;
	for (int i = 1; i < sz(a); i++) {
		for (int t = 0; t < 2; t++) {
			if (can[i - 1][t]) {
				can[i][max(t, a[i] - '0')] = true;
				can[i][min(t, a[i] - '0')] = true;
			}
		}
	}
	if (!can[n - m][b[0] - '0']) {
		cout << "NO\n";
		return;
	}
	for (int i = n - m + 1; i < n; i++) {
		if (a[i] != b[m - (n - i)]) {
			cout << "NO\n";
			return;
		}
	}
	cout << "YES\n";
}


int main(){
	cin.tie(0)->sync_with_stdio(0);
	int t;
	t = 1;
	cin >> t;
	while (t--) {
		solve();
	}
}