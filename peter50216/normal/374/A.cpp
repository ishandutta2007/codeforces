int a,b;
int n,m;
const int INF=1000000000;
inline int solve(int i,int j){
  if(i==0&&j==0)return 0;
  if(i%a!=0)return INF;
  if(j%b!=0)return INF;
  int ii=i/a,jj=j/b;
  if(ii%2!=jj%2)return INF;
  if(a>=n||b>=m)return INF;
  return max(ii,jj);
}
int main(){
  RII(n,m);
  DRII(i,j);
  RII(a,b);
  i--;j--;
  int r=min(min(solve(i,j),solve(n-1-i,j)),min(solve(i,m-1-j),solve(n-1-i,m-1-j)));
  if(r==INF)puts("Poor Inna and pony!");
  else printf("%d\n",r);
}
