void solve(){
    int c1, c2, c3;
    int a1, a2, a3, a4, a5;
    cin >> c1 >> c2 >> c3 >> a1 >> a2 >> a3 >> a4 >> a5;
    if(a1 > c1 || a2 > c2){
        cout << "NO" << endl;
        return;
    }
    c1 -= a1;
    c2 -= a2;
    a4 -= c1;
    a5 -= c2;
    a4 = max(0, a4);
    a5 = max(0, a5);
    if(a3 + a4 + a5 > c3){
        cout << "NO" << endl;
        return;
    }
    cout << "YES" << endl;
}

