int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, m, k;
	cin >> n >> m >> k;
	vl w(n);
	cin >> w;
	vector<vl> adj(n, vl(n));
	while(k --){
		int x, y, c;
		cin >> x >> y >> c, x --, y --;
		adj[x][y] = c;
	}
	vector<vl> d(1 << n, vl(n));
	for(int i = 0; i < n; ++ i){
		d[1 << i][i] = w[i];
	}
	for(int i = 1; i < 1 << n; ++ i){
		if(__builtin_popcount(i) < m){
			for(int j = 0; j < n; ++ j){
				if(i & 1 << j){
					for(int k = 0; k < n; ++ k){
						if(!(i & 1 << k)){
							d[i ^ 1 << k][k] = max(d[i ^ 1 << k][k], d[i][j] + adj[j][k] + w[k]);
						}
					}
				}
			}
		}
	}
	ll res = 0;
	for(auto &x: d){
		for(auto y: x){
			res = max(res, y);
		}
	}
	cout << res;
	return 0;
}

