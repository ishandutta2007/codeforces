#include<bits/stdc++.h>
#define ll long long
using namespace std;
const ll inf=1LL<<60;

ll read()
{
	ll s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	ll f=read(),T=read(),t0=read(),ans=inf;
	ll a1=read(),t1=read(),p1=read();
	ll a2=read(),t2=read(),p2=read();
	for (ll i=0;i*t1<=T;i++)
	{
		ll j=max((f*t0-i*t0-T+i*t1+max(a2*t0-a2*t2,1LL)-1)/max(a2*t0-a2*t2,1LL),0LL);
		if (t0<=t2) j=0;
		if (i*t1+j*a2*t2<=T&&i+j*a2+(T-i*t1-j*a2*t2)/t0>=f)
		ans=min(ans,(i+a1-1)/a1*p1+j*p2);
	}
	swap(a1,a2),swap(t1,t2),swap(p1,p2);
	for (ll i=0;i*t1<=T;i++)
	{
		ll j=max((f*t0-i*t0-T+i*t1+max(a2*t0-a2*t2,1LL)-1)/max(a2*t0-a2*t2,1LL),0LL);
		j=max(j,0LL);
		if (t0<=t2) j=0;
		if (i*t1+j*a2*t2<=T&&i+j*a2+(T-i*t1-j*a2*t2)/t0>=f)
		ans=min(ans,(i+a1-1)/a1*p1+j*p2);
	}
	if (ans==inf) puts("-1");
			 else print(ans);
	
	return 0;
}
