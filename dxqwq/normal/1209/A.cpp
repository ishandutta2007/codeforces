#include<bits/stdc++.h>
using namespace std;
int a[103];
bool f[103];
int main()
{
	int n;
	scanf("%d",&n);
	for(int i=1; i<=n; i++) scanf("%d",&a[i]);
	sort(a+1,a+n+1);
	for(int i=n; i>1; i--) for(int j=i-1; j>0; j--) if(a[i]%a[j]==0){
		f[i]=1;
		break;
	}
	f[1]=0;
	int ans=n;
	for(int i=1; i<=n; i++) ans-=f[i];
	printf("%d",ans);
	return 0;
}