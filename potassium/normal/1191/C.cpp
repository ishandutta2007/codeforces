vector<long long> v;

int main() {
	long long n,m,k;
	cin >> n >> m >> k;
	
	v.resize(m,0);
	
	for (int i = 0; i < m; i++)
		cin >> v[i];
		
	long long decre = 0;
	vector<long long>::iterator it = v.begin();	
	int ans = 0;
		
	while (it < v.end()) {
		long long leftest = (*it-decre)-(*it-decre-1)%k;
		while (it != v.end() && *it <= leftest+k+decre-1) it++;
		decre = it-v.begin();
		ans++;
	}
	
	cout << ans;
	return 0;
}
 
