namespace atcoder {

// Reference: https://en.wikipedia.org/wiki/Fenwick_tree
template <class T> struct fenwick_tree {
    using U = internal::to_unsigned_t<T>;

  public:
    fenwick_tree() : _n(0) {}
    fenwick_tree(int n) : _n(n), data(n) {}

    void add(int p, T x) {
        assert(0 <= p && p < _n);
        p++;
        while (p <= _n) {
            data[p - 1] += U(x);
            p += p & -p;
        }
    }

    T sum(int l, int r) {
        assert(0 <= l && l <= r && r <= _n);
        return sum(r) - sum(l);
    }

  private:
    int _n;
    std::vector<U> data;

    U sum(int r) {
        U s = 0;
        while (r > 0) {
            s += data[r - 1];
            r -= r & -r;
        }
        return s;
    }
};

}  // namespace atcoder

using namespace std;
using namespace atcoder;

void solve(){
    int n;
    cin >> n;
    vector<int> P(n);
    for(int i = 0; i < n; i++){
        cin >> P[i];
        P[i]--;
    }
    fenwick_tree<long long> L(n);
    L.add(P[0], 1);
    long long ans = 0;
    for(int b = 1; b < n; b++){
        fenwick_tree<long long> R(n);
        R.add(P[n - 1], 1);
        for(int c = n - 2; c > b; c--){
            ans += R.sum(0, P[b]) * L.sum(0, P[c]);
            R.add(P[c], 1);
        }
        L.add(P[b], 1);
    }
    cout << ans << "\n";

}

int main(){
	int t;
    cin >> t;
    while(t--) solve();
}