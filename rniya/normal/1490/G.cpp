const int INF = 1e9;
const long long IIINF = 3e14;
const long long IINF = 1e17;
const int dx[4] = {1, 0, -1, 0}, dy[4] = {0, 1, 0, -1};
const char dir[4] = {'D', 'R', 'U', 'L'};
const long long MOD = 1000000007;
// const long long MOD = 998244353;

const int MAX_LOG = 48, MAX_N = 200010;
tuple<ll, ll, int> D[MAX_LOG + 1][MAX_N];

void solve() {
    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    cin >> a;

    ll cycle = 0, Max = 0;
    for (int i = 0; i < n; i++) {
        cycle += a[i];
        chmax(Max, cycle);
    }

    auto merge = [](const tuple<ll, ll, int> a, const tuple<ll, ll, int> b) {
        int nxt, nnxt;
        ll cur, maxi, ncur, nmaxi;
        tie(cur, maxi, nxt) = a;
        tie(ncur, nmaxi, nnxt) = b;
        return make_tuple(max(-IINF, min(IINF, cur + ncur)), max(-IINF, min(IINF, max(maxi, cur + nmaxi))), nnxt);
    };
    for (int i = 0; i < n; i++) D[0][i] = make_tuple(a[i], max(0, a[i]), (i + 1) % n);
    for (int i = 0; i < MAX_LOG; i++) {
        for (int j = 0; j < n; j++) {
            int nxt = get<2>(D[i][j]);
            D[i + 1][j] = merge(D[i][j], D[i][nxt]);
        }
    }

    auto query = [&](int x) -> ll {
        if (cycle <= 0 && Max < x) return -1;

        ll cur = 0, res = 0;
        int nxt = 0;
        for (int i = MAX_LOG; i >= 0; i--) {
            if (x > cur + get<1>(D[i][nxt])) {
                cur += get<0>(D[i][nxt]);
                nxt = get<2>(D[i][nxt]);
                res += 1LL << i;
            }
        }
        return res;
    };
    for (; m--;) {
        int x;
        cin >> x;
        cout << query(x) << (!m ? '\n' : ' ');
    }
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t;
    cin >> t;
    for (; t--;) solve();
    return 0;
}