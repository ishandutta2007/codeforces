void solve() {
	int n;
	string s;
	cin >> n >> s;
	int mx = n;
	vector<int> ans1(n, -1);

	for(int i = 0; i < n - 1; ++i) {
		int j = i;
		while(j < n - 1 && s[j] == '>')
			++j;
		for(int k = i; k < j; ++k) {
			if (ans1[k] == -1)
				ans1[k] = mx--;
		}
		i = max(j - 1, i);
	}
	int mn = 1;
	for(int i = n - 2; i >= 0; --i) {
		int j = i;
		while(j > -1 && s[j] == '<')
			--j;
		// cout << j + 1 << ':' << i << ':' << mx << '\n';
		for(int k = j + 1; k <= i + 1; ++k) {
			if (ans1[k] == -1)
				ans1[k] = mn++;
		}
		i = min(i, j + 1);
	}
	for(int i = 0; i < n; ++i)
		cout << ans1[i] << ' ';
	cout << '\n';

	mx = n;
	ans1 = vector<int> (n, -1);
	for(int i = n - 1; i >= 1; --i) {
		if (s[i - 1] == '<') {
			if (ans1[i] == -1)
				ans1[i] = mx--;
		}
	}
	for(int i = 0; i < n; ++i) {
		if (ans1[i] == -1)
			ans1[i] = mx--;
	}
	for(int i = 0; i < n; ++i)
		cout << ans1[i] << ' ';
	cout << '\n';

}

