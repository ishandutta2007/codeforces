#include<bits/stdc++.h>
#define mk make_pair
#define fi first
#define se second
using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
vector<int>ans;
bool vis[N];
int a[N],q[N],h=1,t=0,siz[N];
vector<pair<int,int>>e[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=read();
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=1;i<=m;i++)
	{
		int u=read(),v=read();
		e[u].push_back(mk(v,i));
		e[v].push_back(mk(u,i));
		siz[u]++,siz[v]++;
	}
	for (int i=1;i<=n;i++) if (siz[i]<=a[i]) q[++t]=i;
	while (h<=t)
	{
		int now=q[h++];
		for (auto i:e[now]) if (!vis[i.se])
		{
			ans.push_back(i.se);
			vis[i.se]=1;
			siz[now]--;
			if (--siz[i.fi]==a[i.fi]) q[++t]=i.fi;
		}
	}
	if (t<n) puts("DEAD");
	else
	{
		puts("ALIVE");
		reverse(ans.begin(),ans.end());
		for (int i:ans) print(i,' ');
	}
	
	return 0;
}
