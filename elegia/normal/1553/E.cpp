template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int T; cin >> T;
  while (T--) {
    int n, m; cin >> n >> m;
    vector<int> a(n); cin >> a;
    vector<int> cnt(n);
    for (int i = 0; i != n; ++i) {
      --a[i];
      ++cnt[(a[i] - i + n) % n];
    }
    vector<int> ans;
    for (int i = 0; i != n; ++i) {
      if (cnt[i] + (n - cnt[i] + 1) / 2 < n - m) continue;
      vector<int> f(n);
      for (int j = 0; j != n; ++j) f[j] = (a[j] + n - i) % n;
      int cyc = 0;
      vector<int> vis(n);
      for (int j = 0; j != n; ++j) {
        if (vis[j]) continue;
        int x = j;
        ++cyc;
        while (!vis[x]) {
          vis[x] = true;
          x = f[x];
        }
      }
      if (n - cyc <= m) {
        ans.push_back((n - i) % n);
      }
    }
    sort(ans.begin(), ans.end());
    cout << ans.size() << ' ' << ans << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}