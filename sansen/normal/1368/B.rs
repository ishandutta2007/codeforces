fn run() {
    input! {
        k: u64,
    }
    let c: Vec<_> = "codeforces".chars().collect();
    let mut ans = vec![1u64; c.len()];
    while ans.iter().fold(1u64, |s, a| s * a) < k {
        let p = ans[0];
        if let Some(x) = ans.iter().position(|v| *v < p) {
            ans[x] += 1;
        } else {
            ans[0] += 1;
        }
    }
    for (c, &a) in c.iter().zip(ans.iter()) {
        for _ in 0..a {
            print!("{}", *c);
        }
    }
    println!();
}

fn main() {
    run();
}