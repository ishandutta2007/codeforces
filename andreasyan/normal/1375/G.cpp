const int N = 200005;

int n;
vector<int> g[N];

int q[2];
void dfs(int x, int p, int c)
{
    q[c]++;
    for (int i = 0; i < g[x].size(); ++i)
    {
        int h = g[x][i];
        if (h == p)
            continue;
        dfs(h, x, 1 - c);
    }
}

void solv()
{
    scanf("%d", &n);
    for (int i = 0; i < n - 1; ++i)
    {
        int x, y;
        scanf("%d%d", &x, &y);
        g[x].push_back(y);
        g[y].push_back(x);
    }
    dfs(1, 1, 0);
    printf("%d\n", min(q[0], q[1]) - 1);
}

