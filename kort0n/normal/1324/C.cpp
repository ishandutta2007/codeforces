void solve() {
    string S;
    cin >> S;
    int Last = -1;
    int ans = 0;
    for(int i = 0; i < S.size(); i++) {
        if(S[i] == 'L') chmax(ans, i - Last);
        else Last = i;
    }
    cout << ans + 1 << endl;
}

