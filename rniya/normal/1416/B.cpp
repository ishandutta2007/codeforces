void solve(){
    int n; cin >> n;
    vector<int> a(n); cin >> a;

    ll sum=accumulate(ALL(a),0LL);
    if (sum%n!=0){cout << -1 << '\n'; return;}

    ll each=sum/n;
    vector<int> I,J,X;
    auto put=[&](int i,int j,int x){
        I.emplace_back(i+1);
        J.emplace_back(j+1);
        X.emplace_back(x);
    };
    for (int i=1;i<n;++i){
        int nxt=((i+1)-(a[i]%(i+1)))%(i+1);
        put(0,i,nxt);
        a[0]-=nxt; a[i]+=nxt;
        put(i,0,a[i]/(i+1));
        a[0]+=a[i]; a[i]=0;
    }
    for (int i=1;i<n;++i){
        put(0,i,each);
        a[0]-=each;
        a[i]+=each;
    }

    cout << I.size() << '\n';
    for (int i=0;i<I.size();++i) cout << I[i] << ' ' << J[i] << ' ' << X[i] << '\n';
}

