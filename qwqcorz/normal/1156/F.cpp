#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=5e3+5;
const int p=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int power(int a,int b)
{
	int ret=1;
	while (b)
	{
		if (b&1) ret=ret*a%p;
		a=a*a%p;
		b/=2;
	}
	return ret;
}
int a[N],f[N][N],g[N][N],inv[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),ans=0;
	for (int i=1;i<=n;i++) a[read()]++;
	for (int i=1;i<=n;i++) inv[i]=power(i,p-2);
	f[0][0]=g[0][0]=1;
	for (int i=1;i<=n;i++)
	{
		for (int j=1;j<=n;j++) f[i][j]=g[i-1][j-1]*inv[n-j+1]%p*a[i]%p;
		for (int j=0;j<=n;j++) g[i][j]=(g[i-1][j]+f[i][j])%p;
		if (a[i]>1)
		for (int j=0;j<=n;j++) ans=(ans+f[i][j]*inv[n-j]%p*(a[i]-1)%p)%p;
	}
	print(ans);
	
	return 0;
}
