vi rows(3e5, 0);
vi cols(3e5, 0);

int solve()
{
    int q;
    cin >> n >> m >> k >> q;

    vpii data(q);

    for (int i = 0; i < q; ++i)
    {
        cin >> data[i].ft >> data[i].sd;
        data[i].ft--;
        data[i].sd--;
    }


    int rown = 0;
    int colm = 0;

    ans = 0;

    for (int i = q-1; i >= 0; --i)
    {
        if (rown == n || colm == m)
            break;

        if (rows[data[i].ft] && cols[data[i].sd])
            continue;

        ++ans;

        //log(i);

        if (!rows[data[i].ft])
        {
            rows[data[i].ft] = 1;
            rown++;
        }

        if (!cols[data[i].sd])
        {
            cols[data[i].sd] = 1;
            colm++;
        }
    }

    for (int i = q-1; i >= 0; --i)
    {
        rows[data[i].ft] = 0;

        cols[data[i].sd] = 0;
    }

    mint as(k);
    as = as^(ans);




    cout << as.x << endl;

    return 0;
}


