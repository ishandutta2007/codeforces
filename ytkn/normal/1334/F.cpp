ll op(ll x, ll y){
    return x+y;
}

ll e(){
    return 0;
}

using Seg = atcoder::segtree<ll, op, e>;
const ll inf = 1e18;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n; cin >> n;
    vector<vector<int>> indices(n+1);
    indices[0].push_back(0);
    for(int i = 1; i <= n; i++){
        int a; cin >> a;
        indices[a].push_back(i);
    }
    vector<ll> p(n+1);
    for(int i = 1; i <= n; i++) cin >> p[i];
    Seg seg(p);
    int m; cin >> m;
    vector<int> b(m+1);
    for(int i = 1; i <= m; i++) cin >> b[i];
    vector<ll> dp(n+1, inf);
    dp[0] = 0;
    vector<int> pre = {0};
    for(int i = 1; i <= m; i++){
        if(pre.empty()){
            cout << "NO" << endl;
            return 0;
        }
        auto cur = indices[b[i]];
        int j = 0;
        ll mn = inf;
        for(int cur_idx: cur){
            if(cur_idx < pre[0]) continue;
            while(j < pre.size() && pre[j] < cur_idx){
                if(j > 0){
                    mn = min(mn+seg.prod(pre[j-1], pre[j]), dp[pre[j-1]]+seg.prod(pre[j-1]+1, pre[j]));
                }
                j++;
            }
            chmin(dp[cur_idx], mn+seg.prod(pre[j-1], cur_idx));
            chmin(dp[cur_idx], dp[pre[j-1]]+seg.prod(pre[j-1]+1, cur_idx));
        }

        for(int x = b[i-1]; x <= b[i]; x++){
            for(int idx: indices[x]){
                if(seg.get(idx) > 0) seg.set(idx, 0);
            }
        }
        pre = cur;
    }
    ll ans = inf;
    for(int i: indices[b.back()]){
        if(dp[i] != inf) chmin(ans, dp[i]+seg.prod(i+1, n+1));
    }
    // print_vector(dp);
    if(ans >= inf/2) cout << "NO" << endl;
    else{
        cout << "YES" << endl;
        cout << ans << endl;
    }
}