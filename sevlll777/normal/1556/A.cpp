void solve() {
  int x, y;
  cin >> x >> y;
  if (x % 2 != y % 2) {
    cout << "-1\n";
  } else {
    if (x == y || x == -y) {
      if (x == 0) {
        cout << "0\n";
      } else {
        cout << "1\n";
      }
    } else {
      cout << "2\n";
    }
  }
}

