vector<ll> D;

ll f(ll x, ll d){
	return (d-(x%d))%d;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>a>>b;
	if (a>b) swap(a, b);
	ll ans=1e18, out=0;
	ll x=b-a;
	for (ll d=1; d*d<=x; d++) if (x%d==0){
		D.pb(d);
		D.pb(x/d);
	}
	sort(all(D));
	for (ll d:D){
		ll k=(d-(b%d))%d;
		ll aa=a+k;
		ll bb=b+k;
		ll lcm=(aa*bb)/d;
		//if (ans==lcm) out=min(out, k);
		if (lcm<ans){
			ans=lcm;
			out=k;
		}
	}
	cout<<out<<'\n';
	
	return 0;
}
