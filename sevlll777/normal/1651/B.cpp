void solve() {
  int n;
  cin >> n;
  vector<int> ans = {1};
  while ((int) ans.size() < n && ans.back() * 3 <= (int) 1e9) {
    ans.pb(ans.back() * 3);
  }
  if ((int) ans.size() < n) {
    cout << "NO";
  } else {
    cout << "YES\n";
    for (auto x : ans) {
      cout << x << ' ';
    }
  }
  cout << '\n';
}

