typedef long long base; 
typedef pair<long long, long long> point;
typedef complex<double> comp;

const int N = 1e6 + 5;

const long long INF = 2.1e16;

point s, cur, a, b;
long long t;

vector<point> res;

long long dist(point a, point b) {
    return abs(a.x - b.x) + abs(a.y - b.y);
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> cur >> a >> b;
    cin >> s >> t;
    while (cur.x < INF && cur.y < INF) {
        res.pb(cur);
        cur.x = cur.x * a.x + b.x;
        cur.y = cur.y * a.y + b.y;
    }
    int ans = 0;
    for (int L = 0; L < res.size(); ++L) {
        for (int R = L; R < res.size(); ++R) {
            if (dist(s, res[R]) + dist(res[L], res[R]) <= t) {
                chkmax(ans, R - L + 1);
            }
            if (dist(s, res[L]) + dist(res[L], res[R]) <= t) {
                chkmax(ans, R - L + 1);
            }
        }
    }
    cout << ans << '\n';
}