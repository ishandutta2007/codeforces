fn main() {
	let mut rin = reader();
	let mut rout = writer();

	let n = rin.u();
	l!(a,r,m = rin.l());
	let h = rin.vl(n);
	let mut sol = std::i64::MAX;
	let f = |x: i64| -> i64 {
		l!(mut aa,rr = 0);
		for i in 0..n {
			if h[i] < x {
				aa += x - h[i];
			} else {
				rr += h[i] - x;
			}
		}
		let mm = aa.min(rr);
		aa -= mm;
		rr -= mm;
		mm * m.min(a+r) + aa * a + rr * r
	};
	let mut lo = *h.iter().min().unwrap();
	let mut hi = *h.iter().max().unwrap();
	while lo + 10 <= hi {
		let m1 = (lo+lo+hi)/3;
		let m2 = (lo+hi+hi)/3;
		let r1 = f(m1);
		let r2 = f(m2);
		if r1 <= r2 {
			min!(sol, r1);
			hi = m2;
		} else {
			min!(sol, r2);
			lo = m1;
		}
	}
	for i in lo..=hi {
		min!(sol, f(i));
	}
	writeln!(rout, "{}", sol).ok();
}