void solve(){
    int n;
    cin >> n;
    // num, idx
    vpii v(n);
    for(int i = 0; i < n; i++){
        int x;
        cin >> x;
        v[i] = make_pair(x, i);
    }
    sort(v.begin(), v.end());
    vll prefix(n);
    prefix[0] = v[0].first;
    for(int i = 1; i < n; i++){
        prefix[i] = prefix[i-1] + v[i].first;
    }
    debug(prefix, v);
    int threshold = 0;
    for(int i = n-2; i >= 0; i--){
        if(prefix[i] < v[i+1].first){
            threshold = i+1;
            break;
        }
    }
    vi ans;
    for(int i = threshold; i < n; i++){
        ans.push_back(v[i].second);
    }
    sort(ans.begin(), ans.end());
    cout << ans.size() << endl;
    for(int i : ans){
        cout << i+1 << " ";
    }
    cout << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}