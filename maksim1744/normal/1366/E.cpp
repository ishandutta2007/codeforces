const ll mod = 998244353;

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n, m;
    cin >> n >> m;
    vector<int> a(n), b(m);
    cin >> a >> b;
    auto pa = a;
    for (int i = n - 2; i >= 0; --i)
        pa[i] = min(pa[i], pa[i + 1]);
    vector<int> need(m);
    show(pa);
    for (int i = 0; i < m; ++i) {
        int ind = lowb(pa, b[i] + 1) - 1;
        if (ind < 0) {
            cout << 0 << endl;
            return 0;
        }
        if (pa[ind] != b[i]) {
            cout << 0 << endl;
            return 0;
        }
        need[i] = ind;
    }
    if (pa[0] != b[0]) {
        cout << 0 << endl;
        return 0;
    }
    show(need);
    ll ans = 1;
    for (int i = 1; i < m; ++i) {
        int ind = need[i];
        while (ind >= 0 && pa[ind] == b[i]) --ind;
        show(i, ind);
        ans = ans * (need[i] - ind) % mod;
    }
    cout << ans << endl;

    return 0;
}