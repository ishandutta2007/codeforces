template< typename F >
struct FixPoint : F {
  FixPoint(F &&f) : F(forward< F >(f)) {}

  template< typename... Args >
  decltype(auto) operator()(Args &&... args) const {
    return F::operator()(*this, forward< Args >(args)...);
  }
};

template< typename F >
inline decltype(auto) MFP(F &&f) {
  return FixPoint< F >{forward< F >(f)};
}


template< typename Monoid, typename OperatorMonoid = Monoid >
struct LazySegmentTree {
  using F = function< Monoid(Monoid, Monoid) >;
  using G = function< Monoid(Monoid, OperatorMonoid, int) >;
  using H = function< OperatorMonoid(OperatorMonoid, OperatorMonoid) >;

  int sz;
  vector< Monoid > data;
  vector< OperatorMonoid > lazy;
  const F f;
  const G g;
  const H h;
  const Monoid M1;
  const OperatorMonoid OM0;


  LazySegmentTree(int n, const F f, const G g, const H h,
                  const Monoid &M1, const OperatorMonoid OM0)
      : f(f), g(g), h(h), M1(M1), OM0(OM0) {
    sz = 1;
    while(sz < n) sz <<= 1;
    data.assign(2 * sz, M1);
    lazy.assign(2 * sz, OM0);
  }

  void set(int k, const Monoid &x) {
    data[k + sz] = x;
  }

  void build() {
    for(int k = sz - 1; k > 0; k--) {
      data[k] = f(data[2 * k + 0], data[2 * k + 1]);
    }
  }

  void propagate(int k, int len) {
    if(lazy[k] != OM0) {
      if(k < sz) {
        lazy[2 * k + 0] = h(lazy[2 * k + 0], lazy[k]);
        lazy[2 * k + 1] = h(lazy[2 * k + 1], lazy[k]);
      }
      data[k] = g(data[k], lazy[k], len);
      lazy[k] = OM0;
    }
  }

  Monoid update(int a, int b, const OperatorMonoid &x, int k, int l, int r) {
    propagate(k, r - l);
    if(r <= a || b <= l) {
      return data[k];
    } else if(a <= l && r <= b) {
      lazy[k] = h(lazy[k], x);
      propagate(k, r - l);
      return data[k];
    } else {
      return data[k] = f(update(a, b, x, 2 * k + 0, l, (l + r) >> 1),
                         update(a, b, x, 2 * k + 1, (l + r) >> 1, r));
    }
  }

  Monoid update(int a, int b, const OperatorMonoid &x) {
    return update(a, b, x, 1, 0, sz);
  }


  Monoid query(int a, int b, int k, int l, int r) {
    propagate(k, r - l);
    if(r <= a || b <= l) {
      return M1;
    } else if(a <= l && r <= b) {
      return data[k];
    } else {
      return f(query(a, b, 2 * k + 0, l, (l + r) >> 1),
               query(a, b, 2 * k + 1, (l + r) >> 1, r));
    }
  }

  Monoid query(int a, int b) {
    return query(a, b, 1, 0, sz);
  }

  Monoid operator[](const int &k) {
    return query(k, k + 1);
  }
};


int main() {
  int N;
  cin >> N;
  vector< int > A(N);
  cin >> A;
  int Q;
  cin >> Q;

  auto f = [](int64 a, int64 b) { return a+b; };
  using pi = pair< int64, int >;
  auto g = [](int64 a, pi b, int p) {
    if(b.second == 1) return max(a, b.first); // chmax
    return b.first; // set
  };
  auto h = [](pi a, pi b) {
    if(a.second == 0) {
      return b;
    } else if(a.second == 1) { // chmax
      if(b.second == 1) { // chmax
        return pi(max(a.first, b.first), 1);
      } else { // set
        return b;
      }
    } else { // set
      if(b.second == 1) { // chmax
        return pi(max(a.first, b.first), 2);
      } else { // set
        return b;
      }
    }
  };
  LazySegmentTree< int64, pi > seg(N, f, g, h, 0, pi(0, 0));
  for(int i = 0; i < N; i++) { seg.set(i, A[i]);}
  seg.build();

  while(Q--) {
    int T;
    cin >> T;
    if(T == 1) {
      int P, X;
      cin >> P >> X;
      --P;
      seg.update(P, P + 1, pi(X, 2));
    } else {
      int X;
      cin >> X;
     seg.update(0, N, pi(X, 1));
    }
  }
  for(int i = 0; i < N; i++) cout << seg[i] << " ";
  cout << endl;
}