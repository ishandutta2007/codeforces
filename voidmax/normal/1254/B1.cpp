const int N = 1e6 + 5;

int n;
vector<int> pos;
vector<int> npos;
vector<long long> pref;

long long get_sum(int L, int R) {
	return pref[R] - pref[L];
}

long long get_val(int L, int R) {
	int M = (L + R) >> 1;
	long long coor = npos[M];
	return coor * ((M - L) - (R - M)) - get_sum(L, M) + get_sum(M, R);
}

