vector<int> n(3);
vector<vector<int> > a(3);
ll sol=LLONG_MAX;
void mini(int a,int b,int c){
	sol=min(sol,(ll)(a-b)*(a-b)+(ll)(a-c)*(a-c)+(ll)(b-c)*(b-c));
}
void solve(vector<int> ind){
	int l=0,r=0;
	while(l<n[ind[0]]&&r<n[ind[1]]){
		if(a[ind[1]][r]<a[ind[0]][l]){
			r++;
			continue;
		}
		auto it=lower_bound(all(a[ind[2]]),a[ind[1]][r]);
		if(it!=a[ind[2]].end()){
			int v=*it;
			mini(a[ind[0]][l],a[ind[1]][r],v);
		}
		if(l==n[ind[0]]-1||a[ind[0]][l+1]>a[ind[1]][r])
			r++;
		else
			l++;
	}
}
void test(){
	scanf("%i %i %i",&n[0],&n[1],&n[2]);
	for(int i=0;i<3;i++){
		a[i].resize(n[i]);
		for(int j=0;j<n[i];j++)
			scanf("%i",&a[i][j]);
		sort(all(a[i]));
	}
	sol=LLONG_MAX;
	vector<int> p={0,1,2};
	do{
		solve(p);
	}while(next_permutation(all(p)));
	printf("%lld\n",sol);
}
