int l;
vector<int> tin, tout;
int timer;
vector < vector<int> > up;
vvpi g;

void dfs1 (int v, int p = 0) {
    tin[v] = ++timer;
    up[v][0] = p;
    for (int i=1; i<=l; ++i)
        up[v][i] = up[up[v][i-1]][i-1];
    for (size_t i=0; i<g[v].size(); ++i) {
        int to = g[v][i].first;
        if (to != p)
            dfs1 (to, v);
    }
    tout[v] = ++timer;
}

bool upper (int a, int b) {
    return tin[a] <= tin[b] && tout[a] >= tout[b];
}

int lca (int a, int b) {
    if (upper (a, b))  return a;
    if (upper (b, a))  return b;
    for (int i=l; i>=0; --i)
        if (! upper (up[a][i], b))
            a = up[a][i];
    return up[a][0];
}
vi ans;
vi add;
int dfs(int v, int p) {
    int ret = add[v];
    for(auto u : g[v]) {
        if(u.first == p) continue;
        int cur = dfs(u.first, v);
        ans[u.second] = cur;
        ret += cur;
    }
    return ret;
}


