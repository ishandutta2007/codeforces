#define lc(x) ((x) << 1)
#define rc(x) ((x) << 1 | 1)
#define maxn 100005
using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(!isdigit(ch)){w |= ch == '-';ch = getchar();}
	while(isdigit(ch)){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int n, cnt[maxn];
vector<int> G[maxn];
void dfs(int x, int dep)
{
	cnt[dep]++;
	for (auto V: G[x]) dfs(V, dep + 1);
}
int main()
{
	n = read();
	for (int i = 2; i <= n; i++)
	{
		G[read()].push_back(i);
	}
	dfs(1, 0);
	int ans = 0;
	for (int i = 0; i <= n; i++) ans += cnt[i] % 2;
	printf("%d\n", ans);
	return 0;
}