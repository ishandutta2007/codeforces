ModInt pow(ModInt a, ll n) {
    assert(n >= 0);
	ModInt ans = 1;
    while (n > 0) {
        if (n&1) ans = ans*a;
        a = a*a;
        n >>= 1;
    }
	return ans;
}

using mint = ModInt;

void solve(){
    int n, s; cin >> n >> s;
    vector<int> a(n);
    vector<int> a_pos(n, -1);
    for(int i = 0; i < n; i++) {
        cin >> a[i]; a[i]--;
        a_pos[a[i]] = i;
    }
    vector<int> b(n);
    vector<int> b_pos(n, -1);
    for(int i = 0; i < n; i++) {
        cin >> b[i];
        if(b[i] > 0) {
            b[i]--;
            b_pos[b[i]] = i;
        }
    }
    mint ans = 1;
    int cnt = 0;
    for(int i = 0; i < s; i++){
        if(b[a_pos[i]] == -1){
            cnt++;
        }
    }
    for(int i = 0; i < n; i++){
        if(i+s < n){
            if(b[a_pos[i+s]] == -1){
                cnt++;
            }   
        }
        if(b_pos[i] >= 0){
            if(a[b_pos[i]] > i+s){
                cout << 0 << endl;
                return;
            }
            continue;
        }
        if(cnt <= 0){
            cout << 0 << endl;
            return;
        }
        ans *= cnt;
        cnt--;
    }
    cout << ans << endl;
}

