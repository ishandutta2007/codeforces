const int MAXN = 100228;


int n, d;
int a[MAXN];


void solve() {
    cin >> n >> d;
    d++;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a, a + n);
    int l = 0;
    int r = n - 1;
    int ans = 0;
    while (l <= r) {
        int f = (d + a[r] - 1) / a[r];
        l += f - 1;
        if (l <= r) {
            ans++;
        } else {
            break;
        }
        r--;
    }
    cout << ans << '\n';
}


