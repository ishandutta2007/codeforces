#include<cstdio>
#include<algorithm>

using namespace std;

int a[1010][1010],n;

int main()
{
    scanf("%d",&n);
    if(n==4)return puts("-1"),0;
    if(n&1)
    {
        a[1][2]=a[2][3]=a[3][1]=1;
        for(int i=4;i<=n;i+=2)
        {
            a[1][i]=a[i][i+1]=a[i+1][1]=1;
            for(int j=1;j<i;j++)
                if(a[1][j])a[i][j]=1,a[j][i+1]=1;
                else a[j][i]=1,a[i+1][j]=1;
        }
    }
    else
    {
        a[1][1]=0;a[1][2]=1;a[1][3]=1;a[1][4]=1;a[1][5]=0;a[1][6]=0;
        a[2][1]=0;a[2][2]=0;a[2][3]=1;a[2][4]=1;a[2][5]=1;a[2][6]=0;
        a[3][1]=0;a[3][2]=0;a[3][3]=0;a[3][4]=1;a[3][5]=0;a[3][6]=1;
        a[4][1]=0;a[4][2]=0;a[4][3]=0;a[4][4]=0;a[4][5]=1;a[4][6]=1;
        a[5][1]=1;a[5][2]=0;a[5][3]=1;a[5][4]=0;a[5][5]=0;a[5][6]=1;
        a[6][1]=1;a[6][2]=1;a[6][3]=0;a[6][4]=0;a[6][5]=0;a[6][6]=0;
        for(int i=7;i<=n;i+=2)
        {
            a[1][i]=a[i][i+1]=a[i+1][1]=1;
            for(int j=1;j<i;j++)
                if(a[1][j])a[i][j]=1,a[j][i+1]=1;
                else a[j][i]=1,a[i+1][j]=1;
        }
    }
    for(int i=1;i<=n;i++)
    {
        for(int j=1;j<=n;j++)
            printf("%d ",a[i][j]);
        putchar('\n');
    }
}