int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	const ll mod = 1e9 + 7;
	int n;
	cin >> n;
	vl a(n);
	cin >> a;
	vector<vi> adj(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vl res(a);
	vector<vl> sum(n, vl(2)), cnt(n, vl(2));
	function<void(int, int)> dfs = [&](int u, int p){
		res[u] = sum[u][1] = a[u], cnt[u][1] = 1;
		for(auto v: adj[u]){
			if(v != p){
				dfs(v, u);
				res[u] += res[v];
				(res[u] += 2 * (sum[u][1] * cnt[v][0] + cnt[u][1] * sum[v][0])) %= mod;
				(res[u] += 2 * (sum[u][0] * cnt[v][1] + cnt[u][0] * sum[v][1])) %= mod;
				(sum[u][0] += sum[v][1] - a[u] * cnt[v][1]) %= mod;
				(sum[u][1] += sum[v][0] + a[u] * cnt[v][0]) %= mod;
				cnt[u][0] += cnt[v][1];
				cnt[u][1] += cnt[v][0];
			}
		}
	};
	dfs(0, 0);
	cout << (res[0] % mod + mod) % mod;
	return 0;
}
