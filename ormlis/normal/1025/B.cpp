void solve() {
    int n; cin >> n;
    ll ans = 0;
    int x, y;
    rep(i, n) {
        cin >> x >> y;
        ll to = 1ll * x * y;
        ans = __gcd(ans, to);
    }
    if (ans == 1) {
        cout << "-1\n";
        return;
    }
    ll g1 = __gcd(1ll * x, ans);
    ll g2 = __gcd(1ll * y, ans);
    ans = max(g1, g2);
    for(ll x = 2; x * x <= ans; ++x) {
        if (ans % x == 0) {
            cout << x << '\n';
            return;
        }
    }
    cout << ans << '\n';
}

