#include<bits/stdc++.h>
using namespace std;
const int N=1e6+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N];

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=n-1;
	for (int i=m-(m%2==0),l=1,r=i+1;i>=1;i-=2,l++,r--) a[l]=a[r]=i;
	for (int i=m-(m%2==1),l=m-(m%2==0)+2,r=n*2-1;i>=2;i-=2,l++,r--) a[l]=a[r]=i;
	for (int i=1;i<=n*2;i++) print(n-a[i],' ');
	
	return 0;
}
