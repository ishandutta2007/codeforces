const int N=1e6+5;
int main()
{
	ios::sync_with_stdio(0); cin.tie(0);
	cout<<setiosflags(ios::fixed)<<setprecision(15);
	int T; cin>>T;
	while (T--)
	{
		int n,i,j;
		ll k,b,s;
		cin>>n>>k>>b>>s;
		vector<ll> a(n,b/n*k);
		for (i=0; i<b%n; i++) a[i]+=k;
		ll tot=accumulate(all(a),0ll);
		assert(tot==k*b);
		if (s<tot) { cout<<"-1\n"; continue; }
		for (ll &x:a)
		{
			ll d=min(k-1,s-tot);
			tot+=d;
			x+=d;
		}
		if (s==tot) cout<<a<<'\n'; else cout<<"-1\n";
	}
}