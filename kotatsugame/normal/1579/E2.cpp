namespace atcoder {

template <class T> struct fenwick_tree {
    using U = internal::to_unsigned_t<T>;

  public:
    fenwick_tree() : _n(0) {}
    explicit fenwick_tree(int n) : _n(n), data(n) {}

    void add(int p, T x) {
        assert(0 <= p && p < _n);
        p++;
        while (p <= _n) {
            data[p - 1] += U(x);
            p += p & -p;
        }
    }

    T sum(int l, int r) {
        assert(0 <= l && l <= r && r <= _n);
        return sum(r) - sum(l);
    }

  private:
    int _n;
    std::vector<U> data;

    U sum(int r) {
        U s = 0;
        while (r > 0) {
            s += data[r - 1];
            r -= r & -r;
        }
        return s;
    }
};

}  // namespace atcoder

using namespace std;
int T,N;
int A[2<<17];
main()
{
	cin>>T;
	for(;T--;)
	{
		cin>>N;
		for(int i=0;i<N;i++)cin>>A[i];
		vector<int>vs(A,A+N);
		sort(vs.begin(),vs.end());
		vs.erase(unique(vs.begin(),vs.end()),vs.end());
		atcoder::fenwick_tree<int>P(vs.size());
		for(int i=0;i<N;i++)
		{
			A[i]=lower_bound(vs.begin(),vs.end(),A[i])-vs.begin();
			P.add(A[i],1);
		}
		long long ans=0;
		for(int i=N;i--;)
		{
			int l=P.sum(0,A[i]),r=P.sum(A[i]+1,vs.size());
			ans+=min(l,r);
			P.add(A[i],-1);
		}
		cout<<ans<<"\n";
	}
}