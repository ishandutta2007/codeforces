const int Mod=1'000'000'007;

int ksmii(int a,int b=Mod-2){
	if(!b){
		return 1;
	}
	int x=ksmii(a,b>>1);
	x=1LL*x*x%Mod;
	if(b&1){
		x=1LL*x*a%Mod;
	}
	return x;
}

int f[7890][7890];
int rev[7890];
int ans[7890];

int n;
int dx[]={-1,0,1,0},dy[]={0,1,0,-1};
int p[4],r;
vector<pair<int,int> > pts;

bool good(int x,int y){
	return x*x+y*y<=r*r;
}

int getid(int nx,int ny){
	pair<int,int> p(nx,ny);
	int id=lower_bound(pts.begin(),pts.end(),p)-pts.begin();
	return id;
}

int main(){
	scanf("%d",&r);
	int sum=0;
	for(int i=0;i<4;i++){
		scanf("%d",p+i);
		sum+=p[i];
	}
	sum=ksmii(sum);
	for(int i=0;i<4;i++){
		p[i]=1LL*p[i]*sum%Mod;
	}
	for(int i=-r;i<=r;i++){
		for(int j=-r;j<=r;j++){
			if(good(i,j)){
				pts.emplace_back(i,j);
			}
		}
	}
	n=pts.size();
	for(int i=0;i<n;i++){
		f[i][i]=1;
		f[i][n]=1;
		int x=pts[i].first,y=pts[i].second;
		for(int j=0;j<4;j++){
			int xx=x+dx[j],yy=y+dy[j];
			if(good(xx,yy)){
				f[i][getid(xx,yy)]=(-p[j]+Mod)%Mod;
			}
		}
	}
	for(int i=0;i<n;i++){
		vector<int> ids;
		ids.push_back(n);
		int rg=min(n,i+105);
		for(int j=i;j<rg;j++){
			ids.push_back(j);
		}
		rev[i]=ksmii(f[i][i]);
		for(int j=i+1;j<rg;j++){
			int x=1LL*f[j][i]*rev[i]%Mod;
			if(!x){
				continue;
			}
			for(int k:ids){
				f[j][k]=(f[j][k]-1LL*f[i][k]*x%Mod+Mod)%Mod;
			}
		}
	}
	for(int i=n-1;i>=0;i--){
		long long fff=f[i][n];
		int rg=min(n,i+105);
		for(int j=i+1;j<rg;j++){
			fff=(fff-1LL*ans[j]*f[i][j]%Mod+Mod)%Mod;
		}
		ans[i]=1LL*fff*rev[i]%Mod;
	}
	printf("%d\n",ans[getid(0,0)]);
	return 0;
}