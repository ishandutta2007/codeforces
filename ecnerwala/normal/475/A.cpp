#include<bits/stdc++.h>
using namespace std;

string S[6] = {
"+------------------------+",
"|#.#.#.#.#.#.#.#.#.#.#.|D|)",
"|#.#.#.#.#.#.#.#.#.#.#.|.|",
"|#.......................|",
"|#.#.#.#.#.#.#.#.#.#.#.|.|)",
"+------------------------+",
};

int main() {
	int n; cin >> n;
	for(int j = 0; n; j++) {
		for(int i = 0; n && i < 6; i++) {
			if(S[i][j] == '#') S[i][j] = 'O', n--;
		}
	}
	for(int i = 0; i < 6; i++) {
		cout << S[i] << '\n';
	}
	return 0;
}