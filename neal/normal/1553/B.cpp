void run_case() {
    string S, T;
    cin >> S >> T;
    string rev_T = T;
    reverse(rev_T.begin(), rev_T.end());
    int N = int(S.size());
    int M = int(T.size());
    bool ans = false;

    for (int mid = 0; mid < N; mid++)
        for (int start = 0; start <= mid; start++)
            if (mid - start + 1 <= M) {
                int end = mid - (M - (mid - start + 1));

                if (end < 0)
                    continue;

                if (S.compare(start, mid - start + 1, T, 0, mid - start + 1) == 0 && S.compare(end, mid - end + 1, rev_T, 0, mid - end + 1) == 0)
                    ans = true;
            }

    cout << (ans ? "YES" : "NO") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}