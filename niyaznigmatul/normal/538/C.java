class TaskC {
    public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int n = in.nextInt();
        int m = in.nextInt();
        int ans = 0;
        int lastDay = -1;
        int lastHeight = -1;
        for (int i = 0; i < m; i++) {
            int day = in.nextInt();
            int height = in.nextInt();
            ans = Math.max(ans, height);
            if (lastDay < 0) {
                ans = Math.max(height + day - 1, ans);
            } else {
                if (Math.abs(day - lastDay) < Math.abs(height - lastHeight)) {
                    out.println("IMPOSSIBLE");
                    return;
                }
                int dif = Math.abs(day - lastDay) - Math.abs(height - lastHeight);
                ans = Math.max(ans, dif / 2 + Math.max(height, lastHeight));
            }
            lastDay = day;
            lastHeight = height;
        }
        ans = Math.max(ans, n - lastDay + lastHeight);
        out.println(ans);
    }
}

class FastScanner extends BufferedReader {

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
//            if (isEOF && ret < 0) {
//                throw new InputMismatchException();
//            }
//            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    static boolean isWhiteSpace(int c) {
        return c >= 0 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (c >= 0 && !isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    public String readLine() {
        try {
            return super.readLine();
        } catch (IOException e) {
            return null;
        }
    }

}

