using namespace std;
inline int read(){
   int s=0,w=1;
   char ch=getchar();
   while(ch<'0'||ch>'9'){if(ch=='-')w=-1;ch=getchar();}
   while(ch>='0'&&ch<='9') s=s*10+ch-'0',ch=getchar();
   return s*w;
}
const int p=998244353;
int qp(int x,int y)
{
	int res=1;
	for(int t=x; y; y>>=1,t=t*t%p) if(y&1) res=res*t%p;
	return res;
}
int d(int x)
{
	int t=0;
	while(x) t+=x%10,x/=10;
	return t;
}
signed main()
{
    for(int T=read();T--;)
    {
    	int n=read();
    	while(__gcd(n,d(n))==1) ++n;
    	printf("%lld\n",n);
    }
	return 0;
}