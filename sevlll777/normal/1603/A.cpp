int lcm(int a, int b) {
  return a * b / gcd(a, b);
}

void solve() {
  int n;
  cin >> n;
  vector<int> A(n);
  for (int i = 0; i < n; i++) {
    cin >> A[i];
  }
  int l = 1;
  for (int i = 0; i < n; i++) {
    l = lcm(l, i + 2);
    if (l > (int) 1e9) {
      break;
    }
    if (A[i] % l == 0) {
      cout << "NO\n";
      return;
    }
  }
  cout << "YES\n";
}

