double get_time() {
	return (double) clock() / CLOCKS_PER_SEC;
}

signed main() {
	setup();
	// SOLVE;

	int n;
	cin >> n;
	vector<int> a(n);
	long long sum = 0;
	for(int i = 0; i < n; ++i) {
		cin >> a[i];
		sum += a[i];
	}
	if (sum == 1) {
		cout << "-1\n";
		return 0;
	}
	vector<long long> primes;
	for(int i = 2; (long long) i * i <= sum; ++i) {
		if (sum % i == 0) {
			primes.push_back(i);
			while(sum % i == 0)
				sum /= i;
		}
	}
	if (sum != 1)
		primes.push_back(sum);
	long long ans = 1e18 + 228;
	for(auto p: primes) {
		// if (get_time() > 0.4) 
		// 	break;
		int sum = 0;
		vector<int> b = a;
		for(auto &i: b)
			i %= p;
		int j = 0;
		long long res = 0;	
		for(int i = 0; i < n; ++i) {
			sum += b[i];
			if (sum >= (p + 1) / 2) {
				long long now = 0;
				while(j < n && now < p) {
					long long c = min(b[j], p - now);
					res += abs(i - j) * c;
					b[j] -= c;
					now += c;
					j += 1;
				}
				j -= 1;
				i = max((long long) 0, j - 2);
				sum = 0;
			}
		}
		ans = min(ans, res);
	}
	cout << ans << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

