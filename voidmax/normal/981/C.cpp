const int MAXN = 2e5 + 1;

int n;
int root;
vector<int> edge[MAXN];
vector<pair<int, int>> ans;

void dfs(int i, int p = -1) {
	if (p != -1 && edge[i].size() > 2) {
		cout << "No" << endl;
		exit(0);
	}
	for (auto j : edge[i]) {
		if (j == p) continue;
		dfs(j, i);
	}
	if (edge[i].size() == 1 && p != -1) {
		ans.pb({root, i});
	}
}

int main() {
	ios::sync_with_stdio(0);
	cin >> n;
	for (int i = 1; i < n; ++i) {
		int a, b;
		cin >> a >> b;
		edge[a].pb(b);
		edge[b].pb(a);
	}
	for (int i = 1; i <= n; ++i) {
		if (edge[root].size() < edge[i].size()) {
			root = i;
		}
	} 
	dfs(root);
	cout << "Yes\n";
	cout << ans.size() << '\n';
	for (auto i : ans) {
		cout << i << '\n';
	}
}