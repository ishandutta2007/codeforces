template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {

  int n; cin >> n;
  int l = 1, r = n;
  auto ask = [&](int p) {
    cout << "? " << p << endl;
    int ret; cin >> ret;
    return ret;
  };
  while (l != r) {
    int mid = (l + r) / 2;
    if (ask(mid) < ask(mid + 1)) r = mid;
    else l = mid + 1;
  }
  cout << "! " << l << endl;

  return 0;
}