#include <bits/stdc++.h>
using namespace std;
const long double pi=acosl(-1);

int n;
long long vcl[2555];
int x[2555],y[2555];
long long dp[2555][4],qzh[2555][4];
int pos[2555];

int main(){
	scanf("%d",&n);
	for(int i=2;i<=n;i++){
		vcl[i]=vcl[i-1]+1ll*(i-2)*(i-1)/2;
	}
	for(int i=0;i<n;i++){
		scanf("%d%d",x+i,y+i);
	}
	long long alls=0;
	for(int i=0;i<n;i++){
		vector<long double> jj;
		for(int j=0;j<n;j++){
			if(j!=i){
				jj.push_back(atan2l(y[j]-y[i],x[j]-x[i]));
			}
		}
		sort(jj.begin(),jj.end());
		for(int j=0;j<n-1;j++){
			pos[j]=lower_bound(jj.begin(),jj.end(),jj[j]-pi)-jj.begin();
		}
		memset(dp,0,sizeof(dp));
		memset(qzh,0,sizeof(qzh));
		for(int j=0;j<n-1&&jj[j]<0;j++){
			dp[j][0]=1;
		}
		for(int j=1;j<n;j++){
			qzh[j][0]=qzh[j-1][0]+dp[j-1][0];
		}
		for(int i=1;i<4;i++){
			for(int j=0;j<n-1;j++){
				dp[j][i]=qzh[j][i-1]-qzh[pos[j]][i-1];
			}
			for(int j=1;j<n;j++){
				qzh[j][i]=qzh[j-1][i]+dp[j-1][i];
			}
		}
		long long ans=0;
		for(int j=0;j<n-1;j++){
			ans+=dp[j][3];
		}
		for(int j=0;j<n-1&&jj[j]<0;j++){
			int cnt=upper_bound(jj.begin(),jj.end(),jj[j]+pi)-jj.begin()-j-1;
			ans-=vcl[cnt];
		}
		alls+=ans;
	}
	printf("%lld\n",alls);
	return 0;
}