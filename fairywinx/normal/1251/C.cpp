void solve() {
	string s;
	cin >> s;
	vector<char> a[2];
	for(auto i: s) {
		a[(i - '0') % 2].push_back(i);
	}
	reverse(all(a[0]));
	reverse(all(a[1]));
	while(a[0].size() || a[1].size()) {
		if (a[1].size() && (!a[0].size() || a[1].back() < a[0].back())) {
			cout << a[1].back();
			a[1].pop_back();
		}
		else {
			cout << a[0].back();
			a[0].pop_back();
		}
	}
	cout << '\n';
}

