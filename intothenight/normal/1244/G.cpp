mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	ll n, k;
	cin >> n >> k;
	if(k < n * (n + 1) / 2){
		cout << -1;
		return 0;
	}
	k = min(k, n * (n + 1) - n / 2 * (n / 2 + 1) - n % 2 * (n + 1) / 2);
	cout << k << "\n";
	k -= n * (n + 1) / 2;
	vi used(n);
	vi res;
	for(int i = 0, val = n - 1; k; i ++, val -= 2){
		if(k >= val){
			res.push_back(i + 1 + val);
			used[i + val] = true;
			k -= val;
		}
		else{
			res.push_back(i + 1 + k);
			used[i + k] = true;
			k = 0;
		}
	}
	for(int i = 0; i < n; i ++){
		cout << i + 1 << " ";
		if(!used[i]){
			res.push_back(i + 1);
		}
	}
	cout << "\n" << res;
}

