class TaskA {
    public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int[] count = new int[26];
        char[] c = in.next().toCharArray();
        int k = c.length - in.nextInt();
        for (char e : c) {
            count[e - 'a']++;
        }
        boolean[] got = new boolean[26];
        while (k > 0) {
            int max = -1;
            for (int i = 0; i < 26; i++) {
                if (got[i]) {
                    continue;
                }
                if (max < 0 || count[i] > count[max]) {
                    max = i;
                }
            }
            got[max] = true;
            k -= count[max];
        }
        int cntLetters = 0;
        for (int i = 0; i < 26; i++) {
            if (got[i]) {
                cntLetters++;
            }
        }
        out.println(cntLetters);
        for (int i = 0; i < c.length; i++) {
            if (got[c[i] - 'a']) {
                out.print(c[i]);
            }
        }
        out.println();
    }
}

class FastScanner extends BufferedReader {

    boolean isEOF;

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
            if (isEOF && ret < 0) {
                throw new InputMismatchException();
            }
            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    public String next() {
        StringBuilder sb = new StringBuilder();
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        while (!isWhiteSpace(c)) {
            sb.appendCodePoint(c);
            c = read();
        }
        return sb.toString();
    }

    static boolean isWhiteSpace(int c) {
        return c >= -1 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (!isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    }

