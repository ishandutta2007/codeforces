int main() {
    setIO();

    int n;
    re(n);
    vi r(n);
    re(r);

    int target = *min_element(all(r));
    int total_moves = 0;
    int max_moves = 0;
    for (int i = 0; i < n; i++) {
        total_moves += r[i] - target;
        ckmax(max_moves, r[i] - target);
    }

    while (target > 0 && max_moves > total_moves - max_moves) {
        target--;
        max_moves++;
        total_moves += n;
    }

    vi moves;
    for (int i = 0; i < n; i++) {
        if (target == 0)
            moves.push_back(100);
        else
            moves.push_back(r[i] - target);
    }

    total_moves = accumulate(all(moves), 0);

    priority_queue<pair<int, int>> pq;
    for (int i = 0; i < n; i++) {
        pq.push({moves[i], i});
    }

    vvi ans;
    while (total_moves >= 2) {
        total_moves -= 2;

        auto u = pq.top().second;
        pq.pop();
        auto v = pq.top().second;
        pq.pop();

        ans.push_back(vi{u, v});

        if (total_moves == 1)
            ans.back().push_back(pq.top().second);

        for (int i : ans.back()) {
            moves[i]--;
            pq.push({moves[i], i});
        }
    }

    ps(target);
    ps(sz(ans));
    for (auto &a : ans) {
        string line(n, '0');
        for (int i : a)
            line[i] = '1';
        ps(line);
    }

    return 0;
}