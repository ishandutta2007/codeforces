typedef long double base;        
typedef pair<base, base> point;

int n, k;
bitset<100001> used;
bitset<100001> can;

int gcd(int a, int b) {
    return a ? gcd(b % a, a) : b;
}

vector<bool> res[100001];

int main() {
    srand(time(0));
    ios::sync_with_stdio(0);
    can[0] =  true;
    cin >> n >> k;
    set<int> cool;
    for (int i = 0; i < n; ++i) {
        int v;
        cin >> v;
        v = gcd(v, k);
        cool.insert(v);
    }
    for (auto i : cool) {
        res[i].resize(i);
    }
    queue<int> que;
    can[0] = true;
    que.push(0);
    while (que.size()) {
        int v = que.front();
        que.pop();
        for (auto i : cool) {
            if (!res[i][v % i]) {
                res[i][v % i] = true;
                for (int j = v % i; j < k; j += i) {
                    if (!can[j]) {
                        can[j] = true;
                        que.push(j);
                    }
                }
            }
        }
    }
    vector<int> ans;
    for (int i = 0; i < k; ++i) {
        if (can[i]) {
            ans.pb(i);
        }
    }
    cout << ans.size() << endl;
    cout << ans << endl;
}