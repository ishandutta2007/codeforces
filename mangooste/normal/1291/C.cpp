signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        int n, m, k;
        cin >> n >> m >> k;
        vector<int> a(n);
        for (auto &el : a)
            cin >> el;
        int ans = 0;
        k = min(k, m - 1);
        for (int lft = 0; lft <= k; lft++) {
            int rgt = k - lft;
            int cur_ans = 2e9;
            int ost = m - 1 - k;
            for (int i = 0; i <= ost; i++)
                cur_ans = min(cur_ans, max(a[lft + i], a[n - rgt - 1 - ost + i]));
            ans = max(ans, cur_ans);
        }
        cout << ans << '\n';
    }
}