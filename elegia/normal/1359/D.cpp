template<class T>
istream &operator>>(istream &is, vector<T> &v) {
  for (T &x : v)
    is >> x;
  return is;
}

template<class T>
ostream &operator<<(ostream &os, const vector<T> &v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int n;
  cin >> n;
  vector<int> a(n);
  cin >> a;
  ll ans = 0;
  vector<ll> val(n, -1LL << 40);
  for (int i = -30; i <= 30; ++i) {
    for (int j = 0; j < n; ++j)
      if (a[j] == i)
        val[j] = i;
    ll mx = -1LL << 40, pre = -1LL << 40;
    for (int j = 0; j < n; ++j) {
      pre = max(pre, 0LL) + val[j];
      mx = max(mx, pre);
    }
    ans = max(ans, mx - i);
  }
  cout << ans << '\n';

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}