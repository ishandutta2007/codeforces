Mint Dfs(int bit, const vector<int>& p, const vector<int>& q) {
//  ++Q2;
//  debug("two", bit, p, q);
  if (p.empty() || q.empty() || bit == -1) {
    return Power((int) p.size() + (int) q.size()) - 1;
  }
  vector<int> p0, p1;
  for (int i : p) {
    if (i & (1 << bit)) {
      p1.push_back(i);
    } else {
      p0.push_back(i);
    }
  }
  vector<int> q0, q1;
  for (int i : q) {
    if (i & (1 << bit)) {
      q1.push_back(i);
    } else {
      q0.push_back(i);
    }
  }
  if (k & (1 << bit)) {
    return (Dfs(bit - 1, p0, q1) + 1) * (Dfs(bit - 1, p1, q0) + 1) - 1;
  } else {
    Mint ret = Dfs(bit - 1, p0, q0) + Dfs(bit - 1, p1, q1);
    ret += Power((int) p0.size() + (int) p1.size()) - Power((int) p0.size()) - Power((int) p1.size()) + 1;
    ret += Power((int) q0.size() + (int) q1.size()) - Power((int) q0.size()) - Power((int) q1.size()) + 1;
    return ret;
  }
}

