int chk(int r,int la,int ra,int x){
	if(!r)return 0;
	if(la==ra)return val[r];
	int mid=(la+ra)>>1;
	if(x<=mid)return chk(ls[r],la,mid,x);
	else return chk(rs[r],mid+1,ra,x); 
}
int clone(int x){
	++t;
	ls[t]=ls[x];rs[t]=rs[x];val[t]=val[x];return t;
}
int insert(int r,int la,int ra,int x,int y){
	int n=clone(r);	
	if(la==ra){val[n]=y;return n;}
	int mid=(la+ra)>>1;
	if(x<=mid)ls[n]=insert(ls[n],la,mid,x,y);
	else rs[n]=insert(rs[n],mid+1,ra,x,y);
	return n;
}
