typedef vector<tpl> vtpl;
 
 
 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, m, k;
	cin >> n >> m >> k;
	vector<map<int, int>> adj(n);
	for(int i = 0; i < m; ++ i){
		int u, v, w;
		cin >> u >> v >> w, u --, v --;
		if(!adj[u].count(v) || adj[u][v] > w){
			adj[u][v] = adj[v][u] = w;
		}
	}
	vector<vi> weights(n);
	for(int i = 0; i < k; ++ i){
		int v, w;
		cin >> v >> w, v --;
		weights[v].push_back(w);
	}
	for(auto &x: weights){
		sort(all(x));
	}
	vpii test(n);
	int res = 0;
	for(int u = 0; u < n; ++ u){
		if(!weights[u].empty()){
			if(!adj[0].count(u) || adj[0][u] > weights[u][0]){
				test[u] = {true, weights[u][0]};
				adj[0][u] = adj[u][0] = weights[u][0];
				res += sz(weights[u]) - 1;
			}
			else{
				res += sz(weights[u]);
			}
		}
	}
	priority_queue<pli, vpli, greater<pli>> q;
	vl dist(n, 4e18);
	dist[0] = 0;
	q.push({0, 0});
	vi prev(n);
	while(!q.empty()){
		auto [d, u] = q.top();
		q.pop();
		if(d == dist[u]){
			for(auto [v, w]: adj[u]){
				if(dist[v] > dist[u] + w){
					prev[v] = u;
					dist[v] = dist[u] + w;
					q.push({dist[v], v});
				}
			}
		}
	}
	for(int u = 0; u < n; ++ u){
		if(test[u].first){
			if(prev[u]){
				res ++;
			}
			else{
				for(auto [v, w]: adj[u]){
					if(v && w + dist[v] == test[u].second){
						res ++;
						break;
					}
				}
			}
		}
	}
	cout << res;
	return 0;
}
 
