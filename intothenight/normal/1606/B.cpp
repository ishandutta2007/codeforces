#include <bits/stdc++.h>
using namespace std;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	auto __solve_tc = [&](auto __tc_num)->int{
		long long n, k;
		cin >> n >> k;
		long long res = 0;
		for(auto x = 1LL; x < n; ){
			if(x < k){
				++ res;
				x <<= 1;
			}
			else{
				res += (n - x + k - 1) / k;
				x = n;
			}
		}
		cout << res << "\n";
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

