void write(const char *s) { for (; *s; ++s) putchar(*s); }
template <typename __Tp1, typename ...__Tp2> void write(__Tp1 x, __Tp2 ... y) { write(x), write(y...); }

char s[20];
void work() {
	scanf("%s", s + 1);
	int len = strlen(s + 1);
	if (len <= 2) return write(s[len], '\n');
	int mn = 10;
	for (int i = 1; i <= len; ++i) mn = min(mn, s[i] - '0');
	write(mn, '\n');
}

int main() {
	int T; read(T);
	while (T--) work();
	return 0;
}