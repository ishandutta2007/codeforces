template<typename T>
class Compress{
    public:
    vector<T> data;
    int offset;
    Compress(vector<T> data_, int offset=0): offset(offset){
        set<T> st;
        for(T x: data_) st.insert(x);
        for(T x: st) data.push_back(x);
    };
    int operator[](T x) {
        auto p = lower_bound(data.begin(), data.end(), x);
        assert(x == *p);
        return offset+(p-data.begin());
	}
    T inv(int x){
        return data[x-offset];
    }
    int size(){
        return data.size();
    }
};

struct S{
    int val, idx;
};

S op(S a, S b){
    if(a.val > b.val) return a;
    else return b;
}

S e(){
    return S{0, -1};
}

struct F{
    int val, idx;
};

F id(){
    return F{-1, -1};
}

S mapping(F l, S r) {
    if(l.val == -1) return r;
    return S{l.val, l.idx};
}

F composition(F l, F r) { 
    if(l.val == -1) return r;
    return l;
}

using Seg = atcoder::lazy_segtree<S, op, e, F, mapping, composition, id>;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, m; cin >> n >> m;
    vector<int> l(m), r(m);
    vector<vector<int>> indices(n);
    vector<int> v;
    for(int i = 0; i < m; i++){
        int idx;
        cin >> idx >> l[i] >> r[i]; idx--; l[i]--; r[i]--;
        indices[idx].push_back(i);
        v.push_back(l[i]);
        v.push_back(r[i]);
    }
    auto cp = Compress<int>(v);
    int sz = cp.size();
    for(int i = 0; i < m; i++){
        l[i] = cp[l[i]];
        r[i] = cp[r[i]];
    }
    vector<int> pre(n, -1);
    Seg seg(sz);
    for(int idx = 0; idx < n; idx++){
        S s = e();
        for(int i: indices[idx]){
            s = op(s, seg.prod(l[i], r[i]+1));
        }
        pre[idx] = s.idx;
        F f = F{s.val+1, idx};
        for(int i: indices[idx]){
            seg.apply(l[i], r[i]+1, f);
        }
    }
    S mx = seg.all_prod();
    int cur = mx.idx;
    vector<bool> ok(n);
    while(true){
        ok[cur] = true;
        if(pre[cur] == -1) break;
        cur = pre[cur];
    }
    vector<int> ans;
    for(int i = 0; i < n; i++){
        if(!ok[i]) ans.push_back(i+1);
    }
    assert(ans.size()+mx.val == n);
    cout << ans.size() << endl;
    print_vector(ans);
}