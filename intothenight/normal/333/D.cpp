mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int N, M;
	cin >> N >> M;
	vector<vi> a(N, vi(M));
	cin >> a;
	int l = 0, r = 1e9 + 1;
	while(r - l >= 2){
		int m = l + r >> 1;
		vector<bitset<1000>> q(N);
		for(int i = 0; i < N; i ++){
			for(int j = 0; j < M; j ++){
				if(a[i][j] >= m){
					q[i].set(j);
				}
			}
		}
		bool ok = false;
		for(int i = 0; i < N; i ++){
			for(int j = i + 1; j < N; j ++){
				if((q[i] & q[j]).count() >= 2){
					ok = true;
					goto label;
				}
			}
		}
	label:
		ok ? l = m : r = m;
	}
	cout << l;
	return 0;
}

