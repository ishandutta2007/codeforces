#include <bits/stdc++.h>
using namespace std;

const int jt=998244353,Mod=998244352;
int rev[300005];

int ksmii(int a,int b,int md){
	if(!b){
		return 1;
	}
	int x=ksmii(a,b>>1,md);
	x=1ll*x*x%md;
	if(b&1){
		x=1ll*x*a%md;
	}
	return x;
}

void ntt(vector<int> &a,int len,int inv){
	while(a.size()<(1<<len)){
		a.push_back(0);
	}
	for(int i=1;i<(1<<len);i++){
		rev[i]=((rev[i>>1]>>1)|((i&1)<<(len-1)));
		if(i<rev[i]){
			swap(a[i],a[rev[i]]);
		}
	}
	for(int i=0;i<len;i++){
		int omega=ksmii(3,inv*Mod/(2<<i)+Mod,jt);
		for(int j=0;j<(1<<len);j+=(2<<i)){
			for(int k=0,omg=1;k<(1<<i);k++,omg=1ll*omg*omega%jt){
				int y=1ll*omg*a[j+k+(1<<i)]%jt;
				a[j+k+(1<<i)]=(a[j+k]-y+jt)%jt;
				a[j+k]=(a[j+k]+y)%jt;
			}
		}
	}
	if(inv==-1){
		int flen=ksmii((1<<len),jt-2,jt);
		for(int i=0;i<(1<<len);i++){
			a[i]=1ll*a[i]*flen%jt;
		}
	}
}

int n;
int cnt[3000005];
vector<pair<int,int> > sus;

vector<int> fendge(int l,int r){
	if(l==r){
		vector<int> v;
		for(int i=0;i<=sus[l].second;i++){
			v.push_back(1);
		}
		return v;
	}
	if(l>r){
		vector<int> v;
		v.push_back(1);
		return v;
	}
	int m=(l+r)>>1;
	vector<int> L=fendge(l,m);
	vector<int> R=fendge(m+1,r);
	int Ls=L.size()+R.size();
	int len=0;
	while(Ls>=(1<<len)){
		len++;
	}
	ntt(L,len,1);
	ntt(R,len,1);
	for(int i=0;i<(1<<len);i++){
		L[i]=1ll*L[i]*R[i]%jt;
	}
	ntt(L,len,-1);
	while(L.back()==0){
		L.pop_back();
	}
	return L;
}

