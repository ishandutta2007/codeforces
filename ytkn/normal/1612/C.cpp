template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    ll k, x; cin >> k >> x;
    auto calc = [&](ll p){
        if(p <= k){
            return (p*(p+1))/2;
        }
        ll ans = (k*(k+1))/2;
        ll rem = p-k;
        ans += (((k-1)+(k-rem))*rem)/2;
        return ans;
    };
    if(calc(2*k-1) < x){
        cout << 2*k-1 << endl;
        return;
    }
    ll l = 0, r = 2*k-1;
    while(r-l > 1){
        ll p = (l+r)/2;
        if(calc(p) >= x) r = p;
        else l = p;
    }
    cout << r << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}