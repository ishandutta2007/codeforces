class TaskB {
    public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int n = in.nextInt();
        int k = in.nextInt();
        int p = in.nextInt();
        int x = in.nextInt();
        int y = in.nextInt();
        int[] a = new int[n];
        int s = 0;
        for (int i = 0; i < k; i++) s += a[i] = in.nextInt();
        for (int c = 0; c <= n - k; c++) {
            if (s + c * y + (n - k - c) > x) {
                continue;
            }
            for (int i = k; i < k + c; i++) a[i] = y;
            for (int i = k + c; i < n; i++) a[i] = 1;
            int[] b = a.clone();
            Arrays.sort(b);
            if (b[b.length / 2] >= y) {
                for (int i = k; i < k + c; i++) out.print(y + " ");
                for (int i = k + c; i < n; i++) out.print("1 ");
                out.println();
                return;
            }
        }
        out.println("-1");
    }
}

class FastScanner extends BufferedReader {

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
//            if (isEOF && ret < 0) {
//                throw new InputMismatchException();
//            }
//            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    static boolean isWhiteSpace(int c) {
        return c >= 0 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (c >= 0 && !isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    public String readLine() {
        try {
            return super.readLine();
        } catch (IOException e) {
            return null;
        }
    }

    }

