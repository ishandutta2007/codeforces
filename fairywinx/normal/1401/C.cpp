void solve() {
	int n;
	cin >> n;
	vector<int> a(n), s;
	for(int i = 0; i < n; ++i)
		cin >> a[i];
	s = a;
	sort(s.begin(), s.end());
	vector<int> used(n);
	int mn = 1e9 + 228;
	for(int i = 0; i < n; ++i) 
		mn = min(mn, a[i]);
	vector<int> b;
	int uk = 0;
	for(int i = 0; i < n; ++i) {
		if (a[i] % mn == 0) {
			b.push_back(a[i]);
			used[i] = 1;
		}
	}
	sort(b.begin(), b.end());
	for(int i = 0; i < n; ++i) {
		if (used[i])
			a[i] = b[uk++];
	}
	if (a == s)
		cout << "YES\n";
	else
		cout << "NO\n";
}

