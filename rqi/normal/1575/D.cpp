string s;
int rem = 0;
int ans = 0;
bool was = false;
int t = -1;


void get(int i) {
    if (i == sz(s) ) {
        if (rem % 25 == 0) {
            ans++;
        }
        return;
    }
    if (s[i] == '_') {
        for (int j = (i == 0 && sz(s) >= 2 ? 1: 0); j < 10; j++) {
            int prem = rem;
            rem *= 10;
            rem += j;
            get(i + 1);
            rem = prem;
        }
    } else if (s[i] == 'X') {
        if (was) {
            int prem = rem;
            rem *= 10;
            rem += t;
            get(i + 1);
            rem = prem;
        } else {
            for (int j = (i == 0 && sz(s) >= 2 ? 1: 0); j < 10; j++) {
                int prem = rem;
                rem *= 10;
                rem += j;
                was = true;
                t = j;
                get(i + 1);
                was = false;
                rem = prem;
            } 
        }
    } else {
        if (i == 0 && sz(s) >= 2 && s[0] == '0') {
            return;
        }
        int prem = rem;
        rem *= 10;
        rem += s[i] - '0';
        get(i + 1);
        rem = prem;
    }
}

void solve() {
    cin >> s;
    get(0);
    cout << ans << '\n';
}   


