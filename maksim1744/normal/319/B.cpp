int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n;
    cin >> n;
    vector<int> a(n);
    cin >> a;
    vector<int> prev(n, -1), next(n, -1);
    for (int i = 0; i < n - 1; ++i) {
        prev[i + 1] = i;
        next[i] = i + 1;
    }
    set<int> check;
    int steps = 0;
    vector<bool> u(n, false);
    for (int i = 0; i < n - 1; ++i) {
        if (a[i] > a[i + 1])
            check.insert(i);
    }
    while (true) {
        bool change = false;
        ++steps;
        vector<int> vc(check.begin(), check.end());
        reverse(vc.begin(), vc.end());
        show(vc);
        check.clear();
        for (auto k : vc) {
            if (!u[k] && next[k] != -1 && a[next[k]] < a[k]) {
                u[next[k]] = true;
                next[k] = next[next[k]];
                if (!u[k]) {
                    check.insert(k);
                }
                change = true;
            }
        }
        if (!change) break;
    }
    cout << steps - 1 << '\n';

    return 0;
}