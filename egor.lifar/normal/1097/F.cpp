const int MAXN = 100001;



bitset<7001> kek[MAXN];
bitset<7001> divs[7001];
bitset<7001> rdivs[7001];
bool sq[7001];
int n, q;


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    //read(FILENAME);
    cin >> n >> q;
    for (int i = 1; i <= 7000; i++) {
        for (int j = 2; j * j <= i; j++) {
            if (i % (j * j) == 0) {
                sq[i] = true;
            }
        }
    }
    for (int i = 1; i <= 7000; i++) {
        for (int j = i; j <= 7000; j += i) {
            divs[j][i] = 1;
            if (!sq[j / i]) {
                rdivs[i][j] = 1;
            }
        }
    }
    for (int it = 0; it < q; it++) {
        int t;
        cin >> t;
        if (t == 1) {
            int x, v;
            cin >> x >> v;
            x--;
            kek[x] = divs[v];
        } else if (t == 2) {
            int x, y, z;
            cin >> x >> y >> z;
            x--, y--, z--;
            kek[x] = kek[y] ^ kek[z];
        } else if (t == 3) {
            int x, y, z;
            cin >> x >> y >> z;
            x--, y--, z--;
            kek[x] = kek[y] & kek[z];
        } else if (t == 4) {
            int x, v;
            cin >> x >> v;
            x--;
            int t = (kek[x] & rdivs[v]).count() % 2;
            cout << t;
        }
    }
    cout << endl;
    return 0; 
}