struct UnionFind {
  vector< int > data;

  UnionFind(int sz) {
    data.assign(sz, -1);
  }

  bool unite(int x, int y) {
    x = find(x), y = find(y);
    if(x == y) return (false);
    if(data[x] > data[y]) swap(x, y);
    data[x] += data[y];
    data[y] = x;
    return (true);
  }

  int find(int k) {
    if(data[k] < 0) return (k);
    return (data[k] = find(data[k]));
  }

  int size(int k) {
    return (-data[find(k)]);
  }
};

template< typename G >
struct DoublingLowestCommonAncestor {
  const int LOG;
  vector< int > dep;
  vector< int64 > dep2;
  const G &g;
  vector< vector< int > > table;

  DoublingLowestCommonAncestor(const G &g) : g(g), dep(g.size()), LOG(32 - __builtin_clz(g.size())), dep2(g.size()) {
    table.assign(LOG, vector< int >(g.size(), -1));
  }

  void dfs(int idx, int par, int d) {
    table[0][idx] = par;
    dep[idx] = d;
    for(auto &to : g[idx]) {
      if(to != par) {
        dep2[to] = dep2[idx] + to.cost;
        dfs(to, idx, d + 1);
      }
    }
  }


  void build() {
    dfs(0, -1, 0);
    for(int k = 0; k + 1 < LOG; k++) {
      for(int i = 0; i < table[k].size(); i++) {
        if(table[k][i] == -1) table[k + 1][i] = -1;
        else table[k + 1][i] = table[k][table[k][i]];
      }
    }
  }

  int query(int u, int v) {
    if(dep[u] > dep[v]) swap(u, v);
    for(int i = LOG - 1; i >= 0; i--) {
      if(((dep[v] - dep[u]) >> i) & 1) v = table[i][v];
    }
    if(u == v) return u;
    for(int i = LOG - 1; i >= 0; i--) {
      if(table[i][u] != table[i][v]) {
        u = table[i][u];
        v = table[i][v];
      }
    }
    return table[0][u];
  }


  int64 dist(int u, int v) {
    return dep2[u] + dep2[v] - 2 * dep2[query(u, v)];
  }

};

template< typename T >
vector< T > dijkstra(WeightedGraph< T > &g, int s) {
  const auto INF = numeric_limits< T >::max();
  vector< T > dist(g.size(), INF);

  using Pi = pair< T, int >;
  priority_queue< Pi, vector< Pi >, greater< Pi > > que;
  dist[s] = 0;
  que.emplace(dist[s], s);
  while(!que.empty()) {
    T cost;
    int idx;
    tie(cost, idx) = que.top();
    que.pop();
    if(dist[idx] < cost) continue;
    for(auto &e : g[idx]) {
      auto next_cost = cost + e.cost;
      if(dist[e.to] <= next_cost) continue;
      dist[e.to] = next_cost;
      que.emplace(dist[e.to], e.to);
    }
  }
  return dist;
}

template< typename T = int >
void warshall_floyd(Matrix< T > &g) {
  for(int k = 0; k < g.size(); k++) {
    for(int i = 0; i < g.size(); i++) {
      for(int j = 0; j < g.size(); j++) {
        g[i][j] = min(g[i][j], g[i][k] + g[k][j]);
      }
    }
  }
}


int main() {
  int N, M;
  cin >> N >> M;
  WeightedGraph< int64 > g(N);
  Edges< int64 > add;
  UnionFind uf(N);
  set< int > app;
  for(int i = 0; i < M; i++) {
    int a, b, c;
    cin >> a >> b >> c;
    --a, --b;
    if(uf.unite(a, b)) {
      g[a].emplace_back(b, c);
      g[b].emplace_back(a, c);
    } else {
      app.emplace(a);
      app.emplace(b);
      add.emplace_back(a, b, c);
      add.emplace_back(b, a, c);
    }
  }
  DoublingLowestCommonAncestor< WeightedGraph< int64 > > lca(g);
  lca.build();
  int Q;
  cin >> Q;


  map< int, int > rev;
  int ptr = 0;
  for(auto &p : app) rev[p] = ptr++;
  Matrix< int64 > tap(ptr + 2, vector< int64 >(ptr + 2, infll));
  for(auto &p : rev) {
    for(auto &q : rev) {
      if(p != q) chmin(tap[p.second][q.second], lca.dist(p.first, q.first));
    }
  }
  for(auto &p : add) {
    chmin(tap[rev[p.src]][rev[p.to]], p.cost);
  }
  warshall_floyd(tap);


  while(Q--) {
    int A, B;
    cin >> A >> B;
    --A, --B;
    int64 pre = lca.dist(A, B);
    vector< int64 > d, e;
    for(auto &p : rev) d.emplace_back(lca.dist(A, p.first));
    for(auto &p : rev) e.emplace_back(lca.dist(B, p.first));
    for(int i = 0; i < d.size(); i++) {
      for(int j = 0; j < e.size(); j++) {
        chmin(pre, d[i] + e[j] + tap[i][j]);
      }
    }
    cout << pre << endl;
  }
}