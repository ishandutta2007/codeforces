void solve() {
	int n;
	cin >> n;
	vector<int> a(n), b(n), c(n);
	for (int i = 0; i < n; i++) {
		cin >> a[i];
	}
	for (int i = 0; i < n; i++) {
		cin >> b[i];
	}
	for (int i = 0; i < n; i++) {
		cin >> c[i];
	}
	cout << a[0] << " ";
	int last = a[0];
	for (int i = 1; i + 1 < n; i++) {
		if (a[i] == last) {
			cout << b[i] << " ";
			last = b[i];
		} else {
			cout << a[i] << " ";
			last = a[i];
		}
	}
	for (int i: {a[n - 1], b[n - 1], c[n - 1]}) {
		if (i != a[0] && i != last) {
			cout << i << "\n";
			return;
		}
	}
}
 
 
