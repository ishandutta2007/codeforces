bool check(string s) {
	for(int i = 0; i + 1 < s.size(); ++i) {
		if (abs(s[i] - s[i + 1]) == 1)
			return false;
	}
	return true;
}

void solve() {
	string s;
	cin >> s;
	vector<int> cnt(26);
	for(auto i: s) 
		cnt[i - 'a']++;
	{
		string tmp;
		for(int i = 0; i < 26; i += 2) {
			for(int j = 0; j < cnt[i]; ++j) 
				tmp += (char)('a' + i);
		}
		for(int i = 1; i < 26; i += 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		// cout << tmp << '\n';
		if (check(tmp)) {
			cout << tmp << '\n';
			return;
		}
	}
	{
		string tmp;
		for(int i = 0; i < 26; i += 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		for(int i = 25; i >= 0; i -= 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		if (check(tmp)) {
			cout << tmp << '\n';
			return;
		}
	}
	{
		string tmp;
		for(int i = 24; i >= 0; i -= 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		for(int i = 25; i >= 0; i -= 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		if (check(tmp)) {
			cout << tmp << '\n';
			return;
		}
	}
	{
		string tmp;
		for(int i = 24; i >= 0; i -= 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		for(int i = 1; i < 26; i += 2) {
			for(int j = 0; j < cnt[i]; ++j)
				tmp += (char)('a' + i);
		}
		if (check(tmp)) {
			cout << tmp << '\n';
			return;
		}
	}
	cout << "No answer\n";
}

