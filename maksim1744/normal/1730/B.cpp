void test_case(int test) {
    int n;
    cin >> n;
    vector<ll> x(n);
    vector<ll> t(n);
    cin >> x >> t;

    ld L = -1, R = 1e12;
    long double ans = 0;
    for (int i = 0; i < 100; ++i) {
        ld C = (L + R) / 2;
        ld minr = 1e18, maxl = -1e18;

        bool ok = true;

        for (int i = 0; i < n; ++i) {
            if (t[i] > C) {
                ok = false;
                break;
            }
            minr = min(minr, x[i] + (C - t[i]));
            maxl = max(maxl, x[i] - (C - t[i]));
        }
        if (!ok || maxl > minr) {
            L = C;
        } else {
            R = C;
            ans = (long double)(minr + maxl) / 2.;
        }
    }
    cout << fixed << setprecision(20) << ans << '\n';
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);

    int T;
    cin >> T;
    for (int test = 1; test <= T; ++test) {
        test_case(test);
    }

    return 0;
}