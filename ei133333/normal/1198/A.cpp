template< typename F >
struct FixPoint : F {
  FixPoint(F &&f) : F(forward< F >(f)) {}

  template< typename... Args >
  decltype(auto) operator()(Args &&... args) const {
    return F::operator()(*this, forward< Args >(args)...);
  }
};

template< typename F >
inline decltype(auto) MFP(F &&f) {
  return FixPoint< F >{forward< F >(f)};
}


int main() {
  int64 N, I;
  cin >> N >> I;
  I *= 8;

  int64 sz = 1, ng = N + 1;
  auto check = [&](int64 K) {
    int64 k = 0;
    while((1LL << k) < K) ++k;
    return k * N <= I;
  };
  while(ng - sz > 1) {
    int64 md = (sz + ng) / 2;
    if(check(md)) sz = md;
    else ng = md;
  }

  map< int, int > mp;
  for(int i = 0; i < N; i++) {
    int x;
    cin >> x;
    mp[x]++;
  }
  mp[-1] = 0;
  vector< pair< int, int64 > > A(mp.begin(), mp.end());

  for(int i = 1; i < A.size(); i++) {
    A[i].second += A[i - 1].second;
  }
  int ret = 0;
  for(int i = 0; i < A.size(); i++) {
    chmax(ret, A[i].second - A[max(0LL, i - sz)].second);
  }
  cout << N - ret << endl;
}