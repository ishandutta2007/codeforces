template<typename T> struct simple_queue {
  vector<T> a;
  int p = 0;
  void pop() {
    assert(p < int(a.size()));
    ++p;
  }

  void push(T x) {
    a.push_back(x);
  }

  bool empty() {
    return p == int(a.size());
  }

  T front() {
    return a[p];
  }

  void clear() {
    p = 0;
    a.clear();
  }
};

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<int> a(n);
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
  }

  const int B = 20;
  int ans = 0;
  vector<int> val(n);
  vector<int> push(n);
  vector<array<simple_queue<int>, 2>> que(1 << B);
  for (int b = B - 1; b >= 0; --b) {
    int cnt = 0;
    int lst = -1;
    for (int i = 0; i < n; ++i) {
      int ch = 0;
      if ((a[i] >> b) & 1) {
        cnt ^= 1;
        ch = 1;
      } else {
        lst = i;      
      }
      while (!que[val[i]][i & 1].empty() && que[val[i]][i & 1].front() <= lst) {
        que[val[i]][i & 1].pop();
      }

      if (!que[val[i]][i & 1].empty()) {
        ans = max(ans, i - que[val[i]][i & 1].front() + 1);
      }
      que[val[i] ^ push[i]][~i & 1].push(i);
      val[i] |= (cnt << b);
      push[i] |= (ch << b);
    }

    for (int i = 0; i < (1 << B); ++i) {
      que[i][0].clear();
      que[i][1].clear();
    }
  }

  cout << ans << '\n';
}