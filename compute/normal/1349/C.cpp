const int maxn=1e3+7;
string maze[maxn];
int sz[maxn*maxn];
int fa[maxn*maxn];
int Find(int x)
{
    return x==fa[x]?x:fa[x]=Find(fa[x]);
}
void Union(int x,int y)
{
    x=Find(x),y=Find(y);
    if(x!=y)
    {
        fa[y]=x;
        sz[x]+=sz[y];
    }
}
int dx[]={0,1,0,-1},dy[]={1,0,-1,0};
int vis[maxn][maxn][2];
struct D{
    int x,y,c;
};
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,m,t;
    cin>>n>>m>>t;
    auto getid=[&](int x,int y){
        return x*m+y;
    };
    for(int i=0;i<maxn*maxn;i++) fa[i]=i,sz[i]=1;
    for(int i=0;i<n;i++) cin>>maze[i];
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
            for(int k=0;k<4;k++)
            {
                int nx=i+dx[k],ny=j+dy[k];
                if(nx>=0&&ny>=0&&nx<n&&ny<m&&maze[i][j]==maze[nx][ny])
                    Union(getid(i,j),getid(nx,ny));
            }
        }
    }
    memset(vis,-1,sizeof(vis));
    queue<D> q;
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
            if(sz[Find(getid(i,j))]>1)
            {
                q.push(D{i,j,maze[i][j]-'0'});
                vis[i][j][maze[i][j]-'0']=0;
            }
        }
    }
    while(!q.empty())
    {
        auto u=q.front();q.pop();
        for(int k=0;k<4;k++)
        {
            int nx=u.x+dx[k],ny=u.y+dy[k];
            if(nx>=0&&ny>=0&&nx<n&&ny<m&&vis[nx][ny][u.c^1]==-1)
            {
                vis[nx][ny][u.c^1]=vis[u.x][u.y][u.c]+1;
                q.push(D{nx,ny,u.c^1});
            }
        }
    }
    while(t--)
    {
        int i,j;
        ll p;
        cin>>i>>j>>p;
        i--,j--;
        int T=vis[i][j][maze[i][j]-'0'];
        if(p<=T||T==-1)
            cout<<maze[i][j]<<'\n';
        else{
            //dbg(x,y);
            int ans=(p-T)&1^(maze[i][j]-'0');
            print(ans);
        }
    }

}