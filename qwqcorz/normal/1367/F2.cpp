#include<bits/stdc++.h>
using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0)
	{
		putchar('-');
		x=-x;
	}
	if (x<10) putchar(x+'0');
	else
	{
		write(x/10);
		putchar(x%10+'0');
	}
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N],b[N],l[N],r[N],last[N],tot[N],dp[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		int n=read(),ans=0;
		for (int i=1;i<=n;i++) b[i]=a[i]=read();
		for (int i=1;i<=n;i++) tot[i]=last[i]=l[i]=r[i]=0;
		sort(b+1,b+1+n);
		int cnt=unique(b+1,b+1+n)-b-1;
		for (int i=1;i<=n;i++) a[i]=lower_bound(b+1,b+1+cnt,a[i])-b;
		dp[0]=0;
		r[0]=n+1;
		for (int i=1;i<=n;i++) r[a[i]]=i;
		for (int i=n;i>=1;i--) l[a[i]]=i;
		for (int i=1;i<=n;i++)
		{
			dp[i]=max(dp[last[a[i]]],tot[a[i]-1])+1;
			if (r[a[i]-1]<=i) dp[i]=max(dp[i],dp[l[a[i]-1]]+tot[a[i]-1]);
			last[a[i]]=i;
			tot[a[i]]++;
			ans=max(ans,dp[i]);
		}
		print(n-ans);
	}
	
	return 0;
}
