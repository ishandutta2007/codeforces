using namespace std;

inline int read () {
	int ret = 0, t = 1;
	char c = getchar();
	while ((c < '0' || c > '9') && c != '-') c = getchar();
	if (c == '-') t = -1, c = getchar();
	while (c >= '0' && c <= '9') ret = ret * 10 + c - '0', c = getchar();
	return ret * t;
}

const int MAXN = 4004000;
int n, mod;

int t[MAXN];

int main () {
	n = read(), mod = read();
	int pre = 0, i, j; t[1] = 1, t[2] = -1;
	for (i = 1; i <= n; i++) {
		pre = (t[i] + pre) % mod; int now = pre;
		for (j = 2; j * i <= n; j++) { t[j * i] = (now + t[j * i]) % mod; if (j * (i + 1) <= n) t[j * (i + 1)] = (t[j * (i + 1)] - now) % mod; }
		pre = (pre + now) % mod;
		if (i == n) { printf("%d\n", (now + mod) % mod); return 0; }
	}
	return 0;
}