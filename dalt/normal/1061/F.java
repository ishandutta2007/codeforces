    static class FLostRoot {
        FastInput in;
        FastOutput out;
        int n;

        public void solve(int testNumber, FastInput in, FastOutput out) {
            this.in = in;
            this.out = out;

            n = in.readInt();
            int k = in.readInt();

            DigitBase base = new DigitBase(k);
            int h = base.ceilLog(n * (k - 1) + 1) - 1;

            RandomWrapper wrapper = new RandomWrapper();
            int leafA = 0;
            int leafB = 0;

            for (int i = 1; i <= n; i++) {
                if (isLeaf(i)) {
                    leafA = i;
                    break;
                }
            }

            IntegerList path = null;
            for (int i = leafA + 1; i <= n; i++) {
                if (isLeaf(i)) {
                    leafB = i;
                    path = getPath(leafA, leafB);
                    if (path.size() == h + h + 1) {
                        break;
                    }
                }
            }
            int[] nodes = path.toArray();

            int end = leafA;
            CompareUtils.quickSort(nodes, (a, b) -> query(end, a, b) ? -1 : 1, 0, nodes.length);
            int root = nodes[h];
            out.printf("! %d", root);
            out.flush();
        }

        public IntegerList getPath(int a, int c) {
            IntegerList ans = new IntegerList();
            for (int i = 1; i <= n; i++) {
                if (query(a, i, c)) {
                    ans.add(i);
                }
            }
            return ans;
        }

        public boolean isLeaf(int x) {
            int a = x == 1 ? 2 : 1;
            for (int i = 1; i <= n; i++) {
                if (i == a || i == x) {
                    continue;
                }
                if (query(a, x, i)) {
                    return false;
                }
            }
            return true;
        }

        public boolean query(int a, int b, int c) {
            out.printf("? %d %d %d", a, b, c).println();
            out.flush();
            return in.readString().equals("Yes");
        }

    }

    static class LongList implements Cloneable {
        private int size;
        private int cap;
        private long[] data;
        private static final long[] EMPTY = new long[0];

        public LongList(int cap) {
            this.cap = cap;
            if (cap == 0) {
                data = EMPTY;
            } else {
                data = new long[cap];
            }
        }

        public LongList(LongList list) {
            this.size = list.size;
            this.cap = list.cap;
            this.data = Arrays.copyOf(list.data, size);
        }

        public LongList() {
            this(0);
        }

        public void ensureSpace(int req) {
            if (req > cap) {
                while (cap < req) {
                    cap = Math.max(cap + 10, 2 * cap);
                }
                data = Arrays.copyOf(data, cap);
            }
        }

        private void checkRange(int i) {
            if (i < 0 || i >= size) {
                throw new ArrayIndexOutOfBoundsException();
            }
        }

        public void add(long x) {
            ensureSpace(size + 1);
            data[size++] = x;
        }

        public void addAll(long[] x, int offset, int len) {
            ensureSpace(size + len);
            System.arraycopy(x, offset, data, size, len);
            size += len;
        }

        public void addAll(LongList list) {
            addAll(list.data, 0, list.size);
        }

        public long tail() {
            checkRange(0);
            return data[size - 1];
        }

        public long[] toArray() {
            return Arrays.copyOf(data, size);
        }

        public String toString() {
            return Arrays.toString(toArray());
        }

        public boolean equals(Object obj) {
            if (!(obj instanceof LongList)) {
                return false;
            }
            LongList other = (LongList) obj;
            return SequenceUtils.equal(data, 0, size - 1, other.data, 0, other.size - 1);
        }

        public int hashCode() {
            int h = 1;
            for (int i = 0; i < size; i++) {
                h = h * 31 + Long.hashCode(data[i]);
            }
            return h;
        }

        public LongList clone() {
            LongList ans = new LongList();
            ans.addAll(this);
            return ans;
        }

    }

    static class SequenceUtils {
        public static void swap(int[] data, int i, int j) {
            int tmp = data[i];
            data[i] = data[j];
            data[j] = tmp;
        }

        public static boolean equal(int[] a, int al, int ar, int[] b, int bl, int br) {
            if ((ar - al) != (br - bl)) {
                return false;
            }
            for (int i = al, j = bl; i <= ar; i++, j++) {
                if (a[i] != b[j]) {
                    return false;
                }
            }
            return true;
        }

        public static boolean equal(long[] a, int al, int ar, long[] b, int bl, int br) {
            if ((ar - al) != (br - bl)) {
                return false;
            }
            for (int i = al, j = bl; i <= ar; i++, j++) {
                if (a[i] != b[j]) {
                    return false;
                }
            }
            return true;
        }

    }

