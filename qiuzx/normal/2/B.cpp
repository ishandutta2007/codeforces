#include <bits/stdc++.h>
#define N 1010
using namespace std;
long long n,x,a,b,dp[N][N][2],i,j,z1=-1,z2=-1;
string res="";
int main(){
	scanf("%d",&n);
	for(i=0;i<n;i++)
	{
		for(j=0;j<n;j++)
		{
			scanf("%d",&x);
			if(x==0)
			{
				z1=i;
				z2=j;
				if(i==0&&j==0)
				{
					dp[0][0][0]=1; 
					dp[0][0][1]=1;
				}
				else if(i==0)
				{
					dp[i][j][0]=dp[i][j-1][0]+1;
					dp[i][j][1]=dp[i][j-1][1]+1;
				}
				else if(j==0)
				{
					dp[i][j][0]=dp[i-1][j][0]+1;
					dp[i][j][1]=dp[i-1][j][1]+1;
				}
				else
				{
					dp[i][j][0]=min(dp[i-1][j][0],dp[i][j-1][0])+1;
					dp[i][j][1]=min(dp[i-1][j][1],dp[i][j-1][1])+1;
				}
			}
			else
			{
				a=0;
				b=0;
				while(x%2==0)
				{
					x/=2;
					a++;
				}
				while(x%5==0)
				{
					x/=5;
					b++;
				}
				if(i==0&&j==0)
				{
					dp[0][0][0]=a; 
					dp[0][0][1]=b;
				}
				else if(i==0)
				{
					dp[i][j][0]=dp[i][j-1][0]+a;
					dp[i][j][1]=dp[i][j-1][1]+b;
				}
				else if(j==0)
				{
					dp[i][j][0]=dp[i-1][j][0]+a;
					dp[i][j][1]=dp[i-1][j][1]+b;
				}
				else
				{
					dp[i][j][0]=min(dp[i-1][j][0],dp[i][j-1][0])+a;
					dp[i][j][1]=min(dp[i-1][j][1],dp[i][j-1][1])+b;
				}
			}
		}
	}
	if(z1!=-1)
	{
		printf("%d\n",min(min(dp[n-1][n-1][0],dp[n-1][n-1][1]),(long long)1));
	}
	else
	{
		printf("%d\n",min(dp[n-1][n-1][0],dp[n-1][n-1][1]));
	}
	i=j=n-1;
	if(z1!=-1&&min(dp[n-1][n-1][0],dp[n-1][n-1][1])>0)
	{
		for(i=1;i<=z1;i++)
		{
			printf("D");
		}
		for(j=1;j<n;j++)
		{
			 printf("R");
		}
		for(;i<n;i++)
		{
			printf("D");
		}
		printf("\n");
		return 0;
	}
	if(dp[n-1][n-1][0]<dp[n-1][n-1][1])
	{
		while(i||j)
		{
			if(i&&j)
			{
				if(dp[i-1][j][0]<dp[i][j-1][0])
				{
					res='D'+res;
					i--;
				}
				else
				{
					res='R'+res;
					j--;
				}
			}
			else if(j)
			{
				res='R'+res;
				j--;
			}
			else
			{
				res='D'+res;
				i--;
			}
		}
	}
	else
	{
		while(i||j)
		{
			if(i&&j)
			{
				if(dp[i-1][j][1]<dp[i][j-1][1])
				{
					res='D'+res;
					i--;
				}
				else
				{
					res='R'+res;
					j--;
				}
			}
			else if(j)
			{
				res='R'+res;
				j--;
			}
			else
			{
				res='D'+res;
				i--;
			}
		}
	}
	printf("%s",res.c_str());
	return 0;
}