	void submit() {
		int r = nextInt();
		int c = nextInt();
		int[] low = new int[r];
		int[] high = new int[c];

		for (int i = 0; i < r; i++) {
			low[i] = nextInt();
		}

		for (int j = 0; j < c; j++) {
			high[j] = nextInt();
		}

		int maxLow = Arrays.stream(low).max().getAsInt();

		int minHigh = Arrays.stream(high).min().getAsInt();

		if (maxLow > minHigh) {
			out.println(-1);
			return;
		}

		long sumLow = 0;
		for (int x : low) {
			sumLow += x;
		}

		long sumHigh = 0;
		for (int x : high) {
			sumHigh += x;
		}

		long ret = c * (sumLow - maxLow) + sumHigh;
		
		if (maxLow != minHigh) {
			int max1 = -1;
			int max2 = -1;
			for (int x : low) {
				if (x > max1) {
					max2 = max1;
					max1 = x;
				} else if (x > max2) {
					max2 = x;
				}
			}
			ret += max1 - max2;
		}
		
		out.println(ret);
	}

