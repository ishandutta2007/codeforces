void solve(){
	int n,a,b;
	scanf("%d%d%d",&n,&a,&b);
	bool win=0;
	for(int i=0;i<a;i++){
		int x;
		scanf("%d",&x);
		if(x==n){
			win=1;
		}
	}
	for(int i=0;i<b;i++){
		int x;
		scanf("%d",&x);
	}
	puts(win?"YES":"NO");
}

