void run_case() {
    int N, K;
    cin >> N >> K;
    vector<int> H(N);

    for (auto &h : H)
        cin >> h;

    array<int, 2> range = {H[0], H[0] + 1};

    for (int i = 1; i < N; i++) {
        range[0] = max(range[0] - (K - 1), H[i]);
        range[1] = min(range[1] + K - 1, H[i] + K);

        if (range[0] >= range[1])
            break;
    }

    cout << (range[0] <= H.back() && H.back() < range[1] ? "YES" : "NO") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}