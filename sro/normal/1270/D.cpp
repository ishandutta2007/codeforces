#include <bits/stdc++.h>
using namespace std;

int n,m;
int val[505];
int qry[505];

void ask(){
	printf("?");
	for(int i=1;i<=m;i++){
		printf(" %d",qry[i]);
	}
	printf("\n");
	fflush(stdout);
}

int main(){
	scanf("%d%d",&n,&m);
	memset(val,-1,sizeof(val));
	for(int i=1;i<=m;i++){
		qry[i]=i;
	}
	for(int i=m;i<=n;i++){
		ask();
		int x,y;
		scanf("%d%d",&x,&y);
		val[x]=y;
		for(int j=1;j<=m;j++){
			if(qry[j]==x){
				qry[j]=i+1;
			}
		}
	}
	int X=-1,Y;
	for(int i=1;i<=n;i++){
		for(int j=1;j<=n;j++){
			if(val[i]!=-1&&val[j]!=-1&&val[i]<val[j]&&X==-1){
				X=i;
				Y=j;
			}
		}
	}
	for(int i=1;i<=m;i++){
		if(qry[i]==n+1){
			qry[i]=Y;
		}
	}
	int cnt=1;
	for(int i=1;i<=m;i++){
		if(qry[i]==Y){
			continue;
		}
		int ini=qry[i];
		qry[i]=X;
		ask();
		int x,y;
		scanf("%d%d",&x,&y);
		if(x==Y){
			cnt++;
		}
		qry[i]=ini;
	}
	printf("! %d\n",cnt);
	fflush(stdout);
	return 0;
}