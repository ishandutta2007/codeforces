#include <bits/stdc++.h>
using namespace std;

namespace Suffix_Automaton{
	struct State{
		int len,lnk;
		int nxt[26];
	}st[10005];
	int sz,np;
	void init(){
		sz++;
		np=1;
	}
	void addch(int c){
		int p=np;
		np=++sz;
		st[np].len=st[p].len+1;
		while(p&&!st[p].nxt[c]){
			st[p].nxt[c]=np;
			p=st[p].lnk;
		}
		if(!p){
			st[np].lnk=1;
		}else{
			int q=st[p].nxt[c];
			if(st[p].len+1==st[q].len){
				st[np].lnk=q;
			}else{
				int nq=++sz;
				st[nq]=st[q];
				st[q].lnk=st[np].lnk=nq;
				st[nq].len=st[p].len+1;
				while(p&&st[p].nxt[c]==q){
					st[p].nxt[c]=nq;
					p=st[p].lnk;
				}
			}
		}
	}
}

using namespace Suffix_Automaton;

char t[10005],sb[10005],se[10005];
int n,lb,le;
vector<int> son[10005];
int tmc,dfi[10005],dfo[10005];
bool lgl[10005];
int dp[10005][2005];

void dfs(int now){
	dfi[now]=++tmc;
	for(int i=0;i<son[now].size();i++){
		dfs(son[now][i]);
	}
	dfo[now]=tmc;
}

int dfs2(int now,int len,int nowt){
	if(~dp[now][len]){
		return dp[now][len];
	}
	if(dfi[now]>=dfi[nowt]&&dfo[now]<=dfo[nowt]&&len>=le-lb){
		dp[now][len]=1;
	}else{
		dp[now][len]=0;
	}
	for(int i=0;i<26;i++){
		int to=st[now].nxt[i];
		if(to){
			dp[now][len]+=dfs2(to,len+1,nowt);
		}
	}
	return dp[now][len];
}

int main(){
	scanf("%s",t+1);
	n=strlen(t+1);
	init();
	for(int i=1;i<=n;i++){
		addch(t[i]-'a');
	}
	int nows=1;
	scanf("%s",sb+1);
	scanf("%s",se+1);
	le=strlen(se+1);
	lb=strlen(sb+1);
	for(int i=1;sb[i];i++){
		nows=st[nows].nxt[sb[i]-'a'];
	}
	if(!nows){
		puts("0");
		return 0;
	}
	for(int i=2;i<=sz;i++){
		son[st[i].lnk].push_back(i);
	}
	dfs(1);
	int nowt=1;
	for(int i=1;se[i];i++){
		nowt=st[nowt].nxt[se[i]-'a'];
	}
	memset(dp,-1,sizeof(dp));
	printf("%d\n",dfs2(nows,0,nowt));
	return 0;
}