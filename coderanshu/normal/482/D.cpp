const int N = 100005;

vector<int> v[N];

Mint dp[N][2];

void dfs(int s)
{
    int n = sz(v[s]);

    vector<vector<Mint>> new_dp(n+1,vector<Mint>(2,0)),dp2 = new_dp;
    new_dp[0][0] = 1;
    dp2[0][0] = 1;
    Mint only_even = 1,only_odd = 1;
    for(int i=0;i<sz(v[s]);++i)
    {
        dfs(v[s][i]);
        for(int j=0;j<2;++j)
        {
            for(int k=0;k<2;++k)
                new_dp[i+1][(j+k)%2] += dp[v[s][i]][j] * new_dp[i][k];
            new_dp[i+1][j] += new_dp[i][j];
        }
        only_even *= 1 + dp[v[s][i]][0];
        for(int k=0;k<2;++k)
        {
            dp2[i+1][k] += dp2[i][k];
            dp2[i+1][k^1] += dp2[i][k] * dp[v[s][i]][1];
        }
    }
    --only_even;
    --new_dp[n][0];
    --dp2[n][0];
    dp[s][1] += 2 * new_dp[n][0] - only_even + 1;
    dp[s][0] += 2 * new_dp[n][1] - dp2[n][1];
}

