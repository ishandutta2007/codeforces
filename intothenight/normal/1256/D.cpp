mt19937 rnd(time(0));
 
 
 
int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int qq;
	cin >> qq;
	while(qq --){
		int n;
		ll k;
		cin >> n >> k;
		string s;
		cin >> s;
		vi pos;
		for(int i = 0; i < n; i ++){
			if(s[i] == '0'){
				pos.push_back(i);
			}
		}
		int m = pos.size();
		for(int i = 0; i < m && k; i ++){
			if(pos[i] - i <= k){
				k -= pos[i] - i;
				pos[i] = i;
			}
			else{
				pos[i] -= k;
				k = 0;
			}
		}
		string res(n, '1');
		for(auto x: pos){
			res[x] = '0';
		}
		cout << res << "\n";
	}
	return 0;
}
 
