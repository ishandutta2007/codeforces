#include <bits/stdc++.h>

using namespace std;

const int maxn = 1e4;
bool bl[maxn];

int main (){
    ios_base::sync_with_stdio(false);
    int n, m, a, b, sum = 0;
    cin >> n >> m >> a >> b;
    double x;
    x = b / m;
    if (x >= a)
        return cout << n * a, 0;
    int k = n % m;
    n /= m;
    sum += n * b;
    int g = min(k * a, b);
    sum += g;
    cout << sum;
}