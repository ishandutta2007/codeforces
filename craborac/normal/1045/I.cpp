void solve() {
	int n;
	cin >> n;
	map<int, int> ma;
	for (int i = 0; i < n; i++)
	{
		string s;
		cin >> s;
		int cur = 0;
		for (char c: s)
			cur ^= (1 << (c - 'a'));
		ma[cur] += 1;
	}
	ll ans = 0;
	for (auto p: ma)
	{
		ans += p.second * (p.second - 1ll) / 2ll;
		for (int i = 0; i < 26; i++)
		{
			if (p.first & (1 << i))
				continue;
			if (ma.count(p.first + (1 << i)))
				ans += (ll)p.second * ma[p.first + (1 << i)];
		}
	}
	cout << ans << endl;
}
 
 
