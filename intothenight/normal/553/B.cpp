int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	vl f(51);
	f[0] = f[1] = 1;
	for(int i = 2; i <= 50; i ++){
		f[i] = f[i - 1] + f[i - 2];
	}
	int n;
	ll k;
	cin >> n >> k;
	for(int i = 1; i <= n; ){
		if(k <= f[n - i]){
			cout << i << " ";
			i ++;
		}
		else{
			k -= f[n - i];
			cout << i + 1 << " " << i << " ";
			i += 2;
		}
	}
	return 0;
}
