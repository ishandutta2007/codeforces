int n, m, k, u, v, x, y, t, a, b, root;
int h[MAXN];
vector<int> G[MAXN];
priority_queue<pii, vector<pii>, greater<pii>> st[MAXN];

void dfs(int node, int p){
	h[node]=h[p]+1;
	if (G[node].size()==1){
		st[node].push({h[node], node});
		return ;
	}
	for (int i:G[node]) if (i!=p){
		dfs(i, node);
		if (st[node].size()<st[i].size()) st[node].swap(st[i]);
	}
	for (int i:G[node]) if (i!=p){
		while (!st[node].empty() && !st[i].empty()){
			int u=(st[node].top()).second, v=(st[i].top()).second;
			if (h[u]+h[v]-2*h[node]<=k){
				if (h[u]<h[v]) st[node].pop();
				else st[i].pop();
			}
			else break ;
		}
		while (!st[i].empty()){
			st[node].push(st[i].top());
			st[i].pop();
		}
	}
}

