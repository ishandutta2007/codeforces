struct DSU {
	vector<int> p;
	vector<int> mx;
	DSU(int n) {
		p.resize(n + 5);
		mx.resize(n + 5, 1);
		for(int i = 0; i < n + 5; ++i) 
			p[i] = i;
	}

	int get(int a) {
		if (a == p[a])
			return a;
		return p[a] = get(p[a]);
	}

	void merge(int a, int b) {
		a = get(a);
		b = get(b);
		if (a == b)
			return;
		p[a] = b;
		mx[b] = mx[a] + mx[b];
	}
	int operator[] (int a) {
		return mx[a];
	}
};

void res(int a, vector<int> &ans, vector<pair<int, int>> &who) {
	if (ans[a] != -1)
		return;
	res(who[a].first, ans, who);
	ans[a] = ans[who[a].first] - who[a].second;
}

signed main() {
	setup();
	
	int n;
	cin >> n;
	DSU d(n);
	vector<int> ans(n + 1, -1);
	vector<pair<int, int>> who(n + 1);
	for(int i = 0; i < n - 1; ++i) {
		int a, b;
		cin >> a >> b;
		a = d.get(a), b = d.get(b);
		if (d[a] > d[b])
			swap(a, b);
		who[a] = {b, d[b]};
		d.merge(a, b);
	}
	ans[d.get(1)] = n;
	for(int i = 1; i <= n; ++i)
		res(i, ans, who);
	vector<int> ANS(n);
	for(int i = 0; i < n; ++i) 
		ANS[ans[i + 1] - 1] = i + 1;
	for(auto i: ANS)
		cout << i << ' ';
	cout << '\n';


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

