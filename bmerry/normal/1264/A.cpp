int main(int argc, const char **argv)
{
    redirect(argc, argv);
    int T;
    cin >> T;
    for (int cas = 0; cas < T; cas++)
    {
        int N;
        cin >> N;
        vi groups;
        int last = -1;
        for (int i = 0; i < N; i++)
        {
            int p;
            cin >> p;
            if (p != last)
                groups.emplace_back(1);
            else
                groups.back()++;
            last = p;
        }
        int g = 0, b = 0, s = 0;
        int G = SZ(groups);
        if (G >= 4)
        {
            g = groups[0];
            int q = 1;
            while (q < G && s <= g)
                s += groups[q++];
            while (q < G && b <= g)
                b += groups[q++];
            while (q < G && b + s + g + groups[q] <= N / 2)
                b += groups[q++];
            if (g + s + b > N / 2)
                g = s = b = 0;
        }
        cout << g << ' ' << s << ' ' << b << '\n';
    }

    return 0;
}