const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

ll C2(ll x){ return x*(x-1)/2;}
ll C3(ll x){ return x*(x-1)*(x-2)/6;}

ll n, m, q, u, v, x, y, t, r, w;
pii E[MAXN];
ll W[MAXN];
ll val[MAXN];
vector<pii> G[MAXN];

ll dfs(int node, int par, int id){
	ll res=1;
	for (pii p:G[node]){
		int v=p.first, idd=p.second;
		if (v==par) continue ;
		res+=dfs(v, node, idd);
	}
	val[id]=2ll*(res*C2(n-res)+(n-res)*C2(res));
	return res;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n-1; i++){
		cin>>u>>v>>W[i];
		G[u].pb({v, i});
		G[v].pb({u, i});
	}
	dfs(1, 0, 0);
	ld ans=0;
	for (int i=1; i<=n-1; i++) ans+=W[i]*val[i];
	ld tmp=C3(n);
	setprecision(10);
	cin>>q;
	while (q--){
		cin>>r>>w;
		ans-=W[r]*val[r];
		W[r]=w;
		ans+=W[r]*val[r];
		cout<<fixed<<ans/tmp<<'\n';
	}
	
	return 0;
}
