	public static class DJSet { public int[] upper; public DJSet(int n){ upper = new int[n]; Arrays.fill(upper, -1);} public int root(int x){	return upper[x] < 0 ? x : (upper[x] = root(upper[x]));}	public boolean equiv(int x, int y){ return root(x) == root(y);} public void union(int x, int y){ x = root(x);y = root(y);if(x != y) { if(upper[y] < upper[x]) { int d = x; x = y; y = d;	} upper[x] += upper[y]; upper[y] = x;}} public int count(){	int ct = 0;	for(int i = 0;i < upper.length;i++){ if(upper[i] < 0)ct++; } return ct; }}
	
	void solve()
	{
		int n = ni(), m = ni();
		char[][] b = new char[n][];
		for(int i = 0;i < n;i++){
			b[i] = ns(m);
		}
		
		int alive = 0;
		for(int i = 0;i < n;i++){
			for(int j = 0;j < m;j++){
				if(b[i][j] == '#')alive++;
			}
		}
		
		if(alive <= 2){
			out.println(-1);
			return;
		}
		
		for(int x = 0;x < n;x++){
			for(int y = 0;y < m;y++){
				if(b[x][y] == '#'){
					b[x][y] = '.';
					DJSet ds = new DJSet(n*m);
					for(int i = 0;i < n;i++){
						for(int j = 0;j < m-1;j++){
							if(b[i][j] == '#' && b[i][j+1] == '#'){
								ds.union(i*m+j, i*m+j+1);
							}
						}
					}
					for(int i = 0;i < n-1;i++){
						for(int j = 0;j < m;j++){
							if(b[i][j] == '#' && b[i+1][j] == '#'){
								ds.union(i*m+j, (i+1)*m+j);
							}
						}
					}
					if(ds.count() != n*m-alive+2){
						out.println(1);
						return;
					}
					b[x][y] = '#';
				}
			}
		}
		
		out.println(2);
	}
	
