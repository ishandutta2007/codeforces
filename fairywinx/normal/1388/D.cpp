const int N = 2e5 + 228;

vector<int> G[N];

signed main() {
	setup();
	
	int n;
	cin >> n;
	vector<int> a(n);
	vector<int> b(n);
	for(int i = 0; i < n; ++i)
		cin >> a[i];
	vector<int> kol(n);
	for(int i = 0; i < n; ++i) {
		cin >> b[i];
		--b[i];
		if (b[i] != -2) {
			G[i].push_back(b[i]);
			++kol[b[i]];
		}
	}
	set<pair<int, int>> z;
	for(int i = 0; i < n; ++i)
		z.emplace(kol[i], i);
	int ans = 0;
	vector<int> d;
	// for(auto i: z)
	// 	cout << i.first << ' ' << i.second << '\n';
	vector<int> _ans;
	while(z.size()) {
		auto c = *z.begin();
		// cout << c.first << ' ' << c.second << '\n';
		z.erase(c);
		bool po = false;
		if (G[c.second].size() != 0) {
			int to = G[c.second][0];
			if (a[c.second] >= 0)
				a[to] += a[c.second];
			else {
				po = true;
				_ans.push_back(c.second + 1);
			}
			if (z.count({kol[to], to})) {
				z.erase({kol[to], to});
				--kol[to];
				z.emplace(kol[to], to);
			}
		}
		if (!po)
			d.push_back(c.second + 1);
		ans += a[c.second];
	}
	cout << ans << '\n';	
	for(auto i: d)
		cout << i << ' ';
	for(int i = _ans.size(); i >= 1; --i)
		cout << _ans[i - 1] << ' ';
	
	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

