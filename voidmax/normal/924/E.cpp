typedef long long base;
typedef pair<base, base> point;

const string FILENAME = "input";

const int MAXN = 1e4 + 1;

int n, L, R;
vector<int> a, G, B;
bitset<MAXN> f[MAXN]; 

int main() {
	srand(time(0));
	ios::sync_with_stdio(false);
	cin >> n >> L >> R;
	a.resize(n);
	for (int i = 0; i < n; ++i) {
		cin >> a[i];
	}
	for (int i = 0; i < n; ++i) {
		int b;
		cin >> b;
		if (b) {
			G.pb(a[i]);
		} else {
			B.pb(a[i]);
		}
	}
	sort(G.begin(), G.end());
	f[G.size()][0] = 1; 
	for (int i : B) {
		f[G.size()] = f[G.size()] | (f[G.size()] << i);
	}
	for (int i = G.size(); i--;) {
		f[i] = f[i + 1] | (f[i + 1] << G[i]);
	}
	int ans = 0;
	for (int i = 0; i < G.size(); ++i) {
		bool OK = false;
		for (int j = L; j <= R; ++j) {
			if (f[i + 1][j]) {
				OK = true;
				break;
			}
		}
		if (OK) {
			++ans;
			R -= G[i];
		} else {
			L -= G[i];
			R -= G[i];
			chkmax(L, 0);
		}
	}
	cout << ans << endl;
}