bool hmm[2][bs + 20];

int qry(int n)
{
	int bucket = n / 150000;
	for(int i = 0; i < bs; i++)
	{
		hmm[0][i] = hmm[1][i] = false;
	}
	int l = bucket * 150000;
	for(int i = 2; i * i < l + bs; i++)
	{
		int pos = (l / i) * i;
		while(pos < l)
			pos += i;
		pos = std::max(pos, 2 * i);
		while(pos < l + bs)
		{
			hmm[0][pos - l] = true;
			pos += i;
		}
	}
	int start = 0;
	for(int i = 1; i * i < l + bs; i++)
	{
		while(i * i + start * start < l + bs)
			start++;
		while(i * i + start * start > l + bs)
			start--;
		int cur = start;
		while(i * i + cur * cur >= l && cur > 0)
		{
			hmm[1][i * i + cur * cur - l] = true;
			cur--; 
		}
	}
	int ans = pre[bucket];
	for(int i = l + 1; i <= n; i++)
	{
		if(hmm[0][i - l] == false && hmm[1][i - l] == true && (i > 2 || i == 2))
			ans++;
	}
	return ans;
}

