class TaskA {
    public void solve(int testNumber, FastScanner in, FastPrinter out) {
        int[] a = in.readIntArray(6);
        int first = 2 * a[0] - 1;
        int ans = 0;
        if (a[1] < a[5]) {
            int t = a[1];
            a[1] = a[5];
            a[5] = t;
            t = a[2];
            a[2] = a[4];
            a[4] = t;
        }
        for (int i = 0; i < a[5]; i++) {
            first += 2;
            ans += first;
        }
        for (int i = a[5]; i < Math.min(a[1], a[4] + a[5]); i++) {
            first++;
            ans += first;
            first--;
        }
        for (int i = Math.min(a[1], a[4] + a[5]); i < a[1] + a[2]; i++) {
            ans += first;
            first -= 2;
        }
        out.println(ans);
    }
}

class FastScanner extends BufferedReader {

    public FastScanner(InputStream is) {
        super(new InputStreamReader(is));
    }

    public int read() {
        try {
            int ret = super.read();
//            if (isEOF && ret < 0) {
//                throw new InputMismatchException();
//            }
//            isEOF = ret == -1;
            return ret;
        } catch (IOException e) {
            throw new InputMismatchException();
        }
    }

    static boolean isWhiteSpace(int c) {
        return c >= 0 && c <= 32;
    }

    public int nextInt() {
        int c = read();
        while (isWhiteSpace(c)) {
            c = read();
        }
        int sgn = 1;
        if (c == '-') {
            sgn = -1;
            c = read();
        }
        int ret = 0;
        while (c >= 0 && !isWhiteSpace(c)) {
            if (c < '0' || c > '9') {
                throw new NumberFormatException("digit expected " + (char) c
                        + " found");
            }
            ret = ret * 10 + c - '0';
            c = read();
        }
        return ret * sgn;
    }

    public String readLine() {
        try {
            return super.readLine();
        } catch (IOException e) {
            return null;
        }
    }

    public int[] readIntArray(int n) {
        int[] ret = new int[n];
        for (int i = 0; i < n; i++) {
            ret[i] = nextInt();
        }
        return ret;
    }

}

