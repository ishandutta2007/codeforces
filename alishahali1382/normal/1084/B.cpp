const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

ll n, s, u, v, x, y, t, a, b, ans, mn=inf;
ll A[MAXN];

bool check(ll x){
	ll res=0;
	for (int i=0; i<n; i++){
		if (A[i]<x) return 0;
		res+=A[i]-x;
	}
	return res>=s;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>s;
	for (int i=0; i<n; i++){
		cin>>A[i];
		t+=A[i];
	}
	if (s>t){
		cout<<-1<<endl;
		return 0;
	}
	ll dwn=0, up=inf;
	while (up-dwn>1){
		//cerr<<dwn<<' '<<up<<endl;
		ll mid=(up+dwn)/2;
		if (check(mid)) dwn=mid;
		else up=mid;
	}
	ans=dwn;
	if (check(ans+1)) ans++;
	cout<<ans<<endl;
	return 0;
}
