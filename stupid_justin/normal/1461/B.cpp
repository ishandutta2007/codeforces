#include<bits/stdc++.h>
#define int long long
#define rd(x) x=read()
using namespace std;
const int N=1005;
const int M=5005;
const int mod=998244353;
const int inf=0x3f3f3f3f3f3f3f;
inline int read(){int x=0,f=1;char ch=getchar();while(ch>'9'||ch<'0'){if(ch=='-')f=-1;ch=getchar();}while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}return x*f;}
int ksm(int x,int y){int ret=1;while (y){if (y&1) ret=(ret*x)%mod;x=(x*x)%mod;y>>=1;}return ret;}

int n,m,ans;
int l[N][N],r[N][N];
char ch[N][N];
signed main()
{
	int T;
	cin>>T;
	while(T--)
	{
		memset(l,0,sizeof(l));
		memset(r,0,sizeof(r));
		rd(n);rd(m);
		for (int i=1;i<=n;i++) for (int j=1;j<=m;j++) cin>>ch[i][j];
		for (int i=1;i<=n;i++) for (int j=1;j<=m;j++) if (ch[i][j]=='.') l[i][j]=0; else l[i][j]=l[i][j-1]+1;
		for (int i=1;i<=n;i++) for (int j=m;j>=1;j--) if (ch[i][j]=='.') r[i][j]=0; else r[i][j]=r[i][j+1]+1;
		for (int i=1;i<=n;i++) for (int j=1;j<=m;j++) 
		{
			if (ch[i][j]=='.') continue;
			l[i][j]=min(l[i][j],l[i-1][j]+1);
			r[i][j]=min(r[i][j],r[i-1][j]+1);
		}
		ans=0;
		for (int i=1;i<=n;i++) for (int j=1;j<=m;j++)
		{
			int Min=min(l[i][j],r[i][j]);
			for (int k=1;k<=Min;k++) if (l[i-1][j]>=k-1 && r[i-1][j]>=k-1) ans++;
		}
		cout<<ans<<endl;
	}
}