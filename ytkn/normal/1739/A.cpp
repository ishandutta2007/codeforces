void solve(){
    int n, m; cin >> n >> m;
    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            int x = max(abs(i), abs(n-i-1));
            int y = max(abs(j), abs(m-j-1));
            if(min(x, y) >= 1 && max(x, y) >= 2) continue;
            cout << i+1 << ' ' << j+1 << endl;
            return;
        }
    }
    cout << 1 << ' ' << 1 << endl;
}

