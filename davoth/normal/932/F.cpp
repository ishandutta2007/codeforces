void dfs(ll v, ll p){
    ans[v]=INF;
    if(adj[v].size()==1 && v>1) ans[v]=0;
    for(ll u : adj[v]){
        if(u==p) continue;
        dfs(u,v);
        ans[v]=min(ans[v],-cht[u].query(a[v]));
        if(cht[u].size()>cht[v].size()) swap(cht[u],cht[v]);
        for(auto l : cht[u]) cht[v].add(l.k,l.m);
    }
    cht[v].add(-b[v],-ans[v]);
}
