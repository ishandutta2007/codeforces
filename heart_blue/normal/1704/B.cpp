int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int ncase;
	scanf("%d", &ncase);
	while (ncase--)
	{
		int n, k;
		scanf("%d%d", &n, &k);
		int ans = 0;
		int maxv = -INF;
		int minv = INF;
		while (n--)
		{
			int x;
			scanf("%d", &x);
			maxv = max(maxv, x);
			minv = min(minv, x);
			if (maxv - minv > 2 * k) ans++, maxv = minv = x;
		}
		printf("%d\n", ans);
	}
	return 0;
}