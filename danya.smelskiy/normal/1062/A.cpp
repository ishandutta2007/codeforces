int n;
int a[1005];
int ans;


int main(int argc, const char * argv[]) {
//    freopen("/Users/danya.smelskiy/Documents/Danya/Resources/input.txt","r",stdin);
    ios_base::sync_with_stdio(false); cin.tie(0); cout.tie(0);
    cin>>n;
    for (int i=1;i<=n;++i) {
        cin>>a[i];
    }
    a[0]=0,a[n+1]=1001;
    for (int i=0;i<=n;++i) {
        for (int j=i+1;j<=n+1;++j) if (j-i-1!=n && a[j]-a[i]==j-i) ans=max(ans,j-i-1);
    }
    cout<<ans<<endl;
}