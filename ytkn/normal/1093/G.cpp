const int inf = 1e9;

int pos_inf(){
    return inf;
}

int neg_inf(){
    return -inf;
}

int calc_min(int x, int y){
    return min(x, y);
}

int calc_max(int x, int y){
    return max(x, y);
}

using SegMin = atcoder::segtree<int, calc_min, pos_inf>;
using SegMax = atcoder::segtree<int, calc_max, neg_inf>;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, k; cin >> n >> k;
    auto a = vec2d(n, k, 0);
    for(int i = 0; i < n; i++){
        for(int j = 0; j < k; j++){
            cin >> a[i][j];
        }
    }
    int q; cin >> q;
    vector<int> t(q), l(q), r(q), idx(q);
    auto b = vec2d(q, k, 0);
    for(int i = 0; i < q; i++){
        cin >> t[i];
        if(t[i] == 1){
            cin >> idx[i]; idx[i]--;
            for(int j = 0; j < k; j++){
                cin >> b[i][j];
            }
        }else{
            cin >> l[i] >> r[i]; l[i]--;
        }
    }
    vector<int> ans(q);
    for(int s = 0; s < (1<<k); s++){
        SegMin seg_min(n);
        SegMax seg_max(n);
        auto set_point = [&](int i, const vector<int> &p){
            int sum = 0;
            for(int j = 0; j < k; j++){
                if(s&(1<<j)) sum += p[j];
                else sum -= p[j];
            }
            seg_min.set(i, sum);
            seg_max.set(i, sum);
        };
        for(int i = 0; i < n; i++) set_point(i, a[i]);
        for(int i = 0; i < q; i++){
            if(t[i] == 1){
                set_point(idx[i], b[i]);
            }else{
                chmax(ans[i], seg_max.prod(l[i], r[i])-seg_min.prod(l[i], r[i]));
            }
        }
    }
    for(int i = 0; i < q; i++){
        if(t[i] == 2) cout << ans[i] << '\n';
    }
}