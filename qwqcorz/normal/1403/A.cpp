#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=1e6+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int gcd(int x,int y)
{
	return !y?x:gcd(y,x%y);
}
int a[N],n,ans=0,tot[N],d[N];
void check(int x)
{
	int cnt=0;
	for (int i=1;i*i<=x;i++) if (x%i==0) d[++cnt]=i;
	for (int i=sqrt(x);i>=1;i--) if (x%i==0&&i*i!=x) d[++cnt]=x/i;
	for (int i=1;i<=n;i++)
	tot[lower_bound(d+1,d+1+cnt,gcd(x,a[i]))-d]++;
	for (int i=1;i<=cnt;i++)
	for (int j=i+1;j<=cnt;j++)
	if (d[j]%d[i]==0) tot[i]+=tot[j];
	for (int i=1;i<=cnt;i++) if (tot[i]>=(n+1)/2) ans=max(ans,d[i]);
	for (int i=1;i<=cnt;i++) tot[i]=0;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	srand(114514);
	n=read();
	for (int i=1;i<=n;i++) a[i]=read();
	random_shuffle(a+1,a+1+n);
	for (int i=1;i<=min(n,10LL);i++) check(a[i]);
	print(ans);
	
	return 0;
}
