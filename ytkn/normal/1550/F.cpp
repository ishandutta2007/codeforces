template<typename T>
class Compress{
    public:
    vector<T> data;
    int offset;
    Compress(vector<T> data_, int offset=0): offset(offset){
        data = data_;
        sort(begin(data), end(data));
        data.erase(unique(begin(data), end(data)), end(data));
    };
    int operator[](T x) {
        auto p = lower_bound(data.begin(), data.end(), x);
        assert(x == *p);
        return offset+(p-data.begin());
	}
    T inv(int x){
        return data[x-offset];
    }
    int size(){
        return data.size();
    }
};

const int inf = 1e9;

struct S{
    int idx, val;
    S(int idx, int val): idx(idx), val(val) {}
};

S e(){
    return S(-1, inf);
}

S op(S x, S y){
    if(x.val < y.val) return x;
    return y;
}

S mapping(int f, S x){
    if(x.val == inf) return x;
    return S(x.idx, x.val+f);
}

int composition(int f, int g){
    return f+g;
}

int id(){
    return 0;
}

using Seg = atcoder::lazy_segtree<S, op, e, int, mapping, composition, id>;

const int d_max = 1e7;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, q, s, d; cin >> n >> q >> s >> d; s--;
    vector<int> a(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    Seg from_left(n);
    Seg from_right(n);
    set<int> visited_targets = {-d_max, d_max};
    for(int i = 0; i < n; i++){
        from_left.set(i, S(i, d_max+a[i]));
        from_right.set(i, S(i, d_max-a[i]));
    }
    from_left.set(s, S(s, 0));
    from_right.set(s, S(s, 0));
    auto visit_target = [&](int x){
        if(visited_targets.count(x)) return;
        auto p = visited_targets.upper_bound(x);
        int xr = *p;
        int xl = *prev(p);
        {
            int l = upper_bound(a.begin(), a.end(), xl) - a.begin();
            int r = upper_bound(a.begin(), a.end(), x) - a.begin();
            from_right.apply(l, r, x-xr);
        }
        {
            int l = lower_bound(a.begin(), a.end(), x) - a.begin();
            int r = upper_bound(a.begin(), a.end(), xr) - a.begin();
            from_left.apply(l, r, xl-x);
        }
        visited_targets.insert(x);
    };
    int rem = n;
    vector<int> min_k(n);
    auto visit_point = [&](int idx, int k){
        rem--;
        min_k[idx] = k;
        from_left.set(idx, e());
        from_right.set(idx, e());
        visit_target(a[idx]-d);
        visit_target(a[idx]+d);
    };
    int cur_k = 0;
    while(rem){
        auto sl = from_left.all_prod();
        auto sr = from_right.all_prod();
        if(sl.val < sr.val){
            chmax(cur_k, sl.val);
            visit_point(sl.idx, cur_k);
        }else{
            chmax(cur_k, sr.val);
            visit_point(sr.idx, cur_k);
        }
    }
    while(q--){
        int i, k; cin >> i >> k; i--;
        if(min_k[i] <= k) cout << "Yes\n";
        else cout << "No\n";
    }
}