#include<bits/stdc++.h>
using namespace std;
const int N=5e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int fa[N],q[N];
int find(int x){return x==fa[x]?x:fa[x]=find(fa[x]);}
struct edge
{
	int u,v,w,x,y,id;
}e[N];
bool cmpw(edge a,edge b){return a.w<b.w;}
bool cmpid(edge a,edge b){return a.id<b.id;}
bool cmpe(int a,int b){return e[a].w<e[b].w;}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read();
	for (int i=1;i<=m;i++)
		e[i].u=read(),
		e[i].v=read(),
		e[i].w=read(),
		e[i].id=i;
	sort(e+1,e+1+m,cmpw);
	for (int i=1;i<=n;i++) fa[i]=i;
	for (int i=1,j=0;i<=m;i++)
	{
		e[i].x=find(e[i].u);
		e[i].y=find(e[i].v);
		if (i==m||e[i].w!=e[i+1].w)
		{
			for (int k=i;k>j;k--) fa[find(e[k].u)]=find(e[k].v);
			j=i;
		}
	}
	sort(e+1,e+1+m,cmpid);
	int Q=read();
	while (Q--)
	{
		int nm=read(),flag=1;
		for (int i=1;i<=nm;i++) q[i]=read();
		sort(q+1,q+1+nm,cmpe);
		for (int i=1,j=0;i<=nm;i++)
		if (i==nm||e[q[i]].w!=e[q[i+1]].w)
		{
			for (int k=i;k>j;k--) fa[e[q[k]].x]=e[q[k]].x,fa[e[q[k]].y]=e[q[k]].y;
			for (int k=i;k>j;k--)
			if (find(e[q[k]].x)!=find(e[q[k]].y)) fa[find(e[q[k]].x)]=find(e[q[k]].y);
			else flag=0;
			j=i;
		}
		puts(flag?"YES":"NO");
	}
	
	return 0;
}
