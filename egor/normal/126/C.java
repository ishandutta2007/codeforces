class TaskC {
	public void solve(int testNumber, InputReader in, OutputWriter out) {
		int size = in.readInt();
		char[][] data = IOUtils.readTable(in, size, size);
		boolean[] upRows = new boolean[size];
		boolean[] upColumns = new boolean[size];
		boolean[] downRows = new boolean[size];
		boolean[] downColumns = new boolean[size];
		int answer = 0;
		for (int i = size - 1; i > 0; i--) {
			for (int j = 0; i + j < size; j++) {
				int row = j;
				int column = i + j;
				if ((upRows[row] ^ upColumns[column]) != (data[row][column] == '1')) {
					answer++;
					upRows[row] = !upRows[row];
					upColumns[column] = !upColumns[column];
				}
				row = i + j;
				column = j;
				if ((downRows[row] ^ downColumns[column]) != (data[row][column] == '1')) {
					answer++;
					downRows[row] = !downRows[row];
					downColumns[column] = !downColumns[column];
				}
			}
		}
		for (int i = 0; i < size; i++) {
			if ((upRows[i] ^ upColumns[i] ^ downRows[i] ^ downColumns[i]) != (data[i][i] == '1'))
				answer++;
		}
		out.printLine(answer);
	}
}

class InputReader {

	private InputStream stream;
	private byte[] buf = new byte[1024];
	private int curChar;
	private int numChars;

	public InputReader(InputStream stream) {
		this.stream = stream;
	}

	public int read() {
		if (numChars == -1)
			throw new InputMismatchException();
		if (curChar >= numChars) {
			curChar = 0;
			try {
				numChars = stream.read(buf);
			} catch (IOException e) {
				throw new InputMismatchException();
			}
			if (numChars <= 0)
				return -1;
		}
		return buf[curChar++];
	}

	public int readInt() {
		int c = read();
		while (isSpaceChar(c))
			c = read();
		int sgn = 1;
		if (c == '-') {
			sgn = -1;
			c = read();
		}
		int res = 0;
		do {
			if (c < '0' || c > '9')
				throw new InputMismatchException();
			res *= 10;
			res += c - '0';
			c = read();
		} while (!isSpaceChar(c));
		return res * sgn;
	}

	private boolean isSpaceChar(int c) {
		return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
	}

	public char readCharacter() {
		int c = read();
		while (isSpaceChar(c))
			c = read();
		return (char) c;
	}

	}

class OutputWriter {
	private final PrintWriter writer;

	public OutputWriter(OutputStream outputStream) {
		writer = new PrintWriter(outputStream);
	}

	public OutputWriter(Writer writer) {
		this.writer = new PrintWriter(writer);
	}

	public void print(Object...objects) {
		for (int i = 0; i < objects.length; i++) {
			if (i != 0)
				writer.print(' ');
			writer.print(objects[i]);
		}
	}

	public void printLine(Object...objects) {
		print(objects);
		writer.println();
	}

	public void close() {
		writer.close();
	}
}

class IOUtils {

	public static char[] readCharArray(InputReader in, int size) {
		char[] array = new char[size];
		for (int i = 0; i < size; i++)
			array[i] = in.readCharacter();
		return array;
	}

	public static char[][] readTable(InputReader in, int rowCount, int columnCount) {
		char[][] table = new char[rowCount][];
		for (int i = 0; i < rowCount; i++)
			table[i] = readCharArray(in, columnCount);
		return table;
	}

	}