using namespace std;

typedef long long ll;
typedef unsigned long long ull;

// mt19937 rng(chrono::steady_clock::now().time_since_epoch().count());

template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

const ll K = ll(1e18) + 5;

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int N = 1e5;
  vector<ll> f(N + 1);
  f[0] = 1;
  for (int n = 1; n <= N; ++n) {
    for (int i = 1; i <= n; ++i) {
      f[n] += f[n - i];
      if (f[n] >= K) {
        f[n] = K;
        break;
      }
    }
  }

  int T;
  cin >> T;
  while (T--) {
    int n; ll k;
    cin >> n >> k; --k;
    if (k >= f[n]) {
      cout << "-1\n";
      continue;
    }
    int v = 0;
    vector<int> ans;
    while (n) {
      int p = 1;
      while (p <= n && f[n - p] <= k) k -= f[n - p++];
      for (int i = p; i; --i) ans.push_back(v + i);
      n -= p;
      v += p;
    }
    //for (++v; n--; ++v) ans.push_back(v);
    cout << ans << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}