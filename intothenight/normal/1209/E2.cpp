mt19937 rnd(chrono::steady_clock::now().time_since_epoch().count());



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int tt;
	cin >> tt;
	while(tt --){
		int n, m;
		cin >> n >> m;
		vector<vi> a(n, vi(m));
		cin >> a;
		vector<pii> col(m); // max, col id
		for(int j = 0; j < m; j ++){
			col[j].second = j;
			for(int i = 0; i < n; i ++){
				col[j].first = max(col[j].first, a[i][j]);
			}
		}
		sort(all(col), greater<pii>{});
		col.resize(m = min(n, m));
		vector<vi> trans(m, vi(1 << n));
		for(int i = 0; i < m; i ++){
			for(int k = 0; k < 1 << n; k ++){
				for(int shift = 0; shift < n; shift ++){
					int sum = 0;
					for(int bit = 0; bit < n; bit ++){
						if(k & 1 << bit){
							sum += a[(bit + shift) % n][col[i].second];
						}
					}
					trans[i][k] = max(trans[i][k], sum);
				}
			}
		}
		vi dp(1 << n); // mask on empty col
		for(int j = 0; j < m; j ++){
			for(int k = 0; k < 1 << n; k ++){
				for(int sk = k; sk; sk = (sk - 1) & k){
					dp[k ^ sk] = max(dp[k] + trans[j][sk], dp[k ^ sk]);
				}
			}
		}
		cout << dp[0] << "\n";
	}
	return 0;
}
