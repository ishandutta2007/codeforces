#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<x<<endl;
#define debugp(x) cerr<<#x<<"= {"<<x.first<<", "<<x.second<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 1000010;

ll get(int a, int b){
	if (a<b) return 0;
	return a/b+get(a/b, b);
}

ll n, a, b, x, y, t;
int A[MAXN]; 
vector<int> prime;

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	if (n%4==0) prime.pb(4);
	int i=3;
	int tmp=n/(n&(-n));
	for (; i*i<=tmp; i++){
		if (tmp%i) continue ;
		prime.pb(i);
		while (tmp%i==0) tmp/=i;
	}
	if (tmp>1) prime.pb(tmp);
	
	for (int i=0; i<n; i++) cin>>A[i];
	
	for (auto x:prime){
		int p=n/x;
		for (int i=0; i<p; i++){
			bool flag=true;
			for (int j=i; j<n; j+=p){
				if (!A[j]){
					flag=false;
					break ;
				}
			}
			if (flag){
				cout<<"YES"<<endl;
				return 0;
			}
		}
	}
	cout<<"NO"<<endl;
	return 0;
}
