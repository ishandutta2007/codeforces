void solve() {
	int n;
	cin >> n;
	string s;
	cin >> s;
	string c = "abacaba";
	for(int i = 0; i + c.size() <= n; ++i) {
		string res = s;
		bool can = true;
		for(int j = i; j < i + c.size(); ++j) {
			if (res[j] == c[j - i] || res[j] == '?')
				res[j] = c[j - i];
			else
				can = false;
		}
		if (can) {
			int kol = 0;
			for(int j = 0; j + c.size() <= n; ++j) {
				bool z = false;
				for(int k = 0; k < c.size(); ++k) {
					if (res[j + k] != c[k]) 
						z = true;
				}
				if (!z)
					++kol;
			}
			if (kol == 1) {
				cout << "YES\n";
				for(auto j: res) {
					if (j == '?')
						j = 'p';
					cout << j;
				}
				cout << '\n';
				return;
			}
		}
	}
	cout << "NO\n";
}

