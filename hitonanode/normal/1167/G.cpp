constexpr double PI = 3.141592653589793238;
int N, D, M;

int main()
{
    cin >> N >> D;

    vector<lint> A(N);
    cin >> A;

    cin >> M;

    lint xprev = -7000;
    bitset<7001> L, R;

    int ir = 0, il = 0;
    REP(_, M)
    {
        lint x;
        cin >> x;
        R >>= x - xprev;
        L <<= x - xprev;
        while (ir < N and A[ir] < x) ir++;
        while (ir < N and A[ir] <= x + 7000) R[A[ir] - x] = 1, ir++;
        while (il < N and A[il] < x - 7000) il++;
        while (il < N and A[il] < x) L[x - A[il] - 1] = 1, il++;

        auto D0 = L | R;
        auto DD = L & R;
        auto DD1 = (L & (R >> 1)) | (R & (L >> 1));

        int d0f = D0._Find_first();
        double theta_D0 = (d0f ? atan2(1, d0f) : PI / 2);
        int ddf = DD._Find_first();
        double theta_DD = (ddf ? atan2(1, ddf) * 2 : PI);
        int d01f = DD1._Find_first();
        double theta_D01 = atan2(1, d01f + 1) * 2;
        printf("%.15lf\n", max(max(theta_D0, theta_DD), theta_D01));
        xprev = x;
    }
}