#include<bits/stdc++.h>
using namespace std;
const int N=2.5e3+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int x[N],y[N];
double dp[N][N][2];
long long sqr(long long x){return x*x;}
double dis(int i,int j){return sqrt(sqr(x[i]-x[j])+sqr(y[i]-y[j]));}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read();
	for (int i=0;i<n;i++) x[i]=read(),y[i]=read();
	for (int i=2;i<=n;i++)
	for (int j=0;j+i-1<n*2;j++)
	{
		int l=j,r=j+i-1,L=l+1,R=r-1;
		l%=n,r%=n,L%=n,R%=n;
		dp[l][r][0]=max(dp[L][r][0]+dis(l,L),dp[L][r][1]+dis(l,r));
		dp[l][r][1]=max(dp[l][R][0]+dis(l,r),dp[l][R][1]+dis(R,r));
	}
	double ans=0;
	for (int i=0;i<n;i++) ans=max(ans,max(dp[i][(i+n-1)%n][0],dp[i][(i+n-1)%n][1]));
	printf("%.15lf",ans);
	
	return 0;
}
