vector<int> c;

void f(vector<int> a) {
	if (a.size() == 0)
		return;
	pair<int, int> mx = {-1, -1};
	for(int i = 0; i < a.size(); ++i) 
		mx = max(mx, {a[i], i});
	c.push_back(a.size() - mx.second);
	vector<int> p;
	for(int i = 0; i < mx.second; ++i)
		p.push_back(a[i]);
	f(p);
}

void solve() {
	int n;
	cin >> n;
	c.clear();
	vector<int> a(2 * n);
	for(int i = 0; i < 2 * n; ++i)
		cin >> a[i];
	f(a);
	vector<vector<int>> dp(c.size() + 1, vector<int> (n + 1));
	dp[0][0] = 1;
	for(int i = 0; i < c.size(); ++i) {
		for(int j = 0; j <= n; ++j) {
			if (dp[i][j] && j + c[i] <= n)
				dp[i + 1][j + c[i]] = 1;
			if (dp[i][j])
				dp[i + 1][j] = 1;
		}
	}
	if (dp[c.size()][n])
		cout << "YES\n";
	else
		cout << "NO\n";
}

