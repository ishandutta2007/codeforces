#include<bits/stdc++.h>
#define ll long long
#define N 100015
#define inf 0x3f3f3f3f
#define pb push_back
#define mp make_pair
#define lowbit(i) ((i)&(-i))
#define VI vector<int>
using namespace std;
int t,n,a[N],b[N],pre[N][2];
void solve(){
	cin>>n;
	bool flag1,flag11;
	flag1 = flag11 = 0;
	for(int i = 1;i <= n;++i) cin>>a[i],pre[i][0] = pre[i][1] = 0;
	for(int i = 1;i <= n;++i){
		if(a[i]==1) pre[i][1]++;
		if(a[i]==-1) pre[i][0]++;
		pre[i][1] += pre[i-1][1];
		pre[i][0] += pre[i-1][0];
	}
	for(int i = 1;i <= n;++i) cin>>b[i];
	for(int i = n;i > 0;--i){
		if(b[i]>a[i])
			if(pre[i-1][1] == 0){puts("NO");return;}
		if(b[i]<a[i])
			if(pre[i-1][0] == 0){puts("NO");return;}
	}
	puts("YES");
	return;
}
int main(){
	//freopen(".in","r",stdin);
	//freopen(".out","w",stdout);
	ios::sync_with_stdio(0);
	cin.tie(0);
	cin>>t;
	while(t--){
		solve();
	}
	return 0;
}