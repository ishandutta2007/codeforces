string num[10]={"1110111","0010010","1011101","1011011","0111010","1101011","1101111","1010010","1111111","1111011"};
int cost[1<<7][10];
const int maxn=2005;
int dp[maxn][maxn];
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    for(int i=0;i<(1<<7);i++)
    {
        for(int j=0;j<10;j++)
        {
            string a=num[j];
            int cc=0;
            for(int k=0;k<7;k++)
                cc+=(1<<k)*(a[k]=='1');
            if((cc&i)==i)
                cost[i][j]=__builtin_popcount(cc^i);
            else cost[i][j]=-1;
            //dbg(i,j,cost[i][j]);
        }
    }
    //dbg(cost[36][9]);
    int n,kk;
    cin>>n>>kk;
    vc<string> a(n+1);
    for(int i=1;i<=n;i++) cin>>a[i];
    dp[n+1][0]=1;
    for(int i=n;i>=1;i--)
    {
        int cur=0;
        for(int k=0;k<7;k++)
            cur+=(1<<k)*(a[i][k]=='1');
        for(int k=0;k<=9;k++)
        {
            if(cost[cur][k]==-1) continue;
            for(int j=cost[cur][k];j<=kk;j++)
            {
                dp[i][j]|=dp[i+1][j-cost[cur][k]];
                //dbg(i,j,k,dp[i][j]);
            }
        }
    }
    if(!dp[1][kk]) print(-1);
    else{
        string ans;
        int cnt=kk;
        for(int i=1;i<=n;i++)
        {
            int cur=0;
            for(int k=0;k<7;k++)
                cur+=(1<<k)*(a[i][k]=='1');
            for(int j=9;j>=0;j--)
            {
                if(cost[cur][j]==-1) continue;
                if(dp[i+1][cnt-cost[cur][j]])
                {
                    cnt-=cost[cur][j];
                    ans+='0'+j;
                    break;
                }
            }
        }
        cout<<ans<<'\n';

    }
}