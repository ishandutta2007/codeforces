template<typename T>
void output_vector(const vector<T> &v, bool add_one = false, int start = -1, int end = -1) {
    if (start < 0) start = 0;
    if (end < 0) end = v.size();

    for (int i = start; i < end; i++)
        cout << v[i] + (add_one ? 1 : 0) << (i < end - 1 ? ' ' : '\n');
}

int N;
vector<long long> B;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    cin >> N;
    B.resize(N);

    for (auto &b : B)
        cin >> b;

    vector<long long> A(N);
    long long maximum = 0;

    for (int i = 0; i < N; i++) {
        A[i] = B[i] + maximum;
        maximum = max(maximum, A[i]);
    }

    output_vector(A);
}