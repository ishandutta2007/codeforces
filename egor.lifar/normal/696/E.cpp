inline void push(int v) {
    if (mod[v] != 0) {
        d[v].first += mod[v];
        if (v < ss) {
            mod[v * 2] += mod[v];
            mod[v * 2 + 1] += mod[v];
        }
        mod[v] = 0;
    }
}


void add(int v, int vl, int vr, int l, int r, long long x) {
    if (vl > r || vr < l) {
        push(v);
        return;
    }
    if (l <= vl && vr <= r) {
        mod[v] += x;
        push(v);
        return;
    }
    push(v);
    add(v * 2, vl, (vl + vr) / 2, l, r, x);
    add(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r, x);
    d[v] = min(d[v * 2], d[v * 2 + 1]);
}


pair<long long, int> rmq(int v, int vl, int vr, int l, int r) {
    if (vl > r || vr < l) {
        return make_pair((long long)1e18, -1);
    }
    if (l <= vl && vr <= r) {
        push(v);
        return d[v];
    }
    push(v);
    return min(rmq(v * 2, vl, (vl + vr) / 2, l, r), rmq(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r));
}


