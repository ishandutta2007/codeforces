using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(!isdigit(ch)){w |= ch == '-';ch = getchar();}
	while(isdigit(ch)){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int n, a[100005];
void solve()
{
	scanf("%d", &n);
	for (int i = 1; i <= n; i++) a[i] = read();
	sort(a + 1, a + n + 1);
	printf("%d\n", a[n - 1] + a[n]);
}
int main()
{
	int T = read();
	while(T--) solve();
	return 0;
}