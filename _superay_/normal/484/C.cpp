#include <bits/stdc++.h>
using namespace std;
const int N = 1000005;
char S[N], T[N];
int n, yhttxdy, p[N], vis[N];
int main() {
	scanf("%s", S);
	n = strlen(S);
	scanf("%d", &yhttxdy);
	while (yhttxdy--) {
		int k, d;
		scanf("%d%d", &k, &d);
		for (int i = k; i < n; i++) p[i] = i;
		for (int r = 0, cnt = 0; r < d; r++) {
			for (int i = r; i < k; i += d) {
				p[i] = cnt++;
			}
		}
		for (int i = 0; i < n; i++) {
			p[i] = (p[i] + n - 1) % n;
			vis[i] = 0;
		}
		for (int i = 0; i < n; i++) if (!vis[i]) {
			vector<int> vec;
			int j = i;
			while (!vis[j]) {
				vis[j] = 1;
				vec.push_back(j);
				j = p[j];
			}
			for (j = 0; j < (int)vec.size(); j++) {
				p[vec[j]] = vec[(j + n - k + 1) % vec.size()];
			}
		}
		for (int i = 0; i < n; i++) {
			T[(p[i] - k + 1 + n) % n] = S[i];
		}
		for (int i = 0; i < n; i++) S[i] = T[i];
		puts(S);
	}
	return 0;
}