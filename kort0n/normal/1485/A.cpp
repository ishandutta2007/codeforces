#define endl "\n"
using namespace std;
typedef long long ll;
typedef pair<ll, ll> l_l;
typedef pair<int, int> i_i;
template<class T>
inline bool chmax(T &a, T b) {
    if(a < b) {
        a = b;
        return true;
    }
    return false;
}

template<class T>
inline bool chmin(T &a, T b) {
    if(a > b) {
        a = b;
        return true;
    }
    return false;
}

const long long INF = 1e18;
//const ll mod = 1000000007;

void solve() {
    ll a, b;
    cin >> a >> b;
    ll ans = INF;
    for(ll i = 0; i <= ans; i++) {
        ll t = i + b;
        if(t == 1) continue;
        ll tmpans = i;
        ll copy = a;
        while(copy != 0) {
            copy /= t;
            tmpans++;
        }
        chmin(ans, tmpans);
    }
    cout << ans << endl;
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    ll T;
    cin >> T;
    while(T--) solve();
    return 0;
}