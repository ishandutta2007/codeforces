int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int tt;
    cin >> tt;
    while(tt--) {
        int n;
        cin >> n;
        string s;
        cin >> s;
        bool done[26] = {};
        char pre = '.';
        bool ok = true;
        for(char c: s) {
            if (c == pre) continue;
            if (pre != '.') done[pre-'A'] = true;
            if (done[c - 'A']) ok = false;
            pre = c;
        }
        cout << (ok ? "YES" : "NO") << '\n';
    }
}