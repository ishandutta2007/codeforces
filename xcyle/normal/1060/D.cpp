using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(!isdigit(ch)){w |= ch == '-';ch = getchar();}
	while(isdigit(ch)){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int n, l[maxn], r[maxn];
signed main()
{
	n = read();
	for (int i = 1; i <= n; i++) l[i] = read(), r[i] = read();
	sort(l + 1, l + n + 1), sort(r + 1, r + n + 1);
	int ans = n;
	for (int i = 1; i <= n; i++) ans += max(l[i], r[i]);
	printf("%lld\n", ans); 
	return 0;
}