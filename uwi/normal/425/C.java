	void solve()
	{
		int n = ni(), m = ni(), s = ni(), e = ni();
		int[] a = na(n);
		int[] b = na(m);
		int[][] g = makeBuckets(b, 100001);
		
		int[][] dp = new int[n+1][301];
		for(int i = 0;i <= n;i++){
			Arrays.fill(dp[i], 999999);
		}
		dp[0][0] = -1;
		for(int i = 1;i <= n;i++){
			for(int j = 0;j <= 300;j++){
				dp[i][j] = dp[i-1][j];
				if(j-1 >= 0 && dp[i-1][j-1] != 999999){
					int ind = Arrays.binarySearch(g[a[i-1]], dp[i-1][j-1]+1);
					if(ind < 0)ind = -ind-1;
					if(ind < g[a[i-1]].length){
						dp[i][j] = Math.min(dp[i][j], g[a[i-1]][ind]);
					}
				}
			}
		}
		for(int j = 300;j >= 0;j--){
			for(int i = 0;i <= n;i++){
				if(dp[i][j] == 999999)continue;
				long sc = (long)e * j + dp[i][j] + 1 + i;
				if(sc <= s){
					out.println(j);
					return;
				}
			}
		}
		out.println(0);
	}
	
	public static int[][] makeBuckets(int[] a, int sup)
	{
		int n = a.length;
		int[][] bucket = new int[sup+1][];
		int[] bp = new int[sup+1];
		for(int i = 0;i < n;i++)bp[a[i]]++;
		for(int i = 0;i <= sup;i++)bucket[i] = new int[bp[i]];
		for(int i = n-1;i >= 0;i--)bucket[a[i]][--bp[a[i]]] = i;
		return bucket;
	}
	
