#include<bits/stdc++.h>
#define int long long
#define N 300015
#define inf 0x3f3f3f3f3f3f3f3fll
#define pb push_back
#define mp make_pair
#define lowbit(i) ((i)&(-i))
#define VI vector<int>
using namespace std;
int t,n,a[N],b[N],d[N],sum,ans; 
signed main(){
	//freopen(".in","r",stdin);
	//freopen(".out","w",stdout);
	ios::sync_with_stdio(0);
	cin.tie(0);
	cin>>t;
	while(t--){
		cin>>n;
		sum = 0ll;
		for(int i = 1;i <= n;++i) cin>>a[i]>>b[i],d[i] = 0;
		for(int i = 1;i <= n;++i){
			if(i==1) d[i] = a[i]-b[n];
			else d[i] = a[i]-b[i-1];
			d[i] = max(d[i],0ll);
			sum += d[i];
		}
		ans = inf;
		for(int i = 1;i <= n;++i) ans = min(ans,sum-d[i]+a[i]);
		cout << ans << endl;
	}
	return 0;
}