void run_case() {
    int N, M;
    cin >> N >> M;

    auto convert = [&](const vector<int> &A) -> vector<pair<int, int64_t>> {
        vector<pair<int, int64_t>> groups;

        for (auto &a : A) {
            int value = a;
            int64_t count = 1;

            while (value % M == 0) {
                value /= M;
                count *= M;
            }

            if (!groups.empty() && groups.back().first == value)
                groups.back().second += count;
            else
                groups.emplace_back(value, count);
        }

        return groups;
    };

    vector<int> A(N);

    for (auto &a : A)
        cin >> a;

    int K;
    cin >> K;
    vector<int> B(K);

    for (auto &b : B)
        cin >> b;

    cout << (convert(A) == convert(B) ? "Yes" : "No") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}