const int N = 3e5 + 228;

vector<pair<int, pair<int, int>>> G[N];
long long d[N];
const long long INF = 1e15 + 228;
int k;
vector<int> ans;
int used[N];

void dfs(int v) {
	used[v] = 1;
	for(auto i: G[v]) {
		if (!k)
			return;
		if (used[i.second.first])
			continue;
		if (d[i.second.first] == d[v] + i.second.second) {
			ans.push_back(i.first);
			--k;
			dfs(i.second.first);
		}
	}
}

signed main() {
	setup();
	fill(d, d + N, INF);
	fill(used, used + N, 0);

	int n, m;
	cin >> n >> m >> k;
	for(int i = 0; i < m; ++i) {
		int a, b, c;
		cin >> a >> b >> c;
		G[a - 1].emplace_back(make_pair(i + 1, make_pair(b - 1, c)));
		G[b - 1].emplace_back(make_pair(i + 1, make_pair(a - 1, c)));
	}
	set<pair<long long, int>> q;
	for(int i = 1; i < n; ++i) {
		q.emplace(d[i], i);
	}
	q.emplace(0, 0);
	d[0] = 0;
	while(q.size()) {
		int now = q.begin()->second;
		long long len = q.begin()->first;
		q.erase(*q.begin());
		for(auto _: G[now]) {
			auto v = _.second;
			if (q.count(make_pair(d[v.first], v.first))) {
				q.erase(make_pair(d[v.first], v.first));
				d[v.first] = min(d[v.first], len + v.second);
				q.emplace(make_pair(d[v.first], v.first));
			}
		}
	}
	dfs(0);
	cout << ans.size() << '\n';
	for(auto i: ans)
		cout << i << ' ';
	cout << '\n';


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

