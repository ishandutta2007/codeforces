int fix(int x){
	while (x >= MOD) x -= MOD;
	while (x < 0) x += MOD;
	return x;
}

int pw(int a, int b){
	int ret = 1;
	while (b){
		if (b & 1)
			ret = 1ll*ret*a%MOD;
		b >>= 1;
		a = 1ll*a*a%MOD;
	}
	return ret;
}

void solve() {
	string s; cin >> s;
	int n = s.size();

	int mask = 0;
	for (int l = 0; l < n;){
		int r = l;
		while (r < n && s[l] == s[r]) r++;

		if (r-l&1)
			mask |= 1<<int(s[l]-'a');
		l = r;
	}
	for (int i = 0; i < 26; i++)
		if (mask>>i&1)
			cout << char('a' + i);
	cout << "\n";
}

