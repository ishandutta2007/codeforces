const int MAXN = 100010, LOG=20;

struct DSU{
	int par[MAXN];
	DSU(){ for (int i=1; i<MAXN; i++) par[i]=i;}
	int get(int x){
		if (par[x]==x) return x;
		return par[x]=get(par[x]);
	}
	void join(int x, int y){ par[get(x)]=get(y);}
} dsu;

int n, m, k, u, v, x, y, t, a, b;
int col[MAXN];
vector<int> G[MAXN], ans;
pii E[MAXN];

void dfs(int node, int c){
	col[node]=c;
	for (int v:G[node]){
		if (col[v]==c){
			puts("Impossible");
			exit(0);
		}
		if (!col[v]) dfs(v, 3-c);
	}
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	while (m--){
		cin>>u>>v>>x;
		if (x) dsu.join(u, v);
		else E[t++]={u, v};
	}
	for (int i=0; i<t; i++){
		G[dsu.get(E[i].first)].pb(dsu.get(E[i].second));
		G[dsu.get(E[i].second)].pb(dsu.get(E[i].first));
	}
	for (int i=1; i<=n; i++) if (!col[i]) dfs(i, 1);
	for (int i=1; i<=n; i++) if (col[dsu.get(i)]==1) ans.pb(i);
	
	cout<<ans.size()<<'\n';
	for (int i:ans) cout<<i<<' ';
	
	return 0;
}
