namespace atcoder {

namespace internal {

// @param n `0 <= n`
// @return minimum non-negative `x` s.t. `n <= 2**x`
int ceil_pow2(int n) {
    int x = 0;
    while ((1U << x) < (unsigned int)(n)) x++;
    return x;
}

// @param n `1 <= n`
// @return minimum non-negative `x` s.t. `(n & (1 << x)) != 0`
int bsf(unsigned int n) {
#ifdef _MSC_VER
    unsigned long index;
    _BitScanForward(&index, n);
    return index;
#else
    return __builtin_ctz(n);
#endif
}

}  // namespace internal

}  // namespace atcoder

#include <cassert>
#include <vector>

namespace atcoder {

template <class S, S (*op)(S, S), S (*e)()> struct segtree {
  public:
    segtree() : segtree(0) {}
    segtree(int n) : segtree(std::vector<S>(n, e())) {}
    segtree(const std::vector<S>& v) : _n(int(v.size())) {
        log = internal::ceil_pow2(_n);
        size = 1 << log;
        d = std::vector<S>(2 * size, e());
        for (int i = 0; i < _n; i++) d[size + i] = v[i];
        for (int i = size - 1; i >= 1; i--) {
            update(i);
        }
    }

    void set(int p, S x) {
        assert(0 <= p && p < _n);
        p += size;
        d[p] = x;
        for (int i = 1; i <= log; i++) update(p >> i);
    }

    S get(int p) {
        assert(0 <= p && p < _n);
        return d[p + size];
    }

    S prod(int l, int r) {
        assert(0 <= l && l <= r && r <= _n);
        S sml = e(), smr = e();
        l += size;
        r += size;

        while (l < r) {
            if (l & 1) sml = op(sml, d[l++]);
            if (r & 1) smr = op(d[--r], smr);
            l >>= 1;
            r >>= 1;
        }
        return op(sml, smr);
    }

    S all_prod() { return d[1]; }

    template <bool (*f)(S)> int max_right(int l) {
        return max_right(l, [](S x) { return f(x); });
    }
    template <class F> int max_right(int l, F f) {
        assert(0 <= l && l <= _n);
        assert(f(e()));
        if (l == _n) return _n;
        l += size;
        S sm = e();
        do {
            while (l % 2 == 0) l >>= 1;
            if (!f(op(sm, d[l]))) {
                while (l < size) {
                    l = (2 * l);
                    if (f(op(sm, d[l]))) {
                        sm = op(sm, d[l]);
                        l++;
                    }
                }
                return l - size;
            }
            sm = op(sm, d[l]);
            l++;
        } while ((l & -l) != l);
        return _n;
    }

    template <bool (*f)(S)> int min_left(int r) {
        return min_left(r, [](S x) { return f(x); });
    }
    template <class F> int min_left(int r, F f) {
        assert(0 <= r && r <= _n);
        assert(f(e()));
        if (r == 0) return 0;
        r += size;
        S sm = e();
        do {
            r--;
            while (r > 1 && (r % 2)) r >>= 1;
            if (!f(op(d[r], sm))) {
                while (r < size) {
                    r = (2 * r + 1);
                    if (f(op(d[r], sm))) {
                        sm = op(d[r], sm);
                        r--;
                    }
                }
                return r + 1 - size;
            }
            sm = op(d[r], sm);
        } while ((r & -r) != r);
        return 0;
    }

  private:
    int _n, size, log;
    std::vector<S> d;

    void update(int k) { d[k] = op(d[2 * k], d[2 * k + 1]); }
};

}  // namespace atcoder

using namespace std;
using namespace atcoder;

int op(int a, int b) { return (a+b); }

int e() { return 0; }

long long inv_cnt(string &s,string &t){
  map<char,queue<int>> mp;
  int l=s.size();
  for(int i=0;i<l;i++){
    mp[s[i]].push(i);
  }
  vector<int> p;
  for(int i=0;i<l;i++){
    p.push_back(mp[t[i]].front());
    mp[t[i]].pop();
  }

  long long res=0;
  segtree<int, op, e> seg(l);
  for(int i=0;i<l;i++){
    res+=(i-seg.prod(0,p[i]));
    seg.set(p[i],1);
  }
  return res;
}

using pls=pair<long long,string>;

int main(){
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int t;
  cin >> t;
  while(t>0){
    t--;
    string s;
    cin >> s;
    int l=s.size();
    int a=0,n=0,o=0,t=0;
    vector<char> cv={'A','N','O','T'};
    for(int i=0;i<l;i++){
      if(s[i]=='A'){a++;}
      if(s[i]=='N'){n++;}
      if(s[i]=='O'){o++;}
      if(s[i]=='T'){t++;}
    }
    pls res={-1e9,""};
    do{
      string cur;
      for(auto &nx : cv){
        if(nx=='A'){
          for(int i=0;i<a;i++){cur.push_back('A');}
        }
        if(nx=='N'){
          for(int i=0;i<n;i++){cur.push_back('N');}
        }
        if(nx=='O'){
          for(int i=0;i<o;i++){cur.push_back('O');}
        }
        if(nx=='T'){
          for(int i=0;i<t;i++){cur.push_back('T');}
        }
      }
      res=max(res,{inv_cnt(cur,s),cur});
    }while(next_permutation(cv.begin(),cv.end()));
    cout << res.second << '\n';
  }
  return 0;
}