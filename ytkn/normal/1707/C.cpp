struct UnionFind {
    vector<int> data;
    UnionFind(int size) : data(size, -1) {}
    bool unionSet(int x, int y) {
        x = root(x); y = root(y);
        if (x != y) {
        if (data[y] < data[x]) swap(x, y);
        data[x] += data[y]; data[y] = x;
        }
        return x != y;
    }
    bool findSet(int x, int y) {
        return root(x) == root(y);
    }
    int root(int x) {
        return data[x] < 0 ? x : data[x] = root(data[x]);
    }
    int size(int x) {
        return -data[root(x)];
    }
};


class Lca{
    public:
        vector<int> depth;
        vector<vector<int>> parent;
        vector<vector<int>> G;
        int root;
        int n;
        const int N_LOG_MAX = 25;
        bool initialized = false;

        Lca(vector<vector<int>> g, int _root){
            n = g.size();
            G = g;
            root = _root;
            parent = vector<vector<int>>(N_LOG_MAX, vector<int>(n));
            depth = vector<int>(n, 0);
        }

        void init(){
            initialized = true;
            dfs(root, -1, 0);
            for(int i = 0; i < N_LOG_MAX-1; i++){
                for(int v = 0; v < n; v++){
                    if(parent[i][v] < 0) parent[i+1][v] = -1;
                    else parent[i+1][v] = parent[i][parent[i][v]];
                }
            }
        }

        int lca(int u, int v){
            assert(initialized);
            if(depth[u] > depth[v]) swap(u, v);
            for(int i = 0; i < N_LOG_MAX; i++){
                if((depth[v] - depth[u]) >> i & 1){
                    v = parent[i][v];
                }
            }
            if(u == v) return u;
            for(int i = N_LOG_MAX-1; i >= 0; i--){
                if(parent[i][u] != parent[i][v]) {
                    u = parent[i][u];
                    v = parent[i][v];
                }
            }
            return parent[0][u];
        }

        int ansestor(int u, int x){
            int ans = u;
            for(int i = N_LOG_MAX-1; i >= 0; i--){
                if(x&(1<<i)) ans = parent[i][ans];
            }
            return ans;
        }

        int dist(int u, int v){
            int l = lca(u, v);
            return depth[u]+depth[v]-2*depth[l];
        }

    private:
        void dfs(int v, int p, int d){
            parent[0][v] = p;
            depth[v] = d;
            for(int i = 0; i < G[v].size(); i++){
                if(G[v][i] != p) dfs(G[v][i], v, d+1); 
            }
        }
};

/**
 * [maspy](https://maspypy.com/euler-tour-%E3%81%AE%E3%81%8A%E5%8B%89%E5%BC%B7#toc1)()
 */ 
class EulerTour{
    public:
    vector<vector<int>> g;
    int root;
    int n;
    vector<int> depth;
    vector<int> ord;
    vector<int> in, out;
    EulerTour(vector<vector<int>> g, int root): g(g), root(root){
        n = g.size();
        depth.resize(n);
        in.resize(n);
        out.resize(n);
        build();
    }
    private:
    void build(){
        vector<bool> seen(n, false);
        dfs(root, seen);
        for(int i = 0; i < n; i++) in[i] = 1e9;
        for(int i = 0; i < ord.size(); i++){
            chmin(in[ord[i]], i);
            chmax(out[ord[i]], i);
        }
        assert(ord.size() == 2*n);
    }
    void dfs(int v, vector<bool> &seen){
        ord.push_back(v);
        seen[v] = true;
        int cnt = 0;
        for(int to: g[v]){
            if(seen[to]) continue;
            depth[to] = depth[v]+1;
            dfs(to, seen);
            cnt++;
        }
        ord.push_back(v);
    }
};

int op(int x, int y){
    return x+y;
}

int e(){
    return 0;
}

using Seg = atcoder::lazy_segtree<int, op, e, int, op, op, e>;


int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, m; cin >> n >> m;
    vector<int> u(m), v(m);
    UnionFind uf(n);
    vector<bool> in_mst(m);
    vector<vector<int>> mst(n);
    for(int i = 0; i < m; i++){
        cin >> u[i] >> v[i]; u[i]--; v[i]--;
        if(uf.findSet(u[i], v[i])) continue;
        in_mst[i] = true;
        uf.unionSet(u[i], v[i]);
        mst[u[i]].push_back(v[i]);
        mst[v[i]].push_back(u[i]);
    }
    int root = 0;
    Lca lca(mst, root);
    lca.init();
    auto et = EulerTour(mst, root);
    Seg seg(et.ord.size());
    for(int i = 0; i < m; i++){
        if(in_mst[i]) continue;
        int l = lca.lca(u[i], v[i]);
        if(l == u[i]){
            int d = lca.dist(u[i], v[i]);
            int cu = lca.ansestor(v[i], d-1);
            seg.apply(et.in[root], et.out[root]+1, 1);
            seg.apply(et.in[cu], et.out[cu]+1, -1);
            seg.apply(et.in[v[i]], et.out[v[i]]+1, 1);
        }else if(l == v[i]){
            int d = lca.dist(u[i], v[i]);
            int cv = lca.ansestor(u[i], d-1);
            seg.apply(et.in[root], et.out[root]+1, 1);
            seg.apply(et.in[cv], et.out[cv]+1, -1);
            seg.apply(et.in[u[i]], et.out[u[i]]+1, 1);
        }else{
            seg.apply(et.in[u[i]], et.out[u[i]]+1, 1);
            seg.apply(et.in[v[i]], et.out[v[i]]+1, 1);
        }
    }
    string ans;
    for(int i = 0; i < n; i++){
        if(seg.get(et.in[i])+n-1 == m) ans += '1';
        else {
            ans += '0';
        }
    }
    cout << ans << endl;
}