int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int k, qn;
	cin >> k >> qn;
	vector<vector<double>> dp(10 * k, vector<double>(k + 1));
	dp[0][0] = 1;
	for(auto i = 0; i < 10 * k - 1; ++ i){
		for(auto j = 0; j <= k; ++ j){
			dp[i + 1][j] += dp[i][j] * j / k;
			if(j < k){
				dp[i + 1][j + 1] += dp[i][j] * (k - j) / k;
			}
		}
	}
	for(auto qi = 0; qi < qn; ++ qi){
		int p;
		cin >> p;
		int res = 0;
		while(dp[res][k] * 2000 < p){
			++ res;
		}
		cout << res << "\n";
	}
	return 0;
}

