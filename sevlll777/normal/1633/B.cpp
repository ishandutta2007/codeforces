void solve() {
  string s;
  cin >> s;
  int c0 = 0, c1 = 0;
  for (auto c : s) {
    c0 += (c == '0');
    c1 += (c == '1');
  }
  if (c0 != c1) {
    cout << min(c0, c1) << '\n';
  } else {
    cout << c0 - 1 << '\n';
  }
}

