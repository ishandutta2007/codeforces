template<class T1, class T2>
istream &operator>>(istream &in, pair<T1, T2> &P){
	in >> P.first >> P.second;
	return in;
}
template<class T1, class T2>
ostream &operator<<(ostream &out, pair<T1, T2> &P){
	out << "(" << P.first << ", " << P.second << ")";
	return out;
}
template<class T>
istream &operator>>(istream &in, vector<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, vector<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	ll n, m;
	int qq;
	cin >> n >> m >> qq;
	ll g = gcd(n, m);
	n /= g, m /= g;
	while(qq --){
		vl x(2), y(2), num(2);
		cin >> x[0] >> y[0] >> x[1] >> y[1], y[0] --, y[1] --;
		for(int i = 0; i < 2; i ++){
			if(x[i] == 1){
				num[i] = y[i] / n;
			}
			else{
				num[i] = y[i] / m;
			}
		}
		num[0] == num[1] ? cout << "YES\n" : cout << "NO\n";
	}
	return 0;
}

