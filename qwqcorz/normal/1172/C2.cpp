#include<bits/stdc++.h>
#define ll long long
using namespace std;
const int N=2e5+5;
const int M=3e3+5;
const int p=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0)
	{
		putchar('-');
		x=-x;
	}
	if (x<10) putchar(x+'0');
	else
	{
		write(x/10);
		putchar(x%10+'0');
	}
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
int power(ll a,int b)
{
	ll ret=1;
	while (b)
	{
		if (b&1) ret=ret*a%p;
		a=a*a%p;
		b/=2;
	}
	return ret;
}
int a[N],w[N],f[M][M],g[M][M];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=read(),A=0,B=0;
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=1;i<=n;i++) w[i]=read();
	for (int i=1;i<=n;i++) if (a[i]) A+=w[i]; else B+=w[i];
	for (int i=m;i>=0;i--)
	for (int j=m-i;j>=0;j--)
	if (i+j==m) f[i][j]=1;
	else f[i][j]=(1LL*(A+i+1)*power(A+B+i-j,p-2)%p*f[i+1][j]+
				  1LL*(B-j)*power(A+B+i-j,p-2)%p*f[i][j+1])%p;
	for (int i=m;i>=0;i--)
	for (int j=m-i;j>=0;j--)
	if (i+j==m) g[i][j]=1;
	else g[i][j]=(1LL*(A+i)*power(A+B+i-j,p-2)%p*g[i+1][j]+
				  1LL*(B-j-1)*power(A+B+i-j,p-2)%p*g[i][j+1])%p;
	for (int i=1;i<=n;i++)
	if (a[i]) print(1LL*w[i]*f[0][0]%p);
		 else print(1LL*w[i]*g[0][0]%p);

	return 0;
}
