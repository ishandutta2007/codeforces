#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    int n, k;
    cin >> n >> k;
    k = 240 - k;
    int cnt = 0;
    for (int i = 1; i <= n; i++){
        if (5 * i <= k){
            cnt++;
            k -= (5 * i);
        }
        else
            return cout << cnt, 0;
    }
    cout << cnt;

}