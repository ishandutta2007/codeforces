int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n;
	cin >> n;
	vector<array<int, 2>> a(n);
	for(auto &[x, y]: a){
		cin >> x >> y;
	}
	sort(a.begin(), a.end());
	vector<int> dp(n);
	int res = numeric_limits<int>::max();
	for(auto i = 0; i < n; ++ i){
		auto it = lower_bound(a.begin(), a.end(), array{a[i][0] - a[i][1], -1});
		if(it == a.begin()){
			dp[i] = i;
		}
		else{
			int j = it - a.begin();
			dp[i] = i - j + dp[j - 1];
		}
		ctmin(res, dp[i] + n - 1 - i);
	}
	debug(dp);
	cout << res << "\n";
	return 0;
}

