const LL INF = 1e9 + 7;
const int N = 2e5 + 10;


int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	cin >> n;
	vector<int> v(n);
	for (auto &x : v) cin >> x;
	v.push_back(INF);
	LL ans = 0;
	LL cnt = 1;
	for (int i = 1; i <= n; i++)
	{
		if (v[i] != v[i - 1]) ans += cnt*(cnt + 1) / 2, cnt = 1;
		else cnt++;
	}
	cout << ans << endl;
	return 0;
}