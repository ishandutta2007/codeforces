    static class B1SocialNetworkEasyVersion {
        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int n = in.i();
            int k = in.i();
            int[] a = in.ia(n);
            MultiSet<Integer> freq = new MultiSet<>();
            ArrayDeque<Integer> fifo = new ArrayDeque<>();
            for (int i = 0; i < n; i++) {
                if (fifo.size() < k) {
                    if (!freq.contains(a[i])) {
                        fifo.addFirst(a[i]);
                        freq.add(a[i]);
                    }
                } else {
                    if (!freq.contains(a[i])) {
                        int removed = fifo.removeLast();
                        freq.remove(removed);
                        fifo.addFirst(a[i]);
                        freq.add(a[i]);
                    }
                }
            }
            out.printLine(fifo.size());
            fifo.forEach(e -> out.print(e + " "));
        }

    }

    static class InputReader {
        private InputStream is;
        private byte[] inbuf = new byte[1024];
        private int lenbuf = 0;
        private int ptrbuf = 0;

        public InputReader(InputStream is) {
            this.is = is;
        }

        private int readByte() {
            if (lenbuf == -1) throw new InputMismatchException();
            if (ptrbuf >= lenbuf) {
                ptrbuf = 0;
                try {
                    lenbuf = is.read(inbuf);
                } catch (IOException e) {
                    throw new InputMismatchException();
                }
                if (lenbuf <= 0) return -1;
            }
            return inbuf[ptrbuf++];
        }

        public int[] ia(int n) {
            int[] a = new int[n];
            for (int i = 0; i < n; i++) a[i] = i();
            return a;
        }

        public int i() {
            int num = 0, b;
            boolean minus = false;
            while ((b = readByte()) != -1 && !((b >= '0' && b <= '9') || b == '-')) ;
            if (b == '-') {
                minus = true;
                b = readByte();
            }

            while (true) {
                if (b >= '0' && b <= '9') {
                    num = num * 10 + (b - '0');
                } else {
                    return minus ? -num : num;
                }
                b = readByte();
            }
        }

    }

