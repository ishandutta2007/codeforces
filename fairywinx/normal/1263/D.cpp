const int N = 2e5 + 228;

vector<int> G[27];
int used[N];
string A[N];
int _used[27];

void dfs(int v) {
	used[v] = 1;
	for(auto i: A[v]) {
		if (_used[i - 'a'])
			continue;
		_used[i - 'a'] = 1;
		for(auto j: G[i - 'a']) {
			if (!used[j]) 
				dfs(j);
		}
	}
}

signed main() {
	setup();
	fill(used, used + N, 0);
	fill(_used, _used + 27, 0);

	int n;
	cin >> n;
	for(int i = 0; i < n; ++i) {
		cin >> A[i];
		for(auto j: A[i]) 
			G[j - 'a'].push_back(i);
	}
	int ans = 0;
	for(int i = 0; i < n; ++i) {
		if (!used[i]) {
			dfs(i);
			++ans;
		}
	}
	cout << ans << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

