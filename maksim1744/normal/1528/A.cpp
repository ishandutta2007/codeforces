void test_case(int test) {
    int n;
    cin >> n;
    vector<ll> l(n), r(n);
    for (int i = 0; i < n; ++i) {
        cin >> l[i] >> r[i];
    }

    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    vector<ll> dpl(n, 0);
    vector<ll> dpr(n, 0);
    vector<bool> u(n, false);

    y_combinator([&](auto dfs, int v) -> void {
        u[v] = true;
        vector<int> ch;
        for (int k : g[v]) {
            if (!u[k]) {
                dfs(k);
                ch.pb(k);
            }
        }
        for (int k : ch) {
            dpl[v] += max(abs(l[v] - l[k]) + dpl[k], abs(l[v] - r[k]) + dpr[k]);
            dpr[v] += max(abs(r[v] - l[k]) + dpl[k], abs(r[v] - r[k]) + dpr[k]);
        }
    })(0);
    cout << max(dpl[0], dpr[0]) << '\n';
}

