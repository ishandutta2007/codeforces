	void solve()
	{
		int n = ni(), K = ni();
		int[][] co = new int[n][];
		for(int i = 0;i < n;i++){
			int x1 = ni(), y1 = ni();
			int x2 = ni(), y2 = ni();
			co[i] = new int[]{x1+x2, y1+y2, 0};
		}
		Arrays.sort(co, new Comparator<int[]>() {
			public int compare(int[] a, int[] b) {
				return Long.compare(a[0], b[0]);
			}
		});
		for(int i = 0;i < n;i++)co[i][2] = i;
		int[][] xco = Arrays.copyOf(co, n);
		Arrays.sort(xco, new Comparator<int[]>() {
			public int compare(int[] a, int[] b) {
				return Long.compare(a[1], b[1]);
			}
		});
		int[] ia = new int[n];
		for(int i = 0;i < n;i++)ia[xco[i][2]] = i;
		
		LST lst = new LST(n+1);
		for(int i = 0;i < n;i++)lst.set(i);
		
		long min = Long.MAX_VALUE;
		for(int l = 0;l <= K;l++){
			for(int r = 0;l+r <= K;r++){
				long w = co[n-1-r][0] - co[l][0];
				
				int[] us = new int[K-l-r+1];
				int[] ds = new int[K-l-r+1];
				int pos = -1;
				for(int u = 0;u+l+r <= K;u++){
					int ind = lst.next(pos+1);
					us[u] = xco[ind][1];
					pos = ind;
				}
				pos = n;
				for(int d = 0;d+l+r <= K;d++){
					int ind = lst.prev(pos-1);
					ds[d] = xco[ind][1];
					pos = ind;
				}
				long h = Integer.MAX_VALUE;
				for(int u = 0;u+l+r <= K;u++){
					h = Math.min(h, ds[K-l-r-u] - us[u]);
				}
				min = Math.min(min, Math.max(1L, (h+1)/2) * Math.max(1L, (w+1)/2));
				lst.unset(ia[n-1-r]);
			}
			for(int r = 0;l+r <= K;r++){
				lst.set(ia[n-1-r]);
			}
			lst.unset(ia[l]);
		}
		out.println(min);
	}
	
	public static class LST {
		public long[][] set;
		public int n;
//		public int size;
		
		public LST(int n) {
			this.n = n;
			int d = 1;
			for(int m = n;m > 1;m>>>=6, d++);
			
			set = new long[d][];
			for(int i = 0, m = n>>>6;i < d;i++, m>>>=6){
				set[i] = new long[m+1];
			}
//			size = 0;
		}
		
		// [0,r)
		public LST setRange(int r)
		{
			for(int i = 0;i < set.length;i++, r=r+63>>>6){
				for(int j = 0;j < r>>>6;j++){
					set[i][j] = -1L;
				}
				if((r&63) != 0)set[i][r>>>6] |= (1L<<r)-1;
			}
			return this;
		}
		
		// [0,r)
		public LST unsetRange(int r)
		{
			if(r >= 0){
				for(int i = 0;i < set.length;i++, r=r+63>>>6){
					for(int j = 0;j < r+63>>>6;j++){
						set[i][j] = 0;
					}
					if((r&63) != 0)set[i][r>>>6] &= ~((1L<<r)-1);
				}
			}
			return this;
		}
		
		public LST set(int pos)
		{
			if(pos >= 0 && pos < n){
//				if(!get(pos))size++;
				for(int i = 0;i < set.length;i++, pos>>>=6){
					set[i][pos>>>6] |= 1L<<pos;
				}
			}
			return this;
		}
		
		public LST unset(int pos)
		{
			if(pos >= 0 && pos < n){
//				if(get(pos))size--;
				for(int i = 0;i < set.length && (i == 0 || set[i-1][pos] == 0L);i++, pos>>>=6){
					set[i][pos>>>6] &= ~(1L<<pos);
				}
			}
			return this;
		}
		
		public boolean get(int pos)
		{
			return pos >= 0 && pos < n && set[0][pos>>>6]<<~pos<0;
		}
		
		public int prev(int pos)
		{
			for(int i = 0;i < set.length && pos >= 0;i++, pos>>>=6, pos--){
				int pre = prev(set[i][pos>>>6], pos&63);
				if(pre != -1){
					pos = pos>>>6<<6|pre;
					while(i > 0)pos = pos<<6|63-Long.numberOfLeadingZeros(set[--i][pos]);
					return pos;
				}
			}
			return -1;
		}
		
		public int next(int pos)
		{
			for(int i = 0;i < set.length && pos>>>6 < set[i].length;i++, pos>>>=6, pos++){
				int nex = next(set[i][pos>>>6], pos&63);
				if(nex != -1){
					pos = pos>>>6<<6|nex;
					while(i > 0)pos = pos<<6|Long.numberOfTrailingZeros(set[--i][pos]);
					return pos;
				}
			}
			return -1;
		}
		
		private static int prev(long set, int n)
		{
			long h = Long.highestOneBit(set<<~n);
			if(h == 0L)return -1;
			return Long.numberOfTrailingZeros(h)-(63-n);
		}
		
		private static int next(long set, int n)
		{
			long h = Long.lowestOneBit(set>>>n);
			if(h == 0L)return -1;
			return Long.numberOfTrailingZeros(h)+n;
		}
		
		@Override
		public String toString()
		{
			List<Integer> list = new ArrayList<Integer>();
			for(int pos = next(0);pos != -1;pos = next(pos+1)){
				list.add(pos);
			}
			return list.toString();
		}
	}

	
