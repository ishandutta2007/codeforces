int f(int l, int r, int depth) {
	int mn = *min_element(a + l, a + r);
	int ans = r - l;
	int last = l;
	int tmp = mn - depth;
	for (int i = l; i < r; i++) {
		if (a[i] == mn) {
			if(last != i) tmp += f(last, i, mn);
			last = i + 1;
		}
	}
	if (last != r) tmp += f(last, r, mn);
	return min(ans, tmp);
}

