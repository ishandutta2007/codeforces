const ld eps=1e-7;
const int inf=1000000010;
const ll INF=1000000000000000011LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

ll n, m, k, l, x, y, t, a, b, ans;
ll A[MAXN];
ll ps[MAXN];
vector<ll> vec;

void query(){
	cin>>a>>b;
	l=b-a+1;
	ll tmp=upper_bound(A, A+n, l)-A-1;
	ans=ps[tmp]; // might be bug
	ans+=(n-tmp-1)*l;
	cout<<ans<<' ';
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	while (n--) cin>>x, vec.pb(x);
	sort(all(vec));
	vec.resize(unique(all(vec))-vec.begin());
	for (int i=1; i<vec.size(); i++) vec[i]-=vec[0]-1;
	vec[0]=1;
	
	n=vec.size();
	for (int i=0; i<n-1; i++) A[i]=vec[i+1]-vec[i];
	A[n-1]=INF;
	sort(A, A+n);
	ps[0]=A[0];
	for (int i=1; i<n; i++) ps[i]=ps[i-1]+A[i];
	/*
	debugv(vec)
	cerr<<endl;
	for (int i=0; i<n; i++) cerr<<A[i]<<' ';cerr<<endl;
	cerr<<endl;
	*/
	int q;
	cin>>q;
	while (q--) query();
	
	return 0;
}
