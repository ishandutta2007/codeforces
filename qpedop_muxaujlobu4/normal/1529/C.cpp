vector<vector<ll>>dp, g;
vector<pair<ll, ll>>ar;
void dfs(int v, int p = -1) {
	for (auto x : g[v]) {
		if (x == p)continue;
		dfs(x, v);
		dp[0][v] += max(abs(ar[v].first - ar[x].first) + dp[0][x],
			abs(ar[v].first - ar[x].second) + dp[1][x]);

		dp[1][v] += max(abs(ar[v].second - ar[x].first) + dp[0][x],
			abs(ar[v].second - ar[x].second) + dp[1][x]);
	}
}
void solve() {
	int n;
	cin >> n;
	dp.assign(2, vector<ll>(n));
	g.assign(n, vector<ll>());
	ar.resize(n);
	for (auto& x : ar)cin >> x.first >> x.second;
	for (int i = 0; i + 1 < n; ++i) {
		int a, b;
		cin >> a >> b;
		--a, --b;
		g[a].push_back(b);
		g[b].push_back(a);
	}
	dfs(0);
	cout << max(dp[0][0], dp[1][0]) << "\n";
}
