int r[N] , parent[N];
ll mx = 0;
void make_set (int v)
{
    parent[v] = v;
    r[v] = 0;
    sm[v] = -1;
}

int find_set (int v)
{
    if (v == parent[v])
        return v;
    return parent[v] = find_set (parent[v]);
}

void union_sets (int a, int b)
{
    a = find_set (a);
    b = find_set (b);
    if (a != b)
    {
        if (r[a] < r[b])
            swap (a, b);
        parent[b] = a;
        if (r[a] == r[b])
            ++r[a];
    }
    sm[a] += sm[b];
    mx = max(mx , max(sm[a] , sm[b]));
    
}
