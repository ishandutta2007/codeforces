#include<bits/stdc++.h>
#define ll long long
using namespace std;

ll read()
{
	ll s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
priority_queue<ll,vector<ll>,greater<ll>>q;

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	ll n=read(),ans=0;
	for (ll i=1;i<=n;i++) q.push(read());
	if (~n&1) q.push(0);
	for (ll i=1;i<=n/2;i++)
	{
		ll x=0;
		x+=q.top(),q.pop();
		x+=q.top(),q.pop();
		x+=q.top(),q.pop();
		q.push(x);
		ans+=x;
	}
	print(ans);
	
	return 0;
}
