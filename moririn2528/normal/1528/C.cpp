    void dfs_a(int pos,int& d){
        vl[pos]=d++;
        for(auto to:path2[pos]){
            dfs_a(to,d);
        }
        vr[pos]=d;
    }

    int op(int a,int b){return max(a,b);}
    int id(){return 0;}
    int add(int a,int b){return a+b;}
    typedef lazy_segtree<int,op,id,int,add,add,id> SEG;
    SEG seg;
    vector<int> vd;
    int cnt=0;

    int dfs(int pos){
        int a,b;
        int s=0;
        int bef_pos=-1;
        bool flag=false;
        vd.push_back(pos);

        a=seg.prod(vl[pos],vr[pos])-1;
        if(a==-1)flag=true,cnt++;
        else if(a<pos)bef_pos=a,flag=true;
        s=max(s,cnt);

        if(bef_pos!=-1)seg.apply(vl[bef_pos],vr[bef_pos],-bef_pos-1);
        if(flag)seg.apply(vl[pos],vr[pos],pos+1);

        for(auto to:path1[pos]){
            s=max(dfs(to),s);
        }

        vd.pop_back();
        if(bef_pos!=-1)seg.apply(vl[bef_pos],vr[bef_pos],bef_pos+1);
        if(flag)seg.apply(vl[pos],vr[pos],-pos-1);
        if(flag && bef_pos==-1)cnt--;

        return s;
    }

    void solve(){
        int n,m;
        int i,j,k;
        int a,b,c;
        cin>>n;
        create_path(n,path1);
        create_path(n,path2);
        a=0;
        vl.resize(n);
        vr.resize(n);
        dfs_a(0,a);
        vd.clear();
        seg=SEG(n);
        cout<<dfs(0)<<endl;
    }
