bool dp[15][130][130][2];

 
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);    
    //read(FILENAME);
    string n;
    cin >> n;
    reverse(all(n));
    dp[0][0][0][0] = true;
    for (int i = 0; i < sz(n); i++) {
        for (int cura = 0; cura <= 120; cura++) {
            for (int curb = 0; curb <= 120; curb++) {
                for (int t = 0; t < 2; t++) {
                    if (!dp[i][cura][curb][t]) {
                        continue;
                    }
                    for (int g = 0; g < 10; g++) {
                        for (int g1 = 0; g1 < 10; g1++) {
                            int f = (g + g1 + t) % 10;
                            int nt = (g + g1 + t) / 10;
                            if (f == n[i] - '0') {
                                dp[i + 1][cura + g][curb + g1][nt] = true;
                            }
                        }
                    }
                }
            }
        }
    }
    int ans = 0;
    for (int cura = 0; cura <= 120; cura++) {
        for (int curb = 0; curb <= 120; curb++) {
            if (dp[sz(n)][cura][curb][0]) {
               // cout << cura << ' ' << curb << endl;
                chkmax(ans, cura + curb);
            }
        }
    }
    cout << ans << '\n';
    return 0;
}