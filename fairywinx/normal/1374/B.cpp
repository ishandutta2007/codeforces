void solve() {
	int kol2 = 0, kol3 = 0;
	int n;
	cin >> n;
	int m = n;
	if (n == 1) {
		cout << 0 << '\n';
		return;
	}
	while(n % 2 == 0)
		++kol2, n = n / 2;
	while(n % 3 == 0)
		++kol3, n = n / 3;
	if (n != 1 || kol3 < kol2) 
		cout << -1 << '\n';
	else {
		// cout << kol2 << ' ' << kol3 << '\n';
		cout << max(0, kol3 - kol2) + kol3 << '\n';
	}
}

