template<class T>
using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
mt19937 rnd(time(0));
 
struct T{
	int l, r, d;
	bool operator<(const T &otr) const{
		return l < otr.l;
	}
};
 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int m, n, k, t;
	cin >> m >> n >> k >> t;
	vi a(m);
	cin >> a;
	sort(all(a));
	vector<T> trap(k);
	for(auto &[l, r, d]: trap){
		cin >> l >> r >> d;
	}
	int l = 0, r = m + 1;
	while(r - l > 1){
		int x = l + r >> 1;
		ll cnt = n + 1;
		vector<pii> pp;
		for(auto &[l, r, d]: trap){
			if(d > a[m - x]){
				pp.push_back({l, r});
			}
		}
		sort(all(pp));
		vector<pii> p;
		for(auto tt: pp){
			while(!p.empty() && p.back().first == tt.first){
				p.pop_back();
			}
			if(p.empty() || p.back().second < tt.second){
				p.push_back(tt);
			}
		}
		if(!p.empty()){
			cnt += 2 * (p[0].second - p[0].first + 1);
		}
		for(int i = 1; i < p.size(); i ++){
			if(p[i].first > p[i - 1].second){
				cnt += 2 * (p[i].second - p[i].first + 1);
			}
			else{
				cnt += 2 * (p[i].second - p[i - 1].second);
			}
		}
		cnt > t ? r = x : l = x;
	}
	cout << l;
	return 0;
}
