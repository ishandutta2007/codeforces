fn run() {
    input! {
        n: usize,
        k: u64,
        a: [u64; n],
    }
    let mut a = a;
    a.sort();
    a = a[(n / 2)..].to_vec();
    let mut l = 1;
    let mut r = 2000000000 + 1;
    while r - l > 1 {
        let m = (l + r) / 2;
        let mut c = 0;
        for a in &a {
            if *a < m {
                c += m - *a;
            }
        }
        if c <= k {
            l = m;
        } else {
            r = m;
        }
    }
    println!("{}", l);
}

fn main() {
    run();
}