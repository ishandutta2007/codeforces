void run_case() {
    int64_t A, S;
    cin >> A >> S;
    string B;

    while (S > 0) {
        int64_t D = A % 10;
        int64_t SD = S % 10;
        int64_t BD = (10 + SD - D) % 10;

        if (D + BD == SD) {
            B = to_string(BD) + B;
            A /= 10;
            S /= 10;
        } else if (D + BD == S % 100) {
            B = to_string(BD) + B;
            A /= 10;
            S /= 100;
        } else {
            cout << -1 << '\n';
            return;
        }
    }

    if (A != 0) {
        cout << -1 << '\n';
        return;
    }

    while (B.size() > 1 && B.front() == '0')
        B = B.substr(1);

    cout << B << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}