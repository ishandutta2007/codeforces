template<class T>
inline bool chmax(T &a, T b) {
    if(a < b) {
        a = b;
        return true;
    }
    return false;
}

template<class T>
inline bool chmin(T &a, T b) {
    if(a > b) {
        a = b;
        return true;
    }
    return false;
}

const long long INF = 1e18;
//const ll mod = 1000000007;

void solve() {
    string S;
    cin >> S;
    ll ans = 0;
    ll mini = 0;
    ll now = 0;
    for(int i = 0; i < S.size(); i++) {
        auto c = S[i];
        if(c == '+') now++;
        else now--;
        if(chmin(mini, now)) {
            ans += i + 1;
        }
    }
    ans += S.size();
    cout << ans << endl;
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    ll T;
    cin >> T;
    while(T--) solve();
    return 0;
}