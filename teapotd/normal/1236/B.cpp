constexpr int MOD = 1e9+7;

ll modPow(ll a, ll e, ll m) { // a^e mod m
    ll t = 1 % m;
    while (e) {
        if (e % 2) t = t*a % m;
        e /= 2; a = a*a % m;
    }
    return t;
}

void run() {
    ll n, m; cin >> n >> m;
    ll opt = modPow(2, m, MOD) - 1;
    ll ans = modPow(opt, n, MOD);
    cout << ans << endl;
}