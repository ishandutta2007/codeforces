	void solve()
	{
		int n = ni();
		int[] a = new int[n];
		int[] b = new int[n];
		for(int i = 0;i < n;i++){
			a[i] = ni();
			b[i] = ni();
		}
		int[] xs = new int[2*n];
		for(int i = 0;i < n;i++){
			xs[i] = a[i];
			xs[i+n] = b[i];
		}
		int[] imap = shrinkX(xs);
		int m = imap.length;
		int[] g = new int[m];
		for(int i = 0;i < m;i++)g[i] = i;
		for(int i = 0;i < n;i++){
			int d = g[xs[i]]; g[xs[i]] = g[xs[i+n]]; g[xs[i+n]] = d;
		}
		
		int[] ft = new int[2*m+1+2];
		long ret = 0;
		long all = 0;
		for(int i = 0;i < m;i++){
			ret += all - sumFenwick(ft, 2*g[i]);
			addFenwick(ft, 2*g[i], 1);
			all++;
			
			if(i == m-1)break;
			long del = imap[i+1] - imap[i] - 1;
			ret += (all - sumFenwick(ft, 2*i+1)) * del;
			addFenwick(ft, 2*i+1, (int)del);
			all += del;
		}
		out.println(ret);
	}
	
	public static int sumFenwick(int[] ft, int i)
	{
		int sum = 0;
		for(i++;i > 0;i -= i&-i)sum += ft[i];
		return sum;
	}
	
	public static void addFenwick(int[] ft, int i, int v)
	{
		if(v == 0 || i < 0)return;
		int n = ft.length;
		for(i++;i < n;i += i&-i)ft[i] += v;
	}
	
	public static int[] shrinkX(int[] a) {
		int n = a.length;
		long[] b = new long[n];
		for (int i = 0; i < n; i++)
			b[i] = (long) a[i] << 32 | i;
		Arrays.sort(b);
		int[] ret = new int[n];
		int p = 0;
		ret[0] = (int) (b[0] >> 32);
		for (int i = 0; i < n; i++) {
			if (i > 0 && (b[i] ^ b[i - 1]) >> 32 != 0) {
				p++;
				ret[p] = (int) (b[i] >> 32);
			}
			a[(int) b[i]] = p;
		}
		return Arrays.copyOf(ret, p + 1);
	}
	
