#define all(x) x.begin(), x.end()
using namespace std;

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    a.push_back(0);
    sort(all(a));
    if (a.back() - a[a.size()-2] >= 2)
        cout << "NO\n";
    else
        cout << "YES\n";
}
signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);cout.tie(0);
    int t;
    cin >> t;
    while (t--) solve();
}
