ll rec(int i, int l, int r){
	char ind = i+'a';
	if(l == r) return (ind != s[l]);
	int mid = (l+r+1)/2, lct = 0, rct = 0, ct = (r-l+1)/2;
	for(int i = l; i < mid; i++) if(s[i] == ind) lct++;
	for(int i = mid; i <= r; i++) if(s[i] == ind) rct++;
	return min(rec(i+1,l,mid-1)+ct-rct,rec(i+1,mid,r)+ct-lct);
}

