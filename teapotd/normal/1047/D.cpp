ll fast(ll n, ll m) {
    if (n > m) swap(n, m);
    if (n >= 3) return n*m/2*2;

    if (n == 1) {
        return m/6*6 + max(m%6-3, 0LL)*2;
    } else if (n == 2) {
        if (m == 2) return 0;
        else if (m == 3) return 4;
        else if (m == 7) return 12;
        else return m*2;
    } else {
        assert(0);
    }
}

