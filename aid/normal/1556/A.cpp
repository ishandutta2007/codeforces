int c, d;

bool read() {
  if (scanf("%d%d", &c, &d) < 2) {
    return false;
  }
  return true;
}

void solve() {
  if ((c & 1) != (d & 1)) {
    printf("-1\n");
    return;
  }
  if (c == 0 && d == 0) {
    printf("0\n");
    return;
  }
  if (c == d) {
    printf("1\n");
    return;
  }
  printf("2\n");
}

