string solve(const string &s) {
    if (s.length() % 2)
        return s;
    string a = solve(s.substr(0, s.length() / 2)), b = solve(s.substr(s.length() / 2));
    return a < b ? a + b : b + a;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("input.txt", "r", stdin);
    //freopen("output.txt", "w", stdout);

    string a, b;
    cin >> a >> b;

    cout << (solve(a) == solve(b) ? "YES" : "NO") << "\n";

    return 0;
}