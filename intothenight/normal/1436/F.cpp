int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int m;
	cin >> m;
	const int mx = 1e5;
	number_theory<mx> nt;
	vector<int> freq(mx + 1);
	for(auto rep = 0; rep < m; ++ rep){
		int x, f;
		cin >> x >> f;
		freq[x] = f;
	}
	Zp res = 0;
	for(auto x = 1; x <= mx; ++ x){
		Zp sum = 0, sqsum = 0, cur = 0;
		long long n = 0;
		for(auto y = x; y <= mx; y += x){
			sum += Zp(freq[y]) * y;
			sqsum += Zp(freq[y]) * y * y;
			n += freq[y];
		}
		if(n >= 2){
			cur = ((n - 1) * sqsum + n * (sum * sum - sqsum) / 2) * (Zp(2) ^ n - 2);
			res += nt.mu[x] * cur;
			debug(x, n, cur, sqsum, sum);
		}
	}
	cout << res << "\n";
	return 0;
}

