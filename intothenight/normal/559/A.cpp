using namespace std;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	array<int, 6> a;
	for(auto i = 0; i < 6; ++ i){
		cin >> a[i];
	}
	cout << (a[0] + a[1] + a[2]) * (a[0] + a[1] + a[2]) - a[0] * a[0] - a[2] * a[2] - a[4] * a[4] << "\n";
	return 0;
}

