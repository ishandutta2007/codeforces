int main() {
    cin.tie(nullptr);
    ios::sync_with_stdio(false);
    cout << fixed << setprecision(20);

    int ca = 1;
    for (int tm = 0; tm < ca; tm++) {
        string s;
        cin >> s;
        int n = int(s.size()) + 1;
        V<Mint> dp(n + 2);
        dp[0] = Mint(1);
        for (int i = 0; i < n; i++) {
            if (s[i] == 'w' || s[i] == 'm') {
                continue;
            }
            if (s[i] == 'u' && s[i + 1] == 'u') {
                dp[i + 2] += dp[i];
            }
            if (s[i] == 'n' && s[i + 1] == 'n') {
                dp[i + 2] += dp[i];
            }
            dp[i + 1] += dp[i];
        }
        dbg(dp);
        cout << dp[n] << endl;
    }
    return 0;
}