#pragma GCC optimize("O3")
#include <bits/stdc++.h>
using namespace std;
int x;
int main(){
  cin >> x;
  while( x % 10 == 0 ) x /= 10;
  vector<int> v;
  while( x ){
    v.push_back( x % 10 );
    x /= 10;
  }
  for( size_t i = 0 ; i < v.size() ; i ++ )
    if( v[ i ] != v[ (int)v.size() - i - 1 ] ){
      puts( "NO" );
      exit(0);
    }
  puts( "YES" );
}