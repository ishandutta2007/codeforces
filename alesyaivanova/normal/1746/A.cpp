const int maxn = 109;
int a[maxn];
int n, k;

void solve() {
    cin >> n >> k;
    int mx = 0;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        mx = max(mx, a[i]);
    }
    if (mx == 0) {
        cout << "NO\n";
    } else {
        cout << "YES\n";
    }
}

