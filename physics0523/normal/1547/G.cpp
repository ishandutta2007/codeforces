#include <cassert>
#include <vector>

namespace atcoder {

struct scc_graph {
  public:
    scc_graph() : internal(0) {}
    scc_graph(int n) : internal(n) {}

    void add_edge(int from, int to) {
        int n = internal.num_vertices();
        assert(0 <= from && from < n);
        assert(0 <= to && to < n);
        internal.add_edge(from, to);
    }

    std::vector<std::vector<int>> scc() { return internal.scc(); }

  private:
    internal::scc_graph internal;
};

}  // namespace atcoder

using namespace std;
using namespace atcoder;
using Graph=vector<vector<int>>;

int ope(int a,int b){
  if(a==-1 || b==-1){return -1;}
  return min(2,a+b);
}

int main(){
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int t;
  cin >> t;
  while(t>0){
    t--;
    int n,m;
    cin >> n >> m;
    vector<int> u(m),v(m);
    scc_graph scc_g(n);
    for(int i=0;i<m;i++){
      cin >> u[i] >> v[i];
      u[i]--;v[i]--;
      scc_g.add_edge(u[i],v[i]);
    }
    vector<int> cid(n);
    vector<int> csz(n,0);
    int id=0;
    auto scc=scc_g.scc();
    for(auto &v : scc){
      csz[id]=v.size();
      for(auto &nx : v){
        cid[nx]=id;
      }
      id++;
    }
    Graph g(id);
    for(int i=0;i<m;i++){
      g[cid[u[i]]].push_back(cid[v[i]]);
      if(u[i]==v[i]){csz[cid[u[i]]]=1e9;}
    }
    vector<int> dp(id,0);
    dp[cid[0]]=1;
    for(int i=0;i<id;i++){
      if(dp[i]>0 && csz[i]>1){dp[i]=-1;}
      for(auto &nx : g[i]){
        dp[nx]=ope(dp[nx],dp[i]);
      }
    }
    for(int i=0;i<n;i++){
      if(i){cout << ' ';}
      cout << dp[cid[i]];
    }cout << '\n';
  }
  return 0;
}