static int fix(vector<int> &v, int p)
{
    int pos = upper_bound(RA(v), p) - v.begin();
    if (pos == SZ(v))
        v.push_back(p);
    else
        v[pos] = p;
    return pos;
}

int main(int argc, const char **argv)
{
    redirect(argc, argv);
    int N, M, C;
    cin >> N >> M >> C;
    vector<int> left, right;
    while (SZ(left) + SZ(right) < N)
    {
        int p;
        cin >> p;
        p--;
        int pos;
        if (p < C / 2)
            pos = fix(left, p);
        else
            pos = N - 1 - fix(right, C - 1 - p);
        cout << pos + 1 << endl;
    }

    return 0;
}