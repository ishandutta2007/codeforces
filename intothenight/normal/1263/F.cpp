typedef tuple<int, int, int> tpl;
template<class T>
using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
mt19937 rnd(time(0));



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	int a;
	cin >> a;
	vector<vi> adj1(a);
	for(int u = 1; u < a; u ++){
		int v;
		cin >> v, v --;
		adj1[v].push_back(u);
	}
	vector<tpl> d1(a, {2e9, -2e9, 1});
	for(int i = 0; i < n; i ++){
		int x;
		cin >> x, x --;
		d1[x] = {i, i + 1, 1};
	}
	int b;
	cin >> b;
	vector<vi> adj2(b);
	for(int u = 1; u < b; u ++){
		int v;
		cin >> v, v --;
		adj2[v].push_back(u);
	}
	vector<tpl> d2(b, {2e9, -2e9, 1});
	for(int i = 0; i < n; i ++){
		int x;
		cin >> x, x --;
		d2[x] = {i, i + 1, 1};
	}
	vector<vector<pii>> t(n);
	auto dfs = [&](int u, vector<vi> &adj, vector<tpl> &d, auto &dfs)->void{
		for(auto v: adj[u]){
			dfs(v, adj, d, dfs);
			auto [l, r, w] = d[v];
			auto [ll, rr, ww] = d[u];
			d[u] = {min(l, ll), max(r, rr), w + ww};
		}
		if(u){
			auto [l, r, w] = d[u];
			t[l].push_back({r, w});
		}
	};
	dfs(0, adj1, d1, dfs);
	dfs(0, adj2, d2, dfs);
	vi res(n + 1);
	for(int l = 0; l < n; l ++){
		for(auto [r, w]: t[l]){
			res[r] = max(res[r], res[l] + w);
		}
	}
	cout << res[n];
	return 0;
}
