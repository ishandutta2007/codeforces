int pow(int a, int b) {
	if (b == 0)
		return 1; 
	if (b % 2 == 0) {
		int z = pow(a, b / 2);
		return z * z;
	}
	return pow(a, b - 1) * a;
}

void solve() {
	int n, k;
	cin >> n >> k;
	vector<int> x(n);
	for(int i = 0; i < n; ++i)
		cin >> x[i];
	for(int i = 0; i < n; ++i) {
		int tmp;
		cin >> tmp;
	}
	vector<int> kol(n + 1);
	sort(all(x));
	int ans = 0;
	for(int i = n - 1; i >= 0; --i) {
		int z = upper_bound(all(x), x[i] + k) - x.begin() - i;
		kol[i] = max(z, kol[i + 1]);
		ans = max(ans, z + kol[z + i]);
	}
	cout << ans << '\n';
}

