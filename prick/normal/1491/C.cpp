mt19937_64 rng(chrono::steady_clock::now().time_since_epoch().count());
ll myRand(ll mod) {
    return (ull)rng() % mod;
}

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    vector<int> jumps(n + 1);
    int ans = 0;
    for (int start = 0; start < n; ++start) {
        jumps[start + 1] += max(0ll, jumps[start] - (a[start] - 1));
        ans += max(0ll, a[start] - jumps[start] - 1);
        
        a[start] = min(a[start], n - 1 - start);
        while (a[start] > 1) {
            ++jumps[start + a[start]];
            --a[start];
        }
    }
    cout << ans << '\n';
}

