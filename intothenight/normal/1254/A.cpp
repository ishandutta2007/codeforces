using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
mt19937 rnd(time(0));


 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	auto conv = [&](int c){
	    if(c < 26){
	    	return char(c + 'a');
	    }
	    if(c < 52){
	    	return char(c - 26 + 'A');
	    }
	    return char(c - 52 + '0');
	};
	int tt;
	cin >> tt;
	while(tt --){
		int r, c, k;
		cin >> r >> c >> k;
		vs f(r);
		cin >> f;
		int cnt = 0;
		for(auto s: f){
			for(auto c: s){
				cnt += (c == 'R');
			}
		}
		auto next = [&](pii &p){
			if(p.first % 2){
				p.second --;
				if(p.second < 0){
					p.first ++, p.second = 0;
				}
			}
			else{
				p.second ++;
				if(p.second == c){
					p.first ++, p.second = c - 1;
				}
			}
		};
		vs res(r, string(c, 'x'));
		int num = 0;
		vi t(k, cnt / k);
		for(int i = 0; i < cnt % k; i ++){
			t[i] ++;
		}
		for(pii p = {0, 0}; p.first < r; next(p)){
			if(f[p.first][p.second] == 'R'){
				if(!t.back()){
					t.pop_back();
					num ++;
				}
				t.back() --;
				res[p.first][p.second] = conv(num);
			}
			else{
				res[p.first][p.second] = conv(num);
			}
		}
		for(auto s: res){
			cout << s << "\n";
		}
	}
	return 0;
}
 
