#include <bits/stdc++.h>
using namespace std;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n;
	cin >> n;
	vector<int> a(n);
	for(auto i = 0; i < n; ++ i){
		cin >> a[i];
	}
	cout << max<long long>((accumulate(a.begin(), a.end(), 0LL) + n - 2) / (n - 1), *max_element(a.begin(), a.end())) << "\n";
	return 0;
}

