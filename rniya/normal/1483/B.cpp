void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    cin >> a;

    vector<int> left(n), right(n);
    set<int> cand;
    for (int i = 0; i < n; i++) {
        left[i] = (i + n - 1) % n, right[i] = (i + 1) % n;
        if (gcd(a[i], a[(i + 1) % n]) == 1) {
            cand.emplace(i);
            cand.emplace(i + n);
        }
    }

    if (cand.empty()) {
        cout << 0 << '\n';
        return;
    }

    vector<int> ans;
    int rest = n;
    auto erase = [&](int x) {
        ans.emplace_back(x + 1);
        rest--;
        if (cand.count(x)) {
            cand.erase(x);
            cand.erase(x + n);
        }
        if (rest == 0) return;
        int l = left[x], r = right[x];
        right[l] = r, left[r] = l;
        if (gcd(a[l], a[r]) == 1) {
            cand.emplace(l);
            cand.emplace(l + n);
        }
    };
    int cur = *cand.begin();

    while (1) {
        cand.erase(cur);
        cand.erase(cur + n);
        erase(right[cur]);
        auto itr = cand.upper_bound(cur);
        if (itr == cand.end()) break;
        cur = (*itr) % n;
    }

    cout << ans.size();
    for (int& x : ans) cout << ' ' << x;
    cout << '\n';
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t;
    cin >> t;
    for (; t--;) solve();
    return 0;
}