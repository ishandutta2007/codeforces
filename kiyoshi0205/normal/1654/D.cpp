vector<int> linear_sieve(int N){
  vector<int> p,lpf(N+1,0);
  for(int i=2;i<=N;++i){
    if(lpf[i]==0){
      p.emplace_back(i);
      lpf[i]=i;
    }
    for(auto x:p){
      if(x>lpf[i]||i*x>N)break;
      lpf[i*x]=x;
    }
  }
  return lpf;
}


