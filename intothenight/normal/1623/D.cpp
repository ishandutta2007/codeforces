using modular = modular_base<integral_constant<decay<decltype(mod)>::type, mod>>;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	auto __solve_tc = [&](auto __tc_num)->int{
		int n, m, sx, sy, ox, oy;
		modular p;
		cin >> n >> m >> sx >> sy >> ox >> oy >> p, -- sx, -- sy, -- ox, -- oy, p /= 100;
		auto invp = 1 / (1 - p);
		int x = sx, y = sy, dx = 1, dy = 1;
		if(clamp(x + dx, 0, n - 1) != x + dx){
			dx = -dx;
		}
		if(clamp(y + dy, 0, m - 1) != y + dy){
			dy = -dy;
		}
		modular c = 1, d = 0;
		map<array<int, 4>, array<modular, 2>> mp;
		while(!mp.contains({x, y, dx, dy})){
			mp[{x, y, dx, dy}] = {c, d};
			if(x == ox || y == oy){
				tie(c, d) = pair{c * invp, d * invp - 1};
			}
			else{
				-- d;
			}
			x += dx;
			y += dy;
			if(clamp(x + dx, 0, n - 1) != x + dx){
				dx = -dx;
			}
			if(clamp(y + dy, 0, m - 1) != y + dy){
				dy = -dy;
			}
		}
		auto [c2, d2] = mp[{x, y, dx, dy}];
		cout << (d2 - d) / (c - c2) * c + d << endl;
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

