int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, a;
	cin >> n >> a;
	vi cnt(1e6 + 1);
	set<pii> q;
	for(int i = 1; i <= 1000000; i ++){
		q.insert({0, i});
	}
	q.erase({0, a});
	vi ok(1e6 + 1, 1);
	for(int i = 0, acnt = 0; i < n; i ++){
		int c;
		cin >> c;
		if(c == a){
			acnt ++;
			while(!q.empty() && q.begin()->first < acnt){
				ok[q.begin()->second] = false;
				q.erase(q.begin());
			}
		}
		else{
			if(ok[c]){
				q.erase({cnt[c], c});
				q.insert({++ cnt[c], c});
			}
		}
		/*cout << "i = " << i << "\n";
		for(auto x: q){
			if(x.second < 10) cout << x << " ";
		}
		cout << "\n";*/
	}
	if(q.empty()){
		cout << -1;
	}
	else{
		cout << q.begin()->second;
	}
	return 0;
}

