fn main() {
	let (mut rin,mut rout) = rio();

	l!(s,t = rin.vb());
	let n = t.len();
	l!(mut l,r = v!([n+1]));
	l.push(0);
	let mut j = 0;
	for i in 0..s.len() {
		if j < n && s[i] == t[j] {
			j += 1;
			l.push(i+1);
		}
	}
	r.push(s.len());
	j = n;
	for i in (0..s.len()).rev() {
		if j > 0 && s[i] == t[j-1] {
			j -= 1;
			r.push(i);
		}
	}
	r.reverse();
	let mut ans = 0;
	for i in 0..=n {
		max!(ans, r[i] - l[i]);
	}
	writeln!(rout, "{}", ans).unwrap();
}