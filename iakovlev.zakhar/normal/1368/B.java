	public static void quickSort(int[] a, int from, int to) {
		if (to - from <= 1) {
			return;
		}
		int i = from;
		int j = to - 1;
		int x = a[from + (new Random()).nextInt(to - from)];
		while (i <= j) {
			while (a[i] < x) {
				i++;
			}
			while (a[j] > x) {
				j--;
			}
			if (i <= j) {
				int t = a[i];
				a[i] = a[j];
				a[j] = t;
				i++;
				j--;
			}
		}
		quickSort(a, from, j + 1);
		quickSort(a, j + 1, to);
	}

	public long gcd(long x, long y) {
		if (y == 0) {
			return x;
		}
		if (x == 0) {
			return y;
		}
		return gcd(y, x % y);
	}

	public boolean prime(long x) {
		for (int i = 2; i * i <= x; i++) {
			if (x % i == 0) {
				return false;
			}
		}
		return true;
	}

	public long pow(long x, long p) {
		if (p == 0) {
			return 1;
		}
		long t = pow(x, p / 2);
		t *= t;
		t %= mod;
		if (p % 2 == 1) {
			t *= x;
			t %= mod;
		}
		return t;
	}

	public class Fenvik {
		long[] sum;

		public Fenvik(int n) {
			sum = new long[n];
		}

		public void add(int x, long d) {
			for (int i = x; i < sum.length; i = (i | (i + 1))) {
				sum[i] += d;
			}
		}

		public long sum(int r) {
			long ans = 0;
			for (int i = r; i >= 0; i = (i & (i + 1)) - 1) {
				ans += sum[i];
			}
			return ans;
		}

		public long sum(int l, int r) {
			if (l > r) {
				return 0;
			}
			return sum(r) - sum(l - 1);
		}
	}

	public class Pair implements Comparable<Pair> {
		int x;
		int y;

		public Pair(int x, int y) {
			this.x = x;
			this.y = y;
		}

		@Override
		public int compareTo(Pair o) {
			if (x > o.x) {
				return 1;
			}
			if (x < o.x) {
				return -1;
			}
			if (y > o.y) {
				return 1;
			}
			if (y < o.y) {
				return -1;
			}
			return 0;
		}
	}

	long mod = 1000000007;

	public class Vector implements Comparable<Vector> {
		int x;
		int y;

		public Vector(int x, int y) {
			this.x = x;
			this.y = y;
		}

		public String toString() {
			return x + " " + y;
		}

		public int quater() {
			if (x > 0 && y >= 0) {
				return 1;
			}
			if (y > 0 && x <= 0) {
				return 2;
			}
			if (y <= 0 && x < 0) {
				return 3;
			}
			return 4;
		}

		@Override
		public int compareTo(Vector o) {
			if (quater() != o.quater()) {
				return quater() - o.quater();
			}
			return -dp(this, o);
		}
	}

	public int dp(Vector a, Vector b) {
		return a.x * b.y - a.y * b.x;
	}


	public void solve() {
		long k = in.nextLong();
		long prod = 1;
		long[] n = new long[10];
		for (int i = 0; i < n.length; i++) {
			n[i] = 1;
		}
		int i = 0;
		while (prod < k) {
			prod /= n[i];
			n[i]++;
			prod *= n[i];
			i++;
			i %= n.length;
		}
		String s = "codeforces";
		for (int j = 0; j < n.length; j++) {
			for (int l = 0; l < n[j]; l++) {
				out.print(s.charAt(j));
			}
		}
	}

