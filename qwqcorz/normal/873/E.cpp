#include<bits/stdc++.h>
#define mk make_pair
#define fi first
#define se second
using namespace std;
const int N=3e3+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
pair<int,int>a[N],st[N][15];
int c[N],Log[N],out[N];
struct answer
{
	int i,j,k;
	bool operator <(const answer &a)const
	{
		return mk(mk(c[i],c[j]),c[k])<mk(mk(c[a.i],c[a.j]),c[a.k]);
	}
}ans=(answer){0,0,0};
pair<int,int>query(int l,int r)
{
	int k=Log[r-l+1];
	return max(st[l][k],st[r-(1<<k)+1][k]);
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	c[0]=-5005;
	int n=read();
	for (int i=1;i<=n;i++) a[i]=mk(read(),i);
	sort(a+1,a+1+n,greater<pair<int,int>>());
	for (int i=2;i<=n;i++) Log[i]=Log[i/2]+1;
	for (int i=1;i<=n;i++) st[i][0]=mk(c[i]=a[i].fi-a[i+1].fi,i);
	for (int j=1;j<=12;j++)
	for (int i=1;i+(1<<j)-1<=n;i++)
	st[i][j]=max(st[i][j-1],st[i+(1<<(j-1))][j-1]);
	for (int i=1;i<n;i++)
	for (int j=i+1;j<n;j++)
	if (i<=(j-i)*2&&j-i<=i*2)
	{
		int l=j+1,r=n;
		int low=max((i+1)/2,(j-i+1)/2),up=min(i*2,(j-i)*2);
		l=max(l,j+low),r=min(r,j+up);
		if (l>r) continue;
		int k=query(l,r).se;
		ans=max(ans,(answer){i,j,k});
	}
	for (int i=1;i<=ans.i;i++) out[a[i].se]=1;
	for (int i=ans.i+1;i<=ans.j;i++) out[a[i].se]=2;
	for (int i=ans.j+1;i<=ans.k;i++) out[a[i].se]=3;
	for (int i=ans.k+1;i<=n;i++) out[a[i].se]=-1;
	for (int i=1;i<=n;i++) print(out[i],' ');
	
	return 0;
}
