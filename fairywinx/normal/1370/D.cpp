bool check(int n, vector<int> a, int m, int k) {
	if (n == 0)
		return false;
	vector<int> dp(n + 1);
	if (a[0] <= m)
		dp[0] = 1;
	if (a.size() >= 2) {
		if (a[1] <= m)
			dp[1] = 1;
	}	
	dp[1] = max(dp[0], dp[1]);
	for(int i = 2; i < n; ++i) {
		dp[i] = dp[i - 1];
		if (a[i] <= m)
			dp[i] = max(dp[i], dp[i - 2] + 1);
	}
	// for(int i = 0; i < n; ++i)
	// 	cout << a[i] << ' ';
	// cout << dp[n - 1] << ':' << k << '\n';
	if (dp[n - 1] >= k / 2)
		return true;
	return false;
}

bool _check(int n, vector<int> a, int m, int k) {
	vector<int> b;
	if (k % 2 == 1) {
		if (check(n, a, m, k + 1))
			return true;
	}
	for(int i = 0; i < n - 1; ++i) 
		b.push_back(a[i]);
	if (check(n - 1, b, m, k + 1))
		return true;
	b.clear();
	for(int i = 1; i < n; ++i)
		b.push_back(a[i]);
	if (check(n - 1, b, m, k + 1))
		return true;
	b.clear();
	for(int i = 1; i < n - 1; ++i)
		b.push_back(a[i]);
	if (check(n - 2, b, m, k))
		return true;
	return false;	
}

