void solve() {
  int x, y;
  cin >> x >> y;
  int s = (int) round(sqrt(x * x + y * y));
  if (x + y == 0) {
    cout << 0;
  } else if (s * s == x * x + y * y) {
    cout << 1;
  } else {
    cout << 2;
  }
  cout << '\n';
}

