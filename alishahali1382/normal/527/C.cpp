using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<pii, int> piii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<(x)<<endl;
#define debugp(x) cerr<<#x<<"= {"<<(x.first)<<", "<<(x.second)<<"}"<<endl;
#define debug2(x, y) cerr<<"{"<<#x<<", "<<#y<<"} = {"<<(x)<<", "<<(y)<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back
#define kill(x) return cout<<x<<'\n', 0;

const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

ll n, m, k, w, h, x, y, t, a, b;
set<pll> V, H;
set<pll> X, Y;
char ch;

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>w>>h>>n;
	V.insert({0, w});
	H.insert({0, h});
	X.insert({w, 0});
	Y.insert({h, 0});
	
	while (n--){
		cin>>ch>>x;
		if (ch=='V'){
			pll p=*--V.lower_bound({x, inf});
			V.erase(p);
			X.erase({p.second-p.first, p.first});
			V.insert({p.first, x});
			V.insert({x, p.second});
			X.insert({x-p.first, p.first});
			X.insert({p.second-x, x});
		}
		if (ch=='H'){
			pll p=*--H.lower_bound({x, inf});
			H.erase(p);
			Y.erase({p.second-p.first, p.first});
			H.insert({p.first, x});
			H.insert({x, p.second});
			Y.insert({x-p.first, p.first});
			Y.insert({p.second-x, x});
		}
		
		cout<<(*X.rbegin()).first*(*Y.rbegin()).first<<'\n';
	}
	
	
	return 0;
}
