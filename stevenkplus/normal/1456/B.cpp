const int inf = 1000 << 20;
// ============= Solution ============= //
int sub_to_errichto() { // fml
   int n;
   cin >> n;
   vector<int> v(n);
   for (int &i: v) cin >> i;
   if (n > 100) {
      cout << 1 << "\n";
      return 0;
   }

   // consider xor(i...j) vs (j + 1...k) for all i <= j < k
   int ans = inf;
   for (int i = 0; i < n; ++i) {
      int a = 0;
      for (int j = i; j < n; ++j) {
         a ^= v[j];
         int b = 0;
         for (int k = j + 1; k < n; ++k) {
            b ^= v[k];
            pp(i, j, k);
            pp(a, b);
            if (a > b) {
               ans = min(ans, j - i + k - j - 1);
            }
         }
      }
   }

   if (ans == inf) ans = -1;
   cout << ans << "\n";
   return 0;
}

