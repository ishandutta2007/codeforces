int a[N],b[N];
void solve()
{
    int n=read();
    for(int i=0;i<n;++i)cin>>a[i];
    if(n&1)return cout<<"NO\n",void();
    sort(a,a+n);
    int m=n>>1;
    for(int i=0;i<m;++i)b[ls]=a[i],b[rs]=a[i+m];
    for(int i=1;i<n;++i)if(b[i]==b[i-1])return cout<<"NO\n",void();
    cout<<"YES\n";
    for(int i=0;i<n;++i)cout<<b[i]<<' ';
    cout<<'\n';
}
