const int MN = 3003;
ll n, m, i, j, dp[2][MN], arr[MN], rev[MN], go, ans;
map<ll,ll> mp;

int main(){
    for(scanf("%lld",&n),i=1;i<=n;i++){
        scanf("%lld",&arr[i]); arr[i]-=i;
        mp[arr[i]]=0;
    }
    i=0; auto it=mp.begin();
    for(;it!=mp.end();it++)
        it->second=++i,rev[i]=it->first;
    m=mp.size();
    for(i=1;i<=n;i++){
        go=1LL<<60;
        ll idx=mp[arr[i]];
        for(j=1;j<=m;j++){
            go=min(go,dp[(i+1)&1][j]);
            dp[i&1][j]=abs(rev[idx]-rev[j])+go;
        }
    }
    ans=1LL<<60;
    for(j=1;j<=m;j++)
        ans=min(ans,dp[n&1][j]);
    printf("%lld\n",ans);
    return 0;
}