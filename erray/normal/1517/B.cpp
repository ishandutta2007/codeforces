int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int T;
  cin >> T;
  while (T--) {
    int n, m;
    cin >> n >> m;
    vector<vector<int>> a(n, vector<int>(m));
    for (int i = 0; i < n; ++i) {
      for (int j = 0; j < m; ++j) {
        cin >> a[i][j];
      }
      sort(a[i].begin(), a[i].end());
    }
    vector<pair<int, int>> ord;
    for (int i = 0; i < n; ++i) {
      for (int j = 0; j < m; ++j) {
        ord.emplace_back(i, j);
      }
    }

    sort(ord.begin(), ord.end(), [&](auto x, auto y) {
      return a[x.first][x.second] < a[y.first][y.second];
    });

    debug(ord);
    vector<vector<bool>> vis(n, vector<bool>(m));
    vector<int> p(n, m - 1);
    vector<vector<int>> ans(m);
    int cur = 0;
    for (auto[x, y] : ord) {
      if (vis[x][y]) {
        continue;
      }
      vis[x][y] = true;
      for (int i = 0; i < n; ++i) {
        if (i == x) {
          ans[cur].push_back(a[x][y]);
          continue;
        }
        while (vis[i][p[i]]) {
          --p[i];
        }
        ans[cur].push_back(a[i][p[i]]);
        vis[i][p[i]] = true;
        --p[i];
      }
      ++cur;
    }
    for (int i = 0; i < n; ++i) {
      for (int j = 0; j < m; ++j) {
        cout << ans[j][i] << ' ';
      }
      cout << '\n';
    }
  }
}