ll solve(vector<int> const& a, vector<int> const& b) {
  int i2 = 0;
  ll ans = 0;
  for (int i = 0; i < sz(a); ++i) {
    if (a[i]) {
      while (!b[i2]) ++i2;
      ans += abs(i - i2);
      ++i2;
    }
  }
  return ans;
}

void solve() {
  int n;
  cin >> n;
  vector<int> a(n);
  cin >> a;
  for (int &x : a) x %= 2;
  int c1 = 0;
  for (int i = 0; i < n; ++i)
    if (a[i] == 1) {
      c1++;
    }
  vector<int> b0(n), b1(n);
  for (int i = 0; i < n; ++i)
    (i % 2 == 0 ? b0 : b1)[i] = 1;
  if (n % 2 == 0) {
    if (c1 != n / 2) {
      cout << "-1\n";
      return;
    }
    cout << min(solve(a, b0), solve(a, b1)) << "\n";
  } else {
    if (c1 == n / 2) {
      cout << solve(a, b1) << "\n";
    } else if (c1 == n / 2 + 1) {
      cout << solve(a, b0) << "\n";
    } else {
      cout << "-1\n";
    }
  }
}

