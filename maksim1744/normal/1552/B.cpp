void test_case(int test) {
    int n;
    cin >> n;
    using S = array<int, 5>;
    int num = -1;
    const int inf = 1e9;
    S cur;
    cur.fill(inf);
    vector<array<int, 5>> v(n);
    for (int i = 0; i < n; ++i) {
        array<int, 5> a;
        for (int i = 0; i < 5; ++i) {
            cin >> a[i];
        }
        v[i] = a;

        int cnt = 0;
        for (int i = 0; i < 5; ++i) {
            if (cur[i] < a[i])
                ++cnt;
        }
        if (cnt >= 3) continue;
        swap(a, cur);
        num = i;
    }
    bool ok = true;
    for (int i = 0; i < n; ++i) {
        if (i == num) continue;
        int cnt = 0;
        for (int j = 0; j < 5; ++j) {
            if (v[i][j] < v[num][j])
                ++cnt;
        }
        if (cnt >= 3) {
            ok = false;
            break;
        }
    }
    cout << (ok ? num + 1 : -1) << '\n';
}

