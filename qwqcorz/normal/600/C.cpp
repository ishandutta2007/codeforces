{
	(void)Goodbye,(void)Wangang;
	scanf("%s",a+1);
	int n=strlen(a+1);
	for (int i=1;i<=n;i++) tot[a[i]-'a']++;
	for (int i=0;i<26;i++) if (tot[i]&1) x.push_back(i);
	for (int i=0;i<(int)x.size()/2;i++) tot[x[i]]++,tot[x[x.size()-1-i]]--;
	for (int i=0;i<26;i++) for (int j=1;j*2<=tot[i];j++) putchar(i+'a');
	for (int i=0;i<26;i++) if (tot[i]%2==1) putchar(i+'a');
	for (int i=25;i>=0;i--) for (int j=1;j*2<=tot[i];j++) putchar(i+'a');
	
	return 0;
}
