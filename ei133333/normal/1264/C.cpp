template< typename Monoid >
struct SegmentTree {
  using F = function< Monoid(Monoid, Monoid) >;

  int sz;
  vector< Monoid > seg;

  const F f;
  const Monoid M1;

  SegmentTree(int n, const F f, const Monoid &M1) : f(f), M1(M1) {
    sz = 1;
    while(sz < n) sz <<= 1;
    seg.assign(2 * sz, M1);
  }

  void set(int k, const Monoid &x) {
    seg[k + sz] = x;
  }

  void build() {
    for(int k = sz - 1; k > 0; k--) {
      seg[k] = f(seg[2 * k + 0], seg[2 * k + 1]);
    }
  }

  void update(int k, const Monoid &x) {
    k += sz;
    seg[k] = x;
    while(k >>= 1) {
      seg[k] = f(seg[2 * k + 0], seg[2 * k + 1]);
    }
  }

  Monoid query(int a, int b) {
    Monoid L = M1, R = M1;
    for(a += sz, b += sz; a < b; a >>= 1, b >>= 1) {
      if(a & 1) L = f(L, seg[a++]);
      if(b & 1) R = f(seg[--b], R);
    }
    return f(L, R);
  }

  Monoid operator[](const int &k) const {
    return seg[k + sz];
  }

  template< typename C >
  int find_subtree(int a, const C &check, Monoid &M, bool type) {
    while(a < sz) {
      Monoid nxt = type ? f(seg[2 * a + type], M) : f(M, seg[2 * a + type]);
      if(check(nxt)) a = 2 * a + type;
      else M = nxt, a = 2 * a + 1 - type;
    }
    return a - sz;
  }


  template< typename C >
  int find_first(int a, const C &check) {
    Monoid L = M1;
    if(a <= 0) {
      if(check(f(L, seg[1]))) return find_subtree(1, check, L, false);
      return -1;
    }
    int b = sz;
    for(a += sz, b += sz; a < b; a >>= 1, b >>= 1) {
      if(a & 1) {
        Monoid nxt = f(L, seg[a]);
        if(check(nxt)) return find_subtree(a, check, L, false);
        L = nxt;
        ++a;
      }
    }
    return -1;
  }

  template< typename C >
  int find_last(int b, const C &check) {
    Monoid R = M1;
    if(b >= sz) {
      if(check(f(seg[1], R))) return find_subtree(1, check, R, true);
      return -1;
    }
    int a = sz;
    for(b += sz; a < b; a >>= 1, b >>= 1) {
      if(b & 1) {
        Monoid nxt = f(seg[--b], R);
        if(check(nxt)) return find_subtree(b, check, R, true);
        R = nxt;
      }
    }
    return -1;
  }
};

int main() {
  int N, Q;
  cin >> N >> Q;
  vector< modint > P(N);
  cin >> P;
  modint rev = modint(100).inverse();
  for(int i = 0; i < N; i++) P[i] *= rev;


  vector< modint > mul(N + 1);
  mul[0] = 1;
  for(int i = 1; i <= N; i++) {
    mul[i] = mul[i - 1] * P[i - 1];
  }

  vector< modint > sum(mul);
  for(int i = 1; i <= N; i++) {
    sum[i] += sum[i - 1];
  }


  set< int > st{0, N};

  auto qs = [&](int l, int r) { // [l, r)
    modint sita = mul[r] / mul[l];
    modint ue = sum[r - 1];
    if(l) ue -= sum[l - 1];
    ue /= mul[l];
    return ue / sita;
  };

  modint all = qs(0, N);

  while(Q--) {
    int U;
    cin >> U;
    --U;
    auto L = *--st.lower_bound(U);
    auto R = *st.upper_bound(U);
    if(st.count(U)) {
      all -= qs(L, U);
      all -= qs(U, R);
      st.erase(U);
      all += qs(L, R);
    } else {
      all -= qs(L, R);
      st.emplace(U);
      all += qs(L, U);
      all += qs(U, R);
    }
    cout << all << "\n";
  }

}