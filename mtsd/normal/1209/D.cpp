    UnionFind uf(n);
    rep(i,k){
        int a,b;
        cin >> a >> b;
        a--;b--;
        uf.unite(a,b);
    }
    vector<bool>flag(n);
    int sz = 0;
    rep(i,n){
        if(!flag[uf.find(i)]){
            sz += uf.size(i)-1;
            flag[uf.find(i)]=1;
        }
    }
