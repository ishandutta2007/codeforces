const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

pld f(ld x, ld y){
	if (y==0) return {1e18, x*x+y*y};
	return {x/y, x*x+y*y};
}

ll n, m, k, u, v, x, y, t, a, b, ans;
ld X[MAXN], Y[MAXN];
map<pld, ll> mp;

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=0; i<n; i++){
		cin>>X[i]>>Y[i];
		for (int j=0; j<i; j++) mp[f(X[i]-X[j], Y[i]-Y[j])]++;
	}
	for (auto it:mp){
		ll t=it.second;
		ans+=t*(t-1)/2;
	}
	cout<<ans/2<<'\n';
	
	return 0;
}
