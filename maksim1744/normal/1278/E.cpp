int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n;
    cin >> n;
    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v;
        g[u].push_back(v);
        g[v].push_back(u);
    }
    vector<list<int>> lists(n);
    vector<bool> u(n, false);
    function<void(int)> dfs = [&](int v) {
        u[v] = true;
        lists[v].push_back(v + 1);
        lists[v].push_back(-(v + 1));
        for (auto k : g[v]) {
            if (!u[k]) {
                dfs(k);
                auto it = lists[v].begin();
                ++it;
                lists[v].insert(it, *lists[k].begin());
                lists[k].pop_front();
                lists[v].splice(lists[v].end(), lists[k]);
            }
        }
    };
    dfs(0);
    vector<pair<int, int>> ans(n + 1);
    int ind = 1;
    for (auto it = lists[0].begin(); it != lists[0].end(); ++it) {
        if (*it > 0)
            ans[*it].first = ind;
        else
            ans[-*it].second = ind;
        ++ind;
    }
    for (int i = 1; i <= n; ++i)
        cout << ans[i] << '\n';

    return 0;
}