const int B = 30;

struct Trie {
    struct Node {
        int s[2] = {0};
        Node* c[2] = {nullptr};
    }* root = nullptr;

    Node* add(Node* cur, int x, int val, int d) {
        if(d < 0) return cur;
        if(!cur) cur = new Node();
        int bit = (x >> d) & 1;
        cur->s[bit] += val;
        cur->c[bit] = add(cur->c[bit], x, val, d - 1);
        return cur;
    }

    void add(int x, int val) { root = add(root, x, val, B); }

    int sum(Node* cur, int y, int s, int d) {
        if(d < 0 || !cur) return 0;
        int ybit = (y >> d) & 1;
        int sbit = (s >> d) & 1;
        int ans = sum(cur->c[ybit ^ sbit], y, s, d - 1);
        if(sbit) ans += cur->s[ybit ^ sbit ^ 1];
        return ans;
    }

    // sum for x: x * y <= s
    int sum(int y, int s) { return sum(root, y, s, B); }
};

int main() {
    int q;
    scanf("%d", &q);
    Trie tr;
    while(q--) {
        int t, p;
        scanf("%d%d", &t, &p);
        if(t == 1) {
            tr.add(p, 1);
        } else if(t == 2) {
            tr.add(p, -1);
        } else {
            int l;
            scanf("%d", &l);
            int ans = tr.sum(p, l);
            printf("%d\n", ans);
        }
    }
    return 0;
}