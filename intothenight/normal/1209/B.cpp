int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	string s;
	cin >> s;
	vector<pii> a(n);
	cin >> a;
	int res = 0;
	for(int t = 0; t <= 300; t ++){
		int temp = 0;
		for(int i = 0; i < n; i ++){
			if(t < a[i].second){
				temp += s[i] - '0';
			}
			else{
				if((t - a[i].second) / a[i].first % 2){
					temp += s[i] - '0';
				}
				else{
					temp += '1' - s[i];
				}
			}
		}
		res = max(res, temp);
	}
	cout << res;
	return 0;
}

