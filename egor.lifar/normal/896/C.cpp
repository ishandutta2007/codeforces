int powm(int x, int st, int y) {
    if (st == 0) {
        return 1 % y; 
    } else if (st & 1) {
        int f = powm(x, st - 1, y);
        return (1LL * f * x) % y;
    } else {
        int f = powm(x, st >> 1, y);
        return (1LL * f * f) % y;
    }
}


