use std::io::Write;

fn run() {
    input! {
        n: usize,
        m: usize,
        e: [(usize1, usize1, u32); m],
        q: usize,
        ask: [(usize1, usize1); q],
    }
    let mut dsu = vec![DSU::new(n); 30];
    let mut neighbor = vec![vec![false; n]; 30];
    for (i, (dsu, neighbor)) in dsu.iter_mut().zip(neighbor.iter_mut()).enumerate() {
        for &(a, b, w) in e.iter() {
            if w >> i & 1 == 1 {
                dsu.unite(a, b);
            }
        }
        for &(a, b, w) in e.iter() {
            if i > 0 && w & 1 == 0 {
                neighbor[dsu.root(a)] = true;
                neighbor[dsu.root(b)] = true;
            }
        }
    }
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    for (a, b) in ask {
        let mut ans = 2;
        if dsu.iter().any(|dsu| dsu.same(a, b)) {
            ans = 0;
        } else if (0..30).any(|k| neighbor[k][dsu[k].root(a)]) {
            ans = 1;
        }
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}

