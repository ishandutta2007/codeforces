	public static int[][] enumFIF(int n, int mod) {
		int[] f = new int[n + 1];
		int[] invf = new int[n + 1];
		f[0] = 1;
		for (int i = 1; i <= n; i++) {
			f[i] = (int) ((long) f[i - 1] * i % mod);
		}
		long a = f[n];
		long b = mod;
		long p = 1, q = 0;
		while (b > 0) {
			long c = a / b;
			long d;
			d = a;
			a = b;
			b = d % b;
			d = p;
			p = q;
			q = d - c * q;
		}
		invf[n] = (int) (p < 0 ? p + mod : p);
		for (int i = n - 1; i >= 0; i--) {
			invf[i] = (int) ((long) invf[i + 1] * (i + 1) % mod);
		}
		return new int[][] { f, invf };
	}

	
	public static long C(int n, int r, int mod, int[][] fif) {
		if (n < 0 || r < 0 || r > n)
			return 0;
		return (long) fif[0][n] * fif[1][r] % mod * fif[1][n - r] % mod;
	}

	
	void solve()
	{
		int[] lpf = enumLowestPrimeFactors(1000005);
		int mod = 1000000007;
		int[][] fif = enumFIF(1100005, mod);
		for(int Q = ni();Q > 0;Q--){
			int r = ni(), n = ni();
			int pre = -1;
			long ret = 1;
			int e = 0;
			for(int j = n;j != 1;j /= lpf[j]){
				if(pre != lpf[j]){
					if(pre != -1){
						ret = ret * (C(r+e-1, e, mod, fif) + C(r+e-1, r, mod, fif)*2L)%mod;
					}
					pre = lpf[j];
					e = 1;
				}else{
					e++;
				}
			}
			if(e > 0){
				ret = ret * (C(r+e-1, e, mod, fif) + C(r+e-1, r, mod, fif)*2L)%mod;
			}
			assert ret >= 0;
			
			out.println(ret);
		}
	}
	
	public static int[] enumLowestPrimeFactors(int n) {
		int tot = 0;
		int[] lpf = new int[n + 1];
		int u = n + 32;
		double lu = Math.log(u);
		int[] primes = new int[(int) (u / lu + u / lu / lu * 1.5)];
		for (int i = 2; i <= n; i++)
			lpf[i] = i;
		for (int p = 2; p <= n; p++) {
			if (lpf[p] == p)
				primes[tot++] = p;
			int tmp;
			for (int i = 0; i < tot && primes[i] <= lpf[p] && (tmp = primes[i] * p) <= n; i++) {
				lpf[tmp] = primes[i];
			}
		}
		return lpf;
	}

	
