const int N = 1e5 + 228;

pair<int, int> p[N];
int r[N];

pair<int, int> get(int a) {
	if (a == p[a].first)
		return p[a]; 
	return p[a] = get(p[a].first);
}

void merge(int a, int b) {
	a = get(a).first;
	b = get(b).first;
	if (a == b)
		return;
	if (r[a] == r[b])
		++r[a];
	if (r[a] > r[b])
		p[a].second = get(b).second + get(a).second, p[b] = get(a);
	else
		p[b].second = get(b).second + get(a).second, p[a] = get(b);
}

vector<int> G[N];

signed main() {
	setup();

	int n, m;
	cin >> n >> m;
	for(int i = 0; i < m; ++i) {
		int a, b;
		cin >> a >> b;
		--a, --b;
		G[a].push_back(b);
		G[b].push_back(a);
	}
	for(int i = 0; i < n; ++i)
		p[i] = {i, 1}, r[i] = 0;
	set<int> comp;
	for(int i = 0; i < n; ++i)
		comp.insert(i);
	for(int i = 0; i < n; ++i) {
		vector<int> del;
		map<int, int> cnt;
		for(auto v: G[i]) {
			v = get(v).first;
			if (comp.count(v))
				++cnt[v];
		}
		for(auto v: comp) {
			// cout << v << ':' << cnt[v] << ':' << get(v).second << ' ';
			if (cnt[v] == get(v).second) 
				del.push_back(v);
		}
		// cout << '\n';
		for(auto i: del)
			comp.erase(i);
		for(auto v: comp)
			merge(v, i);
		comp.clear();
		comp.insert(get(i).first);
		// cout << '\n';
		for(auto v: del)
			comp.insert(v);
	}
	cout << comp.size() - 1 << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


