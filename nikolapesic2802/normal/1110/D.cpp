const int M=1e6+5;
int dp[M][3][3];
int main()
{
	int n,m;
	scanf("%i %i",&n,&m);
	vector<int> cnt(m);
	for(int i=0;i<n;i++)
    {
        int a;
        scanf("%i",&a);
        cnt[a-1]++;
    }
    for(int i=0;i<m;i++)
        for(int x=0;x<3;x++)
            for(int y=0;y<3;y++)
                for(int z=0;z<3;z++)
                    if(x+y+z<=cnt[i])
                        dp[i+1][y][z]=max(dp[i+1][y][z],dp[i][x][y]+z+(cnt[i]-x-y-z)/3);
    printf("%i\n",dp[m][0][0]);
    return 0;
}