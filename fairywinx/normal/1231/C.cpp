const int N = 550;

int a[N][N];

signed main() {
	setup();
	for(int i = 0; i < N; ++i) 
		fill(a[i], a[i] + N, 1000000);
	// SOLVE;

	int n, m;
	cin >> n >> m;
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j) 
			cin >> a[i][j];
	}
	for(int i = n - 1; i >= 0; --i) {
		for(int j = m - 1; j >= 0; --j) {
			if (a[i][j] == 0)
				a[i][j] = min(a[i + 1][j] - 1, a[i][j + 1] - 1);
		}
	}
	int sum = 0;
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j) {
			if (a[i][j] >= min(a[i + 1][j], a[i][j + 1])) {
				cout << -1 << '\n';
				return 0;
			}
			sum += a[i][j];
		}
	}
	cout << sum << '\n';


	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


