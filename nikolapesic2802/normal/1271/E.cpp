ll n,k;
int cntN=-1;
ll can(ll a){
    if(a>n)
        return 0;
    ll sol=0;
    ll add=1;
    if(!(a&1))
        sol+=can(a+1);
    while(a<=n){
        ll sub=n-a+1;
        sol+=min(sub,add);
        a<<=1;
        add*=2;
    }
    return sol;
}
int main()
{
	scanf("%lld %lld",&n,&k);
	int cntN=-1;
	ll tr=n;
	while(tr)
        cntN++,tr>>=1;
    ll trsol=0;
    for(int i=cntN;i>=0;i--)
        if(can(trsol+(1LL<<i))>=k)
            trsol+=(1LL<<i);
    printf("%lld\n",trsol);
    return 0;
}