int f[9];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("input.txt", "r", stdin);
    //freopen("output.txt", "w", stdout);

    int n;
    string a;
    cin >> n >> a;
    for (int i=0; i<9; i++)
        cin >> f[i];

    int i = 0;
    while (i < n && a[i] - '0' >= f[a[i]-'1'])
        i++;
    while (i < n && a[i] - '0' <= f[a[i]-'1']) {
        a[i] = f[a[i]-'1'] + '0';
        i++;
    }

    cout << a << "\n";

    return 0;
}