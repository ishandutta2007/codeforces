	void solve()
	{
		int n = ni(), a = ni(), b = ni();
		long[] nya = new long[n];
		Arrays.fill(nya, 1);
		if(b == 0 && a > 0){
			if(n == a+1){
				out.println(-1);
				return;
			}
			for(int i = 0;i <= a;i++){
				nya[i+1] += i;
			}
		}else{
			long sum = nya[0];
			for(int i = 1;i <= b;i++){
				nya[i] = sum+1;
				if(nya[i] > 50000){
					out.println(-1);
					return;
				}
				sum += nya[i];
			}
			for(int i = b+1;i <= b+a;i++){
				nya[i] = nya[i-1]+1;
				if(nya[i] > 50000){
					out.println(-1);
					return;
				}
			}
		}
		for(int i = 0;i < n;i++){
			if(i > 0)out.print(" ");
			out.print(nya[i]);
		}
		out.println();
	}
	
