int dp[4000005], s[4000005];
int main(int argc, char const *argv[]) {
  std::ios_base::sync_with_stdio(false);
  std::cin.tie(nullptr), std::cout.tie(nullptr);
  std::cin >> n >> m;
  for (int i = 1; i <= n; i++) {
    Add(s[i], s[i - 1]);
    dp[i] = s[i];
    if (i == 1) dp[i] = 1;
    Add(s[i + 1], dp[i]);
    for (int j = 2; j <= n / i; j++)
      Add(s[i * j], dp[i]), Sub(s[std::min(i * j + j, n + 1)], dp[i]);
  }
  std::cout << dp[n];
  return 0;
}