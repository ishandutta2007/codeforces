template< int mod >
struct ModInt {
  int x;

  ModInt() : x(0) {}

  ModInt(int64_t y) : x(y >= 0 ? y % mod : (mod - (-y) % mod) % mod) {}

  ModInt &operator+=(const ModInt &p) {
    if((x += p.x) >= mod) x -= mod;
    return *this;
  }

  ModInt &operator-=(const ModInt &p) {
    if((x += mod - p.x) >= mod) x -= mod;
    return *this;
  }

  ModInt &operator*=(const ModInt &p) {
    x = (int) (1LL * x * p.x % mod);
    return *this;
  }

  ModInt &operator/=(const ModInt &p) {
    *this *= p.inverse();
    return *this;
  }

  ModInt &operator^=(int64_t n) {
    int y = x;
    x = 1;
    while(n > 0) {
      if(n & 1) x = 1LL * x * y % mod;
      y = 1LL * y * y % mod;
      n >>= 1;
    }
    return *this;
  }

  ModInt operator-() const { return ModInt(-x); }

  ModInt operator+(const ModInt &p) const { return ModInt(*this) += p; }

  ModInt operator-(const ModInt &p) const { return ModInt(*this) -= p; }

  ModInt operator*(const ModInt &p) const { return ModInt(*this) *= p; }

  ModInt operator/(const ModInt &p) const { return ModInt(*this) /= p; }

  ModInt operator^(const int64_t n) const { return ModInt(*this) ^= n; }

  bool operator==(const ModInt &p) const { return x == p.x; }

  bool operator!=(const ModInt &p) const { return x != p.x; }

  ModInt inverse() const {
    int a = x, b = mod, u = 1, v = 0, t;
    while(b > 0) {
      t = a / b;
      a -= t * b;
      swap(a, b);
      u -= t * v;
      swap(u, v);
    }
    return ModInt(u);
  }

  friend ostream &operator<<(ostream &os, const ModInt< mod > &p) {
    return os << p.x;
  }

  friend istream &operator>>(istream &is, ModInt< mod > &a) {
    int64_t t;
    is >> t;
    a = ModInt< mod >(t);
    return (is);
  }
};

using modint = ModInt< mod >;

int N;
pair< modint, bool > dp[2002][2002];
bool v[2002][2002];

pair< modint, bool > rec(int idx, int dep) {
  if(idx == N) return make_pair(0, dep==0);
  if(exchange(v[idx][dep], true)) return dp[idx][dep];
  modint ret = 0;
  bool f = false;
  if(dep > 0) {
    auto res = rec(idx + 1, dep - 1);
    ret += res.first;
    f |= res.second;
  }

  auto res = rec(idx + 1, dep + 1);
  ret += res.first;
  f |= res.second;

  if(f && (N - idx) % 2 == 1) {
    ret += 1;
    f = true;
  }

  return dp[idx][dep] = make_pair(ret, f);
}

