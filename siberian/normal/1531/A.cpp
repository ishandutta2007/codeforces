string now = "blue";
bool change = true;

signed main() {
    ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
    cout.precision(20), cout.setf(ios::fixed);
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;
        if (s == "lock") {
            change = false;
        } else if (s == "unlock") {
            change = true;
        } else if (change) {
            now = s;
        }
    }
    cout << now << endl;
    return 0;
}