int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n;
    cin >> n;
    vector<ll> c(n);
    cin >> c;

    ll ans = 0;

    auto inter = [&](pair<ll, ll> a, pair<ll, ll> b) {
        return mp(max(a.first, b.first), min(a.second, b.second));
    };

    auto get_len = [&](pair<ll, ll> a) {
        return max(0ll, a.second - a.first + 1);
    };

    for (int i = 0; i < n; i += 2) {
        pair<ll, ll> cur = {1, c[i]};
        for (int j = i + 1; j < n; ++j) {
            if (j % 2 == 0) {
                cur.first += c[j];
                cur.second += c[j];
            } else {
                ans += get_len(inter(cur, mp(1ll, c[j])));
                cur.first -= c[j];
                cur.second -= c[j];
                cur.first = max(0ll, cur.first);
                if (cur.first > cur.second) break;
            }
            show(i, j, cur);
        }
    }

    cout << ans << "\n";

    return 0;
}