typedef long long int int64;


const int MOD = 998244353;

void sadd(int &a, int b)
{
    a += b;
    if (a >= MOD) a -= MOD;
}
int add(int a, int b)
{
    sadd(a, b);
    return a;
}
int mul(int a, int b)
{
    return (a * 1LL * b) % MOD;
}

const int N = 2005;
int C[N][N];


int main(int,  char **)
{
#ifdef LOCAL
//	freopen("input.txt", "r", stdin);
//	freopen("output.txt", "w", stdout);
#endif

    for (int i = 0; i < N; i++)
    {
        C[i][0] = C[i][i] = 1;
        for (int j = 1; j < i; j++)
            C[i][j] = add(C[i - 1][j - 1], C[i - 1][j]);
    }

    int n, m, k;
    scanf("%d%d%d", &n, &m, &k);
    int ans = m;
    for (int i = 0; i < k; i++)
        ans = mul(ans, m - 1);
    ans = mul(ans, C[n - 1][k]);
    printf("%d\n", ans);

	return 0;
}