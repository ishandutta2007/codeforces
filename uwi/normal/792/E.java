	boolean go(int d, int[] a)
	{
		if(d == 0)return true;
		for(int v : a){
			if(v % (d+1) == 0)continue;
			int h = v/d;
			if(v <= (d+1)*h)continue;
			return false;
		}
		return true;
	}
	
	void solve()
	{
		int n = ni();
		int[] a = na(n);
		// n exists hx+(h-1)(n-x)=v
		// (h-1)n <= v <= hn
		// 
		Random gen = new Random();
		a = shuffle(a, gen);
		Arrays.sort(a);
		int max = -1;
		for(int d = 1;d*d <= a[0];d++){
			if(go(a[0]/d, a)){
				max = a[0]/d;
				break;
			}
			if(go(a[0]/d-1, a)){
				max = a[0]/d-1;
				break;
			}
		}
		for(int d = 0;d*d <= a[0];d++){
			if(d > max && go(d, a))max = d;
		}
		
		long ret = 0;
		for(int i = 0;i < n;i++){
			ret += (a[i]+max)/(max+1);
		}
		out.println(ret);
	}
	
	public static int[] shuffle(int[] a, Random gen){ for(int i = 0, n = a.length;i < n;i++){ int ind = gen.nextInt(n-i)+i; int d = a[i]; a[i] = a[ind]; a[ind] = d; } return a; }

	
