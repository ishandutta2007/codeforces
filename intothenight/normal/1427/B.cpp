int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	auto __solve_tc = [&](int __tc_num){
		int n, k;
		string s;
		cin >> n >> k >> s;
		int win = count(s.begin(), s.end(), 'W');
		if(win + k >= n){
			cout << 2 * n - 1 << "\n";
		}
		else if(win == 0 && k == 0){
			cout << "0\n";
		}
		else{
			int res = 2 * (win + k);
			vector<int> a;
			int i = 0;
			while(i < n && s[i] == 'L'){
				++ i;
			}
			while(i < n){
				while(i < n && s[i] == 'W'){
					++ i;
				}
				if(i < n){
					int j = i;
					while(j < n && s[j] == 'L'){
						++ j;
					}
					if(j < n){
						a.push_back(j - i);
					}
					i = j + 1;
				}
			}
			sort(a.begin(), a.end());
			int comp = (int)a.size() + 1;
			for(auto x: a){
				if(k >= x){
					k -= x;
					-- comp;
				}
				else{
					break;
				}
			}
			cout << res - comp << "\n";
		}
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

