const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 200010, LOG=20;

int n, m, k, u, v, x, y, t, a, b;
ll A[MAXN];
int par[MAXN][LOG];
ll dist[MAXN][LOG];
int ans[MAXN];
vector<int> G[MAXN];

void dfs(int node){
	for (int i=1; i<LOG; i++) par[node][i]=par[par[node][i-1]][i-1];
	for (int i=1; i<LOG; i++) dist[node][i]=min(1ll*inf, dist[par[node][i-1]][i-1] + dist[node][i-1]);
	for (int v:G[node]){
		dfs(v);
		ans[node]+=ans[v];
	}
	ans[par[node][0]]++;
	ll v=node, tmp=A[node];
	for (int i=LOG-1; i>=0; i--) if (dist[v][i]<=tmp){
		tmp-=dist[v][i];
		v=par[v][i];
	}
	v=par[v][0];
	//debug2(node, v)
	ans[v]--;
}

