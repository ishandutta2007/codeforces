using namespace std;
int cnt[26],n,k,t;
string s;
int main(){
	//freopen(".in","r",stdin);
	//freopen(".out","w",stdout);
	ios::sync_with_stdio(0);
	cin.tie(0);
	cin>>t;
	while(t--){
		cin>>n>>k>>s;
		int ans = 0;
		for(int i=0;i < (k+1)/2;++i){
			for(int j = 0;j < 26;++j) cnt[j] = 0;
			for(int j = i;j < n;j+=k) cnt[s[j]-'a']++;
			for(int j =n-i-1;j >= 0;j-=k) cnt[s[j]-'a']++;
			int Max = *max_element(cnt,cnt+26);
			if(i*2+1==k) ans += (n/k*2-Max)/2;
			else ans += (n/k*2-Max); 
		}
		cout <<ans << endl;
	}
	
	return 0;
}