#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=1e6+5;
const int p=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int power(int a,int b)
{
	int ret=1;
	while (b)
	{
		if (b&1) ret=ret*a%p;
		a=a*a%p;
		b/=2;
	}
	return ret;
}
int C2(int n)
{
	return n*(n-1)/2%p;
}
int C3(int n)
{
	return n*(n-1)/2%p*(n-2)%p*power(3,p-2)%p;
}
int f[N],F[N],g[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	f[0]=g[0]=2,F[0]=1;
	for (int i=1;i<=n;i++) f[i]=(C2(f[i-1]+1)+1)%p;
	for (int i=1;i<=n;i++) F[i]=(f[i]-f[i-1]+p)%p;
	for (int i=1;i<=n;i++) g[i]=(C3(f[i-1]+2)+1)%p;
	int ans=(2*(g[n]-g[n-1]+p)%p-1+p)%p;
	for (int i=1;i<n;i++) ans=(ans+(F[i]-F[i-1]+p)*(F[n-i-1]-1)%p+p)%p;
	print(ans);
	
	return 0;
}
