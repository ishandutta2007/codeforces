const int MAXN = 200228;


int n;
vector<int> v[MAXN];
vector<int> g[MAXN];
int prs[MAXN];


void dfs(int u, int pr = -1) {
    for (auto x: v[u]) {
        if (x == pr) {
            continue;
        }
        g[u].pb(x);
        prs[x] = u;
        dfs(x, u);
    }
}


long long ans = 0;
int sz[MAXN];
