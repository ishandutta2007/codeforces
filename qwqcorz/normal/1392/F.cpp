#include<bits/stdc++.h>
#define ll long long
using namespace std;
const int N=1e6+5;

ll read()
{
	ll s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
ll check(ll x,ll n)
{
	if (x+x+n-1>2e18/n) return 2e18;
	return (x+x+n-1)*n/2;
}
ll h[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	ll n=read(),sum=0;
	for (ll i=1;i<=n;i++) sum+=read();
	ll l=0,r=1e12;
	while (l<=r)
	{
		ll mid=(l+r)/2;
		if (check(mid,n)<sum) l=mid+1;
						 else r=mid-1;
	}
	h[1]=l;
	ll len=check(l,n)-sum;
	for (ll i=2;i<=n;i++) h[i]=h[i-1]+(i+len-1!=n);
	for (ll i=1;i<=n;i++) print(h[i],' ');
	
	return 0;
}
