fn run() {
    input! {
        n: usize,
        d: [usize; n],
    }
    let mut d = d;
    d.sort();
    let x = d[n - 1];
    let mut used = vec![false; 10_000 + 1];
    let mut y = 0;
    for &d in d.iter().rev() {
        if x % d == 0 && !used[d] {
            used[d] = true;
        } else {
            y = d;
            break;
        }
    }
    println!("{} {}", x, y);
}

fn main() {
    run();
}