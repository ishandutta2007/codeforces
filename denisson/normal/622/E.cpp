int n;
vector<int> g[5 * 100007];
vector<int> s;
int h[5 * 100007];

void dfs(int v, int pred, int hh) {
	bool f = 1;
	h[v] = hh;
	for (int to : g[v]) {
		if (to != pred)
			dfs(to, v, hh + 1), f = 0;
	}
	if (f)
		s.pub(h[v]);
}

int get(int v) {
	s.clear();
	dfs(v, 0, 0);
	sort(all(s));
	int last = s[0];

	for (int i = 1; i < s.size(); i++) {
		last = max(last + 1, s[i]);
	}

	return last;
}

