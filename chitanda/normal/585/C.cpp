ll x, y;
vector<ll> ans;

ll gcd(){
	while(1){
		if(x == y) return x;
		if(x < y){
			ll p = (y - 1) / x;
			ans.pb(-p);
			y -= x * p;
		}else{
			ll p = (x - 1) / y;
			ans.pb(p);
			x -= y * p;
		}
	}
}

