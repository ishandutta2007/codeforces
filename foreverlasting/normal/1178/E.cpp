const int N=1e6+10;
namespace MAIN{
    char str[N];
    int n;
    bool fl[N];
    int cnt;
    inline void MAIN(){
        scanf("%s",str+1),n=int(strlen(str+1));
        res i=1,j=n;
        while(i<=j){
            if(str[i]==str[j]){fl[i]=fl[j]=1,cnt+=1+(i!=j),i++,j--;continue;}
            if(i<j&&str[i+1]==str[j]){fl[i+1]=fl[j]=1,cnt+=1+(i+1!=j),i+=2,j--;continue;}
            if(i<j&&str[i]==str[j-1]){fl[i]=fl[j-1]=1,cnt+=1+(i!=j-1),i++,j-=2;continue;}
            i++,j--;
        }
        if(cnt>=n/2){for(res i=1;i<=n;i++)if(fl[i])putchar(str[i]);}
        else puts("IMPOSSIBLE");
//        printf("%d\n",cnt);
    }
}
