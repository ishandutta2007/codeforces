ll m;

ll get_cnt(ll x)
{
	ll res = 0;
	for (ll i = 2; i * i * i <= x; ++i)
		res += x / (i * i * i);
	return res;
}

void solve()
{
	cin >> m;
	ll l = 7;
	ll r = m * 8;
	while(r - l > 1)
	{
		ll mid = (l + r) / 2;
		if (get_cnt(mid) >= m)
			r = mid;
		else
			l = mid;
	}
	cout << (get_cnt(r) == m ? r : -1);
}

