bitset<7001> a[100005],ok[7005];
int mu[7005],p[7005],pri[7005],ps,t;
void init_mu(int n){
	mu[1]=1;
	for(int i=1;i<=n;++i)ok[i][i]=1;
	for(int i=2;i<=n;++i){
		if(!p[i]){
			pri[++ps]=i;
			mu[i]=-1;
		}
		if(mu[i])
		for(int j=1;j*i<=n;++j){
			ok[j][i*j]=1;
		}
		for(int j=1;j<=ps && i*pri[j]<=n;++j){
			p[i*pri[j]]=1;
			if(!(i%pri[j])){
				mu[i*pri[j]]=0;break;
			}else mu[i*pri[j]]=-mu[i];
		}
	}
}
