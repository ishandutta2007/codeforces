void run_case() {
    int N;
    cin >> N;
    vector<int> A(N);
    map<int, int> freq;

    for (auto &a : A) {
        cin >> a;
        freq[a]++;
    }

    int winner = -1;

    for (int i = 0; i < N; i++)
        if (freq[A[i]] == 1 && (winner < 0 || A[i] < A[winner]))
            winner = i;

    cout << (winner < 0 ? winner : winner + 1) << '\n';
}

