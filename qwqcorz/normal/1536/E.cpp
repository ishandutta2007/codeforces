#include<bits/stdc++.h>
using namespace std;
const int p=1e9+7;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
char get()
{
	char c=getchar();
	while (c!='0'&&c!='#') c=getchar();
	return c;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		int n=read(),m=read(),flag=1,ans=1;
		for (int i=1;i<=n*m;i++)
		if (get()=='#') ans=ans*2%p;
		else flag=0;
		print(ans-flag);
	}
	
	return 0;
}
