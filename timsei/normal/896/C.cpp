LL Pow(LL x , LL y) {
	if(y == 1) return x % mod;
	if(!y) return 1ll;
	LL it = Pow(x , y >> 1);
	it = it * it % mod;
	if(y & 1)it = it * x % mod;
	return it;
}

LL seed , vmax;

const int MOD = 1e9 + 7;

LL rnd(void) {
	LL ret = seed;
	seed = (seed * 7ll + 13ll) % MOD;
	return ret;
}

