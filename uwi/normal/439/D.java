	void solve()
	{
		int n = ni(), m = ni();
		int[] a = na(n);
		int[] b = na(m);
		int mina = Integer.MAX_VALUE;
		for(int i = 0;i < n;i++){
			mina = Math.min(mina, a[i]);
		}
		int maxb = Integer.MIN_VALUE;
		for(int i = 0;i < m;i++){
			maxb = Math.max(maxb, b[i]);
		}
		if(maxb <= mina){
			out.println(0);
			return;
		}
		
		long low = 0, high = 1000000007;
		while(high - low > 2){
			long llow = (low*2+high)/3;
			long lhigh = (low+high*2)/3;
			long fllow = f(llow, a, b);
			long flhigh = f(lhigh, a, b);
			if(fllow < flhigh){
				high = lhigh;
			}else{
				low = llow;
			}
		}
		
		long ret = Long.MAX_VALUE;
		for(long i = low;i < low+5;i++){
			ret = Math.min(ret, f(i, a, b));
		}
		out.println(ret);
	}
	
	long f(long x, int[] a, int[] b)
	{
		long val = 0;
		for(int v : a){
			if(v <= x)val += x - v;
		}
		for(int v : b){
			if(v >= x)val += v-x;
		}
		return val;
	}
	
