const int MAXN = 600228;


int n, q;
int a[MAXN];
int t[MAXN];
int id[MAXN], val[MAXN];
int l[MAXN], r[MAXN];
int k[MAXN];
ll hs[MAXN];
mt19937_64 as;



struct fenw {
	vector<ll> f;
	void init(int n) {
		f.resize(n, 0);
	}
	void inc(int i, ll x) {
		for (; i < sz(f); i |= (i + 1)) {
			f[i] += x;
		}
	}
	ll get(int i) {
		ll ans = 0;
		for (; i >= 0; i = (i & (i + 1)) - 1) {
			ans += f[i];
		}
		return ans;
	}
	ll get(int l, int r) {
		return get(r) - get(l - 1);
	}
};


fenw kek[60];


void add(int val, int i, int mod) {
	for (int j = 0; j < 50; j++) {
		if (hs[val] & (1LL << j)) {
			kek[j].inc(i, mod);
		}
	}
} 


void solve() {
	as.seed(time(NULL));
	cin >> n >> q;
	vector<int> st;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
		st.pb(a[i]);
	}
	for (int i = 0; i < q; i++) {
		cin >> t[i];
		if (t[i] == 1) {
			cin >> id[i] >> val[i];
			id[i]--;
			st.pb(val[i]);
		} else {
			cin >> l[i] >> r[i] >> k[i];
			l[i]--, r[i]--;
		}
	}
	sort(all(st));
	st.resize(distance(st.begin(), unique(all(st))));
	for (int i = 0; i < n; i++) {
		a[i] = lower_bound(all(st), a[i]) - st.begin();
	}
	for (int i = 0; i < sz(st); i++) {
		hs[i] = as();
	}
	for (int i = 0; i < q; i++) {
		if (t[i] == 1) {
			val[i] = lower_bound(all(st), val[i]) - st.begin();
		}
	}
	for (int i = 0; i < 60; i++) {
		kek[i].init(n);
	}
	for (int i = 0; i < n; i++) {
		add(a[i], i, 1);
	}
	for (int i = 0; i < q; i++) {
		if (t[i] == 1) {
			add(a[id[i]], id[i], -1);
			a[id[i]] = val[i];
			add(a[id[i]], id[i], 1);
		} else {
			bool bad = false;
			for (int f = 0; f < 50; f++) {
				ll t = kek[f].get(l[i], r[i]);
				if (t % k[i] != 0) {
					bad = true;
					break;
				}
			}
			if (!bad) {
				cout << "YES\n";
			} else {
				cout << "NO\n";
			}
		}
	}
}


