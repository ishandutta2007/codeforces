using ll = long long int;
int main() {
	ios::sync_with_stdio(false);
	int N;cin>>N;
	vector<ll>v(8,ll(1e18));
	v[0]=0;
	for(int i=0;i<N;++i){
		int cost;cin>>cost;
		string st;cin>>st;
		int x=0;
		for(char ch='A';ch<='C';++ch){
			if(st.find(ch)!=-1)x|=1<<(ch-'A');
		}
		auto nv(v);
		for(int j=0;j<8;++j){
			nv[x|j]=min(nv[x|j],v[j]+cost);
		}
		v=nv;
	}
	if(v[7]>1e17)v[7]=-1;
	cout<<v[7]<<endl;
	return 0;
}