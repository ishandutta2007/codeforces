#include <bits/stdc++.h>
using namespace std;
int main(){
    int n;
    cin >> n;
    int a[n], b[n], d[] = {0, n-1};
    for (int i = 0; i < n; i++) cin >> a[i];
    sort(a, a+n);
    int c = 0;
    for (int i = 0; i < n; i++){
        b[d[c]] = a[i];
        if (c == 1) d[c] --;
        else d[c] ++;
        c = 1-c;
    }
    for (int i = 0; i < n; i++) cout << b[i] << ' ';
    return 0;
    
}
