template <class D, class Op> struct SimpleSeg {
    D e;
    Op op;
    int sz, lg;  // size(extended to 2^i), lg
    V<D> d;

    SimpleSeg(const V<D>& v, D _e, Op _op) : e(_e), op(_op) {
        int n = int(v.size());
        lg = 1;
        while ((1 << lg) < n) lg++;
        sz = 1 << lg;
        d = V<D>(2 * sz, e);
        for (int i = 0; i < n; i++) d[sz + i] = v[i];
        for (int i = sz - 1; i >= 0; i--) {
            update(i);
        }
    }

    void update(int k) { d[k] = op(d[2 * k], d[2 * k + 1]); }

    void set(int p, D x) {
        p += sz;
        d[p] = x;
        for (int i = 1; i <= lg; i++) update(p >> i);
    }

    D single(int p) { return d[p + sz]; }

    D sum(int a, int b) {
        D sml = e, smr = e;
        a += sz;
        b += sz;

        while (a < b) {
            if (a & 1) sml = op(sml, d[a++]);
            if (b & 1) smr = op(d[--b], smr);
            a >>= 1;
            b >>= 1;
        }
        return op(sml, smr);
    }
};

template <class D, class Op>
SimpleSeg<D, Op> get_simple_seg(V<D> v, D e, Op op) {
    return SimpleSeg<D, Op>(v, e, op);
}

template <class D, class L, class OpDD, class OpDL, class OpLL> struct SegTree {
    D e_d;
    L e_l;
    OpDD op_dd;
    OpDL op_dl;
    OpLL op_ll;
    int sz, lg;  //(2^lg), lg
    V<D> d;
    V<L> lz;

    SegTree(const V<D>& v,
            D _e_d,
            L _e_l,
            OpDD _op_dd,
            OpDL _op_dl,
            OpLL _op_ll)
        : e_d(_e_d), e_l(_e_l), op_dd(_op_dd), op_dl(_op_dl), op_ll(_op_ll) {
        int n = int(v.size());
        lg = 1;
        while ((1 << lg) < n) lg++;
        sz = 1 << lg;
        d = V<D>(2 * sz, e_d);
        lz = V<L>(2 * sz, e_l);
        for (int i = 0; i < n; i++) d[sz + i] = v[i];
        for (int i = sz - 1; i >= 0; i--) {
            update(i);
        }
    }

    void all_add(int k, L x) {
        d[k] = op_dl(d[k], x);
        lz[k] = op_ll(lz[k], x);
    }
    void push(int k) {
        all_add(2 * k, lz[k]);
        all_add(2 * k + 1, lz[k]);
        lz[k] = e_l;
    }
    void update(int k) { d[k] = op_dd(d[2 * k], d[2 * k + 1]); }

    void set(int p, D x) {
        p += sz;
        for (int i = lg; i >= 1; i--) push(p >> i);
        d[p] = x;
        for (int i = 1; i <= lg; i++) update(p >> i);
    }

    void add(int a, int b, L x, int l, int r, int k) {
        if (b <= l || r <= a) return;
        if (a <= l && r <= b) {
            all_add(k, x);
            return;
        }
        push(k);
        int mid = (l + r) / 2;
        add(a, b, x, l, mid, 2 * k);
        add(a, b, x, mid, r, 2 * k + 1);
        update(k);
    }
    void add(int a, int b, L x) { add(a, b, x, 0, sz, 1); }

    D single(int p) {
        p += sz;
        for (int i = lg; i >= 1; i--) push(p >> i);
        return d[p];
    }

    D sum(int a, int b, int l, int r, int k) {
        if (b <= l || r <= a) return e_d;
        if (a <= l && r <= b) return d[k];
        push(k);
        int mid = (l + r) / 2;
        return op_dd(sum(a, b, l, mid, 2 * k), sum(a, b, mid, r, 2 * k + 1));
    }
    D sum(int a, int b) { return sum(a, b, 0, sz, 1); }
};

template <class D, class L, class OpDD, class OpDL, class OpLL>
SegTree<D, L, OpDD, OpDL, OpLL> get_seg(V<D> v,
                                        D e_d,
                                        L e_l,
                                        OpDD op_dd,
                                        OpDL op_dl,
                                        OpLL op_ll) {
    return SegTree<D, L, OpDD, OpDL, OpLL>(v, e_d, e_l, op_dd, op_dl, op_ll);
}


int main() {
    Scanner sc(stdin);
    Printer pr(stdout);
    int n;
    sc.read(n);

    V<int> p(n), rp(n);
    for (int i = 0; i < n; i++) {
        sc.read(p[i]); p[i]--;
        rp[p[i]] = i;
    }
    int q;
    sc.read(q);
    struct Q {
        int l, idx;
    };
    VV<Q> qv(n);
    for (int i = 0; i < q; i++) {
        int l, r;
        sc.read(l, r); l--; r--;
        qv[r].push_back({l, i});
    }

    struct D {
        int ma, mc; //max, max_count
        ll sum; // count sum
    };
    struct L {
        // add pre -> count(snap time) -> add aft
        int pre, snap, aft;
    };
    auto seg = get_seg(V<D>(n, {0, 1, 0}),
        D{-TEN(9), 0, 0},
        L{0, 0, 0},
        [&](D l, D r){
            int nma = max(l.ma, r.ma);
            int nmc = 0;
            if (l.ma == nma) nmc += l.mc;
            if (r.ma == nma) nmc += r.mc;
            return D{nma, nmc, l.sum + r.sum};
        },
        [&](D l, L r) {
            ll nsum = l.sum;
            if (l.ma + r.pre == 0) {
                nsum += ll(l.mc) * r.snap;
            }
            return D{l.ma + r.pre + r.aft, l.mc, nsum};
        },
        [&](L l, L r) {
            if (l.aft + r.pre < 0) {
                // use l
                return L{l.pre, l.snap, l.aft + r.pre + r.aft};
            } else if (l.aft + r.pre > 0) {
                // use r
                return L{l.pre + l.aft + r.pre, r.snap, r.aft};
            } else {
                return L{l.pre, l.snap + r.snap, r.aft};
            }
        }
        );
    V<ll> res(q);
    for (int i = 0; i < n; i++) {
        seg.add(0, i, L{-1, 0, 0});
        if (p[i] && rp[p[i] - 1] < i) {
            int j = rp[p[i] - 1];
            seg.add(0, j + 1, L{1, 0, 0});
        }
        if (p[i] < n - 1 && rp[p[i] + 1] < i) {
            int j = rp[p[i] + 1];
            seg.add(0, j + 1, L{1, 0, 0});
        }
        seg.add(0, i + 1, L{0, 1, 0});

        for (auto qu: qv[i]) {
            res[qu.idx] = seg.sum(qu.l, i + 1).sum;
        }
    }

    for (auto x: res) pr.writeln(x);
    return 0;
}