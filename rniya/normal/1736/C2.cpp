int op(int l, int r) { return min(l, r); }

int e() { return INF; }

int target;
bool f(int x) { return x >= target; }

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<int> a(n);
    for (int& x : a) cin >> x;

    for (int i = 0; i < n; i++) a[i] -= i + 1;
    atcoder::segtree<int, op, e> seg(a);
    vector<vector<int>> nxt(n, vector<int>(2));
    vector<ll> sum(n + 1, 0), sum2(n + 1, 0);
    for (int i = 0; i < n; i++) {
        target = -i;
        nxt[i][0] = seg.max_right<f>(i);
        if (nxt[i][0] == n)
            nxt[i][1] = n;
        else
            nxt[i][1] = seg.max_right<f>(nxt[i][0] + 1);
        sum[i + 1] = sum[i] + (nxt[i][0] - i);
        sum2[i + 1] = sum2[i] + (nxt[i][1] - i);
    }
    vector<int> Max(n);
    Max[0] = nxt[0][0];
    for (int i = 1; i < n; i++) Max[i] = max(Max[i - 1], nxt[i][0]);

    auto query = [&](int p, int x) {
        x -= p + 1;
        ll res = sum.back();
        if (x <= a[p]) {
            if (x >= 0) return res;
            int L = max(0, -a[p]), R = min(-x, p + 1);
            if (R <= L) return res;
            int lb = L - 1, ub = R;
            while (ub - lb > 1) {
                int mid = (ub + lb) >> 1;
                (Max[mid] >= p ? ub : lb) = mid;
            }
            L = max(L, ub);
            res -= sum[R] - sum[L];
            res += 1LL * p * (R - L);
            res -= 1LL * (L + R - 1) * (R - L) / 2;
        } else {
            int L, R;
            {
                int lb = -1, ub = n;
                while (ub - lb > 1) {
                    int mid = (lb + ub) >> 1;
                    (Max[mid] >= p ? ub : lb) = mid;
                }
                L = ub;
            }
            {
                int lb = -1, ub = n;
                while (ub - lb > 1) {
                    int mid = (lb + ub) >> 1;
                    (Max[mid] > p ? ub : lb) = mid;
                }
                R = ub;
            }
            if (L == R) return res;
            L = max(L, -x);
            res -= sum[R] - sum[L];
            res += sum2[R] - sum2[L];
        }
        return res;
    };

    int q;
    cin >> q;
    for (; q--;) {
        int p, x;
        cin >> p >> x;
        cout << query(--p, x) << '\n';
    }
    return 0;
}