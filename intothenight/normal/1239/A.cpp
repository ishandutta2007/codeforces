struct recurrence{
	int N;
	vl init, coef;
	ll mod;
	recurrence(vl init, vl coef, ll mod):
		N(init.size()), init(init), coef(coef), mod(mod){
	}
	ll operator[](ll n) const{
		auto combine = [&](vl a, vl b){
			vl res(2 * N + 1);
			for(int i = 0; i <= N; i ++){
				for(int j = 0; j <= N; j ++){
					res[i + j] = (res[i + j] + a[i] * b[j]) % mod;
				}
			}
			for(int i = 2 * N; i > N; i --){
				for(int j = 0; j < N; j ++){
					res[i - 1 - j] = (res[i - 1 - j] + res[i] * coef[N - 1 - j]) % mod;
				}
			}
			res.resize(N + 1);
			return res;
		};
		vl pol(N + 1), e(pol);
		pol[0] = e[1] = 1;
		for(n ++; n; n /= 2){
			if(n % 2) pol = combine(pol, e);
			e = combine(e, e);
		}
		ll res = 0;
		for(int i = 0; i < N; i ++){
			res = (res + pol[i + 1] * init[i]) % mod;
		}
		return res;
	}
};

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	ll n, m;
	cin >> n >> m;
	const ll mod = 1e9 + 7;
	recurrence f({1, 1}, {1, 1}, mod);
	cout << (f[n] + f[m] + mod - 1) * 2 % mod;
	return 0;
}

