template<typename T, typename U> inline void amin(T &x, U y) { if(y < x) x = y; }
template<typename T, typename U> inline void amax(T &x, U y) { if(x < y) x = y; }

int main() {
	int n, a, b;
	scanf("%d%d%d", &n, &a, &b);
	long long s = n * 6LL;
	pair<long long,pll> ans(INFL,mp(-1,-1)); 
	for(long long aa = a; ; ) {
		long long t = (s + aa - 1) / aa;
		long long bb = max(b * 1LL, t);
//		cerr << aa << ", " << bb << endl;
		amin(ans, mp(aa * bb, mp(aa, bb)));
		if(aa * b >= s) break;
		//s / aa > t-1 <-> s/(t-1) > aa
		long long na = (s + t-2) / (t-1);
		aa = na;
	}
	cout << ans.first << endl;
	cout << ans.second.first << " " << ans.second.second << endl;
	return 0;
}