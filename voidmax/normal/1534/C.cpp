const int N = 5e5 + 5;
const int p = 1e9 + 7;


int t, n;
bool used[N];
int val[N][2];
vector<int> edge[N];

void dfs(int i) {
    if (used[i]) return;
    used[i] = true;
    for (int j : edge[i]) {
        dfs(j);
    }
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> t;
    while (t--) {
        for (int i = 0; i < n; ++i) {
            edge[i].clear();
            used[i] = false;
        }
        cin >> n;
        for (int i = 0; i < n; ++i) {
            cin >> val[i][0];
        }
        for (int i = 0; i < n; ++i) {
            used[i] = false;
            cin >> val[i][1];
        }
        int ans = 1;
        for (int i = 0; i < n; ++i) {
            int a = val[i][0] - 1;
            int b = val[i][1] - 1;
            edge[a].push_back(b);
            edge[b].push_back(a);
            if (edge[a].size() > 2 || edge[b].size() > 2) {
                cout << 0 << '\n';
                ans = 0;
                break;
            }
        }
        if (!ans) continue;
        for (int i = 0; i < n; ++i) {
            if (used[i]) continue;
            dfs(i);
            ans = (ans + ans) % p;
        }
        cout << ans << '\n';
    }
}