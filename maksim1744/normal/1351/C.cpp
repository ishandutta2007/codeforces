void solve() {
    string s;
    cin >> s;
    set<pair<pair<int, int>,pair<int, int>>> was;
    int x = 0, y = 0;
    int ans = 0;
    for (auto c : s) {
        int x1 = x, y1 = y;
        if (c == 'N')
            y1++;
        else if (c == 'S')
            y1--;
        else if (c == 'W')
            x1--;
        else
            x1++;
        if (was.count(make_pair(make_pair(x, y), make_pair(x1, y1))))
            ++ans;
        else {
            auto p = make_pair(make_pair(x, y), make_pair(x1, y1));
            auto p1 = p;
            swap(p1.first, p1.second);
            ans += 5;
            was.insert(p);
            was.insert(p1);
        }
        x = x1;
        y = y1;
    }
    cout << ans << '\n';
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int T;
    cin >> T;
    while (T--) {
        solve();
    }

    return 0;
}