#include<bits/stdc++.h>
using namespace std;
const int N=1e6+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,nxt;
}e[N*2];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
int tot[N],deep[N],dfn[N],ndfn[N],siz[N],son[N],ans[N],cntdfn,Max=0;
void clear(int x)
{
	Max=0;
	for (int i=dfn[x];i<dfn[x]+siz[x];i++) tot[deep[ndfn[i]]]=0;
}
void add(int x)
{
	x=deep[x];
	tot[x]++;
	if (tot[x]>tot[Max]||(tot[x]==tot[Max]&&x<Max)) Max=x;
}
void dfs0(int now,int father)
{
	siz[now]=1,son[now]=0;
	deep[now]=deep[father]+1;
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father) continue;
		dfs0(to,now);
		siz[now]+=siz[to];
		if (siz[son[now]]<siz[to]) son[now]=to;
	}
}
void dfs1(int now,int father)
{
	ndfn[dfn[now]=++cntdfn]=now;
	if (son[now]) dfs1(son[now],now);
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father||to==son[now]) continue;
		dfs1(to,now);
	}
}
void dfs(int now,int father)
{
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father||to==son[now]) continue;
		dfs(to,now);
		clear(to);
	}
	if (son[now])
	{
		dfs(son[now],now);
		for (int i=dfn[son[now]]+siz[son[now]];i<dfn[now]+siz[now];i++) add(ndfn[i]);
	}
	add(now);
	ans[now]=Max;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	for (int i=1;i<n;i++)
	{
		int u=read(),v=read();
		add_edge(u,v);
		add_edge(v,u);
	}
	dfs0(1,0);
	dfs1(1,0);
	dfs(1,0);
	for (int i=1;i<=n;i++) print(ans[i]-deep[i]);
	
	return 0;
}
