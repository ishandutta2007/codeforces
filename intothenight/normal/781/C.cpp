ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m, k;
	cin >> n >> m >> k;
	vector<vi> adj(n);
	for(int i = 0; i < m; i ++){
		int u, v;
		cin >> u >> v, u --, v--;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vi line, visited(n);
	function<void(int)> dfs = [&](int u){
		visited[u] = true;
		line.push_back(u);
		for(auto v: adj[u]){
			if(!visited[v]){
				dfs(v);
				line.push_back(u);
			}
		}
	};
	dfs(0);
	int s = (2 * n - 1) / k;
	int r = (2 * n - 1) % k;
	for(int i = 0; i < k; i ++){
		if(r){
			r --;
			cout << s + 1 << " " << line.back() + 1 << " ";
			line.pop_back();
		}
		else{
			cout << s << " ";
		}
		for(int j = 0; j < s; j ++){
			cout << line.back() + 1 << " ";
			line.pop_back();
		}
		cout << "\n";
	}
	return 0;
}

