struct trys {
	int i;
	int a, b, c;
	trys(int a, int b, int c, int i = -1): a(a), b(b), c(c), i(i) {}
	trys() {}
	bool operator<(const trys &o) const {
		if (a == o.a) {
			if (b == o.b)
				return c < o.c;

			return b < o.b;
		}

		return a < o.a;
	}
	bool operator>(const trys &o) const {
		if (a == o.a) {
			if (b == o.b)
				return c > o.c;

			return b > o.b;
		}

		return a > o.a;
	}
};

int main () {
	int n, m;
	cin >> n >> m;
	trys a[m];

	vi ats(n + 1, 0);

	for (int i = 0; i < m; i++) {
		cin >> a[i].a >> a[i].b >> a[i].c;
		a[i].i = i + 1;
		ats[a[i].b] = i + 1;
	}

	sort(a, a + m);

	priority_queue<trys, vector<trys>, greater<trys>> eil;

	int j = 0;

	for (int i = 1; i <= n; i++) {
		while (j < m and a[j].a <= i) {
			eil.push(trys(a[j].b, a[j].a, a[j].c, a[j].i));
			j++;
		}

		if (ats[i] > 0) {
			if (!eil.empty() and eil.top().i == ats[i]) {
				cout << -1;
				exit(0);
			}

			ats[i] = m + 1;
		}
		else {
			if (!eil.empty()) {
				ats[i] = eil.top().i;
				trys x = eil.top();
				eil.pop();
				x.c--;

				if (x.c > 0)
					eil.push(x);
			}
		}
	}

	for (int i = 1; i <= n; i++) {
		cout << ats[i] << " ";
	}
}