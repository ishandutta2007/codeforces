template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {

  int n; cin >> n;
  queue<pair<int, int>> A, B;
  for (int i = 1; i <= n; ++i) for (int j = 1; j <= n; ++j)
      (((i + j) % 2 == 0) ? A : B).emplace(i, j);
  auto answer = [&](queue<pair<int, int>>& Q, int c) {
    auto f = Q.front(); Q.pop();
    cout << c << ' ' << f.first << ' ' << f.second << endl;
  };
  for (int rep = 0; rep != n * n; ++rep) {
    if (A.empty() || B.empty()) break;
    int a; cin >> a;
    if (a == 1) answer(B, 2);
    else answer(A, 1);
  }
  while (!A.empty()) {
    int a; cin >> a;
    answer(A, a == 1 ? 3 : 1);
  }
  while (!B.empty()) {
    int a; cin >> a;
    answer(B, a == 2 ? 3 : 2);
  }

  return 0;
}