struct UnionFind {
  vector< int > data;

  UnionFind(int sz) {
    data.assign(sz, -1);
  }

  bool unite(int x, int y) {
    x = find(x), y = find(y);
    if(x == y) return (false);
    if(data[x] > data[y]) swap(x, y);
    data[x] += data[y];
    data[y] = x;
    return (true);
  }

  int find(int k) {
    if(data[k] < 0) return (k);
    return (data[k] = find(data[k]));
  }

  int size(int k) {
    return (-data[find(k)]);
  }
};


int main() {
  int N, M;
  cin >> N >> M;
  vector< int > g[200000];
  vector< pair< int, int > > e;
  for(int i = 0; i < M; i++) {
    int x, y;
    cin >> x >> y;
    --x, --y;
    g[x].emplace_back(y);
    g[y].emplace_back(x);
    e.emplace_back(x, y);
  }

  int pos = 0;
  for(int i = 1; i < N; i++) {
    if(g[pos].size() < g[i].size()) {
      pos = i;
    }
  }

  UnionFind uf(N);
  vector< pair< int, int > > edges;
  for(auto &p : g[pos]) {
    uf.unite(pos, p);
    edges.emplace_back(pos, p);
  }
  for(auto &p : e) {
    if(uf.unite(p.first, p.second)) {
      edges.emplace_back(p.first, p.second);
    }
  }

  for(auto &p : edges) cout << p.first + 1 << " " << p.second + 1 << endl;
}