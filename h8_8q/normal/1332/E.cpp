const int mod=998244353;
int n,m,x,y,l,r;

inline int q_pow(int a,int b)
{
	int c=1;
	while(b)
	{
		if(b&1) c=a*c%mod;
		a=a*a%mod;b>>=1;
	}
	return c;
}

signed main()
{
	n=read();m=read();l=read();r=read();
	x=(r+1)/2-l/2;y=r/2-(l-1)/2;
	if(n*m%2==1) printf("%lld\n",q_pow(r-l+1,n*m));
	else printf("%lld\n",(q_pow(x+y,n*m)+q_pow((x-y+mod)%mod,n*m))%mod*q_pow(2,mod-2)%mod);
	return 0;
}