void Solve () {
    int n; cin >> n;
    vector<int> p(n), c(n), used(n, 0);
    cin >> p >> c;
    for (auto& v : p) v--;
    int ans = linf;
    for (int i = 0; i < n; i++){
        if (used[i]) continue;
        vector<int> cyc;
        used[i] = 1; cyc.pb(i);
        int j = i;
        while(p[j] != i){
            j = p[j];
            cyc.pb(j);
            used[j] = 1;
        }
        for (int st = 1; st <= sz(cyc); st ++){
            if(sz(cyc) % st != 0) continue;
            for(int x = 0; x < st; x++) {
                bool eq = true;
                for(int pos = x; pos + st < sz(cyc); pos += st) {
                    if(c[cyc[pos]] != c[cyc[pos + st]])
                        eq = false;
                }
                if(eq) {
                    ans = min(ans, st);
                    break;
                }
            }
        }
    }
    cout << ans << endl;
}

