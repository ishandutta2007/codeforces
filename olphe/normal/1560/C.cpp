void Solve() {
	cin >> N;
	int add = 1;
	int bef = 0;
	int mx = 0;
	while (1) {
		mx++;
		if (bef + add >= N) {
			N -= bef;
			if (N <= add / 2) {
				cout << N << " " << mx << endl;
			}
			else {
				cout << mx << " " << mx + 1 + add / 2 - N << endl;
			}
			return;
		}
		bef += add;
		add += 2;
	}
}

