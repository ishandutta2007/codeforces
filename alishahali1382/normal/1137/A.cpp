const int MAXN = 1010, LOG=20;

int n, m, k, u, v, x, y, t, a, b, ans;
int A[MAXN][MAXN];
vector<int> row[MAXN], col[MAXN];


int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=1; i<=n; i++) for (int j=1; j<=m; j++){
		cin>>A[i][j];
		row[i].pb(A[i][j]);
		col[j].pb(A[i][j]);
	}
	for (int i=1; i<=n; i++) sort(all(row[i])), row[i].resize(unique(all(row[i]))-row[i].begin());
	for (int i=1; i<=m; i++) sort(all(col[i])), col[i].resize(unique(all(col[i]))-col[i].begin());
	
	for (int i=1; i<=n; i++){
		for (int j=1; j<=m; j++){
			int a=lower_bound(all(row[i]), A[i][j])-row[i].begin()+1;
			int b=lower_bound(all(col[j]), A[i][j])-col[j].begin()+1;
			int r=row[i].size(), c=col[j].size();
			//debug2(a, b)
			cout<<max(max(r, c), max(a+c-b, r-a+b))<<' ';
		}
		cout<<'\n';
	}
	
	
	return 0;
}
