using namespace std;
const int N=505;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N],b[N];
bool dp[N][N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),k=read(),ra=0,rb=0;
	for (int i=1;i<=n;i++)
	{
		a[i]=read(),b[i]=read();
		ra+=a[i],rb+=b[i];
	}
	if ((ra+rb)/k==ra/k+rb/k) return print((ra+rb)/k),0;
	dp[0][0]=1;
	for (int i=1;i<=n;i++)
	{
		memcpy(dp[i],dp[i-1],sizeof(dp[i]));
		for (int j=1;j<k;j++)
		if (j<=a[i]&&k-j<=b[i])
		for (int t=0;t<k;t++) dp[i][(t+j)%k]|=dp[i-1][t];
	}
	for (int i=k-rb%k;i<=ra%k;i++) if (dp[n][i]) return print((ra+rb)/k),0;
	print(ra/k+rb/k);
	
	return 0;
}
