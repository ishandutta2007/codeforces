ll modexp(ll b, ll e, const ll &MOD){
	ll res = 1;
	while(e){
		if(e & 1){
			(res *= b) %= MOD;
		}
		(b *= b) %= MOD;
		e >>= 1;
	}
	return res;
}
ll modinv(ll b, const ll &MOD){
	return modexp(b, MOD - 2, MOD);
}

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	const ll mod = 1e9 + 7;
	int n;
	cin >> n;
	vl a(n - 1);
	cin >> a;
	vl prod(n), inv(n);
	prod[0] = 1, partial_sum(all(a), prod.begin() + 1, [&](ll x, ll y){return x * y % mod;});
	for(int i = 0; i < n; i ++) inv[i] = modinv(prod[i], mod);
	ll inv2 = modinv(2, mod);
	vl prev(prod);
	for(int i = 1; i <= 2 * n - 2; i ++){
		vl d(n);
		ll res = 0;
		for(int j = i / 2 + 1; j < n; j ++){
			res += d[j] = prev[j - 1] * a[j - 1] % mod;
		}
		res %= mod;
		if(i % 2 == 0){
			for(int j = i / 2; j < n; j ++){
				ll val = prod[j] * prod[j] % mod * inv[j - i / 2 + 1] % mod * (a[j - i / 2] - 1) % mod;
				(d[j] += val) %= mod;
				(res += val * inv2) %= mod;
			}
		}
		cout << res << " ";
		prev = d;
	}
	return 0;
}

