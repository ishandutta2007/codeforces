	void solve()
	{
		int n = ni();
		double[] ps = new double[n];
		double psum = 0;
		for(int i = 0;i < n;i++){
			ps[i] = 1-nd();
			psum += ps[i];
		}
		
		int[] from = new int[n - 1];
		int[] to = new int[n - 1];
		for (int i = 0; i < n - 1; i++) {
			from[i] = ni();
			to[i] = ni();
		}
		int[][] g = packU(n, from, to);
		int[][] pars = parents3(g, 0);
		int[] par = pars[0], ord = pars[1], dep = pars[2];
		
		int B = (int)Math.sqrt(n);
		int[] map = new int[n];
		int[] imap = new int[n];
		Arrays.fill(imap, -1);
		
		int p = 0;
		for(int i = 0;i < n;i++){
			if(g[i].length >= B){
				map[p] = i;
				imap[i] = p;
				p++;
			}
		}
		boolean[][] bg = new boolean[p][p];
		
		double[] osum = new double[p];
		for(int i = 0;i < p;i++){
			for(int e : g[map[i]]){
				osum[i] += ps[e];
				if(imap[e] != -1){
					bg[i][imap[e]] = true;
				}
			}
		}
		
		double ans = 0;
		for(int i = 0;i < n;i++){
			for(int e : g[i]){
				if(i < e){
					ans += 1-ps[i]*ps[e];
				}
			}
		}
		
		
		for(int z = ni();z > 0;z--){
			int ind = ni();
			double P = 1-nd();
			psum += P - ps[ind];
			
			if(imap[ind] == -1){
				for(int e : g[ind]){
					ans -= 1-ps[ind]*ps[e];
					ans += 1-P*ps[e];
					if(imap[e] != -1){
						osum[imap[e]] -= ps[ind];
						osum[imap[e]] += P;
					}
				}
			}else{
				ans -= 1-osum[imap[ind]]*ps[ind];
				ans += 1-osum[imap[ind]]*P;
				for(int j = 0;j < p;j++){
					if(bg[imap[ind]][j]){
						osum[j] -= ps[ind];
						osum[j] += P;
					}
				}
			}
			
			ps[ind] = P;
			out.printf("%.14f\n", ans + 1 - (n-psum));
		}
	}

	public static int[][] parents3(int[][] g, int root) {
		int n = g.length;
		int[] par = new int[n];
		Arrays.fill(par, -1);

		int[] depth = new int[n];
		depth[0] = 0;

		int[] q = new int[n];
		q[0] = root;
		for (int p = 0, r = 1; p < r; p++) {
			int cur = q[p];
			for (int nex : g[cur]) {
				if (par[cur] != nex) {
					q[r++] = nex;
					par[nex] = cur;
					depth[nex] = depth[cur] + 1;
				}
			}
		}
		return new int[][] { par, q, depth };
	}

	static int[][] packU(int n, int[] from, int[] to) {
		int[][] g = new int[n][];
		int[] p = new int[n];
		for (int f : from)
			p[f]++;
		for (int t : to)
			p[t]++;
		for (int i = 0; i < n; i++)
			g[i] = new int[p[i]];
		for (int i = 0; i < from.length; i++) {
			g[from[i]][--p[from[i]]] = to[i];
			g[to[i]][--p[to[i]]] = from[i];
		}
		return g;
	}

	
