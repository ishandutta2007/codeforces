using namespace std;

int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);
    int T;
    cin >> T;
    while (T--) {
        int N;
        cin >> N;
        vector<int> A(N);
        for (int i = 0; i < N; i++) {
            cin >> A[i];
        }
        if (is_sorted(begin(A), end(A))) {
            cout << "NO\n";
        } else {
            cout << "YES\n";
        }
    }
    return 0;
}