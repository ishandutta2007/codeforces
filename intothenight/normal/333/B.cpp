mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vector<vi> ok(4, vi(n, 1));
	while(m --){
		int x, y;
		cin >> x >> y, x --, y --;
		ok[0][x] = ok[1][y] = ok[2][n - 1 - x] = ok[3][n - 1 - y] = false;
	}
	int res = 0;
	for(int i = 1; 2 * i < n - 1; i ++){
		int t1 = 0, t2 = 0;
		for(int j = 0; j < 4; j ++){
			t1 += ok[j][i], t2 += ok[j][n - 1 - i];
		}
		res += max(t1, t2);
	}
	if(n % 2){
		res += (ok[0][n / 2] | ok[1][n / 2]);
	}
	cout << res;
	return 0;
}

