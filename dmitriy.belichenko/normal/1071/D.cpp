int cnt = 0;
int get(int x)
{
    int cl = 0;
    vector<int> a(8 , 0);
    for(int i = 1; i < cnt + 1; i++)
    {
        if(p[i] * p[i] > x)
        {
            break;
        }
        if(x % p[i])
        {
            continue;
        }
        while (x % p[i] == 0)
        {
            a[cl]++;
            x /= p[i];
        }
        cl++;
        if(x == 1)
        {
            break;
        }
    }
    if(x != 1)
    {
        a[cl++] = 1;
    }
    sort(a.begin() , a.begin() + 7);
    reverse(a.begin(), a.begin() + 7);
    for (int i = dp[a[0]]; ; i++)
    {
        bool flag = 1;
        for (int j= 1 ; j < 7; j++)
        {
            if (pre[i][j] != a[j])
            {
                flag = 0;
                break;
            }
        }
        if (flag)
        {
            return i;
        }
    }
    return 0;
}
