void solve() {
  int n;
  cin >> n;
  for (int x = 1; x <= n; x++) {
    for (int _ = 0; _ < x; _++) cout << "(";
    for (int _ = 0; _ < x; _++) cout << ")";
    for (int _ = 0; _ < n - x; _++) cout << "()";
    cout << '\n';
  }
  cout << '\n';
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t;
  cin >> t;
  while (t--) {
    solve();
  }
}