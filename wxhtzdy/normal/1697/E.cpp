vector<Mint> fact(1, 1);
vector<Mint> inv_fact(1, 1);
 
Mint C(int n, int k) {
  if (k < 0 || k > n) {
    return 0;
  }
  while ((int) fact.size() < n + 1) {
    fact.push_back(fact.back() * (int) fact.size());
    inv_fact.push_back(1 / fact.back());
  }
  return fact[n] * inv_fact[k] * inv_fact[n - k];
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);  
  int n;
  cin >> n;
  vector<int> x(n), y(n);
  for (int i = 0; i < n; i++) {
    cin >> x[i] >> y[i];
  }
  vector<vector<int>> d(n, vector<int>(n));
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < n; j++) {
      d[i][j] = abs(x[i] - x[j]) + abs(y[i] - y[j]);
    }
  }                        
  vector<vector<int>> g(n);
  vector<vector<bool>> f(n, vector<bool>(n));
  for (int i = 0; i < n; i++) {
    int mn = 1e9;
    for (int j = 0; j < n; j++) {
      if (i != j) {
        mn = min(mn, d[i][j]);
      }
    }
    for (int j = 0; j < n; j++) {
      if (i == j) {
        continue;
      }
      if (d[i][j] == mn) {
        g[i].push_back(j);
        f[i][j] = true;
      }
    } 
  }   
  vector<bool> skip(n, false);
  vector<int> sz;
  int cc = 0;
  for (int v = 0; v < n; v++) {
    if (skip[v]) {
      continue;
    }
    vector<int> que(1, v);
    vector<bool> was(n, false);
    was[v] = true;
    for (int b = 0; b < (int) que.size(); b++) {
      int i = que[b];
      for (int j : g[i]) {
        if (!was[j]) {
          was[j] = true;
          que.push_back(j);
        }
      }
    }
    bool ok = true;
    for (int i = 0; i < (int) que.size(); i++) {
      for (int j = 0; j < (int) que.size(); j++) {
        if (i == j) {
          continue;
        }
        if (!f[que[i]][que[j]]) {
          ok = false;
        }
      }
    }
    if (ok) {                      
      debug(que);
      sz.push_back((int) que.size());
      for (int j : que) {
        skip[j] = true;
      }
    } else {
      cc += 1;
    }
  }
  vector<Mint> dp(n + 1);
  dp[cc] = 1;
  for (int i = 0; i < (int) sz.size(); i++) {
    vector<Mint> new_dp(n + 1);
    for (int j = n; j >= 1; j--) {
      new_dp[j] = dp[j - 1];
      if (j >= sz[i]) {  
        new_dp[j] += dp[j - sz[i]];
      }
    }
    swap(dp, new_dp);
  }
  C(n, 0);
  Mint ans = 0;
  for (int i = 1; i <= n; i++) {
    ans += dp[i] * C(n, i) * fact[i];
  }
  cout << ans << '\n';
  return 0;
}