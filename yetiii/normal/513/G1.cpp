#define FROGS

#include <cstdio>
#include <algorithm>
#include <utility>
#include <vector>
#include <set>
#include <map>
#include <iostream>
#include <string>
#include <iomanip>
#include <string.h>
#include <numeric>
using namespace std;
typedef long long ll;

typedef vector<int> vi;
map<vi, double> m[10];
int main()
{
    //freopen("in.txt", "r", stdin);
    //freopen("out.txt", "w", stdout);
    int n, k;
    scanf("%d %d", &n, &k);
    vi p(n);
    for (int i=0;i<n;++i) cin>>p[i];
    m[0][p]=1;
    for (int it=0;it<k;++it)
    {
        map<vi, double> pr=m[it], &nx=m[it+1];
        for (auto &a:pr)
        {
            vi p=a.first;
            double pr=a.second, z=2.0/(n*(n+1));
            for (int i=0;i<n;++i) for (int j=i;j<n;++j)
            {
                reverse(p.begin()+i, p.begin()+j+1);
                nx[p]+=pr*z;
                reverse(p.begin()+i, p.begin()+j+1);
            }
        }
    }
    double rs=0;
    for (auto &a:m[k])
    {
        vi p=a.first;
        double pr=a.second;
        for (int i=0;i<n;++i) for (int j=i;j<n;++j)
        {
            if (p[j]<p[i]) rs+=pr;
        }
    }
    cout<<setprecision(13)<<fixed<<rs<<endl;
    return 0;
}