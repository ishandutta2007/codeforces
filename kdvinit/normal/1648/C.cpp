// Takes n&r as input and Returns : nCr%mod
int nCr(int $n,int $r)
{
   if($n<$r || $n<0 || $r<0) return 0;
   //if($n>Fact_Length) { cout<<"Error"<<endl; return; }
   int $ans=(Factorial[$n]*Mod_Inv(Factorial[$r]))%mod;
   $ans=($ans*Mod_Inv(Factorial[$n-$r]))%mod;
   return $ans;
}

//Segment_Tree1
//Range Query And Node Update (replace a[pos] = new_val)
//For any associative operator (+, *, max, min, gcd, xor)
//Use .update(pos, new_val) and .query(l, r)
class Segment_Tree1
{
private:
    int n;      //Size of array
    int *t;     //Segment Tree

public:
    //Kindly change only this part
    inline int combine(int xx, int yy) { return (xx+yy)%mod; }

    void build(int a[], int v, int tl, int tr)
    {
        if(tl==tr) t[v]=a[tl];
        else
        {
            int tm = (tl+tr)/2;
            build(a, 2*v, tl, tm);
            build(a, 2*v+1, tm+1, tr);
            t[v] = combine(t[2*v], t[2*v+1]);
        }
    }

    Segment_Tree1(int array[], int array_size) { n = array_size; t = new int[(4*n)]; build(array, 1, 1, n); }
    ~Segment_Tree1() { delete[] t; }

    int get_query(int v, int tl, int tr, int l, int r)
    {
        if(l==tl && r==tr) return t[v];
        int tm = (tl+tr)/2;
        if(l<=tm && tm<r) return combine(get_query(2*v, tl, tm, l, tm), get_query(2*v+1, tm+1, tr, tm+1, r));
        else if(tm<l) return get_query(2*v+1, tm+1, tr, l, r);
        else return get_query(2*v, tl, tm, l, r);
    }

    int query(int l, int r)     //Combine value of a[l...r]
    {
        if(l>r || l<1 || r>n) { cout<<"Invalid Query\n"; return -1; }
        return get_query(1, 1, n, l, r);
    }

    void get_update(int v, int tl, int tr, int pos, int new_val)
    {
        if(tl==tr) t[v]=new_val;
        else
        {
            int tm = (tl+tr)/2;
            if(pos<=tm) get_update(2*v, tl, tm, pos, new_val);
            else get_update(2*v+1, tm+1, tr, pos, new_val);
            t[v] = combine(t[2*v], t[2*v+1]);
        }
    }

    void update(int pos, int new_val)   //update a[pos]=new_val
    {
        //Note that this would not change the value in the original array
        if(pos<1 || pos>n) cout<<"Invalid Update\n";
        else get_update(1, 1, n, pos, new_val);
    }
};

const int N = 2e5 + 5;
int cnt[N], sts[N], stt[N];
int n, m;
int ans, cur;

void eval(int i, int j, Segment_Tree1& st)
{
    if(i>n || j>m)
    {
        if(j<=m) { ans++; ans%=mod; }
        return;
    }

    int len = n-i+1;

    cur = (cur*Mod_Inv(len))%mod;
    int val = stt[i];

    if(val!=1)
    {
        int x = (st.query(1, val-1))%mod;
        int tmp = (cur*x)%mod;
        ans += tmp;
        ans %= mod;
    }

    if(cnt[val]==0) return;

    cur = (cur*cnt[val])%mod;
    cnt[val]--;
    st.update(val, cnt[val]);

    eval(i+1, j+1, st);
    return;
}

void solve()
{
    cin>>n>>m;

    for(int i=1; i<=N; i++) cnt[i]=0;

    int mx=0;
    cur=Factorial[n];

    for(int i=1; i<=n; i++)
    {
        int x; cin>>x;
        sts[i]=x;
        cnt[x]++;
        mx = max(mx, x);
        cur = (cur*Mod_Inv(cnt[x]))%mod;
    }

    for(int i=1; i<=m; i++) cin>>stt[i];

    Segment_Tree1 st(cnt, mx);

    ans=0;
    eval(1, 1, st);
    cout<<ans<<endl;
}

