const ld PI = 3.14159265359;

const ll MOD = (ll) 998244353ll;
const ll MAXN = (ll) 2e5 + 10;
const ll INF = (ll) 9223372036854775;
const ld EPS = (ld) 1e-8;

ll a[MAXN];

map<ll, ll> ma;

int main(){
    ios::sync_with_stdio(0);cin.tie(0);cout.tie(0);
	ll n, k;
	cin >> n >> k;
	for(int i = 0;i<n;i++) cin >> a[i];
	ll m = (1ll << k) - 1;
	ll ans = n*(n+1) / 2;
	ll px = 0;
	ma[0]++;
	for(int i = 0; i < n; i++){
		px = px ^ a[i];
		ma[min(px, px ^ m)]++;
	}
	ll up,dw;
	for(auto node : ma){
		dw = (node.S)/2;
		up = (node.S + 1) /2;
		ans = ans - (dw*(dw - 1) / 2) - (up*(up - 1) / 2);
	}
	cout << ans;
	return 0;
}


