void precalc() {
}

const int maxn = (int) 2e5 + 5;
int n, k;
vector<int> g[maxn];

bool read() {
  if (scanf("%d%d", &n, &k) < 2) {
    return false;
  }
  for (int i = 0; i < n; i++) {
    g[i].clear();
  }
  for (int i = 0; i < n - 1; i++) {
    int v, u;
    scanf("%d%d", &v, &u);
    v--;
    u--;
    g[v].push_back(u);
    g[u].push_back(v);
  }
  return true;
}

int dep[maxn], ts[maxn];

void dfs(int v, int p) {
  ts[v] = 1;
  for (int i = 0; i < sz(g[v]); i++) {
    int u = g[v][i];
    if (u == p) {
      continue;
    }
    dep[u] = dep[v] + 1;
    dfs(u, v);
    ts[v] += ts[u];
  }
}

int a[maxn];

void solve() {
  dep[0] = 1;
  dfs(0, -1);
  for (int i = 0; i < n; i++) {
    a[i] = dep[i] - ts[i];
  }
  sort(a, a + n, greater<int>());
  long long res = 0;
  for (int i = 0; i < k; i++) {
    res += a[i];
  }
  printf("%lld\n", res);
}

int main() {
  precalc();
#ifdef DEBUG
  assert(freopen(TASK ".in", "r", stdin));
  assert(freopen(TASK ".out", "w", stdout));
#endif
  while (read()) {
    solve();
#ifdef DEBUG
    eprintf("Time %.2f\n", (double) clock() / CLOCKS_PER_SEC);
#endif
  }
  return 0;
}