#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 1e2 + 10;
int a[N][N];
int n, m;
bool check(int r)
{
	int l = 1;
	while (l < r)
	{
		for (int i = 1; i <= m; i++)
		{
			if (a[l][i] != a[r][i]) return false;
		}
		l++, r--;
	}
	return true;
}
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin >> n >> m;
	for (int i = 1; i <= n; i++)
	{
		for (int j = 1; j <= m; j++)
		{
			cin >> a[i][j];
		}
	}
	int ans = n;
	while (n % 2 == 0 && check(n))
	{
		n /= 2;
		ans = n;
	}
	cout << ans << endl;
	return 0;
}