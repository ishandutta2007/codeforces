const int Maxn = 1e6 + 10;
const ll Inf = 2242545357980376863LL;
const ll Log = 30;

ll k = 1000;
ll cnt[Maxn];


int main(){
	ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	ll n;
	cin >> n;
	str s;
	cin >> s;
	
	
	
	ll a, b;
	for(int i = 0; i < n; i++){
		cin >> a >> b;
		ll nw = s[i] - '0';
		for(int j = 0; j < k; j++){
			if(j>=b) if((j-b) % a == 0) nw = 1- nw;
			cnt[j] += nw;
		}
	}
	cout << *max_element(cnt, cnt + Maxn);
	return 0;
}

