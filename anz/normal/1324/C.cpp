typedef long long ll;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;

ll gcd(ll a, ll b) { for (; b; a %= b, swap(a, b)); return a; }

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0), cout.tie(0);

	int t; cin >> t;
	while (t--)
	{
		string s; cin >> s;
		int ans = 1;

		int cur = 1;
		for (auto c : s)
		{
			if (c == 'L')
			{
				cur++;
				ans = max(ans, cur);
			}
			else cur = 1;
		}

		ans = max(ans, cur);
		cout << ans << '\n';
	}
}