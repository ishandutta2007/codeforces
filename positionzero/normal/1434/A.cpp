template<class T>
istream &operator>>(istream &is, vector<T> &v) {
  for (T &x : v)
    is >> x;
  return is;
}

template<class T>
ostream &operator<<(ostream &os, const vector<T> &v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  vector<int> a(6);
  cin >> a;
  int n;
  cin >> n;
  vector<int> b(n);
  cin >> b;
  int ans = numeric_limits<int>::max();
  vector<pair<int, int>> v;
  v.reserve(n * 6);
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < 6; ++j)
      v.emplace_back(b[i] - a[j], i);
  }
  sort(v.begin(), v.end());
  multiset<int> mns;
  vector<int> cur(n, -1);
  for (int i = 0; i < n; ++i) mns.insert(-1);
  for (const auto&[x, id] : v) {
    mns.erase(mns.find(cur[id]));
    mns.insert(cur[id] = x);
    if (*mns.begin() != -1)
      ans = min(ans, x - *mns.begin());
  }
  cout << ans << '\n';

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}