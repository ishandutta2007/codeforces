char s[200020];
int csu[200020][30];

int main() {
	cin>>(s+1);
	int n = strlen(s+1);
	for(int i=1;i<=n;i++){
		memcpy(csu[i],csu[i-1],sizeof(csu[i-1]));
		csu[i][s[i]-'a']+=1;
	}
	int q,l,r;
	read(q);
	while(q--){
		read(l,r);
		int cnt = 0;
		for(int j=0;j<26;j++)if(csu[r][j]-csu[l-1][j])cnt++;
		int ans = 0;
		if(s[l]!=s[r])ans = 1;
		if(l == r)ans = 1;
		if(cnt>2) ans = 1;
		if(ans){
			cout<<"Yes\n";
		}else{
			cout<<"No\n";
		}
	}
	return 0;
}