struct disjoint: vi{
	int N;
	// vector<pii> Log; // For persistency
	disjoint(int N): N(N){
		this->resize(N);
		iota(all((*this)), 0);
	}
	int root(int u){
		// Log.emplace_back(u, (*this)[u]);
		return (*this)[u] == u ? u : ((*this)[u] = root((*this)[u]));
	}
	void merge(int u, int v){
		(*this)[root(v)] = root(u);
	}
	int share(int u, int v){
		return root((*this)[u]) == root((*this)[v]);
	}
	/*void reverse(){
		auto [u, p] = Log.back();
		Log.pop_back();
		(*this)[u] = p;
	}*/
};
 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vtpl a(n);
	for(auto &[x, y, ind]: a){
		cin >> x >> y;
		static int cnt = 0;
		ind = cnt ++;
	}
	sort(all(a));
	auto cmp = [&](const tpl &x, const tpl &y){return get<1>(x) == get<1>(y) ? get<0>(x) < get<0>(y) : get<1>(x) < get<1>(y);};
	set<tpl, decltype(cmp)> q(cmp);
	disjoint dsu(n);
	int cnt = 0;
	for(auto [x, y, ind]: a){
		if(q.empty()){
			q.insert({x, y, ind});
			continue;
		}
		auto i = q.lower_bound({0, x, 0}), j = q.lower_bound({0, y, 0});
		while(i != j){
			auto [x1, y1, ind1] = *i;
			if(dsu.share(ind, ind1)){
				cout << "NO";
				return 0;
			}
			dsu.merge(ind, ind1);
			cnt ++;
			if(cnt == n){
				cout << "NO";
				return 0;
			}
			i ++;
		}
		q.insert({x, y, ind});
	}
	if(cnt < n - 1) cout << "NO";
	else cout << "YES";
	return 0;
}
 
