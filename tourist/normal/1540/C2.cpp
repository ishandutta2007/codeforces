int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<int> c(n);
  for (int i = 0; i < n; i++) {
    cin >> c[i];
  }
  vector<int> b(n - 1);
  for (int i = 0; i < n - 1; i++) {
    cin >> b[i];
  }
  int tt;
  cin >> tt;
  vector<int> x(tt);
  for (int i = 0; i < tt; i++) {
    cin >> x[i];
  }
  Mint prod = 1;
  for (int i = 0; i < n; i++) {
    prod *= c[i] + 1;
  }
  const int M = 100000;
  int val = -M;
  vector<int> bound(n);
  for (int i = 0; i < n; i++) {
    bound[i] = val * (i + 1);
    for (int k = 0; k < i; k++) {
      bound[i] += b[k] * (i - k);
    }
  }
  vector<Mint> res(2 * M + 1, 0);
  while (val <= M) {
    bool fail = false;
    bool win = true;
    for (int i = 0; i < n; i++) {
      if (bound[i] > 100 * (i + 1)) {
        fail = true;
        break;
      }
      if (bound[i] > 0) {
        win = false;
      }
    }
    if (fail) {
      break;
    }
    if (win) {
      res[val + M] = prod;
    } else {
      // s = a[0] + a[1] + ... + a[i]
      // s = f + (f + b[0]) + (f + b[0] + b[1]) + ... + (f + b[0] + b[1] + ... + b[i - 1])
      // f = (s - b[0] * i - b[1] * (i - 1) - ... - b[i - 1]) / (i + 1)
      vector<Mint> dp(1, 1);
      for (int i = 0; i < n; i++) {
        vector<Mint> new_dp(dp.size() + c[i] + 1, 0);
        for (int j = 0; j < (int) dp.size(); j++) {
          new_dp[j] += dp[j];
          new_dp[j + c[i] + 1] -= dp[j];
        }
        for (int j = 0; j < (int) new_dp.size() - 1; j++) {
          new_dp[j + 1] += new_dp[j];
        }
        new_dp.pop_back();
        swap(dp, new_dp);
        for (int j = 0; j < bound[i] && j < (int) dp.size(); j++) {
          dp[j] = 0;
        }
      }
      res[val + M] = accumulate(dp.begin(), dp.end(), Mint(0));
    }
    for (int i = 0; i < n; i++) {
      bound[i] += i + 1;
    }
    val += 1;
  }
  for (int qq = 0; qq < tt; qq++) {
    cout << res[x[qq] + M] << '\n';
  }
  return 0;
}