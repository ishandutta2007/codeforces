mt19937 rnd(time(0));


 
int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vector<vi> adj(n);
	for(int u = 1; u < n; u ++){
		int v;
		cin >> v, v --;
		adj[v].push_back(u);
	}
	vector<vl> d(n); // f: not choose, s: choose
	const ll mod = 998244353;
	auto dfs = [&](int u, auto &dfs)->void{
		if(adj[u].empty()){
			d[u] = {1, 0, 0};
		}
		else{
			d[u] = {0, 0, 1};
		}
		for(auto v: adj[u]){
			dfs(v, dfs);
			d[u] = {
				(d[u][0] * (2 * d[v][0] + d[v][1] + d[v][2]) + d[u][1] * (d[v][0] + d[v][1])) % mod
				, (d[u][1] * (d[v][0] + d[v][2]) + d[u][2] * (d[v][0] + d[v][1])) % mod
				, d[u][2] * (d[v][0] + d[v][2]) % mod
			};
		}
	};
	dfs(0, dfs);
	cout << (d[0][0] + d[0][2]) % mod;
	return 0;
}
