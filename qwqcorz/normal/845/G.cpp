#include<bits/stdc++.h>
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,val,nxt;
}e[N*2];
int head[N],cnte=0;
void add_edge(int u,int v,int w)
{
	e[++cnte].to=v;
	e[cnte].val=w;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
struct xor_basis
{
	int f[30];
	void insert(int x)
	{
		for (int i=29;i>=0;i--)
		if ((x>>i)&1)
		{
			if (!f[i]) return void(f[i]=x);
			x^=f[i];
		}
	}
	int querymin(int x)
	{
		for (int i=29;i>=0;i--) if (((x>>i)&1)&&f[i]) x^=f[i];
		return x;
	}
	int querymax(int x)
	{
		for (int i=29;i>=0;i--) if (((~x>>i)&1)&&f[i]) x^=f[i];
		return x;
	}
}t;
bool vis[N];
int dis[N],deep[N];
void dfs(int now,int father)
{
	vis[now]=1;
	deep[now]=deep[father]+1;
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father) continue;
		if (vis[to]) deep[to]<=deep[now]?t.insert(dis[now]^dis[to]^e[i].val):void();
		else dis[to]=dis[now]^e[i].val,dfs(to,now);
	}
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read();
	for (int i=1;i<=m;i++)
	{
		int u=read(),v=read(),w=read();
		add_edge(u,v,w);
		add_edge(v,u,w);
	}
	dfs(1,0);
	print(t.querymin(dis[n]));
	
	return 0;
}
