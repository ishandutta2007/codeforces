using namespace std;
const int MOD = 998244353;

int n;

int add(int x, int y)
{
    return (x + y) % MOD;
}

int mul(int x, int y)
{
    return x * y % MOD;
}

int binpow(int x, int y)
{
    if(y == 0)
        return 1;

    if(y % 2)
        return mul(x, binpow(x, y - 1));

    int z = binpow(x, y / 2);

    return mul(z, z);
}

int32_t main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    cin >> n;

    for(int i = 1; i < n; i++)
        cout << add(mul(18, binpow(10, n - i)), mul(binpow(10, n - i - 1), mul(81, max(0ll, n - i - 1)))) << ' ';

    cout << 10;
}