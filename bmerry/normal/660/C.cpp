static void solve()
{
    int N, K;
    cin >> N >> K;
    vector<char> a(N);
    for (int i = 0; i < N; i++)
        cin >> a[i];
    int L = 0;
    int flip = 0;
    int bestL = -1, bestR = -2;
    for (int R = 1; R <= N; R++)
    {
        char c = a[R - 1];
        if (c == '0')
        {
            flip++;
            while (flip > K)
            {
                if (a[L] == '0')
                    flip--;
                L++;
            }
        }
        if (R - L > bestR - bestL)
        {
            bestL = L;
            bestR = R;
        }
    }

    cout << bestR - bestL << '\n';
    fill(a.begin() + bestL, a.begin() + bestR, '1');
    for (char c : a)
        cout << c << ' ';
    cout << '\n';
}

int main(int argc, const char **argv)
{
    redirect(argc, argv);
    solve();

    return 0;
}