const int Mod = 1000000009;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}


const int MAXN = 100228;

int n;
int a[MAXN];
int val[MAXN];



struct fenwick
{
    vector<int> f;
    void init(int n) {
        f.resize(n, 0);
    }
    void inc(int i, int val) {
        for (; i < sz(f); i |= (i + 1)) {
            f[i] += val;
        }
    }
    int get(int i) {
        int res = 0;
        for (; i >= 0; i = (i & (i + 1)) -1) {
            res += f[i];
        }
        return res;
    }
};


void solve() {
    cin >> n;
    vector<int> pos[2];
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        a[i] &= 1;
        pos[a[i]].pb(i);
    }
    ll ans = 1e18;
    for (int t = 0; t < 2; t++) {
        int cnt[2] = {0, 0};
        for (int i = 0; i < n; i++) {
            int need = (i + t) & 1;
            cnt[need]++;
        }
        if (cnt[0] != sz(pos[0]) || cnt[1] != sz(pos[1])) {
            continue;
        }
        int cur[2] = {0, 0};
        for (int i = 0; i < n; i++) {
            int need = (i + t) & 1;
            val[pos[need][cur[need]]] = i;
            cur[need]++;
        }
        fenwick kek;
        kek.init(n);
        ll sum = 0;
        for (int i = 0; i < n; i++) {
            sum += i - kek.get(val[i]);
            kek.inc(val[i], 1);
        }
        chkmin(ans, sum);
    }   
    if (ans > 1e17) {
        ans = -1;
    }
    cout << ans << '\n';
}


