namespace atcoder {

template <class S, S (*op)(S, S), S (*e)()> struct segtree {
  public:
    segtree() : segtree(0) {}
    explicit segtree(int n) : segtree(std::vector<S>(n, e())) {}
    explicit segtree(const std::vector<S>& v) : _n(int(v.size())) {
        log = internal::ceil_pow2(_n);
        size = 1 << log;
        d = std::vector<S>(2 * size, e());
        for (int i = 0; i < _n; i++) d[size + i] = v[i];
        for (int i = size - 1; i >= 1; i--) {
            update(i);
        }
    }

    void set(int p, S x) {
        assert(0 <= p && p < _n);
        p += size;
        d[p] = x;
        for (int i = 1; i <= log; i++) update(p >> i);
    }

    S get(int p) const {
        assert(0 <= p && p < _n);
        return d[p + size];
    }

    S prod(int l, int r) const {
        assert(0 <= l && l <= r && r <= _n);
        S sml = e(), smr = e();
        l += size;
        r += size;

        while (l < r) {
            if (l & 1) sml = op(sml, d[l++]);
            if (r & 1) smr = op(d[--r], smr);
            l >>= 1;
            r >>= 1;
        }
        return op(sml, smr);
    }

    S all_prod() const { return d[1]; }

    template <bool (*f)(S)> int max_right(int l) const {
        return max_right(l, [](S x) { return f(x); });
    }
    template <class F> int max_right(int l, F f) const {
        assert(0 <= l && l <= _n);
        assert(f(e()));
        if (l == _n) return _n;
        l += size;
        S sm = e();
        do {
            while (l % 2 == 0) l >>= 1;
            if (!f(op(sm, d[l]))) {
                while (l < size) {
                    l = (2 * l);
                    if (f(op(sm, d[l]))) {
                        sm = op(sm, d[l]);
                        l++;
                    }
                }
                return l - size;
            }
            sm = op(sm, d[l]);
            l++;
        } while ((l & -l) != l);
        return _n;
    }

    template <bool (*f)(S)> int min_left(int r) const {
        return min_left(r, [](S x) { return f(x); });
    }
    template <class F> int min_left(int r, F f) const {
        assert(0 <= r && r <= _n);
        assert(f(e()));
        if (r == 0) return 0;
        r += size;
        S sm = e();
        do {
            r--;
            while (r > 1 && (r % 2)) r >>= 1;
            if (!f(op(d[r], sm))) {
                while (r < size) {
                    r = (2 * r + 1);
                    if (f(op(d[r], sm))) {
                        sm = op(d[r], sm);
                        r--;
                    }
                }
                return r + 1 - size;
            }
            sm = op(d[r], sm);
        } while ((r & -r) != r);
        return 0;
    }

  private:
    int _n, size, log;
    std::vector<S> d;

    void update(int k) { d[k] = op(d[2 * k], d[2 * k + 1]); }
};

}  // namespace atcoder

using namespace std;
long long op(long long a,long long b){return max(a,b);}
long long e(){return 0LL;}
int N,Qc;
int A[2<<17];
vector<int>Y;
vector<vector<long long> >sum;
vector<atcoder::segtree<long long,op,e> >Q;
void solve()
{
	long long ret=0;
	for(int id=0;id<Y.size();id++)
	{
		int k=Y[id];
		ret=max(ret,Q[id].all_prod()*k);
	}
	cout<<ret<<"\n";
}
int main()
{
	ios::sync_with_stdio(false);
	cin.tie(nullptr);
	int T;cin>>T;
	for(;T--;)
	{
		cin>>N>>Qc;
		for(int i=0;i<N;i++)cin>>A[i];
		Y.clear();
		sum.clear();
		Q.clear();
		vector<int>d;
		for(int i=N-1;i>=1;i--)if(N%i==0)
		{
			bool fn=false;
			for(int q:d)if(q%i==0)fn=true;
			if(fn)continue;
			d.push_back(i);
			Y.push_back(i);
			sum.push_back(vector<long long>(i,0LL));
			for(int j=0;j<i;j++)
			{
				for(int k=j;k<N;k+=i)sum.back()[j]+=A[k];
			}
			Q.push_back(atcoder::segtree<long long,op,e>(sum.back()));
		}
		solve();
		for(int i=0;i<Qc;i++)
		{
			int p,x;cin>>p>>x;
			p--;
			for(int id=0;id<Y.size();id++)
			{
				int j=p%Y[id];
				sum[id][j]+=x-A[p];
				Q[id].set(j,sum[id][j]);
			}
			A[p]=x;
			solve();
		}
	}
}