int n,k;
string s;
vector<vector<int> > moves;
bool move(){
	vector<int> tr;
	for(int i=0;i<n-1;i++){
		if(s[i]=='R'&&s[i+1]=='L'){
			tr.pb(i);
			s[i]='L';
			s[i+1]='R';
			i++;
		}
	}
	if(tr.size()){
		moves.pb(tr);
		return 1;
	}
	return 0;
}
int main()
{
	scanf("%i %i",&n,&k);
	cin >> s;
	while(move());
	if(k<moves.size()){
		printf("-1\n");
		return 0;
	}
	int sum=0;
	for(auto p:moves)
		sum+=p.size();
	if(sum<k){
		printf("-1\n");
		return 0;
	}
	vector<int> extra;
	int cnt=moves.size();
	while(cnt<k){
		//cout << moves << extra << endl;
		if(moves.back().size()==1){
			extra.pb(moves.back()[0]);
			moves.pop_back();
		}
		else{
			extra.pb(moves.back().back());
			moves.back().pop_back();
			cnt++;
		}
	}
	for(auto p:moves){
		printf("%i ",p.size());
		for(auto d:p)
			printf("%i ",d+1);
		printf("\n");
	}
	reverse(all(extra));
	for(auto p:extra)
		printf("1 %i\n",p+1);
	return 0;
}