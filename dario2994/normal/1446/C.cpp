struct Node {
    Node* sons[2];
    Node() {
        sons[0] = sons[1] = nullptr;
    }
    Node* get_son(int id) {
        if (sons[id] == nullptr) sons[id] = new Node();
        return sons[id];
    }
};

int solve(Node* v) {
    if (v == nullptr) return 0;
    int a = solve(v->sons[0]);
    int b = solve(v->sons[1]);

    if (a == 0 and b == 0) return 1;
    if (a == 0) return b;
    if (b == 0) return a;
    return max(a, b) + 1;
}

