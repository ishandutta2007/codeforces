#include<bits/stdc++.h>

using namespace std;

// B - ???????, C - ???, S - ???????

string r;
int b, c, s; 
int pb, pc, ps;
long long m; // ??????? ?????? ? ????????? ????

bool can(long long k)
{
	map<char, int> recipe;
	for(auto x : r)
		recipe[x]++;
	long long req_money = 0; // ??????? ??? ???????????, ????? ??????????? k ????????????
	req_money += max(recipe['B'] * k - b, 0ll) * pb;
	req_money += max(recipe['S'] * k - s, 0ll) * ps;
	req_money += max(recipe['C'] * k - c, 0ll) * pc;
	return req_money <= m;
}			
 	
int main()
{
	cin >> r;
	cin >> b >> s >> c; // ???-?? ??????? ???????????
	cin >> pb >> ps >> pc;
	cin >> m;

	long long lf = 0;
	long long rg = (long long)(1e13);

	// ?????????: can(lf) == true, can(rg) == false

	while(rg - lf > 1)
	{
		long long mid = (lf + rg) / 2;
	 	if(can(mid))
			lf = mid;
		else
			rg = mid; 
	}

	cout << lf << endl;
        
}