constexpr int md = 998244353;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

typedef vector<Mint> poly;
poly operator-(poly a, poly b) {
  poly ret(max(a.size(), b.size()));
  for (int i = 0; i < (int)a.size(); i++)
    ret[i] += a[i];
  for (int i = 0; i < (int)b.size(); i++)
    ret[i] -= b[i];
  return ret;
}

poly integrate(poly a) {
  poly ret(a.size()+1);
  for (int i = 0; i < (int)a.size(); i++) {
    ret[i+1] = a[i];
  }
  return ret;
}

map<pair<int,ll>, Mint> mem;
Mint f(int n, ll m) {
  if (!mem.count({n,m})) {
    Mint p = 1, q = 1;
    for (int i = 1; i <= n; i++) {
      p *= m+n+1-i;
      q *= i;
    }
    mem[{n,m}] = p / q;
  }
  return mem[{n,m}];
}

Mint evaluate(poly a, ll x) {
  Mint ans = 0;
  for (int i = 0; i < (int)a.size(); i++) {
    ans += f(i, x) * a[i];
  }
  return ans;
}

