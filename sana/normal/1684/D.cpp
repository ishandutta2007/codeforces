#define all(x) begin(x), end(x)


typedef string str;
typedef long long ll;


#define ts to_string


str ts(const char* s) { return (str)s; }


template<class A> void pr(A x) { cout << ts(x); }

void ps() { pr("\n"); }
template<class H, class... T> void ps(const H& h, const T&... t) { pr(h); if (sizeof...(t)) pr(" "); ps(t...); }
 

void go() {
    int n, k;
    cin >> n >> k;
    
    ll tot = 0;
    
    vector<ll> tr(n);
    for (int i = 0; i < n; i++) {
        cin >> tr[i];
        tot += tr[i];
        tr[i] -= (n-i-1);
    }
    
    ll jmp = (k * 1ll * (k-1) )/2;
    
    sort(all(tr));
    for (int i = n-1; i >= n-k; i--) tot -= tr[i];
    tot -= jmp;
    ps(tot);
}

void solve()
{
    int t;
    for (cin >> t; t; t--) go();
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    solve();
    return 0;
}