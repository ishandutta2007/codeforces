#define MIN(a,b) ((a) < (b) ? (a) : (b))
#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 1e2 + 10;
map<char, int> mc[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n, m;
	cin >> n >> m;
	for (int i = 0; i < n; i++)
	{
		string str;
		cin >> str;
		for (int j = 0; j < m; j++)
		{
			mc[j][str[j]] = 1;
		}
	}
	LL ans = 1;
	for (int i = 0; i < m; i++)
	{
		ans = ans*mc[i].size() % INF;
	}
	cout << ans << endl;
	return 0;
}