fn main() {
	let (mut rin,mut rout) = rio();
	const N: i64 = 1e9 as i64;

	writeln!(rout, "? 1 1").unwrap();
	writeln!(rout, "? 1 {}", N).unwrap();
	rout.flush().unwrap();
	l!(d1,d2 = rin.l());
	let x = (N-1 - d1 - d2) / 2;
	let p = 1 + d1 + x;
	writeln!(rout, "? 1 {}", p).unwrap();
	writeln!(rout, "? {} {}", N, p).unwrap();
	rout.flush().unwrap();
	l!(z1,z2 = rin.l());
	let x1 = 1 + z1;
	let x2 = N - z2;
	let y1 = d1 - x1 + 2;
	let y2 = x1 - 1 + N - d2;
	writeln!(rout, "! {} {} {} {}", x1, y1, x2, y2).unwrap();
}