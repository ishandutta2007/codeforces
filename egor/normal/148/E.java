class TaskE {
	public void solve(int testNumber, InputReader in, OutputWriter out) {
		int rowCount = in.readInt();
		int count = in.readInt();
		int[] answer = new int[count + 1];
		for (int i = 0; i < rowCount; i++) {
			int length = in.readInt();
			int[] price = IOUtils.readIntArray(in, length);
			int[] maxCost = new int[length + 1];
			int sumLeft = 0;
			for (int j = 0; j <= length; j++) {
				int sumRight = 0;
				for (int k = length; k >= j; k--) {
					maxCost[j + length - k] = Math.max(maxCost[j + length - k], sumLeft + sumRight);
					if (k != 0)
						sumRight += price[k - 1];
				}
				if (j != length)
					sumLeft += price[j];
			}
			for (int j = count; j >= 0; j--) {
				for (int k = 1; k <= j && k <= length; k++)
					answer[j] = Math.max(answer[j], answer[j - k] + maxCost[k]);
			}
		}
		out.printLine(answer[count]);
	}
}

class InputReader {

	private InputStream stream;
	private byte[] buf = new byte[1024];
	private int curChar;
	private int numChars;

	public InputReader(InputStream stream) {
		this.stream = stream;
	}

	public int read() {
		if (numChars == -1)
			throw new InputMismatchException();
		if (curChar >= numChars) {
			curChar = 0;
			try {
				numChars = stream.read(buf);
			} catch (IOException e) {
				throw new InputMismatchException();
			}
			if (numChars <= 0)
				return -1;
		}
		return buf[curChar++];
	}

	public int readInt() {
		int c = read();
		while (isSpaceChar(c))
			c = read();
		int sgn = 1;
		if (c == '-') {
			sgn = -1;
			c = read();
		}
		int res = 0;
		do {
			if (c < '0' || c > '9')
				throw new InputMismatchException();
			res *= 10;
			res += c - '0';
			c = read();
		} while (!isSpaceChar(c));
		return res * sgn;
	}

	public static boolean isSpaceChar(int c) {
		return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
	}

	}

class OutputWriter {
	private final PrintWriter writer;

	public OutputWriter(OutputStream outputStream) {
		writer = new PrintWriter(outputStream);
	}

	public OutputWriter(Writer writer) {
		this.writer = new PrintWriter(writer);
	}

	public void print(Object...objects) {
		for (int i = 0; i < objects.length; i++) {
			if (i != 0)
				writer.print(' ');
			writer.print(objects[i]);
		}
	}

	public void printLine(Object...objects) {
		print(objects);
		writer.println();
	}

	public void close() {
		writer.close();
	}

	}

class IOUtils {

	public static int[] readIntArray(InputReader in, int size) {
		int[] array = new int[size];
		for (int i = 0; i < size; i++)
			array[i] = in.readInt();
		return array;
	}

	}