const ll mod = 1e9+7;
ll x, n, m, i, r, p, ans=1;
ll qpow(ll b,ll exp){
    if(exp<=0) return 1;
    else if(exp&1) return b*qpow(b,exp-1)%mod;
    else{ll r=qpow(b,exp>>1); return r*r%mod;}
}

int main(){
    scanf("%lld%lld",&x,&n);
    r = x;
    for(i=2;i*i<=x;i++){
        if(r%i==0){
            while(r%i==0) r/=i;
            m = n;
            while(m){
                m /= i;
                ans=(ans*qpow(i,m))%mod;
            }
        }
    }
    if(r!=1){
        m = n;
        while(m){
            m /= r;
            ans=(ans*qpow(r,m))%mod;
        }
    }
    printf("%lld\n",ans);
    return 0;
}