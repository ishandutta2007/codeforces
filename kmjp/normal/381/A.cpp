    while(l<=r) {
        if(A[l]<A[r]) ff+=A[r],r--;
        else ff+=A[l],l++;
        if(l>r) break;
        if(A[l]<A[r]) ss+=A[r],r--;
        else ss+=A[l],l++;
    }
