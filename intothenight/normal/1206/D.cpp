#include<bits/stdc++.h>
#include<chrono>
using namespace std;
using namespace chrono;
#define all(a) a.begin(), a.end()
#define sz(x) (int(x.size()))
typedef long long ll;
typedef vector<int> vi;
typedef vector<ll> vl;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
typedef pair<int, ll> pil;
typedef pair<ll, int> pli;
typedef vector<string> vs;
template<class T1, class T2>
istream &operator>>(istream &in, pair<T1, T2> &P){
	in >> P.first >> P.second;
	return in;
}
template<class T1, class T2>
ostream &operator<<(ostream &out, pair<T1, T2> &P){
	out << "(" << P.first << ", " << P.second << ")";
	return out;
}
template<class T>
istream &operator>>(istream &in, vector<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, vector<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vl a(n);
	cin >> a;
	vector<vi> adj(n);
	for(ll k = 1; k <= 1e18; k <<= 1){
		vl cur;
		for(int i = 0; i < n; i ++){
			if(a[i] & k) cur.push_back(i);
			if(sz(cur) >= 3){
				cout << 3;
				return 0;
			}
		}
		if(sz(cur) == 2){
			adj[cur[0]].push_back(cur[1]), adj[cur[1]].push_back(cur[0]);
		}
	}
	int res = -1;
	for(int u = 0; u < n; u ++){
		if(!adj[u].empty()){
			vector<int> depth(n, -1);
			deque<tuple<int, int, int>> q({{u, 0, u}});
			while(!q.empty()){
				auto [v, d, p] = q.front();
				q.pop_front();
				if(depth[v] == -1){
					depth[v] = d;
					for(auto &w: adj[v]){
						if(w != p){
							if(depth[w] != -1){
								if(res == -1 || res > depth[w] + d + 1){
									res = depth[w] + d + 1;
								}
							}
							else{
								q.push_back({w, d + 1, v});
							}
						}
					}
				}
			}
		}
	}
	cout << res;
	return 0;
}

