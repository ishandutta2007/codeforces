int main(){
	cin.tie(0)->sync_with_stdio(0);
	int tt;
	cin >> tt;
	while(tt --){
		int k;
		cin >> k;
		vector<vpii> adj(2 * k);
		ll G = 0, B = 0;
		for(int i = 1; i < 2 * k; i ++){
			int u, v, w;
			cin >> u >> v >> w, u --, v --;
			G += w;
			adj[u].push_back({v, w}), adj[v].push_back({u, w});
		}
		vi size(2 * k, 1);
		auto dfs = [&](int u, int p, auto &dfs)->void{
			for(auto [v, w]: adj[u]){
				if(v != p){
					dfs(v, u, dfs);
					if(size[v] % 2 == 0){
						G -= w;
					}
					B += 1LL * w * min(size[v], 2 * k - size[v]);
					size[u] += size[v];
				}
			}
		};
		dfs(0, 0, dfs);
		cout << G << " " << B << "\n";
	}
	return 0;
}
