void test_case(int test) {
    int n, m;
    cin >> n >> m;
    string s;
    cin >> s;
    vector<int> ans(n * m, 0);
    {
        vector<int> colgood(m, 0);
        int sm = 0;
        for (int i = 0; i < s.size(); ++i) {
            if (s[i] == '1') {
                int ind = i % m;
                if (!colgood[ind]) {
                    colgood[ind] = 1;
                    ++sm;
                }
            }
            ans[i] += sm;
        }
    }
    {
        vector<int> rows(n * m, 0);
        int last = -1e9;
        for (int i = 0; i < s.size(); ++i) {
            if (s[i] == '1') last = i;
            if (i >= m) rows[i] += rows[i - m];
            rows[i] += (last > i - m);
            ans[i] += rows[i];
        }
    }
    cout << ans << '\n';
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);

    int T;
    cin >> T;
    for (int test = 1; test <= T; ++test) {
        test_case(test);
    }

    return 0;
}