#include<bits/stdc++.h>
#define int long long
#define rd(x) x=read()
using namespace std;
const int N=1e5+5;
const int M=5005;
const int mod=998244353;
inline int read(){int x=0,f=1;char ch=getchar();while(ch>'9'||ch<'0'){if(ch=='-')f=-1;ch=getchar();}while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}return x*f;}
int ksm(int x,int y){int ret=1;while (y){if (y&1) ret=(ret*x)%mod;x=(x*x)%mod;y>>=1;}return ret;}

int n,m,k,id;
int a[N];
int r;double p;
double ans;
signed main()
{
	int T;
	cin>>T;
	while (T--)
	{
		rd(n);rd(m);id=0;
		ans=1;
		for (int i=1;i<=n;i++) rd(a[i]);
		for (int i=n;i>=1;i--)
		{
			if (a[i]!=i) 
			{
				id=i;
				break;
			}
		}
		//1~id 
		for (int i=1;i<=m;i++)
		{
			rd(r);cin>>p;
			if (r>=id) ans*=(1-p);
		}
		if (id==0) cout<<1<<endl;
		else printf("%.8lf\n",1-ans);
	}
}