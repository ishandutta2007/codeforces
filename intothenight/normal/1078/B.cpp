int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi cnt(101);
	for(int i = 0; i < n; i ++){
		int x;
		cin >> x;
		cnt[x] ++;
	}
	vector<vi> d(101, vi(10001));
	d[0][0] = 1;
	int mx = 0, tcnt = 0;
	for(int w = 1; w <= 100; w ++){
		vector<vi> p(d);
		mx += w * cnt[w];
		for(int t = mx; t >= w; t --){ // target weight
			for(int i = 1; i <= cnt[w] && t >= i * w; i ++){// number of w being used
				for(int j = 0; j <= tcnt; j ++){
					if(p[j][t - i * w] == 1){
						d[i + j][t] ++;
					}
					else if(p[j][t - i * w] >= 2){
						d[i + j][t] = 2;
					}
				}
			}
		}
		tcnt += cnt[w];
	}
	int res = 0;
	for(int w = 1; w <= 100; w ++){
		for(int i = 1; i <= cnt[w]; i ++){
			if(d[i][i * w] == 1){
				cnt[w] -= i;
				int t = 0;
				for(int i = 1; i <= 100; i ++){
					if(cnt[i]){
						t ++;
					}
				}
				if(t == 1){
					cout << n;
					return 0;
				}
				else{
					res = max(res, i);
				}
				cnt[w] += i;
			}
		}
	}
	cout << res;
	return 0;
}
