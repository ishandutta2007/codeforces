template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

const int N = 100010;

int a[N], nxt[N], pl[N];

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int n; cin >> n;
  for (int i = 1; i <= n; ++i) cin >> a[i];
  fill(pl, pl + n + 1, N);
  for (int i = n; i; --i) {
    nxt[i] = pl[a[i]];
    pl[a[i]] = i;
  }
  int ans = 0, x = 0, y = 0;
  for (int i = 1; i <= n; ++i) {
    if (x != a[i] && y != a[i]) {
      if (pl[x] > pl[y]) swap(x, y);
      y = a[i];
      ++ans;
    }
    pl[a[i]] = nxt[i];
  }
  cout << ans << '\n';

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}