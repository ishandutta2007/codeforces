mt19937 rnd(time(0));



int main(){
	cin.tie(0)->sync_with_stdio(0);
	const ll mod = 1e9 + 7;
	int n, q;
	cin >> n >> q;
	vl a(n);
	cin >> a;
	while(q --){
		ll x;
		cin >> x;
		vector<pil> d(n);
		ll s = a[n - 1];
		for(int i = n - 1, j = n; i >= 0; i ? s += a[-- i] : -- i){
			while(s > x){
				s -= a[-- j];
			}
			d[i] = (j == n ? pil{1, s} : pil{d[j].first + 1, d[j].second});
		}
		s = 0;
		int res = 2e9;
		for(int j = 0; j <= n && s <= x; s += a[j ++]){
			if(s + d[j].second <= x){
				res = min(res, d[j].first);
			}
		}
		cout << res << "\n";
	}
	return 0;
}
