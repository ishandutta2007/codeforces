int n,a,c,b,d,e;
int main()
{
	cin>>n;
	for(int i=1;i<=n;i++)
	{
		cin>>a>>b>>c>>d>>e;
		for(char ch='a';ch<='z';ch++)
		{
			auto z=x[ch-'a'];
			int aa=z[0]+z[1];
			int bb=z[2]+z[3];
			int cc=z[4]+z[5];
			int dd=z[0]+z[2]+z[4];
			int ee=z[1]+z[3]+z[5];
			if(aa==a&&bb==b&&cc==c&&dd==d&&ee==e)
			{
				cout<<ch;
				break;
			}
		}
	}
	
	return 0;
}