signed main() {
    int n;
    cin >> n;
    auto answer = [&] (int i) {
    	cout << "! " << i << endl;
    	exit(0);
    };
    if (n == 1) {
    	answer(1);
    }
    auto get = [&] (int i) {
    	cout << "? " << i << endl;
    	int ans;
    	cin >> ans;
    	return ans;
    };
    auto comp = [&] (int i) {
    	return get(i) < get(i + 1);
    };
    if (comp(1)) {
    	answer(1);
    }
    if (!comp(n - 1)) {
    	answer(n);
    }
    int l = 1;
    int r = n - 1;
    while (l < r - 1) {
    	int m = (l + r) / 2;
    	if (!comp(m))
    		l = m;
    	else
    		r = m;
    }
    answer(r);
}