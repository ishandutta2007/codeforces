template<typename T>
T binpow(T n, T s)
{
    if (s <= 0)
        return 1LL;
    if (s % 2 == 0){
        T b = binpow(n, s / 2);
        return ( 1LL * b * b ) % mod;
    } else {
        return (1LL* binpow(n, s - 1) * n) % mod;
    }
}

int solve(int n) {
	vector<int> p(n);
	iota(all(p), 1);

	int cnt = 0;
	do {
		for (int i = 0; i < n; i++) {
			int mx = -inf, mn = inf;
			for (int j = i; j < n; j++) {
				upmax(mx, p[j]);
				upmin(mn, p[j]);

				if (mx - mn == j - i) {
					cnt++;
				}
			}
		}
	} while (next_permutation(all(p)));
	return cnt;
}

int fact[maxn];
int brute(int n, int mod) {
	fact[0] = 1;
	for (int i = 1; i < maxn; i++) {
		fact[i] = 1ll * fact[i - 1] * i % mod;
	}

	int ans = 0;
	for (int k = 1; k <= n; k++) {
		ans += 1ll * (n - k + 1) * (n - k + 1) % mod * fact[k] % mod * fact[n - k] % mod;
		ans %= mod;
	}
	return ans;
}

