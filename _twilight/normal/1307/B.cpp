template <typename A, typename B>
ostream& operator << (ostream& os, const pair<A, B>& z) {
	os << "(" << z.first << ", " << z.second << ')';
	return os;
}
template <typename T>
ostream& operator << (ostream& os, const vector<T>& a) {
	boolean isfirst = true;
	os << "{";
	for (auto z : a) {
		if (!isfirst) {
			os << ", ";
		}
		os << z;
		isfirst = false;
	}
	os << '}';
	return os;
}

int main() {
	ios::sync_with_stdio(false);
	cin.tie(0), cout.tie(0);
	int T, n, x, y;
	cin >> T;
	while (T--) {
		cin >> n >> x;
		int mx = 0;
		boolean hasx = false;
		while (n--) {
			cin >> y;
			mx = max(mx, y);
			hasx = hasx || x == y;
		}
		if (hasx) {
			cout << "1\n";
		} else {
			cout << max(2, (x + mx - 1) / mx) << '\n';
		}
	}
	return 0;
}