fn solve(input: &mut Input, _test_case: usize) {
    let n = input.read_usize();
    let a = input.read_table::<u32>(n, n);

    let mut w = Arr2d::new(n, n, false);
    fn mark(w: &mut Arr2d<bool>, r: usize, c: usize) {
        for cell in D4::iter(r, c, w.d1(), w.d2()) {
            if w[cell] {
                panic!("");
            }
            w[cell] = true;
        }
    }
    let mut ans = 0;
    let val = |r, c| a[(r, c)] ^ a[(r, n - c - 1)];
    for i in (1..n).step_by(4) {
        ans ^= val(0, i);
        mark(&mut w, 0, i);
    }
    for i in 1..n {
        for j in (i % 2..n).step_by(2) {
            if !w[(i, j)] {
                ans ^= val(i + 1, j);
                mark(&mut w, i + 1, j);
            }
        }
    }
    out_line!(ans);
}

pub(crate) fn run(mut input: Input) -> bool {
    #[allow(dead_code)]
    enum TestType {
        Single,
        MultiNumber,
        MultiEof,
    }
    let test_type = TestType::MultiNumber;
    match test_type {
        TestType::Single => solve(&mut input, 1),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 0usize..t {
                solve(&mut input, i + 1);
            }
        }
        TestType::MultiEof => {
            let mut i = 1usize;
            while input.peek().is_some() {
                solve(&mut input, i);
                i += 1;
            }
        }
    }
    output().flush();
    input.skip_whitespace();
    !input.peek().is_some()
}

