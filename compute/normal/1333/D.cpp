struct D{
    int p,cnt,pre;
    bool operator<(const D&a)const{
        return p<a.p;
    }
};
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,k;
    cin>>n>>k;
    string s;
    cin>>s;
    ll sum=0;
    int cc=0;
    vc<D> lpos;
    for(int i=0;i<n;i++)
    {
        if(s[i]=='R') cc++;
        else{
            if(!cc) continue;
            if(i&&s[i-1]==s[i])
                lpos.back().cnt++;
            else lpos.push_back(D{i,1,cc});
            sum+=cc;
        }
    }
    set<D> st;
    for(auto &u:lpos) st.insert(u);
    if(k>sum)
    {
        cout<<-1<<'\n';
        return 0;
    }
    vc<vi> ans;
    bool ok=(sum==0);
    while(k&&st.size())
    {
        int curm=st.size();
        vi cur;
        if(sum-curm>k-1)
        {
            set<D> nxt;
            vc<D> rem;
            for(auto u:st)
            {
                u.pre--;
                cur.push_back(u.p);
                sum--;
                if(u.pre) rem.push_back(D{u.p-1,1,u.pre});
                if(u.cnt>1) rem.push_back(D{u.p+1,u.cnt-1,u.pre+1});
            }
            vc<D> rr;
            for(auto &u:rem)
            {
                if(rr.size()&&rr.back().p+rr.back().cnt==u.p)
                    rr.back().cnt+=u.cnt;
                else rr.push_back(u);
            }
            for(auto &u:rr) nxt.insert(u);
            st=nxt;
        }
        else{
            int delta=sum-k+1;
            if(delta==1)
            {
                auto u=*st.begin();
                //dbg(u.p,u.cnt,u.pre);
                st.erase(st.begin());
                u.pre--;
                cur.push_back(u.p);
                sum--;
                if(u.pre) st.insert(D{u.p-1,1,u.pre});
                if(u.cnt>1) st.insert(D{u.p+1,u.cnt-1,u.pre+1});
            }
            else{
                set<D> nxt;
                vc<D> rem;
                for(auto u:st)
                {
                    if(delta)
                    {
                        u.pre--;
                        cur.push_back(u.p);
                        sum--;
                        if(u.pre) rem.push_back(D{u.p-1,1,u.pre});
                        if(u.cnt>1) rem.push_back(D{u.p+1,u.cnt-1,u.pre+1});
                        delta--;
                    }
                    else rem.push_back(u);
                }
                vc<D> rr;
                for(auto &u:rem)
                {
                    if(rr.size()&&rr.back().p+rr.back().cnt==u.p)
                        rr.back().cnt+=u.cnt;
                    else rr.push_back(u);
                }
                for(auto &u:rr) nxt.insert(u);
                st=nxt;
            }
        }
        ans.push_back(cur);
        k--;
        //for(auto u:st) dbg(u.p,u.cnt,u.pre);
        //dbg(k,st.size());
    }
    if(sum) cout<<-1<<'\n';
    else{
        for(auto &u:ans)
        {
            cout<<u.size();
            for(auto v:u) cout<<" "<<v;
            cout<<'\n';
        }
    }

    
}