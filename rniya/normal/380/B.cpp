const int MAX_X = 1000010;

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n, m;
    cin >> n >> m;
    vector<int> cnt(n, 0);
    cnt[0] = 1;
    for (int i = 0; i + 1 < n; i++) cnt[i + 1] = cnt[i] + topbit(cnt[i]) + 1;
    auto left = [](int x) { return x + topbit(x) + 1; };

    vector<bool> check(MAX_X, false);
    vector<vector<tuple<int, int, int>>> add(n);
    auto query1 = [&](int t, int l, int r, int x) { add[t].emplace_back(l, r, x); };
    auto query2 = [&](int t, int v) -> int {
        vector<int> cand;
        int L = v, R = v + 1;
        for (int i = t; i < n; i++) {
            for (auto& e : add[i]) {
                int l, r, x;
                tie(l, r, x) = e;
                if (max(l, L) < min(r, R)) cand.emplace_back(x);
            }
            if (i + 1 == n) break;
            L = left(L);
            if (R >= cnt[i])
                R = cnt[i + 1];
            else
                R = left(R);
        }

        int res = 0;
        for (int& x : cand) {
            res += !check[x];
            check[x] = true;
        }
        for (int& x : cand) check[x] = false;
        return res;
    };

    for (; m--;) {
        int type;
        cin >> type;
        if (type == 1) {
            int t, l, r, x;
            cin >> t >> l >> r >> x;
            query1(--t, --l, r, x);
        } else {
            int t, v;
            cin >> t >> v;
            cout << query2(--t, --v) << '\n';
        }
    }
    return 0;
}