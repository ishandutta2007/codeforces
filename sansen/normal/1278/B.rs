use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        ask: [(i64, i64); t],
    }
    for (a, b) in ask {
        let mut d = -(a - b).abs();
        let mut k = 1;
        let mut cnt = 0;
        while d < 0 || (d >= 0 && d % 2 != 0) {
            d += k;
            k += 1;
            cnt += 1;
        }
        writeln!(out, "{}", cnt).ok();
    }
}

fn main() {
    run();
}