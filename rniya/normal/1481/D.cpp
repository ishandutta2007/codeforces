void solve() {
    int n, m;
    cin >> n >> m;
    vector<string> S(n);
    cin >> S;

    vector<int> ans;
    for (int i = 0; i < n; i++) {
        for (int j = i + 1; j < n; j++) {
            if (S[i][j] == S[j][i]) {
                for (int k = 0; k <= m; k++) ans.emplace_back(k & 1 ? i + 1 : j + 1);
                cout << "YES" << '\n';
                cout << ans << '\n';
                return;
            }
        }
    }

    if (m & 1) {
        for (int i = 0; i <= m; i++) ans.emplace_back(i & 1 ? 1 : 2);
        cout << "YES" << '\n';
        cout << ans << '\n';
        return;
    }

    for (int i = 0; i < n; i++) {
        vector<int> u(2, -1);
        for (int j = 0; j < n; j++) {
            if (i == j) continue;
            u[S[i][j] - 'a'] = j;
        }
        if (u[0] < 0 || u[1] < 0) continue;
        cout << "YES" << '\n';
        if (m % 4 == 0) {
            for (int j = 0; j < m / 2; j++) ans.emplace_back(j & 1 ? u[0] + 1 : i + 1);
            for (int j = 0; j <= m / 2; j++) ans.emplace_back(j & 1 ? u[1] + 1 : i + 1);
        } else {
            for (int j = 0; j < m / 2; j++) ans.emplace_back(j & 1 ? i + 1 : u[0] + 1);
            for (int j = 0; j <= m / 2; j++) ans.emplace_back(j & 1 ? u[1] + 1 : i + 1);
        }
        cout << ans << '\n';
        return;
    }

    cout << "NO" << '\n';
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t;
    cin >> t;
    for (; t--;) solve();
    return 0;
}