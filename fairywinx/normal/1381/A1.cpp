void solve() {
	int n;
	cin >> n;
	vector<int> a(n), b(n);
	string s1, s2;
	cin >> s1 >> s2;
	for(int i = 0; i < n; ++i)
		a[i] = s1[i] - '0';
	for(int i = 0; i < n; ++i)
		b[i] = s2[i] - '0';
	vector<int> ans;
	for(int i = n - 1; i >= 1; --i) {
		if (a[i] == b[i])
			continue;
		if (a[0] != b[i]) {
			// cout << i << ':';
			// for(auto j: a)
			// 	cout << j << ' ';
			// cout << " - ";
			reverse(a.begin(), a.begin() + i + 1);
			for(int j = 0; j <= i; ++j)
				a[j] = 1 - a[j];
			// for(auto j: a)
			// 	cout << j << ' ';
			// cout << '\n';
			ans.push_back(i + 1);
		}
		else {
			ans.push_back(1);
			ans.push_back(i + 1);
			a[0] = 1 - a[0];
			reverse(a.begin(), a.begin() + i + 1);
			for(int j = 0; j <= i; ++j)
				a[j] = 1 - a[j];
		}
	}
	if (a[0] != b[0]) {
		ans.push_back(1);
		a[0] = 1 - a[0];
	}
	// for(int i = 0; i < n; ++i)
	// 	cout << a[i] << ':' << b[i] << ' ';
	cout << ans.size() << ' ';
	for(auto i: ans)
		cout << i << ' ';
	cout << '\n';
}

