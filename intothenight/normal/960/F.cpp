struct edge{
	int to, w;
};

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vector<map<int, int>> d(n, {{-1, 0}});
	int res = 0;
	for(int i = 0; i < m; i ++){
		int u, v, w;
		cin >> u >> v >> w, u --, v --;
		int l = (-- d[u].lower_bound(w))->second + 1;
		auto it = -- d[v].upper_bound(w);
		if(it->second >= l){
			continue;
		}
		if(it->first != w) it ++;
		for(; it != d[v].end() && it->second <= l; it = d[v].erase(it)){}
		d[v][w] = l;
		res = max(res, l);
	}
	cout << res;
	return 0;
}

