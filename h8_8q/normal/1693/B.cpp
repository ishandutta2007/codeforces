		n=read();
		for(int i=2;i<=n;++i)
		{
			int fa=read();
			add(i,fa);add(fa,i);
		}
		for(int i=1;i<=n;++i)
			l[i]=read(),r[i]=read(),s1[i]=0,s2[i]=0;
		dfs(1,0);
