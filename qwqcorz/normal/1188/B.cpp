{
	(void)Recall,(void)_902_;
	int n=read(),p=read(),k=read(),ans=0;
	for (int i=1;i<=n;i++) a[read()]++;
	for (auto i:a)
	{
		if (i.fi*i.fi%p*i.fi%p*4%p==k) ans+=i.se*(i.se-1)/2;
		int val=(i.fi*i.fi%p*i.fi%p*i.fi%p-k*i.fi%p+p)%p;
		ans+=b[val]*i.se;
		b[val]+=i.se;
	}
	print(ans);
	
	return 0;
}
