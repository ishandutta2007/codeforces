using namespace std;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
      cin >> a[i];
    }

    int zero = count(a.begin(), a.end(), 0);
    int one = count(a.begin(), a.end(), 1);
    cout << (one * (1LL << zero)) << '\n';
  }
}