const int A = 26;

void solve(){
    string s;
    cin >> s;
    int n, k;
    n = s.size();
    k = 2;
    vi v(n);
    vvi color(A);
    for(int i = 0; i < n; i++){
        v[i] = s[i]-'a';
        color[v[i]].push_back(i);
    }
    vi ans(n, -1);
    vi order;
    for(int i = 0; i < A; i++){
        for(int j = 0; j < min((int)color[i].size(), k); j++){
            order.push_back(color[i][j]);
        }
    }
    debug(order);
    for(int i = 0; i < ((int)order.size()/k)*k; i++){
        ans[order[i]] = i%k;
    }
    int num = 0;
    for(int i = 0; i < n; i++){
        if(ans[i] == 0) num++;
    }
    cout << num << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}