void run_case() {
    int N;
    cin >> N;
    int S = int(sqrt(N));
    int best = N;

    // S * S
    // S * (S + 1)
    // (S + 1) * (S + 1)

    for (int x = max(S - 2, 1); x <= S + 2; x++)
        for (int y = max(S - 2, 1); y <= S + 2; y++)
            if (x * y >= N)
                best = min(best, x + y - 2);

    cout << best << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}