const int mod=998244353;
const int maxn=5e5+7;
ll dp[maxn];
ll pre[maxn];
struct Event{
    int l,r,x;
};
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,k,m;
    cin>>n>>k>>m;
    vc<vc<Event>> all(n+1);
    for(int i=0,l,r,x;i<m;i++)
    {
        cin>>l>>r>>x;
        all[l].push_back(Event{l,r,x});
        all[r].push_back(Event{l,r,x});
    }
    ll ans=1;
    for(int j=0;j<k;j++)
    {
        memset(dp,0,sizeof(dp));
        memset(pre,0,sizeof(pre));
        int cur=-1;
        pre[0]=dp[0]=1;
        int c1=0;
        for(int i=1;i<=n;i++)
        {
            for(auto u:all[i])
            {
                if(u.x>>j&1)
                    if(u.l==i) c1++;
            }
            if(c1)
                pre[i]=pre[i-1];
            else{
                dp[i]=(pre[i-1]-(cur==-1?0:pre[cur])+mod)%mod;
                pre[i]=(pre[i-1]+dp[i])%mod;
            }
            for(auto u:all[i])
            {
                if(u.x>>j&1^1)
                {
                    if(u.r==i) cur=max(cur,u.l-1);
                }
                else {
                    if(u.r==i) c1--;
                }
                
            }
            //dbg(i,dp[i]);
        }
        ans=ans*((pre[n]-pre[cur]+mod)%mod)%mod;
        //dbg(j,ans);
    }
    print(ans);

}