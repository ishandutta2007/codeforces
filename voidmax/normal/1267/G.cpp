typedef long long base; 
typedef pair<int, int> point;      
typedef complex<double> comp;

const int N = 1e3;
const int T = 10001;

int n;
long double x, ans;
long double dp[101][T];
int c[N];

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n >> x;
    for (int i = 0; i < n; ++i) {
    	cin >> c[i];
    }
    dp[0][0] = 1;
    for (int i = 0; i < n; ++i) {
    	for (int j = i; j >= 0; --j) {
    		for (int tt = c[i]; tt < T; ++tt) {
    			dp[j + 1][tt] += dp[j][tt - c[i]];
    		}
    	}
    }
    for (int i = 1; i <= n; ++i) {
    	long double sum = 0;
    	for (int j = 0; j < T; ++j) {
    		sum += dp[i][j];
    	}
    	if (sum == 0) continue;
    	long double cost1 = ((long double)n / i + 1) * (x / 2);
    	for (int j = 0; j < T; ++j) {
    		dp[i][j] /= sum;
    		long double cost2 = ((long double)j / i);
    		ans += dp[i][j] * min(cost1, cost2);
    	}
    }
    cout.precision(20);
    cout << fixed << ans << '\n';
}