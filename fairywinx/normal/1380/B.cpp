void solve() {
	string s;
	cin >> s;
	int kol[3] = {0, 0, 0};
	for(auto i: s) {
		if (i == 'R')
			++kol[0];
		else if (i == 'S')
			++kol[1];
		else 
			++kol[2];
	}
	char win = 'R';
	int mx = max({kol[0], kol[1], kol[2]});
	if (mx == kol[0]) 
		win = 'P';
	if (mx == kol[1])
		win = 'R';
	if (mx == kol[2])
		win = 'S';
	for(auto i: s)
		cout << win;
	cout << '\n';
}

