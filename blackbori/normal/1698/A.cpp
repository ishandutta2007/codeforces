#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(0);
    
    int t, n, a;
    
    cin >> t;
    for (int i = 0; i < t; i++) {
        cin >> n;
        for (int j = 0; j < n; j++) {
            cin >> a;
        }
        cout << a << "\n";
    }
    return 0;
}