#include<bits/stdc++.h>
#define int long long
#define rd(x) x=read()
using namespace std;
const int N=3e5+5;
inline int read(){int x=0,f=1;char ch=getchar();while(ch>'9'||ch<'0'){if(ch=='-')f=-1;ch=getchar();}while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}return x*f;}
int n,v,c,l,r,head;
int dp[N],nxt[N];
map<int,int> Map[N];
int ans,a[N];
signed main()
{
    rd(n);
    for (int i=1;i<=n;i++)
    {
        rd(v);rd(c);rd(l);rd(r);
        if (l==0 || Map[l].count(r+c))
        {
            dp[i]=dp[Map[l][r+c]]+v;
            nxt[i]=Map[l][r+c];
            if (r==0 && dp[i]>dp[head]) head=i;
            if (dp[i]>dp[Map[l+c][r]])
                Map[l+c][r]=i;
        }
    }
    int cnt=0;
    for (int i=head;i;i=nxt[i])
    {
        cnt++;
        a[cnt]=i;
    }
    cout<<cnt<<endl;
    for (int i=cnt;i>=1;i--) cout<<a[i]<<" ";
    cout<<endl;
}