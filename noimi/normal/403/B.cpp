using i128 = __int128_t;
ll get_factor(ll n) {
    if(n == 3 or n == 5) return 0;
    ll x, y;
    ll iter = rnd(100);
    while(true) {
        ++iter;
        x = iter % n;
        y = (i128(iter) * i128(iter) + 1) % i128(n);
        while(1) {
            ll g = gcd(n, abs(x - y));
            if(g == n) break;
            if(g > 1) return g;
            x = (i128(x) * i128(x) + 1) % i128(n);
            y = (i128(y) * i128(y) + 1) % i128(n);
            y = (i128(y) * i128(y) + 1) % i128(n);
        }
    }
}
ll modpow(ll a, ll n, ll mod) {
    ll res = 1;
    while(n) {
        if(n & 1) res = i128(a) * i128(res) % i128(mod);
        n >>= 1, a = i128(a) * i128(a) % i128(mod);
    }
    return res;
}
bool miller_rabin_test(ll p, ll s, ll d, ll n) {
    ll x = modpow(p, d, n);
    if(x == 1) return true;
    ll y = n - 1;
    rep(i, s) {
        if(x == y) return true;
        x = i128(x) * i128(x) % i128(n);
    }
    return false;
}
bool miller_rabin(ll n) {
    if(n == 1) return false;
    ll d = n - 1, s = 0;
    while(~d & 1) d >>= 1, ++s;
    if(n < 4759123141LL) {
        for(auto p : {2, 7, 61}) {
            if(p >= n) break;
            if(!miller_rabin_test(p, s, d, n)) return false;
        }
    } else {
        for(auto p : {2, 325, 9375, 28178, 450775, 9780504, 1795265022}) {
            if(p >= n) break;
            if(!miller_rabin_test(p, s, d, n)) return false;
        }
    }
    return true;
}

map<ll, int> factorize(ll n) {
    map<ll, int> res;
    while(~n & 1) res[2]++, n >>= 1;
    queue<ll> que;
    que.emplace(n);
    while(!que.empty()) {
        ll now = que.front();
        que.pop();
        if(now == 1) continue;
        if(miller_rabin(now)) {
            res[now]++;
            continue;
        }
        ll g = get_factor(now);
        que.emplace(g);
        que.emplace(now / g);
    }
    return res;
}

