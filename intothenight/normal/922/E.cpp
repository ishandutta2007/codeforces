template<class T>
istream &operator>>(istream &in, deque<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	ll w, b, x;
	cin >> n >> w >> b >> x;
	vi c(n);
	vl cost(n);
	cin >> c >> cost;
	int s = accumulate(all(c), 0);
	vector<vl> d(n, vl(s + 1, -1));
	d[0][0] = w;
	for(int i = 1; i <= c[0] && cost[0] * i <= w; i ++){
		d[0][i] = w - cost[0] * i;
	}
	for(int i = 1; i < n; i ++){
		for(int j = 0; j <= s && d[i - 1][j] != -1; j ++){ // Cap: w + j * b, Mana: min(d[i - 1][j] + x, cap)
			ll cap = w + j * b, mana = min(d[i - 1][j] + x, cap);
			for(int k = 0; k <= c[i] && mana >= k * cost[i]; k ++){
				d[i][j + k] = max(d[i][j + k], mana - k * cost[i]);
			}
		}
	}
	for(int x = s; x >= 0; x --){
		if(d[n - 1][x] != -1){
			cout << x;
			return 0;
		}
	}
	return 0;
}

