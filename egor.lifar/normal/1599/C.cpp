int n;
double p;


void solve() {
    cin >> n >> p;
    for (int k = 0; k <= n; k++) {
        double p1 = double(k * (k - 1) * (k - 2) / 6) / double(n * (n - 1) * (n - 2) / 6);
        double p2 = double(k * (k - 1) / 2 * (n - k)) / double(n * (n - 1) * (n - 2) / 6);
        double p3 = double(k * (n - k) * (n - k - 1) / 2) / double(n * (n - 1) * (n - 2) / 6);
        double p4 = double((n - k) * (n - k - 1) * (n - k - 2) / 2) / double(n * (n - 1) * (n - 2) / 6);
        double q = p1 + p2 + p3 * 0.5;
        if (q >= p - 1e-9) {
            cout << k << '\n';
            return;
        }
    }   
}


