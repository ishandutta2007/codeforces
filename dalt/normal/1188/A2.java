    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        List<Node> leaf = new ArrayList<>();

        public void solve() {
            int n = io.readInt();
            Node[] nodes = new Node[n + 1];
            for (int i = 1; i <= n; i++) {
                nodes[i] = new Node();
                nodes[i].id = i;
            }

            for (int i = 1; i < n; i++) {
                Node a = nodes[io.readInt()];
                Node b = nodes[io.readInt()];
                Edge edge = new Edge();
                edge.a = a;
                edge.b = b;
                edge.target = io.readInt();
                a.next.add(edge);
                b.next.add(edge);
            }

            for (int i = 1; i <= n; i++) {
                Node node = nodes[i];
                if (node.next.size() != 2) {
                    continue;
                }
                io.cache.append("NO");
                return;
            }

            io.cache.append("YES").append('\n');
            for (int i = 1; i <= n; i++) {
                Node node = nodes[i];
                if (node.next.size() == 1) {
                    Edge e = node.next.get(0);
                    dfs(e.other(node), e, node, new ArrayDeque<>());
                    break;
                }
            }

            io.cache.append(opList.size()).append('\n');
            for (OP op : opList) {
                io.cache.append(op.a.id).append(' ').append(op.b.id).append(' ').append(op.flow).append('\n');
            }
        }

        List<OP> opList = new ArrayList<>();

        public Node findTwoLeaf(Node x, Edge e) {
            for (Edge next : x.next) {
                if (next == e) {
                    continue;
                }
                return findTwoLeaf(next.other(x), next);
            }
            return x;
        }

        public void add(Deque<Edge> deque, int x) {
            for (Edge edge : deque) {
                edge.target -= x;
            }
        }

        public void addOP(Node a, Node b, int f) {
            OP op = new OP();
            op.a = a;
            op.b = b;
            op.flow = f;
            opList.add(op);
        }

        public void dfs(Node root, Edge father, Node center, Deque<Edge> stack) {
            stack.addLast(father);

            Node leaf1 = null;
            Node leaf2 = null;
            for (Edge next : root.next) {
                if (next == father) {
                    continue;
                }
                dfs(next.other(root), next, center, stack);
                if (leaf1 == null) {
                    leaf1 = findTwoLeaf(next.other(root), next);
                } else if (leaf2 == null) {
                    leaf2 = findTwoLeaf(next.other(root), next);
                }
            }
            stack.removeLast();
            add(stack, father.target);
            if (root.next.size() == 1) {
                addOP(root, center, father.target);
                return;
            }
            addOP(leaf1, center, father.target / 2);
            addOP(leaf2, center, father.target / 2);
            addOP(leaf1, leaf2, -father.target / 2);
        }
    }


