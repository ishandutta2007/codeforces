int main() {
  int64 n, r1, r2, r3, d;
  cin >> n >> r1 >> r2 >> r3 >> d;
  vector<int64> a(n);
  for (auto& x : a) cin >> x;
  vector<int64> dp(n + 1);
  for (int i = n - 1; i >= 0; --i) {
    dp[i] = a[i] * r1 + r3 + dp[i + 1];
    int64 R = min(r2, r1 * (a[i] + 1));
    if (i + 1 < n) {
      int64 nxtR = min(r2, r1 * (a[i + 1] + 1));
      trace(i, r1, R, d, nxtR);
      dp[i] = min(dp[i], r1 + R + d + r1 + nxtR + d + dp[i + 2]);
    }
    if (i + 1 == n - 1) {
      dp[i] = min(dp[i], d + R + r1 + dp[i + 1]);
    } else {
      dp[i] = min(dp[i], 2 * d + R + r1 + dp[i + 1]);
    }
    trace(i, d, R, r1, dp[i + 1], dp[i]);
  }
  cout << dp[0] + (n - 1) * d << '\n';
  return 0;
}