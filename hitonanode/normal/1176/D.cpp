set<int> makePrimeList(int N)
{
    set<int> ans;
    vector<int> alive(N+1, 1);
    lint now = 2;
    for (; now * now <= N; now++)
    {
        if (alive[now]) ans.insert(now);
        for (int t = now; t <= N; t += now) alive[t] = 0;
    }
    for (; now <= N; now++) if (alive[now]) ans.insert(now);
    return ans;
}
set<int> primes;
map<int, int> primes_inv;

int main()
{
    int N;
    cin >> N;
    vector<int> B(N * 2);
    cin >> B;
    primes = makePrimeList(2750131);
    int id_ = 1;
    for (auto v : primes) primes_inv[v] = id_++;
    sort(B.rbegin(), B.rend());
    vector<int> A;
    multiset<int> se;
    for (auto v : B)
    {
        if (se.count(v))
        {
            se.erase(se.lower_bound(v));
        }
        else if (primes.count(v))
        {
            A.push_back(primes_inv[v]);
            se.insert(primes_inv[v]);
        }
        else
        {
            A.push_back(v);
            for (auto p : primes) if (v % p == 0)
            {
                se.insert(v / p);
                break;
            }
        }
    }
    
    for (auto v : A) printf("%d ", v);
    puts("");
}