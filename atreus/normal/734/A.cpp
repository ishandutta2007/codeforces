#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    int cnt = 0, ans = 0, n;
    cin >> n;
    string s;
    cin >> s;
    for (int i = 0; i < s.size(); i++){
        if (s[i] == 'A')
            cnt++;
        else
            ans++;
    }
    if (cnt > ans)
        cout << "Anton" << endl;
    else if (cnt < ans)
        cout << "Danik" << endl;
    else
        cout << "Friendship" << endl;
}