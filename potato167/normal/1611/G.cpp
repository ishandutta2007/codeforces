using F= int;
F op(F a,F b){return max(a,b);}
F e(){return -1;}
int target=0;
bool f(F x){
	return (target>x);
}


int f(vector<vector<int>> &p){
	int c=0;
	int n=p.size();
	vector<int> q(n);
	segment_tree<F,op,e> seg(n);
	rep(i,n){
		q[i]=p[i].size(),q[i]--;
		if(q[i]!=-1) seg.set(i,p[i][q[i]]);
	}
	while(true){
		int tmp=0,J=0;
		target=0;
		int i=seg.max_right<f>(0);
		while(i!=n){
			J=1;
			int a=p[i][q[i]];
			while(q[i]!=-1&&p[i][q[i]]>=tmp) q[i]--;
			if(q[i]==-1){
				seg.set(i,-1);
			}else{
				seg.set(i,p[i][q[i]]);
			}
			tmp=a;
			target=tmp;
			i=seg.max_right<f>(i);
		}
		if(J==0) break;
		c++;
	}
	return c;
}

