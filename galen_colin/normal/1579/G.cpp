void solve(int tc = 0) {
	memset(dp, 1, sizeof(dp));
	dp[0][0] = 0;
	
	cin >> n;
	
	for (ll i = 0; i < n; i++) {
		cin >> x;
		ll b = i % 2;
		
		memset(dp[!b], 1, sizeof(dp[!b]));
		
		for (ll j = 0; j < 3002; j++) {
			{ // go left
				ll nx = dp[b][j] + max(0LL, x - j);
				ll ns = max(0LL, j - x);
				
				if (ns < 3002 && nx < 3002) {
					dp[!b][ns] = min(dp[!b][ns], nx);
				}
			}
			
			{ // go right
				ll nx = max(dp[b][j], j + x);
				ll ns = j + x;
				
				if (ns < 3002 && nx < 3002) {
					dp[!b][ns] = min(dp[!b][ns], nx);
				}
			}
		}
	}
	
	ll ans = 1e9;
	for (ll i = 0; i < 3002; i++) ans = min(ans, dp[(n % 2)][i]);
	cout << ans << '\n';
}

