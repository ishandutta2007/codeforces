const int MAXN = 300055;

int A[MAXN], B[MAXN];
int N, M, Ans;

bool isp(int X) {
	for(int i = 1; i <= N; i++) {
		int s = A[i], e = A[i]+X;
		if(e < M) {
			if(B[i-1] <= e) {
				B[i] = max(s, B[i-1]);
			} else return false;
		} else {
			e %= M;
			// [s, M), [0, e]
			if(B[i-1] <= e) {
				B[i] = B[i-1];
			} else {
				B[i] = max(s, B[i-1]);
			}
		}
	}
	return true;
}

int main() {
	ios::sync_with_stdio(false);

	cin >> N >> M;
	for(int i = 1; i <= N; i++) cin >> A[i];

	int s = 0, e = M-1; for(int m; s < e;) {
		m = (s+e) >> 1;
		if(isp(m)) e = m;
		else s = m+1;
	}
	cout << s << endl;
	return 0;
}