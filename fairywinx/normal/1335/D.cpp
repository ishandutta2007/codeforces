int a[9][9];

void solve() {
	int n = 9;
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < n; ++j) {
			char c;
			cin >> c;
			a[i][j] = c - '0';
		}
	}

	for(int i = 0; i < 3; ++i) {
		for(int j = 0; j < 3; ++j) {
			// cout << i + 1 << ' ' << j + 1 << ':' << 3 * i + j + 1 << ' ' << 3 * j + i + 1 << ':' << a[3 * i + j][3 * j + i];
			for(int k = 1; k < 10; ++k) {
				bool z = false;
				for(int u = 0; u < n; ++u) {
					if (u == 3 * j + i)
						continue;
					if (a[3 * i + j][u] == k) {
						// cout << u << '=';
						z = true;
					}
				}
				if (!z)
					continue;
				z = false;
				for(int u = 0; u < n; ++u) {
					if (u == 3 * i + j)
						continue;
					if (a[u][3 * j + i] == k) {
						// cout << u << '=';
						z = true;
					}
				}
				if (!z)
					continue;
				a[3 * i + j][3 * j + i] = k;
				break;
			}
			// cout << ' ' << a[3 * i + j][3 * j + i] << '\n';
		}
	}
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < n; ++j)
			cout << a[i][j];
		cout << '\n';
	}
}

signed main() {
	setup();
	SOLVE;
	

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


