const int P = 1e9 + 7;
int spow(int a, int b) {
  int res = 1;
  while (b) {
    if (b % 2 == 1) {
      res = res * a % P;
    }
    a = a * a % P;
    b /= 2;
  }
  return res;
}
int Solve(int n, int m, int k) {
  if (k % 2 == 0) {
    if (n == k) {
      return spow(m, n / 2);
    } else if (n < k || k == 0) {
      return spow(m, n);
    } else {
      return m;
    }
  }
  int fl = n / 2;
  int ceil = n - fl;
  return Solve(fl, m, 2 * (k / 2)) * Solve(ceil, m, 2 * (k / 2)) % P;
}
