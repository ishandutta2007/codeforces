const LL INF = 1e9 + 7;
const int N = 1e6 + 3;
int deg[N];
LL ans = 0;

int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	cin >> n;
	for (int i = 1; i < n; i++)
	{
		int x, y;
		cin >> x >> y;
		deg[x]++;
		deg[y]++;
	}
	for (int i = 1; i <= n; i++)
	{
		LL x = deg[i];
		ans += x*(x - 1) / 2;
	}
	cout << ans << endl;
	return 0;
}