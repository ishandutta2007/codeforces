const int MN = 2e5+5;
int N, C, T, i, j, arr[MN], cnt[MN], nxt[MN], ans;
map<int,int> pos;

bool solve(int n,int lim){
    if(!n) return 1;
    auto it=pos.upper_bound(min(n,lim));
    if(it==pos.begin()) return 0;
    it--; int c=it->first;
    int r=min(cnt[c],n/c);
    return solve(n-r*c,c-1);
}

