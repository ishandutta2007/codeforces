int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);

    ll ans = 1e18;

    auto ask = [&](int w) {
        assert(w != 0);
        cout << "? " << w << endl;
        int h;
        cin >> h;
        if (h == 0) h = 1e9;
        ans = min(ans, (ll)w * h);
        return h;
    };

    int n;
    cin >> n;
    int L = 0, R = 1e9;
    while (R - L > 1) {
        int C = (L + R) / 2;
        int h = ask(C);
        if (h > 1) L = C;
        else R = C;
    }

    int S = R - (n - 1);
    show(S);

    for (int h = 1; h <= n; ++h) {
        int c = (S + n + h - 1) / h - 1;
        if (c > 0) {
            ask(c);
        }
    }

    cout << "! " << ans << endl;

    return 0;
}