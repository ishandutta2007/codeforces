struct dsu {
  vector<int> par, sz;
  dsu(int n) {
    par.resize(n);
    sz.resize(n, 1);
    iota(par.begin(), par.end(), 0);
  }
  inline int get(int v) {
    return par[v] = (v == par[v] ? v : get(par[v]));
  }
  inline bool unite(int x, int y) {
    x = get(x);
    y = get(y);
    if (x == y) return false;
    sz[x] += sz[y];
    par[y] = x;
    return true;
  }
  int size(int v) {
    return sz[get(v)];
  }
  bool same(int x, int y) {
    return get(x) == get(y);
  }
};

 
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n, m, k;
  cin >> n >> m >> k;
  vector<vector<int>> g(k);
  for (int i = 0; i < n; ++i) {
    int s;
    cin >> s;
    vector<int> a(s);
    for (int j = 0; j < s; ++j) {
      cin >> a[j];
      --a[j];
    }
    for (int j = 0; j < s - 1; ++j) {
      g[a[j]].push_back(a[j + 1]);
    }
  }


  dsu d(k);
  vector<int> indeg(k);
  vector<int> outdeg(k);
  for (int i = 0; i < k; ++i) {
    sort(g[i].begin(), g[i].end());
    g[i].erase(unique(g[i].begin(), g[i].end()), g[i].end());
    for (auto u : g[i]) {
      ++indeg[u];
      d.unite(i, u);
    }
    outdeg[i] = int(g[i].size());
  }

  vector<vector<int>> gs(k);
  for (int i = 0; i < k; ++i) {
    gs[d.get(i)].push_back(i); 
  }      


  vector<int> ct(k + 1);
  for (int i = 0; i < k; ++i) {
    array<int, 4> c = {};
    for (auto v : gs[i]) {
      if (indeg[v] > 1 || outdeg[v] > 1) {
        continue;
      }
      ++c[indeg[v] + 2 * outdeg[v]];
    }  
    
    int size = int(gs[i].size());
    if ((c[1] == 1 && c[2] == 1 && c[3] == size - 2) || size == 1) {
      ++ct[size];
    }
  }


  vector<int> add;
  for (int i = 0; i <= k; ++i) {
    if (ct[i] > 0) {
      add.push_back(i);
    }
  }

  vector<Mint> dp(m + 1);
  dp[0] = 1;
  for (int i = 1; i <= m; ++i) {
    for (auto j : add) {
      dp[i] += (i >= j ? dp[i - j] * ct[j] : 0);
    }
  }
  cout << dp[m] << '\n';
}