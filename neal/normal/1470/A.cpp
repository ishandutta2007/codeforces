void run_case() {
    int N, M;
    cin >> N >> M;
    vector<int> K(N);

    for (auto &k : K) {
        cin >> k;
        k--;
    }

    sort(K.rbegin(), K.rend());
    vector<int> C(M);

    for (auto &c : C)
        cin >> c;

    int current = 0;
    int64_t answer = 0;

    for (auto &k : K)
        if (current < k)
            answer += C[current++];
        else
            answer += C[k];

    cout << answer << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}