int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    vector<int> n(4);
    cin >> n;
    vector<vector<int>> a(4);
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < n[i]; j++) {
            int x;
            cin >> x;
            a[i].emplace_back(x);
        }
    }
    vector<int> m(3);
    vector<vector<vector<int>>> ng(4);
    for (int i = 0; i < 3; i++) {
        cin >> m[i];
        ng[i + 1].resize(n[i + 1]);
        for (int j = 0; j < m[i]; j++) {
            int x, y;
            cin >> x >> y;
            ng[i + 1][--y].emplace_back(--x);
        }
    }

    int ans = INF;
    vector<vector<int>> dp(4);
    for (int i = 0; i < 4; i++) dp[i].resize(n[i]);
    for (int i = 0; i < n[0]; i++) dp[0][i] = a[0][i];
    for (int i = 1; i < 4; i++) {
        multiset<int> s;
        for (int j = 0; j < n[i - 1]; j++) s.emplace(dp[i - 1][j]);
        for (int j = 0; j < n[i]; j++) {
            for (int k : ng[i][j]) s.erase(s.lower_bound(dp[i - 1][k]));
            dp[i][j] = (!s.empty() ? *s.begin() + a[i][j] : INF);
            if (i == 3) chmin(ans, dp[i][j]);
            for (int k : ng[i][j]) s.emplace(dp[i - 1][k]);
        }
    }

    cout << (ans == INF ? -1 : ans) << '\n';
    return 0;
}