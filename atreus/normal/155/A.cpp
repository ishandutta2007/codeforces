#include <bits/stdc++.h>

using namespace std;

const int maxn = 1000;
int a[maxn + 10];

int main (){
    ios_base::sync_with_stdio(false);
    int n, cnt = 0, b = 0, k = 10000;
    cin >> n;
    cin >> a[0];
    b = a[0];
    k = a[0];
    for (int i = 1; i < n; i++){
        cin >> a[i];
        if (a[i] > b){
            b = a[i];
            cnt++;
        }
        if (a[i] < k){
            k = a[i];
            cnt++;
        }
    }
    cout << cnt;
}