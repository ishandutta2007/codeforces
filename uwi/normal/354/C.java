	void solve()
	{
		int n = ni(), K = ni();
		int[] a = na(n);
		int[] hit = new int[1000002];
		for(int v : a)hit[v]++;
		for(int i = 1;i <= 1000001;i++){
			hit[i] += hit[i-1];
		}
		
		int min = 1000000;
		for(int i = 0;i < n;i++){
			min = Math.min(min, a[i]);
		}
		for(int d = min;d > K;d--){
			int sum = 0;
			for(int e = d;e <= 1000000;e += d){
				// [d,d+K]
				sum += hit[Math.min(e+K, 1000001)] - hit[e-1];
			}
			if(sum == n){
				out.println(d);
				return;
			}
		}
		out.println(Math.min(min, K));
	}
	
