bool topological_sort(vector<vector<int>> g, vector<int> &order){
    int n = g.size();
    order.clear();
    vector<bool> used(n, false);
    function<void(int)> dfs = [&](int v){
        used[v] = true;
        for(int to : g[v]){
            if(!used[to]) dfs(to);
        }
        order.push_back(v);
    };
    for(int v = 0; v < n; v++){
        if(!used[v]) dfs(v);
    }
    reverse(order.begin(), order.end());
    vector<int> inv_order(n);
    for(int i = 0; i < n; i++) inv_order[order[i]] = i;
    for(int v = 0; v < n; v++){
        for(int u : g[v]){
            if(inv_order[v] > inv_order[u]) return false;
        }
    }
    return true;
}

using P = pair<int, int>;

void solve(){
    int n; cin >> n;
    vector<int> a(n), b(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    for(int i = 0; i < n; i++) cin >> b[i];
    vector<vector<int>> g(n);
    for(int i = 0; i < n; i++){
        for(int j = i+1; j < n; j++){
            if(a[i] < a[j] || b[i] < b[j]){
                g[i].push_back(j);
            }
            if(a[i] > a[j] || b[i] > b[j]){
                g[j].push_back(i);
            }
        }
    }
    vector<int> ord(n);
    if(!topological_sort(g, ord)){
        cout << -1 << endl;
        return;
    }
    vector<P> ans;
    vector<int> p(n);
    iota(p.begin(), p.end(), 0);
    for(int i = 0; i < n; i++){
        if(p[i] == ord[i]) continue;
        for(int j = i+1; j < n; j++){
            if(p[j] == ord[i]){
                ans.push_back(P(i, j));
                swap(p[i], p[j]);
            }
        }
    }
    cout << ans.size() << endl;
    for(auto [i, j]: ans) cout << i+1 << ' ' << j+1 << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}