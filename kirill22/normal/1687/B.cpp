void solve() {
    int n, m;
    cin >> n >> m;
    vector<int> was;
    string tmp(m, '0');
    ve<int> w(m);
    for (int i = 0; i < m; i++) {
        tmp[i] = '1';
        w[i] = ask(tmp);
        tmp[i] = '0';
    }
    vector<int> id(m);
    iota(all(id), 0);
    sort(all(id), [&] (int i, int j) { return w[i] < w[j]; });
    int all = 0;
    forn (i, m) {
        int j = id[i];
        tmp[j] = '1';
        int res = ask(tmp);
        if (res - all != w[j]) {
            tmp[j] = '0';
        } else {
            all = res;
        }
    }
    cout << "! " << all << endl;
}

