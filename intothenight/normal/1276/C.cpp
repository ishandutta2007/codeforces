typedef vector<pii> vpii;
template<class T>
using Ftn = function<T(T, T)>;
template<class T>
using Tree = tree<T, null_type, less<T>, rb_tree_tag, tree_order_statistics_node_update>;
typedef tuple<int, int, int> tpl;
typedef vector<tpl> vtpl;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi tt(n);
	cin >> tt;
	sort(all(tt));
	vpii a;
	for(auto x: tt){
		if(a.empty() || a.back().second != x){
			a.push_back({1, x});
		}
		else{
			a.back().first ++;
		}
	}
	sort(all(a));
	vi sum(n + 1);
	for(int i = 0; i < a.size(); i ++){
		sum[i + 1] = sum[i] + a[i].first;
	}
	int mr = 0, mc = 0;
	for(int r = 1; r * r <= n; r ++){
		int p = lower_bound(all(a), pii{r, 0}) - a.begin();
		int tot = sum[p] + (a.size() - p) * r;
		int c = tot / r;
		if(r <= c && r * c > mr * mc){
			tie(mr, mc) = pii{r, c};
		}
	}
	for(auto &p: a){
		p.first = min(p.first, mr);
	}
	vector<vi> res(mr, vi(mc));
	for(int j = 0; j < mc; j ++){
		for(int i = 0; i < mr; i ++){
			res[i][(i + j) % mc] = a.back().second;
			a.back().first --;
			if(!a.back().first) a.pop_back();
		}
	}
	cout << mr * mc << "\n" << mr << " " << mc << "\n";
	cout << res;
	return 0;
}
 
