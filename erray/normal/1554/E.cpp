constexpr int md = 998244353;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;
 
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    int n;
    cin >> n;
    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; ++i) {
      int x, y;
      cin >> x >> y;
      --x, --y;
      g[x].push_back(y);
      g[y].push_back(x);
    }

    vector<int> ord;
    ord.push_back(0);
    for (int i = 0; i < n; ++i) {
      int v = ord[i];
      for (auto u : g[v]) {
        g[u].erase(find(g[u].begin(), g[u].end(), v));
        ord.push_back(u);
      }  
    }
    assert(int(ord.size()) == n);
    reverse(ord.begin(), ord.end());

    vector<Mint> ans(n + 1);
    ans[1] = power(Mint(2), n - 1);
    for (int k = 2; k <= n; ++k) {
      if ((n - 1) % k != 0) {
        continue;
      }
      vector<array<Mint, 2>> dp(n, {0, 0});
      for (auto v : ord) {
        array<vector<int>, 3> all;
        for (auto u : g[v]) {
          if (dp[u][1] == 0) {
            all[0].push_back(u);
          } else if (dp[u][0] == 0) {
            all[1].push_back(u);
          } else {
            all[2].push_back(u);
          }
        }
        
        Mint start = 1;
        for (int j = 0; j < 2; ++j) {
          for (auto u : all[j]) {
            start *= dp[u][j];
          }
        }
        int shift = int(all[0].size()) % k;
        for (int add = 0; add < 2; ++add) {
          int non_zero = (add + shift) % k;
          if (all[2].empty()) {
            if (non_zero == 0) {
              dp[v][add] = start;
            }
          } else {
            vector<Mint> ct(k);
            ct[non_zero] = start;
            for (auto u : all[2]) {
              vector<Mint> new_ct(k);
              for (int i = 0; i < k; ++i) {
                for (int j = 0; j < 2; ++j) {
                  new_ct[(i + (j ^ 1)) % k] += ct[i] * dp[u][j]; 
                }
              }
              swap(ct, new_ct);
            }
            dp[v][add] = ct[0];
          }  
        }
        
        if (dp[v][0] == 0 && dp[v][1] == 0) {
          break;
        }
      }
      ans[k] = dp[0][0];
    }

    for (int k = n; k >= 1; --k) {
      for (int j = k * 2; j <= n; j += k) {
        ans[k] -= ans[j];
      }
    }
    for (int i = 1; i <= n; ++i) {
      cout << ans[i] << ' ';
    }
    cout << '\n';
  }
}