#include <bits/stdc++.h>

using namespace std;

signed main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    
    int len = 1000;
    for (int i = 0; i < len; ++i) cout << '5';
    cout << '\n';
    for (int i = 0; i < len - 1; ++i) cout << '4';
    cout << '5';
    cout << '\n';

    return 0;
}