template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int n; cin >> n;
    vector<int> a(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    vector<int> mx(n), mn(n);
    mx[0] = a[0];
    for(int i = 1; i < n; i++){
        mx[i] = max(a[i], mx[i-1]);
    }
    mn[n-1] = a[n-1];
    for(int i = n-2; i >= 0; i--){
        mn[i] = min(a[i], mn[i+1]);
    }
    for(int i = 0; i < n-1; i++){
        if(mx[i] > mn[i]){
            cout << "YES" << endl;
            return;
        }
    }
    cout << "NO" << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}