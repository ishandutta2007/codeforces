	void solve()
	{
		int n = ni();
		int[][] ns = new int[n][];
		int[] par = new int[n];
		Arrays.fill(par, -1);
		for(int i = 0;i < n;i++){
			ns[i] = na(3);
			if(ns[i][1] >= 1){
				par[ns[i][1]-1] = i;
			}
			if(ns[i][2] >= 1){
				par[ns[i][2]-1] = i;
			}
		}
		int root = -1;
		for(int i = 0;i < n;i++)if(par[i] == -1)root = i;
		int[][] g = parentToG(par);
		int[][] pars = parents3(g, root);
		int[] ord = pars[1];
		boolean[] nr = new boolean[n];
		int[] ls = new int[n];
		int[] rs = new int[n];
		ls[root] = -1;
		rs[root] = 1000000007;
		for(int i = 0;i < n;i++){
			int cur = ord[i];
			if(ns[cur][2] >= 1){
				ls[ns[cur][2]-1] = Math.max(ls[cur], ns[cur][0]);
				rs[ns[cur][2]-1] = rs[cur];
			}
			if(ns[cur][1] >= 1){
				ls[ns[cur][1]-1] = ls[cur];
				rs[ns[cur][1]-1] = Math.min(rs[cur], ns[cur][0]);
			}
			
			if(!(ls[cur] < ns[cur][0] && ns[cur][0] < rs[cur])){
				nr[cur] = true;
			}
		}
//		tr(ls, rs);
//		tr(nr);
		int[] oks = new int[n];
		int p = 0;
		for(int i = 0;i < n;i++){
			if(!nr[i]){
				oks[p++] = ns[i][0];
			}
		}
		oks = Arrays.copyOf(oks, p);
		oks = radixSort(oks);
		int ng = 0;
		for(int i = 0;i < n;i++){
			if(Arrays.binarySearch(oks, ns[i][0]) < 0){
				ng++;
			}
		}
		out.println(ng);
	}
	
	public static int[] radixSort(int[] f){ return radixSort(f, f.length); }
	public static int[] radixSort(int[] f, int n)
	{
		int[] to = new int[n];
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(f[i]&0xffff)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[f[i]&0xffff]++] = f[i];
			int[] d = f; f = to;to = d;
		}
		{
			int[] b = new int[65537];
			for(int i = 0;i < n;i++)b[1+(f[i]>>>16)]++;
			for(int i = 1;i <= 65536;i++)b[i]+=b[i-1];
			for(int i = 0;i < n;i++)to[b[f[i]>>>16]++] = f[i];
			int[] d = f; f = to;to = d;
		}
		return f;
	}

	
	public static int[][] parentToG(int[] par)
	{
		int n = par.length;
		int[] ct = new int[n];
		for(int i = 0;i < n;i++){
			if(par[i] >= 0){
				ct[i]++;
				ct[par[i]]++;
			}
		}
		int[][] g = new int[n][];
		for(int i = 0;i < n;i++){
			g[i] = new int[ct[i]];
		}
		for(int i = 0;i < n;i++){
			if(par[i] >= 0){
				g[par[i]][--ct[par[i]]] = i;
				g[i][--ct[i]] = par[i];
			}
		}
		return g;
	}
	
	public static int[][] parents3(int[][] g, int root) {
		int n = g.length;
		int[] par = new int[n];
		Arrays.fill(par, -1);

		int[] depth = new int[n];
		depth[0] = 0;

		int[] q = new int[n];
		q[0] = root;
		for (int p = 0, r = 1; p < r; p++) {
			int cur = q[p];
			for (int nex : g[cur]) {
				if (par[cur] != nex) {
					q[r++] = nex;
					par[nex] = cur;
					depth[nex] = depth[cur] + 1;
				}
			}
		}
		return new int[][] { par, q, depth };
	}


	
