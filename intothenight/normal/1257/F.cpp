int main(){
	cin.tie(0)->sync_with_stdio(0);
	int m = 1 << 15;
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	vector<pair<vi, int>> p(m), q(m);
	for(int i = 0; i < m; i ++){
		p[i].second = q[i].second = i;
		for(int j = 0; j < n; j ++){
			p[i].first.push_back(__builtin_popcount(a[j] % m ^ i));
			q[i].first.push_back(__builtin_popcount(a[j] / m ^ i));
		}
		int t = *min_element(all(p[i].first));
		for(auto &x: p[i].first){
			x -= t;
		}
		t = *min_element(all(q[i].first));
		for(auto &x: q[i].first){
			x -= t;
		}
	}
	sort(all(p)), sort(all(q));
	for(auto &[arr, val]: p){
		int x = *max_element(all(arr));
		for(auto &tt: arr){
			tt = x - tt;
		}
		auto t = lower_bound(all(q), pair<vi, int>{arr, 0});
		if(t != q.end() && t->first == arr){
			cout << t->second * m + val;
			return 0;
		}
	}
	cout << -1;
	return 0;
}
 
