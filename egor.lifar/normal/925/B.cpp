typedef long long base;
typedef pair<base, base> point;

const string FILENAME = "input";

const double PI = acos(-1);

int n;
long long A, B;
vector<int> v, res;

bool cmp(int i, int j) {
	return v[i] < v[j];
}

int main() {
	srand(time(0));
	//read(FILENAME);
	cin >> n >> A >> B;
	v.resize(n);
	res.resize(n);
	cin >> v;
	for (int i = 0; i < n; ++i)
		res[i] = i;
	sort(res.begin(), res.end(), cmp);
	for (int t = 0; t < 2; ++t) {
		for (int i = 0; i < n; ++i) {
			int need = (A + v[res[i]] - 1) / v[res[i]];
			if (i + need >= n) continue;
			long long C = 1LL * v[res[i + need]] * (n - i - need);
			if (C >= B) {
				vector<int> ans[2];
				for (int j = i; j < i + need; ++j) {
					ans[0].pb(res[j] + 1);
				}	
				for (int j = i + need; j < n; ++j) {
					ans[1].pb(res[j] + 1);
				}
				if (t) swap(ans[0], ans[1]);
				cout << "Yes" << endl;
				cout << ans[0].size() << ' ' << ans[1].size() << '\n';
				cout << ans[0] << '\n' << ans[1] << '\n';
				return 0;
			}
		}
		swap(A, B);
	}
	cout << "No" << endl;
}