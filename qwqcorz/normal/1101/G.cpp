#include<bits/stdc++.h>
using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N],ans,t[35];
void insert(int x)
{
	for (int i=30;i>=0;i--)
	if ((x>>i)&1)
	{
		if (!t[i]) return t[i]=x,ans++,void();
		x^=t[i];
	}
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	for (int i=1;i<=n;i++) a[i]=a[i-1]^read();
	if (!a[n]) return puts("-1"),0;
	for (int i=n;i>=1;i--) insert(a[i]);
	print(ans);
	
	return 0;
}
