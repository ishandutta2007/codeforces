const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
LL getans(int n, int mod, int r)
{
	LL ret = n / mod;
	if (r != 0 && n%mod >= r) ret++;
	return ret;
}
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n, m;
	cin >> n >> m;
	LL ans = 0;
	for (int i = 0; i < 5; i++)
	{
		ans += getans(n, 5, i) * getans(m, 5, (5 - i) % 5);
	}
	cout << ans << endl;
	return 0;
}