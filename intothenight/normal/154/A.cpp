int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	string s;
	cin >> s;
	int n = s.size();
	int k;
	cin >> k;
	vector<vi> ok(26, vi(26, 1));
	while(k --){
		string t;
		cin >> t;
		ok[t[0] - 'a'][t[1] - 'a'] = ok[t[1] - 'a'][t[0] - 'a'] = false;
	}
	vector<vi> d(n, vi(26, 1));
	d[0] = vi(26, 0);
	d[0][s[0] - 'a'] = 1;
	for(int i = 1; i < n; i ++){
		for(int c = 0; c < 26; c ++){
			if(ok[c][s[i] - 'a']){
				d[i][s[i] - 'a'] = max(d[i][s[i] - 'a'], d[i - 1][c] + 1);
			}
		}
		for(int c = 0; c < 26; c ++){
			if(c != s[i] - 'a'){
				d[i][c] = d[i - 1][c];
			}
		}
	}
	cout << n - *max_element(all(d[n - 1]));
	return 0;
}

