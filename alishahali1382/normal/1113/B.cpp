int n, m, k, u, v, x, y, t, s, mn;
int ans[MAXN][MAXN]; // i --> j
int cnt[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	for (int i=1; i<=100; i++) for (int j=1; j<=i; j++){
		for (int d=1; d<=i; d++) if (i%d==0) ans[i][j]=min(ans[i][j], i/d+j*d-(i+j));
	}
	cin>>n;
	while (n--){
		cin>>x;
		cnt[x]++;
		s+=x;
	}
	for (int i=1; i<=100; i++) if (cnt[i]){
		for (int j=1; j<i; j++) if (cnt[j]) mn=min(mn, ans[i][j]);
		if (cnt[i]>=2) mn=min(mn, ans[i][i]);
	}
	cout<<s+mn<<'\n';
	
	return 0;
}
