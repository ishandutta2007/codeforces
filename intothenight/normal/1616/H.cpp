template<class F>
struct y_combinator_result{
	F f;
	template<class T> explicit y_combinator_result(T &&f): f(forward<T>(f)){ }
	template<class ...Args> decltype(auto) operator()(Args &&...args){ return f(ref(*this), forward<Args>(args)...); }
};
template<class F>
decltype(auto) y_combinator(F &&f){
	return y_combinator_result<decay_t<F>>(forward<F>(f));
}

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, s;
	cin >> n >> s;
	auto power = precalc_power(modular(2), n);
	vector<int> a(n);
	for(auto i = 0; i < n; ++ i){
		cin >> a[i];
	}
	ranges::sort(a);
	const int mx = 30;
	auto res = y_combinator([&](auto self, int bit, int l, int r)->modular{
		modular res = 0;
		if(!~bit || l == r){
			res = power[r - l] - 1;
		}
		else{
			int m = ranges::partition_point(begin(a) + l, begin(a) + r, [&](int x){ return ~x & 1 << bit; }) - begin(a);
			if(~s & 1 << bit){
				res = self(bit - 1, l, m) + self(bit - 1, m, r);
			}
			else{
				res = y_combinator([&](auto self2, int bit, int l1, int r1, int l2, int r2)->modular{
					modular res = 0;
					if(!~bit || l1 == r1 || l2 == r2){
						res = (power[r1 - l1] - 1) * (power[r2 - l2] - 1);
					}
					else{
						int m1 = ranges::partition_point(begin(a) + l1, begin(a) + r1, [&](int x){ return ~x & 1 << bit; }) - begin(a);
						int m2 = ranges::partition_point(begin(a) + l2, begin(a) + r2, [&](int x){ return ~x & 1 << bit; }) - begin(a);
						if(~s & 1 << bit){
							res = self2(bit - 1, l1, m1, l2, m2) + self2(bit - 1, m1, r1, m2, r2);
						}
						else{
							auto left = self2(bit - 1, l1, m1, m2, r2);
							auto right = self2(bit - 1, m1, r1, l2, m2);
							res =
								left +
								right +
								(power[m1 - l1] - 1) * (power[m2 - l2] - 1) +
								(power[r1 - m1] - 1) * (power[r2 - m2] - 1) +
								left * (power[r1 - m1] - 1 + power[m2 - l2] - 1) +
								right * (power[m1 - l1] - 1 + power[r2 - m2] - 1) +
								left * right;
						}
					}
					return res;
				})(bit - 1, l, m, m, r) + power[m - l] - 1 + power[r - m] - 1;
			}
		}
		return res;
	})(mx - 1, 0, n);
	cout << res << "\n";
	return 0;
}

