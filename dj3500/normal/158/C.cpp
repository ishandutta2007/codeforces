vector<string> current;

void go (string path) {
   if (path.empty()) return;
   int pos = 0;
   while (pos < SZ(path) && path[pos] != '/') {
      ++pos;
   }
   
   string dir = path.substr(0,pos);
   if (dir == "..") {
      current.pop_back();
   } else {
      current.pb(dir);
   }
   
   if (pos == SZ(path)) return;
   go(path.substr(pos+1));
}

