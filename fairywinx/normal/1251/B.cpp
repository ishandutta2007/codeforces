void solve() {
	int kol[3] = {0, 0, 0};
	int n;
	cin >> n;
	for(int i = 0; i < n; ++i) {
		string s;
		cin >> s;
		int _kol[2] = {0, 0};
		for(auto j: s) 
			_kol[j - '0']++;
		_kol[0] %= 2, _kol[1] %= 2;
		++kol[_kol[0] + _kol[1]];
	}
	if (kol[1] != 0) 
		cout << n << '\n';
	else if (kol[2] % 2 == 0)
		cout << n << '\n';
	else
		cout << n - 1 << '\n';
}

