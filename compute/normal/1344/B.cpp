bool vis[1005][1005];
int dx[]={0,1,0,-1},dy[]={1,0,-1,0};
vc<string> a;
    int n,m;
void dfs(int x,int y)
{
    vis[x][y]=1;
    for(int i=0;i<4;i++)
    {
        int nx=x+dx[i],ny=y+dy[i];
        if(nx>=0&&nx<n&&ny>=0&&ny<m&&!vis[nx][ny]&&a[nx][ny]=='#') dfs(nx,ny);
    }
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cin>>n>>m;
    a=vc<string>(n);
    vi row(n),col(m);
    for(int i=0;i<n;i++) cin>>a[i];
    int er=0,ec=0;
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
            if(a[i][j]=='#') row[i]++,col[j]++;
    }
    for(int i=0;i<n;i++)
    {
        if(row[i]==0) er++;
    }
    for(int i=0;i<m;i++)
    {
        if(col[i]==0) ec++;
    }
    if((ec&&!er)||(!ec&&er))
    {
        print(-1);
        return 0;
    }
    bool ok=1;
    for(int i=0;i<n;i++)
    {
        int cc=0;
        for(int j=0;j<m;j++)
        {
            if(a[i][j]=='#') cc++;
            else{
                if(cc&&cc!=row[i]) ok=0;
            }
        }
    }
    for(int j=0;j<m;j++)
    {
        int cc=0;
        for(int i=0;i<n;i++)
        {
            if(a[i][j]=='#') cc++;
            else{
                if(cc&&cc!=col[j]) ok=0;
            }
        }
    }
    if(!ok)
    {
        print(-1);
        return 0;
    }
    int ans=0;
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
            if(!vis[i][j]&&a[i][j]=='#')
            {
                dfs(i,j);
                ans++;
            }
        }
    }
    print(ans);
}