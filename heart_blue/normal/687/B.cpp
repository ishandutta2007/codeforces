const LL INF = 1e9 + 7;
const int N = 5e2 + 10;
int gcd(int n, int m)
{
	return m == 0 ? n : gcd(m, n%m);
}
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n, k;
	scanf("%d%d", &n, &k);
	int lcm = 1;
	for (int i = 1; i <= n; i++)
	{
		int x;
		scanf("%d", &x);
		x = gcd(x, k);
		int g = gcd(lcm, x);
		lcm = lcm / g*x;
	}
	if (lcm == k) puts("Yes");
	else puts("No");
	return 0;
}