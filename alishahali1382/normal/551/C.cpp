const ld eps=1e-7;
const int inf=1000000010;
const ll INF=1000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

ll n, m, k, u, v, x, y, t, a, b, ans;
ll A[MAXN];
ll B[MAXN];

bool check(ll T){
	for (int i=1; i<=n; i++) B[i]=A[i];
	int i=1;
	for (int j=0; j<m; j++){
		ll t=T;
		if (i>t) return false;
		t-=i;
		while (t){
			if (!B[i]){
				if (++i>n) return true;
				t--;
				continue ;
			}
			if (B[i]<=t){
				t-=B[i];
				B[i]=0;
			}
			else{
				B[i]-=t;
				t=0;
			}
		}
		if (i>n) return true;
	}
	return i>=n && !B[n];
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=1; i<=n; i++) cin>>A[i];
	while (!A[n]) n--;
	//debug(check(4));
	ll dwn=0, up=INF;
	while (up-dwn>1){
		ll mid=(dwn+up)/2;
		if (check(mid)) up=mid;
		else dwn=mid;
	}
	cout<<up<<endl;
	
	return 0;
}
