void test_case(int test) {
    int n, m;
    cin >> n >> m;
    vector<pair<int, int>> D;
    for (int i = 0; i < 3; ++i) {
        for (int j = 0; j < 3; ++j) {
            if (i == 1 && j == 1) continue;
            D.eb(i - 1, j - 1);
        }
    }
    vector<string> v(n, string(m, '0'));
    for (int s = 0; s < n + m; ++s) {
        for (int i = 0; i < n; ++i) {
            int j = s - i;
            if (j < 0 || j >= m) continue;
            if (i != 0 && i != n - 1 && j != 0 && j != m - 1) continue;
            bool can = true;
            for (auto [di, dj] : D) {
                if (i + di >= 0 && i + di < n && j + dj >= 0 && j + dj < m && v[i + di][j + dj] == '1')
                    can = false;
            }
            if (can)
                v[i][j] = '1';
        }
    }
    for (string s : v)
        cout << s << '\n';
}

