#define MEM(a,b) memset((a),(b),sizeof(a))
const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
int a[N];
int b[N];
bool solve()
{
	int n;
	scanf("%d", &n);
	for (int i = 1; i <= n; i++) scanf("%d", &a[i]);
	for (int i = 1; i <= n; i++) scanf("%d", &b[i]);
	a[n + 1] = a[1];
	b[n + 1] = b[1];
	for (int i = 1; i <= n; i++)
	{
		if (a[i] > b[i]) return false;
		if (a[i] == b[i]) continue;
		if (b[i + 1] + 1 < b[i]) return false;
	}
	return true;
}
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int ncase;
	scanf("%d", &ncase);
	while (ncase--)
	{
		if (solve()) puts("Yes");
		else puts("No");
	}
	return 0;
}