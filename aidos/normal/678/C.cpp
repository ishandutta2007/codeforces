int n, a, b, p, q;
void solve(){
	cin >> n >> a >> b >> p >> q;
	if(p < q){
		swap(a, b);
		swap(p, q);
	}
	ll m = a/__gcd(a, b) * 1ll * b;
	if(m > n){
		ll sum = n/a * 1ll * p + n/b * 1ll * q;
		cout << sum << endl;
		return;
	}
	ll sum = n/a * 1ll * p + (n/b - n/m) * 1ll * q;
	cout << sum << endl;
}

