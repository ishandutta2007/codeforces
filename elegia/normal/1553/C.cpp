template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  auto calc = [&](string s) {
    int a = 0, b = 0;
    for (int i = 0; i != 10; ++i) {
      if (s[i] == '1') {
        if (i & 1) ++b;
        else ++a;
      }
      if (a < b) {
        int ta = a;
        for (int j = 0; j < 10; j += 2) if (j > i) ++ta;
        if (ta < b) return i + 1;
      } else {
        int tb = b;
        for (int j = 1; j < 10; j += 2) if (j > i) ++tb;
        if (tb < a) return i + 1;
      }
    }
    return 10;
  };

  int T; cin >> T;
  while (T--) {
    string s; cin >> s;
    string x = s, y = s;
    for (int i = 0; i != 10; ++i) if (s[i] == '?') {
      x[i] = '0' + (i & 1);
      y[i] = '0' + !(i & 1);
    }
    cout << min(calc(x), calc(y)) << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}