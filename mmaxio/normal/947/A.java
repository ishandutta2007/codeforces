	int largestPrime(int x) {
		int init = x;
		int ret = -1;
		for (int i = 2; i * i <= x; i++) {
			while (x % i == 0) {
				ret = i;
				x /= i;
			}
		}
		if (x != 1 && x != init) {
			ret = Math.max(ret, x);
		}
		return ret;
	}

