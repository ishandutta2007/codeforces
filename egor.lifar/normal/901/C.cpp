inline void push(int v, int g) {
    if (mod[v] != 0) {
        d[v] += mod[v] * g; 
        if (v < ss) {
            mod[v * 2] += mod[v];
            mod[v * 2 + 1] += mod[v];
        }
        mod[v] = 0;
    }
}


void add(int v, int vl, int vr, int l, int r, int x) {
    if (vl > r || vr < l) {
        push(v, vr - vl + 1);
        return;
    }
    if (l <= vl && vr <= r) {
        mod[v] += x;
        push(v, vr - vl + 1);
        return;
    }
    push(v, vr - vl + 1);
    add(v * 2, vl, (vl + vr) / 2, l, r, x);
    add(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r, x);
    d[v] = d[v * 2] + d[v * 2 + 1];
}


long long rmq(int v, int vl, int vr, int l, int r) {
    if (vl > r || vr < l) {
        return 0;
    }
    if (l <= vl && vr <= r) {
        push(v, vr - vl + 1);
       //cout << d[v] << endl;
       // cout << vl << ' ' << vr << endl;
        return d[v];
    }
    push(v, vr - vl + 1);
    return rmq(v * 2, vl, (vl + vr) / 2, l, r) + rmq(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r);
}



