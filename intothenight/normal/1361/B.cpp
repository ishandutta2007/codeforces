constexpr int mod = (int)1e9 + 7;
//constexpr int mod = 998244353;
using Zp = Z_p<integral_constant<decay<decltype(mod)>::type, mod>>;

int SOLVE(){
	int n, p;
	cin >> n >> p;
	vector<int> a(n);
	for(auto i = 0; i < n; ++ i){
		cin >> a[i];
	}
	sort(a.rbegin(), a.rend());
	if(p == 1){
		cout << n % 2 << "\n";
		return 0;
	}
	array<deque<array<int, 2>>, 2> s; // exp, coef
	auto insert = [&](int t, int x){
		while(!s[t].empty() && s[t].back() == array<int, 2>{x, p - 1}){
			s[t].pop_back();
			++ x;
		}
		if(!s[t].empty() && s[t].back()[0] == x){
			++ s[t].back()[1];
		}
		else{
			s[t].push_back({x, 1});
		}
		while(!s[0].empty() && !s[1].empty() && s[0].front() == s[1].front()){
			s[0].pop_front();
			s[1].pop_front();
		}
	};
	auto get = [&](){ // returns index of the smaller set, 2 if tied
		if(s[0].empty()) return 0;
		if(s[1].empty()) return 1;
		return s[0].front() <= s[1].front() ? 0 : 1;
	};
	for(auto x: a){
		insert(get(), x);
	}
	auto t = get();
	Zp res = 0;
	for(auto [x, c]: s[!t]){
		res += c * (Zp(p) ^ x);
	}
	for(auto [x, c]: s[t]){
		res -= c * (Zp(p) ^ x);
	}
	cout << res << "\n";
	return 0;
}

int main(){
	cin.tie(0)->sync_with_stdio(0);
	int ttt;
	cin >> ttt;
	while(ttt --){
		SOLVE();
	}
	return 0;
}

