#include<bits/stdc++.h>
#define ll long long
using namespace std;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x,char c='\n')
{
	write(x);
	putchar(c);
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int T=read();
	while (T--)
	{
		ll ans=1;
		int n=read(),tot=0;
		for (int i=1;i<=n;i++)
		{
			int x=read();
			if (x==1) tot++;
			if (x==0) ans*=2;
		}
		print(ans*tot);
	} 
	
	return 0;
}