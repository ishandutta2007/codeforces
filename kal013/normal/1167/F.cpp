struct node{
    int l=0,r=0,tot=0;
};

node seg[maxn];

#define M ((s+e)/2)
void update(int node,int s,int e,int idx){
    if(s>idx || e<idx) return;
    if (s==e){
        seg[node].l=seg[node].r=seg[node].tot=1;
        return;
    }
    update(2*node,s,M,idx);
    update(2*node+1,M+1,e,idx);
    seg[node].tot=seg[2*node].tot+seg[2*node+1].tot;
    seg[node].l=(seg[2*node].l+seg[2*node+1].l+((seg[2*node].tot*(e-M))%mod))%mod;
    seg[node].r=(seg[2*node].r+seg[2*node+1].r+((seg[2*node+1].tot*(M-s+1))%mod))%mod;
}


int query(int node,int s,int e,int idx,int n){
    // trace(node,s,e,idx,n,seg[node].r,seg[node].l,seg[node].tot);
    if (idx>e){
        return ((seg[node].r+(seg[node].tot*(s))%mod)*(n-idx))%mod;
    }
    if (idx<s){
        return ((seg[node].l+(seg[node].tot*(n-1-e))%mod)*(idx+1))%mod;
    }
    if (s==e){
        return ((n-idx)*(idx+1))%mod;
    }
    return (query(2*node,s,M,idx,n)+query(2*node+1,M+1,e,idx,n))%mod;
}


