int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n, m;
    cin >> n >> m;
    vector<ll> a(n);
    cin >> a;

    auto calc = y_combinator([&](auto calc, int l, int r) -> vector<ll> {
        if (l > r) return {0};
        if (l == r) return {0, a[l] * (m - 1)};
        int im = -1;
        for (int i = l; i <= r; ++i) {
            if (im == -1 || a[i] < a[im])
                im = i;
        }
        ll mn = a[im];
        if (im == r) --im;
        auto v1 = calc(l, im);
        auto v2 = calc(im + 1, r);
        vector<ll> res(v1.size() + v2.size() - 1, 0);
        if (res.size() > m + 1) res.resize(m + 1);
        for (int i = 0; i < v1.size(); ++i) {
            for (int j = 0; j < v2.size() && i + j < res.size(); ++j) {
                res[i + j] = max(res[i + j], v1[i] + v2[j] - (ll)i * j * 2 * mn);
            }
        }

        return res;
    });

    auto res = calc(0, n - 1);
    cout << res[m] << '\n';

    return 0;
}