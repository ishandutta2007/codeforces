    int n, k;
    cin >> n >> k;
    function<vector<int>(int, int)> solve = [&](int n, int k) {
        if (n - (k - 1) <= n / 2) {
            vector<int> v(k, 1);
            v.back() = n - (k - 1);
            return v;
        }
        if (n % k == 0) {
            vector<int> v(k, n / k);
            return v;
        }
        if (n % 2 == 0) {
            auto v = solve(n / 2, k);
            for (int i = 0; i < v.size(); ++i) {
                v[i] *= 2;
            }
            return v;
        } else {
            auto v = solve(n - 1, k - 1);
            v.pb(1);
            return v;
        }
    };
