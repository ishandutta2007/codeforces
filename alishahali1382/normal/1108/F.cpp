const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 200010;

struct DSU{
	int par[MAXN];
	DSU(){for (int i=1; i<MAXN; i++) par[i]=i; }
	int get(int x){
		if (par[x]==x) return x;
		return par[x]=get(par[x]);
	}
	int join(int x, int y){
		if (get(x)==get(y)) return 0;
		par[get(x)]=get(y);
		return 1;
	}
} dsu;

int n, m, k, u, v, w, x, t, a, b, ans;
piii E[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=0; i<m; i++){
		cin>>u>>v>>w;
		E[i]={w, {u, v}};
	}
	sort(E, E+m);
	E[m]={inf, {0, 0}};
	for (int i=0, j=0; i<m; i=j){
		while (E[i].first==E[j].first) j++;
		for (int x=i; x<j; x++){
			int u=E[x].second.first, v=E[x].second.second;
			ans+=(dsu.get(u)!=dsu.get(v));
		}
		for (int x=i; x<j; x++){
			int u=E[x].second.first, v=E[x].second.second;
			dsu.join(u, v);
		}
	}
	cout<<ans-n+1<<endl;
	
	return 0;
}
