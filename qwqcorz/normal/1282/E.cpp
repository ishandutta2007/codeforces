#include<bits/stdc++.h>
#define mk make_pair
#define fi first
#define se second
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct node
{
	int x,y,z;
}a[N];
int tot[N];
map<pair<int,int>,int>e;
vector<int>p,q;
void solve(int i,int u,int v)
{
	if (!i) return;
	int id[6]={a[i].x,a[i].y,a[i].z,a[i].x,a[i].y,a[i].z},w=-1;
	for (int i=0;i<4;i++) if (id[i]==u&&id[i+1]==v) w=id[i+2];
	for (int i=5;i>1;i--) if (id[i]==u&&id[i-1]==v) w=id[i-2];
	assert(w>=0);
	solve(e[mk(v,w)]^i,w,v);
	p.push_back(w);
	solve(e[mk(w,u)]^i,u,w);
	q.push_back(i);
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		int n=read(),x;
		e.clear(),p.clear(),q.clear();
		for (int i=1;i<=n;i++) tot[i]=0;
		for (int i=1;i<=n-2;i++)
		{
			int x=a[i].x=read();
			int y=a[i].y=read();
			int z=a[i].z=read();
			tot[x]++,tot[y]++,tot[z]++;
			e[mk(x,y)]^=i,e[mk(x,z)]^=i;
			e[mk(y,x)]^=i,e[mk(y,z)]^=i;
			e[mk(z,x)]^=i,e[mk(z,y)]^=i;
		}
		for (x=1;tot[x]>1;x++);
		for (int i=1;i<=n;i++)
		if (a[i].x==x||a[i].y==x||a[i].z==x)
		{
			int u,v;
			if (a[i].x==x) u=a[i].y,v=a[i].z;
			else if (a[i].y==x) u=a[i].x,v=a[i].z;
			else u=a[i].x,v=a[i].y;
			p.push_back(u);
			p.push_back(x);
			p.push_back(v);
			q.push_back(i);
			solve(e[mk(u,v)]^i,u,v);
			break;
		}
		for (int i:p) print(i,' ');puts("");
		for (int i:q) print(i,' ');puts("");
	}
	
	return 0;
}
