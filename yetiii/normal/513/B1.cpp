vi br(int n, int m)
{
    int rs=0;
    vi p(n);
    for (int i=0;i<n;++i) p[i]=i+1;
    vector<vi> h;
    do
    {
        int t=0;
        for (int i=0;i<n;++i)
        {
            int w=n+1;
            for (int j=i;j<n;++j)
            {
                w=min(w, p[j]);
                t+=w;
            }
        }
        if (t>rs) rs=t, h.clear();
        if (t==rs) h.emplace_back(p);
    } while (next_permutation(p.begin(), p.end()));
    return h[m-1];
}
void go(vi &p, int a, int l, int r, vector<vi> &h)
{
    if (l==r)
    {
        h.push_back(p);
        return;
    }
    p[l]=a; go(p, a+1, l+1, r, h);
    p[r-1]=a; go(p, a+1, l, r-1, h);
}
vi fa(int n, int m)
{
    vi p(n);
    vector<vi> h;
    go(p, 1, 0, n, h);
    sort(h.begin(), h.end());
    cerr<<h.size()<<endl;
    h.erase(unique(h.begin(), h.end()), h.end());
    cerr<<h.size()<<endl;
    return h[m-1];
}
void go1(vi &p, int a, int l, int r, int m)
{
    if (l==r) return;
    int n=r-l-1, h=1<<n;
    h/=2;
    if (h>=m)
    {
        p[l++]=a++;
        go1(p, a, l, r, m);
        return;
    }
    else
    {
        p[--r]=a++;
        go1(p, a, l, r, m-h);
    }
}
vi fa1(int n, int m)
{
    vi p(n);
    go1(p, 1, 0, n, m);
    return p;
}
