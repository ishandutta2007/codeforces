template<typename T_vector>
void output_vector(const T_vector &v, bool add_one = false, int start = -1, int end = -1) {
    if (start < 0) start = 0;
    if (end < 0) end = int(v.size());

    for (int i = start; i < end; i++)
        cout << v[i] + (add_one ? 1 : 0) << (i < end - 1 ? ' ' : '\n');
}


void run_case() {
    int N;
    cin >> N;
    vector<int64_t> A(N);
    array<int64_t, 2> sums = {0, 0};

    for (auto &a : A)
        cin >> a;

    for (int i = 0; i < N; i++)
        sums[i % 2] += A[i];

    if (sums[0] < sums[1]) {
        for (int i = 0; i < N; i += 2)
            A[i] = 1;
    } else {
        for (int i = 1; i < N; i += 2)
            A[i] = 1;
    }

    output_vector(A);
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}