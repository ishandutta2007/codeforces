constexpr int md = 998244353;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  string s;
  cin >> s;
  int n = (int) s.size();
  int l = 0, r = 0, q = 0;
  for (char c : s) {
    if (c == '(') ++l; else
    if (c == ')') ++r;
    else ++q;
  }
  vector<int> pref(n + 1);
  for (int i = 0; i < n; i++) {
    pref[i + 1] = pref[i] + (s[i] == '?');
  }
  vector<int> pref_open(n + 1);
  for (int i = 0; i < n; i++) {
    pref_open[i + 1] = pref_open[i] + (s[i] == '(');
  }
  vector<Mint> fact(n + 1);
  fact[0] = 1;
  for (int i = 1; i <= n; i++) {
    fact[i] = fact[i - 1] * i;
  }
  vector<Mint> inv_fact(n + 1);
  for (int i = 0; i <= n; i++) {
    inv_fact[i] = 1 / fact[i];
  }
  auto C = [&](int N, int K) {
    if (K < 0 || K > N) return Mint(0);
    return fact[N] * inv_fact[K] * inv_fact[N - K];
  };
  Mint ans = 0;
  for (int qtor = 0; qtor <= q; qtor++) {
    int qtol = q - qtor;
    int x = l + qtol;
    int y = r + qtor;
    int lft = pref[y];
    int rgt = q - lft;
    int z = pref_open[y];
    for (int k = 0; k <= lft; k++) {
      ans += C(lft, k) * C(rgt, qtol - k) * (z + k);
    }
  }
  cout << ans << '\n';
  return 0;
}