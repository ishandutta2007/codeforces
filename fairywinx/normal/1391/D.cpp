const int N = 5e5 + 228;

int dp[N][8];

void solve() {
	int n, m;
	cin >> n >> m;
	if (min(n, m) == 1) {
		cout << 0 << '\n';
		return;
	}
	for(int i = 0; i < max(n, m); ++i)
		fill(dp[i], dp[i] + 8, 1e9);
	vector<vector<int>> tmp(n, vector<int> (m));
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j) {
			char c;
			cin >> c;
			tmp[i][j] = (c - '0');
		}
	}
	if (min(n, m) > 3) {
		cout << -1 << '\n';
		return;
	}
	vector<vector<int>> a(max(n, m), vector<int> (min(n, m)));
	if (m > n) {
		for(int i = 0; i < m; ++i) {
			for(int j = 0; j < n; ++j)
				a[i][j] = (1 << j) * tmp[j][i];
		}
		swap(n, m);
	}
	else {
		for(int i = 0; i < n; ++i) {
			for(int j = 0; j < m; ++j)
				a[i][j] = (1 << j) * tmp[i][j];
		}
	}
	for(int i = 0; i < (1 << m); ++i) {
		dp[0][i] = 0;
		for(int j = 0; j < m; ++j) {
			if (a[0][j] != (i & (1 << j))) {
				++dp[0][i];
			}
		}
	}
	for(int i = 1; i < n; ++i) {
		for(int mask1 = 0; mask1 < (1 << m); ++mask1) {
			for(int mask2 = 0; mask2 < (1 << m); ++mask2) {
				int c = dp[i - 1][mask2];
				for(int j = 0; j < m; ++j) {
					if (a[i][j] != ((1 << j) & mask1))
						++c;
				}
				bool can = true;
				for(int j = 1; j < m; ++j) {
					int cnt = 0;
					if (mask1 & (1 << j))
						++cnt;
					if (mask1 & (1 << (j - 1)))
						++cnt;
					if (mask2 & (1 << j))
						++cnt;
					if (mask2 & (1 << (j - 1)))
						++cnt;
					if (cnt % 2 == 0)

						can = false;
				}
				if (can)
					dp[i][mask1] = min(dp[i][mask1], c);
			}
		}
	}
	int ans = 1e9 + 228;
	for(int i = 0; i < (1 << m); ++i) {
		ans = min(ans, dp[n - 1][i]);
	}
	cout << ans << '\n';
}

signed main() {
	setup();
	// SOLVE;
	solve();

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

