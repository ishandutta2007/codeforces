const int N = 510200;

int n;
int a[N], p[N];
int equality[N];

map<int, int> where;
set<pair<int, int> > same;
int ans = 0;

signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >> n;
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        p[i] = a[i] + p[i - 1];
    }
    for (int i = n; i >= 0; i--)
    {
        same.erase({equality[i], p[i]});
        if (where.find(p[i]) == where.end()) 
        {
            equality[i] = n;
            where[p[i]] = i;
        }
        else
        {
            equality[i] = where[p[i]] - 1;
            where[p[i]] = i;
        }
        same.insert({equality[i], p[i]});
        pair<int, int> it = *(same.begin());
        ans = ans + it.first - i;
    }
    cout << ans;
}