using namespace std;
#define ll long long



int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        ll a,b;
        cin>>a>>b;
        if(a == b || a - b == 1)
        {
            printf("NO\n");
            continue ;
        }
        printf("YES\n");


    }



    return  0;
}