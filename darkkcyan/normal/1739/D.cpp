const int maxn = 201010;

int n, k;
vector<int> children[maxn];

pair<int, int> find_min_cut(int u, int acceptable_h) {
    int max_h = 0;
    int total_cut = 0;
    for (auto v: children[u]) {
        auto [vc, vh] = find_min_cut(v, acceptable_h);
        total_cut += vc;
        if (u != 1 and vh + 1 >= acceptable_h) {
            ++total_cut;
        } else {
            max_h = max(max_h, vh + 1);
        }
    }
    return {total_cut, max_h};
}

