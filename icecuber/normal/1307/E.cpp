pair<int,Mint> ans = {-1,0};

void operator+=(pair<int,Mint>&a, pair<int,Mint> b) {
  if (b.second == 0) return;
  if (b.first > a.first) a = b;
  else if (b.first == a.first) a.second += b.second;
}

inline void solve(vector<vector<pair<int,int>>>&lr, int n, int skipf, int mid, pair<int,Mint> start) {
  pair<int,Mint> dp = start;
  for (int f = 0; f < n; f++) {
    if (f == skipf) continue;
    if (lr[f].empty()) continue;
    pair<int,Mint> ndp[4];
    ndp[0] = dp;
    pair<int,Mint> nndp[4];
    for (int i = 0; i < (int)lr[f].size(); i++) {
      auto [l,r] = lr[f][i];
      copy_n(ndp, 4, nndp);
      //nndp = ndp;
      //Send left
      if (l < mid) {
	nndp[1] += {ndp[0].first+1, ndp[0].second};
	nndp[3] += {ndp[2].first+1, ndp[2].second};
      }
      //Send right
      if (n-1-r > mid) {
	nndp[2] += {ndp[0].first+1, ndp[0].second};
	nndp[3] += {ndp[1].first+1, ndp[1].second};
      }
      copy_n(nndp, 4, ndp);
      //ndp = nndp;
    }
    dp = {0,0};
    for (int i = 0; i < 4; i++)
      dp += ndp[i];
  }
  ans += dp;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, m;
  cin >> n >> m;
  vector<int> s(n);
  for (int&v : s) {cin >> v;v--;}

  vector<vector<pair<int,int>>> lr(n);
  for (int i = 0; i < m; i++) {
    int f, h;
    cin >> f >> h;
    f--;
    int l = 0,el = 0;
    while (l < n) {
      el += (s[l] == f);
      if (el == h) break;
      l++;
    }
    int r = 0,er = 0;
    while (r < n) {
      er += (s[n-1-r] == f);
      if (er == h) break;
      r++;
    }
    lr[f].emplace_back(l,r);
  }

  //Send everything from the right
  solve(lr, n, -1, -1, {0, 1});
  for (int f = 0; f < n; f++) {
    for (int i = 0; i < (int)lr[f].size(); i++) {
      auto [l,r] = lr[f][i];
      if (l == n || r == n) continue;
      //Send from left
      int rights = 0;
      for (int j = 0; j < (int)lr[f].size(); j++)
	if (i != j && n-1-lr[f][j].second > l) rights++;
      pair<int,Mint> start = {1,1};
      start += {2, rights};
      solve(lr, n, f, l, start);
    }
  }
  cout << ans.first << ' ' << ans.second << endl;
}