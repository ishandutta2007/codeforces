#include<bits/stdc++.h>
#define int long long
#define rd(x) x=read()
using namespace std;
inline int read(){int x=0,f=1;char ch=getchar();while(ch>'9'||ch<'0'){if(ch=='-')f=-1;ch=getchar();}while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}return x*f;}
inline int ksm(int x,int y,int z){int ret=1;while (y){if (y&1) ret=(ret*x)%z;x=(x*x)%z;y>>=1;}return ret;}

string N,B;
int c,len1,len2,phi,p;
bool flag=0;
int n,b;
signed main()
{
    cin>>B>>N>>c;
    len1=B.length();
    len2=N.length();
    N=" "+N;B=" "+B;
    p=phi=c;
    for (int i=2;i*i<=p;i++)
    {
        if (p%i==0)
        {
            phi-=phi/i;
            while (p%i==0) p/=i;
        }
    }
    if (p>1) phi-=phi/p;
    for (int i=1;i<=len1;i++) b=(b*10+B[i]-'0')%c;
    for (int i=len2;i>=1;i--)
        if (N[i]=='0') N[i]='9';
        else
        {
            N[i]--;
            break;
        }
    for (int i=1;i<=len2;i++)
    {
        n=n*10+N[i]-'0';
        if (n>=phi) flag=1;
        n%=phi;
    }
    if (flag) n+=phi;
    int ans=((b-1)*ksm(b,n,c)+c)%c;
    if (ans==0) ans=c;
    cout<<ans<<endl;
}