#include <bits/stdc++.h>
using namespace std;

int n,m;
int a[105][105];
vector<int> res;

int main(){
	scanf("%d%d",&n,&m);
	for(int i=1;i<=m;i++){
		res.push_back(i);
		for(int j=1;j<=n;j++){
			scanf("%d",a[i]+j);
		}
	}
	for(int i=1;i<n;i++){
		vector<pair<int,int> > vs;
		int xju=0;
		for(int j=1;j<=m;j++){
			vs.push_back(make_pair(a[j][i]-a[j][n],j));
			xju+=a[j][i]-a[j][n];
		}
		sort(vs.begin(),vs.end());
		int now=0;
		vector<int> rem;
		while(xju<0&&now<m){
			xju-=vs[now].first;
			rem.push_back(vs[now].second);
			now++;
		}
		if(now>=m){
			continue;
		}
		if(rem.size()<res.size()){
			res=rem;
		}
	}
	printf("%d\n",(int)res.size());
	for(int i=0;i<res.size();i++){
		printf("%d%c",res[i],i==n?'\n':' ');
	}
	return 0;
}