#include<bits/stdc++.h>
using namespace std;
const int N=505;
const int dx[4]={0,0,1,-1};
const int dy[4]={1,-1,0,0};

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N][N],n,m;
void dfs(int x,int y,int ex,int ey)
{
	for (int i=0;i<4;i++)
	{
		int nx=x+dx[i],ny=y+dy[i];
		if (nx<1||nx>n||ny<1||ny>m) continue;
		if (a[nx][ny])
		{
			if (nx==ex&&ny==ey) puts("YES"),exit(0);
			continue;
		}
		a[nx][ny]=1;
		dfs(nx,ny,ex,ey);
	}
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	n=read(),m=read();
	for (int i=1;i<=n;i++)
	for (int j=1;j<=m;j++)
	{
		char c;
		cin>>c;
		a[i][j]=c=='X';
	}
	int sx=read(),sy=read(),ex=read(),ey=read();
	dfs(sx,sy,ex,ey);
	puts("NO");
	
	return 0;
}
