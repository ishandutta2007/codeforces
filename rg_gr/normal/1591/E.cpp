void add(int x)
{
	s[t[x]].erase(x);
	t[x]++;
	s[t[x]].insert(x);
	if(t[x]==1)adv++;
	else sum[t[x]-1]--;
}
void rmv(int x)
{
	s[t[x]].erase(x);
	t[x]--;
	s[t[x]].insert(x);
	if(t[x]==0)adv--;
	else sum[t[x]]++;
}
