            int k = 6 - i - j;
            vector<int> v;
            for (int p = 0; p < i; p++) v.emplace_back(0);
            for (int p = 0; p < j; p++) v.emplace_back(4);
            for (int p = 0; p < k; p++) v.emplace_back(7);
            int sum = accumulate(v.begin(), v.end(), 0);
            idx[sum] = digits.size();
