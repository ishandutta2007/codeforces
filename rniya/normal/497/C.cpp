int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<tuple<int, int, int>> part;
    for (int i = 0; i < n; i++) {
        int a, b;
        cin >> a >> b;
        part.emplace_back(a, b, i);
    }
    int m;
    cin >> m;
    vector<tuple<int, int, int, int>> actor;
    for (int i = 0; i < m; i++) {
        int c, d, k;
        cin >> c >> d >> k;
        actor.emplace_back(c, d, k, i);
    }

    sort(ALL(part));
    reverse(ALL(part));
    sort(ALL(actor));
    reverse(ALL(actor));

    set<pair<int, int>> s;
    vector<int> ans(n, -1);

    for (int i = 0, j = 0; i < m; i++) {
        int c, d, k, id;
        tie(c, d, k, id) = actor[i];
        while (j < n && c <= get<0>(part[j])) {
            int a, b, nid;
            tie(a, b, nid) = part[j++];
            s.emplace(-b, nid);
        }
        auto itr = s.lower_bound({-d, 0});
        vector<pair<int, int>> cand;
        for (; k-- && itr != s.end(); itr++) {
            int nid = (*itr).second;
            ans[nid] = id + 1;
            cand.emplace_back(*itr);
        }
        for (auto p : cand) {
            auto nitr = s.find(p);
            s.erase(nitr);
        }
    }

    for (int i = 0; i < n; i++) {
        if (ans[i] < 0) {
            cout << "NO" << '\n';
            return 0;
        }
    }

    cout << "YES" << '\n';
    cout << ans << '\n';
    return 0;
}