const int N=505;
const int ans1[4][4]={
{0,0,0,0},
{0,4,5,9},
{0,7,3,6},
{0,1,2,8}
};
const int ans2[4][4]={
{0,0,0,0},
{0,4,7,1},
{0,5,3,2},
{0,9,6,8}
};

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N][N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),now=0,m=n;
	if (n<3) return puts("-1"),0;
	int x=n,y=1;
	for (;n>3;n--)
	{
		if (y==1)
		{
			while (y<n) a[x][y++]=++now;a[x][y]=++now;x--;
			while (x>1) a[x--][y]=++now;a[x][y--]=++now;
		}
		else
		{
			while (x<n) a[x++][y]=++now;a[x][y]=++now;y--;
			while (y>1) a[x][y--]=++now;a[x--][y]=++now;
		}
	}
	if (y==1)
	for (int i=1;i<=3;i++)
	for (int j=1;j<=3;j++)
	a[i][j]=ans1[i][j]+now;
	else
	for (int i=1;i<=3;i++)
	for (int j=1;j<=3;j++)
	a[i][j]=ans2[i][j]+now;
	for (int i=1;i<=m;i++,putchar('\n'))
	for (int j=1;j<=m;j++) print(a[i][j],' ');
	
	return 0;
}
