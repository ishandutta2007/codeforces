#include<bits/stdc++.h>
using namespace std;
const int N=105;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
bool ans[N][N];
int S[N],l[N];
bool A(int x,int y)
{
	printf("1 %d %d",x-1,y-1);
	cout<<endl;
	return read();
}
bool B(int x,int l,int r)
{
	printf("2 %d %d",x-1,r-l+1);
	for (int i=l;i<=r;i++) printf(" %d",S[i]-1);
	cout<<endl;
	return read();
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		int n=read();
		S[1]=1;
		for (int i=2;i<=n;i++)
		{
			if (A(i,S[1])) {for (int j=i-1;j>=1;j--) S[j+1]=S[j];S[1]=i;continue;}
			if (A(S[i-1],i)) {S[i]=i;continue;}
			int l=2,r=i-1;
			while (l<=r)
			{
				int mid=(l+r)/2;
				if (A(i,S[mid])) r=mid-1;
							else l=mid+1;
			}
			for (int j=i-1;j>=l;j--) S[j+1]=S[j];
			S[l]=i;
		}
		for (int i=n,j=n;i>=1;i--)
		{
			j=min(j,i);
			while (j>1&&B(S[i],1,j-1)) j--;
			l[i]=j;
		}
		for (int i=1;i<=n;i++)
		for (int j=1;j<=n;j++) ans[i][j]=0;
		for (int i=1;i<=n;i++)
		for (int j=l[i];j<i;j++) l[i]=min(l[i],l[j]);
		for (int i=1;i<=n;i++)
		for (int j=l[i];j<=n;j++) ans[S[i]][S[j]]=1;
		print(3);
		for (int i=1;i<=n;i++,putchar('\n'))
		for (int j=1;j<=n;j++) putchar(ans[i][j]+'0');
		fflush(stdout);
		if (read()<0) return -1;
	}
	
	return 0;
}
