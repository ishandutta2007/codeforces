#include <bits/stdc++.h>
#define ll long long
#define lc(x) ((x) << 1)
#define rc(x) ((x) << 1 | 1)
#define ru(i, l, r) for (int i = (l); i <= (r); i++)
#define rd(i, r, l) for (int i = (r); i >= (l); i--)
#define mid ((l + r) >> 1)
using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(!isdigit(ch)) {w |= ch == '-'; ch = getchar();}
	while(isdigit(ch)) {x = x * 10 + ch - '0'; ch = getchar();}
	return w ? -x : x;
}
void solve()
{
	int n = read();
	if(n < 100) {
	printf("%d\n", n % 10); return;}
	int mn = 9;
	while(n) mn = min(mn, n % 10), n /= 10;
	printf("%d\n", mn);
}
int main()
{
	int T = read(); while(T--) solve(); 
	return 0;
}