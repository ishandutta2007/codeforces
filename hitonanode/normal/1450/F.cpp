int solve(vector<int> &A) {
    const int N = A.size();
    vector<int> cnt(N);
    for (auto &a : A) a--, cnt[a]++;
    if (*max_element(ALL(cnt)) > (N + 1) / 2) return -1;

    int seg = 0;
    vector<int> dbl(N), sgl(N);
    int l = 0;
    REP(r, N) {
        if (r == N - 1 or A[r + 1] == A[r]) {
            const int cl = A[l], cr = A[r];
            if (cl == cr) dbl[cl]++;
            else sgl[cl]++, sgl[cr]++;
            seg++;
            l = r + 1;
        }
    }
    int ret = 0;
    REP(i, N) {
        int n2 = dbl[i], n1 = sgl[i], n0 = seg - n2 - n1;
        chmax(ret, n2 - n0 - 1);
    }

    return ret + seg - 1;
}

int main()
{
    int T;
    cin >> T;
    while (T--) {
        int N;
        cin >> N;
        vector<int> A(N);
        cin >> A;
        cout << solve(A) << '\n';
    }
}