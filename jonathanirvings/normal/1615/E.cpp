using S = pii;
using F = int;

S op(S l, S r) {
  return max(l, r);
}

S e() {
  return mp(-INF,-1);
}

S mapping(F l, S r) {
  return mp(r.fi - l, r.se);
}

F composition(F l, F r) {
  return l + r;
}

F id() {
  return 0;
}

int n,k;
vi adj[200005];
int a,b;
pii range[200005];
int balik[200005];
int t = 0;
int depth[200005];
bool ilang[200005];
int parent[200005];

void dfs(int u)
{
  range[u] = mp(t,t);
  balik[t] = u;
  ++t;
  for (int v : adj[u]) if (v != parent[u])
  {
    parent[v] = u;
    depth[v] = depth[u] + 1;
    dfs(v);
    range[u].se = range[v].se;
  }
}

