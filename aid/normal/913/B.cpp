void precalc() {
}

const int maxn = 1005;
int n;
vector<int> g[maxn];

int read() {
  if (scanf("%d", &n) < 1) {
    return false;
  }
  for (int i = 0; i < n; i++) {
    g[i].clear();
  }
  for (int i = 1; i < n; i++) {
    int p;
    scanf("%d", &p);
    p--;
    g[p].push_back(i);
  }
  return true;
}

bool leaf(int v) {
  return (g[v].empty() && v);
}

void solve() {
  bool ok = true;
  for (int v = 0; v < n; v++) {
    if (leaf(v)) {
      continue;
    }
    int cnt = 0;
    for (int i = 0; i < sz(g[v]); i++) {
      if (leaf(g[v][i])) {
        cnt++;
      }
    }
    if (cnt < 3) {
      ok = false;
      break;
    }
  }
  printf(ok ? "Yes\n" : "No\n");
}

int main() {
  precalc();
#ifdef DEBUG
  assert(freopen(TASK ".in", "r", stdin));
  assert(freopen(TASK ".out", "w", stdout));
#endif
  while (read()) {
    solve();
#ifdef DEBUG
    eprintf("Time %.2f\n", (double) clock() / CLOCKS_PER_SEC);
#endif
  }
  return 0;
}