const int mod=998244353;

char a[1<<18];
int n,ans=1;

string dfs(int u){
    if(u>=(1<<n))return "";
    string t="";t+=a[u];
    string L=dfs(2*u),R=dfs(2*u+1);
    if(L!=R)ans=2*ans%mod;
    if(L>R)swap(L,R);
    t+=L+R;
    return t;
}

int main(){
    scanf("%d",&n);
    scanf("%s",a+1);
    dfs(1);
    printf("%d\n",ans);
    return 0;
}