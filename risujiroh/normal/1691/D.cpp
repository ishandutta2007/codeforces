using i64 = long long;

i64 const inf = 1e18;

struct S {
  i64 s = 0;
  i64 l = -inf;
  i64 m = -inf;
  i64 r = -inf;
};

S op(S x, S y) {
  return {
      x.s + y.s,
      std::max(x.l, x.s + y.l),
      std::max({x.m, y.m, x.r + y.l}),
      std::max(x.r + y.s, y.r),
  };
}
S e() { return {}; }

int main() {
  using namespace std;
  cin.tie(nullptr)->sync_with_stdio(false);

  int T;
  cin >> T;
  while (T--) {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int& e : a) cin >> e;

    atcoder::segtree<S, op, e> seg(n);
    for (int i = 0; i < n; ++i) {
      seg.set(i, {a[i], a[i], a[i], a[i]});
    }

    vector<int> order(n);
    iota(begin(order), end(order), 0);
    sort(begin(order), end(order), [&](int x, int y) { return a[y] < a[x]; });

    bool ok = true;

    set<int> s{-1, n};
    for (int i : order) {
      int ri = *s.upper_bound(i);
      int li = *prev(s.lower_bound(i));

      if (a[i] < seg.prod(li + 1, ri).m) {
        ok = false;
        break;
      }

      s.insert(i);
    }

    cout << (ok ? "YES\n" : "NO\n");
  }
}