const int max_n = 1 << 2;
modint fact[max_n], factinv[max_n];
void init_f() {
	fact[0] = modint(1);
	for (int i = 0; i < max_n - 1; i++) {
		fact[i + 1] = fact[i] * modint(i + 1);
	}
	factinv[max_n - 1] = modint(1) / fact[max_n - 1];
	for (int i = max_n - 2; i >= 0; i--) {
		factinv[i] = factinv[i + 1] * modint(i + 1);
	}
}
modint comb(int a, int b) {
	if (a < 0 || b < 0 || a < b)return 0;
	return fact[a] * factinv[b] * factinv[a - b];
}
modint combP(int a, int b) {
	if (a < 0 || b < 0 || a < b)return 0;
	return fact[a] * factinv[a - b];
}

int gcd(int a, int b) {
	if (a < b)swap(a, b);
	while (b) {
		int r = a % b; a = b; b = r;
	}
	return a;
}


void solve() {
	int x1, x2; cin >> x1 >> x2;
	int c1, c2; cin >> c1 >> c2;
	int w1, w2; cin >> w1 >> w2;
	if (w1 > w2) {
		swap(w1, w2);
		swap(c1, c2);
	}
	ll ans = 0;
	for (ll i = 0; i <= c1; i++) {
		if (i * w1 > x1)continue;
		ll num = i;
		ll r1 = c1 - i;
		ll r2 = c2;
		ll z = x1 - (i * w1);
		ll m = min(z / w2, r2);
		r2 -= m; num += m;
		z = x2;
		m = min(z / w1, r1);
		r1 -= m; num += m; z -= m * w1;
		m = min(z / w2, r2);
		num += m;
		ans = max(ans, num);
	}
	cout << ans << "\n";
}

