int p[1005], where[1005];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("input.txt", "r", stdin);
    //freopen("output.txt", "w", stdout);

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        for (int i=0; i<n; i++) {
            cin >> p[i];
            where[p[i]] = i;
        }
        for (int i=n; i>0; i--)
            if (where[i] - 1 >= 0 && p[where[i]-1] < i && where[i] + 1 < n && p[where[i]+1] < i) {
                cout << "YES\n" << where[i] << " " << where[i] + 1 << " " << where[i] + 2 << "\n";
                goto done;
            }
        cout << "NO\n";
        done:;
    }

    return 0;
}