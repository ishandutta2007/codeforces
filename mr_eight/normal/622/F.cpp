const long long mod=1000000007;
long long pw(long long x,long long p)
{
    long long res=1;
    for(;p;p>>=1,x=x*x%mod)
        if(p&1)res=res*x%mod;
    return res;
}
long long getm(long long top,long long bot) {
	return (top*pw(bot,mod-2))%mod;
}
