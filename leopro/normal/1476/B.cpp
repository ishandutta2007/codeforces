void solve() {
    autoint n, k;
    vector<int> p(n);
    cin >> p;
    auto ok = [&](int sm){
        for (int i = 1; i < p.size(); ++i){
            sm += p[i - 1];
            if (p[i] * 100 > sm * k) return false;
        }
        return true;
    };
    ll l = -1, r = 1e12;
    while (r - l > 1){
        ll m = (l + r) / 2;
        (ok(m) ? r : l) = m;
    }
    cout << r << '\n';
}