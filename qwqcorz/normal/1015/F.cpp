#include<bits/stdc++.h>
using namespace std;
const int N=205;
const int p=1e9+7;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
inline void add(int &x,int y)
{
	x+=y;
	if (x>=p) x-=p;
}
char a[N];
int dp[N][N][N],kmp[N],nxt[N][2];
void build(int m)
{
	for (int i=2,j=0;i<=m;i++)
	{
		while (j&&a[j+1]!=a[i]) j=kmp[j];
		if (a[j+1]==a[i]) j++;
		kmp[i]=j;
	}
	for (int i=0;i<m;i++)
	{
		int x=i;
		while (x&&a[x+1]!='(') x=kmp[x];
		if (a[x+1]=='(') x++;
		nxt[i][0]=x;
		x=i;
		while (x&&a[x+1]!=')') x=kmp[x];
		if (a[x+1]==')') x++;
		nxt[i][1]=x;
	}
	nxt[m][0]=nxt[m][1]=m;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read()*2;
	scanf("%s",a+1);
	int m=strlen(a+1);
	build(m);
	dp[0][0][0]=1;
	for (int i=0;i<n;i++)
	for (int j=0;j<=m;j++)
	for (int k=0;k<=i;k++)
	{
		if (k) add(dp[i+1][nxt[j][1]][k-1],dp[i][j][k]);
		add(dp[i+1][nxt[j][0]][k+1],dp[i][j][k]);
	}
	print(dp[n][m][0]);
	
	return 0;
}
