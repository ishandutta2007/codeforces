int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    string S; ll x,y; cin >> S >> x >> y;

    int n=S.size();
    ll sum=0;
    vector<int> pos;
    vector<vector<int>> cnt(2,vector<int>(n+1,0));
    for (int i=0;i<n;++i){
        for (int j=0;j<2;++j) cnt[j][i+1]=cnt[j][i];
        if (S[i]=='?') pos.emplace_back(i);
        else if (S[i]=='0') sum+=y*cnt[1][i],++cnt[0][i+1];
        else sum+=x*cnt[0][i],++cnt[1][i+1];
    }
    int m=pos.size();

    auto calc=[&](int a){
        vector<ll> left(m+1,0),right(m+1,0);
        for (int i=0;i<m;++i){
            left[i+1]=left[i]+y*cnt[a^1][pos[i]]+x*(cnt[a^1][n]-cnt[a^1][pos[i]+1]);
        }
        for (int i=m-1;i>=0;--i){
            right[i]=right[i+1]+x*cnt[a][pos[i]]+y*(cnt[a][n]-cnt[a][pos[i]+1]);
        }
        ll res=IINF;
        for (int i=0;i<=m;++i) res=min(res,left[i]+right[i]+x*i*(m-i));
        return res;
    };

    ll ans=IINF;
    for (int i=0;i<2;++i){
        ans=min(ans,calc(i));
        swap(x,y);
    }

    cout << ans+sum << '\n';
}