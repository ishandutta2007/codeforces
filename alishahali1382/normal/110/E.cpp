#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<int, pii> piii;
typedef pair<ll, ll> pll;
#define debug(x) cerr<<#x<<'='<<x<<endl;
#define debugp(x) cerr<<#x<<"= {"<<x.first<<", "<<x.second<<"}"<<endl;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 100010;

bool check(int w){
	while (w){
		if (w%10!=4 && w%10!=7) return true;
		w/=10;
	}
	return false;
}

ll n, m, b, x, y, w, t=1, ans, v, p, q;
vector<int> G[MAXN];
bool mark[MAXN];

int dfs(int node){
	int res=1;
	mark[node]=true;
	for (int i:G[node]){
		if (mark[i]) continue ;
		res+=dfs(i);
	}
	return res;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<n; i++){
		cin>>x>>y>>w;
		if (check(w)){
			G[x].pb(y);
			G[y].pb(x);
		}
	}
	for (int i=1; i<=n; i++){
		if (mark[i]) continue ;
		x=dfs(i);
		ans+=x*(n-x)*(n-x-1);
	}
	cout<<ans<<endl;
	return 0;
}
