template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int t;
  cin >> t;
  while (t--) {
    int n, m, x, y;
    cin >> n >> m >> x >> y;
    y = min(y, x * 2);
    vector<string> grid(n);
    cin >> grid;
    int ans = 0;
    auto deal = [&](int v) {
      ans += v / 2 * y;
      ans += v % 2 * x;
    };
    for (const auto& row : grid) {
      int cur = 0;
      for (int i = 0; i < m; ++i)
        if (row[i] == '*') {
          deal(cur);
          cur = 0;
        } else {
          ++cur;
        }
      deal(cur);
    }
    cout << ans << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}