use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        ask: [(usize, bytes); t],
    }
    for (_, s) in ask {
        let mut map = std::collections::BTreeMap::new();
        map.insert(0i32, 1u64);
        let mut sum = 0i32;
        let mut ans = 0;
        for (i, c) in s.iter().enumerate() {
            sum += (c - b'0') as i32;
            let r = i as i32 + 1;
            if let Some(&v) = map.get(&(sum - r)) {
                ans += v;
            }
            *map.entry(sum - r).or_insert(0) += 1;
        }
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}