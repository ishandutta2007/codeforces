int main()
{
    int T;
    cin>>T;
    while(T--)
    {
        int n,g,b;
        cin>>n>>g>>b;
        int tar=(n+1)/2;
        ll L=n,R=1e18,ans=-1;
        while(L<=R)
        {
            ll mid=L+R>>1;
            ll cnt=mid/(g+b);
            ll good=cnt*g;
            good+=min(1LL*g,mid%(g+b));
            if(good>=tar)
            {
                ans=mid;
                R=mid-1;
            }
            else L=mid+1;
        }
        assert(ans!=-1);
        print(ans);
    }
}