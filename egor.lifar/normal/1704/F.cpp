int n;
string s;
int dp[1005];
int used[1005];
int uk = 0;


void calcdp() {
	dp[0] = 0;
    dp[1] = 1;
    for (int i = 2; i < 1000; i++) {
        uk++;
        used[dp[i - 2]] = uk;
        for (int j = 0; j < i / 2 + 1; j++) {
            used[dp[i - j - 2] ^ dp[j - 1]] = uk;
        }
		while (used[dp[i]] == uk) {
			dp[i]++;
		}
    }
}


void solve() {
	cin >> n >> s;
	int bal = 0;
	for (auto x: s) {
		if (x == 'R') {
			bal++;
		} else {
			bal--;
		}
	}
	if (bal > 0) {
		cout << "Alice\n";
		return;
	} else if (bal < 0) {
		cout << "Bob\n";
		return;
	}
	vector<int> groups;
	int group = 0;
	for (int i = 1; i < n; i++) {
		if (s[i] == s[i - 1]) {
			if (group > 0) {
				groups.pb(group);
			}
			group = 0;
		} else {
			group++;
		}
	}
	if (group) {
		groups.pb(group);
	}
	int grundy = 0;
	for (auto val: groups) {
		while (val >= 1000) {
			val -= 34;
		}
		grundy ^= dp[val];
	}
	if (grundy) {
		cout << "Alice\n";
	} else {
		cout << "Bob\n";
	}
}


int main(){
	cin.tie(0)->sync_with_stdio(0);
	calcdp();
	int t;
	t = 1;
	cin >> t;
	while (t--) {
		solve();
	}
}