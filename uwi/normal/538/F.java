	void solve()
	{
		int n = ni();
		int[] a = na(n);
		int k = 1;
		int v = 0;
		for(;k*k <= n && k < n;k++){
			v = 0;
			for(int i = 1;i < n;i++){
				if(a[i] < a[(i-1)/k])v++;
			}
			out.print(v + " ");
		}
		for(;k < n;k++){
			// (i-1)/k != (i-1)/(k-1)
			// (i-1)/k<q<=(i-1)/(k-1)
			// (i-1)<qk
			// (i-1)>=q(k-1)
			// q(k-1)+1<=i<qk+1
			for(int q = 1;q <= n/(k-1);q++){
				for(int u = q*(k-1)+1;u < q*k+1 && u < n;u++){
					if(a[u] < a[(u-1)/(k-1)])v--;
					if(a[u] < a[(u-1)/k])v++;
				}
			}
			out.print(v + " ");
		}
		out.println();
	}
	
