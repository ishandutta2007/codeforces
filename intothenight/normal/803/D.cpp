mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int k;
	cin >> k;
	vi a;
	for(string t; cin >> t; ){
		int p = 0;
		for(int i = 0; i <= t.size(); i ++){
			if(i == t.size() || t[i] == '-'){
				a.push_back(i - p + 1);
				p = i + 1;
			}
		}	
	}
	a.back() --;
	function<int(int)> check = [&](int m){
		int p = 0;
		for(int i = 0; i < k; i ++){
			int sum = 0;
			while(p < a.size() && sum + a[p] <= m){
				sum += a[p ++];
			}
			if(p == a.size()){
				return true;
			}
		}
		return false;
	};
	int l = *max_element(all(a)) - 1, r = 1e6;
	while(r - l > 1){
		int m = l + r >> 1;
		if(check(m)){
			r = m;
		}
		else{
			l = m;
		}
	}
	cout << r;
	return 0;
}
