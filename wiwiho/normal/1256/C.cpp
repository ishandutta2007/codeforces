    vector<int> c(m), pos(m);
    int lst = 0;
    for(int i = 0; i < m; i++){
        cin >> c[i];
        pos[i] = lst + 1;
        lst = pos[i] + c[i] - 1;
    }

    lst = n + 1;
    for(int i = m - 1; i >= 0; i--){
        pos[i] += max(0, lst - (pos[i] + c[i] - 1) - d);
        lst = pos[i];
    }

    //printv(pos, cerr);
    if(pos[0] > d) return cout << "NO\n", 0;

    cout << "YES\n";

    vector<int> ans(n);
    for(int i = 0; i < m; i++){
        for(int j = pos[i]; j < pos[i] + c[i]; j++) ans[j - 1] = i + 1;
    }

    printv(ans, cout);

    return 0;
