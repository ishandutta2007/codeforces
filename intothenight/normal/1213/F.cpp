template<class T>
istream &operator>>(istream &in, deque<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, k;
	cin >> n >> k;
	vector<vi> p(n, vi(2)), ip(n, vi(2));
	for(int j = 0; j < 2; j ++){
		for(int i = 0; i < n; i ++){
			cin >> p[i][j], p[i][j] --;
			ip[p[i][j]][j] = i;
		}
	}
	int cnt = 0;
	vi assigned(n);
	function<void(int, int)> assign = [&](int u, int t){
		assigned[p[u][t]] = cnt;
		if(u && !assigned[p[u - 1][t]]) assign(u - 1, t);
		int v = ip[p[u][t]][!t];
		if(v && !assigned[p[v - 1][!t]]) assign(v - 1, !t);
	};
	for(int u = 0; u < n; u ++){
		if(!assigned[p[u][0]]){
			cnt ++;
			assign(u, 0);
		}
	}
	if(cnt < k){
		cout << "NO";
	}
	else{
		cout << "YES\n";
		for(auto x: assigned) x <= 26 ? cout << char(x + 'a' - 1) : cout << 'z';
	}
	return 0;
}

