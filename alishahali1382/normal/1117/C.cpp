const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010, LOG=20;

ll x1, y1, x2, y2, n, t, a, b, ans, s;
int X[MAXN];
int Y[MAXN];
char ch;

bool check(ll dx, ll dy, ll moves){ return abs(dx)+abs(dy)<=moves;}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>x1>>y1>>x2>>y2>>n;
	
	for (int i=1; i<=n; i++){
		cin>>ch;
		if (ch=='L') X[i]=-1;
		if (ch=='R') X[i]=1;
		if (ch=='D') Y[i]=-1;
		if (ch=='U') Y[i]=1;
	}
	ll sx=0, sy=0;
	for (int i=1; i<=n; i++) sx+=X[i];
	for (int i=1; i<=n; i++) sy+=Y[i];
	
	ll dwn=0, up=1000000000000000000ll;
	while (up-dwn>1){
		ll mid=(dwn+up)/2;
		ll dx=x1-x2, dy=y1-y2;
		dx+=sx*(mid/n);
		dy+=sy*(mid/n);
		
		for (int i=1; i<=(mid%n); i++) dx+=X[i], dy+=Y[i];
		
		if (check(dx, dy, mid)) up=mid;
		else dwn=mid;
	}
	if (up>=10000000000000000ll) up=-1;
	
	cout<<up<<'\n';
	
	return 0;
}
