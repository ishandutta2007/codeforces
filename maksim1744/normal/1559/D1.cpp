struct DSU {
    vector<int> rk;
    vector<int> p;
    int n;

    DSU(int n = 1) : n(n) {
        reset(n);
    }

    void reset(int n) {
        p.resize(n);
        iota(p.begin(), p.end(), 0);
        rk.assign(n, 1);
    }

    int par(int v) {
        return v == p[v] ? v : p[v] = par(p[v]);
    }

    bool un(int u, int v) {
        u = par(u);
        v = par(v);
        if (u == v) return false;
        if (rk[u] > rk[v]) swap(u, v);
        p[u] = v;
        rk[v] += rk[u];
        return true;
    }

    bool check(int u, int v) {
        return par(u) == par(v);
    }
};

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n, m1, m2;
    cin >> n >> m1 >> m2;
    DSU d1(n), d2(n);
    for (int i = 0; i < m1; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v;
        d1.un(u, v);
    }
    for (int i = 0; i < m2; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v;
        d2.un(u, v);
    }
    vector<pair<int, int>> ans;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < i; ++j) {
            if (!d1.check(i, j) && !d2.check(i, j)) {
                ans.eb(i, j);
                d1.un(i, j);
                d2.un(i, j);
            }
        }
    }

    cout << ans.size() << '\n';
    ++ans;
    for (auto p : ans)
        cout << p << '\n';

    return 0;
}