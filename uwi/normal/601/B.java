	void solve()
	{
		int n = ni(), Q = ni();
		int[] a = na(n);
		long[] b = new long[n-1];
		for(int i = 0;i < n-1;i++){
			b[i] = Math.abs(a[i+1] - a[i]);
		}
		int[] stack = new int[n+2];
		// 7 8 2 1 2
		for(int i = 0;i < Q;i++){
			int l = ni()-1, r = ni()-1;
			int sp = 0;
			long ret = 0;
			long sum = 0;
			for(int j = l;j <= r-1;j++){
				while(sp > 0 && b[stack[sp-1]] < b[j]){
					int ind = stack[sp-1];
					sum -= b[ind] * (ind - (sp == 1 ? l : stack[sp-2]+1) + 1);
					sp--;
				}
				stack[sp++] = j;
				sum += b[j] * (j - (sp == 1 ? l : stack[sp-2]+1) + 1);
				ret += sum;
			}
			out.println(ret);
		}
	}
	
