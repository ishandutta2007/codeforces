    int n, t;
    cin >> n >> t;
    rep(_, t) {
        int k;
        cin >> k;
        k--;
        int l = 0, r = n;
        while(r - l > 1) {
            int c = (l + r) / 2;
            int res = query(c);
            if (res <= k) l = c;
            else r = c;
        }
        answer(l);
    }
