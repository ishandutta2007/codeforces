int main(){
	cin.tie(0)->sync_with_stdio(0);
	auto kill = [](){
	    cout << "NO";
	    exit(0);
	};
	int n;
	cin >> n;
	vs a(n);
	cin >> a;
	vi next(26, -1), prev(26, -1), used(26, 1);
	for(auto &s: a){
		for(auto c: s){
			used[c - 'a'] = false;
		}
		for(int i = 1; i < s.size(); i ++){
			int x = s[i - 1] - 'a', y = s[i] - 'a';
			if(x == y || next[x] + 1 && next[x] - y || prev[y] + 1 && prev[y] - x){
				kill();
			}
			next[x] = y, prev[y] = x;
		}
	}
	vector<deque<int>> res;
	for(int i = 0; i < 26; i ++){
		if(!used[i]){
			used[i] = true;
			res.push_back({i});
			for(int u = next[i]; u != -1; u = next[u]){
				if(used[u]){
					kill();
				}
				used[u] = true;
				res.back().push_back(u);
			}
			for(int u = prev[i]; u != -1; u = prev[u]){
				if(used[u]){
					kill();
				}
				used[u] = true;
				res.back().push_front(u);
			}
		}
	}
	sort(all(res));
	for(auto s: res){
		for(auto i: s){
			cout << char(i + 'a');
		}
	}
	return 0;
}

