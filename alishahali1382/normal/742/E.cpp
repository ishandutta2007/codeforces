const int MAXN = 100010;

int n, m, k, u, v, x, y, t, a, b, ans;
int A[MAXN][2];
vector<int> G[MAXN*2];
int color[MAXN*2];

void dfs(int node, int c){
	color[node]=c;
	for (int i:G[node]) if (!color[i]) dfs(i, 3-c);
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n; i++){
		cin>>A[i][0]>>A[i][1];
		G[A[i][0]].pb(A[i][1]);
		G[A[i][1]].pb(A[i][0]);
	}
	for (int i=1; i<=n; i++){
		G[2*i-1].pb(2*i);
		G[2*i].pb(2*i-1);
	}
	for (int i=1; i<=2*n; i++) if (!color[i]) dfs(i, 1);
	for (int i=1; i<=n; i++) cout<<color[A[i][0]]<<' '<<color[A[i][1]]<<endl;
	
	
	return 0;
}
