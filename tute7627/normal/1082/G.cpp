template< typename flow_t >
struct Dinic {
  const flow_t INF;

  struct edge {
    ll to;
    flow_t cap;
    ll rev;
    bool isrev;
  };

  vector< vector< edge > > graph;
  vector< ll > min_cost, iter;

  Dinic(ll V) : INF(numeric_limits< flow_t >::max()), graph(V) {}

  void add_edge(ll from, ll to, flow_t cap) {
    graph[from].emplace_back((edge) {to, cap, (ll) graph[to].size(), false});
    graph[to].emplace_back((edge) {from, 0, (ll) graph[from].size() - 1, true});
  }

  bool bfs(ll s, ll t) {
    min_cost.assign(graph.size(), -1);
    queue< ll > que;
    min_cost[s] = 0;
    que.push(s);
    while(!que.empty() && min_cost[t] == -1) {
      ll p = que.front();
      que.pop();
      for(auto &e : graph[p]) {
        if(e.cap > 0 && min_cost[e.to] == -1) {
          min_cost[e.to] = min_cost[p] + 1;
          que.push(e.to);
        }
      }
    }
    return min_cost[t] != -1;
  }

  flow_t dfs(ll idx, const ll t, flow_t flow) {
    if(idx == t) return flow;
    for(ll &i = iter[idx]; i < graph[idx].size(); i++) {
      edge &e = graph[idx][i];
      if(e.cap > 0 && min_cost[idx] < min_cost[e.to]) {
        flow_t d = dfs(e.to, t, min(flow, e.cap));
        if(d > 0) {
          e.cap -= d;
          graph[e.to][e.rev].cap += d;
          return d;
        }
      }
    }
    return 0;
  }

  flow_t max_flow(ll s, ll t) {
    flow_t flow = 0;
    while(bfs(s, t)) {
      iter.assign(graph.size(), 0);
      flow_t f = 0;
      while((f = dfs(s, t, INF)) > 0) flow += f;
    }
    return flow;
  }

  void output() {
    for(ll i = 0; i < graph.size(); i++) {
      for(auto &e : graph[i]) {
        if(e.isrev) continue;
        auto &rev_e = graph[e.to][e.rev];
        cout << i << "->" << e.to << " (flow: " << rev_e.cap << "/" << e.cap + rev_e.cap << ")" << endl;
      }
    }
  }
}; 
