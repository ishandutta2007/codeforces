const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

int n, m, k, u, v, x, y, t, a, b, ans;
vector<int> G[MAXN];

pii dfs(int node, int par, int bad){
	int res=0, out=node;
	for (int v:G[node]){
		if (v==par || v==bad) continue ;
		pii p=dfs(v, node, bad);
		if (p.first+1>res){
			res=p.first+1;
			out=p.second;
		}
	}
	return {res, out};
}

int f(int node, int bad){
	int x=dfs(node, 0, bad).second;
	return dfs(x, 0, bad).first;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=0; i<n-1; i++){
		cin>>u>>v;
		G[u].pb(v);
		G[v].pb(u);
	}
	
	for (int i=1; i<=n; i++) for (int j:G[i]) if (i<j) ans=max(ans, f(i, j)*f(j, i));
	cout<<ans<<endl;
	
	return 0;
}
