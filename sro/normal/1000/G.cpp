#include <bits/stdc++.h>
using namespace std;

int n,m;
int a[300005];
vector<pair<int,int> > nei[300005];
vector<int> son[300005];
int ceng[300005];
int fa[20][300005];
long long facos[300005],fac[20][300005];
long long dp[300005],dp2[300005];
long long ds[300005];

void dfs(int now,int lst){
	ceng[now]=ceng[lst]+1;
	fa[0][now]=lst;
	for(int i=1;i<20;i++){
		fa[i][now]=fa[i-1][fa[i-1][now]];
	}
	for(int i=0;i<nei[now].size();i++){
		int to=nei[now][i].first;
		if(to==lst){
			continue;
		}
		son[now].push_back(to);
		dfs(to,now);
		facos[to]=nei[now][i].second;
	}
}

void dfsdown(int now){
	ds[now]=0;
	for(int i=0;i<son[now].size();i++){
		int to=son[now][i];
		dfsdown(to);
		ds[now]+=dp[to];
	}
	dp[now]=max(0ll,ds[now]+a[now]-2*facos[now]);
}

void dfsup(int now){
	dp2[now]=(!fa[0][now]?0ll:max(0ll,dp2[fa[0][now]]+ds[fa[0][now]]-dp[now]+a[fa[0][now]]-2*facos[now]));
	for(int i=0;i<son[now].size();i++){
		dfsup(son[now][i]);
	}
}

int lca(int a,int b){
	if(ceng[a]>ceng[b]){
		swap(a,b);
	}
	int toup=ceng[b]-ceng[a];
	for(int i=0;i<20;i++){
		if((toup>>i)&1){
			b=fa[i][b];
		}
	}
	if(a==b){
		return a;
	}
	for(int i=19;~i;i--){
		if(fa[i][a]!=fa[i][b]){
			a=fa[i][a];
			b=fa[i][b];
		}
	}
	return fa[0][a];
}

long long calc(int &a,int ceng){
	long long ans=0;
	for(int i=19;~i;i--){
		if((ceng>>i)&1){
			ans+=fac[i][a];
			a=fa[i][a];
		}
	}
	return ans;
}

int main(){
	scanf("%d%d",&n,&m);
	for(int i=1;i<=n;i++){
		scanf("%d",a+i);
	}
	for(int i=1;i<n;i++){
		int a,b,c;
		scanf("%d%d%d",&a,&b,&c);
		nei[a].push_back(make_pair(b,c));
		nei[b].push_back(make_pair(a,c));
	}
	dfs(1,0);
	dfsdown(1);
	dfsup(1);
	for(int i=1;i<=n;i++){
		fac[0][i]=(!fa[0][i]?0:ds[fa[0][i]]-dp[i]+a[fa[0][i]]-facos[i]);
	}
	for(int i=1;i<20;i++){
		for(int j=1;j<=n;j++){
			fac[i][j]=fac[i-1][j]+fac[i-1][fa[i-1][j]];
		}
	}
	while(m--){
		int u,v;
		scanf("%d%d",&u,&v);
		int y=lca(u,v);
		long long ans=0;
		if(u!=y){
			ans+=a[u]+ds[u]+calc(u,ceng[u]-ceng[y]-1);
		}
		if(v!=y){
			ans+=a[v]+ds[v]+calc(v,ceng[v]-ceng[y]-1);
		}
		ans+=(a[y]+dp2[y]+ds[y]-(u==y?0:dp[u]+facos[u])-(v==y?0:dp[v]+facos[v]));
		printf("%lld\n",ans);
	}
	return 0;
}