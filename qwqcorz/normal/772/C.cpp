#include<bits/stdc++.h>
using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
bool can[N];
vector<int>a[N],ans;
int exgcd(int a,int b,int &x,int &y)
{
	if (!b) return x=1,y=0,a;
	int g=exgcd(b,a%b,x,y),X=x,Y=y;
	x=Y;
	y=X-a/b*Y;
	return g;
}
int dp[N],to[N];

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read(),x,y;
	memset(can,1,sizeof(can));
	for (int i=1;i<=n;i++) can[read()]=0;
	for (int i=1;i<m;i++) if (can[i]) a[exgcd(i,m,x,y)].push_back(i);
	for (int i=m-1;i>=1;i--)
	{
		for (int j=i;j<m;j+=i)
		if (dp[j]>dp[i])
		{
			dp[i]=dp[j];
			to[i]=j;
		}
		dp[i]+=a[i].size();
	}
	for (int i=1;i;i=to[i])
	for (int j:a[i]) ans.push_back(j);
	if (can[0]) ans.push_back(0);
	print(ans.size());
	int last=1;
	for (int i=0;i<(int)ans.size();i++)
	{
		int g=exgcd(last,m,x,y);
		x=(x%(m/g)+(m/g))%(m/g);
		print(1LL*ans[i]*x/g%m,' ');
		last=ans[i];
	}
	
	return 0;
}
