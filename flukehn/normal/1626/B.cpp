string solve(){
  string s;
  cin>>s;
  int n=s.size();
  int i;
  auto F=[](char c){return c-'0';};
  for(i=n-2;i>=0;--i){
    if(F(s[i])+F(s[i+1]) >= 10) break;
  }
  auto G=[&](int i){
    return s.substr(0,i) + to_string(F(s[i])+F(s[i+1])) + s.substr(i+2, n-i-2);
  };
  if(i>=0) return G(i);
  for(i=0;i<n-1;++i)
    if(F(s[i+1])) break;
  if(i<n-1)
    return G(i);
  return G(0);
}
