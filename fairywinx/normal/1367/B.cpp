void solve() {
	int n;
	cin >> n;
	int kol[2] = {0, 0};
	for(int i = 0; i < n; ++i) {
		int a;
		cin >> a;
		if (a % 2 == 1 && i % 2 == 0)
			++kol[0];
		else if (a % 2 == 0 && i % 2 == 1)
			++kol[1];
	}
	if (kol[0] != kol[1])
		cout << -1 << '\n';
	else
		cout << kol[0] << '\n';
}

