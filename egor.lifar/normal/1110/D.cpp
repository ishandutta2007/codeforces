const int MAXN = 1000228;


int n, m;
int a[MAXN];
int cnt[MAXN];
int dp[MAXN][4][4];

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	//read(FILENAME);
	cin >> n >> m;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
		cnt[a[i]]++;
	}
	for (int i = 1; i <= m; i++) {
		for (int t = 0; t < 4; t++) {
			for (int t1 = 0; t1 < 4; t1++) {
				dp[i][t][t1] = -1e9;
			}
		}
	}
	dp[1][0][0] = 0;
	for (int i = 1; i <= m; i++) {
        for (int prev = 0; prev <= 3; prev++) {
        	for (int prev2 = 0; prev2 <= 3; prev2++) {
				for (int cur = 0; cur <= min(3, cnt[i]); cur++) {
					if (prev + prev2 + cur <= cnt[i]) {
						if (cur != 0 && i >= m - 1) {
							continue;
						}
						chkmax(dp[i + 1][cur][prev], dp[i][prev][prev2] + cur + (cnt[i] - cur - prev2 - prev) / 3);
					}
				}
        	}
        }
	}
	int ans = 0;
	for (int i = 0; i < 4; i++) {
		for (int j1 = 0; j1 < 4; j1++) {
			chkmax(ans, dp[m + 1][i][j1]);
		}	
	}
	cout << ans << '\n';
}