ll cnt[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n; i++) cin>>A[i];
	cnt[1]=1;
	if (A[1]==1) cnt[1]++;
	for (int i=2; i<=n; i++){
		if (A[i-1]+1==A[i]) cnt[i]=cnt[i-1]+1;
		else cnt[i]=1;
		if (i==n && A[i]==1000) cnt[i]++;
		mx=max(mx, cnt[i]);
	}
	cout<<mx-2<<endl;
	
	return 0;
}
