// ============= Solution ============= //
int main() {
   vector<ll> fac(21);
   fac[0] = 1;
   for (int i = 1; i <= 20; ++i) {
      fac[i] = i * fac[i - 1];
   }

   int T;
   cin >> T;
   while (T--) {
      ll n; cin >> n;
      int ans = __builtin_popcountll(n);
      for (int i = 0; i < (1 << 21); i += 8) {
         ll cur = n;
         for (int j = 3; j < 21; ++j) {
            if ((i >> j) & 1) {
               cur -= fac[j];
            }
         }
         int val = __builtin_popcount(i) + __builtin_popcountll(cur);
         ans = min(ans, val);
      }
      cout << ans << "\n";
   }

}

