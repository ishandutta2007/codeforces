int n, nex[MX], dp[MX];
string s;
vi pack;

bool ok(int x) {
    dp[0] = nex[0];
    F0R(i,sz(pack)) {
        if (dp[i] < pack[i]-x) return 0;
        dp[i+1] = nex[pack[i]+1];
        if (i && pack[i]-x <= dp[i-1]) ckmax(dp[i+1],nex[min(n,pack[i-1]+x+1)]);
        if (dp[i] > pack[i]) ckmax(dp[i+1],nex[min(n,pack[i]+x+1)]);
    }
    return dp[sz(pack)] == n;
}

