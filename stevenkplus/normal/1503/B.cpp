int main() { // plan out your code a bit more!

   int n;
   cin >> n;
   vector<vector<bool>> vis(n + 1, vector<bool>(n + 1));
   for (int turn = 0; turn < n * n; ++turn) {
      int a;
      cin >> a;
      bool found = false;
      auto go = [&](int b, int i, int j) {
         cout << b << " " << i << " " << j << endl;
         vis[i][j] = true;
         found = true;
      };

      for (int i = 1; i <= n; ++i) {
         for (int j = 1; j <= n; ++j) {
            if (found) continue;
            if (vis[i][j]) continue;
            if ((i + j) % 2 == 0) {
               if (a != 1) {
                  go(1, i, j);
               }
            } else {
               if (a != 2) {
                  go(2, i, j);
               }
            }
         }
      }

      for (int i = 1; i <= n; ++i) {
         for (int j = 1; j <= n; ++j) {
            if (found) continue;
            if (vis[i][j]) continue;
            go(3, i, j);
         }
      }
   }
}

