int _gcd(int a, int b) {
  if (a == 0) return b;
  return _gcd(b % a, a);
}

int main() {
  int T;
  cin >> T;
  for (int t = 1; t <= T; ++t) {
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;
    vector<int> cnts(26);
    for (char c: s) {
      ++cnts[c - 'a'];
    }
    int ans = 0;
    for (int len = 1; len <= n; ++len) {
      int need = _gcd(len, k);
      int thres = len / need;
      int val = 0;
      for (int i = 0; i < 26; ++i) {
        val += cnts[i] / thres;
      }
      if (val >= need) {
        ans = len;
      }
    }
    cout << ans << "\n";
  }
}

