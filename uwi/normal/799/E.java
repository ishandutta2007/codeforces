	void solve()
	{
		int n = ni(), m = ni(), K = ni();
		int[] a = na(n);
		long[] ai = new long[n];
		for(int i = 0;i < n;i++)ai[i] = (long)a[i]<<32|i;
		int[] like = new int[n];
		for(int L = ni(); L > 0;L--){
			like[ni()-1] |= 1;
		}
		for(int L = ni(); L > 0;L--){
			like[ni()-1] |= 2;
		}
		ai = radixSort(ai);
		int[] iord = new int[n];
		for(int i = 0;i < n;i++)iord[(int)ai[i]] = i;
		
		int[][] asc = new int[4][n];
		int[] ap = new int[4];
		for(int i = 0;i < n;i++){
			int id = (int)ai[i];
			asc[like[id]][ap[like[id]]++] = i;
		}
		
		int[] ft = buildFenwick(n, 1);
		long[] sft = new long[n+3];
		
		for(int i = 0;i < n;i++){
			addFenwick(sft, i, ai[i]>>>32);
		}
		
		long base = 0;
		int s = Math.min(K, ap[3]);
		for(int i = 0;i < s;i++){
			addFenwick(ft, asc[3][i], -1);
			base += ai[asc[3][i]]>>>32;
			addFenwick(sft, asc[3][i], -(ai[asc[3][i]]>>>32));
		}
		int t = K-s;
		if(t > ap[1] || t > ap[2]){
			out.println(-1);
			return;
		}
		for(int i = 0;i < t;i++){
			addFenwick(ft, asc[1][i], -1);
			base += ai[asc[1][i]]>>>32;
			addFenwick(sft, asc[1][i], -(ai[asc[1][i]]>>>32));
			addFenwick(ft, asc[2][i], -1);
			base += ai[asc[2][i]]>>>32;
			addFenwick(sft, asc[2][i], -(ai[asc[2][i]]>>>32));
		}
		
		long ret = Long.MAX_VALUE;
		while(s >= 0){
			int all = s+t+t;
			if(all > m)break;
//			tr(m-all, base, firstGEFenwick(ft, m-all), restoreFenwick(ft));
//			tr(m-all,  firstGEFenwick(ft, m-all), restoreFenwick(sft));
			long val = base + sumFenwick(sft, firstGEFenwick(ft, m-all));
			ret = Math.min(ret, val);
			
			if(s == 0)break;
			{
				int i = s-1;
				addFenwick(ft, asc[3][i], 1);
				base -= ai[asc[3][i]]>>>32;
				addFenwick(sft, asc[3][i], (ai[asc[3][i]]>>>32));
			}
			{
				int i = t;
				if(i >= ap[1] || i >= ap[2])break;
				addFenwick(ft, asc[1][i], -1);
				base += ai[asc[1][i]]>>>32;
				addFenwick(sft, asc[1][i], -(ai[asc[1][i]]>>>32));
				addFenwick(ft, asc[2][i], -1);
				base += ai[asc[2][i]]>>>32;
				addFenwick(sft, asc[2][i], -(ai[asc[2][i]]>>>32));
			}
			
			s--;
			t++;
		}
		if(ret == Long.MAX_VALUE){
			out.println(-1);
		}else{
			out.println(ret);
		}
	}
	
	public static int sumFenwick(int[] ft, int i)
	{
		int sum = 0;
		for(i++;i > 0;i -= i&-i)sum += ft[i];
		return sum;
	}
	
	public static void addFenwick(int[] ft, int i, int v)
	{
		if(v == 0 || i < 0)return;
		int n = ft.length;
		for(i++;i < n;i += i&-i)ft[i] += v;
	}
	
	public static int firstGEFenwick(int[] ft, int v)
	{
		if(v <= 0)return -1;
		int i = 0, n = ft.length;
		for(int b = Integer.highestOneBit(n);b != 0;b >>= 1){
			if((i|b) < n && ft[i|b] < v){
				i |= b;
				v -= ft[i];
			}
		}
		return i;
	}
	
	public static int[] buildFenwick(int n, int v)
	{
		int[] ft = new int[n+1];
		Arrays.fill(ft, 1, n+1, v);
		for(int k = 2, h = 1;k <= n;k*=2, h*=2){
			for(int i = k;i <= n;i+=k){
				ft[i] += ft[i-h];
			}
		}
		return ft;
	}
	
	public static int[] restoreFenwick(int[] ft)
	{
		int n = ft.length-1;
		int[] ret = new int[n];
		for(int i = 0;i < n;i++)ret[i] = sumFenwick(ft, i);
		for(int i = n-1;i >= 1;i--)ret[i] -= ret[i-1];
		return ret;
	}

	
	public static long sumFenwick(long[] ft, int i)
	{
		long sum = 0;
		for(i++;i > 0;i -= i&-i)sum += ft[i];
		return sum;
	}
	
	public static void addFenwick(long[] ft, int i, long v)
	{
		if(v == 0)return;
		int n = ft.length;
		for(i++;i < n;i += i&-i)ft[i] += v;
	}
	
	public static int firstGEFenwick(long[] ft, long v)
	{
		int i = 0, n = ft.length;
		for(int b = Integer.highestOneBit(n);b != 0;b >>= 1){
			if((i|b) < n && ft[i|b] < v){
				i |= b;
				v -= ft[i];
			}
		}
		return i;
	}
	
	public static long[] restoreFenwick(long[] ft)
	{
		int n = ft.length-1;
		long[] ret = new long[n];
		for(int i = 0;i < n;i++)ret[i] = sumFenwick(ft, i);
		for(int i = n-1;i >= 1;i--)ret[i] -= ret[i-1];
		return ret;
	}
	
	public static int findGFenwick(long[] ft, long v)
	{
		int i = 0;
		int n = ft.length;
		for(int b = Integer.highestOneBit(n);b != 0 && i < n;b >>= 1){
			if(i + b < n){
				int t = i + b;
				if(v >= ft[t]){
					i = t;
					v -= ft[t];
				}
			}
		}
		return v != 0 ? -(i+1) : i-1;
	}
	
	public static long[] buildFenwick(long[] a)
	{
		int n = a.length;
		long[] ft = new long[n+1];
		System.arraycopy(a, 0, ft, 1, n);
		for(int k = 2, h = 1;k <= n;k*=2, h*=2){
			for(int i = k;i <= n;i+=k){
				ft[i] += ft[i-h];
			}
		}
		return ft;
	}
	
