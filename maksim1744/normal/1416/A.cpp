const int inf = 1e9;

void test_case(int test) {
    int n;
    cin >> n;
    vector<int> a(n);
    cin >> a;
    vector<vector<int>> where(n + 1);
    for (int i = 0; i < n; ++i) {
        where[a[i]].pb(i);
    }
    vector<int> ans(n + 2, inf);
    for (int i = n; i >= 1; --i) {
        if (where[i].empty()) continue;
        int mx = max(where[i][0], n - where[i].back() - 1);
        for (int j = 0; j + 1 < where[i].size(); ++j)
            mx = max(mx, where[i][j + 1] - where[i][j] - 1);
        ans[mx + 1] = i;
    }
    for (int i = 1; i < ans.size(); ++i)
        ans[i] = min(ans[i], ans[i - 1]);
    for (int i = 1; i <= n; ++i) {
        if (ans[i] == inf) ans[i] = -1;
        cout << ans[i] << ' ';
    }
    cout << '\n';
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int T;
    cin >> T;
    for (int test = 1; test <= T; ++test) {
        test_case(test);
    }

    return 0;
}