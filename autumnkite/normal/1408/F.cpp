#include <bits/stdc++.h>

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int n;
	std::cin >> n;
	std::vector<int> v;
	for (int i = 0; (1 << i) <= n; ++i) {
		if (n >> i & 1) {
			v.push_back(1 << i);
		}
	}
	std::reverse(v.begin(), v.end());
	std::reverse(v.begin() + 1, v.end());

	std::vector<std::pair<int, int>> ans;
	std::function<void(int, int)> solve = [&](int l, int r) {
		if (l + 1 == r) {
			return;
		}
		int mid = (l + r + 1) >> 1;
		solve(l, mid);
		solve(mid, r);
		for (int i = 0; i < mid - l; ++i) {
			ans.emplace_back(l + i, mid + i);
		}
	};
	int now = 0;
	for (int x : v) {
		solve(now, now + x);
		now += x;
	}
	int len = 1;
	for (int i = 1; i < (int)v.size(); ++i) {
		while (len < v[i]) {
			for (int j = v[0] - len; j < v[0]; ++j) {
				ans.emplace_back(j - len, j);
			}
			len <<= 1;
		}
		for (int j = 0; j < len; ++j) {
			ans.emplace_back(v[0] - len + j, v[0] + j);
		}
		v[0] += len;
		len <<= 1;
	}
	std::cout << ans.size() << "\n";
	for (auto [x, y] : ans) {
		std::cout << x + 1 << " " << y + 1 << "\n";
	}
}