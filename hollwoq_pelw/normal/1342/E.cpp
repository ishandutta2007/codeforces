int binpow(int a, int x){
    int res = 1;
    while(x){
        if (x & 1) res = mul(res, a);
        a = mul(a, a);
        x /= 2;
    }
    return res;
}

int inv(int a){
    return binpow(a, mod1 - 2);
}

int fact[MAXM];
int C(int n, int k){
    return mul(fact[n], inv(mul(fact[k], fact[n-k])));
}

