void dfs(int v, vector<vector<int>>& g, vector<int>& used) {
    used[v] = true;
    for (auto x : g[v]) {
        if (!used[x]) {
            dfs(x, g, used);
        }
    }
}

void solve() {
    int n, m;
    cin >> n >> m;
    vector<vector<int>> g(n);
    vector<int> color(n, -1);
    for (int i = 0; i < m; i++) {
        int x, y;
        cin >> x >> y;
        x--, y--;
        g[x].pb(y);
        g[y].pb(x);
    }
    vector<int> used(n, false);
    dfs(0, g, used);
    for (auto x : used) {
        if (x == false) {
            cout << "NO\n";
            return;
        }
    }
    queue<int> st;
    vector<int> ans;
    st.push(0);
    while (st.size() > 0) {
        int v = st.front();
        st.pop();
        if (color[v] != -1) continue;
        color[v] = 0;
        ans.pb(v);
        for (auto x : g[v]) {
            if (color[x] == -1) {
                color[x] = 1;
                for (auto y : g[x]) {
                    if (color[y] == -1) {
                        st.push(y);
                    }
                }
            }
        }
    }
    cout << "YES\n" << (int) ans.size() << '\n';
    for (auto x : ans) cout << x + 1 << " "; cout << '\n';
}

signed main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int q;
    cin >> q;
    while (q--) {
        solve();
    }
}