void solve() {
	string s;
	cin >> s;
	vector<int> a(s.size());
	for(int i = 0; i < s.size(); ++i)
		a[i] = s[i] - '0';
	int ans = s.size();
	for(int a1 = 0; a1 < 10; ++a1) {
		for(int a2 = 0; a2 < 10; ++a2) {
			int kol = 0;
			int type = 0;
			for(int i = 0; i < s.size(); ++i) {
				if (type == 0 && a[i] == a1) 
					type = 1;
				else if (type == 1 && a[i] == a2)
					++kol, type = 0;
			}
			ans = min(ans, s.size() - kol * 2);
		}	
		int kol = 0;
		for(int i = 0; i < s.size(); ++i) {
			if (a[i] == a1)
				++kol;
		}
		ans = min(ans, s.size() - kol);
	}
	cout << ans << '\n';
}

