string solve(const string &S) {
    const int N = S.size();
    // string A, B;
    // int l = 0, r = N - 1;
    // REP(_, N) {
    //     if (S[l] < S[r] or l == r) {
    //         A += S[l++];
    //     } else if (S[l] > S[r]) {
    //         A += S[r--];
    //     } else {
    //         if (S[l + 1] < S[r - 1]) {
    //             A += S[r--];
    //         } else {
    //             A += S[l++];
    //         }
    //     }
    //     swap(A, B);
    // }
    // reverse(ALL(A));
    // reverse(ALL(B));
    // if (A < B) return "Alice";
    // if (A > B) return "Bob";
    // return "Draw";

    vector wins(N + 2, vector<int>(N + 2));
    vector draw_or_better(N + 2, vector<int>(N + 2));

    for (int w = 2; w <= N; w += 2) {
        REP(l, N) {
            int r = l + w;
            if (r > N) continue;

            const char cl = S.at(l), cr = S.at(r - 1);
            const char cll = S.at(l + 1), crr = S.at(r - 2);

            auto set_win = [&]() { wins.at(l).at(r) = draw_or_better.at(l).at(r) = 1; };

            if (w == 2) {
                if (cl != cr) set_win();
                draw_or_better.at(l).at(r) = 1;
                continue;
            }

            if (wins.at(l + 1).at(r - 1) or (draw_or_better.at(l + 1).at(r - 1) and cl < cr)) {
                if (wins.at(l + 2).at(r) or (draw_or_better.at(l + 2).at(r) and cl < cll)) {
                    set_win();
                }
            }

            if (wins.at(l + 1).at(r - 1) or (draw_or_better.at(l + 1).at(r - 1) and cr < cl)) {
                if (wins.at(l).at(r - 2) or (draw_or_better.at(l).at(r - 2) and cr < crr)) {
                    set_win();
                }
            }

            if (wins.at(l + 1).at(r - 1) or (draw_or_better.at(l + 1).at(r - 1) and cl <= cr)) {
                if (wins.at(l + 2).at(r) or (draw_or_better.at(l + 2).at(r) and cl <= cll)) {
                    draw_or_better.at(l).at(r) = 1;
                }
            }

            if (wins.at(l + 1).at(r - 1) or (draw_or_better.at(l + 1).at(r - 1) and cr <= cl)) {
                if (wins.at(l).at(r - 2) or (draw_or_better.at(l).at(r - 2) and cr <= crr)) {
                    draw_or_better.at(l).at(r) = 1;
                }
            }
        }
    }
    for (auto w : wins) dbg(w);
    for (auto d : draw_or_better) dbg(d);

    if (wins[0][N]) return "Alice";
    if (draw_or_better[0][N]) return "Draw";
    return "Bob";
}

