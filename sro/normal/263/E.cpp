#include <bits/stdc++.h>
using namespace std;

int a[2005][2005];
int n,m,k;
long long qzh[2005][2005];

int main(){
	scanf("%d%d%d",&n,&m,&k);
	int N=0,M=0;
	for(int i=1;i<=n;i++){
		for(int j=1;j<=m;j++){
			int x=m+i-j,y=i+j-1;
			scanf("%d",a[x]+y);
			N=max(N,x);
			M=max(M,y);
		}
	}
	for(int i=1;i<=N;i++){
		for(int j=1;j<=M;j++){
			qzh[i][j]=a[i][j]+qzh[i][j-1]+qzh[i-1][j]-qzh[i-1][j-1];
		}
	}
	long long mxx=-1;
	int mi,mj;
	for(int i=k;i<=n-k+1;i++){
		for(int j=k;j<=m-k+1;j++){
			int x=m+i-j,y=i+j-1;
			long long ans=0;
			for(int f=0;f<k;f++){
				int l=i-f,r=i+f;
				int x1=m+l-j,y1=l+j-1;
				int x2=m+r-j,y2=r+j-1;
				ans+=qzh[x2][y2]-qzh[x1-1][y2]-qzh[x2][y1-1]+qzh[x1-1][y1-1];
			}
			if(ans>mxx){
				mxx=ans;
				mi=i;
				mj=j;
			}
		}
	}
	printf("%d %d\n",mi,mj);
	return 0;
}