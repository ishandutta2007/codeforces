#include <iostream>
#include <cmath>
#include<bits/stdc++.h>

using namespace std;
#define int long long

int mod=998244353;

int Power(int $a,int $b)
{
    int $result=1; $a%=mod;
    while($b) {   if($b%2==1)  $result*=$a;     $a*=$a;     $a%=mod;     $result%=mod;    $b/=2; }
    return $result;
}


void solve()
{
    int n,m,ans;
    cin>>n>>m;

    n+=m;
    ans=Power(2,n);
    cout<<ans<<endl;
}

int32_t main()
{
    ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
    solve();
    return 0;
}