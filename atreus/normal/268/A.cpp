#include <bits/stdc++.h>

using namespace std;

int maximum(int n, int m){
    if (n < m)
        return m;
    else
        return n;
}

int a[110], b[110];

int main(){
    ios_base::sync_with_stdio(false);
    int n, ho, aw, ans = 0;
    cin >> n;
    for (int i = 1; i <= n; i++){
        cin >> ho >> aw;
        a[ho]++;
        b[aw]++;
    }
    for (int i = 1; i <= 110; i++){
            ans += a[i] * b[i];
    }
    cout << ans;
}