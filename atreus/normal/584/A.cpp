#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    int n, r;
    cin >> n >> r;
    string s;
    if (r == 10){
        if (n == 1)
            cout << -1;
        else {
            for (int i = 1; i < n; i++)
                s += '1';
            s += '0';
            cout << s << endl;
        }
        return 0;
    }
    for (int i = 1; i <= n; i++)
        s += r + '0';
    cout << s << endl;
}