int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<int> a(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }
  vector<int> b(n);
  for (int i = 0; i < n; i++) {
    cin >> b[i];
  }
  auto as = a;
  sort(as.begin(), as.end());
  int mx = *max_element(a.begin(), a.end());
  Mint ans = 0;
  for (int x = mx; x <= mx + 1; x++) {
    vector<pair<int, int>> all;
    vector<pair<int, int>> uni;
    int ppl = n;
    Mint coeff = 1;
    int L, R;
    if (x == mx) {
      L = mx;
      R = mx;
    } else {
      L = mx + 1;
      R = mx + n;
    }
    for (int i = 0; i < n; i++) {
      if (b[i] == 0) {
        int cnt = (int) (lower_bound(as.begin(), as.end(), a[i]) - as.begin());
        int have = a[i] + cnt;
        int need = have; // - x
        // need - x >= 0
        // need - x <= cnt
        R = min(R, need);
        L = max(L, need - cnt);
        all.emplace_back(cnt, need);
      } else {
        if (a[i] == x) {
          coeff *= ppl;
          ppl -= 1;
        } else {
          int cnt = (int) (upper_bound(as.begin(), as.end(), a[i]) - as.begin());
          int have = a[i] + (n - cnt);
          int need = -have; // + x
          // need + x >= 0
          // need + x <= cnt - 1
          L = max(L, -need);
          R = min(R, cnt - 1 - need);
          uni.emplace_back(cnt, need);
        }
      }
    }
    if (L > R) {
      continue;
    }
    sort(uni.begin(), uni.end());
    if (unique(uni.begin(), uni.end()) != uni.end()) {
      continue;
    }
    sort(all.begin(), all.end());
    int t = 0;
    Mint ways = 1;
    for (int i = 0; i < (int) all.size(); i++) {
      if (i > 0 && all[i] == all[i - 1]) {
        t += 1;
        ways *= t;
      } else {
        t = 1;
      }
    }
    ans += ways * coeff * (R - L + 1);
  }
  cout << ans << '\n';
  return 0;
}