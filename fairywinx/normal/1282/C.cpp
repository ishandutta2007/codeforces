int need_kol(int n, int mx_t, int kol[], int need[], int A, int B) {
	if (A * need[0] + need[1] * B >= mx_t)
		return 0;
	--mx_t;
	int res = need[0] + need[1];
	mx_t -= (A * need[0] + B * need[1]);
	res += min(kol[0], mx_t / A);
	mx_t -= min(kol[0], mx_t / A) * A;
	res += min(kol[1], mx_t / B);
	mx_t -= min(kol[1], mx_t / B) * B;
	// cout << res << '\n';
	return res;
}

void solve() {
	int n, T, A, B;
	cin >> n >> T >> A >> B;
	vector<pair<int, int>> a(n + 1);
	a[n] = {T + 1, 0};
	int kol[2] = {0, 0};
	for(int i = 0; i < n; ++i) {
		cin >> a[i].second;
		kol[a[i].second]++;
	}
	for(int i = 0; i < n; ++i)
		cin >> a[i].first;
	sort(a.begin(), a.end());

	int need[2] = {0, 0};
	int ans = need_kol(n, a[0].first, kol, need, A, B);
	for(int i = 0; i < n; ++i) {
		need[a[i].second]++;
		--kol[a[i].second];
		ans = max(ans, need_kol(n, a[i + 1].first, kol, need, A, B));
	}
	cout << ans << '\n';
}

