const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 100010;

ll n, m, k, u, v, x, y, t, a, b, ans;
ll A[MAXN][5];
ll out[5];
multiset<int> st[5];

ll calc(){
	ll res=0;
	for (int i=0; i<m; i++) res+=*st[i].rbegin();
	return res;
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m>>k;
	for (int i=1; i<=n; i++) for (int j=0; j<m; j++) cin>>A[i][j];
	int i=0, j=1;
	for (; j<=n; j++){
		for (int x=0; x<m; x++) st[x].insert(A[j][x]);
		while (calc()>k && i+1<j){
			i++;
			for (int x=0; x<m; x++) st[x].erase(st[x].find(A[i][x]));
		}
		if (j-i>ans && calc()<=k){
			ans=j-i;
			for (int x=0; x<m; x++) out[x]=*st[x].rbegin();
		}
	}
	for (int x=0; x<m; x++) cout<<out[x]<<' ';
	cout<<endl;
	debug(ans);
	
	return 0;
}
