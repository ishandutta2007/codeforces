const int N=2e5+5;

vector<int> v[N];int vis[N];
int dr[N];
int dx[N];
void dfs(int z,int d){
	vis[z]=1;
	dr[z]=d;
	for(int x:v[z]){
		if(vis[x])continue;
		dfs(x,d+1);
	}
}

void dfs2(int z,int d){
	vis[z]=1;
	dx[z]=d;
	for(int x:v[z]){
		if(vis[x])continue;
		dfs2(x,d+1);
	}
}

