bool Check(int x,int y,int d){
	if(x-d>=0&&a[x-d][y]=='S')return false;
	if(d==8)return true;
	for(int i=0;i<8;i++){
		int X=x+mv[i][0];
		int Y=y+mv[i][1];
		if(X>=0&&X<8&&Y>=0&&Y<8&&(X-d<0||a[X-d][Y]!='S')){
			if(Check(X,Y,d+1))return true;
		}
	}
	if(Check(x,y,d+1))return true;
	return false;
}
