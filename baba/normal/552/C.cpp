int N = 28;
LL w,m,W[50];
const LL INF = LL(2e18);
bool ok=false;
set<LL> S;
void solve1(LL num,int pos)
{
	if(pos==N/2)
	{
		S.insert(num);
		return;
	}
	solve1(num+W[pos],pos+1);
	solve1(num-W[pos],pos+1);
	solve1(num,pos+1);
}
void solve2(LL num,int pos)
{
	if(pos==N)
	{
		if(S.count(m-num))
			ok = true;
		return;
	}
	solve2(num+W[pos],pos+1);
	solve2(num-W[pos],pos+1);
	solve2(num,pos+1);
}
int main()
{
	cin>>w>>m;
	W[0]=1;
	int n=0;
	for(int i=1;i<N && W[i-1]<(INF/w);i++,n++)
		W[i]=W[i-1]*w;
	N = n;
	if(w==2)
	{
		printf("YES\n");
		return 0;
	}
	solve1(0,0);
	solve2(0,N/2);
	printf("%s\n",ok?"YES":"NO");
	return 0;
}