typedef long long ll;
typedef long double ld;

ll solve(string s) {
    const int SIGMA = 26;
    vector<ll> cnt(SIGMA);
    vector<vector<ll>> cnt2(SIGMA, vector<ll>(SIGMA));

    ll ans = 0;
    for (char cc : s) {
        int c = cc - 'a';

        for (int i = 0; i < SIGMA; i++) {
            cnt2[i][c] += cnt[i];
            ans = max(ans, cnt2[i][c]);
        }

        cnt[c]++;
        ans = max(ans, cnt[c]);
    }
    return ans;
}

