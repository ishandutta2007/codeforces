int main(){
	cin.tie(0)->sync_with_stdio(0);
	int tt;
	cin >> tt;
	while(tt --){
		int n, m, a, b;
		cin >> n >> m >> a >> b, a --, b --;
		vector<vi> adj(n);
		for(int i = 0; i < m; i ++){
			int u, v;
			cin >> u >> v, u --, v --;
			adj[u].push_back(v);
			adj[v].push_back(u);
		}
		vi comp(n);
		int cnt = 1;
		auto dfs = [&](int u, auto &dfs)->void{
			comp[u] = cnt;
			for(auto v: adj[u]){
				if(!comp[v]){
					dfs(v, dfs);
				}
			}
		};
		comp[b] = 2;
		dfs(a, dfs);
		for(int u = 0; u < n; u ++){
			if(!comp[u]){
				cnt ++;
				dfs(u, dfs);
			}
		}
		if(cnt < 2){
			cout << "0\n";
			continue;
		}
		auto prev = comp;
		fill(all(comp), 0);
		cnt = 1;
		comp[a] = 2;
		dfs(b, dfs);
		for(int u = 0; u < n; u ++){
			if(!comp[u]){
				cnt ++;
				dfs(u, dfs);
			}
		}
		if(cnt < 2){
			cout << "0\n";
			continue;
		}
		int Acnt = 0, Bcnt = 0;
		for(int u = 0; u < n; u ++){
			if(u != a && u != b){
				if(prev[u] == 1 && comp[u] >= 2){
					Acnt ++;
				}
				if(prev[u] >= 2 && comp[u] == 1){
					Bcnt ++;
				}
			}
		}
		cout << 1LL * Acnt * Bcnt << "\n";
	}
	return 0;
}
 
