const int mod=998244353,N=2e5+5; //998244353
int add(int a,int b){
    a+=b;
    if(a>=mod)
        a-=mod;
    return a;
}
int sub(int a,int b){
    a-=b;
    if(a<0)
        a+=mod;
    return a;
}
int mul(int a,int b){
    return (long long)a*b%mod;
}
int pwrmod(int x,int k){
    int ans=1;
    for(;k;k>>=1,x=mul(x,x))
        if(k&1)
            ans=mul(ans,x);
    return ans;
}
vector<int> fact(1,1),ifact(N);
int nCk(int n,int k){
	return mul(fact[n],mul(ifact[k],ifact[n-k]));
}
int main()
{
	for(int i=1;i<N;i++)
		fact.pb(mul(fact.back(),i));
	ifact[N-1]=pwrmod(fact[N-1],mod-2);
	for(int i=N-2;i>=0;i--)
		ifact[i]=mul(ifact[i+1],i+1);
	int n,m;
	scanf("%i %i",&n,&m);
	printf("%i\n",n==2?0:mul(mul(n-2,pwrmod(2,n-3)),nCk(m,n-1)));
	return 0;
}