using modular = modular_base<integral_constant<decay<decltype(mod)>::type, mod>>;

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, m, qn;
	cin >> n >> m >> qn;
	array<int, 2> cnt{};
	vector<array<int, 2>> xcnt(n), ycnt(m);
	int xempty = n, yempty = m, xconflict = 0, yconflict = 0;
	map<array<int, 2>, int> a;
	for(auto qi = 0; qi < qn; ++ qi){
		int x, y, type;
		cin >> x >> y >> type, -- x, -- y;
		if(a.count({x, y})){
			int val = a[{x, y}];
			a.erase({x, y});
			-- cnt[val];
			if(xcnt[x][val] == 1 && !xcnt[x][!val]){
				++ xempty;
			}
			if(xcnt[x][val] == 1 && xcnt[x][!val]){
				-- xconflict;
			}
			-- xcnt[x][val];
			if(ycnt[y][val] == 1 && !ycnt[y][!val]){
				++ yempty;
			}
			if(ycnt[y][val] == 1 && ycnt[y][!val]){
				-- yconflict;
			}
			-- ycnt[y][val];
		}
		if(~type){
			int val = type ^ x & 1 ^ y & 1;
			a[{x, y}] = val;
			++ cnt[val];
			if(!xcnt[x][val] && !xcnt[x][!val]){
				-- xempty;
			}
			if(!xcnt[x][val] && xcnt[x][!val]){
				++ xconflict;
			}
			++ xcnt[x][val];
			if(!ycnt[y][val] && !ycnt[y][!val]){
				-- yempty;
			}
			if(!ycnt[y][val] && ycnt[y][!val]){
				++ yconflict;
			}
			++ ycnt[y][val];
		}
		modular res = !cnt[0] + !cnt[1];
		if(!xconflict){
			res += (modular(2) ^ xempty) - !cnt[0] - !cnt[1];
		}
		if(!yconflict){
			res += (modular(2) ^ yempty) - !cnt[0] - !cnt[1];
		}
		cout << res << "\n";
	}
	return 0;
}

