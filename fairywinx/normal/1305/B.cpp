void solve() {
	string y;
	cin >> y;
	vector<pair<char, int>> s(y.size());
	for(int i = 0; i < y.size(); ++i)
		s[i] = {y[i], i + 1};
	vector<vector<int>> ans;
	while(true) {
		int n = s.size();
		vector<int> kol1(n + 1);
		kol1[n] = 0;
		for(int i = n - 1; i >= 0; --i) {
			if (s[i].first == ')')
				++kol1[i];
			kol1[i] += kol1[i + 1];
		}
		vector<pair<char, int>> z;
		vector<int> kol2(n + 1);
		for(int i = 0; i < n; ++i) {
			if (s[i].first == '(')
				++kol2[i + 1];
			kol2[i + 1] += kol2[i];
		}
		vector<int> lans;
		int kol = 0;
		for(int i = 0; i < n; ++i) {
			if (s[i].first == '(' && kol1[i] >= kol + 1) {
				lans.push_back(s[i].second);
				++kol;
			}
		}
		for(int j = n - 1; j >= 0; --j) {
			if (kol && s[j].first == ')') {
				--kol;
				lans.push_back(s[j].second);
			}
		}
		sort(all(lans));
		s = z;
		if (lans.size() == 0) 
			break;
		ans.push_back(lans);
		if (s.size() == 0)
			break;
	}
	cout << ans.size() << '\n';
	for(auto i: ans) {
		cout << i.size() << '\n';
		for(auto j: i)
			cout << j << ' ';
	}
}

signed main() {
	setup();
	// SOLVE;
	solve();

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


