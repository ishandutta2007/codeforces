struct item {
    array<ll, 2> sum = {0, 0};
    array<array<ld, 3>, 3> q = {};

    template<typename T>
    void init(const T &t, int l, int r) {
        sum = {};
        q = {};
        for (int i = 0; i < 3; i++)
            for (int j = 0; j < 3; j++)
                q[i][j] = -llinf;
        sum[l & 1] = t;
        q[l & 1][2] = -t;
        q[2][l & 1] = -t;
        q[2][2] = 0;
    }

    void update(const item &first, const item &second, int l, int r) {
        for (int t = 0; t < 2; t++)
            sum[t] = first.sum[t] + second.sum[t];
        for (int x = 0; x < 3; x++)
            for (int y = 0; y < 3; y++) {
                q[x][y] = -llinf;
                for (int mid = 0; mid < 3; mid++) {
                    setmax(q[x][y], first.q[x][mid] + second.q[mid][y]);
                }
                if (y != 2)
                    setmax(q[x][y], first.q[x][y] - second.sum[y] + kf * second.sum[!y]);
                if (x != 2)
                    setmax(q[x][y], second.q[x][y] - first.sum[x] + kf * first.sum[!x]);
                if (q[x][y] < -llinf / 2)
                    q[x][y] = -llinf;
            }
    }

    static item merge(const item &first, const item &second, int l, int r) {
        item res;
        res.update(first, second, l, r);  // careful with different lengths
        return res;
    }
};

struct segtree {
    vector<item> tree;
    int n = 1;

    segtree(int n = 1) : n(n) {
        tree.resize(1 << (__lg(max(1, n - 1)) + 2));
    }

    template<typename T>
    segtree(const vector<T>& v) {
        build(v);
    }

    template<typename T>
    void build(const vector<T> &v, int i, int l, int r) {
        if (l == r) {
            tree[i].init(v[l], l, r);
            return;
        }
        int m = (l + r) >> 1;
        build(v, i * 2 + 1, l, m);
        build(v, i * 2 + 2, m + 1, r);
        tree[i].update(tree[i * 2 + 1], tree[i * 2 + 2], l, r);
    }

    template<typename T>
    void build(const vector<T> &v) {
        n = v.size();
        tree.assign(1 << (__lg(max(1, n - 1)) + 2), item());
        build(v, 0, 0, n - 1);
    }

    item ask(int l, int r, int i, int vl, int vr) {
        if (l == vl && r == vr) {
            return tree[i];
        }
        int m = (vl + vr) >> 1;
        if (r <= m) {
            return ask(l, r, i * 2 + 1, vl, m);
        } else if (m < l) {
            return ask(l, r, i * 2 + 2, m + 1, vr);
        } else {
            return item::merge(ask(l, m, i * 2 + 1, vl, m), ask(m + 1, r, i * 2 + 2, m + 1, vr), l, r);
        }
    }

    item ask(int l, int r) {
        l = max(l, 0); r = min(r, n - 1);
        if (l > r) return item();
        return ask(l, r, 0, 0, n - 1);
    }

    template<typename T>
    void set(int ind, const T &t) {
        static array<pair<int, int>, 30> st;
        int l = 0, r = n - 1, i = 0;
        int ptr = -1;
        while (l != r) {
            st[++ptr] = {l, r};
            int m = (l + r) >> 1;
            if (ind <= m) {
                i = i * 2 + 1;
                r = m;
            } else {
                i = i * 2 + 2;
                l = m + 1;
            }
        }
        tree[i].init(t, l, r);
        while (i != 0) {
            i = (i - 1) / 2;
            tree[i].update(tree[i * 2 + 1], tree[i * 2 + 2], st[ptr].first, st[ptr].second);
            --ptr;
        }
    }
};

