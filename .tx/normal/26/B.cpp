using namespace std;

void init_cin() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
}

int main() {
    init_cin();
    string s;
    getline(cin, s);
    stack<char> st;
    for (char c : s) {
        if (st.empty() || c == '(' || st.top() != '(') {
            st.push(c);
        } else {
            st.pop();
        }
    }
    cout << s.size() - st.size();
    return 0;
}