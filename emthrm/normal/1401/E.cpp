template <typename Abelian>
struct BIT0based {
  BIT0based(int n, const Abelian UNITY = 0) : n(n), UNITY(UNITY), dat(n, UNITY) {}

  void add(int idx, Abelian val) {
    while (idx < n) {
      dat[idx] += val;
      idx |= idx + 1;
    }
  }

  Abelian sum(int idx) const {
    Abelian res = UNITY;
    --idx;
    while (idx >= 0) {
      res += dat[idx];
      idx = (idx & (idx + 1)) - 1;
    }
    return res;
  }

  Abelian sum(int left, int right) const {
    return left < right ? sum(right) - sum(left) : UNITY;
  }

  Abelian operator[](const int idx) const { return sum(idx, idx + 1); }

  int lower_bound(Abelian val) const {
    if (val <= UNITY) return 0;
    int res = 0, exponent = 1;
    while (exponent <= n) exponent <<= 1;
    for (int mask = exponent >> 1; mask > 0; mask >>= 1) {
      if (res + mask - 1 < n && dat[res + mask - 1] < val) {
        val -= dat[res + mask - 1];
        res += mask;
      }
    }
    return res;
  }

private:
  int n;
  const Abelian UNITY;
  vector<Abelian> dat;
};

int main() {
  const int L = 1000000;
  int n, m; cin >> n >> m;
  vector<vector<int>> ins(L), era(L + 1);
  ll ans = 0;
  while (n--) {
    int y, lx, rx; cin >> y >> lx >> rx;
    ins[lx].emplace_back(y);
    era[rx].emplace_back(y);
    ans += lx == 0 && rx == L;
  }
  vector<vector<pair<int, int>>> ver(L);
  while (m--) {
    int x, ly, ry; cin >> x >> ly >> ry;
    ver[x].emplace_back(ly, ry);
    ans += ly == 0 && ry == L;
  }
  BIT0based<int> bit(L + 1);
  REP(x, L) {
    for (int e : ins[x]) bit.add(e, 1);
    for (auto [l, r] : ver[x]) ans += bit.sum(l, r + 1);
    for (int e : era[x]) bit.add(e, -1);
  }
  cout << ans + 1 << '\n';
  return 0;
}