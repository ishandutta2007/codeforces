mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, k;
	cin >> n >> k;
	const int lim = 2e5;
	vi x(lim + 1);
	vector<pii> a(n);
	vector<vector<pii>> p(lim + 1);
	for(int i = 0; i < n; i ++){
		cin >> a[i], a[i].first --;
		x[a[i].first] ++, x[a[i].second] --;
		p[a[i].first].push_back({i, 1}), p[a[i].second].push_back({i, -1});
	}
	vi ok(n);
	priority_queue<pii> q;
	int cur = 0;
	vi res;
	for(int i = 0; i < lim; i ++){
		cur += x[i];
		for(auto [id, t]: p[i]){
			if(t > 0){
				ok[id] = true;
				q.push({a[id].second, id});
			}
			else{
				ok[id] = false;
			}
		}
		while(cur > k){
			auto [t, id] = q.top();
			q.pop();
			if(ok[id]){
				cur --;
				ok[id] = false;
				x[t] ++;
				res.push_back(id + 1);
			}
		}
	}
	cout << res.size() << "\n" << res;
	return 0;
}

