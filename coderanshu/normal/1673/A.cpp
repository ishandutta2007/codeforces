int _runtimeTerror_()
{
    string s;
    cin >> s;
    int ans = 0;
    for(auto j:s) {
        ans += j - 'a' + 1;
    }
    if(sz(s) & 1) {
        ans -= 2 * (min(s[0], s.back()) - 'a' + 1);
    }
    if(ans < 0) {
        cout << "Bob " << -ans << "\n";
    }
    else {
        cout << "Alice " << ans << "\n";
    }
    return 0;
}

