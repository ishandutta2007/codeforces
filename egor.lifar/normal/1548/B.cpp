const int Mod =  998244353;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}


const int MAXN = 200228;


ll gcd(ll a, ll b) {
    while (a) {
        b %= a;
        swap(a, b);
    }
    return b;
}


int n;
ll a[MAXN];
ll b[MAXN];
ll d[MAXN * 4];
int ss = 1;


ll get(int l, int r) {
    l += ss;
    r += ss;
    ll ans = 0;
    while (l <= r) {
        if (l & 1) {
            ans = gcd(ans, d[l]);
            l++;
        }
        if (!(r & 1)) {
            ans = gcd(ans, d[r]);
            r--;
        }
        l >>= 1;
        r >>= 1;
    }
    return ans;
}


void solve() {
    cin >> n;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    int ans = 1;
    for (int i = 0; i < n - 1; i++) {
        b[i] = abs(a[i + 1] - a[i]);
    }
    n--;
    ss = 1;
    while (ss < n) {
        ss <<= 1;
    }
    for (int i = 0; i < n; i++) {
        d[ss + i] = b[i];
    }
    for (int i = ss - 1; i >= 1; i--) {
        d[i] = gcd(d[i * 2], d[i * 2 + 1]);
    }
    int uk = 0;
    for (int i = 0; i < n; i++) {
        if (b[i] == 1) {
            continue;
        }
        chkmax(ans, 2);
        chkmax(uk, i);
        while (uk + 1 < n && get(i, uk + 1) > 1) {
            uk++;
        } 
        chkmax(ans, uk - i + 2);
    }
    cout << ans << '\n';
}


