int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int qq;
	cin >> qq;
	while(qq --){
		int n;
		cin >> n;
		vector<pil> a(n);
		cin >> a;
		vl res{0, a[0].second, a[0].second * 2};
		for(int i = 1; i < n; i ++){
			vl res_new(3, 9e18);
			for(int j = 0; j < 3; j ++){
				for(int k = 0; k < 3; k ++){
					if(a[i - 1].first + j != a[i].first + k){
						res_new[k] = min(res_new[k], res[j] + k * a[i].second);
					}
				}
			}
			res = res_new;
		}
		cout << min({res[0], res[1], res[2]}) << "\n";
	}
	return 0;
}

