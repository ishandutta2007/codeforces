void test_case(int test) {
    int n;
    cin >> n;
    int x = -1;
    for (int k = 2; k < n; ++k) {
        int a = k, b = n;
        for (int j = 0; j < 7; ++j) {
            if (a == 1 && b == 2) break;
            b = (b + a - 1) / a;
            if (a > b) swap(a, b);
        }
        if (a != 1 || b != 2) continue;
        x = k;
        break;
    }
    assert(x != -1);
    vector<pair<int, int>> ans;
    for (int i = 2; i < n; ++i) {
        if (i == x) continue;
        ans.eb(i, n);
    }
    pair<int, int> a = {x, x};
    pair<int, int> b = {n, n};
    while (true) {
        if (a.first == 1 && b.first == 2) break;
        ans.eb(b.second, a.second);
        b.first = (b.first + a.first - 1) / a.first;
        if (a.first > b.first) swap(a, b);
    }
    cout << ans.size() << '\n';
    for (auto p : ans) {
        cout << p << '\n';
    }
}

