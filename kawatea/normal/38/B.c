#include <stdio.h>

int main()
{
     int n = 0, x1, y1, x2, y2, i, j;
     char s[2][3];
     int a[8][8] = {0};
     int d[8][2] = {1, 2, 2, 1, 1, -2, -2, 1, -1, 2, 2, -1, -1, -2, -2, -1};

     scanf("%s %s", s[0], s[1]);

     x1 = s[0][0] - 'a'; y1 = s[0][1] - '1';
     x2 = s[1][0] - 'a'; y2 = s[1][1] - '1';

     for (i = 0; i < 8; i++) {
	  a[x1][i]++;
	  a[i][y1]++;
     }

     a[x2][y2]++;

     for (i = 0; i < 8; i++) {
	  if (x1 + d[i][0] >= 0 && x1 + d[i][0] <= 7 && y1 + d[i][1] >= 0 && y1 + d[i][1] <= 7) {
	       a[x1 + d[i][0]][y1 + d[i][1]]++;
	  }
	  if (x2 + d[i][0] >= 0 && x2 + d[i][0] <= 7 && y2 + d[i][1] >= 0 && y2 + d[i][1] <= 7) {
	       a[x2 + d[i][0]][y2 + d[i][1]]++;
	  }
     }

     for (i = 0; i < 8; i++) {
	  for (j = 0; j < 8; j++) {
	       if (a[i][j] == 0) n++;
	  }
     }

     printf("%d\n", n);

     return 0;
}