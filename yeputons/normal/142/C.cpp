char ans[10][10];
int main() {
  #ifdef DEBUG
  freopen("std.in", "r", stdin);
  freopen("std.out", "w", stdout);
  #endif

  int h, w;
  while (scanf("%d%d", &h, &w) >= 2) {
    bool sw = false;
    if (h < w) { swap(h, w); sw = true; }

    int id = 0;
    for (int cw = 1; cw <= 9; cw++)
    for (int ch = cw; ch <= 9; ch++, id++)
      if (h == ch && w == cw) {
        int st = starts[id] + 1;

        for (int y = 0; y < h; y++)
        for (int x = 0; x < w; x++) if (!sw) {
          ans[y][x] = data[st + y][x];
        } else {
          ans[x][y] = data[st + y][x];
        }
        if (sw) swap(h, w);
        for (int y = 0; y < h; y++)
          ans[y][w] = 0;

        printf("%s\n", data[starts[id]]);
        for (int y = 0; y < h; y++)
          printf("%s\n", ans[y]);
        goto en;
      }
    assert(false);
    en:;
  }
  return 0;
}