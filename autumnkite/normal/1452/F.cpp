#include <bits/stdc++.h>

long long solve(std::vector<long long> a, int x, long long now) {
	int n = a.size();
	for (int i = 0; i <= x; ++i) {
		now -= a[i];
	}
	if (now <= 0) {
		return 0;
	}
	long long ans = 0;
	for (int i = x + 1; i < n; ++i) {
		long long t = std::min(now >> (i - x), a[i]);
		now -= t << (i - x);
		ans += t << (i - x);
		ans -= t;
		a[i] -= t;
		a[x] += t << (i - x);
	}
	long long sum = 0;
	for (int i = 0; i <= x; ++i) {
		sum += a[i] << i;
		sum -= a[i];
	}
	if (now - sum <= 0) {
		return ans + now;
	}
	int p = x + 1;
	while (p < n && !a[p]) {
		++p;
	}
	if (p == n) {
		return -1;
	}
	if (x == 0) {
		for (int i = p; i > 0; --i) {
			long long w = 1ll << (i - x);
			ans += (now - 1) / w + 1;
		}
		return ans;
	}
	for (int i = p; i >= x; --i) {
		long long w = 1ll << (i - x);
		if (now >= w) {
			return ans + now - 1;
		} else {
			++ans;
		}
	}
	return -1;
}

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int n, q;
	std::cin >> n >> q;
	std::vector<long long> a(n);
	for (int i = 0; i < n; ++i) {
		std::cin >> a[i];
	}
	while (q--) {
		int op, x;
		std::cin >> op >> x;
		if (op == 1) {
			std::cin >> a[x];
		} else {
			long long now;
			std::cin >> now;
			std::cout << solve(a, x, now) << "\n";
		}
	}
}