template<typename T>
T isqrt(T const&x){
  static_assert(is_integral<T>::value, "is_integral<T>::value");
  assert(x>=T(0));
  T ret = static_cast<T>(sqrtl(x));
  while(ret>0 && ret*ret>x) --ret;
  while(x-ret*ret>2*ret) ++ret;
  return ret;
}

template<typename T>
T icbrt(T const&x) {
  static_assert(is_integral<T>::value, "is_integral<T>::value");
  assert(x>=T(0));
  T ret = static_cast<T>(cbrt(x));
  while(ret>0 && ret*ret*ret>x) --ret;
  while(x-ret*ret*ret>3*ret*(ret+1)) ++ret;
  return ret;
}

int main(){
  ios::sync_with_stdio(0); cin.tie(0);

  {li n; cin>>n;}
  li m; cin>>m;
  vi D(m); for(li i = 0; i < (li)(m); ++i) cin>>D[i];
  li g,r; cin>>g>>r;

  sort(begin(D), end(D));

  li ans = -1;
  min_queue<tuple<li,li> > Q;
  Q.push(make_tuple(0,0));
  vi DD(m, 1e9);
  vi E(m, 0);
  vector<bitset<1001> > seen(m, 0);
  li date = 0;
  while(!Q.empty()) {
    li d, i; tie(d,i) = Q.top(); Q.pop();
    if(E[i]) continue;
    E[i] = 1;
    date++;

    vvi at(g+1);
    at[0].push_back(i);
    for(li a = 0; a < (li)(g+1); ++a) {
      for(li j : at[a]) {
        if(j == m-1) {
          li lans = (r+g)*d + a;
          if(ans == -1) ans = lans;
          ans = min(ans, lans);
        }
        if(a<g) {
          if(j>0 && (a+D[j]-D[j-1])<=g && !seen[j-1][a+D[j]-D[j-1]]) {
            at[a+D[j]-D[j-1]].emplace_back(j-1);
            seen[j-1][a+D[j]-D[j-1]] = 1;
          }
          if(j+1<m && (a+D[j+1]-D[j])<=g && !seen[j+1][a+D[j+1]-D[j]]) {
            at[a+D[j+1]-D[j]].emplace_back(j+1);
            seen[j+1][a+D[j+1]-D[j]] = 1;
          }
        }
      }
    }
    for(li j : at[g]) if(d+1 < DD[j]) {
        DD[j] = d+1;
        Q.push(make_tuple(DD[j],j));
      }
  }

  cout << ans << endl;

  return 0;
}