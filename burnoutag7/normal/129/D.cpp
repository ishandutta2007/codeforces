#include<bits/stdc++.h>
using namespace std;

string s;
int m,mm,len,k,n,i,j,t;
char ch,cc;
int last[256];
int pred[200010];
int a[200010];
int aa[200010];
char b[200010];

int main(){

    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    cin>>s;
    cin>>k;
    n=s.size();
    s='#'+s;
    long long nnn=n;
    long long kkk=k;
    if(nnn*(nnn+1)/2<kkk){
        cout<<"No such line.\n";
        return 0;
    }
    for(i=1;i<=n;i++){
        pred[i]=last[s[i]];
        last[s[i]]=i;
    }
    cc=' ';
    for(ch='a';ch<='z';ch++){
        t=0;
        j=last[ch];
        while(j>0){
            t+=n-j+1;
            if(t>=k)break;
            j=pred[j];
        }
        if(t>=k){
            cout<<ch;
            cc=ch;
            break;
        }
        k-=t;
    }
    m=0;
    j=last[cc];
    while(j>0){
        m++;
        a[m]=j;
        j=pred[j];
    }
    for(len=2;len<=n;len++){
        if(m>=k)break;
        k-=m;
        memset(last,0,sizeof(last));
        for(i=1;i<=m;i++)
            if(a[i]+len-1<=n){
                b[i]=s[a[i]+len-1];
                pred[i]=last[b[i]];
                last[b[i]]=i;
            }else{
                b[i]=' ';
            }
        cc=' ';
        for(ch='a';ch<='z';ch++){
            t=0;
            j=last[ch];
            while(j>0){
                t+=n-a[j]-len+2;
                if(t>=k)break;
                j=pred[j];
            }
            if(t>=k){
                cout<<ch;
                cc=ch;
                break;
            }
            k-=t;
        }
        mm=0;
        for(i=1;i<=m;i++)
            if(b[i]==cc){
                mm++;
                aa[mm]=a[i];
            }
        m=mm;
        for(i=1;i<=m;i++)a[i]=aa[i];
    }

    return 0;
}