int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n, t;
    cin >> n >> t;
    map<int, int> mem;
    ordered_set<int> changes;

    auto ask = [&](int i) {
        if (mem.count(i)) {
            return mem[i] - (int)changes.order_of_key(i + 1);
        }
        cout << "? " << 1 << ' ' << i + 1 << endl;
        int x;
        cin >> x;
        x = i + 1 - x;
        mem[i] = x + (int)changes.order_of_key(i + 1);
        return mem[i] - (int)changes.order_of_key(i + 1);
    };

    for (int i = 0; i < t; ++i) {
        int k;
        cin >> k;
        int l = -1, r = n;
        while (r - l > 1) {
            int c = (l + r) / 2;
            if (ask(c) < k)
                l = c;
            else
                r = c;
        }
        cout << "! " << r + 1 << endl;
        changes.insert(r);
    }

    return 0;
}