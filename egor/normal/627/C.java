    static class TaskC {
        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int d = in.readInt();
            int n = in.readInt();
            int m = in.readInt();
            int[] x = new int[m];
            int[] p = new int[m];
            IOUtils.readIntArrays(in, x, p);
            m++;
            x = Arrays.copyOf(x, m);
            p = Arrays.copyOf(p, m);
            ArrayUtils.orderBy(x, p);
            int[] poi = new int[2 * m + 1];
            for (int i = 0; i < m; i++) {
                poi[2 * i] = x[i];
                poi[2 * i + 1] = Math.min(d, x[i] + n);
            }
            poi[2 * m] = d;
            ArrayUtils.sort(poi, IntComparator.DEFAULT);
            IntervalTree tree = new ReadOnlyIntervalTree(ArrayUtils.asLong(p)) {

                protected long neutralValue() {
                    return Long.MAX_VALUE;
                }


                protected long joinValue(long left, long right) {
                    return Math.min(left, right);
                }
            };
            long answer = 0;
            int left = 0;
            int right = 0;
            for (int i = 1; i < poi.length; i++) {
                while (left < m && poi[i] - x[left] > n) {
                    left++;
                }
                while (right + 1 < m && poi[i - 1] >= x[right + 1]) {
                    right++;
                }
                if (left > right) {
                    out.printLine(-1);
                    return;
                }
                answer += tree.query(left, right) * (poi[i] - poi[i - 1]);
            }
            out.printLine(answer);
        }

    }

    static abstract class ReadOnlyIntervalTree extends IntervalTree {
        protected long[] value;
        protected long[] array;

        protected ReadOnlyIntervalTree(long[] array) {
            super(array.length, false);
            this.array = array;
            init();
        }


        protected void initData(int size, int nodeCount) {
            value = new long[nodeCount];
        }


        protected void initAfter(int root, int left, int right, int middle) {
            value[root] = joinValue(value[2 * root + 1], value[2 * root + 2]);
        }


        protected void initBefore(int root, int left, int right, int middle) {
        }


        protected void initLeaf(int root, int index) {
            value[root] = array[index];
        }


        protected long queryPostProcess(int root, int left, int right, int from, int to, int middle, long leftResult,
                                        long rightResult) {
            return joinValue(leftResult, rightResult);
        }


        protected void queryPreProcess(int root, int left, int right, int from, int to, int middle) {
        }


        protected long queryFull(int root, int left, int right, int from, int to) {
            return value[root];
        }


        protected long emptySegmentResult() {
            return neutralValue();
        }

        protected abstract long neutralValue();

        protected abstract long joinValue(long left, long right);

    }

