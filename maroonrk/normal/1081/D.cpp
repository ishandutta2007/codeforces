const int Nmax=100010;
int par[Nmax];
int Find(int a){
	return par[a]==-1?a:(par[a]=Find(par[a]));
}
vi sp[Nmax];
void Unite(int a,int b){
	a=Find(a);
	b=Find(b);
	if(a==b)return;
	if(sp[a].size()<sp[b].size())
		swap(a,b);
	for(auto v:sp[b])
		sp[a].PB(v);
	par[b]=a;
}

