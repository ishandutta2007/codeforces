int main()
{
	cin.sync_with_stdio(0);
	cin.tie(0);
	int q;
	cin >> q;
	while (q--)
	{
		ll a, b;
		cin >> a >> b;
		if (2 * a - b < 0 || (2 * a - b) % 3 != 0)
		{
			cout << "NO" << endl;
			continue;
		}

		ll y = (2 * a - b) / 3;
		if (a - 2 * y < 0)
		{
			cout << "NO" << endl;
			continue;
		}

		cout << "YES" << endl;
	}
}

