const int mod = 1000000007;
const int MAXN = 1000010, LOG=20;

ll powmod(ll a, ll b){
	if (!b) return 1;
	if (b&1) return a*powmod(a*a%mod, b>>1)%mod;
	return powmod(a*a%mod, b>>1);
}

ll n, m, k, u, v, x, y, t, a, b, ans;
ll M[MAXN];
int sieve[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	for (ll i=2; i*i<MAXN; i++) if (!sieve[i]) for (ll j=i*i; j<MAXN; j+=i) sieve[j]=i;
	M[1]=1;
	for (ll i=2; i<MAXN; i++){
		if (!sieve[i]) M[i]=-1;
		else M[i]=-M[i/sieve[i]];
	}
	for (ll i=2; i*i<MAXN; i++) if (!sieve[i]) for (ll j=i*i; j<MAXN; j+=i*i) M[j]=0;
	
	
	cin>>m;
	
	for (ll i=2; i<=m; i++){
		ll a=(m/i)*powmod(m-(m/i), mod-2)%mod;
		//debug(a)
		ans=(ans+M[i]*a)%mod;
	}
	ans=(mod-ans)%mod;
	
	ans=(ans+1)%mod;
	cout<<ans<<'\n';
	
	return 0;
}
