struct TwoList
{
    int H,id, pre,nxt;
    bool operator<(const TwoList& o) const 
    {
        return H<o.H;
    }
}L[N];
void Delete(int x)
{
    if (L[x].pre) L[L[x].pre].nxt=L[x].nxt;
    if (L[x].nxt) L[L[x].nxt].pre=L[x].pre;
}

