const int N = 55;

int mx[4] = {1, -1, 0, 0};
int my[4] = {0, 0, -1, 1};
int used[N][N];

void dfs(int i, int j, vector<vector<char>> &a, int n, int m) {
	used[i][j] = 1;
	for(int k = 0; k < 4; ++k) {
		int x = i + mx[k];
		int y = j + my[k];
		if (x < 0 || x >= n || y < 0 || y >= m)
			continue;
		if (a[x][y] == '#' || used[x][y])
			continue;
		dfs(x, y, a, n, m);
	}
}

void solve() {
	for(int i = 0; i < N; ++i) 
		fill(used[i], used[i] + N, 0);

	int n, m;
	cin >> n >> m;
	vector<vector<char>> a(n, vector<char> (m));
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j)
			cin >> a[i][j];
	}
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j) {
			if (a[i][j] == 'B') {
				for(int k = 0; k < 4; ++k) {
					int x = i + mx[k];
					int y = j + my[k];
					if (x < 0 || x >= n || y < 0 || y >= m)
						continue;
					if (a[x][y] == 'G' || a[x][y] == 'B')
						continue;
					a[x][y] = '#';
				}
			}
		}
	}
	if (a[n - 1][m - 1] != '#')
		dfs(n - 1, m - 1, a, n, m);
	int ans = true;
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j) {
			if ((used[i][j] && a[i][j] == 'B') || (!used[i][j] && a[i][j] == 'G'))
				ans = false;
		}
	}
	if (ans)
		cout << "Yes\n";
	else
		cout << "No\n";
}

