C speed(const vector<C> & w, int i, int k) {
	int j = i ? n - i : 0, r = k ? -1 : 1;
	return C{w[i].real + w[j].real * r, w[i].imag
			- w[j].imag * r} * (k ? C{0, -0.5} : C{0.5, 0});
};
