const int mod = 1000000007;
const int MAXN = 100010;

int n, m, k, u, v, x, y, t, a, b, mn, mx, T;
int L[MAXN];
int R[MAXN];
int ind[MAXN];
int ans[MAXN];

bool cmp(int i, int j){
	if (L[i]==L[j]) return R[i]>R[j];
	return L[i]<L[j];
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>T;
	while (T--){
		cin>>n;
		for (int i=0; i<n; i++){
			cin>>L[i]>>R[i];
			ind[i]=i;
			ans[i]=1;
		}
		sort(ind, ind+n, cmp);
		mx=R[ind[0]];
		ans[ind[0]]=2;
		bool ok=0;
		for (int j=1; j<n; j++){
			int i=ind[j];
			if (mx<L[i]){
				for (int x=0; x<n; x++) cout<<ans[x]<<' ';
				cout<<'\n';
				ok=1;
				break ;
			}
			mx=max(mx, R[i]);
			ans[i]=2;
		}
		if (!ok) cout<<"-1\n";
	}
	
	return 0;
}
