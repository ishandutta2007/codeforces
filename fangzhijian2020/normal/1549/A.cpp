using namespace std;
int read(){
	int a=0;bool opt=0;char c=getchar();
	while(c<'0'||c>'9')opt|=c=='-',c=getchar();
	while(c>='0'&&c<='9')a=(a<<3)+(a<<1)+(c^48),c=getchar();
	return opt?-a:a;
}
int T,P;
int main(){
	T=read()+1;
	while(--T){
		P=read();
		cout<<(P-1)/2<<" "<<(P-1)<<"\n";
	}
	return 0;
}