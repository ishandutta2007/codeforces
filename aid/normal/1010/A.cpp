void precalc() {
}

const int maxn = 1005;
int n, m;
int a[maxn], b[maxn];

int read() {
  if (scanf("%d%d", &n, &m) < 2) {
    return false;
  }
  for (int i = 0; i < n; i++) {
    scanf("%d", &a[i]);
  }
  for (int i = 0; i < n; i++) {
    scanf("%d", &b[i]);
  }
  return true;
}

void solve() {
  ld cur = m;
  for (int i = n - 1; i >= 0; i--) {
    int ni = i + 1;
    if (ni >= n) {
      ni = 0;
    }
    for (int it = 0; it < 2; it++) {
      int c = (!it ? b[ni] : a[i]);
      if (c <= 1) {
        printf("-1\n");
        return;
      }
      cur += cur / (c - 1);
    }
  }
  cur -= m;
  printf("%.12f\n", (double) cur);
}

int main() {
  precalc();
#ifdef DEBUG
  assert(freopen(TASK ".in", "r", stdin));
  assert(freopen(TASK ".out", "w", stdout));
#endif
  while (read()) {
    solve();
#ifdef DEBUG
    eprintf("Time %.2f\n", (double) clock() / CLOCKS_PER_SEC);
#endif
  }
  return 0;
}