int solve(vector<int> a) {
    int n = a.size();
    vector<int> pref(n + 1);
    for(int i = 0; i < n; ++i)
        pref[i + 1] = pref[i] + a[i];
    vector<pair<int, int>> s;
    vector<int> mx;
    int ans = 0;
    for(int i = n - 1; i >= 0; --i) {
        int sum = pref[i + 1];
        while(s.size() && s.back().first <= a[i]) {
            sum = max(sum, s.back().second);
            s.pop_back();
            mx.pop_back();
        }
        s.emplace_back(a[i], sum);
        if (mx.size())
            mx.push_back(max(sum - a[i], mx.back()));
        else
            mx.push_back(sum - a[i]);
        ans = max(ans, mx.back() - pref[i]);
    }
    return ans;
}

