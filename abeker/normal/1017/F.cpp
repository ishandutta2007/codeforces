void load() {
	scanf("%d", &N);
	for (int i = 3; i >= 0; i--)
		scanf("%d", coef + i);
}

inline ll add(ll x, ll y) {
	x += y;
	if (x >= MOD)
		x -= MOD;
	return x;
}

inline ll mul(ll x, ll y) {
	return x * y % MOD;
}

void inc(ll &ref, int a, int p) {
	ll prod = 1;
	for (int k = 0; k < 4; k++) {
		ref = add(ref, mul(coef[k], mul(a, prod)));
		prod = mul(prod, p);
	}
}

ll range(int lo, int hi) {
	for (int i = 0; i < hi - lo + 1; i++)
		num[i] = lo + i;
	
	ll res = 0;
	for (int i = 2; i < SQRT; i++) {
		if (ok[i])
			continue;
		for (int j = i; j < SQRT; j += i)
			ok[j] = true;
		for (int j = lo - 1 - (lo - 1) % i + i; j <= hi; j += i) {
			int ex = 0;
			for (; !(num[j - lo] % i); num[j - lo] /= i)
				ex++;
			inc(res, ex, i);
		}
	}
	
	for (int i = 0; i < hi - lo + 1; i++)
		if (num[i] > 1)
			inc(res, 1, num[i]);
	
	return res;
}

