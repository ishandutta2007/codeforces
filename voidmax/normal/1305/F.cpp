typedef long long base; 
typedef pair<int, int> point;      
typedef complex<double> comp;

const int N = 1e6 + 5;

int n;
int ans = N;
bool used[N];
vector<long long> a;
unordered_set<long long> checked;

void count_ans(long long v) {
    if (checked.count(v)) return;
    checked.insert(v);
    long long cur = 0;
    for (int i = 0; i < n && cur < ans; ++i) {
        if (a[i] <= v) {
            cur += v - a[i];
        } else {
            auto r = a[i] % v;
            cur += min(r, v - r);
        }
    }
    chkmin(ans, cur);
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n;
    a.resize(n);
    cin >> a;
    mt19937_64 rnd(rand());
    shuffle(a.begin(), a.end(), rnd);
    while (clock() < 2. * CLOCKS_PER_SEC) {
        int id = rnd() % n;
        if (used[id]) continue;
        used[id] = true;
        for (int t = -1; t <= 1; ++t) {
            auto A = a[id] + t;
            if (A == 0) continue;
            for (int i = 2; 1LL * i * i <= A; ++i) {
                if (A % i == 0) {
                    count_ans(i);
                    while (A % i == 0) A /= i;
                }
            }
            if (A != 1) count_ans(A);
            if (clock() > 2. * CLOCKS_PER_SEC) break;
        }
    }
    cout << ans << '\n';
}