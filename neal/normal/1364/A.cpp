const int INF = 1e9 + 5;

void run_case() {
    int N, X;
    cin >> N >> X;
    vector<int> A(N);

    for (auto &a : A)
        cin >> a;

    int smallest = INF, sum = 0;
    int best = -1;

    for (int end = 1; end <= N; end++) {
        sum = (sum + A[end - 1]) % X;

        if (sum != 0 && end < smallest)
            smallest = end;

        if (sum == 0)
            best = max(best, end - smallest);
        else
            best = max(best, end);
    }

    cout << best << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}