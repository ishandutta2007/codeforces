typedef long long LL;
typedef unsigned long long ULL;
#define MEM(x, y) memset((x),(y),sizeof(x))
const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
int last[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int ncase;
	scanf("%d", &ncase);
	while (ncase--)
	{
		int n;
		scanf("%d", &n);
		MEM(last, -1);
		int ans = -1;
		for (int i = 1; i <= n; i++)
		{
			int x;
			scanf("%d", &x);
			if (last[x] == -1)
			{
				last[x] = i;
				continue;
			}
			ans = max(ans, last[x] + n - i);
			last[x] = i;
		}
		printf("%d\n", ans);
	}
	return 0;
}