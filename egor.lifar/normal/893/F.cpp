void dfs(int u, int pr) {
    in[u] = timer++;
    for (auto h: g[u]) {
        if (h != pr) {
            deep[h] = deep[u] + 1;
            dfs(h, u);
        }
    }
    out[u] = timer;
} 


int rmq(int v, int vl, int vr, int l, int r, int rr) {
    if (l > vr || r < vl) {
        return 1e9 + 1;
    }
    if (l <= vl && vr <= r) {
        if (tr[v].empty() || tr[v][0].first > rr) {
            return 1e9 + 1;
        }
        int f = lower_bound(all(tr[v]), make_pair(rr + 1, -1)) - tr[v].begin();
        f--;
        return tr[v][f].second;
    }
    int vn = v << 1;
    int vm = (vl + vr) >> 1;
    return min(rmq(vn, vl, vm, l, r, rr), rmq(vn + 1, vm + 1, vr, l, r, rr));
}


