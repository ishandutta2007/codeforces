int solve() {
    string ans;
    string s;
    cin >> s;
    for (auto c: s) {
        if (c == 'A')
            ans.PB(c);
        else if (sz(ans))
            ans.pop_back();
        else
            ans.PB(c);
    }
    cout << sz(ans) << '\n';
    return 0;
}

