	void solve()
	{
		int n = ni(), m = ni(), K = ni(), s = ni();
		int[] a = na(n);
		
		int[] from = new int[m];
		int[] to = new int[m];
		for (int i = 0; i < m; i++) {
			from[i] = ni() - 1;
			to[i] = ni() - 1;
		}
		int[][] g = packU(n, from, to);
		int[] q = new int[n+1];
		int[][] ds = new int[n][K];
		for(int i = 0;i < n;i++){
			Arrays.fill(ds[i], 999999999);
		}
		for(int type = 1;type <= K;type++){
			int qp = 0;
			for(int j = 0;j < n;j++){
				if(a[j] == type){
					ds[j][type-1] = 0;
					q[qp++] = j;
				}
			}
			for(int r = 0;r < qp;r++){
				int cur = q[r];
				for(int e : g[cur]){
					if(ds[e][type-1] > ds[cur][type-1] + 1){
						ds[e][type-1] = ds[cur][type-1] + 1;
						q[qp++] = e;
					}
				}
			}
		}
		for(int i = 0;i < n;i++){
			Arrays.sort(ds[i]);
			long sum = 0;
			for(int j = 0;j < s;j++){
				sum += ds[i][j];
			}
			out.print(sum + " ");
		}
		out.println();
	}
	
	static int[][] packU(int n, int[] from, int[] to) {
		int[][] g = new int[n][];
		int[] p = new int[n];
		for (int f : from)
			p[f]++;
		for (int t : to)
			p[t]++;
		for (int i = 0; i < n; i++)
			g[i] = new int[p[i]];
		for (int i = 0; i < from.length; i++) {
			g[from[i]][--p[from[i]]] = to[i];
			g[to[i]][--p[to[i]]] = from[i];
		}
		return g;
	}
	
