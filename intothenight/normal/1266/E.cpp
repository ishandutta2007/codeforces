typedef tuple<int, int, ll> tpl;
typedef vector<tpl> vtpl;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vi ob(n);
	cin >> ob;
	ll tot = accumulate(all(ob), 0LL);
	vi cur(n);
	vector<map<int, int>> ms(n);
	int q;
	cin >> q;
	while(q --){
		int s, t, u;
		cin >> s >> t >> u, s --, u --;
		if(ms[s].count(t)){
			int a = ms[s][t];
			ms[s].erase(t);
			if(cur[a] <= ob[a]){
				tot ++;
			}
			cur[a] --;
		}
		if(u >= 0){
			if(cur[u] < ob[u]){
				tot --;
			}
			cur[u] ++;
			ms[s][t] = u;
		}
		cout << tot << "\n";
	}
	return 0;
}
