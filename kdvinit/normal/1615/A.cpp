#include<bits/stdc++.h>
using namespace std;

#define int long long
#define endl "\n"

void solve()
{
    int n;
    cin>>n;

    int sum=0, x;
    for(int i=1; i<=n; i++) { cin>>x; sum+=x; }

    if(sum%n==0) cout<<0<<endl;
    else cout<<1<<endl;
}

int32_t main()
{
    ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
    int t; cin>>t;
    while(t--) solve();
    return 0;
}