int readint(){
	int x=0,f=1; char ch=getchar();
	while(ch<'0'||ch>'9'){if(ch=='-')f=-1;ch=getchar();}
	while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}
	return x*f;
}

int n;
int a[100005];

int main(){
	int T=readint();
	while(T--){
		n=readint();
		for(int i=1;i<=n;i++) a[i]=readint();
		ll now=1;
		bool fl=0;
		for(int i=1;i<=n;i++){
			now=now/__gcd(now,(ll)i+1)*(i+1);
			if(a[i]%now==0) fl=1;
			if(now>1000000000) break;
		}
		if(fl) printf("NO\n");
		else printf("YES\n");
	}
	return 0;
}