#define all(x) (x).begin(),(x).end()

int solve() {
	int a, b, c;
	cin >> a >> b >> c;
	int ans = 0;
	for (int i = 0; i <= a and i * 2 <= b; i++)
		smax(ans, 3 * i + 3 * min(b - 2 * i, c / 2));
	cout << ans << endl;
}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0); cout.tie(0);
	int q;
	cin >> q;
	while (q--)
		solve();
	return 0;
}