bool dfs(int i,int dep){
	int le = val(i*2), ri = val(i*2+1);
	if(!le && !ri)return dep>g;
	if(le > ri)return dfs(i*2, dep+1);
	return dfs(i*2+1, dep+1);
}

void upd(int i,int dep){
	int le = val(i*2), ri = val(i*2+1);
	if(!le && !ri){
		x[i] = 0;
		return;
	}
	if(le > ri){
		x[i] = le;
		upd(i*2, dep+1);
	}
	else {
		x[i] = ri;
		upd(i*2+1, dep+1);
	}
	return;
}

void f(int i,int dep){
	if(dep>g)return;
	while(dfs(i, dep))upd(i, dep), res.pb(i);
	f(i*2, dep+1), f(i*2+1, dep+1);
	return;
}

