#include<bits/stdc++.h>
#define ll long long
using namespace std;
const int M=3e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(ll x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(ll x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
ll dp[M],ans,sum=0;
vector<int>a[5];
int get1(int x)
{
	int ret=0;
	if (x<(int)a[1].size()) ret+=a[1][x];
	if (x+1<(int)a[1].size()) ret+=a[1][x+1];
	return ret;
}
int get2(int x)
{
	int ret=0;
	if (x<(int)a[2].size()) ret+=a[2][x];
	return ret;
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read();
	for (int i=1;i<=n;i++)
	{
		int w=read(),v=read();
		a[w].push_back(v);
	}
	for (int i=1;i<=3;i++) sort(a[i].begin(),a[i].end(),greater<int>());
	int tot[2][3]={{0,0},{0,1}};
	dp[1]=a[1].empty()?0:a[1][0];
	for (int i=2;i<=m;i++)
	if (get1(tot[i&1][1])>get2(tot[i&1][0]))
	{
		dp[i]=dp[i-2]+get1(tot[i&1][1]);
		tot[i&1][1]+=2;
	}
	else
	{
		dp[i]=dp[i-2]+get2(tot[i&1][0]);
		tot[i&1][0]++;
	}
	ans=dp[m];
	for (int i=0;i<(int)a[3].size();sum+=a[3][i++])
	if ((i+1)*3<=m)
	ans=max(ans,dp[m-(i+1)*3]+sum+a[3][i]);
	print(ans);
	
	return 0;
}
