template<class T>
istream &operator>>(istream &is, vector<T> &v) {
  for (T &x : v)
    is >> x;
  return is;
}

template<class T>
ostream &operator<<(ostream &os, const vector<T> &v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int t;
  cin >> t;
  while (t--) {
    int a, b, q;
    cin >> a >> b >> q;
    vector<int> sum(a * b);
    for (int i = 0; i < a * b; ++i)sum[i] = (i % a % b) != (i % b % a);
    for (int i = 1; i < a * b; ++i)sum[i] += sum[i - 1];
    auto query = [&](ll x) {
      return sum.back() * (x / a / b) + sum[x % (a * b)];
    };
    vector<ll> ans;
    while (q--) {
      ll l, r;
      cin >> l >> r;
      ans.push_back(query(r) - query(l - 1));
    }
    cout << ans << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}