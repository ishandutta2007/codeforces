int ins1(int x){
	if(!hv[x]){hv[x]=1;val[x]=0;return x;}	
	chk(x);
	if(!val[x])return 0;
	int v=ins1(val[x]);
	if(v==val[x]){val[x]=0;}
	return v;
}
int ins2(int x,int y){
	if(x<y)swap(x,y);
	if(x==y)return 0;
	if(!hv[x]){
		hv[x]=1;val[x]=y;
		if(hv[y])val[x]=val[y];
		return x;
	}
	chk(x);
	if(!val[x])return ins1(y);
	if(val[x]==y)return 0;
	int v=ins2(val[x],y);
	return v;
}
