	void solve()
	{
		int n = ni(), m = ni();
		if(n >= m){
			while(true){
				if(n == 0)break;
				out.print("B");
				n--;
				if(m == 0)break;
				out.print("G");
				m--;
			}
			while(n > 0){
				out.print("B");
				n--;
			}
		}else{
			while(true){
				if(m == 0)break;
				out.print("G");
				m--;
				if(n == 0)break;
				out.print("B");
				n--;
			}
			while(m > 0){
				out.print("G");
				m--;
			}
		}
		out.println();
	}
	
	void run() throws Exception
	{
		is = oj ? new FileInputStream("input.txt") : new ByteArrayInputStream(INPUT.getBytes());
		out = oj ?  new PrintWriter("output.txt") : new PrintWriter(System.out);
		
		long s = System.currentTimeMillis();
		solve();
		out.flush();
		tr(System.currentTimeMillis()-s+"ms");
	}
	
	public static void main(String[] args) throws Exception
	{
		new A().run();
	}
	
	public int ni()
	{
		try {
			int num = 0;
			boolean minus = false;
			while((num = is.read()) != -1 && !((num >= '0' && num <= '9') || num == '-'));
			if(num == '-'){
				num = 0;
				minus = true;
			}else{
				num -= '0';
			}
			
			while(true){
				int b = is.read();
				if(b >= '0' && b <= '9'){
					num = num * 10 + (b - '0');
				}else{
					return minus ? -num : num;
				}
			}
		} catch (IOException e) {
		}
		return -1;
	}
	
