template<class H, class... T> void ps(const H& h, const T&... t) { pr(h); if (sizeof...(t)) pr(" "); ps(t...); }
 

// cf1451F: looks like a contrived version of staircase nim, submitting this as a guess
void go() {
    int n, m;
    cin >> n >> m;
    
    vi a(n+m+1);
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            int x; cin >> x;
            a[i+j] ^= x;
        }
    }
    
    for (int i = 0; i <= n+m; i++) if (a[i]) {
        ps("Ashish");
        return;
    }
    ps("Jeel");
}

void solve()
{
    int t;
    for (cin >> t; t; t--) go();
}


