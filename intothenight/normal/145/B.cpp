int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	vi a(4);
	cin >> a;
	if(abs(a[2] - a[3]) > 1){
		cout << -1;
		return 0;
	}
	// a[2], a[3] <= min(a[0], a[1]) && |a[2] - a[3]| <= 1
	if(a[2] == a[3]){
		if((a[0] <= a[2] || a[1] < a[2]) && (a[0] < a[2] || a[1] <= a[2])){
			cout << -1;
			return 0;
		}
		if(a[0] == a[3]){
			for(int i = 0; i < a[3]; i ++){
				cout << "74";
			}
			cout << string(a[1] - a[0], '7');
		}
		else{
			cout << string(a[0] - a[3] - 1, '4');
			for(int i = 0; i < a[3]; i ++){
				cout << "47";
			}
			cout << string(a[1] - a[3], '7') + "4";
		}
	}
	else if(a[2] > a[3]){
		if(min(a[0], a[1]) < a[2]){
			cout << -1;
			return 0;
		}
		cout << string(a[0] - a[2], '4');
		for(int i = 0; i < a[2]; i ++){
			cout << "47";
		}
		cout << string(a[1] - a[2], '7');
	}
	else{
		if(min(a[0], a[1]) < a[3]){
			cout << -1;
			return 0;
		}
		cout << "74" + string(a[0] - a[3], '4');
		for(int i = 0; i < a[3] - 2; i ++){
			cout << "74";
		}
		cout << string(a[1] - a[3], '7') + "74";
	}
	return 0;
}

