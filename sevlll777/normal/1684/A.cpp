void solve() {
  string s;
  cin >> s;
  if (s.size() == 2) {
    cout << s[1] << '\n';
    return;
  }
  char c = '9';
  for (auto cc : s) {
    c = min(c, cc);
  }
  cout << c;
  cout << '\n';
}

