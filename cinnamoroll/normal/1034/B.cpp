void solve(){
  int N,M;
  cin >> N >> M;

  if(N%2==0&&M%2==0){
    if(N==2 && M == 2) cout << 0 << endl;
    else cout << N*M << endl;
  }
  else if(N%2==0 || M%2 == 0){
    if(N%2) swap(N,M); 
    if(M==1){
      int res = N/6*6;
      cout << res + max(0LL,N%6-3)*2 << endl;
    }
    else if(N == 2){
      if(M == 3) cout << 4 << endl;
      else if(M == 7) cout << 12 << endl;
      else cout << N*M << endl;
    }
    else cout << N*M << endl;
  }
  else{
    if(N>M) swap(N,M);
    if(N==1){
      int res = M/6*6;
      cout << res + max(0LL,M%6-3)*2 << endl;
    }
    else cout << N*M-1 << endl;
  }
}

