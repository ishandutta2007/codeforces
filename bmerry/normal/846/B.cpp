int main(int argc, const char **argv)
{
    redirect(argc, argv);
    int N, K;
    ll M;
    cin >> N >> K >> M;
    vector<ll> t(K);
    for (int i = 0; i < K; i++)
        cin >> t[i];
    sort(RA(t));

    ll ans = 0;
    ll T = accumulate(RA(t), 0LL);
    for (int full = 0; full <= N; full++)
    {
        ll rem = M - T * full;
        if (rem < 0)
            break;
        ll score = (K + 1) * full;
        for (int i = 0; i < (N - full) * K; i++)
        {
            ll d = t[i / (N - full)];
            if (d > rem)
                break;
            rem -= d;
            score++;
        }
        ans = max(ans, score);
    }

    cout << ans << '\n';
    return 0;
}