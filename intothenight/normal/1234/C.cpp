mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int qq;
	cin >> qq;
	while(qq --){
		int n;
		cin >> n;
		vs s(2);
		cin >> s;
		bool ok;
		for(int x = 0, y = 0, p = 0; ; ){
			if(x == 1 && y == n){
				ok = true;
				break;
			}
			if(x < 0 || x >= 2 || y < 0 || y >= n){
				ok = false;
				break;
			}
			if(s[x][y] == '1' || s[x][y] == '2'){
				if(!p){
					y ++;
				}
				else{
					ok = false;
					break;
				}
			}
			else{
				if(p == 0){
					x = !x;
					p = 1;
				}
				else{
					y ++;
					p = 0;
				}
			}
		}
		ok ? cout << "YES\n" : cout << "NO\n";
	}
	return 0;
}

