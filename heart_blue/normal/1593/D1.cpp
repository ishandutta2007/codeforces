const LL INF = 1e9 + 7;
const int N = 1e2 + 10;
int a[N];
int gcd(int n, int m)
{
	return m == 0 ? n : gcd(m, n % m);
}
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int ncase;
	scanf("%d", &ncase);
	while (ncase--)
	{
		int n;
		scanf("%d", &n);
		for (int i = 1; i <= n; i++)
			scanf("%d", &a[i]);
		sort(a + 1, a + n + 1);
		if (a[1] == a[n])
		{
			puts("-1");
			continue;
		}
		int g = 0;
		for (int i = 2; i <= n; i++)
		{
			g = gcd(a[i] - a[i - 1], g);
		}
		printf("%d\n", g);
	}
	return 0;
}