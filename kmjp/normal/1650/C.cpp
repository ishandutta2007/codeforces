int T,N,M;
int X[202020],W[202020];


void solve() {
	int i,j,k,l,r,x,y; string s;
	
	cin>>T;
	while(T--) {
		cin>>N>>M;
		vector<pair<int,int>> P,V;
		FOR(i,M) {
			cin>>X[i]>>W[i];
			P.push_back({W[i],i});
		}
		sort(ALL(P));
		P.resize(2*N);
		ll sum=0;
		FORR(p,P) {
			sum+=p.first;
			V.push_back({X[p.second],p.second+1});
		}
		sort(ALL(V));
		cout<<sum<<endl;
		FOR(i,N) {
			cout<<V[i].second<<" "<<V[2*N-1-i].second<<endl;
		}
		
	}
}


