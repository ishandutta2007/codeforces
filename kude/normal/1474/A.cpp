int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int tt;
    cin >> tt;
    while(tt--) {
        int n;
        cin >> n;
        int pre = -1;
        rep(_, n) {
            char c;
            cin >> c;
            int x = c - '0';
            int y = 1;
            if (x + y == pre) y--;
            cout << y;
            pre = x + y;
        }
        cout << '\n';
    }
}