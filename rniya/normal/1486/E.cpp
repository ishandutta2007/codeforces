const int MAX = 55;

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n, m;
    cin >> n >> m;
    vector<vector<pair<int, int>>> G(n);
    for (; m--;) {
        int v, u, w;
        cin >> v >> u >> w;
        v--, u--;
        G[v].emplace_back(u, w);
        G[u].emplace_back(v, w);
    }

    vector<vector<int>> dp(n, vector<int>(MAX, INF));
    priority_queue<tuple<int, int, int>> pq;
    dp[0][0] = 0;
    pq.emplace(0, 0, 0);

    while (!pq.empty()) {
        auto t = pq.top();
        pq.pop();
        int val, v, pre;
        tie(val, v, pre) = t;
        val *= -1;
        if (dp[v][pre] < val) continue;
        for (auto e : G[v]) {
            int u = e.first, w = e.second;
            if (!pre) {
                if (chmin(dp[u][w], val)) {
                    pq.emplace(-dp[u][w], u, w);
                }
            } else {
                if (chmin(dp[u][0], val + (pre + w) * (pre + w))) {
                    pq.emplace(-dp[u][0], u, 0);
                }
            }
        }
    }

    vector<int> ans(n);
    for (int i = 0; i < n; i++) ans[i] = (dp[i][0] == INF ? -1 : dp[i][0]);
    cout << ans << '\n';
    return 0;
}