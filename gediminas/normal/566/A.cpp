int ats = 0;
vector<pair<int, int>> rez;

pair<vector<int>*, vector<int>*> dfs(node *d, int g = 0) {
	pair<vector<int>*, vector<int>*> didz;
	didz.x = didz.y = 0;
	vector<pair<vector<int>*, vector<int>*>> kit;

	for (int i = 0; i < 26; i++) {
		if (d->tol[i]) {
			auto te = dfs(d->tol[i], g + 1);

			if (didz.x == NULL or didz.x->size() < te.x->size())
				didz.x = te.x;

			if (didz.y == NULL or didz.y->size() < te.y->size())
				didz.y = te.y;

			kit.push_back(te);
		}
	}

	if (didz.x == 0) {
		didz.x = d->pab + 0;
		didz.y = d->pab + 1;
	}
	else {
		for (auto && i : d->pab[0]) {
			didz.x->push_back(i);
		}

		for (auto && i : d->pab[1]) {
			didz.y->push_back(i);
		}

		for (auto && [x, y] : kit) {
			if (x != didz.x) {
				for (auto && i : *x) {
					didz.x->push_back(i);
				}
			}

			if (y != didz.y) {
				for (auto && i : *y) {
					didz.y->push_back(i);
				}
			}
		}
	}


	while (didz.x->size() and didz.y->size()) {
		rez.emplace_back(didz.x->back(), didz.y->back());
		didz.x->pop_back();
		didz.y->pop_back();
		ats += g;
	}

	return didz;
}

