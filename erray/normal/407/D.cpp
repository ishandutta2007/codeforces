int main () {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n, m;
  cin >> n >> m;
  vector<vector<int>> mat(n, vector<int>(m));
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < m; ++j) {
      cin >> mat[i][j];
      --mat[i][j];
    }
  }
  
  const int X = 160000;
  vector<vector<int>> dp(m, vector<int>(m));

  vector<vector<short>> lst(m, vector<short>(X, -1));
  int ans = 0;

  for (int j = 0; j < n; ++j) {
    for (int i = 0; i < m; ++i) {
      dp[0][i] = max(dp[0][i], lst[i][mat[j][i]] + 1);
      ans = max(ans, j + 1 - dp[0][i]);
    }

    for (int size = 1; size < m; ++size) {
      for (int i = 0; i < m - size; ++i) {
        dp[size][i] = max({dp[size][i], dp[size - 1][i], dp[size - 1][i + 1], lst[i + size][mat[j][i]] + 1, lst[i][mat[j][i + size]] + 1});
     
        if (mat[j][i] == mat[j][i + size]) {
          dp[size][i] = j + 1;
        }
         
        ans = max(ans, (size + 1) * (j - dp[size][i] + 1)); 
      }
    }
    
    for (int i = 0; i < m; ++i) {
      lst[i][mat[j][i]] = j;
    }
  }
  cout << ans << '\n';
}