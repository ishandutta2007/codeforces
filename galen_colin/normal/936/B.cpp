bool cyclic(int v) {
  isp[v] = 1;
  va[v] = 1;
  
  bool f = 0;
  for (int x: edges[v]) {
    if (isp[x]) return 1;
    if (!va[x]) f |= cyclic(x);
  }

  isp[v] = 0;
  return f;
}

void dfs_ans(int v, int s, int p) {
  vis[v][s] = 1;
  par[v][s] = p;

  int n = (s + 1) % 2;
  for (int x: edges[v]) {
    if (!vis[x][n]) dfs_ans(x, n, v);
  }
}

