template<int m>
struct modint {
    unsigned x;

    modint() : x(0) {}
    modint(long long arg) : x((arg %= m) < 0 ? arg + m : arg) {}

    modint operator+ (const modint& b) const {
        auto t = *this; t.x = (t.x += b.x) >= m ? t.x-m : t.x; return t;
    }

    modint operator- (const modint& b) const {
        auto t = *this; t.x = (t.x += m-b.x) >= m ? t.x-m : t.x; return t;
    }

    modint operator* (const modint& b) const {
        auto t = *this; t.x = ll(t.x) * b.x % m; return t;
    }

    modint operator/ (const modint& b) const {
        return *this * b.inv();
    }

    template<class T> modint operator^ (T n) const {
        modint t;
        if (n == 0) return t.x = 1, t;
        if (n == 1) return t.x = x, t;
        t = *this ^ (n >> 1); t *= t;
        return n & 1 ? *this * t : t;
    }

    modint& operator+= (const modint& b) { return *this = *this + b; }
    modint& operator-= (const modint& b) { return *this = *this - b; }
    modint& operator*= (const modint& b) { return *this = *this * b; }
    modint& operator/= (const modint& b) { return *this = *this / b; }
    modint& operator++ () { if (++x == m) x = 0; return *this; }
    modint& operator-- () { if (x-- == 0) x = m-1; return *this; }
    modint operator++ (int) { modint tmp = *this; ++*this; return tmp; }
    modint operator-- (int) { modint tmp = *this; --*this; return tmp; }
    bool operator== (const modint& other) const { return x == other.x; }
    bool operator!= (const modint& other) const { return x != other.x; }
    int operator() () const { return x; }
    template<class T> modint operator^= (T n) { return *this = *this ^ n; }
    modint inv() const { return *this ^ (m-2); }
};

const int mod = 998'244'353;
typedef modint<mod> mint;

struct sieve {
    const int maxn;
    vector<int> f, primes;

    sieve(int maxn) : maxn(maxn), f(maxn) {
        for (int i=2; i<maxn; i++) {
            if (f[i] == 0) {
                f[i] = i;
                primes.push_back(i);
            }

            for (int p : primes) {
                if (p > f[i] || i * p >= maxn) {
                    break;
                }

                f[i*p] = p;
            }
        }
    }

     vector<pair<int, int>> factor_small(int x) const {
        vector<pair<int, int>> v;
        while (x > 1) {
            int p = f[x], c = 0;
            while (x % p == 0) {
                x /= p;
                c++;
            }
            v.emplace_back(p, c);
        }
        return v;
    }

    template<class T>
    vector<pair<T, int>> factor(T x) const {
        vector<pair<T, int>> v;
        for (int p : primes) {
            if ((ll)p*p > x) {
                break;
            }

            if (x % p == 0) {
                int c = 0;
                while (x % p == 0) {
                    x /= p;
                    c++;
                }
                v.emplace_back(p, c);
            }
        }

        if (x > 1) {
            v.emplace_back(x, 1);
        }

        return v;
    }
} sv(200005);

struct edge {
    int v;
    map<int, int> d;
};

vector<edge> e[200005];
int mx[200005], mn[200005], w[200005];
mint sum = 0, prod = 1;

mint npw(int x, int y) {
    if (y < 0) {
        return mint(x) ^ (mod - 1 + y);
    } else {
        return mint(x) ^ y;
    }
}

void dfs(int x, int p) {
    sum += prod;
    dbg(tuple(x, w[2], w[3]));
    for (auto& [y, d] : e[x]) {
        if (y == p) continue;
        mint oldprod = prod;
        for (auto [k, v] : d) {
            w[k] += v;
            mx[k] = max(mx[k], w[k]);
            mn[k] = min(mn[k], w[k]);
            prod *= npw(k, v);
        }
        dfs(y, x);
        for (auto [k, v] : d) {
            w[k] -= v;
        }
        prod = oldprod;
    }
}

int main() {
    ios::sync_with_stdio(!cin.tie(0));

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        fill(e+1, e+n+1, vector<edge>{});
        fill(mx+1, mx+n+1, 0);
        fill(mn+1, mn+n+1, 0);
        fill(w+1, w+n+1, 0);
        sum = 0;
        prod = 1;
        for (int _i : ra(1, n)) {
            int i, j, x, y;
            cin >> i >> j >> x >> y;
            map<int, int> d;
            for (auto [p, e] : sv.factor_small(x)) {
                d[p] -= e;
            }
            for (auto [p, e] : sv.factor_small(y)) {
                d[p] += e;
            }
            e[i].push_back({j, d});
            for (auto& [k, v] : d) {
                v *= -1;
            }
            e[j].push_back({i, d});
        }

        dfs(1, 1);
        mint z = 1;
        for (int i : ra(1, n+1)) {
            z *= mint(i) ^ (-mn[i]);
        }
        dbg(tuple(z(), sum(), (z*sum)()));
        cout << (sum * z)() << '\n';
    }
}