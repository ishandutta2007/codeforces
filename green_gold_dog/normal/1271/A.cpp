template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
T square(T a) {
	return a * a;
}

template<>
struct std::hash<pair<ll, ll>> {
	ll operator() (pair<ll, ll> p) {
		return ((__int128)p.first * MOD + p.second) % INF64;
	}
};

void solve() {
	ll a, b, c, d, e, f;
	cin >> a >> b >> c >> d >> e >> f;
	ll ans = 0;
	if (e > f) {
		ll x = min(a, d);
		d -= x;
		ans += e * x;
		ans += min(d, min(b, c)) * f;
	} else {
		ll x = min(min(b, c), d);
		d -= x;
		ans += f * x;
		ans += min(d, a) * e;
	}
	cout << ans << '\n';
}

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t = 1;
	//cin >> t;
	for (ll i = 0; i < t; i++) {
		solve();
	}
}