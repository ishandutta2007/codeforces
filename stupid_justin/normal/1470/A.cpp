#include<bits/stdc++.h>
#define int long long
#define rd(x) x=read()
using namespace std;

const int N=3e5+5;
const int mod=998244353;
inline int read(){int x=0,f=1;char ch=getchar();while(ch>'9'||ch<'0'){if(ch=='-')f=-1;ch=getchar();}while(ch>='0'&&ch<='9'){x=x*10+ch-'0';ch=getchar();}return x*f;}
int ksm(int x,int y){int ret=1;while (y){if (y&1) ret=(ret*x)%mod;x=(x*x)%mod;y>>=1;}return ret;}

int n,m; 
int k[N],c[N];
//l1->r1 = l2->r2
signed main()
{
	int T;
	cin>>T;
	while (T--)
	{
		rd(n);rd(m);int ans=0;
		for (int i=1;i<=n;i++) rd(k[i]);
		for (int j=1;j<=m;j++) rd(c[j]);
		sort(k+1,k+n+1);int s=0;
		for (int i=n;i>=1;i--)
		{
			s++;
			if (s<k[i]) ans+=c[s];else ans+=c[k[i]];
		}
		cout<<ans<<endl;
	}
}