const ld eps=1e-7;
const int inf=1000000010;
const ll INF=1000000000000001000LL;
const int mod = 1000000007;
const int MAXN = 1010, LOG=20;

ll n, m, k, u, v, x, y, t, a, b, ans=1;
vector<int> D[MAXN];
int nxt[MAXN];

ll zarb(ll a, ll b){
	ld x=1e18/a;
	if (x<b) return INF;
	return a*b;
}

ll tavan(ll a, ll b){
	ll res=1;
	while (b--){
		res=zarb(res, a);
		if (res==INF) break;
	}
	return res;
}

ll solve(ll n, ll p){
	if (n==1) return 1;
	if (n==2) return p;
	ll res=INF;
	for (int i:D[n]) res=min(res, zarb(tavan(p, i-1), solve(n/i, nxt[p])));
	return res;
}

