using pl=pair<long long,long long>;

long long op(long long a,long long b) { return max(a, b); }
long long e() { return 0; }

int main(){
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int t;
  cin >> t;
  while(t>0){
    t--;
    long long n;
    cin >> n;
    vector<long long> a(n);
    for(auto &nx : a){cin >> nx;}
    map<long long,vector<long long>> mp;
    mp[0].push_back(0);
    long long rw=0;
    for(int i=0;i<n;i++){
      rw+=a[i];
      mp[rw].push_back(i+1);
    }
    vector<long long> comp(n+1);
    int h=0;
    for(auto &nx : mp){
      for(auto &ny : nx.second){comp[ny]=h;}
      h++;
    }

    segtree<long long, op, e> seg(n+1);
    vector<long long> sh(n+1,0);
    long long res=0;
    sh[comp[0]]=n;
    for(long long i=0;i<=n;i++){
      long long cv=sh[comp[i]]-(n-i);
      if(comp[i]>0){cv=max(cv,seg.prod(0,comp[i])-2*(n-i));}
      res=max(res,cv);
      cv=res;
      //cout << i << ' ' << res << '\n';
      sh[comp[i]]=max(sh[comp[i]],cv+(n-i));
      seg.set(comp[i],max(seg.get(comp[i]),cv+2*(n-i)));
    }
    cout << res-n << '\n';
  }
  return 0;
}