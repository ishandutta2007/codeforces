void solve() {
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }
    sort(arr.begin(), arr.end());
    int ans = 0;
    ans += arr[n - 1];
    int pref = 0;
    for (int i = 0; i < n; i++) {
        ans += pref - arr[i] * i;
        pref += arr[i];
    }
    cout << ans << endl;
}

