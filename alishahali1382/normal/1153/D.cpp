int cnt[MAXN];
int dp[MAXN];
int typ[MAXN];
vector<int> G[MAXN];

int dfs(int node){
	dp[node]=1;
	if (G[node].empty()) return cnt[node]=1;
	for (int i:G[node]) cnt[node]+=dfs(i);
	if (typ[node]){
		for (int i:G[node]) dp[node]=max(dp[node], dp[i]+cnt[node]-cnt[i]);
		return cnt[node];
	}
	int tmp=0;
	for (int i:G[node]) tmp+=cnt[i]-dp[i];
	dp[node]=cnt[node]-tmp-G[node].size()+1;
	
	return cnt[node];
}

