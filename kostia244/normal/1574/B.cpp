#pragma GCC optimize("Ofast,unroll-loops")
#pragma GCC target("avx,avx2,sse,sse2")
#include<bits/stdc++.h>
#define all(x) begin(x), end(x)
using namespace std;
using ll = long long;

template<typename F>
void multitest(F func) {
	int t;
	cin >> t;
	while(t--) func();
}
void report(int ok) {
	cout << (ok?"YES":"NO") << '\n';
}

int main() {
	cin.tie(0)->sync_with_stdio(0);
	multitest([&](){
		ll a, b, c, m;
		cin >> a >> b >> c >> m;
		ll mn = max({a-1-b-c, b-1-a-c, c-1-b-a});
		report(mn <= m && m <= max(a-1, 0ll)+max(b-1,0ll)+max(c-1,0ll));
	});
	
}