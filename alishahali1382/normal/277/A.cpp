#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 510;

int n, m, q, x, tmp, ans, a, b, c;
bool flag=false;
bool mark[MAXN];
vector<int> G[MAXN];\

void dfs(int node){
	mark[node]=true;
	for (int v:G[node]){
		if (mark[v]) continue ;
		dfs(v);
	}
}
int main(){
	ios_base::sync_with_stdio(false);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=0; i<n; i++){
		cin>>x;
		if (x) flag=true;
		for (int j=0; j<x; j++){
			cin>>tmp;
			G[i].pb(n+tmp);
			G[n+tmp].pb(i);
		}
	}
	if (!flag){
		cout<<n<<endl;
		return 0;
	}
	for (int i=0; i<n; i++){
		if (mark[i]) continue ;
		dfs(i);
		ans++;
	}
	cout<<ans-1<<endl;
	
	return 0;
}
