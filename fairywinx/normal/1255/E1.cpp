signed main() {
	setup();
	// SOLVE;

	int n;
	cin >> n;
	vector<int> a(n);
	int sum = 0;
	for(int i = 0; i < n; ++i) {
		cin >> a[i];
		sum += a[i];
	}
	if (sum == 1) {
		cout << -1 << '\n';
		return 0;
	}
	long long ans = 1e18 + 228;
	for(int k = 1; k * k <= sum; ++k) {
		if (k != 1 && sum % k == 0) {
			long long res = 0;
			vector<int> pos;
			for(int i = 0; i < n; ++i) {
				if (a[i])
					pos.push_back(i);
				if (pos.size() >= k) {
					int z = pos[pos.size() / 2];
					for(auto j: pos)
						res += abs(j - z);
					pos = vector<int> ();
				}
			}
			assert(!pos.size());
			ans = min(ans, res);
		}
		int c = sum / k;
		if (c != 1 && sum % k == 0) {
			long long res = 0;
			vector<int> pos;
			for(int i = 0; i < n; ++i) {
				if (a[i])
					pos.push_back(i);
				if (pos.size() >= c) {
					int z = pos[pos.size() / 2];
					for(auto j: pos)
						res += abs(j - z);
					pos = vector<int> ();
				}
			}
			assert(!pos.size());
			ans = min(ans, res);
		}
	}
	cout << ans << '\n';	

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

