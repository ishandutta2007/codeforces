void run_case() {
    string str;
    int K;
    cin >> str >> K;
    int D = int(str.size());

    cout << y_combinator([&](auto recurse, string num, int index, int distinct) -> string {
        if (distinct > K || num.substr(0, index) < str.substr(0, index))
            return "";

        if (index >= D)
            return num;

        for (char d = '0'; d <= '9'; d++) {
            num[index] = d;
            string candidate = recurse(num, index + 1, distinct + (num.substr(0, index).find(d) == string::npos));

            if (!candidate.empty())
                return candidate;
        }

        return "";
    })(string(D, '0'), 0, 0) << '\n';
}

