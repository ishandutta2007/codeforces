	void solve()
	{
		int n = ni();
		int[] from = new int[n-1];
		int[] to = new int[n-1];
		int[] w = new int[n-1];
		for(int i = 0;i < n-1;i++){
			from[i] = ni()-1;
			to[i] = ni()-1;
			w[i] = ni();
		}
		int[][][] g = packWU(n, from, to, w);
		int[][] pars = parents(g, 0);
		int[] par = pars[0], ord = pars[1], pw = pars[2];
		
		int[] des = new int[n];
		long S = 0;
		for(int i = n-1;i >= 0;i--){
			int cur = ord[i];
			des[cur]++;
			if(cur != 0)des[par[cur]] += des[cur];
			if(i > 0){
				S += (long)des[cur] * (n-des[cur]) * pw[cur];
			}
		}
		
		for(int Q = ni();Q >= 1;Q--){
			int e = ni()-1, v = ni();
			int dec = w[e] - v;
			w[e] = v;
			if(par[from[e]] == to[e]){
				S -= (long)des[from[e]] * (n-des[from[e]]) * dec;
			}else{
				S -= (long)des[to[e]] * (n-des[to[e]]) * dec;
			}
			out.printf("%.12f\n", (6.*n-12)*S/n/(n-1)/(n-2));
		}
	}
	
	public static int[][] parents(int[][][] g, int root)
	{
		int n = g.length;
		int[] par = new int[n];
		Arrays.fill(par, -1);
		int[] pw = new int[n];
		
		int[] q = new int[n];
		q[0] = root;
		for(int p = 0, r = 1;p < r;p++) {
			int cur = q[p];
			for(int[] nex : g[cur]){
				if(par[cur] != nex[0]){
					q[r++] = nex[0];
					par[nex[0]] = cur;
					pw[nex[0]] = nex[1];
				}
			}
		}
		return new int[][] {par, q, pw};
	}
	
	public static int[][][] packWU(int n, int[] from, int[] to, int[] w) {
		int[][][] g = new int[n][][];
		int[] p = new int[n];
		for (int f : from)
			p[f]++;
		for (int t : to)
			p[t]++;
		for (int i = 0; i < n; i++)
			g[i] = new int[p[i]][2];
		for (int i = 0; i < from.length; i++) {
			--p[from[i]];
			g[from[i]][p[from[i]]][0] = to[i];
			g[from[i]][p[from[i]]][1] = w[i];
			--p[to[i]];
			g[to[i]][p[to[i]]][0] = from[i];
			g[to[i]][p[to[i]]][1] = w[i];
		}
		return g;
	}
	
