fn gcd(a: u64, b: u64) -> u64 {
    if b == 0 {
        a
    } else {
        gcd(b, a % b)
    }
}

fn run() {
    input! {
        n: usize,
        x: [u64; n],
        e: [(usize1, usize1); n - 1],
    }
    let mut g = vec![vec![]; n];
    for (a, b) in e {
        g[a].push(b);
        g[b].push(a);
    }
    let mut ans = ModInt::zero();
    let mut memo: Vec<Option<Vec<(u64, ModInt)>>> = vec![None; n];
    memo[0] = Some(vec![(x[0], ModInt::zero())]);
    let mut stack = vec![(0, 0)];
    while let Some((v, p)) = stack.pop() {
        let mut val = memo[v].take().unwrap();
        let x = x[v];
        for v in val.iter_mut() {
            v.0 = gcd(v.0, x);
        }
        val.push((x, ModInt::one()));
        val.dedup_by(|a, b| {
            if a.0 == b.0 {
                b.1 += a.1;
                true
            } else {
                false
            }
        });
        for &(x, c) in val.iter() {
            ans += ModInt::from(x) * c;
        }
        for &u in g[v].iter() {
            if u != p {
                stack.push((u, v));
                memo[u] = Some(val.clone());
            }
        }
    }
    println!("{}", ans);
}

fn main() {
    run();
}