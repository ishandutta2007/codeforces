#include<bits/stdc++.h>
using namespace std;

#define int long long
#define endl "\n"

void solve()
{
    int n;
    cin>>n;

    int x = n%10;
    int sub = n%7;
    int ad = 7-sub;

    if(sub<=x) n-=sub;
    else n+=ad;

    cout<<n<<endl;
}

int32_t main()
{
    ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
    int t; cin>>t;
    while(t--) solve();
    return 0;
}