int l[200005];
map<int, ll> sum;
int n;

void solve() {
  cin >> n;
  for(int i = 0; i < n; i++) {
    cin >> l[i];
    sum[l[i]-i] += l[i];
  }
  ll ret = 0;
  for(auto out: sum) {
    ret = max(ret, out.second);
  }
  cout << ret << "\n";
}

