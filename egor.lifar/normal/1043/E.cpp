const int MAXN = 300228;


int n, m;
int x[MAXN], y[MAXN];
vector<int> g[MAXN];
long long sum[MAXN];


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);    
   // read(FILENAME);
    cin >> n >> m;
    for (int i = 0; i < n; i++) {
        cin >> x[i] >> y[i];
    }
    for (int i = 0; i < m; i++) {
        int a, b;
        cin >> a >> b;
        a--, b--;
        g[a].pb(b);
        g[b].pb(a);
    }
   // x[i] + y[j] <= y[i] + x[j];
   // x[i] - y[i] < x[j] - y[j];
    vector<pair<int, int> > st;
    for (int i = 0; i < n; i++) {
        st.pb({x[i] - y[i], i});
    }
    sort(all(st));
    long long cur = 0;
    for (int id = 0; id < n; id++) {
        int i = st[id].second;
        sum[i] += 1LL * y[i] * id;
        sum[i] += cur;
        //y[i] 
        cur += x[i];
    }
    cur = 0;
    for (int id = n - 1; id >= 0; id-- ){
        int i = st[id].second;
        sum[i] += 1LL * x[i] * (n - id - 1);
        sum[i] += cur;
        cur += y[i];
    }
    for (int i= 0 ; i < n; i++) {
        for (auto j: g[i]) {
            sum[i] -= min(x[i] + y[j], x[j] + y[i]);
        }
        cout << sum[i] << ' ';
    }
    cout << '\n';
    return 0;
}