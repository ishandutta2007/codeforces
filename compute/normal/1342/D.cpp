struct D{
    int sz,id;
    bool operator<(const D&a)const{
        if(sz==a.sz) return id>a.id;
        return sz>a.sz;
    }
};
int main()
{
    set<D> st;
    int n,k;
    cin>>n>>k;
    vc<vi> ans;
    vi m(n);
    for(int i=0;i<n;i++) cin>>m[i];
    vi c(k+1);
    for(int i=1;i<=k;i++) cin>>c[i];
    sort(m.begin(),m.end(),greater<int>());
    for(int i=0;i<n;i++)
    {
        //dbg(i,m[i],c[m[i]]);
        auto it=st.upper_bound(D{c[m[i]],-1});
        if(it==st.end())
        {
            ans.push_back(vi(1,m[i]));
            st.insert(D{1,int(ans.size())-1});
        }
        else{
            int id=it->id;
            //dbg(id);
            st.erase(it);
            ans[id].push_back(m[i]);
            st.insert(D{int(ans[id].size()),id});
        }
        //for(auto u:ans) dbg(u);
    }
    print(ans.size());
    for(auto u:ans)
    {
        sort(u.begin(),u.end());
        cout<<u.size()<<" ";
        print(u);
    }
}