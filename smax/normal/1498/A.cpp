int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    auto sum = [&] (long long x) -> long long {
        long long ret = 0;
        while (x > 0) {
            ret += x % 10;
            x /= 10;
        }
        return ret;
    };

    int t;
    cin >> t;
    while (t--) {
        long long n;
        cin >> n;
        while (__gcd(n, sum(n)) == 1)
            n++;
        cout << n << "\n";
    }

    return 0;
}