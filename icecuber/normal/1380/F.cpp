constexpr int md = 998244353;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

struct Mat {
  Mint data[2][2];
  Mat() {
    data[0][0] = data[1][1] = 1;
    data[0][1] = data[1][0] = 0;
  }
};

Mat operator*(const Mat& a, const Mat& b) {
  Mat ret;
  for (int i : {0,1})
    for (int j : {0,1}) {
      ret.data[i][j] = 0;
      for (int k : {0,1})
	ret.data[i][j] += a.data[i][k]*b.data[k][j];
    }
  return ret;
}

const int nax = 1<<19;
Mat prod[nax*2];

int c[nax];
void update(int i) {
  Mat m;
  m.data[0][0] = c[i]+1;
  m.data[0][1] = (c[i] == 1 && i ? 9-c[i-1] : 0);
  m.data[1][0] = 1;
  m.data[1][1] = 0;

  i = nax-1-i;
  i += nax;
  prod[i] = m;
  for (i >>= 1; i; i >>= 1) {
    prod[i] = prod[i*2]*prod[i*2+1];
  }
}
Mint query() {
  return prod[1].data[0][0];
}

void myset(int i, int d) {
  c[i] = d;
  update(i);
  update(i+1);
}

