const int maxn=2e3+7;
struct AC_auto
{
    int ch[maxn][14];
    ll num[maxn];
    int fail[maxn];
    // ffail.
    int tot;
    void init()
    {
        tot = 0;
        for (int i = 0; i < 14; ++i)
            ch[0][i] = 0;
    }
    void insert(string& s,int val)
    {
        int u = 0;
        int len=s.length();
        for (int i = 0; i < len; i++)
        {
            if (!ch[u][s[i] - 'a'])
            {
                ch[u][s[i] - 'a'] = ++tot;
                for (int j = 0; j < 14; ++j)
                    ch[tot][j] = 0;
                num[tot] = fail[tot] = 0;
            }
            u = ch[u][s[i] - 'a'];
        }
        num[u]+=val;
    } //Trie.
    void build()
    {
        queue<int> q;
        for (int i = 0; i < 14; i++)
        {
            if (ch[0][i])
                fail[ch[0][i]] = 0,
                //,fail.
                    q.push(ch[0][i]);
        }
        while (!q.empty())
        {
            int u = q.front();
            q.pop();
            for (int i = 0; i < 14; i++)
                if (ch[u][i])
                {
                    fail[ch[u][i]] = ch[fail[u]][i];
                    q.push(ch[u][i]);
                }
                else
                    ch[u][i] = ch[fail[u]][i];
            num[u]+=num[fail[u]];
            //.
        }
    } //Fail.
    
} AC;
ll dp[1<<14][maxn];
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int k;
    cin>>k;
    for(int i=0;i<k;i++)
    {
        string t;
        cin>>t;
        int v;
        cin>>v;
        AC.insert(t,v);
    }
    AC.build();
    string s;
    cin>>s;
    memset(dp,-0x3f,sizeof(dp));
    ll inf=dp[0][0];
    dp[0][0]=0;
    int cc=0;
    ll ans=inf;
    for(int i=1;i<=s.length();i++)
    {
        vc<int> aft(AC.tot+1);
        vc<ll> val(AC.tot+1,0);
        for(int p=0;p<=AC.tot;p++) aft[p]=p;
        while(s[i-1]!='?'&&i<=s.length())
        {
            for(int p=0;p<=AC.tot;p++)
            {
                aft[p]=AC.ch[aft[p]][s[i-1]-'a'];
                val[p]+=AC.num[aft[p]];
            }
            i++;
        }
        {
            int T=(1<<cc)-1;
            while(T<(1<<14))
            {
                vc<ll> g(AC.tot+1,inf);
                for(int p=0;p<=AC.tot;p++)
                    g[aft[p]]=max(g[aft[p]],dp[T][p]+val[p]);
                for(int p=0;p<=AC.tot;p++)
                    dp[T][p]=g[p];
                if(T==0) break;
                int x=T&-T,y=T+x;
                T=((T&~y)/x>>1)|y;
            }
        }
        if(i==s.length()+1) break;
        int T=(1<<cc)-1;
        while(T<(1<<14))
        {
            for(int p=0;p<=AC.tot;p++)
            {
                if(dp[T][p]==inf) continue;
                for(int j=0;j<14;j++)
                {
                    if(T>>j&1) continue;
                    int nxt=AC.ch[p][j];
                    dp[T|(1<<j)][nxt]=max(dp[T|(1<<j)][nxt],dp[T][p]+AC.num[nxt]);
                }
            }
            if(T==0) break;
            int x=T&-T,y=T+x;
            T=((T&~y)/x>>1)|y;
        }
        if(s[i-1]=='?') cc++;
    }
    int T=(1<<cc)-1;
    while(T<(1<<14))
    {
        for(int p=0;p<=AC.tot;p++) ans=max(ans,dp[T][p]);
        if(T==0) break;
        int x=T&-T,y=T+x;
        T=((T&~y)/x>>1)|y;
    }
    print(ans);

}