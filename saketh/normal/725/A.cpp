int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    cout << fixed << setprecision(15);

    int N;
    string bump;
    cin >> N >> bump;

    int i = 0, j = N - 1;

    while (j >= i && bump[j] == '>')
        j--;

    while (i <= j && bump[i] == '<')
        i++;

    cout << N - (j - i + 1) << "\n";

    return 0;
}