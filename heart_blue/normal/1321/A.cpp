int a[N];
int b[N];
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n;
	cin >> n;
	for (int i = 1; i <= n; i++) cin >> a[i];
	for (int i = 1; i <= n; i++) cin >> b[i];
	int ans = -1;
	for (int i = 1; i <= 200; i++)
	{
		int sum = 0;
		for (int j = 1; j <= n; j++)
		{
			if (a[j] == b[j]) continue;
			if (a[j] == 1) sum += i;
			else sum--;
		}
		if (sum > 0)
		{
			ans = i;
			break;
		}
	}
	cout << ans << endl;
	return 0;
}