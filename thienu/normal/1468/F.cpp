void solve(){
    int n;
    cin >> n;
    map<pii, int> directions;
    for(int i = 0; i < n; i++){
        int a, b, c, d;
        cin >> a >> b >> c >> d;
        int x = c - a, y = d - b;
        pii dir;
        if(x != 0 && y != 0){
            dir = {x / __gcd(abs(x), abs(y)), y / __gcd(abs(x), abs(y))};
        }
        else if(y != 0){
            dir = {0, y / abs(y)};
        }
        else{
            dir = {x / abs(x), 0};
        }
        directions[dir]++;
    }

    debug(directions);

    ll ans = 0;
    for(auto it = directions.begin(); it != directions.end(); it++){
        pii p = it->first;
        pii opp = {-p.first, -p.second};
        ans += ((ll) it->second * directions[opp]);
    }

    cout << ans / 2 << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}