int pow(int a, int b) {
	if (b == 0) 
		return 1;
	if (b % 2 == 1)
		return pow(a, b - 1) * a;
	int z = pow(a, b / 2);
	return z * z;
}

string s = "codeforces";

string ch(int k, int m) {
	// cout << k << ' ' << m << '\n';
	if (m == 0)
		return "";
	for(int i = 1; i <= k; ++i) {
		if (pow(i, m) >= k) {
			string z;
			for(int j = 0; j < i; ++j)
				z += s[10 - m];
			// cout << i << ' ' << (k + pow(i, m)) << '\n';
			z += ch((k + i - 1) / i, m - 1);
			return z;
		}
	}
}

void solve() {
	int k;
	cin >> k;
	cout << ch(k, 10);
}

