int powm(int a, int b) {
    if (b == 0) {
        return 1;
    } else if (b & 1) {
        int s = powm(a, b - 1);
        return (1LL * s * a) % Mod;
    } else {
        int s = powm(a, b >> 1);
        return (1LL * s * s) % Mod;
    }
}


