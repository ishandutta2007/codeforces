const int MAXN = 200010;

int n, m, b, x, y, t, lst;
int X[MAXN];
int T[MAXN];
int A[MAXN];// taxi
int B[MAXN];// people
int C[MAXN];
int ans[MAXN];

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=1; i<=n+m; i++) cin>>X[i];
	for (int i=1; i<=n+m; i++){
		cin>>T[i];
		if (T[i]) A[x++]=X[i];
		else B[y++]=X[i];
	}
	for (int i=0; i<n; i++){
		auto it2=lower_bound(A, A+m, B[i]);
		auto it1=(--it2)++;
		//cerr<<B[i]<<' '<<*it1<<' '<<*it2<<endl;
		if (B[i]-*it1<=*it2-B[i] && *it1>0 || *it2==0) ans[it1-A]++;
		else ans[it2-A]++;
	}
	x=0;
	for (int i=1; i<=n+m; i++) if (T[i]) cout<<ans[x++]<<' ';
	
	return 0;
}
