void run_case() {
    int N;
    cin >> N;
    vector<vector<int>> adj(N);
    vector<int> degree(N, 0);

    for (int i = 0; i < N - 1; i++) {
        int x, y;
        cin >> x >> y;
        x--; y--;
        adj[x].push_back(y);
        adj[y].push_back(x);
        degree[x]++; degree[y]++;
    }

    if (N == 1) {
        cout << 0 << '\n';
        return;
    }

    int root = int(max_element(degree.begin(), degree.end()) - degree.begin());
    vector<int> all_but_one(N);

    cout << y_combinator([&](auto self, int node, int parent) -> int {
        int all = 0;
        bool can_skip = false;

        for (int neigh : adj[node]) {
            if (neigh == parent)
                continue;

            all += self(neigh, node);
            can_skip = can_skip || all_but_one[neigh] == 0;
        }

        all_but_one[node] = all - can_skip;
        return max(all_but_one[node], 1);
    })(root, -1) << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}