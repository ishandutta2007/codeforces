typedef long long base; 
typedef pair<int, int> point;      
typedef complex<double> comp;

const int N = 3e3 + 5;

int n, root;
vector<int> edge[N];
int c[N];
int s[N];
int ans[N];

int it;

void dfs_size(int i) {
	s[i] = 1;
	for (int j : edge[i]) {
		dfs_size(j);
		s[i] += s[j];
	}
}

bool solve(int i) {
	if (!ans[i]) {
		if (s[i] == c[i] + 1) {
			ans[i] = it--;
			return true;
		}
	}
	for (int j : edge[i]) {
		if (solve(j)) {
			--s[i];
			return true;
		}
	}
	return false;
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
#ifdef ONPC
    freopen("input", "r", stdin);
#endif
    cin >> n;
    it = n;
    for (int i = 1; i <= n; ++i) {
    	int p;
    	cin >> p >> c[i];
    	if (p == 0) root = i;
    	else        edge[p].pb(i);
    }
    dfs_size(root);
    while (solve(root)) {
    	continue;
    }
   	if (it == 0) {
   		cout << "YES\n";
   		for (int i = 1; i <= n; ++i) {
   			cout << ans[i] << ' ';
   		}
   		cout << '\n';
   	} else {
   		cout << "NO\n";
   	}
}