void solve() {
	string s;
	cin >> s;
	int m;
	cin >> m;
	vector<int> b(m);
	for(int i = 0; i < m; ++i)
		cin >> b[i];
	vector<int> cnt(26);
	for(auto i: s)
		cnt[i - 'a']++;

	vector<int> used(m);
	int last = 25;
	vector<char> t(m);
	for(int i = 0; i < m; ++i) {
		vector<int> pos;
		for(int j = 0; j < m; ++j) {
			if (!used[j] && b[j] == 0) {
				pos.push_back(j);
				used[j] = 1;
			}
		}
		while(cnt[last] < pos.size())
			--last;
		for(auto j: pos)
			t[j] = last + 'a';
		for(int j = 0; j < m; ++j) {
			if (!used[j]) {
				for(auto k: pos)
					b[j] -= abs(k - j);
			}
		}
		--last;
	}
	for(auto i: t)
		cout << i;
	cout << '\n';
}

