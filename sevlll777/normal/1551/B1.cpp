void solve() {
  string s;
  cin >> s;
  vector<int> cnt(26);
  for (auto c : s) {
    cnt[c - 'a']++;
  }
  int e = 0, z = 0;
  for (auto x : cnt) {
    if (x >= 2) {
      z++;
    } else if (x == 1) {
      e++;
    }
  }
  cout << z + e / 2 << '\n';
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t;
  cin >> t;
  while (t--) {
    solve();
  }
}