const int MAXN = 300003;
node TREE[MAXN];
int Link[MAXN];


void init(int u) {
    Link[u] = 0;
    TREE[u].l = 0;
    TREE[u].r = 0;
    TREE[u].y = rand();
    TREE[u].mod = 0;
    TREE[u].pr = 0;
    TREE[u].p = 1;
}


void reverse(int u) {
    TREE[u].mod ^= 1;
}


void update(int v) {
    TREE[v].p = 1;
    if (TREE[v].l) {
        TREE[TREE[v].l].pr = v;
        TREE[v].p += TREE[TREE[v].l].p;
    }
    if (TREE[v].r) {
        TREE[TREE[v].r].pr = v;
        TREE[v].p += TREE[TREE[v].r].p;
    }
}


void push(int v) {
    if (TREE[v].mod) {
        TREE[v].mod = 0;
        swap(TREE[v].l, TREE[v].r);
        if (TREE[v].l) {
            TREE[TREE[v].l].mod ^= 1;
        }
        if (TREE[v].r) {
            TREE[TREE[v].r].mod ^= 1;
        }
    }
}


void split(int v, int x, int &l, int &r) {
    if (v == 0) {
        l = 0;r = 0;
        return;
    }
    push(v);
    int s = (TREE[v].l ? TREE[TREE[v].l].p: 0) + 1;
    if (x >= s) {
        split(TREE[v].r, x - s, TREE[v].r, r);
        l = v;
        update(l);
    } else {
        split(TREE[v].l, x, l, TREE[v].l);
        r = v;
        update(r);
    }
}   


int merge(int l, int r) {
    if (l == 0) {
        return r;
    }
    if (r == 0) {
        return l;
    }
    if (l == r) {
        return l;
    }
    push(l);
    push(r);
    if (TREE[l].y > TREE[r].y) {
        TREE[l].r = merge(TREE[l].r, r);
        update(l);
        return l;
    } else {
        TREE[r].l = merge(l, TREE[r].l);
        update(r);
        return r;
    }
}


int getleft(int v) {
    push(v);
    if (TREE[v].l) {
        return getleft(TREE[v].l);
    }
    return v;
}


inline int find_root(int v) {
    int root = v;
    while (TREE[root].pr) {
        root = TREE[root].pr;
    }
    return root;
}


vector<int> vert;


int getnum(int v) {
    int v1 = v;
    vert.clear();
    vert.push_back(v1);
    while (TREE[v1].pr) {
        v1 = TREE[v1].pr;
        vert.push_back(v1);
    }
    for (int i = (int)vert.size() - 1; i >= 0; i--) {
        push(vert[i]);
    }
    int ans = 1 + (TREE[v].l ? TREE[TREE[v].l].p: 0);
    v1 = v;
    while (TREE[v1].pr) {
        int v2 = TREE[v1].pr;
        if (TREE[v2].l == v1) {
            v1 = v2;
            continue;
        } else {
            ans += 1;
            if (TREE[v2].l) ans += TREE[TREE[v2].l].p;
        }
        v1 = v2;
    }
    return ans;
}


inline void expose(int v) {
    int l = 0, r = 0, rl, a = getnum(v);
    split(find_root(v), a, l, r);
    TREE[r].pr = 0, TREE[l].pr = 0;
    if (r) rl = getleft(r), Link[rl] = v;
    v = getleft(l);
    while (Link[v]) {
        int g = Link[v], l = 0, r = 0;
        split(find_root(g), getnum(g), l, r);
        TREE[r].pr = 0, TREE[l].pr = 0;
        if (r) {
            int gl = getleft(r); 
            Link[gl] = g;
        }
        int a = merge(l, find_root(v));
        Link[v] = 0, TREE[a].pr = 0, v = getleft(a);
    }
}


void make_root(int v) {
    expose(v);
    reverse(find_root(v));
}



int findroot(int u) {
    expose(u);
    return getleft(find_root(u));
}


bool isParent(int u, int v) {
    expose(u);
    expose(v);
    return !Link[getleft(find_root(u))];
}   


void link(int u, int v) {
    Link[u] = v;
    expose(u);
}

void cut(int u, int v) {
    expose(v);
    Link[u] = 0;
    expose(u);
}


void link_edge(int u, int v) {
    make_root(u);
    link(u, v);
}


void cut_edge(int u, int v) {
    make_root(v);
    cut(u, v);
}


