#include <bits/stdc++.h>

class DSU {
	std::vector<int> fa;

public:
	DSU(int n) : fa(n) {
		for (int i = 0; i < n; ++i) {
			fa[i] = i;
		}
	}

	int find(int x) {
		return fa[x] == x ? x : (fa[x] = find(fa[x]));
	}

	bool merge(int x, int y) {
		x = find(x), y = find(y);
		if (x == y) {
			return false;
		}
		fa[y] = x;
		return true;
	}
};

int main() {
	std::ios_base::sync_with_stdio(false);
	std::cin.tie(0);

	int m, n;
	std::cin >> m >> n;
	std::vector<int> a(m), b(n);
	for (int i = 0; i < m; ++i) {
		std::cin >> a[i];
	}
	for (int i = 0; i < n; ++i) {
		std::cin >> b[i];
	}
	
	std::vector<std::tuple<int, int, int>> E;
	for (int i = 0; i < m; ++i) {
		int k;
		std::cin >> k;
		for (int j = 0; j < k; ++j) {
			int x;
			std::cin >> x;
			--x;
			E.emplace_back(a[i] + b[x], i, x + m);
		}
	}
	std::sort(E.begin(), E.end());
	std::reverse(E.begin(), E.end());
	DSU D(m + n);
	long long ans = 0;
	for (auto [w, u, v] : E) {
		if (!D.merge(u, v)) {
			ans += w;
		}
	}
	std::cout << ans << "\n";
}