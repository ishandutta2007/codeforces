void solve() {
	int n, m;
	cin >> n >> m;
	vector<int> ans(n + m + 5);
	vector<int> kol(n + m + 5);
	vector<int> sum(n + m + 5);
	vector<vector<int>> a(n, vector<int> (m));
	for(int i = 0; i < n; ++i) {
		for(int j = 0; j < m; ++j) {
			cin >> a[i][j];
			kol[i + j] += a[i][j];
			++sum[i + j];
		}
	}
	int res = 0;
	for(int i = 0; i < n + m + 5; ++i) {
		if (i > n + m - 2 - i)
			break;
		if (i == n + m - 2 - i)
			continue;
		res += min((sum[i] - kol[i]) + sum[n + m - 2 - i] - kol[n + m - 2 - i], kol[i] + kol[n + m - 2 - i]);
	}
	cout << res << '\n';
}

