int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n;
    cin >> n;
    vector<int> p(n);
    cin >> p;
    vector<int> left(n, 0), right(n, 0);
    for (int i = 1; i < n; ++i) {
        if (p[i] > p[i - 1]) {
            left[i] = left[i - 1] + 1;
        }
    }
    for (int i = n - 2; i >= 0; --i) {
        if (p[i] > p[i + 1]) {
            right[i] = right[i + 1] + 1;
        }
    }
    int mx = max(maxe(left), maxe(right));
    int cnt = 0;
    for (int i = 0; i < n; ++i) {
        cnt += (left[i] == mx);
        cnt += (right[i] == mx);
    }
    if (cnt >= 3) {
        cout << 0 << '\n';
        return 0;
    }

    int ans = 0;
    for (int i = 0; i < n; ++i) {
        if (left[i] == mx && right[i] == mx && mx % 2 == 0) {
            ++ans;
        }
    }
    cout << ans << '\n';

    return 0;
}