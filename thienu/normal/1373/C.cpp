int INF = 1e9 + 5;

void solve(){
    string s;
    cin >> s;
    int n = s.size();
    int mini = 0;
    int pref = 0;
    ll total = 0;
    for(int i = 0; i < n; i++){
        if(s[i] == '+') pref++;
        else pref--;
        while(pref < mini){
            mini--;
            total += i + 1;
        }
    }
    cout << total + n << endl;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while(tc--){
        solve();
    }
    return 0;
}