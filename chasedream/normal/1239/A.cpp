const int N = 100005;
const int mod = 1e9 + 7;

long long f[N];
int n, m;

int main() {
  scanf("%d%d", &n, &m);
  if (n < m) swap(n, m);
  f[0] = f[1] = 1;
  for (int i = 2; i <= n; i++) {
    f[i] = (f[i - 1] + f[i - 2]) % mod;
  }
  printf("%lld\n", (f[n] + f[m] - 1) * 2 % mod);
  return 0;
}