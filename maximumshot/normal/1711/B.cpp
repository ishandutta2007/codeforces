    output fast() {
        if (m % 2 == 0) return output{0};
        vector<int> d(n);
        for (auto [u, v] : es) d[u] ^= 1, d[v] ^= 1;
        int res = inf + 10;
        for (int v = 0; v < n; v++) {
            if (d[v]) res = min(res, a[v]);
        }
        for (auto [u, v] : es) {
            if (d[u] == d[v]) res = min(res, a[u] + a[v]);
        }
        return output{res};
    }

