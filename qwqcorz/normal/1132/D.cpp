using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct node
{
	int v,a,b;
	bool operator <(const node &a)const
	{
		return v>a.v;
	}
}qwq;
priority_queue<node>q;
int n,k,a[N],b[N];
bool check(int x)
{
	while (!q.empty()) q.pop();
	for (int i=1;i<=n;i++) q.push((node){a[i]/b[i],a[i],b[i]});
	for (int i=0;i<k;i++)
	{
		qwq=q.top();
		q.pop();
		if (qwq.v<i) return 0;
		qwq.v=(qwq.a+=x)/qwq.b;
		q.push(qwq);
	}
	return 1;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	n=read(),k=read();
	for (int i=1;i<=n;i++) a[i]=read();
	for (int i=1;i<=n;i++) b[i]=read();
	int l=0,r=5e12,ans=-1;
	while (l<=r)
	{
		int mid=(l+r)/2;
		if (check(mid)) ans=mid,r=mid-1;
				   else l=mid+1;
	}
	print(ans);
	
	return 0;
}
