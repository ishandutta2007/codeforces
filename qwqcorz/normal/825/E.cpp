#include<bits/stdc++.h>
using namespace std;
const int N=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,nxt;
}e[N];
int head[N],cnte=0;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
priority_queue<int>h;
int siz[N],ans[N],cnt=0,level[N];
void topo(int n)
{
	while (!h.empty()) h.pop();
	for (int i=1;i<=n;i++) if (!siz[i]) h.push(i);
	while (!h.empty())
	{
		int now=ans[++cnt]=h.top();h.pop();
		for (int i=head[now];i;i=e[i].nxt)
		{
			int to=e[i].to;
			if (!--siz[to]) h.push(to);
		}
	}
	for (int i=1;i<=n;i++) level[ans[i]]=n-i+1;
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),m=read();
	for (int i=1;i<=m;i++)
	{
		int u=read(),v=read();
		add_edge(v,u);
		siz[u]++;
	}
	topo(n);
	for (int i=1;i<=n;i++) print(level[i],' ');
	
	return 0;
}
