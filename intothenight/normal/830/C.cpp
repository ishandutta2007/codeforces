int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	ll k;
	cin >> n >> k;
	vi a(n);
	cin >> a;
	vi singular;
	int MAX = sqrt(*max_element(all(a))) + 2;
	for(int u = 1; u <= MAX; u ++) singular.push_back(u);
	for(auto x: a){
		int MAX = sqrt(x) + 2;
		for(int u = 1; u <= MAX; u ++){
			singular.push_back((x + u - 1) / u);
		}
	}
	sort(all(singular), greater<int>{});
	singular.resize(unique(all(singular)) - singular.begin());
	for(auto d: singular){
		ll cur = 0;
		for(auto x: a){
			cur += (d - x % d) % d;
		}
		if(cur <= k){
			ll inc = 0;
			for(auto x: a){
				inc += (x + d - 1) / d;
			}
			cout << d + (k - cur) / inc;
			return 0;
		}
	}
	return 0;
}
