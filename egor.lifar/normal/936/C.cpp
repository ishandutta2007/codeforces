inline void shift(int x) {
    if (x == 0) {
        return;
    }
    //cout << s << ' ' << x << endl;
    reverse(s.begin() + n - x, s.begin() + n);
    //cout << s << endl;
    string s1;
    for (int j = n - x; j < n; j++) {
        s1 += s[j];
    }
    for (int j = 0; j < n - x; j++) {
        s1 += s[j];
    }
    s = s1;
}   


int main(){
	ios::sync_with_stdio(0);
    cin.tie(0);
   // read(FILENAME);
    cin >> n;
    cin >> s >> t; 
    vector<int> ans; 
    for (int f = n - 1; f >= 0; f--) {
        int g = n - 1 - (n - f - 1);
        int pos = -1;
      //  cout << f + 1 << endl;
      //  cout << s << ' ' << t << endl;
        for (int i = g; i >= 0; i--) {
            if (t[n - f - 1] == s[i]) {
                pos = i;
                break;
            }
        }
        if (pos == -1) {
            cout << -1 << endl;
            return 0;
        }
        if (pos == n - 1) {
            continue;
        }
       // cout << pos << endl;
        ans.push_back(n - pos - 1);
        shift(n - pos - 1);
        ans.push_back(1);
        shift(1);
        ans.push_back(n);
        shift(n);
    }
    assert(s == t);
    cout << sz(ans) << endl;
    for (auto x: ans) {
        cout << x << ' ';
    }
    cout << endl;
    return 0;
}