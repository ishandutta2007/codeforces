namespace atcoder {

template <class T> struct fenwick_tree {
    using U = internal::to_unsigned_t<T>;

  public:
    fenwick_tree() : _n(0) {}
    explicit fenwick_tree(int n) : _n(n), data(n) {}

    void add(int p, T x) {
        assert(0 <= p && p < _n);
        p++;
        while (p <= _n) {
            data[p - 1] += U(x);
            p += p & -p;
        }
    }

    T sum(int l, int r) {
        assert(0 <= l && l <= r && r <= _n);
        return sum(r) - sum(l);
    }

  private:
    int _n;
    std::vector<U> data;

    U sum(int r) {
        U s = 0;
        while (r > 0) {
            s += data[r - 1];
            r -= r & -r;
        }
        return s;
    }
};

}  // namespace atcoder

using namespace std;
int T,N;
string s,t;
queue<int>ids[26];
main()
{
	ios::sync_with_stdio(false);
	cin.tie(nullptr);
	cin>>T;
	for(;T--;)
	{
		cin>>N>>s>>t;
		atcoder::fenwick_tree<int>P(N);
		for(int i=0;i<N;i++)P.add(i,1);
		for(int i=0;i<26;i++)
		{
			while(!ids[i].empty())ids[i].pop();
		}
		for(int i=0;i<N;i++)ids[s[i]-'a'].push(i);
		long long ans=1e18;
		long long now=0;
		for(int i=0;i<N;i++)
		{
			int tc=t[i]-'a';
			for(int j=0;j<tc;j++)if(!ids[j].empty())
			{
				int x=ids[j].front();
				ans=min(ans,now+P.sum(0,x));
			}
			if(!ids[tc].empty())
			{
				int x=ids[tc].front();
				ids[tc].pop();
				now+=P.sum(0,x);
				P.add(x,-1);
			}
			else break;
		}
		if(ans==(long long)1e18)ans=-1;
		cout<<ans<<"\n";
	}
}