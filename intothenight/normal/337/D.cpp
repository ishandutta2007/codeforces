using namespace std;
using namespace chrono;
#define all(a) a.begin(), a.end()
#define sz(x) (int(x.size()))
typedef long long ll;
typedef vector<int> vi;
typedef vector<ll> vl;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
typedef pair<int, ll> pil;
typedef vector<string> vs;
template<class T1, class T2>
istream &operator>>(istream &in, pair<T1, T2> &P){
	in >> P.first >> P.second;
	return in;
}
template<class T1, class T2>
ostream &operator<<(ostream &out, pair<T1, T2> &P){
	out << "(" << P.first << ", " << P.second << ")";
	return out;
}
template<class T>
istream &operator>>(istream &in, vector<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, vector<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m, d;
	cin >> n >> m >> d;
	vi marked(n);
	for(int i = 0; i < m; i ++){
		int u;
		cin >> u, u --;
		marked[u] = true;
	}
	vector<vi> adj(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	const int MIN = -2e9;
	vi down(n, MIN), up(n, MIN), max1(n, MIN), max2(n, MIN);
	function<void(int, int)> dfs_down = [&](int u, int p){
		if(marked[u]) down[u] = 0;
		for(int v: adj[u]) if(v != p){
			dfs_down(v, u);
			if(max1[u] <= down[v]){
				max2[u] = max1[u];
				max1[u] = down[v];
			}
			else if(max2[u] < down[v]){
				max2[u] = down[v];
			}
			down[u] = max(down[u], down[v] + 1);
		}
	};
	dfs_down(0, 0);
	function<void(int, int)> dfs_up = [&](int u, int p){
		if(marked[u]) up[u] = 0;
		if(u){
			if(down[u] == max1[p]){
				up[u] = max({up[u], up[p] + 1, max2[p] + 2});
			}
			else{
				up[u] = max({up[u], up[p] + 1, max1[p] + 2});
			}
		}
		for(int v: adj[u]) if(v != p){
			dfs_up(v, u);
		}
	};
	dfs_up(0, 0);
	int res = 0;
	for(int i = 0; i < n; i ++){
		res += (up[i] <= d && down[i] <= d);
	}
	cout << res;
	return 0;
}

