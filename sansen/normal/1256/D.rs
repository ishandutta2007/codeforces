use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        q: usize,
        a: [(usize, u64, chars); q],
    }
    for (_n, k, s) in a {
        let mut ans = String::new();
        let mut cnt = 0;
        let mut inv = k;
        for c in s {
            let k = c.to_digit(10).unwrap();
            if k == 1 {
                cnt += 1;
            } else {
                while cnt > inv {
                    ans.push('1');
                    cnt -= 1;
                }
                ans.push('0');
                inv -= cnt;
            }
        }
        while cnt > 0 {
            ans.push('1');
            cnt -= 1;
        }
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}