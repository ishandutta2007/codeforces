#define pb push_back
#define fox(i,x,y) for(int i=(x);i<=(y);i++)
#define foxr(i,x,y) for(int i=(y);i>=(x);i--)

const int MN = 5e5+5;
int t, n, m, i, x, y; ll arr[MN], ans;
ll gcd(ll a,ll b){return a?gcd(b%a,a):b;}
vi adj[MN];
map<vi,ll> mp;

int main(){
    for(scanf("%d",&t);t;t--){
        for(scanf("%d%d",&n,&m),i=1;i<=n;i++){
            scanf("%lld",&arr[i]);
            adj[i].clear();
        }
        mp.clear();
        for(i=1;i<=m;i++){
            scanf("%d%d",&x,&y);
            adj[y].pb(x);
        }
        for(i=1;i<=n;i++){
            if(adj[i].empty()) continue;
            sort(adj[i].begin(),adj[i].end());
            mp[adj[i]]+=arr[i];
        }
        ans = 0;
        for(auto v : mp){
            ans = gcd(ans,v.second);
        }
        printf("%lld\n",ans);
    }
    return 0;
}