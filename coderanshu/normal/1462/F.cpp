template<typename T>
using ordered_set = tree<T , null_type ,less<T> ,rb_tree_tag ,tree_order_statistics_node_update>;

int _runtimeTerror_()
{
    int n;
    cin>>n;
    int ans = n;
    vector<pii> a(n);

    for(int i=0;i<n;++i)
        cin>>a[i].F>>a[i].S;
    ordered_set<pii> r,l;
    for(int i=0;i<n;++i)
        l.insert({a[i].F,i}),r.insert({a[i].S,i});
    for(int i=0;i<n;++i)
    {
        int cnt = 0;
        cnt += r.order_of_key({a[i].F,0});
        cnt += n - l.order_of_key({a[i].S,n+1});
        amin(ans,cnt);
    }
    cout<<ans<<"\n";
    return 0;
}

