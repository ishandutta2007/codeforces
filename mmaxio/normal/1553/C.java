	int f(char[] s) {
		for (int ans = 0; ans < s.length; ans++) {
			
			int[] min = {0, 0};
			int[] max = {0, 0};
			int[] rest = {s.length / 2, s.length / 2};
			
			for (int j = 0; j < ans; j++) {
				int who = j % 2;
				
				rest[who]--;
				min[who] += s[j] == '1' ? 1 : 0;
				max[who] += s[j] == '0' ? 0 : 1;
			}
			
			if (max[0] > min[1] + rest[1]) {
				return ans;
			}
			
			if (max[1] > min[0] + rest[0]) {
				return ans;
			}
			
		}
		return s.length;
	}

	void submit() {
		int t = nextInt();
		while (t-- > 0) {
			char[] s = nextToken().toCharArray();
			out.println(f(s));
		}
	}

