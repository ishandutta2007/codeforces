const ld eps = 1E-10;
int sub_to_errichto() {
   int T;
   cin >> T;
   while (T--) {
      int n, L;
      cin >> n >> L;
      vector<int> v(n);
      for (int &i: v) cin >> i;

      ld a = 0, b = L;
      ld as = 1, bs = 1;
      ld t = 0;
      int i = 0, j = n - 1;
      while (i <= j) {
         ld t1 = (v[i] - a) / as;
         ld t2 = (b - v[j]) / bs;
         ld dt = min(t1, t2);
         a += dt * as;
         b -= dt * bs;
         t += dt;
         if (t1 < t2) {
            as += 1;
            ++i;
         } else {
            bs += 1;
            --j;
         }
      }
      ld tfin = (b - a) / (as + bs);
      t += tfin;
      cout << t << "\n";
   }
}

