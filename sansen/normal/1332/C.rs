use std::io::Write;

fn run() {
    let out = std::io::stdout();
    let mut out = std::io::BufWriter::new(out.lock());
    input! {
        t: usize,
        ask: [(usize, usize, chars); t],
    }
    for (_n, k, s) in ask {
        let mut cnt = vec![vec![0; 26]; k];
        for (i, c) in s.into_iter().enumerate() {
            let x = std::cmp::min(i % k, k - 1 - i % k);
            let k = c.to_digit(36).unwrap() as usize - 10;
            cnt[x][k] += 1;
        }
        let mut ans = 0;
        for cnt in cnt {
            let sum = cnt.iter().fold(0, |s, a| s + *a);
            let max = *cnt.iter().max().unwrap();
            ans += sum - max;
        }
        writeln!(out, "{}", ans).ok();
    }
}

fn main() {
    run();
}