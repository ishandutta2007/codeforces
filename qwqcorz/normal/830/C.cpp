const int N=105;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N],b[N],suma=0;

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	int n=read(),k=read(),ans=0,mx=0;
	for (int i=1;i<=n;i++) b[i]=(a[i]=read())-1,suma+=a[i],mx=max(mx,b[i]);
	for (int l=1,r;l<=mx;l=r+1)
	{
		r=mx;
		int sumb=0;
		for (int i=1;i<=n;i++) if (b[i]>=l) sumb+=b[i]/l,r=min(r,b[i]/(b[i]/l));
		int d=min(r,(k+suma)/(n+sumb));
		if (d>=l) ans=d;
//		print(l,' '),print(r,':'),print(d);
	}
	if ((k+suma)/n>mx) ans=max(ans,(k+suma)/n);
	print(ans);
	
	return 0;
}
