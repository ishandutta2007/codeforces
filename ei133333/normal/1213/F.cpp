template< typename G >
struct StronglyConnectedComponents {
  const G &g;
  UnWeightedGraph gg, rg;
  vector< int > comp, order, used;

  StronglyConnectedComponents(G &g) : g(g), gg(g.size()), rg(g.size()), comp(g.size(), -1), used(g.size()) {
    for(int i = 0; i < g.size(); i++) {
      for(auto e : g[i]) {
        gg[i].emplace_back((int) e);
        rg[(int) e].emplace_back(i);
      }
    }
  }

  int operator[](int k) {
    return comp[k];
  }

  void dfs(int idx) {
    if(used[idx]) return;
    used[idx] = true;
    for(int to : gg[idx]) dfs(to);
    order.push_back(idx);
  }

  void rdfs(int idx, int cnt) {
    if(comp[idx] != -1) return;
    comp[idx] = cnt;
    for(int to : rg[idx]) rdfs(to, cnt);
  }

  void build(UnWeightedGraph &t) {
    for(int i = 0; i < gg.size(); i++) dfs(i);
    reverse(begin(order), end(order));
    int ptr = 0;
    for(int i : order) if(comp[i] == -1) rdfs(i, ptr), ptr++;

    t.resize(ptr);
    for(int i = 0; i < g.size(); i++) {
      for(auto &to : g[i]) {
        int x = comp[i], y = comp[to];
        if(x == y) continue;
        t[x].push_back(y);
      }
    }
  }
};


int main() {
  int N, K;
  cin >> N >> K;
  vector< int > P(N), Q(N);
  cin >> P >> Q;
  for(auto &p : P) p--;
  for(auto &p : Q) p--;

  UnWeightedGraph g(N);
  for(int i = 1; i < N; i++) {
    g[P[i - 1]].emplace_back(P[i]);
    g[Q[i - 1]].emplace_back(Q[i]);
  }
  StronglyConnectedComponents< UnWeightedGraph > scc(g);
  UnWeightedGraph t;
  scc.build(t);
  vector< int > deg(t.size());
  for(int i = 0; i < t.size(); i++) {
    for(auto &to : t[i]) deg[to]++;
  }

  queue< int > que;
  for(int i = 0; i < t.size(); i++) {
    if(deg[i] == 0) que.emplace(i);
  }
  vector< int > uku(t.size());
  int ptr = 0;
  while(que.size()) {
    auto idx = que.front();
    que.pop();
    uku[idx] = ptr++;
    for(auto &to : t[idx]) {
      deg[to]--;
      if(deg[to] == 0) {
        que.emplace(to);
      }
    }
  }
  if(ptr < K) {
    cout << "NO" << endl;
  } else {
    cout << "YES" << endl;
    for(int i = 0; i < N; i++) cout << (char) ('a' + min(25, uku[scc[i]]));
    cout << endl;
  }

}