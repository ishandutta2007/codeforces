int cnt(int x) {
    int sum = 0;
    while (x > 0) {
        sum += x % 10;
        x /= 10;
    }
    return sum;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("input.txt", "r", stdin);
    //freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    vector<int> ret;
    for (int x=0; x<100; x++)
        if (cnt(n - x) == x)
            ret.push_back(n - x);

    cout << ret.size() << "\n";
    for (int i=(int)ret.size()-1; i>=0; i--)
        cout << ret[i] << " \n"[i==0];

    return 0;
}