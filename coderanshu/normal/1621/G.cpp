const int MOD=1000000007;
struct Mint {
    int val;
 
    Mint(long long v = 0) {
        if (v < 0)
            v = v % MOD + MOD;
        if (v >= MOD)
            v %= MOD;
        val = v;
    }
 
    static int mod_inv(int a, int m = MOD) {
        int g = m, r = a, x = 0, y = 1;
        while (r != 0) {
            int q = g / r;
            g %= r; swap(g, r);
            x -= q * y; swap(x, y);
        } 
        return x < 0 ? x + m : x;
    } 
    explicit operator int() const {
        return val;
    }
    Mint& operator+=(const Mint &other) {
        val += other.val;
        if (val >= MOD) val -= MOD;
        return *this;
    }
    Mint& operator-=(const Mint &other) {
        val -= other.val;
        if (val < 0) val += MOD;
        return *this;
    }
    static unsigned fast_mod(uint64_t x, unsigned m = MOD) {
           #if !defined(_WIN32) || defined(_WIN64)
                return x % m;
           #endif
           unsigned x_high = x >> 32, x_low = (unsigned) x;
           unsigned quot, rem;
           asm("divl %4\n"
            : "=a" (quot), "=d" (rem)
            : "d" (x_high), "a" (x_low), "r" (m));
           return rem;
    }
    Mint& operator*=(const Mint &other) {
        val = fast_mod((uint64_t) val * other.val);
        return *this;
    }
    Mint& operator/=(const Mint &other) {
        return *this *= other.inv();
    }
    friend Mint operator+(const Mint &a, const Mint &b) { return Mint(a) += b; }
    friend Mint operator-(const Mint &a, const Mint &b) { return Mint(a) -= b; }
    friend Mint operator*(const Mint &a, const Mint &b) { return Mint(a) *= b; }
    friend Mint operator/(const Mint &a, const Mint &b) { return Mint(a) /= b; }
    Mint& operator++() {
        val = val == MOD - 1 ? 0 : val + 1;
        return *this;
    }
    Mint& operator--() {
        val = val == 0 ? MOD - 1 : val - 1;
        return *this;
    }
    Mint operator++(int32_t) { Mint before = *this; ++*this; return before; }
    Mint operator--(int32_t) { Mint before = *this; --*this; return before; }
    Mint operator-() const {
        return val == 0 ? 0 : MOD - val;
    }
    bool operator==(const Mint &other) const { return val == other.val; }
    bool operator!=(const Mint &other) const { return val != other.val; }
    Mint inv() const {
        return mod_inv(val);
    }
    Mint power(long long p) const {
        assert(p >= 0);
        Mint a = *this, result = 1;
        while (p > 0) {
            if (p & 1)
                result *= a;
 
            a *= a;
            p >>= 1;
        }
        return result;
    }
    friend ostream& operator << (ostream &stream, const Mint &m) {
        return stream << m.val;
    }
    friend istream& operator >> (istream &stream, Mint &m) {
        return stream>>m.val;   
    }
};


template<typename T=long long>
struct fenwick {
    vector<T> bit;
    int n;
    fenwick(int x) {
        n = x;
        bit.resize(x + 1, T(0));
    }
    void update(int j,T val)
    {
        for(;j<=n;j+=j&-j)
            bit[j] += val;
    }
    T get(int r)
    {
        T u = 0;
        for(;r;r-=r&-r)
            u += bit[r]; 
        return u;
    }
    T query(int l,int r)
    {
        return get(r)-get(l-1);
    }
    // kth element
    int getKth(T k) {
        int ans = 0;
        T cnt = 0;
        for(int i=20;i>=0;--i) {
            if(ans + (1 << i) <= n && cnt + bit[ans + (1 << i)] < k) {
                ans += (1 << i);
                cnt += bit[ans];
            }
        }
        if(ans == n) {
            return -1;
        }
        return ans + 1;
    }
    void insert(int x) {
        update(x, 1);
    }
    void erase(int x) {
        update(x, -1);
    }
    void clear() {
        for(int i=1;i<=n;++i) {
            bit[i] = T(0);
        }
    }
};


int _runtimeTerror_()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for(int i=0;i<n;++i) {
        cin >> a[i];
    }
    fenwick<Mint> fenw(n);
    auto b = a;
    sort(all(b));
    b.resize(unique(all(b)) - b.begin());
    for(int i=0;i<n;++i) {
        a[i] = lower_bound(all(b), a[i]) - b.begin() + 1;
    }
    vector<Mint> right(n, 0);
    for(int i=n-1;i>=0;--i) {
        right[i] = 1 + fenw.query(a[i] + 1, n);
        fenw.update(a[i], right[i]);
    }
    vector<Mint> left(n, 0);
    fenw.clear();
    vector<int> suf_max(n+1, 1);
    for(int i=n-1;i>=0;--i) {
        suf_max[i] = max(suf_max[i+1], a[i]);
    }
    vector<vector<int>> endpt(n);
    Mint ans = 0;
    for(int i=0;i<n;++i) {
        left[i] = 1 + fenw.query(1, a[i] - 1);
        fenw.update(a[i], left[i]);
        if(suf_max[i+1] > a[i]) {
            ans += left[i] * right[i];
            auto it = upper_bound(suf_max.rbegin(), suf_max.rend(), a[i]) - suf_max.rbegin();
            endpt[n - it].push_back(i);
        }
    }

    fenw.clear();
    for(int i=0;i<n;++i) {
        fenw.update(a[i], 1);
        reverse(all(endpt[i]));
        vector<pair<int,Mint>> undo = {{a[i], -1}};
        for(auto &j:endpt[i]) {
            Mint u = fenw.query(a[j] + 1, n);
            undo.push_back({a[j], -u});
            ans -= u * left[j];
            fenw.update(a[j], u);
        }
        for(auto &[l, r]:undo) {
            fenw.update(l, r);
        }
    }
    cout << ans << "\n";
    return 0;
}

