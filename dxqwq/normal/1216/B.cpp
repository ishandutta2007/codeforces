#include<bits/stdc++.h>
using namespace std;
struct node
{
	int p;
	int q;
};
node a[100003];
bool cmp(node aa,node bb)
{
	return aa.p>bb.p;
}
int main()
{
	int n;
	scanf("%d",&n);
	for(int i=1; i<=n; i++) a[i].q=i,scanf("%d",&a[i].p);
	sort(a+1,a+n+1,cmp);
	int ans=0;
	for(int i=1; i<=n; i++) ans+=a[i].p*(i-1)+1;
	printf("%d\n",ans);
	for(int i=1; i<=n; i++) printf("%d ",a[i].q);
	return 0;
}