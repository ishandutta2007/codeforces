const ll mod = 998244353;

long long mpow(long long a, long long p, long long mod = mod) {
    long long res = 1;
    while (p) {
        if (p & 1) res = res * a % mod;
        p >>= 1;
        a = a * a % mod;
    }
    return res;
}

ll inv(ll k) {
    return mpow(k, mod - 2);
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    ll n, m, k;
    cin >> n >> m >> k;
    ll invm = inv(m);
    vector<vector<ll>> dp(k + 1, vector<ll>(k + 1, 0));

    for (int i = 0; i <= k; ++i) {
        for (int j = 0; j <= k; ++j) {
            ll N = n - k + i, K = j;
            if (N <= 0) {
                dp[i][j] = 0;
                continue;
            }
            if (K == 0)
                continue;
            if (K == 1) {
                dp[i][j] = N * invm % mod;
                continue;
            }
            if (i > 0)
                dp[i][j] = (dp[i][j - 1] - dp[i - 1][j - 1] + dp[i - 1][j - 1] * invm % mod + mod) % mod * N % mod;
        }
    }
    cout << dp[k][k] << endl;

    return 0;
}