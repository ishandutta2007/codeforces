void solve(){
	

	

	int N, K;
	cin >> N >> K;
	if (K == 1){
		cout << 1 << endl;
		return;
	}
	vector<bool> done_w(N + 1, false), done_f(N + 1, false);
	vector<Mint> W(N + 1), F(N + 1);
	K = min(N, K);
	C(N, 0);

	vector<Mint> facs(K + 1);

	for(int i = 0; i <= K; ++i){
		facs[i] = (i & 1 ? -inv_fact[i] : inv_fact[i]);
		if (i > 0){
			facs[i] += facs[i - 1];
		}
	}
	auto f = [&] (int n) {
		if (done_f[n]){
			return F[n];
		}
		int k = min(n, K);

		Mint ans = 0;
		for(int j = 1; j <= k; ++j){
			Mint lhs = power(Mint(j), n) * inv_fact[j];
			ans += facs[k - j] * lhs;
		}
		F[n] = ans;
		done_f[n] = true;
		return ans;
	};

	
	function<Mint(int)> ways = [&] (int n) {
		if (done_w[n]){
			return W[n];
		}
		assert (1 <= n);
		Mint ans = f(n);


		for(int x = 2; x <= n; ){
			int g = (n + x - 1) / x;
			int y = x;
			if (g == 1){
				++x;
			}
			else{
				x = (n + g - 2) / (g - 1);
			}
			ans -= ways(g) *  (x - y);
		}

		W[n] = ans;
		done_w[n] = true;
		return ans;
	};

	cout << ways(N) << endl;
}  

