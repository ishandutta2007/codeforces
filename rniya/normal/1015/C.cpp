int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n,m; cin >> n >> m;
    priority_queue<int,vector<int>,greater<int>> pq;
    for (int i=0;i<n;++i){
        int a,b; cin >> a >> b;
        m-=b; pq.emplace(a-b);
        if (m<0){cout << -1 << '\n'; return 0;}
    }
    int ans=n;
    while (!pq.empty()){
        int x=pq.top(); pq.pop();
        if (m-x<0) break;
        m-=x; --ans;
    }
    cout << ans << '\n';
}