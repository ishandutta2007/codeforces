template <class A, class B>
bool smin(A &x, B &&y) {
  if (y < x) {
    x = y;
    return true;
  }
  return false;
}

template <class A, class B>
bool smax(A &x, B &&y) {
  if (x < y) {
    x = y;
    return true;
  }
  return false;
}

void solveTest() {
  int n; string s; cin >> n >> s;
  if (n == 1) {
    cout << s << s << '\n';
    return;
  }
  if (s[0] == s[1]) {
    cout << s[0] << s[0] << '\n';
    return;
  }
  int p = 0;
  while (p + 1 < n && s[p] >= s[p + 1]) {
    ++p;
  }
  auto t = s.substr(0, p + 1);
  cout << t;
  reverse(t.begin(), t.end());
  cout << t << '\n';
}

