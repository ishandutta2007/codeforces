int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	string s;
	cin >> s;
	deque<bitset<20>> q;
	const int MAX = 1 << 20;
	bitset<MAX> subset;
	subset.set(0);
	for(auto c: s){
		c -= 'a';
		while(!q.empty() && q.back()[c]) q.pop_back();
		for(auto &x: q) x.set(c);
		bitset<20> temp;
		temp.set(c);
		q.push_front(temp);
		for(auto &x: q){
			subset.set(x.to_ullong());
		}
	}
	vi d(MAX);
	for(int x = 1; x < MAX; x ++){
		if(subset[x]){
			int bitcnt = 0;
			for(int k = 1; k <= x; k <<= 1){
				if(k & x){
					bitcnt ++;
				}
			}
			d[x] = bitcnt;
		}
		else{
			for(int k = 1; k <= x; k <<= 1){
				if(k & x){
					d[x] = max(d[x], d[x ^ k]);
				}
			}
		}
	}
	int res = 0;
	for(int x = 0; x < MAX; x ++){
		int y = x;
		for(int k = 1; k < MAX; k <<= 1){
			y ^= k;
		}
		res = max(res, d[x] + d[y]);
	}
	cout << res;
	return 0;
}

