ll a, s;

ll solve() {
	ll ret = 0, mlt = 1;
	ll aa = a, ss = s;
	while (true) {
		if (aa == 0 && ss == 0) return ret;
		int da = aa % 10;
		int ds = ss % 10;
		aa /= 10;
		ss /= 10;
		int db = ds - da;
		if (db < 0) {
			db += 10;
			if (ss % 10 != 1) return -1;
			ss /= 10;
		}
		ret += mlt * db;
		mlt *= 10;
	}
}

