class TaskB {
    public void solve(int testNumber, InputReader in, OutputWriter out) {
		long gameCount = in.readLong();
		List<Long> answer = new ArrayList<Long>();
		for (long i = 0; gameCount >= i; i = i * 2 + 1) {
			long left = 1;
			long right = Math.min(i == 0 ? gameCount : gameCount / i, Math.round(Math.floor(Math.sqrt(gameCount * 2))) + 1);
			while (left <= right) {
				long middle = (left + right) >> 1;
				long value = i * middle + ((middle * (middle - 1)) >> 1);
				if (value == gameCount) {
					if (middle % 2 == 1)
						answer.add(middle * (i + 1));
					break;
				} else if (value < gameCount)
					left = middle + 1;
				else
					right = middle - 1;
			}
		}
		Collections.sort(answer);
		if (answer.isEmpty())
			answer.add(-1L);
		for (long i : answer)
			out.printLine(i);
    }
}

class InputReader {

	private InputStream stream;
	private byte[] buf = new byte[1024];
	private int curChar;
	private int numChars;
	private SpaceCharFilter filter;

	public InputReader(InputStream stream) {
		this.stream = stream;
	}

	public int read() {
		if (numChars == -1)
			throw new InputMismatchException();
		if (curChar >= numChars) {
			curChar = 0;
			try {
				numChars = stream.read(buf);
			} catch (IOException e) {
				throw new InputMismatchException();
			}
			if (numChars <= 0)
				return -1;
		}
		return buf[curChar++];
	}

	public long readLong() {
		int c = read();
		while (isSpaceChar(c))
			c = read();
		int sgn = 1;
		if (c == '-') {
			sgn = -1;
			c = read();
		}
		long res = 0;
		do {
			if (c < '0' || c > '9')
				throw new InputMismatchException();
			res *= 10;
			res += c - '0';
			c = read();
		} while (!isSpaceChar(c));
		return res * sgn;
	}

	public boolean isSpaceChar(int c) {
		if (filter != null)
			return filter.isSpaceChar(c);
		return isWhitespace(c);
	}

	public static boolean isWhitespace(int c) {
		return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
	}

	public interface SpaceCharFilter {
		public boolean isSpaceChar(int ch);
	}
}

class OutputWriter {
	private final PrintWriter writer;

	public OutputWriter(OutputStream outputStream) {
		writer = new PrintWriter(new BufferedWriter(new OutputStreamWriter(outputStream)));
	}

	public OutputWriter(Writer writer) {
		this.writer = new PrintWriter(writer);
	}

	public void print(Object...objects) {
		for (int i = 0; i < objects.length; i++) {
			if (i != 0)
				writer.print(' ');
			writer.print(objects[i]);
		}
	}

    public void printLine(Object...objects) {
		print(objects);
		writer.println();
	}

	public void close() {
		writer.close();
	}

	}