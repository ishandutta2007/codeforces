template<typename T>
class Compress{
    public:
    vector<T> data;
    int offset;
    Compress(vector<T> data_, int offset=0): offset(offset){
        set<T> st;
        for(T x: data_) st.insert(x);
        for(T x: st) data.push_back(x);
    };
    int operator[](T x) {
        auto p = lower_bound(data.begin(), data.end(), x);
        assert(x == *p);
        return offset+(p-data.begin());
	}
    T inv(int x){
        return data[x-offset];
    }
    int size(){
        return data.size();
    }
};

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n, m; ll k; cin >> n >> m >> k;
    vector<int> a(n), b(m);
    for(int i = 0; i < n; i++){
        cin >> a[i]; a[i]--;
    }
    for(int i = 0; i < m; i++){
        cin >> b[i]; b[i]--;
    }
    int g = gcd(n, m);
    int nn = n/g, mm = m/g;
    auto ar = vec2d(g, nn, 0);
    auto br = vec2d(g, mm, 0);
    vector<ll> same(g, 0);
    auto first_position = vec2d(g, nn+mm, -1ll);
    for(int r = 0; r < g; r++){
        vector<int> v;
        for(int i = 0; i < nn; i++){
            v.push_back(a[i*g+r]);
        }
        for(int i = 0; i < mm; i++){
            v.push_back(b[i*g+r]);
        }
        auto cp = Compress<int>(v);
        for(int i = 0; i < nn; i++) ar[r][i] = cp[a[i*g+r]];
        for(int i = 0; i < mm; i++) br[r][i] = cp[b[i*g+r]];
        int sz = cp.size();
        vector<int> a_idx(sz, -1), b_idx(sz, -1);
        for(int i = 0; i < nn; i++) a_idx[ar[r][i]] = i;
        for(int i = 0; i < mm; i++) b_idx[br[r][i]] = i;
        for(int x = 0; x < sz; x++){
            if(a_idx[x] == -1) continue;
            if(b_idx[x] == -1) continue;
            same[r]++;
            first_position[r][x] = atcoder::crt({a_idx[x], b_idx[x]}, {nn, mm}).first;
        }
    }
    auto count_diff = [&](int r, ll x) -> ll{
        ll ans = x;
        ll prod = (ll)nn*(ll)mm;
        ll cycle = x/prod;
        ans -= same[r]*cycle;
        ll rem = x%prod;
        for(int i = 0; i < nn+mm; i++){
            ll idx = first_position[r][i];
            if(idx == -1) continue;
            if(idx < rem) ans--;
        }
        return ans;
    };
    auto count = [&](ll x)->ll{
        ll rem = x%g;
        ll ans = 0;
        for(int r = 0; r < g; r++){
            if(r < rem){
                ans += count_diff(r, x/g+1);
            }else{
                ans += count_diff(r, x/g);
            }
        }
        return ans;
    };
    ll l = 0, r = max(n, m)*k;
    while(r-l > 1){
        ll x = (l+r)/2;
        if(count(x) >= k) r = x;
        else l = x;
    }
    cout << r << endl;
}