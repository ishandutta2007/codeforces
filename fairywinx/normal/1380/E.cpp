const int N = 2e5 + 228;

int P[N];
int R[N];

int res = 0;

int get(int a) {
	if (a == P[a])
		return a;
	return P[a] = get(P[a]);
}

void merge(int a, int b) {
	a = get(a), b = get(b);
	if (a == b)
		return;

	--res;
	if (R[a] == R[b])
		++R[a];
	if (R[a] < R[b]) 
		P[a] = b;
	else
		P[b] = a;
}

struct snm {
	vector<int> p, r;
	snm(int n) {
		p.resize(n);
		r.resize(n);
		for(int i = 0; i < n; ++i)
			p[i] = i;
	}

	int get(int a) {
		if (a == p[a])
			return a;
		return p[a] = get(p[a]);
	}
};

signed main() {
	setup();

	int n, m;
	cin >> n >> m;
	vector<vector<int>> a(m);
	vector<int> col(n + 2, m + 1);
	for(int i = 0; i < n + 2; ++i)
		R[i] = 0, P[i] = i;
	for(int i = 0; i < n; ++i) {
		int type;
		cin >> type;
		a[type - 1].push_back(i + 1);
		col[i + 1] = type - 1;
	}

	res = n;
	for(int i = 0; i < m; ++i) {
		for(auto j: a[i]) {
			if (col[j - 1] == col[j])
				merge(j - 1, j);
			if (col[j] == col[j + 1])
				merge(j + 1, j);
		}
	}
	// cout << res << '\n';
	// for(int i = 0; i < n; ++i) 
	// 	cout << get(i + 1) << ' ';
	// cout << '\n';
	cout << res - 1 << '\n';

	snm A(m + 2);
	while(m > 1) {
		--m;
		int b1, b2;
		cin >> b1 >> b2;
		--b1, --b2;
		b1 = A.get(b1);
		b2 = A.get(b2);
		// cout << "MEOW\n";
		// cout << b1 << ' ' << b2 << '\n''
		if (a[b1].size() < a[b2].size()) {
			for(auto i: a[b1])
				col[i] = b2;
			for(auto i: a[b1]) {
				if (A.get(col[i - 1]) == b2)
					merge(i - 1, i);
				if (A.get(col[i + 1]) == b2)
					merge(i + 1, i);
				a[b2].push_back(i);
			}
			A.p[b1] = b2;
			// A.merge(b1, b2);
		}
		else {
			for(auto i: a[b2])
				col[i] = b1;
			for(auto i: a[b2]) {
				if (A.get(col[i - 1]) == b1) 
					merge(i - 1, i);
				if (A.get(col[i + 1]) == b1)
					merge(i + 1, i);
				a[b1].push_back(i);
			}
			A.p[b2] = b1;
		}
		cout << res - 1 << '\n';
	}
	
	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

