#include <bits/stdc++.h>

int T, n, m, x, y;

int main() {
    std::ios::sync_with_stdio(0);
    std::cin.tie(0), std::cout.tie(0);
    std::cin >> T;
    while (T--) {
        std::cin >> n >> m >> x >> y;
        std::cout << 1 << ' ' << 1 << ' ' << n << ' ' << m << std::endl;
    }
    return 0;
}