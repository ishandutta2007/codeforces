int main() {
  int n, k;
  cin >> n >> k;
  vector<vector<int>> v;
  ll A = 0, B = 0;
  for (int i = 0; i < n; ++i) {
    int a, b;
    cin >> a >> b;
    A += a;
    B += b;
    vector<int> w;
    for (int j = 1; j < k; ++j) {
      if (j <= a && k - j <= b) w.push_back(j);
    }
    v.push_back(w);
  }

  bitset<1111> can;
  can[0] = 1;
  pp(v);
  for (auto v1: v) {
    bitset<1111> nxt;
    for (auto val: v1) {
      nxt |= (can << val);
    }
    can |= nxt;
    can |= can >> k;
  }

  pp(A, B, k);
  ll ans = A / k + B / k;
  A %= k;
  B %= k;
  int lo = (k - B);
  for (int i = lo; i <= A; ++i) {
    if (can[i]) {
      ++ans;
      break;
    }
  }
  cout << ans << "\n";
}

