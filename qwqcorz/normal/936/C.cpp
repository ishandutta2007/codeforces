#include<bits/stdc++.h>
using namespace std;
const int N=2e3+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
vector<int>ans;
int n;
char s[N],t[N];
void shift(int x)
{
	ans.push_back(x);
	reverse(s+1,s+1+n-x);
	reverse(s+1,s+1+n);
}
int cnt[26];

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	n=read();
	scanf("%s%s",s+1,t+1);
	for (int i=1;i<=n;i++) cnt[s[i]-'a']++,cnt[t[i]-'a']--;
	for (int i=0;i<26;i++) if (cnt[i]) return puts("-1");
	for (int i=1;i<=n;i++)
	for (int j=1;j<=n-i+1;j++)
	if (s[j]==t[i])
	{
		shift(n-j);
		shift(1);
		shift(n);
		break;
	}
	print(ans.size());
	for (int i:ans) print(i,' ');
	
	return 0;
}
