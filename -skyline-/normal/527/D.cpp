using namespace std;
int n,ans,k=-2147483648;
pair<int,int> p[200005];
int main()
{
    scanf("%d",&n);
    for(int i=1;i<=n;i++){
            int x,w;
            scanf("%d%d",&x,&w);
            p[i]=make_pair(x+w,x-w);
    }
    sort(p+1,p+n+1);
    for(int i=1;i<=n;i++){
            if(p[i].second<k) continue;
            ans++,k=p[i].first;
    }
    printf("%d\n",ans);               
	//system("pause");
	return 0;
}