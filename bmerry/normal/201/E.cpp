static ll solveable(ll n, ll m, ll q)
{
    ll bits = q * m;
    ll rem = n;
    ll ch = 1;
    for (int i = 0; i <= q && bits >= 0; i++)
    {
        if (rem <= ch)
        {
            bits -= rem * i;
            return bits >= 0;
        }
        rem -= ch;
        bits -= ch * i;
        ch = ch * (q - i) / (i + 1);
    }
    return false;
}

int main(int argc, const char **argv)
{
    redirect(argc, argv);
    int T;
    cin >> T;
    for (int cas = 0; cas < T; cas++)
    {
        ll n, m;
        cin >> n >> m;
        ll L = -1;
        ll R = n;
        while (R - L > 1)
        {
            ll M = (L + R) / 2;
            if (solveable(n, m, M))
                R = M;
            else
                L = M;
        }
        cout << R << '\n';
    }

    return 0;
}