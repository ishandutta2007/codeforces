fn main() {
    input! {
        n: usize,
        s: bytes,
    }
    let mut memo = s.iter().map(|s| (*s - b'a') as u32).collect::<Vec<_>>();
    let mut ord = (0..s.len()).collect::<Vec<_>>();
    ord.sort_by_cached_key(|p| memo[*p]);
    for i in 0..n {
        let key = |x: usize| (memo[x], memo[x ^ (1 << i)]);
        ord.sort_by_cached_key(|x| key(*x));
        let mut next = vec![0; 1 << n];
        let mut pre = key(ord[0]);
        let mut id = 0;
        for &x in ord[1..].iter() {
            let k = key(x);
            if k != pre {
                id += 1;
            }
            next[x] = id;
            pre = k;
        }
        memo = next;
    }
    use util::*;
    println!("{}", (0..s.len()).map(|p| s[ord[0] ^ p] as char).join(""));
}

mod util {
    pub trait Join {
        fn join(self, sep: &str) -> String;
    }

    impl<T, I> Join for I
    where
        I: Iterator<Item = T>,
        T: std::fmt::Display,
    {
        fn join(self, sep: &str) -> String {
            let mut s = String::new();
            use std::fmt::*;
            for (i, v) in self.enumerate() {
                if i > 0 {
                    write!(&mut s, "{}", sep).ok();
                }
                write!(&mut s, "{}", v).ok();
            }
            s
        }
    }
}
