class InputReader {
public:
    InputReader() {
        input_file = stdin;
        cursor = 0;
        fread(buffer, SIZE, 1, input_file);
    }

    InputReader(const char *file_name) {
        input_file = fopen(file_name, "r");
        cursor = 0;
        fread(buffer, SIZE, 1, input_file);
    }

    inline InputReader &operator>>(int &n) {
        while ((buffer[cursor] < '0' || buffer[cursor] > '9') && buffer[cursor] != '-') {
            advance();
        }
        int sign = 1;
        if (buffer[cursor] == '-') {
            sign = -1;
            advance();
        }
        n = 0;
        while ('0' <= buffer[cursor] && buffer[cursor] <= '9') {
            n = n * 10 + buffer[cursor] - '0';
            advance();
        }
        n *= sign;
        return *this;
    }

private:
    FILE *input_file;
    static const int SIZE = 1 << 17;
    int cursor;
    char buffer[SIZE];

    inline void advance() {
        ++cursor;
        if (cursor == SIZE) {
            cursor = 0;
            fread(buffer, SIZE, 1, input_file);
        }
    }
};

const int MAXN = 300000;

int v[1 + MAXN];
int finish[1 + MAXN + 1], dp[1 + MAXN];
map<int, int> findNext[1 + MAXN + 1];

int main() {
    timeBegin();
    //ifstream cin("input.in");
    //ofstream cout("output.out");
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    srand(time(0));
    int tests;
    cin >> tests;
    for (int test = 1; test <= tests; test++) {
        int n;
        cin >> n;
        for (int i = 1; i <= n; i++) {
            cin >> v[i];
        }
        for (int i = 1; i <= n + 1; i++) {
            finish[i] = n + 1;
            findNext[i].clear();
            dp[i] = 0;
        }
        long long answer = 0;
        for (int i = n; i >= 1; i--) {
            if (findNext[i + 1].find(v[i]) != findNext[i + 1].end()) {
                int p = findNext[i + 1][v[i]];
                finish[i] = p;
                swap(findNext[i], findNext[p + 1]);
                if (p < n) {
                    findNext[i][v[p + 1]] = p + 1;
                }
            }
            findNext[i][v[i]] = i;
            if (finish[i] != n + 1) {
                dp[i] = 1 + dp[finish[i] + 1];
            }
            answer += dp[i];
        }
        cout << answer << "\n";
    }
    return 0;
}