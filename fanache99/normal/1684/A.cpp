    int tests;
    cin >> tests;
    for (int test = 1; test <= tests; test++) {
        string s;
        cin >> s;
        if (s.size() == 2) {
            swap(s[0], s[1]);
            cout << s[0] << "\n";
        } else {
            int mn = 9;
            for (auto ch : s) {
                mn = min(mn, ch - '0');
            }
            cout << mn << "\n";
        }
    }

    auto stopTime = high_resolution_clock::now();
    auto duration = duration_cast<milliseconds>(stopTime - startTime);
