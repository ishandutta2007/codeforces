	void solve()
	{
		int n = ni();
		int[] from = new int[n-1];
		int[] to = new int[n-1];
		for(int i = 0;i < n-1;i++){
			from[i] = ni();
			to[i] = i+1;
		}
		int[][] g = packU(n, from, to);
		int[] f = na(n);
		int root = -1;
		for(int i = 0;i < n;i++){
			if(f[i] == 1){
				root = i;
				break;
			}
		}
		if(root == -1){
			out.println(1);
			return;
		}
		int[][] pars = parents3(g, root);
		int[] par = pars[0], ord = pars[1];
		long[][] dp = new long[n][2];
		for(int i = n-1;i >= 0;i--){
			int cur = ord[i];
			if(f[cur] == 0){
				long z = 1;
				int q = 0;
				long[] z0 = new long[g[cur].length];
				long[] o0 = new long[g[cur].length];
				Arrays.fill(z0, 1);
				for(int e : g[cur]){
					if(e != par[cur]){
						z = z * (dp[e][0]+dp[e][1]) % mod;
						z0[q] = dp[e][0]+dp[e][1];
						o0[q] = dp[e][1];
						q++;
					}
				}
				
				long[] prefix = new long[q+1];
				prefix[0] = 1;
				for(int j = 1;j <= q;j++){
					prefix[j] = prefix[j-1] * z0[j-1] % mod;
				}
				long[] suffix = new long[q+1];
				suffix[q] = 1;
				for(int j = q-1;j >= 0;j--){
					suffix[j] = suffix[j+1] * z0[j] % mod;
				}
				
				long o = 0;
				for(int j = 0;j < q;j++){
					o += o0[j] * prefix[j] % mod * suffix[j+1] % mod;
				}
				o %= mod;
				dp[cur][0] = z;
				dp[cur][1] = o;
			}else{
				long o = 1;
				for(int e : g[cur]){
					if(e != par[cur]){
						o = o * (dp[e][0]+dp[e][1]) % mod;
					}
				}
				dp[cur][0] = 0;
				dp[cur][1] = o;
			}
		}
		out.println(dp[root][1]);
	}
	
	public static int[][] enumFIF(int n, int mod) {
		int[] f = new int[n + 1];
		int[] invf = new int[n + 1];
		f[0] = 1;
		for (int i = 1; i <= n; i++) {
			f[i] = (int) ((long) f[i - 1] * i % mod);
		}
		long a = f[n];
		long b = mod;
		long p = 1, q = 0;
		while (b > 0) {
			long c = a / b;
			long d;
			d = a;
			a = b;
			b = d % b;
			d = p;
			p = q;
			q = d - c * q;
		}
		invf[n] = (int) (p < 0 ? p + mod : p);
		for (int i = n - 1; i >= 0; i--) {
			invf[i] = (int) ((long) invf[i + 1] * (i + 1) % mod);
		}
		return new int[][] { f, invf };
	}
	
	public static long invl(long a, long mod) {
		long b = mod;
		long p = 1, q = 0;
		while (b > 0) {
			long c = a / b;
			long d;
			d = a;
			a = b;
			b = d % b;
			d = p;
			p = q;
			q = d - c * q;
		}
		return p < 0 ? p + mod : p;
	}
	
	public static long C(int n, int r, int mod, int[][] fif)
	{
		if(n < 0 || r < 0 || r > n)return 0;
		return (long)fif[0][n]*fif[1][r]%mod*fif[1][n-r]%mod;
	}
	
	
	static int[][] packU(int n, int[] from, int[] to) {
		int[][] g = new int[n][];
		int[] p = new int[n];
		for (int f : from)
			p[f]++;
		for (int t : to)
			p[t]++;
		for (int i = 0; i < n; i++)
			g[i] = new int[p[i]];
		for (int i = 0; i < from.length; i++) {
			g[from[i]][--p[from[i]]] = to[i];
			g[to[i]][--p[to[i]]] = from[i];
		}
		return g;
	}
	
	public static int[][] parents3(int[][] g, int root) {
		int n = g.length;
		int[] par = new int[n];
		Arrays.fill(par, -1);

		int[] depth = new int[n];
		depth[0] = 0;

		int[] q = new int[n];
		q[0] = root;
		for (int p = 0, r = 1; p < r; p++) {
			int cur = q[p];
			for (int nex : g[cur]) {
				if (par[cur] != nex) {
					q[r++] = nex;
					par[nex] = cur;
					depth[nex] = depth[cur] + 1;
				}
			}
		}
		return new int[][] { par, q, depth };
	}
	
