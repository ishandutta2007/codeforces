template<class T>
T kadane(const vector<T> &arr){
	int n = arr.size();
	T lm = 0, gm = 0;
	for(int i = 0; i < n; i ++){
		lm = max(arr[i], arr[i] + lm);
		gm = max(gm, lm);
	}
	return gm;
}

