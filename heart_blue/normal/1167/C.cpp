const LL INF = 1e9 + 7;
const int N = 1e6 + 10;
class UnionFind
{
public:
	int p[N];
	void init(int n = N)
	{
		memset(p, -1, sizeof(int) * n);
	}
	int treesize(int x)
	{
		return -p[Find(x)];
	}
	int Find(int x)
	{
		int s = x;
		while (p[s] >= 0) s = p[s];
		while (x != s)
		{
			int t = p[x];
			p[x] = s;
			x = t;
		}
		return s;
	}
	void Union(int x, int y)
	{
		int px = Find(x);
		int py = Find(y);
		if (p[px] > p[py]) swap(px, py);
		p[px] += p[py];
		p[py] = px;
	}
} uf;
int main()
{
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int n, m;
	scanf("%d%d", &n, &m);
	uf.init();
	for (int i = 1; i <= m; i++)
	{
		int sz;
		scanf("%d", &sz);
		int pre = 0;
		while (sz--)
		{
			int x;
			scanf("%d", &x);
			if (pre && uf.Find(pre) != uf.Find(x))
				uf.Union(pre, x);
			pre = x;
		}
	}
	for (int i = 1; i <= n; i++)
	{
		printf("%d ", uf.treesize(i));
	}
	return 0;
}