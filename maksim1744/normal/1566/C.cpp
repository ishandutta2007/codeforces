void test_case(int test) {
    int n;
    cin >> n;
    vector<string> v(2);
    cin >> v;

    vector<bool> cut(n - 1, false);
    int has0 = 0;
    int has1 = 0;
    int ans = 0;
    for (int i = 0; i < n; ++i) {
        has0 |= (v[0][i] == '0' || v[1][i] == '0');
        has1 |= (v[0][i] == '1' || v[1][i] == '1');

        auto ad = [&]() {
            if (!has0) ans += 0;
            else if (!has1) ans += 1;
            else ans += 2;
        };

        if (i != n - 1) {
            if ((has0 && has1) || (has0 && (v[0][i + 1] == '0' || v[1][i + 1] == '0'))) {
                cut[i] = true;
                ad();
                has0 = has1 = 0;
            }
        } else {
            ad();
        }
    }
    show(cut);
    cout << ans << '\n';
}

