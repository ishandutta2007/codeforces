const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 200010, LOG=20;

int n, m, k, u, v, x, y, t, a, b;
int A[MAXN*2];
int B[MAXN*2];
char ans[MAXN*2];
bitset<MAXN*2> mark;
vector<pii> G[MAXN*2];

void tour(int node){
	while (!G[node].empty()){
		pii p=G[node].back();
		G[node].pop_back();
		int v=p.first, i=p.second;
		if (mark[i]) continue ;
		mark[i]=1;
		tour(v);
		if (A[i]==v) ans[i]='b';
		else ans[i]='r';
	}
}

void addedge(int u, int v){
	G[u].pb({v, ++t});
	G[v].pb({u, t});
}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n;
	for (int i=1; i<=n; i++){
		cin>>A[i]>>B[i];
		B[i]+=MAXN;
		addedge(A[i], B[i]);
	}
	vector<int> odd;
	for (int i=1; i<2*MAXN; i++) if (G[i].size()&1) odd.pb(i);
	for (int i=0; i<odd.size(); i+=2) addedge(odd[i], odd[i+1]);
	
	for (int i=1; i<2*MAXN; i++) tour(i);
	for (int i=1; i<=n; i++) cout<<ans[i];
	
	return 0;
}
