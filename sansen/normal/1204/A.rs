fn run() {
    input! {
        s: chars,
    }
    if s[0] == '0' {
        println!("0");
        return;
    }
    let l = s.len();
    if l % 2 == 0 {
        println!("{}", l / 2);
    } else {
        let mut ans = (l + 1) / 2;
        if !s[1..].iter().any(|c| *c == '1') {
            ans -= 1;
        }
        println!("{}", ans);
    }
}

fn main() {
    run();
}