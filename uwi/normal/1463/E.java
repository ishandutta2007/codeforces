	void check(int[] ord, int[] a, int[] from, int[] to)
	{
		int n = ord.length;
		for(int i = 0;i < n;i++){
			if(a[i] == -1)continue;
			int st = 0;
			for(int j = 0;j < n;j++){
				if(ord[j] == a[i])st = 1;
				if(ord[j] == i){
					if(st == 0){
						assert false;
					}
				}
			}
		}
		for(int i = 0;i < from.length;i++){
			int f = from[i], t = to[i];
			for(int j = 0;j < n;j++){
				if(ord[j] == f){
					if(j == n-1 || ord[j+1] != t){
						assert false;
					}
				}
			}
		}
	}

	public static class DJSet {
		public int[] upper;

		public DJSet(int n) {
			upper = new int[n];
			Arrays.fill(upper, -1);
		}

		public int root(int x) {
			return upper[x] < 0 ? x : (upper[x] = root(upper[x]));
		}

		public boolean equiv(int x, int y) {
			return root(x) == root(y);
		}

		public boolean union(int x, int y) {
			x = root(x);
			y = root(y);
			if (x != y) {
				if (upper[y] < upper[x]) {
					int d = x;
					x = y;
					y = d;
				}
				upper[x] += upper[y];
				upper[y] = x;
			}
			return x == y;
		}

		public int count() {
			int ct = 0;
			for (int u : upper) if (u < 0) ct++;
			return ct;
		}

		public int[][] toBucket() {
			int n = upper.length;
			int[][] ret = new int[n][];
			int[] rp = new int[n];
			for (int i = 0; i < n; i++) if (upper[i] < 0) ret[i] = new int[-upper[i]];
			for (int i = 0; i < n; i++) {
				int r = root(i);
				ret[r][rp[r]++] = i;
			}
			return ret;
		}
	}


	public static int[] sortTopologically(int[][] g)
	{
		int n = g.length;
		int[] ec = new int[n];
		for(int i = 0;i < n;i++){
			for(int to : g[i])ec[to]++;
		}
		int[] ret = new int[n];
		int q = 0;

		// sources
		for(int i = 0;i < n;i++){
			if(ec[i] == 0)ret[q++] = i;
		}

		for(int p = 0;p < q;p++){
			for(int to : g[ret[p]]){
				if(--ec[to] == 0)ret[q++] = to;
			}
		}
		// loop
		for(int i = 0;i < n;i++){
			if(ec[i] > 0)return null;
		}
		return ret;
	}


	public static int[] sortTopologically(int[][] g, int[] next)
	{
		int n = g.length;
		int[] ec = new int[n];
		for(int i = 0;i < n;i++){
			for(int to : g[i])ec[to]++;
		}

		Deque<Integer> dq = new ArrayDeque<>();

		// sources
		int[] ret = new int[n];
		for(int i = 0;i < n;i++){
			if(ec[i] == 0)dq.addLast(i);
		}

		int p = 0;
		while(!dq.isEmpty()){
			int cur = dq.pollFirst();
			ret[p++] = cur;
			for(int to : g[cur]){
				if(--ec[to] == 0){
					if(next[cur] == to){
						dq.addFirst(next[cur]);
					}else{
						dq.addLast(to);
					}
				}
			}
		}
		// loop
		for(int i = 0;i < n;i++){
			if(ec[i] > 0)return null;
		}
		return ret;
	}


	public static int[][] packD(int n, int[] from, int[] to) {
		return packD(n, from, to, from.length);
	}

	public static int[][] packD(int n, int[] from, int[] to, int sup) {
		int[][] g = new int[n][];
		int[] p = new int[n];
		for (int i = 0; i < sup; i++) p[from[i]]++;
		for (int i = 0; i < n; i++) g[i] = new int[p[i]];
		for (int i = 0; i < sup; i++) {
			g[from[i]][--p[from[i]]] = to[i];
		}
		return g;
	}


