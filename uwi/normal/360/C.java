	void solve()
	{
		int n = ni(), K = ni();
		char[] s = ns(n);
		int mod = 1000000007;
		long[][] dp = new long[n+1][K+1];
		long[][] dpsum = new long[n+1][K+1];
		dp[0][0] = 1;
		dpsum[0][0] = 1;
		for(int i = 0;i < n;i++){
			for(int j = 0;j <= K;j++){
				long sum = 0;
				for(int k = i;k >= 0 && j-(n-i)*(i+1-k) >= 0;k--){
					sum += dp[k][j-(n-i)*(i+1-k)]*(26-1-(s[i]-'a'));
				}
				sum += dpsum[i][j]*(s[i]-'a');
				dp[i+1][j] = sum % mod;
				dpsum[i+1][j] = dpsum[i][j] + dp[i+1][j];
				if(dpsum[i+1][j] >= mod)dpsum[i+1][j] -= mod;
			}
		}
		long ret = 0;
		for(int i = 0;i <= n;i++){
			ret += dp[i][K];
		}
		out.println(ret % mod);
	}
	
