lli solve(int n, int add = 0) {
    if (n < add) return 0;
    if (n == 0) return 1;
    int r = n % 10, ra = add % 10;
    n /= 10, add /= 10;
    lli ans = 0;
    if (ra <= r) ans += solve(n, add) * way(r - ra);
    return ans + solve(n, add + 10) * way(r - ra + 10);
}

