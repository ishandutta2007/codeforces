mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	auto query = [&](int x, int y){
		cout << "? " << x << " " << y << endl;
		char resp;
		cin >> resp;
		if(resp == 'e'){
			exit(0);
		}
		return resp;
	};
	auto answer = [&](int x){
		cout << "! " << x << endl;
	};
	string beg;
	while(cin >> beg){
		if(beg != "start"){
			return 0;
		}
		if(query(1, 2) == 'x' && query(2, 1) == 'x'){
			answer(1);
			continue;
		}
		for(int l = 1, r = 2; ; l *= 2, r *= 2){
			if(query(l, r) == 'x'){ // (l, r]
				while(r - l > 1){
					int m = l + r >> 1;
					if(query(l, m) == 'x'){
						r = m;
					}
					else{
						l = m;
					}
				}
				answer(r);
				break;
			}
		}
	}
	return 0;
}

