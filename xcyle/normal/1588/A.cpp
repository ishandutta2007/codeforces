using namespace std;
inline int read()
{
	int x = 0, w = 0; char ch = getchar();
	while(ch < '0' || ch > '9'){w |= ch == '-';ch = getchar();}
	while(ch >= '0' && ch <= '9'){x = (x << 3) + (x << 1) + ch - '0';ch = getchar();}
	return w ? -x : x;
}
int n, a[20005], b[20005];
void solve()
{
	scanf("%d", &n);
	for (int i = 1; i <= n; i++) scanf("%d", &a[i]);
	for (int i = 1; i <= n; i++) scanf("%d", &b[i]);
	sort(a + 1, a + n + 1);
	sort(b + 1, b + n + 1);
	int flag = 1;
	for (int i = 1; i <= n; i++)
	{
		if(a[i] != b[i] && a[i] != b[i] - 1) flag = -1;
	}
	if(flag == -1) printf("NO\n");
	else printf("YES\n");
} 
int main()
{
	int T = read();
	while(T--) solve();
	return 0;
}