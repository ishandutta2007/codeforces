int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	auto __solve_tc = [&](auto __tc_num)->int{
		int n;
		string s, t;
		cin >> n >> s >> t;
		array<int, 4> dp{};
		auto calc = [&](int mask)->int{
			if(~mask & 1) return 0;
			else if(~mask & 2) return 1;
			else return 2;
		};
		for(auto mask = 0; mask < 4; ++ mask){
			dp[mask] = numeric_limits<int>::min() / 2;
		}
		dp[1 << s[0] - '0' | 1 << t[0] - '0'] = 0;
		for(auto i = 1; i < n; ++ i){
			array<int, 4> dp_next;
			dp_next.fill(numeric_limits<int>::min());
			for(auto mask = 0; mask < 4; ++ mask){
				int nmask = 1 << s[i] - '0' | 1 << t[i] - '0';
				ctmax(dp_next[nmask], dp[mask] + calc(mask));
				ctmax(dp_next[nmask | mask], dp[mask]);
			}
			swap(dp, dp_next);
			debug(s[i], t[i], dp);
		}
		int res = 0;
		for(auto mask = 0; mask < 4; ++ mask){
			ctmax(res, dp[mask] + calc(mask));
		}
		cout << res << "\n";
		return 0;
	};
	int __tc_cnt;
	cin >> __tc_cnt;
	for(auto __tc_num = 0; __tc_num < __tc_cnt; ++ __tc_num){
		__solve_tc(__tc_num);
	}
	return 0;
}

