bool good(int n) {
	if (n == 0) return true;
	if (n % 3 == 2) return false;
	return good(n / 3);
}

int solve() {
	int n;
	cin >> n;
	n--;
	while (++n)
		if (good(n))
			return cout << n << '\n', 0;
}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0); cout.tie(0);
	int q;
	cin >> q;
	while (q--)
		solve();
	return 0;
}