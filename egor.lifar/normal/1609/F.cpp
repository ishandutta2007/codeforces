const int Mod = 1000000007;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}


const int MAXN = 1000228;


int n;
ll a[MAXN];
int lmin[MAXN], rmin[MAXN];
int lmax[MAXN], rmax[MAXN];
ll ans = 0;
vector<int> who[64], who1[64];
vector<pair<int, int> > z[64], z1[64];



void calc(int l, int r) {
    if (l == r) {
        ans++;
        return;
    }
    int mid = (l + r) >> 1;
    for (int t = 0; t < 64; t++) {
        who[t].clear();
        who1[t].clear();
        z[t].clear();
        z1[t].clear();
    }
    int opt = mid;
    int opt1 = mid;
    for (int i = mid; i >= l; i--) {
        if (a[i] < a[opt]) {
            opt = i;
        }
        if (a[i] > a[opt1]) {
            opt1 = i;
        }
        int d = min(r, min(rmin[opt], rmax[opt1]));
        if (__builtin_popcountll(a[opt]) == __builtin_popcountll(a[opt1])) {
            ans += max(0, d - mid);
        }
    }
    opt = mid + 1;
    opt1 = mid + 1;
    for (int i = mid + 1; i <= r; i++) {
        if (a[i] <= a[opt]) {
            opt = i;
        }
        if (a[i] >= a[opt1]) {
            opt1 = i;
        }
        who[__builtin_popcountll(a[opt])].pb(i);
        who1[__builtin_popcountll(a[opt1])].pb(i);
        int d = max(l, max(lmin[opt], lmax[opt1]));
        if (__builtin_popcountll(a[opt]) == __builtin_popcountll(a[opt1])) {
            ans += max(0, mid - d + 1);
            //cout << d << ' ' << mid << endl;
        }
    }
    opt = mid;
    opt1 = mid;
    for (int i = mid; i >= l; i--) {
        if (a[i] < a[opt]) {
            opt = i;
        }
        if (a[i] > a[opt1]) {
            opt1 = i;
        }
        {
            int d = rmin[opt];
            int d1 = rmax[opt1] + 1;
            chkmax(d1, mid + 1);
            chkmin(d, r);
            int f = __builtin_popcountll(a[opt]);
            if (d1 <= d) {
                z1[f].pb(mp(d1, d));
            }
        }
        {
            int d = rmax[opt1];
            int d1 = rmin[opt] + 1;
            chkmax(d1, mid + 1);
            chkmin(d, r);
            int f = __builtin_popcountll(a[opt1]);
            if (d1 <= d) {
                z[f].pb(mp(d1, d));
            }
        }
    }
    for (int t = 0; t < 64; t++) {
        int luk = 0;
        int ruk = -1;
        for (int j = 0; j < sz(z[t]) - 1; j++) {
            assert(z[t][j].first <= z[t][j + 1].first);
            assert(z[t][j].second <= z[t][j + 1].second);
        }
        for (auto x: z[t]) {
            while (luk < sz(who[t]) && who[t][luk] < x.first) {
                luk++;
            }
            while (ruk + 1 < sz(who[t]) && who[t][ruk + 1] <= x.second) {
                ruk++;
            }
            if (luk <= ruk) {
                ans += ruk - luk + 1;
            }
        }
    }
    for (int t = 0; t < 64; t++) {
        int luk = 0;
        int ruk = -1;
        for (int j = 0; j < sz(z1[t]) - 1; j++) {
            assert(z1[t][j].first <= z1[t][j + 1].first);
            assert(z1[t][j].second <= z1[t][j + 1].second);
        }
        for (auto x: z1[t]) {
            while (luk < sz(who1[t]) && who1[t][luk] < x.first) {
                luk++;
            }
            while (ruk + 1 < sz(who1[t]) && who1[t][ruk + 1] <= x.second) {
                ruk++;
            }
            if (luk <= ruk) {
          //      cout << luk << ' ' << ruk << ' ' << x.first << ' ' << x.second << endl;
                ans += ruk - luk + 1;
            }
        }
    }
    calc(l, mid);
    calc(mid + 1, r);
}

void solve() {
    cin >> n;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }   
    {
        vector<ll> st;
        for (int i = 0; i < n; i++) {
            while (!st.empty() && a[st.back()] >= a[i]) {
                st.pop_back();
            }
            lmin[i] = (st.empty() ? 0: st.back() + 1);
            st.pb(i);
        }
    }
    {
        vector<ll> st;
        for (int i = 0; i < n; i++) {
            while (!st.empty() && a[st.back()] <= a[i]) {
                st.pop_back();
            }
            lmax[i] = (st.empty() ? 0: st.back() + 1);
            st.pb(i);
        }
    }
    {
        vector<ll> st;
        for (int i = n - 1; i >= 0; i--) {
            while (!st.empty() && a[st.back()] > a[i]) {
                st.pop_back();
            }
            rmin[i] = (st.empty() ? n - 1: st.back() - 1);
            st.pb(i);
        }
    }
    {
        vector<ll> st;
        for (int i = n - 1; i >= 0; i--) {
            while (!st.empty() && a[st.back()] < a[i]) {
                st.pop_back();
            }
            rmax[i] = (st.empty() ? n - 1: st.back() - 1);
            st.pb(i);
        }
    }
    ans = 0;
    calc(0, n - 1);
    cout << ans << '\n';
}


