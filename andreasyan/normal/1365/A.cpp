int n, m;
int a[N][N];

void solv()
{
    scanf("%d%d", &n, &m);
    set<int> s, t;
    for (int i = 1; i <= n; ++i)
    {
        for (int j = 1; j <= m; ++j)
        {
            scanf("%d", &a[i][j]);
            if (a[i][j])
            {
                t.insert(i);
                s.insert(j);
            }
        }
    }
    if (min(n - sz(t), m - sz(s)) % 2 == 0)
        printf("Vivek\n");
    else
        printf("Ashish\n");
}

