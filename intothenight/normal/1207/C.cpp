template<class T1, class T2>
istream &operator>>(istream &in, pair<T1, T2> &P){
	in >> P.first >> P.second;
	return in;
}
template<class T1, class T2>
ostream &operator<<(ostream &out, pair<T1, T2> &P){
	out << "(" << P.first << ", " << P.second << ")";
	return out;
}
template<class T>
istream &operator>>(istream &in, vector<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, vector<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int t;
	cin >> t;
	while(t --){
		int n;
		ll a, b;
		cin >> n >> a >> b;
		string s;
		cin >> s;
		vector<vl> d(n + 1, vl(2));
		const ll inf = 4e18;
		d[0][0] = b, d[0][1] = inf;
		for(int i = 1; i <= n; i ++){
			if(s[i - 1] == '1' || s[i] == '1'){
				d[i][0] = inf;
				d[i][1] = min(d[i - 1][0] + a, d[i - 1][1]) + a + 2 * b;
			}
			else{
				d[i][0] = min(d[i - 1][0], d[i - 1][1] + a) + a + b;
				d[i][1] = min(d[i - 1][0] + a, d[i - 1][1]) + a + 2 * b;
			}
		}
		cout << d[n][0] << "\n";
	}
	return 0;
}

