#include<bits/stdc++.h>
using namespace std;
const int N=1e6+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int n,a[N],b[N];
int check(int x)
{
	int nxt=a[1]-x;
	for (int i=2;i<=n;i++)
	{
		nxt=a[i]-max(b[i]-nxt,0);
		if (nxt<0) return -2;
	}
	if (nxt+x<b[1]) return -1;
	return 1;
}
bool solve()
{
	int l=0,r=a[1];
	while (l<=r)
	{
		int mid=(l+r)/2;
		int ret=check(mid);
		if (ret>0) return 1;
		if (ret==-1) l=mid+1;
				else r=mid-1;
	}
	return 0;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		n=read();
		for (int i=1;i<=n;i++) b[i]=read();
		for (int i=1;i<=n;i++) a[i]=read();
		puts(solve()?"YES":"NO");
	}
	
	return 0;
}
