int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	auto kill = [](){
	    cout << "NO";
	    exit(0);
	};
	int n, k;
	string s;
	cin >> n >> k >> s;
	vi trans(500);
	trans['D'] = 1, trans['L'] = 2, trans['?'] = 3;
	string alp("LDW");
	vector<vi> pos(n + 1, vi(2 * k + 1)), prev(pos);
	pos[0][k] = true;
	for(int i = 1; i < n; i ++){
		for(int j = 1; j <= 2 * k - 1; j ++){
			for(int t = 0; t <= 2; t ++){
				if((trans[s[i - 1]] == t || trans[s[i - 1]] == 3) && pos[i - 1][j + t - 1]){
					pos[i][j] = true;
					prev[i][j] = j + t - 1;
				}
			}
		}
	}
	if(trans[s[n - 1]] >= 2 && pos[n - 1][1]){
		prev[n][0] = 1;
		int t = 0;
		for(int i = n - 1; i >= 0; i --){
			s[i] = alp[t - prev[i + 1][t] + 1];
			t = prev[i + 1][t];
		}
		cout << s;
		return 0;
	}
	else if((trans[s[n - 1]] == 0 || trans[s[n - 1]] == 3) && pos[n - 1][2 * k - 1]){
		prev[n][2 * k] = 2 * k - 1;
		int t = 2 * k;
		for(int i = n - 1; i >= 0; i --){
			s[i] = alp[t - prev[i + 1][t] + 1];
			t = prev[i + 1][t];
		}
		cout << s;
		return 0;
	}
	cout << "NO";
	return 0;
}
