#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
#define all(x) x.begin(), x.end()
#define pb push_back

const ld eps=1e-7;
const int inf=1000000010;
const int mod = 1000000007;
const int MAXN = 110;

int n, m, q, ans, a, b, c;
bool mark[MAXN];
vector<int> G[MAXN];

void dfs(int node){
	mark[node]=true;
	for (int v:G[node]){
		if (mark[v]) continue ;
		dfs(v);
	}
}

int main(){
	ios_base::sync_with_stdio(false);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>m;
	for (int i=0; i<m; i++){
		cin>>a>>b;
		G[a].pb(b);
		G[b].pb(a); 
	}
	for (int i=1; i<=n; i++){
		if (mark[i]) continue ;
		dfs(i);
		ans++;
	}
	cout<<(1LL<<(n-ans))<<endl;
	
	return 0;
}
