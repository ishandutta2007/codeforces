bool isparent(int a, int b) {
    return in[a] <= in[b] && out[b] <= out[a];
}


int lca(int a, int b) {
    if (isparent(a, b)) {
        return a;
    }
    if (isparent(b, a)) {
        return b;
    }
    for (int k = 17; k >= 0; k--) {
        if (!isparent(parent[a][k], b)) {
            a = parent[a][k];
        }
    }
    return parent[a][0];
}


pair<long long, int> st;


inline void checkpath(int a, int b) {
    while (true) {
        int f = next[a];
        if (isparent(f, b)) {
            chkmin(st, rmq(1, 1, ss, in[b], in[a]));
            break;
        } else {
            chkmin(st, rmq(1, 1, ss, in[f], in[a]));
            a = pr[f];
        }   
    }
}


int getBest(int a, int b) {
    if (!isparent(b, a)) {
        swap(a, b);
    }
    st = make_pair((long long)1e18, (int)1e9);
    if (!isparent(b, a)) {
        int c = lca(a, b);
        checkpath(a, c);
        checkpath(b, c);
    } else {
        checkpath(a, b);
    }
    //cout << st.first << ' ' << st.second << endl;
    if (st.first > (long long)1e17) {
        return -1;
    }
    int where = st.second;
    int j = in[where - 1];
    int sts = *h[where].begin();
    h[where].erase(sts);
 //   cout << j << endl;
   // cout << sts << endl;
   // cout << where << endl;
    if (h[where].empty()) {
        add(1, 1, ss, j, j, (long long)1e18 - (long long)sts);
    } else {
        add(1, 1, ss, j, j, *h[where].begin() - sts);
    }
    return sts;
}


