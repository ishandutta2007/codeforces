using namespace std;
template<typename T>
using minpq = priority_queue<T, vector<T>, greater<T>>;

void solve() {
    string s;
    cin >> s;
    int cnt[2] = {0, 0};
    for(char c : s) cnt[c - '0']++;
    int ans = min(cnt[0], cnt[1]);
    if(ans * 2 == s.length()) ans--;
    cout << ans << '\n';
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int te;
    cin >> te;
    while(te--) solve();
}