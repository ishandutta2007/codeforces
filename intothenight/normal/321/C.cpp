template<class T>
istream &operator>>(istream &in, deque<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<vi> adj(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vi size(n), parent(n), ded(n);
	function<int(int, int)> dfs_sz = [&](int u, int p){
		size[u] = 1;
		for(auto v: adj[u]){
			if(v != p && !ded[v]){
				size[u] += dfs_sz(v, u);
			}
		}
		return size[u];
	};
	function<int(int, int, int)> dfs_cent = [&](int u, int p, int n){
		for(auto v: adj[u]) if(v != p && !ded[v] && size[v] > (n >> 1)) return dfs_cent(v, u, n);
		return u;
	};
	vector<char> assigned(n);
	function<void(int, int, int)> assign = [&](int u, int p, int rank){
		if(rank >= 26){
			cout << "Impossible!";
			exit(0);
		}
		int n = dfs_sz(u, p);
		int c = dfs_cent(u, p, n);
		assigned[c] = rank + 'A';
		ded[c] = true;
		for(auto v: adj[c]){
			if(!ded[v]){
				assign(v, c, rank + 1);
			}
		}
	};
	assign(0, 0, 0);
	cout << assigned;
	return 0;
}

