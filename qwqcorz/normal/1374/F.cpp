#include<bits/stdc++.h>
using namespace std;
const int N=505;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
vector<int>ans;
int a[N];
void move(int x)
{
	ans.push_back(x);
	swap(a[x+1],a[x+2]);
	swap(a[x],a[x+1]);
}
void solve(int n,int &tot,int &flag)
{
	tot=0,flag=0;
	for (int i=1;i<=n;i++)
	for (int j=i+1;j<=n;j++)
	if (a[i]>a[j]) tot++;
	else if (a[i]==a[j]) flag=1;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int T=read();
	while (T--)
	{
		ans.clear();
		int n=read();
		for (int i=1;i<=n;i++) a[i]=read();
		if (n==3)
		{
			int t=0;
			while (!(a[1]<=a[2]&&a[2]<=a[3]))
			{
				t++;
				move(1);
				if (t>3) break;
			}
			if (a[1]<=a[2]&&a[2]<=a[3])
			{
				print(t);
				while (t--) print(1,' ');
				puts("");
			}
			else puts("-1");
			continue;
		}
		int tot,flag;
		solve(n,tot,flag);
		if (!flag&&(tot&1))
		{
			puts("-1");
			continue;
		}
		for (int k=n;k>=3;k--)
		{
			int mx=1;
			for (int i=2;i<=k;i++) if (a[i]>a[mx]) mx=i;
			if (mx==1) move(1),mx++;
			while (mx!=k) move(mx-1),mx++;
			solve(k,tot,flag);
			if (!flag&&(tot&1)) move(k-1),move(k-2);
		}
		solve(2,tot,flag);
		if (!flag&&(tot&1)) move(1),move(1);
		print(ans.size());
		for (int i:ans) print(i,' ');
		puts("");
	}
	
	return 0;
}
