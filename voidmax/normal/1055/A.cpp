typedef complex<double> comp;

int n, s;
vector<int> edge[1001];
vector<int> a, b;
bool used[1001];

bool dfs(int i) {
    if (i == s) return true;
    if (used[i]) return false;
    used[i] = true;
    int ans = 0;
    for (auto j : edge[i]) {
        ans |= dfs(j);
    }
    return ans;
}

int main() {
    ios::sync_with_stdio(0);
    srand(time(0));
    cin >> n >> s;
    --s;
    a.resize(n);
    b.resize(n);
    cin >> a >> b;
    vector<int> st1, st2;
    for (int i = 0; i < n; ++i) {
        if (a[i]) st1.pb(i);
        if (b[i]) st2.pb(i);
    }
    for (int i = 0; i + 1 < st1.size(); ++i) {
        edge[st1[i]].pb(st1[i + 1]);
    }
    for (int i = 0; i + 1 < st2.size(); ++i) {
        edge[st2[i + 1]].pb(st2[i]);
    }
    if (dfs(0)) {
        cout << "YES\n";
    } else {
        cout << "NO\n";
    }
}