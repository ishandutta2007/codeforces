const int mn=21;
int go(string stra, string strb) {
	bitset<mn> A2B[mn];
	for (int x=0;x<mn;x++) A2B[x].reset();
	bitset<mn> B; B.reset();
	int qn=stra.length();
	for (int i=0;i<qn;i++) {
		int a=stra[i]-'a',b=strb[i]-'a';
		if (a>b) {
			return -1;
		}
		if (a<b) {
			A2B[a][b]=true;
			B[b]=true;
		}
	}
	int ans=0;
	for (int b=0;b<mn;b++) {
		if (!B[b]) continue;
		for (int a=0;a<b;a++) {
			if (A2B[a][b]) {
				// transform a to b
				A2B[b] |= A2B[a];
				A2B[a].reset();
				ans++;
			}
		}
	}
	return ans;
}
