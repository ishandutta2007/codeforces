#include<bits/stdc++.h>
using namespace std;
const int N=1e5+5;
const int p=1e9+7;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int power(long long a,int b)
{
	long long ret=1;
	while (b)
	{
		if (b&1) ret=ret*a%p;
		a=a*a%p;
		b/=2;
	}
	return ret;
}
bool vis[N];
int pr[N],cntp=0,miu[N];

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int m=read(),ans=1;
	miu[1]=1;
	for (int i=2;i<=m;i++)
	{
		if (!vis[i]) miu[i]=-1,pr[++cntp]=i;
		for (int j=1;j<=cntp;j++)
		{
			if (i*pr[j]>m) break;
			vis[i*pr[j]]=1;
			if (i%pr[j]) miu[i*pr[j]]=miu[i]*-1;
			else
			{
				miu[i*pr[j]]=0;
				break;
			}
		}
	}
	for (int d=2;d<=m;d++) ans=(ans-1LL*miu[d]*power(m-m/d,p-2)*(m/d)%p+p)%p;
	print(ans);
	
	return 0;
}
