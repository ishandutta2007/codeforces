template <int MOD_> struct modnum {
   static constexpr int MOD = MOD_;
   static_assert(MOD_ > 0, "MOD must be positive");
   private:
   using ll = long long;
   int v;
   static int minv(int a, int m) {
      a %= m;
      assert(a);
      return a == 1 ? 1 : int(m - ll(minv(m, a)) * ll(m) / a);
   }
   public:
   modnum() : v(0) {}
   modnum(ll v_) : v(int(v_ % MOD)) { if (v < 0) v += MOD; }
   explicit operator int() const { return v; }
   friend std::ostream& operator << (std::ostream& out, const modnum& n) { return out << int(n); }
   friend std::istream& operator >> (std::istream& in, modnum& n) { ll v_; in >> v_; n = modnum(v_); return in; }
   friend bool operator == (const modnum& a, const modnum& b) { return a.v == b.v; }
   friend bool operator != (const modnum& a, const modnum& b) { return a.v != b.v; }
   modnum inv() const {
      modnum res;
      res.v = minv(v, MOD);
      return res;
   }
   friend modnum inv(const modnum& m) { return m.inv(); }
   modnum neg() const {
      modnum res;
      res.v = v ? MOD-v : 0;
      return res;
   }
   friend modnum neg(const modnum& m) { return m.neg(); }
   modnum operator- () const {
      return neg();
   }
   modnum operator+ () const {
      return modnum(*this);
   }
   modnum& operator ++ () {
      v ++;
      if (v == MOD) v = 0;
      return *this;
   }
   modnum& operator -- () {
      if (v == 0) v = MOD;
      v --;
      return *this;
   }
   modnum& operator += (const modnum& o) {
      v += o.v;
      if (v >= MOD) v -= MOD;
      return *this;
   }
   modnum& operator -= (const modnum& o) {
      v -= o.v;
      if (v < 0) v += MOD;
      return *this;
   }
   modnum& operator *= (const modnum& o) {
      v = int(ll(v) * ll(o.v) % MOD);
      return *this;
   }
   modnum& operator /= (const modnum& o) {
      return *this *= o.inv();
   }
   friend modnum operator ++ (modnum& a, int) { modnum r = a; ++a; return r; }
   friend modnum operator -- (modnum& a, int) { modnum r = a; --a; return r; }
   friend modnum operator + (const modnum& a, const modnum& b) { return modnum(a) += b; }
   friend modnum operator - (const modnum& a, const modnum& b) { return modnum(a) -= b; }
   friend modnum operator * (const modnum& a, const modnum& b) { return modnum(a) *= b; }
   friend modnum operator / (const modnum& a, const modnum& b) { return modnum(a) /= b; }
};


const ll infl = 1LL << 60;

// ============= Solution ============= //
int sub_to_errichto() {
   int n, q;
   cin >> n >> q;
   auto ask = [&](vector<ll> v, int x, ll k) -> ll {
      ll need = k;
      ll extra = 0;
      for (int i = 0; i <= x; ++i) {
         extra += ((1 << i) - 1) * v[i];
         need -= v[i];
      }

      if (need < 0) return 0;
      ll cost = 0;
      pp(x);
      for (int rep = 0; rep < 40; ++rep) {
         for (int i = 1; x + i < n; ++i) {
            pp(i);
            pp(v[x + i], need);
            ll take = min(need >> i, v[x + i]);
            v[x + i] -= take;
            pp(take, i);
            need -= take << i;
            pp(x);
            cost += take * ((1 << i) - 1);
            extra += (take << i) * ((1 << x) - 1);
         }

         for (int i = 1; x + i < n; ++i) {
            if (v[x + i] && need > extra) {
               pp(x);
               v[x + i] -= 1;
               v[x + i - 1] += 2;
               cost += 1;
               if (i == 1) {
                  need -= 2;
                  extra += 2 * ((1 << x) - 1);
               }
               break;
            }
         }
      }

      if (need > extra) {
         return infl;
      }
      cost += max(0LL, need);

      return cost;
   };
   vector<ll> v(n);
   for (ll &i: v) cin >> i;
   for (int it = 0; it < q; ++it) {
      ll qtype, a, b;
      cin >> qtype >> a >> b;
      if (qtype == 1) {
         v[a] = b;
      } else {
         ll ans = ask(v, a, b);
         if (ans == infl) {
            ans = -1;
         }
         cout << ans << "\n";
      }
   }
}

