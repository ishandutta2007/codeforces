vector<vector<pair<ll,ll>>> g;
vector<int> p;
vector<ll> c;

void dfs(int u)
{
    for (;p[u]<g[u].size();p[u]++)
    {
        int v = g[u][p[u]].X;
        int e = g[u][p[u]].Y;
        if (c[e]!=0) continue;
        if (u<v) c[e] = 1;
        else c[e] = 2;
        dfs(v);
        return;
    }
}

int main()
{
    ios_base::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<ll> L(n), R(n);
    vector<ll> val;
    for (int i=0;i<n;i++)
    {
        cin >> L[i] >> R[i];
        L[i] = L[i]*2;
        R[i] = R[i]*2+1;
        val.push_back(L[i]);
        val.push_back(R[i]);
    }
    sort(val.begin(),val.end());
    int sz = val.size();
    g.resize(sz);
    p.resize(sz);
    vector<ll> d(sz);
    for (int i=0;i<n;i++)
    {
        int l = 0;
        int r = sz;
        while (r-l>1)
        {
            int m  = (r+l)/2;
            if (val[m]<=L[i]) l = m;
            else r = m;
        }
        L[i] = l;
        l = 0;
        r = sz;
        while (r-l>1)
        {
            int m  = (r+l)/2;
            if (val[m]<=R[i]) l = m;
            else r = m;
        }
        R[i] = l;
        d[L[i]]++;
        d[R[i]]++;
        g[L[i]].push_back({R[i],i});
        g[R[i]].push_back({L[i],i});
    }
    int q = n;
    int prv = -1;
    for (int i=0;i<sz;i++)
    {
        if (d[i]%2==1)
        {
            if (prv==-1)
            {
                prv = i;
                continue;
            } else
            {
                g[prv].push_back({i,q});
                g[i].push_back({prv,q});
                q++;
                prv = -1;
            }
        }
    }
    c.resize(q);
    for (int i=0;i<sz;i++)
    {
        dfs(i);
    }
    for (int i=0;i<n;i++)
    {
        if (c[i]==1) cout << 0 << " ";
        else cout << 1 << " ";
    }
}