struct uf {
private:
	vector<int> par, ran,cnt;
public:
	uf(int n) {
		par.resize(n, 0);
		ran.resize(n, 0);
		cnt.resize(n, 1);
		rep(i, n) {
			par[i] = i;
		}
	}
	int find(int x) {
		if (par[x] == x)return x;
		else return par[x] = find(par[x]);
	}
	void unite(int x, int y) {
		x = find(x), y = find(y);
		if (x == y)return;
		if (ran[x] < ran[y]) {
			par[x] = y; cnt[y] += cnt[x];
		}
		else {
			par[y] = x; cnt[x] += cnt[y];
			if (ran[x] == ran[y])ran[x]++;
		}
	}
	bool same(int x, int y) {
		return find(x) == find(y);
	}
	int getsz(int x) {
		return cnt[find(x)];
	}
};
