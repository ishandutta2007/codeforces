int gcd(int a, int b) {
	if (b == 0)
		return a;
	return gcd(b, a % b);
}

void solve() {
	int n;
	cin >> n;
	vector<int> a(n);
	for(int i = 0; i < n; ++i)
		cin >> a[i];
	vector<bool> used(n);
	int res = 0;
	for(int i = 0; i < n; ++i) {
		int mx = -1;
		for(int j = 0; j < n; ++j) {
			if (mx == -1 && !used[j])
				mx = j;
			if (!used[j] && gcd(res, a[j]) == gcd(res, a[mx])) {
				if (a[j] > a[mx])
					mx = j;
			}
			if (!used[j] && gcd(res, a[j]) > gcd(res, a[mx]))
				mx = j;
		}
		res = gcd(res, a[mx]);
		used[mx] = 1;
		cout << a[mx] << ' ';
	}
	cout << '\n';
}

