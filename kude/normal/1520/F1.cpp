constexpr int MX = 200100;
fenwick_tree<int> ft(MX);
int res[MX], asked[MX];
int query(int r) {
    if (asked[r]) {
        return res[r] - ft.sum(0, r);
    }
    if (r == 0) return 0;
    cout << "? " << 1 << ' ' << r << endl;
    int ret;
    cin >> ret;
    ret = r - ret;
    asked[r] = true;
    res[r] = ret + ft.sum(0, r);
    return ret;
}

void answer(int x) {
    cout << "! " << x + 1 << endl;
    ft.add(x, 1);
}

