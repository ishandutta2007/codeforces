void solve() {
    string S;
    cin >> S;

    int n = S.size();
    vector<vector<int>> nxt(n + 1, vector<int>(26, -1));
    for (int i = n - 1; i >= 0; i--) {
        for (int j = 0; j < 26; j++) nxt[i][j] = nxt[i + 1][j];
        nxt[i][S[i] - 'a'] = i;
    }

    string res = "";
    vector<bool> used(26, false);
    int cnt = 0;
    for (int i = 0; i < 26; i++) {
        if (nxt[0][i] < 0)
            used[i] = true;
        else
            cnt++;
    }
    int cur = -1;

    auto next = [&]() -> char {
        for (int i = 25; i >= 0; i--) {
            if (used[i]) continue;
            bool ok = true;
            int will = nxt[cur + 1][i];
            // debug(i, will);
            for (int j = 0; j < 26; j++) {
                if (used[j] || i == j) continue;
                if (nxt[will][j] < 0) ok = false;
            }
            if (ok) {
                cur = will;
                return 'a' + i;
            }
        }
    };

    for (int i = 0; i < cnt; i++) {
        char c = next();
        used[c - 'a'] = true;
        res += c;
    }
    cout << res << '\n';
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int t;
    cin >> t;
    for (; t--;) solve();
    return 0;
}