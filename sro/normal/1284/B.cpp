#include <bits/stdc++.h>
using namespace std;

int n;
vector<int> a,b;

int main(){
	scanf("%d",&n);
	long long ans=1ll*n*n;
	for(int i=0;i<n;i++){
		int li;
		scanf("%d",&li);
		int bg;
		scanf("%d",&bg);
		int lst=bg;
		bool can=1;
		for(int j=1;j<li;j++){
			int x;
			scanf("%d",&x);
			if(lst<x){
				can=0;
			}
			lst=x;
		}
		if(can){
			b.push_back(lst);
			a.push_back(bg);
		}
	}
	sort(a.begin(),a.end());
	for(int i=0;i<b.size();i++){
		int cnt=upper_bound(a.begin(),a.end(),b[i])-a.begin();
		ans-=cnt;
	}
	printf("%lld\n",ans);
	return 0;
}