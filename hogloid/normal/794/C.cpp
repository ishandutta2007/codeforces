  sort(s,s+n);
  sort(t,t+n);
  reverse(t,t+n);
  string res,res2;

  int n2=(n+(n&1))/2,n3=(n-n2);

  int p1=0,p2=n2-1;
  int q1=0,q2=n3-1;
  REP(i,n){
    char c=s[p1],c2=t[q1];
    if(c<c2){
      if(i%2==0) res+=c,++p1;
      else res+=c2,++q1;
    }else{
      if(i%2==0) res2+=s[p2--];
      else res2+=t[q2--];
    }
  }
  reverse(ALL(res2));
  res+=res2;
