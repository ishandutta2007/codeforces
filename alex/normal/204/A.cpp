int64 calc(int64 n)
{
	if (n < 10) return n;
	if (n % 10 == 0) n--;
	int64 res = 9, now = 100;
	while (now < n)
	{
		res += now / 100 * 9;
		now *= 10;
	}
	now /= 10;
	for (int i = 1; i < 10; ++i)
		if (i * now > n) break;
		else if ((i + 1) * now <= n) res += now / 10;
		else res += (n - i * now - i + 10) / 10;
	return res;
}

