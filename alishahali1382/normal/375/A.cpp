const int MAXN = 100010;

int A[]={8169, 6819, 1689, 6198, 1698, 1986, 1896};
string s, out;
bool mark[4];
int cnt;

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>s;
	for (char ch:s){
		if (ch=='1'){
			if (mark[0]) out+=ch;
			mark[0]=true;
		}
		else if (ch=='6'){
			if (mark[1]) out+=ch;
			mark[1]=true;
		}
		else if (ch=='8'){
			if (mark[2]) out+=ch;
			mark[2]=true;
		}
		else if (ch=='9'){
			if (mark[3]) out+=ch;
			mark[3]=true;
		}
		else if (ch=='0') cnt++;
		else out+=ch;
	}
	int x=0, tmp;
	for (char ch:out){
		tmp=ch-'0';
		x=(3*x+tmp)%7;
	}
	cout<<out<<A[x*3%7];
	while (cnt--) cout<<0;
	cout<<endl;
	
	//cerr<<out;
	
	return 0;
}
