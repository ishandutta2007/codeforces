int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n, k;
	string s;
	cin >> n >> k >> s;
	vi ok(26);
	for(int i = 0; i < k; i ++){
		char c;
		cin >> c;
		ok[c - 'a'] = true;
	}
	ll res = 0;
	for(int i = 0; i < n; ){
		int j = i;
		while(j < n && ok[s[j] - 'a']) j ++;
		res += 1LL * (j - i) * (j - i + 1) / 2;
		i = j + 1;
	}
	cout << res;
	return 0;
}
 
