int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n, a, b;
    cin >> n >> a >> b;
    vector<int> p(n);
    cin >> p;

    map<int, int> mp;
    for (int i = 0; i < n; i++) mp[p[i]] = i;
    vector<int> ans(n, -1), other(n, -1);
    for (int i = 0; i < n; i++) {
        if (!mp.count(a - p[i]) && !mp.count(b - p[i])) {
            cout << "NO" << '\n';
            return 0;
        }
        if (!ans[i]) continue;
        if (!mp.count(a - p[i])) continue;
        int x = mp[a - p[i]];
        ans[i] = ans[x] = 0;
        other[i] = x;
        other[x] = i;
    }
    queue<int> que;
    for (int i = 0; i < n; i++) {
        if (ans[i] < 0) {
            que.emplace(i);
        }
    }

    vector<bool> seen(n, false);
    while (!que.empty()) {
        int v = que.front();
        que.pop();
        if (seen[v]) continue;
        seen[v] = true;
        if (~ans[v]) continue;
        if (!mp.count(b - p[v])) {
            cout << "NO" << '\n';
            return 0;
        }
        int x = mp[b - p[v]], y = -1;
        if (~ans[x]) {
            y = other[x];
            ans[x] = ans[y] = -1;
            other[x] = other[y] = -1;
        }
        ans[v] = ans[x] = 1;
        other[v] = x;
        other[x] = v;
        if (~y && ans[y] < 0) que.emplace(y);
    }

    for (int i = 0; i < n; i++) {
        if (ans[i] < 0) {
            cout << "NO" << '\n';
            return 0;
        }
    }
    cout << "YES" << '\n';
    cout << ans << '\n';
    return 0;
}