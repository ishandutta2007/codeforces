const int N = 1e5 + 228;
const int K = 17;

int up[N][K];
int d[N];
int who[N];
vector<int> G[N];
vector<int> A_l[N];

void dfs(int v, int now_d) {
	d[v] = now_d;
	who[v] = A_l[now_d].size();
	A_l[now_d].push_back(v);
	for(auto i: G[v]) 
		dfs(i, now_d + 1);
}

int la(int a, int level) {
	for(int k = K - 1; k >= 0; --k) {
		if ((1 << k) <= level) {
			level -= (1 << k);
			a = up[a][k];
		}
	}
	return a;
}

int lca(int a, int b) {
	if (d[a] < d[b])
		swap(a, b);
	a = la(a, d[a] - d[b]);
	if (a == b)
		return a;

	for(int i = K - 1; i >= 0; --i) {
		if (up[a][i] != up[b][i]) 
			a = up[a][i], b = up[b][i];
	}
	return up[a][0];
}

signed main() {
	setup();
	
	int n;
	cin >> n;
	up[0][0] = 0;
	++n;
	for(int i = 1; i < n; ++i) {
		int p;
		cin >> p;
		G[p].push_back(i);
		up[i][0] = p;
	}
	dfs(0, 0);
	for(int k = 1; k < K; ++k) {
		for(int i = 0; i < n; ++i) 
			up[i][k] = up[up[i][k - 1]][k - 1];
	}
	int q;
	cin >> q;	
	while(q--) {
		int v, p;
		cin >> v >> p;
		if (d[v] <= p) {
			cout << 0 << ' ';
			continue;
		}
		int res = 0;
		int l = -1, r = who[v];
		while(r - l > 1) {
			int m = (l + r) / 2;
			int a = lca(v, A_l[d[v]][m]);
			if (d[v] - d[a] <= p)
				r = m;
			else
				l = m;
		}
		res += who[v] - r;
		l = who[v], r = A_l[d[v]].size();
		while(r - l > 1) {
			int m = (l + r) / 2;
			int a = lca(v, A_l[d[v]][m]);
			if (d[v] - d[a] <= p)
				l = m;
			else
				r = m;
		}
		res += l - who[v];
		cout << res << ' ';
	}

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}

