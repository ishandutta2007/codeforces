using ull = unsigned long long;
using ll = long long;
using ld = long double;

const int mod = 1e9 + 7;
const int inf = INT_MAX;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        int n;
        cin >> n;

        vector<int> a(n);
        for (int i = 0; i < n; i++) cin >> a[i];

        bool f = true;
        for (int i = 1; i < n; i++) f &= a[i] == a[i - 1];

        cout << (f ? n : 1) << "\n";
    }
    return 0;
}