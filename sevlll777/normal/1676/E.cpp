using namespace std;

void solve() {
  int n, q;
  cin >> n >> q;
  vector<int> a(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }
  sort(rall(a));
  for (int i = 1; i < n; i++) {
    a[i] += a[i - 1];
  }
  while (q--) {
    int x;
    cin >> x;
    if (a[n - 1] < x) {
      cout << "-1\n";
    } else {
      cout << (lower_bound(all(a), x) - a.begin() + 1) << '\n';
    }
  }

}

