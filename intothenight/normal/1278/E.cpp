typedef vector<tpl> vtpl;



int main(){
	cin.tie(0)->sync_with_stdio(0);
	int n;
	cin >> n;
	vector<vi> adj(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vpii res(n);
	res[0].first = 1;
	auto dfs = [&](int u, int p, auto &dfs)->void{
		static int cnt = 2;
		vi temp;
		for(auto v: adj[u]){
			if(v != p){
				temp.push_back(v);
				res[v].first = cnt ++;
			}
		}
		res[u].second = cnt ++;
		reverse(all(temp));
		for(auto v: temp){
			dfs(v, u, dfs);
		}
	};
	dfs(0, 0, dfs);
	cout << res;
	return 0;
}
 
