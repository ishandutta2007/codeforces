int main() {
  int cas;
  scanf("%d", &cas);
  while (cas--) {
    int n;
    scanf("%d", &n);
    vector<int64> a(n), b(n);
    for (int i = 0; i < n; ++i) {
      scanf("%lld%lld", &a[i], &b[i]);
    }
    int64 sum = a[0];
    for (int i = 1; i < n; ++i) sum += max(0LL, a[i] - b[i - 1]);
    int64 ret = sum;
    for (int k = 1; k < n; ++k) {
      sum -= a[k - 1];
      sum -= max(0LL, a[k] - b[k - 1]);
      sum += a[k];
      sum += max(0LL, a[(n + k - 1) % n] - b[(n + k - 2) % n]);
      ret = min(ret, sum);
    }
    printf("%lld\n", ret);
  }
  return 0;
}