const int maxn=2e6+7;
struct Edge{
    int to,id;
};
int fa[maxn];
void init(int _n)
{
    for(int i=0;i<=_n;i++) fa[i]=i; 
}
int Find(int x)
{
    return x==fa[x]?x:fa[x]=Find(fa[x]);
}
void Union(int x,int y)
{
    x=Find(x),y=Find(y);
    fa[y]=x;
}
vvc<Edge> G;
vi ans;
bool vis[maxn];
void dfs(int u)
{
    while(G[u].size())
    {
        auto v=G[u].back();
        G[u].pop_back();
        if(vis[v.id])
            continue;
        vis[v.id]=1;
        vis[((v.id-1)^1)+1]=1;
        dfs(v.to);
        ans.push_back(((v.id-1)^1)+1);
        ans.push_back(v.id);
    }
}
