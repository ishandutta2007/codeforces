const int SIZE = 75000000;
bitset<SIZE>p;
typedef unsigned int UI;
UI cc[4];
VI pp;
void prime_init(int n){
    for(int i=2;i*i<=n;i++){
        if(!p[i]){
            pp.PB(i);
            for(int j=i;j*j<=n;j+=i){
                p[j]=1;
            }
        }
    }
}
UI f(int x,int n){
    UI AA=0;
    while(n>=x){
        AA+=n/x;
        n/=x;
    }
    return AA*(((cc[3]*x+cc[2])*x+cc[1])*x+cc[0]);
}
int main(){
    int n,A,B,C,D; R(n,A,B,C,D);
    prime_init(n);
    cc[3]=A;
    cc[2]=B;
    cc[1]=C;
    cc[0]=D;
    int ll=2;
    UI an=0;
    while(ll<=n){
        p.reset();
        int rr=min(ll+SIZE-1,n);
        for(int x:pp){
            int now=ll/x*x;
            while(now<ll||now==x)now+=x;
            for(;now<=rr;now+=x)p[now-ll]=1;
        }
        for(int i=0;ll+i<=rr;i++){
            if(!p[i]){
                int x=ll+i;
                an+=f(x,n);
            }
        }
        ll=rr+1;
    }
    W((LL)an);
    return 0;
}