void run_case() {
    int N, K;
    cin >> N >> K;
    vector<int> A(N);
    int full_xor = 0;

    for (auto &a : A) {
        cin >> a;
        full_xor ^= a;
    }

    vector<vector<int>> adj(N);

    for (int i = 0; i < N - 1; i++) {
        int u, v;
        cin >> u >> v;
        u--; v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    int subtrees = 0;

    auto dfs = y_combinator([&](auto self, int node, int parent) -> int {
        int xor_sum = A[node];

        for (int neigh : adj[node])
            if (neigh != parent)
                xor_sum ^= self(neigh, node);

        if (xor_sum == full_xor) {
            subtrees++;
            xor_sum = 0;
        }

        return xor_sum;
    });

    auto solve = [&]() -> bool {
        if (full_xor == 0)
            return true;

        if (K >= 3) {
            dfs(0, -1);
            return subtrees >= 3;
        }

        return false;
    };

    cout << (solve() ? "YES" : "NO") << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}