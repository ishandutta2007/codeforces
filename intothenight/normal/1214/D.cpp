int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vs a(n);
	cin >> a;
	vector<pii> dr{{1, 0}, {0, 1}};
	vector<vector<vi>> adj(n, vector<vi>(m ,vi(2)));
	for(int i = 0; i + 1 < n; i ++){
		for(int j = 0; j < m; j ++){
			if(a[i][j] == '.' && a[i + 1][j] == '.'){
				adj[i][j][0] = true;
			}
		}
	}
	for(int i = 0; i < n; i ++){
		for(int j = 0; j + 1 < m; j ++){
			if(a[i][j] == '.' && a[i][j + 1] == '.'){
				adj[i][j][1] = true;
			}
		}
	}
	bool found = false;
	vector<vi> visited(n, vi(m));
	function<bool(int, int)> dfs = [&](int x, int y){
		if(x == n - 1 && y == m - 1){
			return true;
		}
		if(x >= n || y >= m || a[x][y] == '#' || visited[x][y]){
			return false;
		}
		visited[x][y] = 1;
		if(dfs(x + 1, y) || dfs(x, y + 1)){
			return true;
		}
		return false;
	};
	if(!dfs(0, 0)){
		cout << 0;
		return 0;
	}
	visited[0][0] = false;
	if(!dfs(0, 0)){
		cout << 1;
		return 0;
	}
	cout << 2;
	return 0;
}

