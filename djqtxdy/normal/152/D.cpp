struct rec{
	short X1,Y1,X2,Y2;
	rec(short a,short b,short c,short d){
		X1=a;Y1=b;X2=c;Y2=d;
	}
};
void print(rec x){
	printf("%d %d %d %d\n",x.X1+1,1+x.Y1,1+x.X2,1+x.Y2);
}
int f(rec x){
	return 2*abs(x.X1-x.X2)+2*abs(x.Y1-x.Y2);
}
