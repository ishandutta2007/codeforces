#pragma warning(disable:4996)
#include<stdio.h>
#include<algorithm>
long double w[5][2] = {
	{ 3.830127018922193, 3.366025403784439 },
	{ -3.601321235851749, 10.057331467373021 },
	{ 0.466045194906253, 19.192786043799030 },
	{ 10.411264148588986, 18.147501411122495 },
	{ 12.490381056766580, 8.366025403784439 }
};
long double p[2], x, y, M[4][2];
int n;
int main()
{
	int i, j, t;
	scanf("%d", &n);
	p[0] = w[2][0] - w[0][0];
	p[1] = w[2][1] - w[0][1];
	M[0][0] = w[4][0] - w[2][0];
	M[0][1] = w[4][1] - w[2][1];
	M[1][0] = w[1][0] - w[4][0];
	M[1][1] = w[1][1] - w[4][1];
	M[2][0] = w[3][0] - w[1][0];
	M[2][1] = w[3][1] - w[1][1];
	M[3][0] = w[0][0] - w[3][0];
	M[3][1] = w[0][1] - w[3][1];
	printf("%d\n", n * 4 + 1);
	for (i = 0; i <= n; i++){
		printf("%.10llf %.10llf\n", p[0] * i, p[1] * i);
	}
	x = p[0] * n, y = p[1] * n;
	for (i = 0; i < n; i++){
		if (i % 2 == 0){
			for (j = 0; j < 4; j++){
				x += M[j][0];
				y += M[j][1];
				if (j != 3){
					printf("%.10llf %.10llf\n", x, y);
				}
			}
		}
		else{
			for (j = 3; j >= 0; j--){
				x += M[j][0];
				y += M[j][1];
				if (j != 0){
					printf("%.10llf %.10llf\n", x, y);
				}
			}
		}
	}
	t = n + 1;
	for (i = 1; i <= n; i++){
		printf("%d %d %d %d %d\n", n + 2 - i, t + 2, n + 1 - i, t + 1, t + 3);
		t += 3;
	}
	t = n + 1;
	for (i = 1; i <= n + 1; i++){
		printf("%d ", i);
	}
	for (i = 1; i <= n; i++){
		printf("%d %d %d %d ", t + 1, t + 2, t + 3, n + 1 - i);
		t += 3;
	}
	return 0;
}