template<class T>
istream &operator>>(istream &in, deque<T> &arr){
	for(auto &x: arr) in >> x;
	return in;
}
template<class T>
ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<vi> adj(n);
	for(int i = 1; i < n; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vi c(n);
	c[0] = 1;
	int k = 0;
	function<void(int, int)> dfs = [&](int u, int p){
		int col = 1;
		for(auto v: adj[u]){
			if(v != p){
				while(col == c[u] || p != -1 && col == c[p]){
					col ++;
				}
				c[v] = col;
				k = max(k, col);
				dfs(v, u);
				col ++;
			}
		}
	};
	dfs(0, -1);
	cout << k << "\n" << c;
	return 0;
}

