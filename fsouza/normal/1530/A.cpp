using namespace std;

typedef long long lint;

int main() {
  int nt;
  scanf("%d", &nt);
  for (int t = 0; t < nt; ++t) {
    int v;
    scanf("%d", &v);
    int result = 0;
    while (v > 0) {
      int dig = v % 10;
      result = max(result, dig);
      v /= 10;
    }
    printf("%d\n", result);
  }
  return 0;
}