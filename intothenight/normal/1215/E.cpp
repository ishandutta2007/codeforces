mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vi a(n);
	cin >> a;
	vector<vi> col(20);
	for(int i = 0; i < n; i ++) col[a[i] - 1].push_back(i);
	vector<vl> cnt(20, vl(20));
	for(int i = 0; i < 20; i ++){
		for(int j = 0; j < 20; j ++){
			if(i != j){
				int p = 0;
				for(auto ind: col[i]){
					while(p < col[j].size() && col[j][p] < ind) p ++;
					cnt[i][j] += p;
				}
			}
		}
	}
	int t = 1 << 20;
	vl d(t, 9e18);
	d[0] = 0;
	for(int mask = 1; mask < t; mask ++){
		vector<vi> q(2);
		for(int i = 0; i < 20; i ++){
			q[!bool(mask & (1 << i))].push_back(i);
		}
		for(auto i: q[0]){
			ll sum = 0;
			for(auto j: q[1]){
				sum += cnt[i][j];
			}
			d[mask] = min(d[mask], d[mask ^ (1 << i)] + sum);
		}
	}
	cout << d.back();
	return 0;
}

