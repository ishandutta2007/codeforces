const ld eps=1e-7;
const int inf=1000000010;
const ll INF=10000000000000010LL;
const int mod = 1000000007;
const int MAXN = 110, LOG=20;

int n, m, k, r1, c1, r2, c2, x, y, t, a, b, ans=inf;
char A[MAXN][MAXN];
bitset<MAXN> mark[MAXN];
vector<pii> v1, v2;

void dfs(int x, int y, vector<pii> &V){
	if (mark[x][y] || A[x][y]=='1') return ;
	mark[x][y]=1;
	V.pb({x, y});
	dfs(x-1, y, V);
	dfs(x+1, y, V);
	dfs(x, y-1, V);
	dfs(x, y+1, V);
}

int f(int x, int y){ return x*x+y*y;}

int main(){
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	cin>>n>>r1>>c1>>r2>>c2;
	for (int i=1; i<=n; i++) for (int j=1; j<=n; j++) cin>>A[i][j];
	for (int i=1; i<=n; i++) A[i][0]=A[i][n+1]=A[0][i]=A[n+1][i]='1';
	dfs(r1, c1, v1);
	memset(mark, 0, sizeof(mark));
	dfs(r2, c2, v2);
	for (pii p1:v1) for (pii p2:v2) ans=min(ans, f(p1.first-p2.first, p1.second-p2.second));
	cout<<ans<<'\n';
	
	
	return 0;
}
