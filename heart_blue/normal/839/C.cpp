const LL INF = 1e9 + 7;
const int N = 2e5 + 10;
vector<int> v[N];
double dfs(int x, int fa = -1)
{
	double sum = 0;
	int cnt = 0;
	for (auto &y : v[x])
	{
		if (y == fa) continue;
		sum += dfs(y, x);
		cnt++;
	}
	if (cnt) return sum / cnt + 1;
	else return sum;
}
