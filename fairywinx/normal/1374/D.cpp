void solve() {
	int n, k;
	cin >> n >> k;
	vector<int> a(n);
	map<int, int> cnt;
	for(int i = 0; i < n; ++i) {
		cin >> a[i];
		if (a[i] % k == 0)
			continue;
		cnt[k - (a[i] % k)]++;
	}
	int ans = 0;
	for(auto i: cnt) 
		ans = max(ans, (i.second - 1) * k + i.first);
	if (cnt.size() != 0)
		cout << ans + 1 << '\n';	
	else 
		cout << 0 << '\n';
}

