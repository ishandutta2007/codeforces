#include <bits/stdc++.h>

using namespace std;

int a[200];

int main(){
    ios_base::sync_with_stdio(false);
    int cnt = 0, ans = 0;
    string s;
    while (cnt == 0){
        cin >> s;
        for (int i = 0; i < s.length(); i++){
            if (s[i] - 'a' >= 0)
                a[s[i] - 'a']++;
            if (s[i] == '}'){
                cnt++;
            }
        }
    }
    for (int i = 0; i < 26; i++)
        if (a[i] != 0)
            ans++;
    cout << ans;
}