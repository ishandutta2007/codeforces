int main()
{
	int n;
	scanf("%i",&n);
	vector<int> a(n),b(n);
	for(int i=0;i<n;i++)
		scanf("%i",&a[i]);
	for(int i=0;i<n;i++)
		scanf("%i",&b[i]);
	ordered_set<pair<int,int> > cmon;
	ll ans=0;
	for(int i=0;i<n;i++){
		auto it=cmon.upper_bound({b[i]-a[i],INT_MAX});
		if(it!=cmon.end())
		{
			int pre=cmon.order_of_key(*it);
			ans+=cmon.size()-pre;
		}
		cmon.insert({a[i]-b[i],i});
	}
	printf("%lld\n",ans);
	return 0;
}