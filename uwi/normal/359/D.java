	void solve()
	{
		int n = ni();
		int[] a = na(n);
		SegmentTreeRMQ st = new SegmentTreeRMQ(a);
		int max = 0;
		TreeSet<Integer> maxc = new TreeSet<>();
		for(int i = 0;i < n;i++){
			int L = -1, R = -1;
			{
				int low = -1, high = i+1;
				while(high - low > 1){
					int h = high+low>>1;
					int g = st.gcdx(h, i+1);
					if(g == a[i]){
						high = h;
					}else{
						low = h;
					}
				}
				L = high;
			}
			{
				int low = i, high = n+1;
				while(high - low > 1){
					int h = high+low>>1;
					int g = st.gcdx(i, h);
					if(g == a[i]){
						low = h;
					}else{
						high = h;
					}
				}
				R = low;
			}
			int len = R-L-1;
			if(len > max){
				max = len;
				maxc.clear();
				maxc.add(L+1);
			}else if(len == max){
				maxc.add(L+1);
			}
//			tr(i, L, R, max, len, maxc);
		}
		out.println(maxc.size() + " " + max);
		for(int v : maxc){
			out.print(v + " ");
		}
	}
	
	public static class SegmentTreeRMQ {
		public int M, H, N;
		public int[] st;
		
		public SegmentTreeRMQ(int n)
		{
			N = n;
			M = Integer.highestOneBit(Math.max(N-1, 1))<<2;
			H = M>>>1;
			st = new int[M];
			Arrays.fill(st, 0, M, Integer.MAX_VALUE);
		}
		
		public SegmentTreeRMQ(int[] a)
		{
			N = a.length;
			M = Integer.highestOneBit(Math.max(N-1, 1))<<2;
			H = M>>>1;
			st = new int[M];
			for(int i = 0;i < N;i++){
				st[H+i] = a[i];
			}
			Arrays.fill(st, H+N, M, Integer.MAX_VALUE);
			for(int i = H-1;i >= 1;i--)propagate(i);
		}
		
		public void update(int pos, int x)
		{
			st[H+pos] = x;
			for(int i = (H+pos)>>>1;i >= 1;i >>>= 1)propagate(i);
		}
		
		private void propagate(int i)
		{
			st[i] = gcd(st[2*i], st[2*i+1]);
		}
		
		public static int gcd(int a, int b) {
			while (b > 0) {
				int c = a;
				a = b;
				b = c % b;
			}
			return a;
		}
		
		
		public int gcdx(int l, int r){
			if(l >= r)return 0;
			int gcd = 0;
			while(l != 0){
				int f = l&-l;
				if(l+f > r)break;
				int v = st[(H+l)/f];
				gcd = gcd(gcd, v);
				l += f;
			}
			
			while(l < r){
				int f = r&-r;
				int v = st[(H+r)/f-1];
				gcd = gcd(gcd, v);
				r -= f;
			}
			return gcd;
		}
	}

	
