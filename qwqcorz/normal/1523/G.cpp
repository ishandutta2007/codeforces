#include<bits/stdc++.h>
using namespace std;
const int N=5e4+5;
const int M=1e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int l[M],r[M],n,m;
vector<int>a[N];
unordered_map<int,int>t[N];
void insert(int id,int x,int y)
{
	for (int i=x;i;i-=i&-i)
	for (int j=y;j<=n;j+=j&-j)
	if (t[i].count(j)) t[i][j]=min(t[i][j],id);
				  else t[i][j]=id;
}
int query(int x,int y)
{
	int ret=1e9;
	for (int i=x;i<=n;i+=i&-i)
	for (int j=y;j;j-=j&-j)
	if (t[i].count(j))
	ret=min(ret,t[i][j]);
	return ret;
}
int calc(int L,int R)
{
	if (L>R) return 0;
	int k=query(L,R);
	if (k==(int)1e9) return 0;
	return calc(L,l[k]-1)+r[k]-l[k]+1+calc(r[k]+1,R);
}
stack<int>ans;

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	n=read(),m=read();
	for (int i=1;i<=m;i++)
	{
		l[i]=read(),r[i]=read();
		a[r[i]-l[i]+1].push_back(i);
	}
	for (int i=n;i>=1;i--)
	{
		for (int j:a[i]) insert(j,l[j],r[j]);
		ans.push(calc(1,n));
	}
	while (!ans.empty()) print(ans.top()),ans.pop();
	
	return 0;
}
