#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N=5e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
int a[N];
int solve(int n,int m,int x)
{
	a[n+1]=0;
	a[0]=1e18;
	for (int i=n;i>=1;i--) a[i]+=a[i+1];
	for (int i=1;i<=n+1;i++) a[i]+=x*(i-1);
	for (int i=1;i<=n+1;i++) a[i]=min(a[i-1],a[i]);
	for (int k=0;k<=m;k++)
	{
		int Min=a[min(m-k+1,n+1)];
		if (Min+k*x>0) return k+n;
	}
	return -1;
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read(),m=(n+1)/2;
	n-=m;swap(n,m);
	for (int i=1;i<=n;i++) a[i]=read();
	int x=read();
	print(solve(n,m,x));
	
	return 0;
}
