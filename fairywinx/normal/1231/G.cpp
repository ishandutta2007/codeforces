const int MOD = 1e9 + 7;

long long gcd(long long a, long long b) {
	if (b == 0)
		return a;
	return gcd(b, a % b);
}

const int N = 1e5 + 228;

vector<int> G[N];
long long ans = 0;

void dfs(int v, int p, vector<long long> &a, map<long long, int> val) {
	map<long long, int> tmp_val;
	tmp_val[a[v]] = 1;
	for(auto i: val) 
		tmp_val[gcd(i.first, a[v])] += i.second;
	for(auto i: tmp_val)
		ans = (ans + i.first * i.second) % MOD;
	for(auto i: G[v]) {
		if (i == p)
			continue;
		dfs(i, v, a, tmp_val);
	}
}

