mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, m;
	cin >> n >> m;
	vector<pii> edge(m);
	vector<vi> adj(n);
	for(int i = 0; i < m; i ++){
		int u, v;
		cin >> u >> v, u --, v --;
		edge[i] = {u, v};
		adj[u].push_back(v), adj[v].push_back(u);
	}
	vi col(n, -1);
	function<void(int, int)> color = [&](int u, int c){
		col[u] = c;
		for(auto v: adj[u]){
			if(col[v] == -1){
				color(v, !c);
			}
			else if(col[v] == c){
				cout << "NO";
				exit(0);
			}
		}
	};
	color(0, 0);
	cout << "YES\n";
	for(auto [u, v]: edge){
		cout << col[u];
	}
	return 0;
}

