	void solve()
	{
		int n = ni(), K = ni();
		int[] a = na(n);
		long[] cum = new long[n+1];
		for(int i = 0;i < n;i++)cum[i+1] = cum[i] + a[i];
		long[][] cumi = new long[n+1][];
		for(int i = 0;i <= n;i++){
			cumi[i] = new long[]{cum[i], i};
		}
		Arrays.sort(cumi, new Comparator<long[]>() {
			public int compare(long[] a, long[] b) {
				return Long.compare(a[0], b[0]);
			}
		});
		long[] vs = new long[n+1];
		int[] iord = new int[n+1];
		for(int i = 0;i <= n;i++){
			vs[i] = cumi[i][0];
			iord[(int)cumi[i][1]] = i;
		}
		
		if(K == 1){
			out.println(count(1, cum, vs, iord, a));
			return;
		}else if(K == -1){
			out.println(count(1, cum, vs, iord, a)+count(-1, cum, vs, iord, a));
			return;
		}
		long x = 1;
		long ret = 0;
		while(Math.abs(x) <= 2e14){
			ret += count(x, cum, vs, iord, a);
			x *= K;
		}
		out.println(ret);
	}
	
	long count(long x, long[] cum, long[] vs, int[] iord, int[] a)
	{
		int n = a.length;
		int[] ft = new int[n+5];
		long ret = 0;
		for(int i = 0;i <= n;i++){
			ret += 
			sumFenwick(ft, lowerBound(vs, cum[i] - x + 1) - 1) - 
			sumFenwick(ft, lowerBound(vs, cum[i] - x) - 1)
			;
			
			addFenwick(ft, iord[i], 1);
		}
		return ret;
	}
	
	public static int[] restoreFenwick(int[] ft)
	{
		int n = ft.length-1;
		int[] ret = new int[n];
		for(int i = 0;i < n;i++)ret[i] = sumFenwick(ft, i);
		for(int i = n-1;i >= 1;i--)ret[i] -= ret[i-1];
		return ret;
	}
	
	public static int lowerBound(long[] a, long v)
	{
		int low = -1, high = a.length;
		while(high-low > 1){
			int h = high+low>>>1;
			if(a[h] >= v){
				high = h;
			}else{
				low = h;
			}
		}
		return high;
	}

	
	public static int sumFenwick(int[] ft, int i)
	{
		int sum = 0;
		for(i++;i > 0;i -= i&-i)sum += ft[i];
		return sum;
	}
	
	public static void addFenwick(int[] ft, int i, int v)
	{
		if(v == 0 || i < 0)return;
		int n = ft.length;
		for(i++;i < n;i += i&-i)ft[i] += v;
	}

	
