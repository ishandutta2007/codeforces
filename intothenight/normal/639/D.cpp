mt19937 rnd(time(0));



int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n, k;
	ll b, c;
	cin >> n >> k >> b >> c;
	b = min(b, 5 * c);
	vl cont(n);
	cin >> cont;
	sort(all(cont));
	if(cont.front() < 0){
		ll x = cont.front();
		for(auto &y: cont) y -= x;
	}
	ll res = 9e18;
	for(ll rem = 0; rem < 5; rem ++){
		auto dis = [&](ll x, ll y){
			return (y - x) / 5 * b + (y - x) % 5 * c;
		};
		vector<deque<ll>> q(5);
		ll time = 0, prev = cont[k - 2];
		while(prev % 5 != rem) prev ++;
		for(int i = 0; i + 1 < k; i ++){
			time += dis(cont[i], prev);
			q[cont[i] % 5].push_back(cont[i]);
		}
		for(int i = k - 1; i < n; i ++){
			ll target = cont[i];
			while(target % 5 != rem) target ++;
			time += (target - prev) / 5 * (k - 1) * b + dis(cont[i], target);
			q[cont[i] % 5].push_back(cont[i]);
			res = min(res, time);
			int p = -1;
			for(int j = 0; j < 5; j ++){
				if(!q[j].empty() && (p == -1 || dis(q[j].front(), target) > dis(q[p].front(), target))){
					p = j;
				}
			}
			time -= dis(q[p].front(), target);
			q[p].pop_front();
			prev = target;
		}
	}
	cout << res;
	return 0;
}

