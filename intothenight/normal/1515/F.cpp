int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, m, x;
	cin >> n >> m >> x;
	vector<long long> rem(n);
	for(auto i = 0; i < n; ++ i){
		cin >> rem[i];
	}
	graph<int> g(n);
	disjoint_set dsu(n);
	for(auto i = 0; i < m; ++ i){
		int u, v;
		cin >> u >> v, -- u, -- v;
		if(dsu.merge(u, v)){
			g.link(u, v, i);
		}
	}
	m = (int)g.edges.size();
	if(accumulate(rem.begin(), rem.end(), 0LL) < 1LL * x * (n - 1)){
		cout << "NO\n";
		return 0;
	}
	vector<int> ind(n);
	iota(ind.begin(), ind.end(), 0);
	dsu = {n};
	cout << "YES\n";
	int ans = 0;
	while(ans < n - 1){
		shuffle(ind.begin(), ind.end(), rng);
		debug(ind);
		vector<bool> vis(n);
		for(auto u: ind){
			if(vis[u]){
				continue;
			}
			y_combinator([&](auto self, int u, int pe)->void{
				vis[u] = true;
				for(auto id: g.adj[u]){
					if(id == pe) continue;
					auto &e = g.edges[id];
					int v = u ^ e.from ^ e.to;
					if(vis[v]) continue;
					if(dsu.share(u, v)){
						self(v, id);
					}
					else if(rem[dsu.root(u)] + rem[dsu.root(v)] >= x){
						long long nrem = rem[dsu.root(u)] + rem[dsu.root(v)] - x;
						dsu.merge(u, v);
						rem[dsu.root(u)] = nrem;
						cout << e.cost + 1 << "\n";
						++ ans;
						self(v, id);
					}
					else{
						break;
					}
				}
			})(u, -1);
		}
	}
	return 0;
}

