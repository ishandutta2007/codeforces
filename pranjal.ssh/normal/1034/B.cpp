int main()
{
	ios::sync_with_stdio(0); cin.tie(0);

	int n, m; cin >> n >> m;
	if (n > m) swap(n, m);
	ll ans = n*1LL*m;
	if (n == 1) {
		vi a{0, 1, 2, 3, 2, 1};
		ans -= a[m % 6];
	} else if (n == 2) {
		if (m == 2) ans -= 4;
		else if (m == 3 or m == 7) ans -= 2;
	} else if (n % 2 == 1) {
		if (m % 2 == 1) ans -= 1;
	}
	cout << ans << "\n";

	return 0;
}