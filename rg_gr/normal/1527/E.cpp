int t[1000005],a[100005],pre[100005],nxt[100005],dp[111][350005];
int l=1,r,res;
void solve(int L,int R)
{
	// odp(L,R);
	while(l>L)--l,res+=nxt[l]<=r?nxt[l]-l:0;
	while(r<R)++r,res+=pre[r]>=l?r-pre[r]:0;
	while(l<L)res-=nxt[l]<=r?nxt[l]-l:0,l++;
	while(r>R)res-=pre[r]>=l?r-pre[r]:0,r--;
	// odl(res);
}
void init(int l,int r,int L,int R,int k)
{
	if(l>r||L>R)return;
	int m=l+r>>1,pos;
	int &ans=dp[k][m];
	ans=0x3f3f3f3f;
	for(int i=L;i<=m&&i<=R;i++)
	{
		solve(i,m);
		int val=dp[k-1][i-1]+res;
		if(ans>val)ans=val,pos=i;
	}
	init(l,m-1,L,pos,k);
	init(m+1,r,pos,R,k);
}
