const int Mod = 998244353;
const int MAXN = 1000002;

int mul(int a, int b) {
    return (1LL * a * b) % Mod;
}

int sum(int a, int b) {
    return (a + b) % Mod;
}

int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        b >>= 1;
        a = mul(a, a);
    }
    return res;
}


int fact[MAXN];



int getcnk(int n, int k) {
    return mul(mul(fact[n], powm(fact[k], Mod - 2)), powm(fact[n - k], Mod - 2));
}



