int main(){
	cin.tie(0)->sync_with_stdio(0);
	auto query = [&](const deque<int> &arr){
		cout << "? ";
		for(auto x: arr){
			cout << x + 1 << " ";
		}
		cout << endl;
		int res1, res2;
		cin >> res1;
		if(res1 == -1) exit(0);
		cin >> res2;
		return pii(res1 - 1, res2);
	};
	int n, k;
	cin >> n >> k;
	deque<int> arr(k);
	iota(all(arr), 0);
	auto [p, u] = query(arr);
	int m = 1, flag; // flag = 0: lower, flag = 1: higher
	arr.erase(arr.begin() + p);
	arr.push_back(k);
	auto [q, v] = query(arr);
	flag = (v > u);
	arr.push_back(p);
	for(int i = 1; i < k; ++ i){
		int j = arr.front();
		arr.pop_front();
		auto [r, t] = query(arr);
		if(flag){
			if(r != p){
				m ++;
			}
		}
		else{
			if(r == p){
				m ++;
			}
		}
		arr.push_back(j);
	}
	cout << "! " << m;
	return 0;
}
 
