int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<pll> a(n);
	for(int i = 0; i < n; i ++){
		cin >> a[i].first;
	}
	for(int i = 0; i < n; i ++){
		cin >> a[i].second;
	}
	sort(all(a));
	vi cand;
	for(int i = 1; i < n; i ++){
		if(a[i - 1].first == a[i].first){
			for(int j = 0; j < n; j ++){
				if((a[i].first | a[j].first) == a[i].first){
					cand.push_back(j);
				}
			}
			while(i < n - 1 && a[i + 1].first == a[i].first){
				i ++;
			}
		}
	}
	sort(all(cand));
	cand.resize(unique(all(cand)) - cand.begin());
	ll res = 0;
	for(auto i: cand) res += a[i].second;
	cout << res;
	return 0;
}

