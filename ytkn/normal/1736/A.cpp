template<typename T>
void print_vector(vector<T> v, char delimiter=' '){
    if(v.empty()) {
        cout << endl;
        return;
    }
    for(int i = 0; i+1 < v.size(); i++) cout << v[i] << delimiter;
    cout << v.back() << endl;
}

void solve(){
    int n; cin >> n;
    vector<int> a(n), b(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    for(int i = 0; i < n; i++) cin >> b[i];
    int x = 0, y = 0;
    for(int i = 0; i < n; i++){
        if(a[i] == 0 && b[i] == 1) x++;
        if(a[i] == 1 && b[i] == 0) y++;
    }
    if(x == 0 || y == 0){
        cout << max(x, y) << endl;
        return;
    }
    int cnt_a = 0, cnt_b = 0;
    for(int i = 0; i < n; i++){
        if(a[i] == 0) cnt_a++;
        if(b[i] == 0) cnt_b++;
    }        
    cout << abs(cnt_a-cnt_b)+1 << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int t; cin >> t;
    while(t--) solve();
}