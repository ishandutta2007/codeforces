#include<bits/stdc++.h>
using namespace std;

#define int long long

void solve()
{
    int n;
    cin>>n;

    int x = n/3;
    int y = x;

    if(n%3==1) x++;
    if(n%3==2) y++;
    cout<<x<<" "<<y<<endl;
}

