void solve() {
    int w, h;
    cin >> w >> h;
    int x1, y1, x2, y2;
    cin >> x1 >> y1 >> x2 >> y2;
    int w1 = x2 - x1, h1 = y2 - y1;
    int w2, h2;
    cin >> w2 >> h2;
    if (w1 + w2 > w && h1 + h2 > h) {
        cout << -1 << endl;
        return;
    }
    int ans = INF;
    if (w1 + w2 <= w) {
        cmin(ans, w2 - x1);
        cmin(ans, x2 - (w - w2));
    }
    if (h1 + h2 <= h) {
        cmin(ans, h2 - y1);
        cmin(ans, y2 - (h - h2));
    }
    cmax(ans, 0);
    cout << ans << endl;
}