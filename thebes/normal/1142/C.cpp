#define foxr(i,x,y) for(int i=(y);i>=(x);i--)

const ll MN = 1e5+5;
ll n, i, x, y, tp=-1; pll st[MN];
map<ll,ll> pnt;

bool cp(pll o,pll a,pll b){
    return (a.first-o.first)*(b.second-o.second)-(a.second-o.second)*(b.first-o.first)>=0;
}

int main(){
    for(scanf("%lld",&n),i=1;i<=n;i++){
        scanf("%lld%lld",&x,&y);
        y-=x*x;
        if(!pnt.count(x)) pnt[x]=y;
        else pnt[x]=max(pnt[x],y);
    }
    for(auto v : pnt){
        pll cur=v;
        while(tp>=1&&cp(st[tp-1],st[tp],cur)) tp--;
        st[++tp]=cur;
    }
    printf("%lld\n",tp);
    return 0;
}