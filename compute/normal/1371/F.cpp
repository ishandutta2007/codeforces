struct D{
    int lc,rc,cnt,rans,lans,len;
    D(int _lc=0,int _rc=0,int _cnt=0,int _rans=0,int _lans=0,int _len=1):lc(_lc),rc(_rc),cnt(_cnt),rans(_rans),lans(_lans),len(_len){}
};
inline void upd(D &o,D l,D r)
{
    o.lc=l.lc;
    if(l.lc==l.len) o.lc+=r.lc;
    o.rc=r.rc;
    if(r.rc==r.len) o.rc+=l.rc;
    if(r.rans==r.len)
    {
        if(r.lc==r.len)
            o.rans=max(r.rans+l.rc,r.rans+l.rans);
        else o.rans=r.rans+l.rc;
    }
    else o.rans=r.rans;
    if(l.lans==l.len)
    {
        if(l.rc==l.len)
            o.lans=max(l.lans+r.lc,l.lans+r.lans);
        else o.lans=l.lans+r.lc;
    }
    else o.lans=l.lans;
    o.cnt=max({l.cnt,r.cnt,o.lans,o.rans,l.rc+r.lc,l.rans+r.lc,r.lans+l.rc,o.lc,o.rc});
    o.len=l.len+r.len;
}
struct N{
    D p,r;
    int lz;
    N(){}
};
const int maxn=5e5+7;
N T[maxn<<2];
int n,q;
string s;
#define lson o<<1
#define rson o<<1|1
#define Lson L,mid,lson
#define Rson mid+1,R,rson
inline void pushup(int o)
{
    upd(T[o].p,T[lson].p,T[rson].p);
    upd(T[o].r,T[lson].r,T[rson].r);
}
void build(int L=0,int R=n-1,int o=1)
{
    if(L==R)
    {
        T[o].p.lc=T[o].r.rc=1,T[o].p.rc=T[o].r.lc=0;
        T[o].p.rans=T[o].r.lans=1;
        T[o].p.cnt=T[o].r.cnt=1;
        if(s[L]!='<') swap(T[o].p,T[o].r);
        return;
    }
    int mid=L+R>>1;
    build(Lson);
    build(Rson);
    pushup(o);
}
inline void pushdown(int o)
{
    if(T[o].lz)
    {
        swap(T[lson].p,T[lson].r);
        swap(T[rson].p,T[rson].r);
        T[o].lz=0;
        T[lson].lz^=1;
        T[rson].lz^=1;
    }
}
void rev(int l,int r,int L=0,int R=n-1,int o=1)
{
    if(l<=L&&r>=R)
    {
        swap(T[o].p,T[o].r);
        T[o].lz^=1;
        return;
    }
    pushdown(o);
    int mid=L+R>>1;
    if(l<=mid) rev(l,r,Lson);
    if(r>mid) rev(l,r,Rson);
    pushup(o);
}
D query(int l,int r,int L=0,int R=n-1,int o=1)
{
    if(l<=L&&r>=R)
        return T[o].p;
    pushdown(o);
    int mid=L+R>>1;
    D ret;
    ret.len=0;
    // dbg(L,R);
    if(l<=mid) upd(ret,ret,query(l,r,Lson));
    if(r>mid) upd(ret,ret,query(l,r,Rson));
    return ret;
}
void solve(int l,int r)
{
    rev(l,r);
    cout<<query(l,r).cnt<<'\n';
}
