const int Mod = 1000000007;


int sum(int a, int b) {
    return (a + b >= Mod ? a + b - Mod: a + b);
}


int mul(int a, int b) {
    return ((ll)a * b) % Mod;
}


int powm(int a, int b) {
    int res = 1;
    while (b) {
        if (b & 1) {
            res = mul(res, a);
        }
        a = mul(a, a);
        b >>= 1;
    }
    return res;
}


int inv(int a) {
    return powm(a, Mod - 2);
}



const int MAXM = 100228;


int n, m, q;
ll a[MAXM];
ll b[MAXM];
int ss = 1;
ll d[MAXM * 4];
ll mod[MAXM * 4];



void push(int v) {
    if (mod[v] != 0) {
        d[v] += mod[v];
        if (v < ss) {
            mod[v * 2] += mod[v];
            mod[v * 2 + 1] += mod[v];
        }
        mod[v] = 0;
    }
}



void add(int v, int vl, int vr, int l, int r, ll val) {
    if (vl > r || vr < l) {
        push(v);
        return;
    }
    if (l <= vl && vr <= r) {
        mod[v] += val;
        push(v);
        return;
    }
    push(v);
    add(v * 2, vl, (vl + vr) / 2, l, r, val);
    add(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r, val);
    d[v] = max(d[v * 2], d[v * 2 + 1]);
}


int get(int v, int vl, int vr, int pos, ll val) {
    push(v);
    if (d[v] < val) {
        return -1;
    }
    if (vr < pos) {
        return -1;
    }
    if (vl == vr) {
        return vl;
    }
    int f = get(v * 2, vl, (vl + vr) / 2, pos, val);
    if (f != -1) {
        return f;
    }
    return get(v * 2 + 1, (vl + vr) / 2 + 1, vr, pos, val);
}



ll d1[MAXM * 4];
ll mod1[MAXM * 4];



void push1(int v, int len) {
    if (mod1[v] != 0) {
        d1[v] += mod1[v] * len;
        if (v < ss) {
            mod1[v * 2] += mod1[v];
            mod1[v * 2 + 1] += mod1[v];
        }
        mod1[v] = 0;
    }
}



void add1(int v, int vl, int vr, int l, int r, ll val) {
    if (vl > r || vr < l) {
        push1(v, vr - vl + 1);
        return;
    }
    if (l <= vl && vr <= r) {
        mod1[v] += val;
        push1(v, vr - vl + 1);
        return;
    }
    push1(v, vr - vl + 1);
    add1(v * 2, vl, (vl + vr) / 2, l, r, val);
    add1(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r, val);
    d1[v] = d1[v * 2] + d1[v * 2 + 1];
}


ll get1(int v, int vl, int vr, int l, int r) {
    if (vl > r || vr < l) {
        return 0;
    }
    if (l <= vl && vr <= r) {
        return d1[v] + (vr - vl + 1) * mod1[v];
    }
    push1(v, vr - vl + 1);
    return get1(v * 2, vl, (vl + vr) / 2, l, r) + get1(v * 2 + 1, (vl + vr) / 2 + 1, vr, l, r);
}



