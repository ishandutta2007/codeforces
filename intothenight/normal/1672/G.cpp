using modular = modular_base<integral_constant<decay<decltype(mod)>::type, mod>>;

template<bool Enable_small_to_large = true>
struct disjoint_set{
	int n, _classes;
	vector<int> p;
	disjoint_set(int n): n(n), _classes(n), p(n, -1){ }
	int make_set(){
		p.push_back(-1);
		++ _classes;
		return n ++;
	}
	int classes() const{
		return _classes;
	}
	int root(int u){
		return p[u] < 0 ? u : p[u] = root(p[u]);
	}
	bool share(int a, int b){
		return root(a) == root(b);
	}
	int size(int u){
		return -p[root(u)];
	}
	bool merge(int u, int v){
		u = root(u), v = root(v);
		if(u == v) return false;
		-- _classes;
		if constexpr(Enable_small_to_large) if(p[u] > p[v]) swap(u, v);
		p[u] += p[v], p[v] = u;
		return true;
	}
	bool merge(int u, int v, auto act){
		u = root(u), v = root(v);
		if(u == v) return false;
		-- _classes;
		if constexpr(Enable_small_to_large) if(p[u] > p[v]) swap(u, v);
		p[u] += p[v], p[v] = u;
		act(u, v);
		return true;
	}
	void clear(){
		_classes = n;
		fill(p.begin(), p.end(), -1);
	}
	vector<vector<int>> group_up(){
		vector<vector<int>> g(n);
		for(auto i = 0; i < n; ++ i) g[root(i)].push_back(i);
		g.erase(remove_if(g.begin(), g.end(), [&](auto &s){ return s.empty(); }), g.end());
		return g;
	}
};

int main(){
	cin.tie(0)->sync_with_stdio(0);
	cin.exceptions(ios::badbit | ios::failbit);
	int n, m;
	cin >> n >> m;
	vector<string> a(n);
	copy_n(istream_iterator<string>(cin), n, a.begin());
	if(~n & 1 && ~m & 1){
		cout << (modular(2) ^ ranges::count(a | ranges::views::join, '?')) << "\n";
	}
	else if(n & 1 && m & 1){
		disjoint_set dsu(n + m);
		vector<int> val(n + m), cnt(n + m);
		for(auto i = 0; i < n; ++ i){
			for(auto j = 0; j < m; ++ j){
				if(a[i][j] == '?'){
					++ cnt[dsu.root(i)];
					dsu.merge(i, n + j, [&](int u, int v){ cnt[u] += cnt[v]; });
				}
				else{
					val[i] ^= a[i][j] - '0';
					val[n + j] ^= a[i][j] - '0';
				}
			}
		}
		auto grps = dsu.group_up();
		modular res = 0;
		for(auto obj = 0; obj < 2; ++ obj){
			modular cur = 1;
			for(auto g: grps){
				int x = 0;
				for(auto u: g){
					x ^= val[u];
				}
				int k = cnt[dsu.root(g[0])];
				if((int)g.size() & 1){
					if(obj == x){
						cur *= modular(2) ^ k - (int)g.size() + 1;
					}
					else{
						cur = 0;
					}
				}
				else{
					if(x == 0){
						cur *= modular(2) ^ k - (int)g.size() + 1;
					}
					else{
						cur = 0;
					}
				}
			}
			res += cur;
		}
		cout << res << "\n";
	}
	else{
		if(n & 1){
			vector<string> b(m);
			for(auto j = 0; j < m; ++ j){
				for(auto i = 0; i < n; ++ i){
					b[j].push_back(a[i][j]);
				}
			}
			swap(n, m);
			swap(a, b);
		}
		modular res = 0;
		for(auto obj = 0; obj < 2; ++ obj){
			modular cur = 1;
			for(auto s: a){
				int q = ranges::count(s, '?');
				int x = ranges::count(s, '1') & 1;
				if(q){
					cur *= modular(2) ^ q - 1;
				}
				else{
					cur *= x == obj;
				}
			}
			res += cur;
		}
		cout << res << "\n";
	}
	return 0;
}

