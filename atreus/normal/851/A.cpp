#include <bits/stdc++.h>

using namespace std;

int main (){
    ios_base::sync_with_stdio(false);
    long long n, k, t;
    cin >> n >> k >> t;
    if (t <= k)
        return cout << t, 0;
    if (t >= n)
        return cout << k - (t - n), 0;
    cout << k << endl;
}