const string FILENAME = "input";

long double ans = -1e18;

int n, w;
vector<int> a;

int main() {
	srand(time(0));
	ios::sync_with_stdio(false);
	cin >> n >> w;
	a.resize(n);
	for (int i = 0; i < n; ++i) {
		cin >> a[i];
	}
	for (int i = 0; i + 1 < n; ++i) {
		int q = upper_bound(a.begin(), a.end(), a[i] + w) - a.begin() - 1;
		if (q - i < 2) continue;
		long double A = a[q], B = a[i + 1], C = a[i];
		chkmax(ans, (A - B) / (A - C));
	}
	if (ans < 0) {
		cout << -1 << endl;
	} else {
		cout.precision(18);
		cout << fixed << ans << endl;
	}
}