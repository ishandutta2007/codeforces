ll n,k,fac[N],inv[N];
const ll maxn = 500000;
ll qpow(ll a,ll b){
	ll res = 1;
	while(b){
		if(b&1) res = (res*a)%mod;
		a = (a*a)%mod;
		b >>= 1;
	}
	return res;
}
ll C(ll x,ll y){
	if(x > y) return 0;
	return (fac[y]*inv[x]%mod)%mod*inv[y-x]%mod;
}
