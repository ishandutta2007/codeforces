int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

    int n, k;
    cin >> n >> k;
    vector<int> a(n);
    cin >> a;
    for (int i = 0; i < n; ++i) {
        a[i] -= i * k;
    }
    vector<int> b = a;
    sort(b.begin(), b.end());

    map<int, int> cnt;
    for (int k : b)
        if (k > 0)
            cnt[k]++;

    assert(!cnt.empty());
    int mx = -1, m = -1;
    for (auto [a, b] : cnt) {
        if (b > mx) {
            mx = b;
            m = a;
        }
    }

    cout << n - count(a.begin(), a.end(), m) << '\n';
    for (int i = 0; i < n; ++i) {
        if (a[i] < m) {
            cout << "+ " << i + 1 << ' ' << m - a[i] << '\n';
        } else if (a[i] > m) {
            cout << "- " << i + 1 << ' ' << a[i] - m << '\n';
        }
    }

    return 0;
}