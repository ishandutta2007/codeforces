#include<bits/stdc++.h>
#define int long long
#define mk make_pair
#define fi first
#define se second
using namespace std;
const int N=2e5+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
vector<int>son[N*2];
bool vis[N*2],used[N];
vector<pair<int,int>>e[N*2];
map<pair<int,int>,int>id;
int cnt=0;
int gcd(int x,int y) {return !y?x:gcd(y,x%y);}
int get(int a,int b,int c,int d)
{
	int x=a*d,y=b*c,g=gcd(x,y);
	x/=g,y/=g;
	if (id.count(mk(x,y))) return id[mk(x,y)];
	return id[mk(x,y)]=++cnt;
}
vector<pair<int,int>>ans;
void dfs0(int now)
{
	vis[now]=1;
	for (auto i:e[now])
	{
		int to=i.fi;
		if (vis[to]) continue;
		dfs0(to);
		son[now].push_back(to);
	}
}
vector<int>tmp;
void dfs1(int now,int father)
{
	int fa=0;
	vis[now]=1;
	for (int i:son[now]) dfs1(i,now);
	for (auto i:e[now])
	if (i.fi==father) fa=i.se;
	else if (!used[i.se]) tmp.push_back(i.se);
//	print(now,':');
//	print(fa,'.');
//	for (int i:tmp) print(i,' ');
//	puts("");
	while ((int)tmp.size()>1)
	{
		int x=tmp.back();
		tmp.pop_back();
		int y=tmp.back();
		tmp.pop_back();
		ans.push_back(mk(x,y));
		used[x]=used[y]=1;
	}
	if (tmp.size()&&fa) used[tmp.back()]=used[fa]=1,ans.push_back(mk(tmp.back(),fa));
	tmp.clear();
}

signed main(signed Recall,char *_902_[])
{
	(void)Recall,(void)_902_;
	int n=read();
	for (int i=1;i<=n;i++)
	{
		int a=read(),b=read(),c=read(),d=read();
		int u=get(a+b,b,c,d),v=get(a,b,c+d,d);
		e[u].push_back(mk(v,i));
		e[v].push_back(mk(u,i));
//		print(u,' ');
//		print(v);
	}
	for (int i=1;i<=cnt;i++) if (!vis[i]) dfs0(i);
	memset(vis,0,sizeof(vis));
	for (int i=1;i<=cnt;i++) if (!vis[i]) dfs1(i,0);
	print(ans.size());
	for (auto i:ans) print(i.fi,' '),print(i.se);
	
	return 0;
}
