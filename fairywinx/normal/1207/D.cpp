const int MOD = 998244353;
const int N = 3e5 + 228;

int fact[N];

signed main() {
	setup();
	// SOLVE;
	
	int n;
	cin >> n;
	map<int, int> cnt1, cnt2;
	map<pair<int, int>, int> cnt3;
	vector<pair<int, int>> z;
	for(int i = 0; i < n; ++i) {
		int a, b;
		cin >> a >> b;
		++cnt1[a], ++cnt2[b], ++cnt3[make_pair(a, b)];
		z.emplace_back(a, b);
	}
	int ans = 1;
	for(int i = 1; i <= n; ++i)
		ans = (ans * i) % MOD;

	fact[0] = 1;
	for(int i = 1; i <= n; ++i) 
		fact[i] = (fact[i - 1] * i) % MOD;

	int res = 1;
	for(auto i: cnt1)
		res = (res * fact[i.second]) % MOD;
	ans = ((ans - res) % MOD + MOD) % MOD;
	res = 1;
	for(auto i: cnt2)
		res = (res * fact[i.second]) % MOD;
	ans = ((ans - res) % MOD + MOD) % MOD;
	// cout << res << '\n';

	sort(all(z));
	bool can = true;
	for(int i = 1; i < n; ++i) {
		if (z[i].second < z[i - 1].second)
			can = false;
	}
	if (can) {
		res = 1;
		for(auto i: cnt3)
			res = (res * fact[i.second]) % MOD;
		ans = ((ans + res) % MOD + MOD) % MOD;
	}
	cout << ans << '\n';

	#ifdef LOCAL
		cerr << "processor time: " << 1.0 * clock() / CLOCKS_PER_SEC << "s    ";
	#endif
}


