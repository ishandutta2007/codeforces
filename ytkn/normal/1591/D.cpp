struct UnionFind {
    vector<int> data;
    UnionFind(int size) : data(size, -1) {}
    bool unionSet(int x, int y) {
        x = root(x); y = root(y);
        if (x != y) {
        if (data[y] < data[x]) swap(x, y);
        data[x] += data[y]; data[y] = x;
        }
        return x != y;
    }
    bool findSet(int x, int y) {
        return root(x) == root(y);
    }
    int root(int x) {
        return data[x] < 0 ? x : data[x] = root(data[x]);
    }
    int size(int x) {
        return -data[root(x)];
    }
};

void solve(){
    int n; cin >> n;
    vector<int> a(n);
    vector<int> cnt(n);
    for(int i = 0; i < n; i++) {
        cin >> a[i];
        a[i]--;
        cnt[a[i]]++;
    }
    int cnt_max = *max_element(cnt.begin(), cnt.end());
    if(cnt_max >= 2){
        cout << "YES" << endl;
        return;
    }
    UnionFind uf(n);
    for(int i = 0; i < n; i++) uf.unionSet(i, a[i]);
    int sum = 0;
    for(int i = 0; i < n; i++){
        if(uf.root(i) != i) continue;
        sum += uf.size(i)-1;
    }
    if(sum%2 == 0){
        cout << "YES" << endl;
    }else{
        cout << "NO" << endl;
    }
}

