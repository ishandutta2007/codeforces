void solve() {
	int d, m;
	cin >> d >> m;
	vector<int> dp(31, 1);
	int ans = 1;
	for(int i = 1; i < 31; ++i) {
		if ((1 << i) > d)
			continue;
		int sum = 1;
		for(int j = 0; j < i; ++j)
			sum = (sum + dp[j]) % m;
		dp[i] = (long long) (sum * (min((1ll << (i + 1)), (long long) d + 1) - (1 << i))) % m;
		ans = (dp[i] + ans) % m;
	}
	cout << ans % m << '\n';
}

