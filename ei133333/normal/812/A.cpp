#include<bits/stdc++.h>

using namespace std;

int main()
{
  int A[4][4];

  for(int i = 0; i < 4; i++) {
    for(int j = 0; j < 4; j++) {
      cin >> A[i][j];
    }
  }

  if((A[0][3] && (A[0][1] | A[2][1] | A[1][0] | A[3][2] | A[0][0] | A[0][2])) ||
     (A[2][3] && (A[0][1] | A[2][1] | A[1][2] | A[3][0] | A[2][0] | A[2][2])) ||
     (A[1][3] && (A[1][1] | A[3][1] | A[0][2] | A[2][0] | A[1][0] | A[1][2])) ||
     (A[3][3] && (A[1][1] | A[3][1] | A[0][0] | A[2][2] | A[3][0] | A[3][2]))) {
    cout << "YES" << endl;
  } else {
    cout << "NO" << endl;
  }

}