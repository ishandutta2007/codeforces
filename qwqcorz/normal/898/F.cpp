#include<bits/stdc++.h>
using namespace std;
const int N=1e6+5;
const int p1=1e9+7;
const int p2=998244353;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0) putchar('-'),x=-x;
	if (x<10) putchar(x+'0');
	else write(x/10),putchar(x%10+'0');
}
void print(int x=-1,char c='\n')
{
	write(x);
	putchar(c);
}
char s[N];
int a[N],h1[N],h2[N],pw1[N],pw2[N],n;
int hash1(int l,int r){return (h1[r]-1LL*h1[l-1]*pw1[r-l+1]%p1+p1)%p1;}
int hash2(int l,int r){return (h2[r]-1LL*h2[l-1]*pw2[r-l+1]%p2+p2)%p2;}
bool check(int l1,int l2)
{
	if (l1<=0||l2<=0||l1+l2>=n||(a[1]==0&&l1>1)||(a[l1+1]==0&&n-l1-l2>1)||(a[n-l2+1]==0&&l2>1)) return 0;
	if ((hash1(1,l1)+hash1(l1+1,n-l2))%p1!=hash1(n-l2+1,n)) return 0;
	if ((hash2(1,l1)+hash2(l1+1,n-l2))%p2!=hash2(n-l2+1,n)) return 0;
	return 1;
}
void output(int l1,int l2)
{
	for (int i=1;i<=n;i++)
	{
		putchar(s[i]);
		if (i==l1) putchar('+');
		if (i+l2==n) putchar('=');
	}
	exit(0);
}

signed main(signed Goodbye,char *Wangang[])
{
	(void)Goodbye,(void)Wangang;
	scanf("%s",s+1);
	n=strlen(s+1);
	for (int i=1;i<=n;i++) a[i]=s[i]-'0';
	for (int i=1;i<=n;i++) h1[i]=(h1[i-1]*10LL+a[i])%p1;
	for (int i=1;i<=n;i++) h2[i]=(h2[i-1]*10LL+a[i])%p2;
	for (int i=pw1[0]=1;i<=n;i++) pw1[i]=pw1[i-1]*10LL%p1;
	for (int i=pw2[0]=1;i<=n;i++) pw2[i]=pw2[i-1]*10LL%p2;
	for (int i=n/3;i<=n/2;i++)
	{
		if (check(i,i)) output(i,i);
		if (check(i-1,i)) output(i-1,i);
		if (check(n-i-i,i)) output(n-i-i,i);
		if (check(n-i-i+1,i)) output(n-i-i+1,i);
	}
	puts("Bomb!");
	
	return 0;
}
