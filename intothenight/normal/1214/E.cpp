ostream &operator<<(ostream &out, const deque<T> &arr){
	for(auto &x: arr) out << x << ' '; cout << "\n";
	return out;
}

struct query{
	int ind, len;
	bool operator<(const query &other) const{return len > other.len;}
};

int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	vector<query> a(n);
	for(int i = 0; i < n; i ++){
		cin >> a[i].len;
		a[i].ind = i + 1;
	}
	sort(all(a));
	vi line(a[0].len);
	vector<vi> adj(a[0].len);
	for(int i = 0; i < a[0].len; i ++){
		line[i] = 2 * a[i].ind;
		if(i + a[i].len == line.size()){
			line.push_back(2 * a[i].ind - 1);
			adj.push_back(vi());
		}
		else{
			adj[i + a[i].len - 1].push_back(2 * a[i].ind - 1);
		}
	}
	for(int i = 0; i + 1 < line.size(); i ++){
		cout << line[i] << " " << line[i + 1] << "\n";
	}
	for(int i = 0; i < line.size(); i ++){
		for(auto p: adj[i]){
			cout << line[i] << " " << p << "\n";
		}
	}
	for(int i = a[0].len; i < n; i ++){
		if(a[i].len == 1){
			cout << a[i].ind * 2 << " " << a[i].ind * 2 - 1 << "\n";
			cout << line[0] << " " << a[i].ind * 2 << "\n";
		}
		else{
			cout << line[0] << " " << a[i].ind * 2 << "\n";
			cout << line[a[i].len - 2] << " " << a[i].ind * 2 - 1 << "\n";
		}
	}
	return 0;
}

