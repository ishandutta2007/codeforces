int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    string S, T;
    cin >> S >> T;

    int n = S.size(), m = T.size();
    vector<vector<int>> dp(n + 1, vector<int>(n + 1, INF));
    dp[0][0] = 0;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j <= n; j++) chmin(dp[i + 1][j], dp[i][j]);

        int k = i, l = 0;
        for (; l < m; k++, l++) {
            while (k < n && S[k] != T[l]) k++;
            if (k == n) break;
        }
        if (l < m) continue;

        for (int j = 0; j < n; j++) chmin(dp[k][j + 1], dp[i][j] + (k - i - m));
    }

    vector<int> ans(n + 1, 0);
    for (int j = 0; j <= n; j++) {
        if (dp[n][j] == INF) continue;
        int need = dp[n][j], can = n - need - m * j;
        for (int i = need; i <= need + can; i++) chmax(ans[i], j);
    }

    cout << ans << '\n';
    return 0;
}