#include<bits/stdc++.h>
using namespace std;
long long n,i,a[256],l,ll,j;
bool b;
string s;
int main()
{
	cin>>n;
	cin>>s;
	if(n%4!=0)
	{
		cout<<"==="<<endl;
	}
	else
	{
		l=n/4;
		b=1;
		for(i=0;i<s.length();i++)
		{
			if(s[i]=='A')
			{
				a[1]++;
			}
			else
			if(s[i]=='C')
			{
				a[2]++;
			}
			else
			if(s[i]=='G')
			{
				a[3]++;
			}
			else
			if(s[i]=='T')
			{
				a[4]++;
			}
			else
			if(s[i]=='?')
			{
				ll++;
			}
		}
		for(i=1;i<=4;i++)
		{
			if(a[i]>l)
			{
				b=0;
				break;
			}
			else
			{
				a[i]=l-a[i];
			}
		}
		if(b==1)
		{
			for(i=0;i<s.length();i++)
			{
				if(s[i]=='?')
				{
					if(a[1]>0)
					{
						cout<<'A';
						a[1]--;
					}
					else
					if(a[2]>0)
					{
						cout<<'C';
						a[2]--;
					}
					else
					if(a[3]>0)
					{
						cout<<'G';
						a[3]--;
					}
					else
					if(a[4]>0)
					{
						cout<<'T';
						a[4]--;
					}
				}
				else
					cout<<s[i];
			}
			cout<<endl;
		}
		else
			cout<<"==="<<endl;
	}
}