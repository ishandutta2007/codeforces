int n, q, a, c;

vector<int> G[300007];

int res[300007];

int sub[300007];

int pre[300007];

struct comp
{
    bool operator()(const int &a, const int &b)
    {
        return sub[a]<sub[b];
    }
};

set<int, comp> s[300007];

void dfs(int v, int f)
{
    pre[v] = ++c;

    for(auto it:G[v])
    {
        if(it!=f)
        {
            dfs(it, v);

            if(s[it]>s[v])
                s[v].swap(s[it]);

            s[v].insert(s[it].begin(), s[it].end());
        }
    }

    sub[v] = c-pre[v]+1;

    s[v].insert(v);

    sub[0] = (sub[v]+1)/2;

    res[v] = *(s[v].lower_bound(0));
}

int main()
{
    cin >> n >> q;

    for(int i = 2; i <= n; ++i)
    {
        cin >> a;

        G[a].pb(i);
        G[i].pb(a);
    }

    dfs(1, -1);

    for(int i = 1; i <= q; ++i)
    {
        cin >> a;

        cout << res[a] << endl;
    }

}