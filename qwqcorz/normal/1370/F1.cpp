#include<bits/stdc++.h>
using namespace std;
const int N=1e3+5;

int read()
{
	int s=0;
	char c=getchar(),lc='+';
	while (c<'0'||'9'<c) lc=c,c=getchar();
	while ('0'<=c&&c<='9') s=s*10+c-'0',c=getchar();
	return lc=='-'?-s:s;
}
void write(int x)
{
	if (x<0)
	{
		putchar('-');
		x=-x;
	}
	if (x<10) putchar(x+'0');
	else
	{
		write(x/10);
		putchar(x%10+'0');
	}
}
void print(int x,char c='\n')
{
	write(x);
	putchar(c);
}
struct edge
{
	int to,nxt;
}e[N*2];
int head[N],cnte;
void add_edge(int u,int v)
{
	e[++cnte].to=v;
	e[cnte].nxt=head[u];
	head[u]=cnte;
}
int x,d;
vector<int>a;
void dfs(int now,int father,int deep,int b)
{
	if (deep==b) a.push_back(now);
	for (int i=head[now];i;i=e[i].nxt)
	{
		int to=e[i].to;
		if (to==father) continue;
		dfs(to,now,deep+1,b);
	}
}
void Q(int k,int b)
{
	a.clear();
	dfs(k,0,0,b);
	if (a.empty()) return void(d=-114514);
	cout<<"? ";
	print(a.size(),' ');
	for (int i:a) print(i,' ');
	cout<<endl;
	x=read(),d=read();
}
void clear(int n)
{
	cnte=0;
	for (int i=1;i<=n;i++) head[i]=0;
}

