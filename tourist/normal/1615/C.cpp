int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    int n;
    cin >> n;
    string sa, sb;
    cin >> sa >> sb;
    vector<int> a(n);
    vector<int> b(n);
    for (int i = 0; i < n; i++) {
      a[i] = (int) (sa[i] - '0');
      b[i] = (int) (sb[i] - '0');
    }
    const int inf = (int) 1e9;
    int ans = inf;
    for (int p = 0; p < 2; p++) {
      auto c = a;
      for (int i = 0; i < n; i++) {
        c[i] ^= p;
      }
      int x = 0;
      int y = 0;
      for (int i = 0; i < n; i++) {
        if (c[i] != b[i]) {
          if (c[i] == 1) {
            x += 1;
          } else {
            y += 1;
          }
        }
      }
      if (y == x + p) {
        ans = min(ans, x + y);
      }
    }
    cout << (ans == inf ? -1 : ans) << '\n';
  }
  return 0;
}