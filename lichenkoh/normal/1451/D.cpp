bool p2wins(int _d, int _k) {
  ll d2=_d*ll(_d);
  ll k2=_k*ll(_k);
  ll n=floor(sqrt((ld)(d2/(2*k2)) ));
  while(2*n*n*k2 > d2) n--;
  while(2*(n+1)*(n+1)*k2 <= d2) n++;
  assert(2*(n+1)*(n+1)*k2 > d2);
  assert(2*n*n*k2 <= d2);
  bool ans=( (n*n + (n+1)*(n+1))*k2 > d2);
  //printf("d:%d k:%d n:%lld p2wins:%d\n",_d,_k,n,ans?1:0);
  return ans;
}

