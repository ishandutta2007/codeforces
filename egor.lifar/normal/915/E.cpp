const int MAXN = 1111111;


int n, q;
int mod[MAXN * 4];
int d[MAXN * 4];
vector<int> vx;


void build(int v, int vl, int vr) {
    if (vl + 1 == vr) {
        mod[v] = 0;
        d[v] = vx[vl + 1] - vx[vl];
        return;
    }
    int m = (vl + vr) >> 1;
    mod[v] = 0;
    build(v * 2 + 1, vl, m);
    build(v * 2 + 2, m, vr);
    d[v] = vx[vr] - vx[vl];
}


void push(int v, int vl, int vr) {
    if (!mod[v]) {
        return;
    }
    int m = (vl + vr) >> 1;
    if (mod[v] == 1) {
        mod[v * 2 + 1] = 1;
        d[v * 2 + 1] = 0;
        mod[v * 2 + 2] = 1;
        d[v * 2 + 2] = 0;
        mod[v] = 0;
    } else {
        mod[v * 2 + 1] = 2;
        d[v * 2 + 1] = vx[m] - vx[vl];
        mod[v * 2 + 2] = 2;
        d[v * 2 + 2] = vx[vr] - vx[m];
        mod[v] = 0;
    }
}


void add(int v, int vl, int vr, int l, int r, int k) {
    if (r <= vl || vr <= l) {
        return;
    }
    if (l <= vl && vr <= r) {
        mod[v] = k;
        if (k == 1) {
            d[v] = 0;
        } else {
            d[v] = vx[vr] - vx[vl];
        }
        return;
    }
    push(v, vl, vr);
    int m = (vl + vr) >> 1;
    add(v * 2 + 1, vl, m, l, r, k);
    add(v * 2 + 2, m, vr, l, r, k);
    d[v] = d[v * 2 + 1] + d[v * 2 + 2];
}


