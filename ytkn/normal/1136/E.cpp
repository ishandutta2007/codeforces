using P = pair<int, ll>;

struct S{
    ll sum, len;
    S(ll sum, ll len): sum(sum), len(len) {}
};

S e(){
    return S(0, 0);
}

S op(S x, S y){
    return S(x.sum+y.sum, x.len+y.len);
}

S mapping(ll f, S x){
    return S(x.sum+f*x.len, x.len);
}

ll add(ll x, ll y){
    return x+y;
}

ll id(){
    return 0ll;
}

using RangeAddRangeSum = atcoder::lazy_segtree<S, op, e, ll, mapping, add, id>;
const ll inf = 1e18;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout << setprecision(10) << fixed;
    int n; cin >> n;
    vector<ll> a(n), k(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    for(int i = 0; i < n-1; i++) cin >> k[i];
    RangeAddRangeSum seg(n);
    for(int i = 0; i < n; i++) seg.set(i, S(a[i], 1));
    set<P> diffs;
    for(int i = 0; i < n-1; i++) {
        if(a[i+1]-a[i] == k[i]) continue;
        diffs.insert(P(i, a[i+1]-a[i]));
    }
    diffs.insert(P(n-1, inf));
    int q; cin >> q;
    while(q--){
        char c; cin >> c;
        if(c == '+'){
            int i; ll x; cin >> i >> x; i--;
            auto p = diffs.lower_bound(P(i, -inf));
            vector<P> erased, inserted;
            if(i > 0) {
                if(p == diffs.begin()){
                    inserted.push_back(P(i-1, k[i-1]+x));
                }else{
                    auto q = *prev(p);
                    auto [idx, d] = q;
                    if(idx == i-1){
                        erased.push_back(q);
                        inserted.push_back(P(i-1, d+x));
                    }else{
                        inserted.push_back(P(i-1, k[i-1]+x));
                    }    
                }
            }
            int l = i;
            while(p != diffs.end()){
                auto [r, diff] = *p;
                seg.apply(l, r+1, x);
                if(diff-x > k[r]){
                    erased.push_back(P(r, diff));
                    inserted.push_back(P(r, diff-x));
                    break;
                }else{
                    erased.push_back(P(r, diff));
                    x = k[r]+x-diff;
                }
                l = r+1;
                p++;
            }
            for(auto p: erased) diffs.erase(p);
            for(auto p: inserted) diffs.insert(p);
        }else{
            int l, r; cin >> l >> r; l--;
            cout << seg.prod(l, r).sum << '\n';
        }
        // for(int i = 0; i < n; i++) cout << seg.get(i).sum << ' ';
        // cout << endl;
        // for(int i = 0; i < n; i++) cout << b[i] << ' ';
        // cout << endl;
    }
}