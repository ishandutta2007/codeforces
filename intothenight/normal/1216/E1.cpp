int main(){
	ios::sync_with_stdio(0);
	cin.tie(0);
	auto cal = [&](int n){
		ll res = 0, temp = 1;
		while(temp <= n){
			res += (n - temp + 1) * (n - temp + 2) / 2;
			temp *= 10;
		}
		return res;
	};
	auto cal2 = [&](int n){
		ll res = 0, temp = 1;
		while(temp <= n){
			res += n - temp + 1;
			temp *= 10;
		}
		return res;
	};
	int qq;
	cin >> qq;
	while(qq --){
		ll k;
		cin >> k;
		int l = 0, r = 1000000001;
		while(r - l > 1){
			int m = l + r >> 1;
			if(cal(m) < k) l = m;
			else r = m;
		}
		int n = l;
		k -= cal(n);
		l = 0, r = n + 1;
		while(r - l > 1){
			int m = l + r >> 1;
			if(cal2(m) < k) l = m;
			else r = m;
		}
		n = l + 1;
		k -= cal2(n - 1);
		int d = 1;
		ll temp = 1;
		while(temp * 10 <= n) temp *= 10, d ++;
		k = d - k;
		for(int i = 0; i < k; i ++) n /= 10;
		cout << n % 10 << "\n";
	}
	return 0;
}

