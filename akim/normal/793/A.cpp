const double eps = 1e-9;
const int mod = (int) 1e+9 + 7;
const double pi = acos(-1.);
const int maxn = 100100;

int a[maxn];

int solution() {

	int n, k;
	scanf("%d%d", &n, &k);
	int mn = mod;
	ll ans = 0;
	for(int i = 0; i < n; i++) {
		scanf("%d", &a[i]);
		mn = min(mn, a[i]);
	}
	bool ok = 1;
	for(int i = 0; i < n; i++) {
		ans += (a[i] - mn) / k;
		ok &= ((a[i] - mn) % k == 0);
	}
	if(!ok) {
		printf("-1");
	} else {
		printf("%lld", ans);
	}

	return (0);
}

inline pair<pair<bool, bool>, pair<string, bool> > useinout() {
	return (mp(mp(0, 0), mp("", 0)));
}

