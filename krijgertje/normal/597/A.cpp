ll gcd(ll a,ll b) { return b==0?a:gcd(b,a%b); }


ll k,a,b;

void run() {
	scanf("%I64d%I64d%I64d",&k,&a,&b);
	ll ret=0;
	if(a>=0) ret-=(a+k-1)/k; else ret+=-a/k;
	if(b>=0) ret+=b/k+1; else ret-=-(b+1)/k;
	printf("%I64d\n",ret);
}

