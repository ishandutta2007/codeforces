fun basicDP(n:Int, k:Int, x:Int):Int {
    //phrase, drops, inversions taken, level down
    var DP = Array(x+1){Array(k+1){IntArray(k+1)} }
    DP[0][0][0] = 1
    for(p in 0 until n){
        val new = Array(x+1){Array(k+1){IntArray(k+1)} }
        for(drop in 0..x){
            for(inversions in 0..k){
                for(leveldown in 0..k){
                    for(nextlevel in 0..k){
                        val newdrop = drop + (nextlevel > leveldown).chi
                        val newinversions = inversions + nextlevel
                        if(nextlevel !in 0..p) continue
                        if(newdrop !in 0..x) continue
                        if(newinversions !in 0..k) continue
                        new[newdrop][newinversions][nextlevel] = new[newdrop][newinversions][nextlevel] modPlus DP[drop][inversions][leveldown]
                    }
                }
            }
        }
        DP = new
//        just dei p
    }
    var total = 0
    val want = DP[x][k]
    for(a in want){
        total = total modPlus a
    }
    return total
}

